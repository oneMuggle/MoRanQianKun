/** 里象功法门派 */
export type 里象功法门派 = '合欢宗' | '血河宗' | '天魔宫' | '自创';

/** 里象功法品级 */
export type 里象功法品级 = '下品' | '中品' | '上品' | '极品';

/** 里象功法风险类型 */
export type 里象功法风险类型 = '心魔' | '反噬' | '正道追杀' | '无';

/** 里象功法接口 */
export interface 里象功法 {
  id: string;
  名称: string;
  门派: 里象功法门派;
  品级: 里象功法品级;
  效果描述: string;
  修炼要求: {
    亲密度等级: number; // 5 = 双修解锁
    最低境界?: string;
  };
  收益: {
    属性类型: 气运属性类型;
    数值: number; // 1-3
  };
  风险: {
    类型: 里象功法风险类型;
    描述: string;
    概率: number; // 0-1，风险触发概率
  };
}

