/**
 * 根据ID查询里象功法
 * @param id - 功法ID
 * @returns 里象功法或undefined
 */
export function getLixiangById(id: string): 里象功法 | undefined {
  return 里象功法列表.find(f => f.id === id);
}

/**
 * 根据门派筛选功法
 * @param 门派 - 里象功法门派
 * @returns 该门派的所有功法
 */
export function getLixiangBySect(门派: 里象功法门派): 里象功法[] {
  return 里象功法列表.filter(f => f.门派 === 门派);
}

/**
 * 根据品级筛选功法
 * @param 品级 - 里象功法品级
 * @returns 该品级的所有功法
 */
export function getLixiangByGrade(品级: 里象功法品级): 里象功法[] {
  return 里象功法列表.filter(f => f.品级 === 品级);
}