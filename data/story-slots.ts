/**
 * 获取指定类型的预设槽位
 */
export const 获取预设槽位By类型 = (类型: 剧情槽位结构['类型']): 剧情槽位结构[] =>
    预设剧情槽位列表.filter(slot => slot.类型 === 类型);

/**
 * 获取指定作用域的预设槽位
 */
export const 获取预设槽位By作用域 = (作用域: 世界书作用域): 剧情槽位结构[] =>
    预设剧情槽位列表.filter(
        slot => slot.作用域.includes(作用域) || slot.作用域.includes('all')
    );

