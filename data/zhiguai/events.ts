/** 按触发类型获取事件 */
export const 获取志怪事件按触发类型 = (类型: 事件触发类型): 志怪事件[] =>
  志怪事件列表.filter(e => e.触发类型 === 类型);

/** 随机获取一个志怪事件 */
export const 随机志怪事件 = (): 志怪事件 =>
  志怪事件列表[Math.floor(Math.random() * 志怪事件列表.length)];

/** 随机获取 N 个志怪事件 */
export const 随机志怪事件组 = (数量: number): 志怪事件[] => {
  const shuffled = [...志怪事件列表].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, Math.min(数量, 志怪事件列表.length));
};

/** 根据关联志怪筛选事件 */
export const 获取志怪事件按关联生物 = (志怪ID: string): 志怪事件[] =>
  志怪事件列表.filter(e => e.关联志怪.includes(志怪ID));
