function 构建人格对话风格(人格标签?: string): string {
  if (!人格标签) return '';

  if (人格标签.includes('傲娇') || 人格标签.includes('大小姐')) {
    return '【人格风格】傲娇型NPC：事后可能口是心非，明明依恋却装作不在乎，或者说反话掩饰害羞。';
  }
  if (人格标签.includes('温柔') || 人格标签.includes('贤妻')) {
    return '【人格风格】温柔型NPC：事后可能依偎在玩家身边，轻声关心，表现出安心和幸福感。';
  }
  if (人格标签.includes('高冷') || 人格标签.includes('清冷')) {
    return '【人格风格】高冷型NPC：事后可能沉默寡言，但细微的肢体语言（如靠近、整理衣物时手抖）透露内心波动。';
  }
  if (人格标签.includes('元气') || 人格标签.includes('活泼')) {
    return '【人格风格】元气型NPC：事后可能很快恢复活力，甚至开玩笑或调侃玩家，但眼神中流露真情。';
  }
  if (人格标签.includes('社恐')) {
    return '【人格风格】社恐型NPC：事后可能极度害羞，不敢直视玩家，蜷缩在一角，小声说话或沉默。';
  }

  return '';
}

function 构建首次引导(是否首次?: boolean): string {
  if (!是否首次) return '';

  return '【首次加成】这是该NPC的第一次NSFW体验，请着重描写：\n' +
    '- 紧张感和不安\n' +
    '- 事后的复杂情绪（可能包含害羞、后悔、安心等多种情绪混合）\n' +
    '- 对玩家态度的微妙变化\n' +
    '- 身体和心理的双重记忆';
}

function 构建互动类型引导(类型?: string): string {
  if (!类型) return '';

  const 映射: Record<string, string> = {
    '温柔': '【互动风格】本次互动温柔，事后NPC可能感受到被珍视和爱护，情感正面。',
    '粗暴': '【互动风格】本次互动粗暴，事后NPC可能感到疼痛、委屈或被伤害，需要更多护理来修复情感。',
    '特殊': '【互动风格】本次互动涉及特殊场景/玩法，事后NPC可能有复杂的情绪反应，需根据具体场景描写。',
  };
  return 映射[类型] ?? '';
}

/**
 * 组装完整的事后对话提示
 */
export function 构建事后对话提示(输入: 事后对话输入): string {
  const 提示列表: string[] = [];

  提示列表.push(构建基础引导());

  const 护理引导 = 构建护理质量引导(输入.护理质量);
  if (护理引导) 提示列表.push(护理引导);

  const 羁绊引导 = 构建羁绊阶段引导(输入.羁绊树);
  if (羁绊引导) 提示列表.push(羁绊引导);

  const 情绪引导 = 构建情绪反应引导(输入.当前情绪);
  if (情绪引导) 提示列表.push(情绪引导);

  const 人格引导 = 构建人格对话风格(输入.人格标签);
  if (人格引导) 提示列表.push(人格引导);

  const 首次引导 = 构建首次引导(输入.是否首次);
  if (首次引导) 提示列表.push(首次引导);

  const 互动引导 = 构建互动类型引导(输入.互动类型);
  if (互动引导) 提示列表.push(互动引导);

  return 提示列表.join('\n\n');
}
