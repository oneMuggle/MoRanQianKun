/**
 * 事后对话系统 — AI提示词注入
 * 根据羁绊值、护理质量、情绪状态生成事后对话引导
 */

import type { 情绪状态 } from '../../models/npcNSFWEnhancement/emotionSystem';
import type { 情感羁绊树 } from '../../models/npcNSFWEnhancement/bondTree';

export interface 事后对话输入 {
  羁绊树?: 情感羁绊树;
  当前情绪?: 情绪状态;
  护理质量?: '无视' | '敷衍' | '温柔' | '用心';
  互动类型?: '温柔' | '正常' | '粗暴' | '特殊';
  人格标签?: string;
  是否首次?: boolean;
}

function 获取关系阶段(羁绊值: number): string {
  if (羁绊值 >= 80) return '深度依恋';
  if (羁绊值 >= 60) return '情感深厚';
  if (羁绊值 >= 40) return '互有好感';
  if (羁绊值 >= 20) return '初步了解';
  return '陌生疏离';
}

function 获取主导结局文本(羁绊树: 情感羁绊树): string {
  const 倾向 = 羁绊树.结局倾向;
  let 最高 = '陌路';
  let 最高值 = 0;

  for (const [结局, 值] of Object.entries(倾向)) {
    if (值 > 最高值) {
      最高值 = 值;
      最高 = 结局;
    }
  }

  const 映射: Record<string, string> = {
    '恋人': '恋人', '炮友': '炮友', '主从': '主从',
    '知己': '知己', '挚友': '挚友', '陌路': '陌路',
  };
  return 映射[最高] ?? '陌路';
}

