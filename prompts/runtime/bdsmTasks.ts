/**
 * 构建调教任务生成 Prompt
 * 根据契约状态、关系阶段、服从度等上下文生成合适的任务
 */
export const 构建调教任务生成提示词 = (参数: {
  契约类型: 契约类型;
  契约状态: 契约状态;
  服从度: number;
  权力倾向: string;
  关系阶段: string;
  已解锁场景: SM场景类型[];
  历史任务数量: number;
  上次任务评价?: BDSM评价等级;
  NPC性格特征?: string;
}): string => {
  const {
    契约类型,
    契约状态,
    服从度,
    权力倾向,
    关系阶段,
    已解锁场景,
    历史任务数量,
    上次任务评价,
    NPC性格特征,
  } = 参数;

  const 难度上限 = 服从度 >= 80 ? '极限'
    : 服从度 >= 60 ? '高级'
      : 服从度 >= 40 ? '中级'
        : 服从度 >= 20 ? '初级'
          : '入门';

  const 可用任务类型 = 契约类型 === '口头约定'
    ? ['服从测试', '心理建设', '日常指令']
    : 契约类型 === '书面契约'
      ? ['服从测试', '忠诚考验', '技能训练', '心理建设', '日常指令', '契约履行']
        : ['服从测试', '忠诚考验', '技能训练', '心理建设', '公开挑战', '日常指令', '信物任务', '契约履行'];

  const 阶段侧重: Record<string, string> = {
    '初识': '以轻度服从测试和心理建设为主，建立初步信任',
    '试探': '增加日常指令和基础技能训练，逐步加深依赖',
    '确立': '引入忠诚考验和中级技能训练，强化关系纽带',
    '深入': '可加入公开挑战和高级技能训练，探索更深层信任',
    '固化': '全面的任务类型，包含极限挑战和信物任务',
  };

  const 评价影响 = 上次任务评价
    ? 上次任务评价 === '完美服从' || 上次任务评价 === '优秀'
      ? '上次任务完成出色，可适当提高难度或引入新任务类型'
      : 上次任务评价 === '失败' || 上次任务评价 === '拒绝'
        ? '上次任务未完成，应降低难度或调整任务类型，重建信任'
        : '上次任务完成一般，保持当前难度或微调'
    : '';

  return `请根据以下上下文生成 2-4 个调教任务。

【契约信息】
契约类型：${契约类型}
契约状态：${契约状态}
当前服从度：${服从度}/100

【关系状态】
权力倾向：${权力倾向}
关系阶段：${关系阶段} — ${阶段侧重[关系阶段] || ''}

【任务约束】
可用任务类型：${可用任务类型.join('、')}
难度上限：${难度上限}
已解锁场景：${已解锁场景.length > 0 ? 已解锁场景.join('、') : '无'}
历史任务数量：${历史任务数量}
${评价影响}
${NPC性格特征 ? `NPC性格特征：${NPC性格特征}` : ''}

【生成规则】
1. 每个任务必须符合契约类型允许的范围内
2. 任务难度不能超过难度上限
3. 服从度低于 30 时，只能生成「入门」难度的「服从测试」和「心理建设」
4. 任务描述应具体、可执行，避免模糊指令
5. 任务应包含明确的完成条件和时限
6. 任务内容需符合双方的安全词和底线
7. 任务应循序渐进，不可跳跃式推进
8. 每个任务必须有明确的奖励和惩罚机制
9. 任务风格应符合校园叙事背景，融入校园场景
10. 如果已解锁场景有限，任务应在已解锁场景范围内

【输出格式】
输出 JSON 数组，每个任务对象包含：
- type: 任务类型（从可用类型中选择）
- title: 任务标题（简短有吸引力）
- description: 任务描述（具体可执行，50-200字）
- difficulty: 难度等级
- deadline: 截止时间（如"下次见面前"、"三天内"、"本周内"）
- obedienceChange: 服从度变化值（-20 到 +30，根据难度和评价预期）
- rewardDescription: 奖励描述（完成任务后的正面反馈）
- punishmentDescription: 惩罚描述（未完成任务的后果）

只输出 JSON 数组，不要其他内容。`;
};

