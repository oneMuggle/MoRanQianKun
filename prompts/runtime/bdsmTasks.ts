import type { 契约类型, 契约状态, SM场景类型 } from '../../models/campusNSFW';

// ==================== 任务类型定义 ====================

export type BDSM任务类型 =
  | '服从测试'      // 测试 NPC 对指令的服从程度
  | '忠诚考验'      // 考验 NPC 对关系的忠诚度
  | '技能训练'      // 训练特定技巧或能力
  | '心理建设'      // 心理层面的引导和强化
  | '公开挑战'      // 在公共场合的暧昧任务
  | '日常指令'      // 每日必须完成的例行任务
  | '信物任务'      // 与信物交换相关的任务
  | '契约履行';     // 契约条款的具体履行

export type BDSM任务难度 = '入门' | '初级' | '中级' | '高级' | '极限';

export type BDSM任务状态 = '待接受' | '进行中' | '已完成' | '已失败' | '已放弃';

export type BDSM评价等级 = '完美服从' | '优秀' | '良好' | '勉强' | '失败' | '拒绝';

export interface BDSM调教任务 {
  id: string;
  类型: BDSM任务类型;
  标题: string;
  描述: string;
  难度: BDSM任务难度;
  发布者: string;       // 发布任务的 NPC 姓名
  接受者: string;       // 接受任务的 NPC 姓名
  状态: BDSM任务状态;
  发布时间: string;
  截止时间?: string;
  完成时间?: string;
  评价?: BDSM评价等级;
  服从度变化: number;   // 完成后服从度变化值
  奖励描述?: string;
  惩罚描述?: string;
  前置任务ID?: string;  // 是否有前置任务
  关联契约ID?: string;  // 关联的契约
  关联场景?: SM场景类型;
}

