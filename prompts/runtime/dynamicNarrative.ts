/**
 * 动态叙事约束 — AI提示词注入
 * 根据情绪、关系、后果动态生成叙事约束
 */

import type { 情绪状态 } from '../../models/npcNSFWEnhancement/emotionSystem';
import type { 情感羁绊树 } from '../../models/npcNSFWEnhancement/bondTree';
import type { 后果条目, 后果系统状态 } from '../../models/npcNSFWEnhancement/unifiedConsequences';

export interface 动态叙事输入 {
  当前情绪?: 情绪状态;
  羁绊树?: 情感羁绊树;
  后果系统?: 后果系统状态;
  亲密度等级: number;
  心理防线: number;
  好感度: number;
  人格标签?: string;
  嫉妒状态?: { 强度: number; 表现形式: string };
}

/**
 * 构建情绪化拒绝提示
 */
function 构建情绪化拒绝提示(情绪?: 情绪状态): string {
  if (!情绪) return '';

  switch (情绪.心情阶段) {
    case '愤怒':
      return '【情绪化拒绝】当前心情愤怒，NPC对亲密行为有强烈抵触情绪。如果玩家强行推进，NPC可能表现出抗拒、愤怒甚至离开的行为。描述时要突出NPC的愤怒情绪和拒绝态度。';
    case '低落':
      return '【情绪化拒绝】当前心情低落，NPC缺乏亲密互动的兴致。可能表现出消极回应、敷衍态度或需要玩家先关心安慰。描述时要体现NPC的消沉状态。';
    case '烦躁':
      return '【情绪化拒绝】当前心情烦躁，NPC对亲密行为的耐心有限。可能表现出不耐烦、急躁或需要玩家先舒缓情绪。';
    case '开心':
      return '【情绪加成】当前心情愉悦，NPC对亲密行为更加开放和积极。可能主动回应、表现热情。描述时可以加入愉快的氛围和积极的情绪。';
    default:
      return '';
  }
}

/**
 * 构建节奏控制约束
 */
function 构建节奏控制约束(
  亲密度等级: number,
  心理防线: number,
  羁绊值?: number
): string {
  const 约束列表: string[] = [];

  if (亲密度等级 <= 1) {
    约束列表.push('【节奏约束】亲密度较低（Lv' + 亲密度等级 + '），只能进行轻微的言语互动和偶尔的身体接触。禁止直接的NSFW描写，需要从氛围和情感开始铺垫。');
  } else if (亲密度等级 <= 2) {
    约束列表.push('【节奏约束】亲密度中等（Lv' + 亲密度等级 + '），可以描写拥抱、亲吻等中等程度亲密行为。NSFW行为需要先有充分的情感铺垫和氛围营造。');
  } else if (亲密度等级 <= 3) {
    约束列表.push('【节奏约束】亲密度良好（Lv' + 亲密度等级 + '），可以描写深度亲密行为。但仍需注意前戏描写和情感递进，不要跳过铺垫直接进入NSFW。');
  }

  if (心理防线 > 75) {
    约束列表.push('【防线约束】心理防线极高（保守），NPC对NSFW行为有强烈心理障碍。需要极长的铺垫和极强的情感信任才能突破。');
  } else if (心理防线 > 50) {
    约束列表.push('【防线约束】心理防线较高（传统），NPC对NSFW行为有顾虑。需要充分的情感建立和信任感。');
  } else if (心理防线 < 25) {
    约束列表.push('【防线加成】心理防线很低（放纵），NPC对NSFW行为接受度高。但仍需保持情感层面的描写深度。');
  }

  if (羁绊值 !== undefined && 羁绊值 > 60) {
    约束列表.push('【羁绊加成】羁绊深厚（' + 羁绊值 + '/100），NPC对玩家有强烈的情感依赖。可能在NSFW中表现出更多的情感投入和主动性。');
  }

  return 约束列表.join('\n');
}

/**
 * 构建事后对话引导
 */
function 构建事后对话引导(羁绊值?: number): string {
  const 引导列表: string[] = [];

  引导列表.push('【事后对话】NSFW结束后，请描写以下至少一项：');
  引导列表.push('- NPC的情绪反应（害羞、依恋、满足、空虚等）');
  引导列表.push('- 肢体语言（蜷缩、依偎、整理衣物等）');
  引导列表.push('- 对话内容（情话、关心、沉默等）');

  if (羁绊值 !== undefined && 羁绊值 > 50) {
    引导列表.push('- 因羁绊深厚，NPC可能主动拥抱或依偎在玩家身边');
  }

  return 引导列表.join('\n');
}

/**
 * 构建后果影响提示
 */
function 构建后果影响提示(后果系统?: 后果系统状态): string {
  if (!后果系统 || 后果系统.活跃后果.length === 0) return '';

  const 活跃后果 = 后果系统.活跃后果.filter((c): c is 后果条目 => Math.abs(c.影响值) > 10);
  if (活跃后果.length === 0) return '';

  const 提示列表 = 活跃后果.slice(0, 3).map((c): string => {
    const 方向 = c.影响值 > 0 ? '积极' : '消极';
    return `【后果影响】${c.描述}（${方向}，影响值${c.影响值}）——请在叙事中体现这一后果。`;
  });

  return 提示列表.join('\n');
}

/**
 * 构建嫉妒影响提示
 */
function 构建嫉妒影响提示(嫉妒状态?: { 强度: number; 表现形式: string }): string {
  if (!嫉妒状态 || 嫉妒状态.强度 < 20) return '';

  const 表现 = 嫉妒状态.表现形式;
  let 提示 = `【嫉妒影响】NPC处于嫉妒状态（强度${嫉妒状态.强度}），表现为「${表现}」。`;

  switch (表现) {
    case '冷淡回应':
      提示 += '对话和互动中表现出冷淡和疏离的态度。';
      break;
    case '主动质问':
      提示 += '可能直接质问玩家关于其他NPC的事情，语气带有不满。';
      break;
    case '暗中报复':
      提示 += '表面正常但暗中可能做出不利于玩家的行为。';
      break;
    case '加速推进':
      提示 += '可能更加主动地推进与玩家的关系，试图"赢得"玩家。';
      break;
    case '拉拢结盟':
      提示 += '可能试图通过其他方式吸引玩家注意。';
      break;
    case '自我伤害':
      提示 += '极端表现，可能有自伤或威胁行为，需要谨慎处理。';
      break;
  }

  return 提示;
}

/**
 * 组装完整的动态叙事约束
 */
export function 构建动态叙事约束(输入: 动态叙事输入): string {
  const 约束列表: string[] = [];

  const 情绪提示 = 构建情绪化拒绝提示(输入.当前情绪);
  if (情绪提示) 约束列表.push(情绪提示);

  const 节奏提示 = 构建节奏控制约束(
    输入.亲密度等级,
    输入.心理防线,
    输入.羁绊树?.羁绊值
  );
  if (节奏提示) 约束列表.push(节奏提示);

  const 后果提示 = 构建后果影响提示(输入.后果系统);
  if (后果提示) 约束列表.push(后果提示);

  const 嫉妒提示 = 构建嫉妒影响提示(输入.嫉妒状态);
  if (嫉妒提示) 约束列表.push(嫉妒提示);

  const 事后提示 = 构建事后对话引导(输入.羁绊树?.羁绊值);
  if (事后提示) 约束列表.push(事后提示);

  return 约束列表.join('\n\n');
}
