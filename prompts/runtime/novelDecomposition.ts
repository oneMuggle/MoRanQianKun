/**
 * 小说分解运行时提示词
 *
 * 本模块负责将原著小说文本分解为可执行的结构化数据。
 *
 * 核心设计原则：
 * 1. 每个关键事件都必须能回答六个问题：
 *    - 这件事是什么
 *    - 最早什么时候能发生
 *    - 还需要什么前置条件
 *    - 什么条件满足后立即可触发
 *    - 什么情况下不能触发或需要延后
 *    - 触发后会沉淀什么结果
 * 2. 第一组特殊处理：开局组不生成"进入本组前置条件"
 * 3. 从第二组开始，必须显式带上前一组部分结果作为参考
 * 4. 关键事件必须拆成"可执行事件"结构
 */

