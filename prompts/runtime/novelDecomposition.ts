export const 小说分解思考协议 = `在分解过程中，请按以下步骤思考：

1. **阅读原文**：理解当前章节的核心内容、人物关系变化、事件发展。

2. **提取时间线**：
   - 确定本章的时间跨度（时间线起点到时间线终点）
   - 识别本章中各个事件的具体时间点
   - 若原文没有明确时间，根据上下文推断合理时间

3. **识别关键事件**：
   - 本章有哪些推动剧情的关键事件
   - 每个事件的触发时间、最早触发时间、最晚触发时间
   - 事件的前置条件、触发条件、阻断条件

4. **角色推进**：
   - 本章中每个角色的状态变化
   - 角色之间的关系发展

5. **势力推进**：
   - 本章中各势力的变化
   - 势力之间的关系变化

6. **生成给下一组的参考**：
   - 本章结束后哪些事实已成立
   - 下一组需要承接什么

请以结构化方式输出分析结果。`;

