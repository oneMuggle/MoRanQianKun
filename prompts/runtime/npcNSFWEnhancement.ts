import { NPC结构 } from '../../models/social';
import { 生成NSFW画像, 应启用增强档案 } from '../../models/npcNSFWEnhancement/linkage';
import { 生成触发事件列表 } from '../../models/npcNSFWEnhancement/eventMapping';
import { 计算场景修饰系数, 生成场景修饰摘要 } from '../../models/npcNSFWEnhancement/sceneModifiers';
import { 生成服装状态文本, 是否暴露 } from '../../models/npcNSFWEnhancement/clothingLayers';
import type { LiModeIntensity } from './eraLiMode';

/**
 * 构建 NPC NSFW 增强注入提示词
 */
export function 构建NPCNSFW注入(
  npc: NPC结构,
  eraId: string | null | undefined,
  nsfwEnabled: boolean = true,
  _intensity?: LiModeIntensity,
  环境?: import('../../models/environment').环境信息结构,
  游戏时间?: string
): string | null {
  if (!nsfwEnabled || !应启用增强档案(npc)) return null;

  const 画像 = 生成NSFW画像(npc, eraId);
  const 人格状态 = npc.当前人格状态 ?? '表';
  const 活跃人格 = 人格状态 === '里' && 画像.人格 ? 画像.人格.里 : 画像.人格?.表;

  const 组件: string[] = [];

  组件.push(`【${npc.姓名} — NSFW 画像】`);
  组件.push(`人格状态：${人格状态}${活跃人格 ? `（${活跃人格.性格描述.substring(0, 30)}…）` : ''}`);

  if (画像.性癖.核心偏好.length > 0) {
    组件.push(`性癖倾向：${画像.性癖.倾向摘要}`);
    组件.push(`具体偏好：${画像.性癖.核心偏好.map(p => `${p.子类型}(${p.强度}/5)`).join('、')}`);
  }

  if (画像.敏感点.主要敏感点.length > 0) {
    组件.push(`敏感区域：${画像.敏感点.主要敏感点.map(p => {
      const 名 = p.时代名称 || p.名称;
      const 开发 = p.开发程度 ? `，${p.开发程度}` : '';
      return `${名}(敏感度${p.敏感度}${开发})`;
    }).join('、')}`);
  }

  if (画像.推荐场景.length > 0) {
    组件.push(`适合场景：${画像.推荐场景.slice(0, 3).join('、')}`);
  }

  // --- 新增：心理防线状态 ---
  if (画像.心理防线) {
    组件.push(`心理防线：${画像.心理防线.当前等级}（防线值${Math.round(画像.心理防线.防线值)}/100）`);
    if (画像.心理防线.防线值 < 30) {
      组件.push('注意：该角色的心理防线已经很低，在亲密场景中更容易放下戒备、展现真实欲望。');
    }
  }

  if (画像.性癖.绝对禁忌.length > 0) {
    组件.push(`绝对禁忌：${画像.性癖.绝对禁忌.join('、')}`);
  }

  if (画像.性癖.隐藏偏好.length > 0) {
    组件.push('注意：该角色还有尚未暴露的隐藏偏好，随着关系深入可能逐渐展现。');
  }

  // --- 新增：偏好漂移 ---
  if (画像.偏好漂移?.漂移方向) {
    组件.push(`偏好漂移：近期逐渐倾向"${画像.偏好漂移.漂移方向}"方向（强度${Math.round(画像.偏好漂移.漂移强度 * 100)}%）`);
  }

  // --- 新增：人格翻转历史 ---
  if (npc.完整演化状态?.人格演化?.人格翻转历史?.length) {
    const 最近翻转 = npc.完整演化状态.人格演化.人格翻转历史.slice(-1)[0];
    组件.push(`人格演化：曾发生"${最近翻转.翻转类型}"（${最近翻转.时间}），人格特质已发生变化。`);
  }

  // --- 新增：孕产状态 ---
  if (画像.孕产状态 && 画像.孕产状态.当前阶段 !== '未受孕') {
    组件.push(`孕产状态：${画像.孕产状态.当前阶段}`);
    if (画像.孕产状态.预产期) {
      组件.push(`预产期：${画像.孕产状态.预产期}`);
    }
  }

  // --- 新增：事后护理 ---
  if (画像.事后护理 && 画像.事后护理.当前情绪.length > 0) {
    const 情绪文本 = 画像.事后护理.当前情绪
      .filter(e => e.强度 > 20)
      .map(e => `${e.情绪类型}(${e.强度}%)`)
      .join('、');
    if (情绪文本) {
      组件.push(`事后情绪：${情绪文本}（护理质量：${画像.事后护理.护理质量}）`);
    }
  }

  // --- 新增：场景修饰器 ---
  if (环境 && 游戏时间) {
    const 修饰 = 计算场景修饰系数(环境, 游戏时间);
    const 摘要 = 生成场景修饰摘要(修饰);
    if (摘要) {
      组件.push(摘要);
    }
    if (修饰.暴露风险 > 30) {
      组件.push(`⚠️ 暴露风险：${修饰.暴露风险}%（${修饰.暴露风险 > 60 ? '高' : '中'}）`);
    }
  }

  // --- 新增：服装层次 ---
  const 服装状态 = 生成服装状态文本(npc);
  if (服装状态) {
    组件.push(`服装：${服装状态}`);
  }
  if (是否暴露(npc)) {
    组件.push('注意：该角色当前处于暴露状态。');
  }

  组件.push('');
  组件.push('叙事指引：');

  if (画像.人格) {
    const 表描述 = 画像.人格.表.性格描述;
    const 里描述 = 画像.人格.里.性格描述;
    组件.push(`- 该角色表人格为"${表描述.substring(0, 20)}…"，里人格为"${里描述.substring(0, 20)}…"`);
    组件.push(`- 当前处于${人格状态}人格，行为对话应符合${人格状态}人格特质`);
    组件.push(`- 里人格触发条件：${画像.人格.激活条件.时间条件 ?? '无特定时间'}，${画像.人格.激活条件.地点条件 ?? '无特定地点'}`);
  }

  if (画像.性癖.核心偏好.length > 0) {
    const 最强 = 画像.性癖.核心偏好.reduce((a, b) => a.强度 > b.强度 ? a : b);
    组件.push(`- 亲密互动中应体现"${最强.子类型}"的倾向（${最强.描述.substring(0, 25)}…）`);
  }

  if (画像.敏感点.主要敏感点.length > 0) {
    const 最敏感 = 画像.敏感点.主要敏感点.reduce((a, b) => a.敏感度 > b.敏感度 ? a : b);
    const 名 = 最敏感.时代名称 || 最敏感.名称;
    组件.push(`- 触碰${名}时应描写"${最敏感.反应描述.substring(0, 20)}…"`);
  }

  组件.push('');
  组件.push('【性癖变化报告】');
  组件.push('当该NPC经历了可能影响其性癖的事件时，在响应末尾输出：');
  组件.push('<性癖变化>');
  组件.push(`{"npc姓名": "${npc.姓名}", "触发事件": "{事件类型}", "事件描述": "{简短描述}"}`);
  组件.push('</性癖变化>');
  组件.push(`可用触发事件：${生成触发事件列表()}`);
  组件.push('注意：仅在确实发生了显著影响性癖的事件时才输出，不要每回合都输出。');

  // --- 新增：人格演化报告模板 ---
  组件.push('');
  组件.push('【人格演化报告】');
  组件.push('当该NPC的心理防线发生显著变化或人格特质改变时，在响应末尾输出：');
  组件.push('<人格演化>');
  组件.push(`{"npc姓名": "${npc.姓名}", "演化类型": "{表里互换|欲望觉醒|解放突破}", "描述": "{简短描述}"}`);
  组件.push('</人格演化>');
  组件.push('注意：仅在确实发生了人格级别的显著变化时才输出。');

  // --- 新增：孕产变化报告模板 ---
  组件.push('');
  组件.push('【孕产变化报告】');
  组件.push('当该NPC发生受孕、妊娠推进、分娩或产后恢复时，在响应末尾输出：');
  组件.push('<孕产变化>');
  组件.push(`{"npc姓名": "${npc.姓名}", "变化类型": "{受孕成功|妊娠推进|分娩完成|产后恢复|受孕失败}", "旧阶段": "{原阶段}", "新阶段": "{新阶段}", "描述": "{简短描述}"}`);
  组件.push('</孕产变化>');
  组件.push('注意：仅在确实发生了孕产级别的变化时才输出。');

  // --- 新增：事后护理报告模板 ---
  组件.push('');
  组件.push('【事后护理报告】');
  组件.push('当该NPC在NSFW场景结束后表现出明显的情绪变化或玩家进行了事后护理时，在响应末尾输出：');
  组件.push('<事后护理>');
  组件.push(`{"npc姓名": "${npc.姓名}", "护理质量": "{无视|敷衍|温柔|用心}", "情绪变化": [{"情绪类型": "{羞耻|依恋|后悔|安心|空虚|兴奋|恐惧|麻木}", "强度": 0-100}]}`);
  组件.push('</事后护理>');
  组件.push('注意：仅在NSFW场景结束后有明显情绪表现时才输出。');

  return 组件.join('\n');
}

/**
 * 构建精简版 NSFW 注入（用于 token 紧张时使用）
 */
export function 构建NPCNSFW精简注入(
  npc: NPC结构,
  eraId: string | null | undefined,
  nsfwEnabled: boolean = true
): string | null {
  if (!nsfwEnabled || !应启用增强档案(npc)) return null;

  const 画像 = 生成NSFW画像(npc, eraId);
  const parts = [`【${npc.姓名}】`];

  if (画像.人格) {
    const 状态 = npc.当前人格状态 ?? '表';
    const 人格 = 状态 === '里' ? 画像.人格.里 : 画像.人格.表;
    parts.push(`${状态}人格：${人格.性格描述.substring(0, 25)}…`);
  }

  if (画像.性癖.倾向摘要) {
    parts.push(画像.性癖.倾向摘要);
  }

  if (画像.敏感点.弱点摘要) {
    parts.push(画像.敏感点.弱点摘要);
  }

  return parts.join(' | ');
}
