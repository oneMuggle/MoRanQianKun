import type { 游戏设置结构 } from '../../models/system';

/**
 * 构建行动选项运行时指令
 * 根据当前游戏设置生成行动选项增强和 NSFW 推进选项的运行时指令
 */
export const 构建行动选项运行时指令 = (gameConfig: 游戏设置结构): string => {
    const parts: string[] = [];

    // 行动选项增强指令
    if (gameConfig.启用行动选项增强) {
        parts.push(`【行动选项增强模式：已开启】
当前启用了行动选项增强模式。请在每回合按以下4个推进速度档位分别生成行动选项组，每组2-3个选项，用档位标题分隔：
- **缓慢**：聚焦当前场景的微观互动，每个选项只涉及极小范围的行动，不跨越时间或空间。
- **正常**：按默认节奏生成选项，平衡叙事与互动的比重。
- **快速**：跳过不必要的过场和寒暄，选项直奔核心行动。
- **跳过至关键节点**：大幅跳跃到下一个重要事件或转折点，选项可包含明确的时间跨度和空间跳跃。
用户可根据当前心情和故事走向，自由选择不同速度档位的行动选项来推进剧情。`);
    }

    // NSFW 推进选项指令
    if (gameConfig.启用NSFW模式 && gameConfig.启用NSFW推进选项) {
        parts.push(`【NSFW推进选项：已开启】
当场景中存在暧昧或亲密氛围时，行动选项中必须包含至少一个促进亲密关系推进的动作（如轻抚、拉近、耳语等）。
选项需符合当前场景的故事节奏和NSFW档位描写风格。若场景为纯战斗/探索/严肃谈判等非暧昧场景，则不强制。
NSFW推进选项同样遵守玩家视角限定和措辞中立原则，不预设对方反应。`);
    }

    return parts.join('\n\n');
};
