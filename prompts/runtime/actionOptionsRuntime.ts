import type { 游戏设置结构 } from '../../models/system';

/**
 * 构建行动选项运行时指令
 * 根据当前游戏设置生成行动选项增强和 NSFW 推进选项的运行时指令
 */
export const 构建行动选项运行时指令 = (gameConfig: 游戏设置结构): string => {
    const parts: string[] = [];

    // 行动选项增强指令
    if (gameConfig.启用行动选项增强) {
        parts.push(`【行动选项增强模式：已开启】
请在 <行动选项> 标签内按4个速度档位分组输出选项。每组以一个中文方括号标题行开头（如[缓慢]），标题行下方每行一个纯文本动作短句。**档位标题不是行动选项，不得作为可点击的选项文本。**

输出格式示例：
<行动选项>
[缓慢]
- 仔细观察对方神情
- 慢慢坐下喝茶
[正常]
- 向茶摊老板打听消息
- 前往城门查看动静
[快速]
- 直接推门而入
- 立即动身前往目的地
[跳过至关键节点]
- 翌日清晨直接赴约
- 三日后出现在密会地点
</行动选项>

各档位选项要求：
- 每组2-3个选项，与常规行动选项规范一致（纯动作、动词开头、中性措辞、玩家视角）。
- 缓慢：聚焦微观互动，不跨越时间或空间。
- 正常：默认节奏，平衡叙事与互动。
- 快速：跳过寒暄，直奔核心行动。
- 跳过至关键节点：大幅跳跃到下一重要事件，可包含时间/空间跳跃。
用户将选择不同档位的选项来推进剧情，请确保每个档位的选项都基于当前场景可执行。`);
    }

    // NSFW 推进选项指令
    if (gameConfig.启用NSFW模式 && gameConfig.启用NSFW推进选项) {
        parts.push(`【NSFW推进选项：已开启】
当场景中存在暧昧或亲密氛围时，行动选项中必须包含至少一个促进亲密关系推进的动作（如轻抚、拉近、耳语等）。
选项需符合当前场景的故事节奏和NSFW档位描写风格。若场景为纯战斗/探索/严肃谈判等非暧昧场景，则不强制。
NSFW推进选项同样遵守玩家视角限定和措辞中立原则，不预设对方反应。`);
    }

    return parts.join('\n\n');
};
