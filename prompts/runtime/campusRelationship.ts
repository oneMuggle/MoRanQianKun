/**
 * 构建关系互动提示词
 * 用于 AI 处理关系互动时的上下文
 */
export function 构建关系互动提示词(参数: {
  npc: NPC结构;
  关系数据: NPC关系数据;
  当前场景?: string;
  互动类型?: 互动类型;
  对话历史?: string[];
}): string {
  const { npc, 关系数据, 当前场景, 互动类型, 对话历史 } = 参数;

  const 阶段 = 计算当前关系阶段(关系数据);

  let 提示词 = `【NPC关系状态】
NPC姓名: ${npc.姓名}
NPC性别: ${npc.性别}
当前关系阶段: ${阶段}
关系状态: ${关系数据.关系状态}

【关系数值】
好感度: ${关系数据.好感度}/100
亲密度: ${关系数据.亲密度}/100
信任度: ${关系数据.信任度}/100
感情值: ${关系数据.感情值}/100

【阶段阈值参考】
${Object.entries(关系阈值表).map(([阶段名, 阈值]) =>
  `${阶段名}: 好感${阈值.好感度} 亲密${阈值.亲密度} 信任${阈值.信任度} 感情${阈值.感情值}`
).join('\n')}
`;

  if (当前场景) {
    提示词 += `\n【当前场景】
${当前场景}
`;
  }

  if (互动类型) {
    提示词 += `\n【互动类型】
${互动类型}
互动效果范围:
  好感度: ${互动效果表[互动类型].好感度[0]} ~ ${互动效果表[互动类型].好感度[1]}
  亲密度: ${互动效果表[互动类型].亲密度[0]} ~ ${互动效果表[互动类型].亲密度[1]}
  信任度: ${互动效果表[互动类型].信任度[0]} ~ ${互动效果表[互动类型].信任度[1]}
  感情值: ${互动效果表[互动类型].感情值[0]} ~ ${互动效果表[互动类型].感情值[1]}
`;
  }

  if (npc.核心性格特征) {
    提示词 += `\n【NPC性格】
${npc.核心性格特征}
`;
  }

  if (关系数据.解锁场景.length > 0) {
    提示词 += `\n【已解锁私密场景】
${关系数据.解锁场景.join(', ')}
`;
  }

  if (关系数据.关键事件.length > 0) {
    提示词 += `\n【关系历史】
`;
    const 最近事件 = 关系数据.关键事件.slice(-5);
    最近事件.forEach((事件) => {
      提示词 += `- ${事件.时间}: [${事件.类型}] ${事件.标题}\n`;
    });
  }

  if (对话历史 && 对话历史.length > 0) {
    提示词 += `\n【近期对话】
${对话历史.slice(-3).join('\n')}
`;
  }

  提示词 += `\n【AI 输出要求】
根据上述关系状态和互动类型，生成符合当前关系阶段的对话和叙事。
数值变化应在指定范围内随机生成。
输出格式:
{
  "互动结果": "成功|失败|部分成功",
  "NPC回复": "NPC的回复文本",
  "关系变化": {
    "好感度": 数值,
    "亲密度": 数值,
    "信任度": 数值,
    "感情值": 数值
  },
  "叙事内容": "详细的叙事描写"
}
`;

  return 提示词;
}

/**
 * 构建关系进展提示词
 * 用于判定关系阶段是否推进
 */
export function 构建关系进展提示词(参数: {
  npc: NPC结构;
  关系数据: NPC关系数据;
}): string {
  const { npc, 关系数据 } = 参数;

  const 当前阶段 = 计算当前关系阶段(关系数据);
  const 阶段顺序: 关系类型[] = ['陌生', '相识', '好感', '亲密', '恋人'];
  const 当前索引 = 阶段顺序.indexOf(当前阶段 as 关系类型);
  const 下一阶段 = 当前索引 < 阶段顺序.length - 1 ? 阶段顺序[当前索引 + 1] : '挚友';

  const 满足条件 = 检查是否满足进阶条件(关系数据, 当前阶段 as 关系类型);

  let 提示词 = `【关系进展判定】
NPC: ${npc.姓名}
当前阶段: ${当前阶段}
`;

  if (当前阶段 !== '恋人' && 当前阶段 !== '挚友') {
    提示词 += `下一阶段: ${下一阶段}
满足进阶条件: ${满足条件 ? '是' : '否'}
`;
  }

  提示词 += `
【当前数值】
好感度: ${关系数据.好感度}/100 (需要 ${关系阈值表[当前阶段 as 关系类型]?.好感度 || 0})
亲密度: ${关系数据.亲密度}/100 (需要 ${关系阈值表[当前阶段 as 关系类型]?.亲密度 || 0})
信任度: ${关系数据.信任度}/100 (需要 ${关系阈值表[当前阶段 as 关系类型]?.信任度 || 0})
感情值: ${关系数据.感情值}/100 (需要 ${关系阈值表[当前阶段 as 关系类型]?.感情值 || 0})

【判定结果格式】
{
  "是否推进": true | false,
  "原阶段": "当前阶段",
  "新阶段": "新阶段（如果有变化）",
  "推进原因": "描述"
}
`;

  return 提示词;
}

