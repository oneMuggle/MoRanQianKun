/**
 * 暴露 NSFW 增强模块 — 潜行/规避机制
 * 玩家主动选择潜行策略，基于多因子计算成功率，失败后有紧急应对
 */

import type { 露出状态, 露出后果记录 } from '../exposureNSFW/types';

// ==================== 类型定义 ====================

export type 潜行策略 = '低调行动' | '快速通过' | '伪装' | '分散注意力';

export type 紧急应对 = '逃跑' | '解释' | '威胁' | '贿赂';

export interface 潜行判定上下文 {
  露出等级: number;
  场景发现概率: number;
  周围人数: number;
  旁观者察觉概率: number;
  服装暴露度: number;  // 0-100
  时间隐私度: number;   // 0-100, 越高越私密
  反侦察等级: number;   // 0-5, 技能等级
}

export interface 潜行结果 {
  成功: boolean;
  被发现程度: '完全隐蔽' | '轻微可疑' | '明显暴露' | '当众曝光';
  紧张度变化: number;
  描述: string;
  触发后果?: 露出后果记录;
}

export interface 紧急应对结果 {
  成功: boolean;
  严重度降低: number;
  描述: string;
  额外后果?: 露出后果记录;
}

// ==================== 策略系数 ====================

const 策略系数: Record<潜行策略, { 基础加成: number; 风险系数: number; 消耗精力: number }> = {
  '低调行动': { 基础加成: 0.3, 风险系数: 0.8, 消耗精力: 1 },
  '快速通过': { 基础加成: 0.2, 风险系数: 1.2, 消耗精力: 2 },
  '伪装': { 基础加成: 0.4, 风险系数: 0.7, 消耗精力: 3 },
  '分散注意力': { 基础加成: 0.25, 风险系数: 1.0, 消耗精力: 2 },
};

const 应对系数: Record<紧急应对, { 基础成功率: number; 风险系数: number }> = {
  '逃跑': { 基础成功率: 0.5, 风险系数: 1.3 },
  '解释': { 基础成功率: 0.4, 风险系数: 0.9 },
  '威胁': { 基础成功率: 0.3, 风险系数: 1.5 },
  '贿赂': { 基础成功率: 0.6, 风险系数: 0.8 },
};

// ==================== 核心引擎函数 ====================

/**
 * 选择潜行策略并判定成功
 */
export function 选择潜行策略(
  策略: 潜行策略,
  上下文: 潜行判定上下文,
  游戏时间: string
): 潜行结果 {
  const 成功率 = 计算潜行成功率(策略, 上下文);
  const 掷骰 = Math.random();

  if (掷骰 <= 成功率) {
    return {
      成功: true,
      被发现程度: 成功率 > 0.8 ? '完全隐蔽' : '轻微可疑',
      紧张度变化: 策略 === '快速通过' ? -5 : 0,
      描述: get潜行成功描述(策略),
    };
  }

  // 失败程度基于超出多少
  const 超出比例 = (掷骰 - 成功率) / (1 - 成功率);
  const 被发现程度 = 超出比例 > 0.6 ? '当众曝光' : 超出比例 > 0.3 ? '明显暴露' : '轻微可疑';

  return {
    成功: false,
    被发现程度,
    紧张度变化: 10 + Math.round(超出比例 * 20),
    描述: get潜行失败描述(策略, 被发现程度),
    触发后果: 被发现程度 === '当众曝光' ? 创建曝光后果(游戏时间) : undefined,
  };
}

/**
 * 计算潜行成功率
 */
export function 计算潜行成功率(
  策略: 潜行策略,
  上下文: 潜行判定上下文
): number {
  const 配置 = 策略系数[策略];
  const 基础成功率 = 0.5;

  // 正面因素
  const 反侦察加成 = 上下文.反侦察等级 * 0.05;
  const 策略加成 = 配置.基础加成;
  const 时间隐私加成 = (上下文.时间隐私度 / 100) * 0.2;

  // 负面因素
  const 暴露度惩罚 = (上下文.服装暴露度 / 100) * 0.3;
  const 人数惩罚 = Math.min(0.3, (上下文.周围人数 / 20) * 0.3);
  const 场景发现惩罚 = 上下文.场景发现概率 * 0.2;
  const 风险乘数 = 配置.风险系数;

  const 原始成功率 = (基础成功率 + 反侦察加成 + 策略加成 + 时间隐私加成
    - 暴露度惩罚 - 人数惩罚 - 场景发现惩罚) / 风险乘数;

  return Math.max(0.1, Math.min(0.9, 原始成功率));
}

/**
 * 紧急应对 — 被发现后的即时选择
 */
export function 紧急应对(
  应对: 紧急应对,
  当前严重度: '轻微可疑' | '明显暴露' | '当众曝光',
  上下文: { 亲密度?: number; 声誉?: number; 持有金钱?: number },
  游戏时间: string
): 紧急应对结果 {
  const 配置 = 应对系数[应对];
  const 严重度系数 = 当前严重度 === '当众曝光' ? 0.6 : 当前严重度 === '明显暴露' ? 0.8 : 1.0;

  // 资源加成
  const 资源加成 = (() => {
    switch (应对) {
      case '贿赂': return Math.min(0.2, (上下文.持有金钱 ?? 0) / 1000 * 0.2);
      case '解释': return Math.min(0.15, (上下文.亲密度 ?? 0) / 100 * 0.15);
      case '威胁': return Math.min(0.1, (上下文.声誉 ?? 0) / 100 * 0.1);
      default: return 0;
    }
  })();

  const 成功率 = Math.max(0.1, Math.min(0.8,
    (配置.基础成功率 + 资源加成) * 严重度系数 / 配置.风险系数
  ));

  if (Math.random() <= 成功率) {
    const 严重度降低 = 当前严重度 === '当众曝光' ? 1 : 当前严重度 === '明显暴露' ? 2 : 3;
    return {
      成功: true,
      严重度降低,
      描述: get应对成功描述(应对),
    };
  }

  return {
    成功: false,
    严重度降低: 0,
    描述: get应对失败描述(应对),
    额外后果: 应对 === '威胁' ? 创建威胁反噬后果(游戏时间) : undefined,
  };
}

/**
 * 获取反侦察技巧等级（基于已有技能）
 */
export function 获取反侦察等级(状态: 露出状态): number {
  // 基于成功露出次数计算经验等级
  const 成功次数 = 状态.成功露出次数 ?? 0;
  if (成功次数 >= 20) return 5;
  if (成功次数 >= 15) return 4;
  if (成功次数 >= 10) return 3;
  if (成功次数 >= 5) return 2;
  if (成功次数 >= 2) return 1;
  return 0;
}

