function 评估源头类型(流言: 网络流言状态): 溯源结果['源头类型'] {
  if (流言.传播渠道.includes('匿名论坛')) return '匿名论坛';
  if (流言.有无证据) return '意外泄露';
  if (流言.当前等级 > 60) return '恶意散播';
  return '熟人传播';
}

