import type { 网络流言状态, 名誉状态, 露出后果记录 } from '../exposureNSFW/types';

// ==================== 类型定义 ====================

export type 反制策略 = '辟谣' | '转移注意力' | '反向造谣' | '收买关键人物';

export type 溯源结果 = {
  找到源头: boolean;
  源头类型: '匿名论坛' | '熟人传播' | '恶意散播' | '意外泄露' | '未知';
  关键传播节点?: string;
  描述: string;
};

export type 声誉修复方式 = '慈善行为' | '公开露面' | '关系修复' | '低调行事';

export interface 反制结果 {
  成功: boolean;
  谣言等级变化: number;  // 负数表示降低
  声誉变化: number;
  描述: string;
  额外后果?: 露出后果记录;
}

export interface 谣言利用结果 {
  成功: boolean;
  暧昧度提升: number;
  描述: string;
}

// ==================== 策略系数 ====================

const 反制系数: Record<反制策略, { 基础成功率: number; 声誉影响: number }> = {
  '辟谣': { 基础成功率: 0.6, 声誉影响: 0.1 },
  '转移注意力': { 基础成功率: 0.5, 声誉影响: 0 },
  '反向造谣': { 基础成功率: 0.3, 声誉影响: -0.15 },
  '收买关键人物': { 基础成功率: 0.7, 声誉影响: -0.05 },
};

// ==================== 核心引擎函数 ====================

/**
 * 反制谣言
 */
export function 反制谣言(
  策略: 反制策略,
  当前流言: 网络流言状态,
  上下文: { 声誉?: number; 持有金钱?: number; 亲密度?: number },
  游戏时间: string
): 反制结果 {
  const 配置 = 反制系数[策略];
  const 流言等级系数 = Math.max(0.3, 1 - (当前流言.当前等级 / 100) * 0.5);

  // 资源加成
  const 资源加成 = (() => {
    switch (策略) {
      case '收买关键人物':
        return Math.min(0.2, (上下文.持有金钱 ?? 0) / 500 * 0.2);
      case '辟谣':
        return Math.min(0.15, (上下文.声誉 ?? 0) / 100 * 0.15);
      case '转移注意力':
        return Math.min(0.1, (上下文.亲密度 ?? 0) / 100 * 0.1);
      default:
        return 0;
    }
  })();

  const 成功率 = Math.max(0.1, Math.min(0.9,
    (配置.基础成功率 + 资源加成) * 流言等级系数
  ));

  if (Math.random() <= 成功率) {
    const 等级降低 = 策略 === '辟谣' ? 15 + Math.round(配置.声誉影响 * 100) :
                     策略 === '转移注意力' ? 10 :
                     策略 === '反向造谣' ? 20 :
                     25;
    const 声誉变化 = Math.round(配置.声誉影响 * 50);

    return {
      成功: true,
      谣言等级变化: -等级降低,
      声誉变化,
      描述: get反制成功描述(策略),
    };
  }

  // 反制失败
  const 反噬程度 = 策略 === '反向造谣' ? 15 : 策略 === '收买关键人物' ? 10 : 5;

  return {
    成功: false,
    谣言等级变化: 反噬程度,
    声誉变化: -Math.round(反噬程度 * 0.5),
    描述: get反制失败描述(策略),
    额外后果: 策略 === '反向造谣' ? 创建反向造谣后果(游戏时间) : undefined,
  };
}

/**
 * 谣言溯源 — 追踪源头
 */
export function 谣言溯源(
  当前流言: 网络流言状态,
  上下文: { 声誉?: number }
): 溯源结果 {
  const 基础成功率 = 0.4;
  const 声誉加成 = Math.min(0.2, (上下文.声誉 ?? 0) / 100 * 0.2);
  const 渠道系数 = 当前流言.传播渠道.length > 2 ? 0.7 : 1.0;

  if (Math.random() > (基础成功率 + 声誉加成) * 渠道系数) {
    return {
      找到源头: false,
      源头类型: '未知',
      描述: '传播路径太过复杂，暂时无法确定源头',
    };
  }

  // 确定源头类型
  const 源头类型 = 评估源头类型(当前流言);

  const 关键节点 = 源头类型 === '熟人传播' ? '某个亲密关系的圈子' :
                   源头类型 === '匿名论坛' ? '匿名板块的某个帖子' :
                   源头类型 === '恶意散播' ? '一个有明确目的的账号' :
                   '意外泄露的现场目击者';

  return {
    找到源头: true,
    源头类型,
    关键传播节点: 关键节点,
    描述: `溯源成功：谣言源自「${关键节点}」，传播类型为「${源头类型}」`,
  };
}

/**
 * 声誉修复 — 通过特定行为恢复声誉
 */
export function 声誉修复(
  方式: 声誉修复方式,
  _当前名誉: 名誉状态,
  _游戏时间: string
): { 公开名誉变化: number; 私密名誉变化: number; 描述: string } {
  const 修复量: Record<声誉修复方式, { 公开: number; 私密: number }> = {
    '慈善行为': { 公开: 8, 私密: 3 },
    '公开露面': { 公开: 5, 私密: 0 },
    '关系修复': { 公开: 2, 私密: 7 },
    '低调行事': { 公开: 3, 私密: 5 },
  };

  const 变化 = 修复量[方式];
  const 随机因子 = 0.8 + Math.random() * 0.4;

  return {
    公开名誉变化: Math.round(变化.公开 * 随机因子),
    私密名誉变化: Math.round(变化.私密 * 随机因子),
    描述: get修复描述(方式),
  };
}

/**
 * 谣言利用 — 某些情况下故意利用谣言制造暧昧氛围
 */
export function 谣言利用(
  当前流言: 网络流言状态,
  上下文: { 亲密度?: number; 冒险倾向?: number }
): 谣言利用结果 {
  if (当前流言.当前等级 < 20) {
    return { 成功: false, 暧昧度提升: 0, 描述: '谣言热度太低，无法利用' };
  }

  const 基础成功率 = 0.4;
  const 亲密度加成 = Math.min(0.2, (上下文.亲密度 ?? 0) / 100 * 0.2);
  const 冒险系数 = (上下文.冒险倾向 ?? 0.5);

  if (Math.random() <= (基础成功率 + 亲密度加成) * 冒险系数) {
    const 暧昧度提升 = Math.round(10 + Math.random() * 15);
    return {
      成功: true,
      暧昧度提升,
      描述: '巧妙地利用了谣言的氛围，增加了彼此的暧昧感',
    };
  }

  return {
    成功: false,
    暧昧度提升: 0,
    描述: '利用谣言的尝试太过明显，反而显得刻意',
  };
}

