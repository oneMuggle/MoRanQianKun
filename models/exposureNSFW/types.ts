// ==================== 旧存档类型迁移 ====================

/** 旧版旁观者类型（校园专属）→ 新版通用类型映射 */
const 旧旁观者类型映射: Record<string, 旁观者类型> = {
  '普通同学': '路人',
  '闺蜜好友': '密友',
  '老师辅导员': '权威人物',
  '情敌对手': '竞争对手',
  '室友': '同住者',
};

/** 旧版旁观者距离（校园专属）→ 新版通用距离映射 */
const 旧距离映射: Record<string, 旁观者距离> = {
  '同桌邻座': '紧邻',
  '对面附近': '附近',
  '同一房间远处': '同室远处',
  '路过经过': '经过',
};

/** 旧版周围人状态（校园专属）→ 新版通用状态映射 */
const 旧周围人状态映射: Record<string, 周围人活动状态> = {
  '正常上课': '专注事务',
};

/** 迁移旧版旁观者类型，无法识别的返回原值（运行时兼容） */
export function 迁移旧旁观者类型(旧类型: string): 旁观者类型 | string {
  return 旧旁观者类型映射[旧类型] ?? (旧类型 as 旁观者类型);
}

/** 迁移旧版旁观者距离 */
export function 迁移旧距离(旧距离: string): 旁观者距离 | string {
  return 旧距离映射[旧距离] ?? (旧距离 as 旁观者距离);
}

/** 迁移旧版周围人状态 */
export function 迁移旧周围人状态(旧状态: string): 周围人活动状态 | string {
  return 旧周围人状态映射[旧状态] ?? (旧状态 as 周围人活动状态);
}

/** 迁移单个旁观者档案 */
export function 迁移旁观者档案(档案: Partial<旁观者>): void {
  if (档案.类型 && !(档案.类型 in 旧旁观者类型映射) === false) {
    档案.类型 = 迁移旧旁观者类型(档案.类型) as 旁观者类型;
  }
  if (档案.距离 && !(档案.距离 in 旧距离映射) === false) {
    档案.距离 = 迁移旧距离(档案.距离) as 旁观者距离;
  }
}

/** 迁移紧张度状态 */
export function 迁移紧张度状态(状态: Partial<紧张度状态>): void {
  if (状态.周围人状态 && !(状态.周围人状态 in 旧周围人状态映射) === false) {
    状态.周围人状态 = 迁移旧周围人状态(状态.周围人状态) as 周围人活动状态;
  }
}
