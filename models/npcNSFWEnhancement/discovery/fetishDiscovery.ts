function ensure完整状态(npc: NPC结构): 完整演化状态 & { 性癖发现?: 性癖发现状态 } {
  if (!npc.完整演化状态) {
    npc.完整演化状态 = {
      演化日志: npc.性癖演化状态?.演化日志 ?? [],
      事件计数器: npc.性癖演化状态?.事件计数器 ?? {},
      最后演化时间: npc.性癖演化状态?.最后演化时间 ?? '',
    };
  }
  return npc.完整演化状态 as 完整演化状态 & { 性癖发现?: 性癖发现状态 };
}

function 构建试探上下文(npc: NPC结构): 试探上下文 {
  const 完整状态 = ensure完整状态(npc);
  const 防线值 = 完整状态.心理防线?.防线值 ?? 50;

  // 信任度从完整演化状态中读取，如果没有则用好感度的一半作为估算
  const 信任度 = (完整状态 as any).信任度 ?? Math.round((npc.好感度 ?? 0) * 0.5);

  return {
    亲密度: npc.好感度 ?? 0,
    信任度,
    心理防线值: 防线值,
  };
}

function 获取候选性癖(npc: NPC结构): 性癖解锁节点[] {
  const 完整状态 = ensure完整状态(npc);
  const 发现状态 = 完整状态.性癖发现;
  const 已解锁标识 = new Set(
    (发现状态?.已解锁性癖 ?? []).map(f => `${f.类别}:${f.子类型}`)
  );

  return 性癖解锁树.filter(节点 => {
    const 标识 = `${节点.性癖类别}:${节点.性癖子类型}`;
    if (已解锁标识.has(标识)) return false;
    return 节点.前置节点.every(p => 已解锁标识.has(p));
  });
}

function 评估最佳候选(
  候选: 性癖解锁节点[],
  上下文: 试探上下文,
  试探: 试探类型
): 性癖解锁节点 {
  return 候选.reduce((best, current) => {
    const 当前评分 = 计算适配评分(current, 上下文, 试探);
    const 最佳评分 = 计算适配评分(best, 上下文, 试探);
    return 当前评分 > 最佳评分 ? current : best;
  });
}

function 计算适配评分(节点: 性癖解锁节点, 上下文: 试探上下文, 试探: 试探类型): number {
  const 亲密度分 = Math.min(100, 上下文.亲密度) / 100;
  const 信任度分 = Math.min(100, 上下文.信任度) / 100;
  const 防线分 = 1 - (上下文.心理防线值 / 100);

  const 试探系数: Record<试探类型, number> = {
    '言语试探': 1.0,
    '肢体试探': 0.9,
    '场景试探': 0.95,
  };

  const 强度惩罚 = (节点.解锁强度 - 1) * 0.1;

  return (亲密度分 * 0.4 + 信任度分 * 0.3 + 防线分 * 0.3 - 强度惩罚) * 试探系数[试探];
}

function 计算试探成功率(
  试探: 试探类型,
  上下文: 试探上下文,
  目标: 性癖解锁节点
): number {
  const 基础成功率 = 0.5;
  const 亲密度加成 = Math.min(0.4, (上下文.亲密度 / 100) * 0.4);
  const 信任度加成 = Math.min(0.3, (上下文.信任度 / 100) * 0.3);
  const 防线减成 = (上下文.心理防线值 / 100) * 0.3;
  const 类型系数: Record<试探类型, number> = {
    '言语试探': 0.8,
    '肢体试探': 0.6,
    '场景试探': 0.7,
  };
  const 强度减成 = (目标.解锁强度 - 1) * 0.08;

  return Math.max(0.05, Math.min(0.95,
    (基础成功率 + 亲密度加成 + 信任度加成 - 防线减成 - 强度减成) * 类型系数[试探]
  ));
}

function 计算深度探索成功率(
  策略: 探索策略,
  上下文: 试探上下文,
  目标: 性癖解锁节点
): number {
  const 基础成功率 = 0.4;
  const 策略系数: Record<探索策略, number> = {
    '安全': 0.7,
    '激进': 1.2,
    '暂停': 0,
  };
  const 亲密度加成 = Math.min(0.3, (上下文.亲密度 / 100) * 0.3);
  const 信任度加成 = Math.min(0.2, (上下文.信任度 / 100) * 0.2);
  const 强度减成 = (目标.解锁强度 - 1) * 0.1;

  return Math.max(0.05, Math.min(0.85,
    (基础成功率 + 亲密度加成 + 信任度加成 - 强度减成) * 策略系数[策略]
  ));
}

