/**
 * NPC NSFW 增强模块 — 人格演化主动触发引擎
 * 玩家通过行为序列主动触发表里人格切换和欲望觉醒
 */

import { NPC结构 } from '../../social';
import type { 表人格表现, 里人格表现, 完整演化状态, 敏感点条目, 敏感点开发程度 } from '../types';

// ==================== 类型定义 ====================

export type 触发目标 = '表里互换' | '欲望觉醒' | '创伤封闭' | '解放突破' | '恢复原人格';

export interface 触发条件配置 {
  亲密度阈值?: number;
  信任度阈值?: number;
  防线值要求?: { 最大值?: number; 最小值?: number };
  性癖解锁数?: number;
  敏感点开发数?: number;
  需要历史事件?: string[];
}

export interface 人格演化步骤 {
  步骤序号: number;
  触发时间: string;
  触发目标: 触发目标;
  旧表人格描述: string;
  新表人格描述: string;
  触发原因: string;
}

export interface 人格演化触发状态 {
  尝试历史: 人格演化步骤[];
  最后触发时间: string;
}

