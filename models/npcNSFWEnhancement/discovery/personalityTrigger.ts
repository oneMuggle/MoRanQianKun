// ==================== 核心引擎函数 ====================

/**
 * 初始化人格演化触发系统
 */
export function 初始化人格触发(npc: NPC结构): void {
  const 完整状态 = ensure完整状态(npc);
  if (!完整状态.人格演化触发) {
    完整状态.人格演化触发 = {
      尝试历史: [],
      最后触发时间: '',
    };
  }
}

/**
 * 检查是否满足特定触发目标的条件
 */
export function 检查触发条件(
  npc: NPC结构,
  目标: 触发目标
): { 满足: boolean; 缺失条件: string[] } {
  const 条件 = 条件映射[目标];
  const 完整状态 = ensure完整状态(npc);
  const 上下文 = 构建触发上下文(npc, 完整状态);
  const 缺失条件: string[] = [];

  if (条件.亲密度阈值 && 上下文.亲密度 < 条件.亲密度阈值) {
    缺失条件.push(`亲密度不足（需要 ${条件.亲密度阈值}，当前 ${上下文.亲密度}）`);
  }
  if (条件.信任度阈值 && 上下文.信任度 < 条件.信任度阈值) {
    缺失条件.push(`信任度不足（需要 ${条件.信任度阈值}，当前 ${上下文.信任度}）`);
  }
  if (条件.防线值要求) {
    if (条件.防线值要求.最大值 && 上下文.防线值 > 条件.防线值要求.最大值) {
      缺失条件.push(`防线值过高（需要 <= ${条件.防线值要求.最大值}，当前 ${上下文.防线值}）`);
    }
    if (条件.防线值要求.最小值 && 上下文.防线值 < 条件.防线值要求.最小值) {
      缺失条件.push(`防线值过低（需要 >= ${条件.防线值要求.最小值}，当前 ${上下文.防线值}）`);
    }
  }
  if (条件.性癖解锁数 && 上下文.性癖解锁数 < 条件.性癖解锁数) {
    缺失条件.push(`性癖解锁数不足（需要 ${条件.性癖解锁数}，当前 ${上下文.性癖解锁数}）`);
  }
  if (条件.敏感点开发数 && 上下文.敏感点开发数 < 条件.敏感点开发数) {
    缺失条件.push(`敏感点开发数不足（需要 ${条件.敏感点开发数}，当前 ${上下文.敏感点开发数}）`);
  }
  if (条件.需要历史事件) {
    const 历史事件 = 完整状态.演化日志?.map(l => l.触发原因) ?? [];
    for (const 所需事件 of 条件.需要历史事件) {
      if (!历史事件.some(e => e.includes(所需事件))) {
        缺失条件.push(`缺少关键事件：${所需事件}`);
      }
    }
  }

  return { 满足: 缺失条件.length === 0, 缺失条件 };
}

/**
 * 触发表里人格切换
 */
export function 触发表里人格切换(
  npc: NPC结构,
  目标: 触发目标,
  游戏时间: string
): { 成功: boolean; 描述: string; 旧表人格?: string; 新表人格?: string } {
  const 检查结果 = 检查触发条件(npc, 目标);
  if (!检查结果.满足) {
    return {
      成功: false,
      描述: `触发失败：${检查结果.缺失条件.join('；')}`,
    };
  }

  const 完整状态 = ensure完整状态(npc);
  const 旧表人格 = npc.人格档案?.表;
  const 旧里人格 = npc.人格档案?.里;

  if (!旧表人格 || !旧里人格) {
    return { 成功: false, 描述: 'NPC 没有配置表里人格数据' };
  }

  let 新表人格: 表人格表现;
  let 新里人格: 里人格表现;
  let 描述: string;

  switch (目标) {
    case '表里互换':
      新表人格 = { ...旧里人格, 性格描述: 旧里人格.性格描述, 行为特征: 旧里人格.行为特征, 对话风格: 旧里人格.对话风格, 服饰偏好: '', 社交面具: '' };
      新里人格 = { ...旧表人格, 性格描述: 旧表人格.性格描述, 行为特征: 旧表人格.行为特征, 对话风格: 旧表人格.对话风格, 欲望驱动: [], 反差触发器: '' };
      描述 = '表里人格互换！原本隐藏的一面展现为表层人格';
      break;

    case '欲望觉醒':
      新表人格 = { ...旧表人格, 行为特征: [...旧表人格.行为特征, '表现出更多主动性'] };
      新里人格 = { ...旧里人格, 欲望驱动: [...旧里人格.欲望驱动, '深层欲望被唤醒'] };
      描述 = '欲望觉醒！NPC 开始展现更深层的欲望倾向';
      break;

    case '创伤封闭':
      新表人格 = { ...旧表人格, 社交面具: '封闭', 行为特征: [...旧表人格.行为特征.filter(b => !b.includes('主动')), '回避亲密接触'] };
      新里人格 = { ...旧里人格, 欲望驱动: [] };
      描述 = '创伤封闭！NPC 关闭了内心，变得疏离';
      break;

    case '解放突破':
      新表人格 = { ...旧表人格, 行为特征: [...旧表人格.行为特征, '完全敞开心扉', '主动表达感受'] };
      新里人格 = { ...旧里人格, 欲望驱动: [...旧里人格.欲望驱动, '无条件的信任与依恋'] };
      描述 = '解放突破！NPC 完全放下了心理防线';
      break;

    case '恢复原人格':
      新表人格 = 旧表人格;
      新里人格 = 旧里人格;
      描述 = '恢复原始人格状态';
      break;
  }

  npc.人格档案 = {
    ...npc.人格档案!,
    表: 新表人格,
    里: 新里人格,
  };

  完整状态.人格演化触发!.尝试历史.push({
    步骤序号: 完整状态.人格演化触发!.尝试历史.length + 1,
    触发时间: 游戏时间,
    触发目标: 目标,
    旧表人格描述: 旧表人格.性格描述,
    新表人格描述: 新表人格.性格描述,
    触发原因: 目标,
  });
  完整状态.人格演化触发!.最后触发时间 = 游戏时间;

  return { 成功: true, 描述, 旧表人格: 旧表人格.性格描述, 新表人格: 新表人格.性格描述 };
}

/**
 * 获取欲望觉醒进度（0-100）
 */
export function 获取欲望觉醒进度(npc: NPC结构): number {
  const 完整状态 = ensure完整状态(npc);
  const 上下文 = 构建触发上下文(npc, 完整状态);

  const 亲密度分 = Math.min(100, 上下文.亲密度);
  const 信任度分 = Math.min(100, 上下文.信任度);
  const 防线分 = Math.max(0, 100 - 上下文.防线值);
  const 性癖分 = Math.min(100, (上下文.性癖解锁数 / 5) * 100);

  return Math.round(亲密度分 * 0.3 + 信任度分 * 0.25 + 防线分 * 0.2 + 性癖分 * 0.25);
}

/**
 * 获取人格演化路径（用于 UI 展示）
 */
export function 获取人格演化路径(npc: NPC结构): 人格演化步骤[] {
  const 完整状态 = ensure完整状态(npc);
  return 完整状态.人格演化触发?.尝试历史 ?? [];
}

/**
 * 生成人格演化摘要 — 用于 prompt 注入
 */
export function 生成人格演化摘要(npc: NPC结构): string {
  const 完整状态 = ensure完整状态(npc);
  const 演化历史 = 完整状态.人格演化触发?.尝试历史 ?? [];

  if (演化历史.length === 0) {
    return '人格尚未发生演化';
  }

  const 最新 = 演化历史[演化历史.length - 1];
  const 觉醒进度 = 获取欲望觉醒进度(npc);

  return `已发生 ${演化历史.length} 次人格演化，最新为「${最新.触发目标}」，欲望觉醒进度：${觉醒进度}%`;
}

// ==================== 内部辅助函数 ====================

function ensure完整状态(npc: NPC结构): 完整演化状态 & { 人格演化触发?: 人格演化触发状态 } {
  if (!npc.完整演化状态) {
    npc.完整演化状态 = {
      演化日志: npc.性癖演化状态?.演化日志 ?? [],
      事件计数器: npc.性癖演化状态?.事件计数器 ?? {},
      最后演化时间: npc.性癖演化状态?.最后演化时间 ?? '',
    };
  }
  return npc.完整演化状态 as 完整演化状态 & { 人格演化触发?: 人格演化触发状态 };
}

function 构建触发上下文(npc: NPC结构, 完整状态: 完整演化状态) {
  const 防线值 = 完整状态.心理防线?.防线值 ?? 50;
  const 信任度 = Math.round((npc.好感度 ?? 0) * 0.5);
  const 性癖解锁数 = 完整状态.性癖发现?.已解锁性癖?.length ?? 0;
  const 敏感点开发数 = (npc.敏感点档案?.主要敏感点 ?? []).filter((p): p is 敏感点条目 & { 开发程度: 敏感点开发程度 } =>
    p.开发程度 && p.开发程度 !== '未开发'
  ).length;

  return {
    亲密度: npc.好感度 ?? 0,
    信任度,
    防线值,
    性癖解锁数,
    敏感点开发数,
  };
}
