/**
 * 回合结束时调用：检查并应用性癖衰减
 */
export function 应用性癖衰减(
  npc: NPC结构,
  当前游戏时间: string
): { 有变化: boolean; 变化日志: 性癖变化日志[] } {
  if (!npc.性癖档案 || !npc.性癖演化状态) {
    return { 有变化: false, 变化日志: [] };
  }

  const 日志: 性癖变化日志[] = [];
  const 档案 = npc.性癖档案;

  for (const 偏好 of [...档案.核心偏好]) {
    if (偏好.来源 === '人格模板' && 偏好.强度 <= 2) continue;

    const 衰减率 = 偏好.衰减率 ?? 默认衰减率;
    const 活跃时间 = 偏好.最近活跃时间;
    if (!活跃时间) continue;

    const 回合差 = 计算回合差(当前游戏时间, 活跃时间);
    if (回合差 < 衰减保护期) continue;

    const 衰减量 = (回合差 - 衰减保护期) * 衰减率;
    if (衰减量 < 0.1) continue;

    const 旧强度 = 偏好.强度;
    const 新强度 = Math.max(1, Math.round(偏好.强度 - 衰减量));

    if (新强度 < 旧强度) {
      if (新强度 === 1 && 偏好.来源 === '事件觉醒') {
        档案.核心偏好 = 档案.核心偏好.filter(p => p !== 偏好);
        偏好.强度 = 0 as 性癖强度;
        档案.隐藏偏好.push(偏好);
        日志.push({
          时间: 当前游戏时间,
          变化类型: '衰减淡化',
          性癖类别: 偏好.类别,
          性癖子类型: 偏好.子类型,
          旧值: 旧强度,
          新值: 0,
          触发原因: `长期未激活(${回合差}回合)`,
          关联事件: '性癖自然淡化',
        });
      } else {
        偏好.强度 = 新强度 as 性癖强度;
        日志.push({
          时间: 当前游戏时间,
          变化类型: '强度降低',
          性癖类别: 偏好.类别,
          性癖子类型: 偏好.子类型,
          旧值: 旧强度,
          新值: 新强度,
          触发原因: `长期未激活(${回合差}回合)`,
          关联事件: '性癖自然淡化',
        });
      }
    }
  }

  if (日志.length > 0) {
    档案.倾向摘要 = 生成性癖摘要(档案.核心偏好);
    const 演化状态 = npc.性癖演化状态!;
    演化状态.演化日志.push(...日志);
    演化状态.最后演化时间 = 当前游戏时间;
  }

  // --- 新增：防线恢复 + 偏好漂移 + 事后护理 + 孕产 ---
  应用防线恢复(npc, 当前游戏时间);
  应用偏好漂移(npc, 当前游戏时间);
  应用事后恢复(npc, 当前游戏时间);
  推进妊娠进程(npc, 当前游戏时间);

  return { 有变化: 日志.length > 0, 变化日志: 日志 };
}

export function 计算回合差(当前时间: string, 参照时间: string): number {
  if (!当前时间 || !参照时间) return 0;

  const 当前分钟 = 解析游戏时间到分钟(当前时间);
  const 参照分钟 = 解析游戏时间到分钟(参照时间);
  if (当前分钟 === 0 || 参照分钟 === 0) return 0;

  const 分钟差 = Math.abs(当前分钟 - 参照分钟);
  return Math.floor(分钟差 / 30);
}

function 解析游戏时间到分钟(时间串: string): number {
  const parts = 时间串.split(':');
  if (parts.length < 5) return 0;

  const 数字部分 = parts.map(Number);
  if (数字部分.some(n => isNaN(n))) return 0;

  const [年, 月, 日, 时, 分] = 数字部分;

  return ((((年 * 12 + 月) * 30 + 日) * 24 + 时) * 60 + 分);
}

