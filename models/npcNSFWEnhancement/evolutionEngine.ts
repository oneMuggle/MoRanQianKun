// ==================== 性癖变化执行 ====================

function 执行性癖变化(
  npc: NPC结构,
  映射: 事件映射配置,
  事件: 性癖触发事件,
  游戏时间: string,
  事件描述: string
): 性癖变化日志[] {
  const 日志: 性癖变化日志[] = [];
  const 档案 = npc.性癖档案!;

  for (const 子类型 of 映射.触发子类型) {
    if (档案.核心偏好.length >= 核心偏好上限) break;

    const 已有条目 = 档案.核心偏好.find(p => p.子类型 === 子类型);

    if (已有条目) {
      const 新日志 = 提升性癖强度(已有条目, 映射, 游戏时间, 事件, 事件描述);
      if (新日志) 日志.push(新日志);
    } else {
      const 隐藏条目 = 档案.隐藏偏好.find(p => p.子类型 === 子类型);
      if (隐藏条目) {
        const 新日志 = 升级隐藏偏好(档案, 隐藏条目, 映射, 游戏时间, 事件, 事件描述);
        if (新日志) 日志.push(新日志);
      } else {
        const 新日志 = 觉醒新性癖(档案, 映射, 游戏时间, 事件, 事件描述);
        if (新日志) 日志.push(新日志);
      }
    }
  }

  if (日志.length > 0) {
    档案.倾向摘要 = 生成性癖摘要(档案.核心偏好);
    const 演化状态 = npc.性癖演化状态!;
    演化状态.演化日志.push(...日志);
    if (演化状态.演化日志.length > 20) {
      演化状态.演化日志 = 演化状态.演化日志.slice(-20);
    }
    演化状态.最后演化时间 = 游戏时间;
    更新穿着风格演变趋势(npc);
  }

  return 日志;
}

function 提升性癖强度(
  条目: 性癖条目,
  映射: 事件映射配置,
  游戏时间: string,
  事件: 性癖触发事件,
  事件描述: string
): 性癖变化日志 | null {
  const 旧强度 = 条目.强度;
  if (旧强度 >= 5) return null;

  const 增量 = Math.round(映射.强度增量 * 10) / 10;
  const 新强度 = Math.min(5, Math.round((旧强度 + 增量) * 10) / 10) as 性癖强度;
  if (新强度 <= 旧强度) return null;

  条目.强度 = 新强度;
  条目.来源 = 条目.来源 ?? '行为演化';
  条目.最近活跃时间 = 游戏时间;

  return {
    时间: 游戏时间,
    变化类型: '强度提升',
    性癖类别: 映射.触发类别,
    性癖子类型: 条目.子类型,
    旧值: 旧强度,
    新值: 新强度,
    触发原因: `${事件}累积(${映射.累积阈值}次)`,
    关联事件: 事件描述,
  };
}

function 升级隐藏偏好(
  档案: NonNullable<NPC结构['性癖档案']>,
  隐藏条目: 性癖条目,
  映射: 事件映射配置,
  游戏时间: string,
  事件: 性癖触发事件,
  事件描述: string
): 性癖变化日志 | null {
  if (档案.核心偏好.length >= 核心偏好上限) return null;

  档案.隐藏偏好 = 档案.隐藏偏好.filter(p => p !== 隐藏条目);
  隐藏条目.强度 = Math.max(1, Math.round(映射.强度增量 * 10) / 10) as 性癖强度;
  隐藏条目.来源 = '事件觉醒';
  隐藏条目.最近活跃时间 = 游戏时间;
  档案.核心偏好.push(隐藏条目);

  return {
    时间: 游戏时间,
    变化类型: '偏好升级',
    性癖类别: 映射.触发类别,
    性癖子类型: 隐藏条目.子类型,
    旧值: 0,
    新值: 隐藏条目.强度,
    触发原因: `${事件}累积(${映射.累积阈值}次)`,
    关联事件: 事件描述,
  };
}

