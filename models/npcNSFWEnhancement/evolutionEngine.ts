/**
 * 确保 NPC 有演化状态
 */
export function 初始化演化状态(npc: NPC结构): void {
  if (!npc.性癖演化状态) {
    npc.性癖演化状态 = {
      演化日志: [],
      事件计数器: {},
      最后演化时间: '',
    };
  }
  // --- 新增：确保完整演化状态存在 ---
  if (!npc.完整演化状态) {
    npc.完整演化状态 = {
      演化日志: npc.性癖演化状态.演化日志,
      事件计数器: npc.性癖演化状态.事件计数器,
      最后演化时间: npc.性癖演化状态.最后演化时间,
    };
  }
  // --- 新增：初始化各子系统 ---
  初始化心理防线(npc, npc.人格档案);
  if (!npc.完整演化状态.潜能池) {
    npc.完整演化状态.潜能池 = 初始化潜能池(npc);
  }
  初始化偏好漂移(npc);
  初始化敏感点演化(npc);
}

