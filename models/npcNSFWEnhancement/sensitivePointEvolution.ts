/**
 * NPC NSFW 增强模块 — 敏感点演化系统
 * 根据NSFW事件推进敏感点开发程度和新发现
 */

import { NPC结构 } from '../social';
import type {
  敏感点开发日志,
  敏感点发现日志,
  身体区域分类,
  敏感点开发程度,
  完整演化状态,
} from './types';
import type { 性癖触发事件 } from './eventMapping';

// ==================== 事件→敏感点区域映射 ====================

export const 事件敏感点映射: Record<性癖触发事件, 身体区域分类[]> = {
  '强势对待': ['腰腹区', '四肢区'],
  '温柔对待': ['头颈区', '背部区'],
  '露出体验': ['胸胸区', '私密区'],
  '束缚体验': ['四肢区', '背部区'],
  '多人场景': ['胸胸区', '私密区', '头颈区'],
  '角色扮演': ['特殊区'],
  '公开冒险': ['胸胸区', '私密区'],
  '感官刺激': ['头颈区', '四肢区', '胸胸区'],
  '语言调教': ['头颈区'],
  '主导体验': ['腰腹区', '四肢区'],
  '服从体验': ['腰腹区', '背部区'],
  '情感依赖': ['头颈区', '背部区'],
  '禁忌突破': ['私密区', '特殊区'],
  '人格切换': ['特殊区'],
};

const 开发程度序列: 敏感点开发程度[] = ['未开发', '初步探索', '渐入佳境', '深度开发', '完全开发'];

/**
 * 初始化敏感点演化状态
 */
export function 初始化敏感点演化(npc: NPC结构): void {
  const 完整状态 = ensure完整状态(npc);
  if (完整状态.敏感点演化) return;

  完整状态.敏感点演化 = {
    开发记录: [],
    新发现记录: [],
    最后演化时间: '',
  };
}

/**
 * 根据NSFW事件更新敏感点开发程度
 */
export function 更新敏感点开发(
  npc: NPC结构,
  事件: 性癖触发事件,
  游戏时间: string,
  _事件描述: string
): { 有变化: boolean; 日志: (敏感点开发日志 | 敏感点发现日志)[] } {
  const 完整状态 = ensure完整状态(npc);
  if (!完整状态.敏感点演化) {
    初始化敏感点演化(npc);
  }

  const 区域列表 = 事件敏感点映射[事件];
  if (!区域列表 || 区域列表.length === 0) {
    return { 有变化: false, 日志: [] };
  }

  if (!npc.敏感点档案) {
    return { 有变化: false, 日志: [] };
  }

  const 日志: (敏感点开发日志 | 敏感点发现日志)[] = [];

  for (const 区域 of 区域列表) {
    // 检查主要敏感点中是否有该区域的点
    for (const 敏感点 of npc.敏感点档案.主要敏感点) {
      if (敏感点.区域 !== 区域) continue;

      const 旧开发程度 = 敏感点.开发程度 ?? '未开发';
      const 旧索引 = 开发程度序列.indexOf(旧开发程度);
      const 新索引 = Math.min(开发程度序列.length - 1, 旧索引 + 1);

      if (新索引 > 旧索引) {
        const 新开发程度 = 开发程度序列[新索引];
        敏感点.开发程度 = 新开发程度;

        日志.push({
          时间: 游戏时间,
          敏感点名称: 敏感点.名称,
          旧开发程度,
          新开发程度,
          触发事件: 事件,
        });

        完整状态.敏感点演化!.开发记录.push(日志[日志.length - 1] as 敏感点开发日志);
      }
    }

    // 随机发现隐藏敏感点（10%概率）
    if (Math.random() < 0.1 && npc.敏感点档案.隐藏敏感点.length > 0) {
      const 隐藏索引 = Math.floor(Math.random() * npc.敏感点档案.隐藏敏感点.length);
      const 隐藏点 = npc.敏感点档案.隐藏敏感点[隐藏索引];

      if (隐藏点.区域 === 区域 && 隐藏点.发现状态 === '未发觉') {
        隐藏点.发现状态 = '已发现';
        隐藏点.开发程度 = '未开发';

        // 移入主要敏感点
        npc.敏感点档案.隐藏敏感点.splice(隐藏索引, 1);
        npc.敏感点档案.主要敏感点.push(隐藏点);

        日志.push({
          时间: 游戏时间,
          敏感点名称: 隐藏点.名称,
          区域: 隐藏点.区域,
          触发事件: 事件,
        });

        完整状态.敏感点演化!.新发现记录.push(日志[日志.length - 1] as 敏感点发现日志);
      }
    }
  }

  if (日志.length > 0) {
    完整状态.敏感点演化!.最后演化时间 = 游戏时间;

    // 记录保留最近20条
    if (完整状态.敏感点演化!.开发记录.length > 20) {
      完整状态.敏感点演化!.开发记录 = 完整状态.敏感点演化!.开发记录.slice(-20);
    }
    if (完整状态.敏感点演化!.新发现记录.length > 10) {
      完整状态.敏感点演化!.新发现记录 = 完整状态.敏感点演化!.新发现记录.slice(-10);
    }
  }

  return { 有变化: 日志.length > 0, 日志 };
}

// ==================== 辅助函数 ====================

function ensure完整状态(npc: NPC结构): 完整演化状态 {
  if (!npc.完整演化状态) {
    npc.完整演化状态 = {
      演化日志: npc.性癖演化状态?.演化日志 ?? [],
      事件计数器: npc.性癖演化状态?.事件计数器 ?? {},
      最后演化时间: npc.性癖演化状态?.最后演化时间 ?? '',
    };
  }
  return npc.完整演化状态;
}
