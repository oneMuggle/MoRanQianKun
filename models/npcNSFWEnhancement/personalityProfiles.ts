// ==================== 查询函数 ====================

export function 匹配人格档案(
  npc核心性格: string | undefined,
  npc身份: string | undefined,
  eraId: string | undefined
): 表里人格档案 | null {
  if (!npc核心性格 && !npc身份) return null;

  const 搜索文本 = `${npc核心性格 || ''} ${npc身份 || ''}`.toLowerCase();
  const 人格库 = eraId ? 获取对应人格库(eraId) : Object.values(全部人格档案).flat();

  // 优先：精确匹配人格名称
  for (const 人格 of 人格库) {
    if (搜索文本.includes(人格.名称.toLowerCase())) return 人格;
  }

  // 次优先：双向标签匹配（标签出现在搜索文本中，或搜索文本出现在标签中）
  for (const 人格 of 人格库) {
    const 标签匹配 = 人格.身份标签.some(tag => {
      const tagLower = tag.toLowerCase();
      return 搜索文本.includes(tagLower) || tagLower.includes(搜索文本);
    });
    if (标签匹配) return 人格;
  }

  // 不匹配时返回null，而非fallback到第一个
  return null;
}

function 获取对应人格库(eraId: string): 表里人格档案[] {
  if (eraId.includes('urban')) return 里都市人格档案;
  if (eraId.includes('rural')) return 里乡土人格档案;
  if (eraId.includes('republic') || eraId.includes('谍战')) return 里谍战人格档案;
  if (eraId.includes('campus')) return 里校园人格档案;
  return Object.values(全部人格档案).flat();
}

export function 解锁隐藏偏好(人格: 表里人格档案, 亲密度等级: number): 性癖条目[] {
  return 人格.关联偏好.filter(p =>
    !p.解锁条件 ||
    (p.解锁条件.类型 === '亲密度阈值' && Number(p.解锁条件.值) <= 亲密度等级)
  );
}
