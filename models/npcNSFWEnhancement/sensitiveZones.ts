// ==================== 查询函数 ====================

export function 获取敏感点推荐(params: 敏感点查询参数): 敏感点条目[] {
  const { eraId, 最大条目数 = 4 } = params;
  const 基础 = [...全时代通用敏感点];

  基础.sort((a, b) => b.敏感度 - a.敏感度);

  if (eraId && 时代敏感点名称映射[eraId]) {
    const 映射 = 时代敏感点名称映射[eraId];
    return 基础.slice(0, 最大条目数).map(p => {
      if (映射[p.名称]) {
        return { ...p, 时代名称: 映射[p.名称] };
      }
      return p;
    });
  }

  return 基础.slice(0, 最大条目数);
}

export function 生成敏感点摘要(敏感点: 敏感点条目[]): string {
  if (敏感点.length === 0) return '无明显敏感区域';
  const 最敏感 = 敏感点.reduce((a, b) => a.敏感度 > b.敏感度 ? a : b);
  const 名称 = 最敏感.时代名称 || 最敏感.名称;
  const 开发 = 最敏感.开发程度 ? `，开发程度：${最敏感.开发程度}` : '';
  return `最敏感的区域是${名称}（敏感度${最敏感.敏感度}/5${开发}），被触碰时会${最敏感.反应描述.substring(0, 15)}…`;
}
