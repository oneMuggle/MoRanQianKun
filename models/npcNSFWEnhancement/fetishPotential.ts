const 默认觉醒阈值 = 5;

/**
 * 初始化潜能池（基于NPC人格和初始性癖）
 */
export function 初始化潜能池(npc: NPC结构): 性癖潜能值[] {
  const 完整状态 = ensure完整状态(npc);
  if (完整状态.潜能池) return 完整状态.潜能池;

  const 已有类别 = new Set<string>();
  if (npc.性癖档案) {
    for (const p of npc.性癖档案.核心偏好) {
      已有类别.add(p.子类型);
    }
    for (const p of npc.性癖档案.隐藏偏好) {
      已有类别.add(p.子类型);
    }
  }

  // 从推荐性癖中筛选不在已有偏好中的条目作为潜能池
  const 推荐 = 获取性癖推荐({ eraId: undefined, 最大条目数: 12 });
  const 潜能池: 性癖潜能值[] = [];

  // 基于人格计算初始潜能值
  const 人格 = npc.人格档案;

  for (const 推荐项 of 推荐) {
    if (已有类别.has(推荐项.子类型)) continue;

    // 人格匹配度决定初始潜能
    let 初始潜能 = 2; // 默认中等偏低
    if (人格) {
      const 全描述 = 人格.表.性格描述 + 人格.里.性格描述 + 人格.里.欲望驱动.join('');
      // 简单的关键词匹配
      if (推荐项.类别 === '主导控制' && (全描述.includes('主导') || 全描述.includes('控制'))) {
        初始潜能 = 5;
      }
      if (推荐项.类别 === '情感依赖' && (全描述.includes('依恋') || 全描述.includes('专属'))) {
        初始潜能 = 5;
      }
      if (推荐项.类别 === '暴露窥视' && 人格.里.反差触发器.includes('暴露')) {
        初始潜能 = 6;
      }
      if (推荐项.类别 === '拘束限制' && (全描述.includes('束缚') || 全描述.includes('限制'))) {
        初始潜能 = 4;
      }
    }

    潜能池.push({
      类别: 推荐项.类别,
      子类型: 推荐项.子类型,
      潜能值: 初始潜能,
      已觉醒: false,
      觉醒条件: 获取觉醒条件(推荐项.类别),
      觉醒阈值: 默认觉醒阈值,
      当前累积: 0,
    });
  }

  完整状态.潜能池 = 潜能池;
  return 潜能池;
}

/**
 * 根据事件更新潜能累积
 */
export function 更新潜能累积(
  npc: NPC结构,
  事件: 性癖触发事件,
  _游戏时间: string
): 性癖潜能值 | null {
  const 完整状态 = ensure完整状态(npc);
  if (!完整状态.潜能池) return null;

  const 映射 = 获取事件映射(事件);
  if (!映射) return null;

  const 触发类别 = 映射.触发类别;

  // 找到对应类别的未觉醒潜能
  for (const 潜能 of 完整状态.潜能池) {
    if (潜能.已觉醒) continue;
    if (潜能.类别 !== 触发类别) continue;

    潜能.当前累积 += 1;

    // 达到觉醒阈值
    if (潜能.当前累积 >= 潜能.觉醒阈值 && 潜能.潜能值 >= 4) {
      return 潜能;
    }
  }

  return null;
}

// ==================== 渐进觉醒阶段 ====================

/**
 * 根据累积进度计算当前觉醒阶段
 */
export function 计算觉醒阶段(潜能: 性癖潜能值): 潜能觉醒阶段 {
  if (潜能.已觉醒) return '已觉醒';

  const 进度 = 潜能.当前累积 / 潜能.觉醒阈值;
  const 潜能系数 = 潜能.潜能值 / 10;

  // 综合进度 = 累积进度 * 潜能系数
  const 综合进度 = 进度 * 潜能系数;

  if (综合进度 >= 0.9) return '即将觉醒';
  if (综合进度 >= 0.6) return '显露';
  if (综合进度 >= 0.3) return '萌芽';
  return '沉睡';
}

/**
 * 生成潜能暗示提示词（注入到 AI 提示词中）
 * 根据觉醒阶段给 AI 不同的叙事指引
 */
export function 生成潜能暗示(潜能: 性癖潜能值): string | null {
  if (潜能.已觉醒) return null;

  const 阶段 = 计算觉醒阶段(潜能);
  const 进度 = Math.round((潜能.当前累积 / 潜能.觉醒阈值) * 100);

  switch (阶段) {
    case '即将觉醒':
      return `【潜能暗示】${潜能.子类型}方向已接近觉醒边缘（累积${进度}%），在适当的情境下可能突然展现这一倾向。`;
    case '显露':
      return `【潜能暗示】${潜能.子类型}方向已有明显萌芽（累积${进度}%），在相关情境中可能偶尔流露出微妙的兴趣。`;
    case '萌芽':
      return `【潜能暗示】${潜能.子类型}方向有极微弱的潜在倾向（累积${进度}%），尚不明显。`;
    default:
      return null;
  }
}

/**
 * 批量生成潜能暗示（用于 prompt 注入）
 */
export function 生成潜能暗示列表(潜能池: 性癖潜能值[]): string[] {
  const 暗示: string[] = [];
  for (const 潜能 of 潜能池) {
    const 文本 = 生成潜能暗示(潜能);
    if (文本) 暗示.push(文本);
  }
  return 暗示.slice(0, 3);  // 最多注入3条暗示
}

/**
 * 将觉醒的潜能转化为实际性癖条目
 */
export function 觉醒潜能(
  npc: NPC结构,
  潜能: 性癖潜能值,
  游戏时间: string
): 性癖变化日志 | null {
  if (!npc.性癖档案) return null;

  const 档案 = npc.性癖档案;
  const 核心偏好上限 = 8;

  if (档案.核心偏好.length >= 核心偏好上限) return null;

  // 检查是否已存在相同子类型
  if (档案.核心偏好.some(p => p.子类型 === 潜能.子类型)) return null;
  if (档案.隐藏偏好.some(p => p.子类型 === 潜能.子类型)) return null;

  // 将隐藏偏好中的对应条目提升（如果存在）
  const 隐藏索引 = 档案.隐藏偏好.findIndex(p => p.子类型 === 潜能.子类型);
  if (隐藏索引 >= 0) {
    const 隐藏条目 = 档案.隐藏偏好.splice(隐藏索引, 1)[0];
    隐藏条目.强度 = Math.max(1, Math.floor(潜能.潜能值 / 2)) as import('./types').性癖强度;
    隐藏条目.来源 = '事件觉醒';
    隐藏条目.最近活跃时间 = 游戏时间;
    档案.核心偏好.push(隐藏条目);

    潜能.已觉醒 = true;

    return {
      时间: 游戏时间,
      变化类型: '新性癖觉醒',
      性癖类别: 潜能.类别,
      性癖子类型: 潜能.子类型,
      旧值: 0,
      新值: 隐藏条目.强度,
      触发原因: `潜能觉醒(累积${潜能.当前累积}次，潜能值${潜能.潜能值})`,
      关联事件: '偏好漂移累积觉醒',
    };
  }

  // 创建新性癖条目
  const 强度 = Math.max(1, Math.min(5, Math.floor(潜能.潜能值 / 2))) as import('./types').性癖强度;
  const 新条目: 性癖条目 = {
    类别: 潜能.类别,
    子类型: 潜能.子类型,
    强度,
    来源: '事件觉醒',
    描述: `${潜能.类别}方向的潜能觉醒`,
    衰减率: 0.02,
    最近活跃时间: 游戏时间,
  };

  档案.核心偏好.push(新条目);
  潜能.已觉醒 = true;

  return {
    时间: 游戏时间,
    变化类型: '新性癖觉醒',
    性癖类别: 潜能.类别,
    性癖子类型: 潜能.子类型,
    旧值: 0,
    新值: 强度,
    触发原因: `潜能觉醒(累积${潜能.当前累积}次，潜能值${潜能.潜能值})`,
    关联事件: '偏好漂移累积觉醒',
  };
}

/**
 * 获取某性癖大类的觉醒条件
 */
function 获取觉醒条件(类别: 性癖大类): string[] {
  const 条件映射: Record<性癖大类, string[]> = {
    '主导控制': ['强势对待', '服从体验', '主导体验'],
    '角色扮演': ['角色扮演', '人格切换'],
    '感官刺激': ['感官刺激', '温柔对待'],
    '暴露窥视': ['露出体验', '公开冒险', '多人场景'],
    '拘束限制': ['束缚体验', '强势对待'],
    '语言调教': ['语言调教', '强势对待'],
    '公共冒险': ['公开冒险', '露出体验'],
    '情感依赖': ['情感依赖', '温柔对待'],
    '特殊偏好': ['禁忌突破', '人格切换'],
  };
  return 条件映射[类别] ?? [];
}

// ==================== 辅助函数 ====================

function ensure完整状态(npc: NPC结构): 完整演化状态 {
  if (!npc.完整演化状态) {
    npc.完整演化状态 = {
      演化日志: npc.性癖演化状态?.演化日志 ?? [],
      事件计数器: npc.性癖演化状态?.事件计数器 ?? {},
      最后演化时间: npc.性癖演化状态?.最后演化时间 ?? '',
    };
  }
  return npc.完整演化状态;
}
