/**
 * 获取性癖推荐 — 根据时代和人格标签筛选
 */
export function 获取性癖推荐(params: 性癖查询参数): 性癖条目[] {
  const { eraId, 人格标签, 年龄层, 最大条目数 = 5 } = params;
  let 候选: 性癖条目[] = [...全时代通用性癖];

  // 添加时代专属偏好
  if (eraId && 时代专属性癖[eraId]) {
    候选 = 候选.concat(时代专属性癖[eraId]);
  }

  // 按强度排序，优先推荐强度高的
  候选.sort((a, b) => b.强度 - a.强度);

  // 如果有人格标签，提升匹配标签的偏好权重
  if (人格标签 && 人格标签.length > 0) {
    候选 = 候选.map(f => {
      const 匹配 = 人格标签.some(tag =>
        f.子类型.includes(tag) || f.描述.includes(tag) || f.类别.includes(tag)
      );
      return 匹配 ? { ...f, 强度: Math.min(5, f.强度 + 1) as 性癖条目['强度'] } : f;
    });
    候选.sort((a, b) => b.强度 - a.强度);
  }

  return 候选.slice(0, 最大条目数);
}

/**
 * 生成性癖倾向摘要 — 一句话总结
 */
export function 生成性癖摘要(偏好: 性癖条目[]): string {
  if (偏好.length === 0) return '无明显偏好倾向';
  const 最强 = 偏好.reduce((a, b) => a.强度 > b.强度 ? a : b);
  const 类别计数: Record<string, number> = {};
  偏好.forEach(f => { 类别计数[f.类别] = (类别计数[f.类别] || 0) + 1; });
  const 主要类别 = Object.entries(类别计数).sort((a, b) => b[1] - a[1])[0];
  return `偏好${主要类别[0]}方向，最强烈的倾向是"${最强.子类型}"（强度${最强.强度}/5）`;
}
