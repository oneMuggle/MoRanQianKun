// ==================== 等级映射 ====================

const 等级防线值范围: Record<心理防线等级, { min: number; max: number }> = {
  '保守': { min: 75, max: 100 },
  '传统': { min: 50, max: 74 },
  '开放': { min: 25, max: 49 },
  '放纵': { min: 0, max: 24 },
};

const 等级初始值: Record<心理防线等级, number> = {
  '保守': 85,
  '传统': 60,
  '开放': 35,
  '放纵': 15,
};

export function 防线值到等级(防线值: number): 心理防线等级 {
  if (防线值 >= 75) return '保守';
  if (防线值 >= 50) return '传统';
  if (防线值 >= 25) return '开放';
  return '放纵';
}

export function 等级到防线值范围(等级: 心理防线等级): { min: number; max: number } {
  return 等级防线值范围[等级];
}

// ==================== 初始化 ====================

/**
 * 初始化心理防线状态（在 初始化演化状态 中调用）
 */
export function 初始化心理防线(npc: NPC结构, 基础人格?: 表里人格档案 | null): void {
  const 完整状态 = ensure完整状态(npc);
  if (完整状态.心理防线) return;

  let 初始等级: 心理防线等级 = '传统';
  if (基础人格) {
    const 里描述 = 基础人格.里.性格描述;
    const 表描述 = 基础人格.表.性格描述;
    const 全描述 = 里描述 + 表描述;
    if (全描述.includes('保守') || 全描述.includes('内向') || 全描述.includes('谨慎')) {
      初始等级 = '保守';
    } else if (全描述.includes('开放') || 全描述.includes('大胆') || 全描述.includes('自由')) {
      初始等级 = '开放';
    } else if (全描述.includes('放纵') || 全描述.includes('堕落') || 全描述.includes('欲望')) {
      初始等级 = '放纵';
    }
  }

  const 初始值 = 等级初始值[初始等级];
  完整状态.心理防线 = {
    当前等级: 初始等级,
    防线值: 初始值,
    历史最低值: 初始值,
    恢复速率: 0.5,
    最近变化时间: '',
    变化日志: [],
  };
}

// ==================== 防线更新 ====================

/**
 * 根据NSFW事件更新心理防线
 */
export function 更新心理防线(
  npc: NPC结构,
  事件: 性癖触发事件,
  _游戏时间: string,
  _事件描述: string
): { 有变化: boolean; 日志: 防线变化日志[] } {
  const 完整状态 = ensure完整状态(npc);
  if (!完整状态.心理防线) return { 有变化: false, 日志: [] };

  const 映射 = 获取事件映射(事件);
  if (!映射) return { 有变化: false, 日志: [] };

  const 防线 = 完整状态.心理防线;
  const 旧值 = 防线.防线值;
  const 旧等级 = 防线.当前等级;

  // 强度增量越大，防线扣减越多：强度增量 * 20 = 防线扣减值
  const 扣减值 = Math.round(映射.强度增量 * 20);
  防线.防线值 = Math.max(0, 防线.防线值 - 扣减值);
  防线.历史最低值 = Math.min(防线.历史最低值, 防线.防线值);

  const 新等级 = 防线值到等级(防线.防线值);
  防线.当前等级 = 新等级;

  const 日志: 防线变化日志[] = [];
  if (防线.防线值 !== 旧值) {
    日志.push({
      时间: _游戏时间,
      旧等级,
      新等级,
      旧防线值: 旧值,
      新防线值: 防线.防线值,
      触发原因: `${事件}（强度增量${映射.强度增量}，扣减${扣减值}）`,
    });
  }

  完整状态.心理防线.变化日志.push(...日志);
  if (完整状态.心理防线.变化日志.length > 10) {
    完整状态.心理防线.变化日志 = 完整状态.心理防线.变化日志.slice(-10);
  }

  return { 有变化: 日志.length > 0, 日志 };
}

// ==================== 防线恢复 ====================

/**
 * 回合结束时应用防线自然恢复
 */
export function 应用防线恢复(
  npc: NPC结构,
  当前游戏时间: string
): { 有变化: boolean } {
  const 完整状态 = ensure完整状态(npc);
  if (!完整状态.心理防线) return { 有变化: false };

  const 防线 = 完整状态.心理防线;
  const 旧值 = 防线.防线值;

  // 恢复速率默认0.5，不超过当前等级上限
  const 上限 = 等级到防线值范围(防线.当前等级).max;
  防线.防线值 = Math.min(上限 + 1, Math.round((防线.防线值 + 防线.恢复速率) * 10) / 10);

  // 跨等级检查
  const 新等级 = 防线值到等级(防线.防线值);
  if (新等级 !== 防线.当前等级) {
    防线.当前等级 = 新等级;
    防线.变化日志.push({
      时间: 当前游戏时间,
      旧等级: 防线值到等级(旧值),
      新等级,
      旧防线值: 旧值,
      新防线值: 防线.防线值,
      触发原因: '自然恢复',
    });
  }

  return { 有变化: 防线.防线值 !== 旧值 };
}

