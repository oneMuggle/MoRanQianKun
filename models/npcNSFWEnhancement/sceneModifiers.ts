// ==================== 天气修饰器 ====================

export function 评估天气修饰器(环境: 环境信息结构 | undefined): { 情感依赖修正: number; 修饰摘要: string } {
  if (!环境?.天气) {
    return { 情感依赖修正: 0, 修饰摘要: '' };
  }

  const 天气 = 环境.天气.天气 ?? '';

  if (天气.includes('雨')) {
    return { 情感依赖修正: 雨天情感加成, 修饰摘要: '雨天缠绵，情绪更容易波动' };
  }
  if (天气.includes('雪')) {
    return { 情感依赖修正: 5, 修饰摘要: '雪夜寂静，氛围凝重' };
  }
  if (天气.includes('雷')) {
    return { 情感依赖修正: 5, 修饰摘要: '雷雨交加，紧张刺激' };
  }
  if (天气.includes('晴') || 天气.includes('朗')) {
    return { 情感依赖修正: -5, 修饰摘要: '晴空万里，心情明朗' };
  }
  if (天气.includes('雾')) {
    return { 情感依赖修正: 5, 修饰摘要: '雾气朦胧，视线模糊' };
  }

  return { 情感依赖修正: 0, 修饰摘要: '' };
}

// ==================== 综合计算 ====================

export function 计算场景修饰系数(
  环境: 环境信息结构 | undefined,
  游戏时间: string
): 场景修饰系数 {
  const 时间修饰 = 评估时间修饰器(游戏时间);
  const 地点修饰 = 评估地点修饰器(环境);
  const 天气修饰 = 评估天气修饰器(环境);

  const 暴露倾向修正 = clamp(时间修饰.暴露倾向修正 + Math.round(地点修饰.暴露风险 / 5) - 20, -100, 100);
  const 情感依赖修正 = clamp(时间修饰.情感依赖修正 + 天气修饰.情感依赖修正, -100, 100);
  const 防线突破修正 = clamp(时间修饰.防线突破修正 + 地点修饰.防线突破修正, -100, 100);
  const 刺激感修正 = clamp(Math.round(地点修饰.暴露风险 / 2) + (地点修饰.类型 === '野外' ? 野外刺激加成 : 0), -100, 100);

  const 修饰摘要 = [
    时间修饰.修饰描述,
    地点修饰.修饰描述,
    天气修饰.修饰摘要,
  ].filter(Boolean).join('，');

  return {
    暴露倾向修正,
    情感依赖修正,
    防线突破修正,
    刺激感修正,
    暴露风险: 地点修饰.暴露风险,
    修饰摘要,
  };
}

/**
 * 生成场景修饰叙事注入（用于 AI prompt）
 */
export function 生成场景修饰摘要(修饰: 场景修饰系数): string | null {
  if (!修饰.修饰摘要) return null;
  return `场景氛围：${修饰.修饰摘要}`;
}

// ==================== 辅助函数 ====================

function clamp(value: number, min: number, max: number): number {
  return Math.max(min, Math.min(max, value));
}

function 提取小时(时间串: string): number {
  const parts = 时间串.split(':');
  if (parts.length < 4) return 12;
  const 时 = Number(parts[3]);
  return isNaN(时) ? 12 : 时;
}
