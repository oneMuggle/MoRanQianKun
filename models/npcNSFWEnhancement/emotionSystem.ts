// ==================== 情绪状态 ====================

export type 心情阶段 = '开心' | '平静' | '烦躁' | '低落' | '愤怒';

export type 情绪触发类型 = '场景' | '对话' | '记忆' | '环境' | 'NSFW互动' | '社交事件';

export interface 情绪触发记录 {
  触发类型: 情绪触发类型;
  触发源: string;
  情绪变化: number;
  时间: string;
}

export interface 情绪状态 {
  心情值: number;           // 0-100，影响互动响应
  心情阶段: 心情阶段;
  情绪波动率: number;        // 0-1，人格决定的波动幅度
  最后情绪更新时间: string;
  情绪触发器: 情绪触发记录[];
  基础心情: number;          // 由人格决定的基准心情值
}

// ==================== 心情阶段映射 ====================

const 心情阶段阈值: { 最小值: number; 阶段: 心情阶段 }[] = [
  { 最小值: 80, 阶段: '开心' },
  { 最小值: 60, 阶段: '平静' },
  { 最小值: 40, 阶段: '烦躁' },
  { 最小值: 20, 阶段: '低落' },
  { 最小值: 0, 阶段: '愤怒' },
];

/**
 * 根据心情值计算心情阶段
 */
export function 计算心情阶段(心情值: number): 心情阶段 {
  for (const { 最小值, 阶段 } of 心情阶段阈值) {
    if (心情值 >= 最小值) {
      return 阶段;
    }
  }
  return '愤怒';
}

// ==================== 人格情绪基线 ====================

export interface 人格情绪基线 {
  基础心情: number;           // 人格决定的基准心情
  波动率: number;             // 情绪波动幅度 (0-1)
  恢复速率: number;           // 心情恢复基准的速率 (每回合)
  负面情绪倾向: number;       // 偏向负面情绪的程度 (0-1)
}

/**
 * 根据人格类型获取情绪基线
 * 这里使用简化版本，实际可以根据54个人格档案精细化配置
 */
export function 获取人格情绪基线(人格标签?: string): 人格情绪基线 {
  const 默认基线: 人格情绪基线 = {
    基础心情: 50,
    波动率: 0.3,
    恢复速率: 2,
    负面情绪倾向: 0.3,
  };

  if (!人格标签) return 默认基线;

  // 傲娇类：波动大，恢复慢
  if (人格标签.includes('傲娇') || 人格标签.includes('大小姐')) {
    return { ...默认基线, 波动率: 0.6, 恢复速率: 1 };
  }

  // 温柔类：稳定，恢复快
  if (人格标签.includes('温柔') || 人格标签.includes('贤妻') || 人格标签.includes('邻家')) {
    return { ...默认基线, 波动率: 0.2, 恢复速率: 3, 负面情绪倾向: 0.1 };
  }

  // 高冷类：基准低，波动小
  if (人格标签.includes('高冷') || 人格标签.includes('清冷')) {
    return { ...默认基线, 基础心情: 40, 波动率: 0.15, 恢复速率: 1.5 };
  }

  // 元气类：基准高，波动大
  if (人格标签.includes('元气') || 人格标签.includes('活泼')) {
    return { ...默认基线, 基础心情: 70, 波动率: 0.5, 恢复速率: 4 };
  }

  // 社恐类：基准低，波动大
  if (人格标签.includes('社恐')) {
    return { ...默认基线, 基础心情: 35, 波动率: 0.5, 恢复速率: 1, 负面情绪倾向: 0.5 };
  }

  // 毒舌类：基准中，波动中
  if (人格标签.includes('毒舌') || 人格标签.includes('飒爽')) {
    return { ...默认基线, 波动率: 0.35, 恢复速率: 2.5 };
  }

  return 默认基线;
}

// ==================== 情绪计算 ====================

/**
 * 计算情绪自然衰减/回归基准
 */
export function 计算情绪衰减(
  当前心情: number,
  基础心情: number,
  恢复速率: number
): number {
  const 差值 = 基础心情 - 当前心情;
  const 变化量 = 差值 * (恢复速率 / 100);
  const 新心情 = 当前心情 + 变化量;
  return clamp(新心情, 0, 100);
}

/**
 * 环境因素对情绪的影响
 */
export interface 环境因素 {
  时间段?: '清晨' | '上午' | '下午' | '傍晚' | '夜晚' | '深夜';
  天气?: '晴天' | '阴天' | '雨天' | '雪天';
  地点类型?: '私密' | '半公开' | '公开' | '危险';
  在场人数?: number;
  是否有亲密对象?: boolean;
}

export function 计算环境影响(环境: 环境因素): number {
  let 影响值 = 0;

  switch (环境.时间段) {
    case '清晨': 影响值 += 3; break;
    case '夜晚': 影响值 += 2; break;
    case '深夜': 影响值 -= 2; break;
    default: break;
  }

  switch (环境.天气) {
    case '晴天': 影响值 += 5; break;
    case '雨天': 影响值 -= 3; break;
    case '阴天': 影响值 -= 2; break;
    case '雪天': 影响值 += 2; break;
    default: break;
  }

  switch (环境.地点类型) {
    case '私密': 影响值 += 3; break;
    case '危险': 影响值 -= 5; break;
    default: break;
  }

  if ((环境.在场人数 ?? 0) > 5) {
    影响值 -= 3;
  }

  if (环境.是否有亲密对象) {
    影响值 += 8;
  }

  return clamp(影响值, -15, 15);
}

/**
 * NSFW互动对情绪的影响
 */
export interface NSFW情绪影响 {
  互动类型: '前戏' | '正常' | '粗暴' | '温柔' | '事后';
  亲密度等级: number;
  心理防线: number;
  事后护理质量?: '无视' | '敷衍' | '温柔' | '用心';
}

export function 计算NSFW情绪影响(影响: NSFW情绪影响): number {
  let 变化 = 0;

  switch (影响.互动类型) {
    case '前戏': 变化 += 5; break;
    case '温柔': 变化 += 8; break;
    case '正常': 变化 += 3; break;
    case '粗暴': 变化 -= 5; break;
    case '事后': 变化 += 2; break;
  }

  const 亲密度加成 = 1 + (影响.亲密度等级 * 0.1);
  变化 *= 亲密度加成;

  if (影响.心理防线 < 50) {
    变化 += 3;
  }

  switch (影响.事后护理质量) {
    case '用心': 变化 += 15; break;
    case '温柔': 变化 += 10; break;
    case '敷衍': 变化 -= 5; break;
    case '无视': 变化 -= 15; break;
  }

  return Math.round(变化);
}

// ==================== 记忆触发 ====================

export interface 情绪记忆 {
  记忆内容: string;
  情绪值: number;        // -100 到 +100
  触发关键词: string[];
  衰减系数: number;      // 每次触发后的衰减
  触发次数: number;
  最后触发时间: string;
}

export function 检查记忆触发(
  对话内容: string,
  记忆列表: 情绪记忆[]
): { 触发记忆: 情绪记忆; 情绪变化: number } | null {
  for (const 记忆 of 记忆列表) {
    for (const 关键词 of 记忆.触发关键词) {
      if (对话内容.includes(关键词)) {
        const 实际变化 = 记忆.情绪值 * Math.pow(记忆.衰减系数, 记忆.触发次数);
        return {
          触发记忆: 记忆,
          情绪变化: Math.round(实际变化),
        };
      }
    }
  }
  return null;
}

// ==================== 主计算函数 ====================

export interface 情绪计算输入 {
  当前情绪: 情绪状态;
  环境因素?: 环境因素;
  NSFW影响?: NSFW情绪影响;
  对话内容?: string;
  情绪记忆?: 情绪记忆[];
  人格标签?: string;
}

export function 计算下一回合情绪(输入: 情绪计算输入): 情绪状态 {
  const 基线 = 获取人格情绪基线(输入.人格标签);
  let 新心情 = 输入.当前情绪.心情值;

  // 1. 自然衰减/恢复
  新心情 = 计算情绪衰减(
    新心情,
    输入.当前情绪.基础心情 || 基线.基础心情,
    基线.恢复速率
  );

  // 2. 环境影响
  if (输入.环境因素) {
    新心情 += 计算环境影响(输入.环境因素);
  }

  // 3. NSFW互动影响
  if (输入.NSFW影响) {
    新心情 += 计算NSFW情绪影响(输入.NSFW影响);
  }

  // 4. 记忆触发
  if (输入.对话内容 && 输入.情绪记忆) {
    const 记忆触发 = 检查记忆触发(输入.对话内容, 输入.情绪记忆);
    if (记忆触发) {
      新心情 += 记忆触发.情绪变化;
    }
  }

  // 5. 人格波动
  const 随机波动 = (Math.random() - 0.5) * 2 * 基线.波动率 * 10;
  新心情 += 随机波动;

  // 6. 边界限制
  新心情 = clamp(新心情, 0, 100);

  return {
    ...输入.当前情绪,
    心情值: Math.round(新心情),
    心情阶段: 计算心情阶段(新心情),
    情绪波动率: 基线.波动率,
    基础心情: 基线.基础心情,
    最后情绪更新时间: new Date().toISOString(),
  };
}

// ==================== 工具函数 ====================

function clamp(value: number, min: number, max: number): number {
  return Math.max(min, Math.min(max, value));
}

export function 创建初始情绪(人格标签?: string): 情绪状态 {
  const 基线 = 获取人格情绪基线(人格标签);
  return {
    心情值: 基线.基础心情,
    心情阶段: 计算心情阶段(基线.基础心情),
    情绪波动率: 基线.波动率,
    基础心情: 基线.基础心情,
    最后情绪更新时间: new Date().toISOString(),
    情绪触发器: [],
  };
}
