/**
 * 计算当前关系类型
 * 根据数值阈值计算当前关系类型
 */
export function 计算关系类型(数据: NPC关系数据): 关系类型 {
  const { 好感度, 亲密度, 信任度, 感情值 } = 数据;
  
  // 恋人条件：最高条件
  if (好感度 >= 80 && 亲密度 >= 75 && 信任度 >= 60 && 感情值 >= 50) {
    return '恋人';
  }
  
  // 亲密条件
  if (好感度 >= 60 && 亲密度 >= 50 && 信任度 >= 40 && 感情值 >= 35) {
    return '亲密';
  }
  
  // 好感条件
  if (好感度 >= 40 && 亲密度 >= 25 && 信任度 >= 20 && 感情值 >= 15) {
    return '好感';
  }
  
  // 相识条件
  if (好感度 >= 20 && 亲密度 >= 10 && 信任度 >= 5 && 感情值 >= 5) {
    return '相识';
  }
  
  return '陌生';
}

/**
 * 检查是否满足进阶条件
 */
export function 能否进阶(当前类型: 关系类型, 数据: NPC关系数据): boolean {
  const 阈值 = 关系阈值配置[当前类型];
  
  return (
    数据.好感度 >= 阈值.好感度 &&
    数据.亲密度 >= 阈值.亲密度 &&
    数据.信任度 >= 阈值.信任度 &&
    数据.感情值 >= 阈值.感情值
  );
}

/**
 * 应用互动效果并返回变化值
 */
export function 计算互动效果(
  类型: 互动类型,
  场景?: string
): { 好感度: number; 亲密度: number; 信任度: number; 感情值: number } {
  const config = 互动效果配置[类型];
  const 场景加成 = 场景 ? 场景加成配置[场景] : undefined;
  
  const 应用加成 = (base: number, 加成?: number) => {
    const 整数值 = Math.floor(base * (1 + (加成 || 0)));
    return 整数值;
  };
  
  const 随机范围 = (min: number, max: number) => {
    return Math.floor(Math.random() * (max - min + 1)) + min;
  };
  
  return {
    好感度: 应用加成(随机范围(config.好感度[0], config.好感度[1]), 场景加成?.好感度),
    亲密度: 应用加成(随机范围(config.亲密度[0], config.亲密度[1]), 场景加成?.亲密度),
    信任度: 应用加成(随机范围(config.信任度[0], config.信任度[1]), 场景加成?.信任度),
    感情值: 应用加成(随机范围(config.感情值[0], config.感情值[1]), 场景加成?.感情值),
  };
}

/**
 * 验证关系数据完整性
 */
export function 验证关系数据(数据: NPC关系数据): boolean {
  return (
    typeof 数据.npcId === 'string' &&
    typeof 数据.关系类型 === 'string' &&
    typeof 数据.关系状态 === 'string' &&
    typeof 数据.好感度 === 'number' &&
    typeof 数据.亲密度 === 'number' &&
    typeof 数据.信任度 === 'number' &&
    typeof 数据.感情值 === 'number' &&
    Array.isArray(数据.关键事件) &&
    Array.isArray(数据.解锁场景)
  );
}
