// ==================== 核心函数 ====================

export function 初始化成就进度(): 成就进度 {
  return {
    已解锁: [],
    进行中: {},
  };
}

export function 检查成就解锁(
  进度: 成就进度,
  当前统计: Record<string, number>,
  资源状态: NSFW资源状态
): { 新进度: 成就进度; 新解锁: string[] } {
  const 新进度: 成就进度 = {
    已解锁: [...进度.已解锁],
    进行中: { ...进度.进行中 },
  };
  const 新解锁: string[] = [];

  for (const 成就 of 成就树) {
    if (新进度.已解锁.includes(成就.id)) continue;

    const 前置满足 = 成就.前置成就.every(id =>
      新进度.已解锁.includes(id)
    );
    if (!前置满足) continue;

    const 当前值 = 获取统计值(成就.解锁条件.类型, 当前统计, 资源状态);
    if (当前值 >= 成就.解锁条件.值) {
      新进度.已解锁.push(成就.id);
      新解锁.push(成就.id);
      delete 新进度.进行中[成就.id];
    } else {
      新进度.进行中[成就.id] = Math.min(
        当前值,
        成就.解锁条件.值
      );
    }
  }

  return { 新进度, 新解锁 };
}

function 获取统计值(
  类型: 成就解锁条件['类型'],
  统计: Record<string, number>,
  资源: NSFW资源状态
): number {
  switch (类型) {
    case '性癖解锁数': return 统计['性癖解锁数'] || 0;
    case '敏感点发现数': return 统计['敏感点发现数'] || 0;
    case '亲密次数': return 统计['亲密次数'] || 0;
    case '信任度阈值': return 统计['最高信任度'] || 0;
    case '暴露成功次数': return 统计['暴露成功次数'] || 0;
    case '拍摄完成数': return 统计['拍摄完成数'] || 0;
    case '户外活动数': return 统计['户外活动数'] || 0;
    case '派对参与数': return 统计['派对参与数'] || 0;
    case '声誉值': return 资源.声誉值;
    case '代币数量': return 资源.亲密度代币;
    case '特定成就解锁': return 统计['特定成就解锁'] || 0;
    default: return 0;
  }
}

export function 获取成就分类统计(
  进度: 成就进度
): Record<成就分类, { 总数: number; 已解锁: number }> {
  const 统计: Record<string, { 总数: number; 已解锁: number }> = {};

  for (const 分类 of ['探索', '关系', '风险', '技巧', '多角色', '派对'] as 成就分类[]) {
    统计[分类] = { 总数: 0, 已解锁: 0 };
  }

  for (const 成就 of 成就树) {
    统计[成就.分类].总数++;
    if (进度.已解锁.includes(成就.id)) {
      统计[成就.分类].已解锁++;
    }
  }

  return 统计 as Record<成就分类, { 总数: number; 已解锁: number }>;
}

export function 获取推荐成就(
  进度: 成就进度
): 成就节点[] {
  return 成就树
    .filter(成就 => {
      if (进度.已解锁.includes(成就.id)) return false;
      const 前置满足 = 成就.前置成就.every(id =>
        进度.已解锁.includes(id)
      );
      if (!前置满足) return false;
      return true;
    })
    .sort((a, b) => {
      const a进度 = (进度.进行中[a.id] || 0) / a.解锁条件.值;
      const b进度 = (进度.进行中[b.id] || 0) / b.解锁条件.值;
      return b进度 - a进度;
    })
    .slice(0, 3);
}

export function 计算成就奖励(
  成就Id: string
): 成就奖励 | null {
  const 成就 = 成就树.find(a => a.id === 成就Id);
  return 成就 ? 成就.奖励 : null;
}
