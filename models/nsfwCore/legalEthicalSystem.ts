/**
 * NSFW 核心 — 法律/道德系统
 * 法律风险追踪、道德评判、法律应对和道德修复
 */

import type { NSFW资源状态 } from './resources';
import { 修改声誉 } from './resources';

// ==================== 类型定义 ====================

export type 法律风险等级 = '无' | '关注' | '警告' | '立案' | '诉讼';

export type 道德评判 = '清白' | '争议' | '风评差' | '声名狼藉' | '社会性死亡';

export interface 法律记录 {
  id: string;
  触发时间: string;
  行为描述: string;
  风险等级: 法律风险等级;
  关联NPC?: string;
  持续回合: number;
  是否已解决: boolean;
  解决方式?: string;
}

export interface 道德记录 {
  当前评判: 道德评判;
  历史事件: { 描述: string; 影响值: number; 时间: string }[];
  社区声望: number;
}

export interface 应对法律结果 {
  成功: boolean;
  新资源状态: NSFW资源状态;
  新法律记录?: 法律记录;
  描述: string;
}

export type 法律应对方式 = '聘请律师' | '私下和解' | '公开道歉' | '反击指控' | '沉默应对';

// ==================== 核心函数 ====================

export function 计算法律风险值(记录列表: 法律记录[]): number {
  return 记录列表
    .filter(r => !r.是否已解决)
    .reduce((sum, r) => {
      switch (r.风险等级) {
        case '无': return sum;
        case '关注': return sum + 5;
        case '警告': return sum + 15;
        case '立案': return sum + 30;
        case '诉讼': return sum + 50;
      }
    }, 0);
}

export function 计算道德评判值(记录: 道德记录): number {
  const 基础值 = 记录.社区声望;
  const 历史影响 = 记录.历史事件.reduce((sum, e) => sum + e.影响值, 0);
  return Math.max(0, Math.min(100, 基础值 + 历史影响));
}

export function 获取道德评判(记录: 道德记录): 道德评判 {
  const 值 = 计算道德评判值(记录);
  if (值 >= 80) return '清白';
  if (值 >= 60) return '争议';
  if (值 >= 40) return '风评差';
  if (值 >= 20) return '声名狼藉';
  return '社会性死亡';
}

export function 添加法律风险(
  记录列表: 法律记录[],
  行为: string,
  严重程度: 法律风险等级,
  关联NPC?: string
): { 新记录列表: 法律记录[]; 新记录: 法律记录 } {
  const 新记录: 法律记录 = {
    id: `legal_${Date.now()}`,
    触发时间: new Date().toISOString(),
    行为描述: 行为,
    风险等级: 严重程度,
    关联NPC,
    持续回合: 严重程度 === '诉讼' ? 10 : 严重程度 === '立案' ? 7 : 5,
    是否已解决: false,
  };

  return { 新记录列表: [...记录列表, 新记录], 新记录 };
}

export function 更新道德记录(
  记录: 道德记录,
  事件描述: string,
  影响值: number
): 道德记录 {
  return {
    ...记录,
    历史事件: [...记录.历史事件, { 描述: 事件描述, 影响值, 时间: new Date().toISOString() }],
    社区声望: Math.max(0, Math.min(100, 记录.社区声望 + 影响值)),
  };
}

export function 执行法律应对(
  方式: 法律应对方式,
  法律记录: 法律记录,
  资源状态: NSFW资源状态
): 应对法律结果 {
  let 新资源 = { ...资源状态 };
  const 描述列表: string[] = [`执行「${方式}」`];

  switch (方式) {
    case '聘请律师': {
      const 成本 = 法律记录.风险等级 === '诉讼' ? 60 : 40;
      if (资源状态.亲密度代币 < 成本) {
        return { 成功: false, 新资源状态: 新资源, 描述: '代币不足，无法聘请律师' };
      }
      新资源 = { ...新资源, 亲密度代币: 新资源.亲密度代币 - 成本 };
      描述列表.push(`消费 ${成本} 代币`);

      const 成功率 = 法律记录.风险等级 === '诉讼' ? 0.6 : 0.8;
      if (Math.random() < 成功率) {
        const 已解决: 法律记录 = { ...法律记录, 是否已解决: true, 解决方式: 方式 };
        新资源 = 修改声誉(新资源, 10, '法律问题解决').新状态;
        描述列表.push('法律风险已解除');
        return { 成功: true, 新资源状态: 新资源, 新法律记录: 已解决, 描述: 描述列表.join('，') };
      }
      新资源 = 修改声誉(新资源, -5, '法律应对失败').新状态;
      描述列表.push('应对失败，风险未解除');
      break;
    }
    case '私下和解': {
      const 成本 = 法律记录.风险等级 === '诉讼' ? 45 : 25;
      if (资源状态.亲密度代币 < 成本) {
        return { 成功: false, 新资源状态: 新资源, 描述: '代币不足，无法私下和解' };
      }
      新资源 = { ...新资源, 亲密度代币: 新资源.亲密度代币 - 成本 };

      const 成功率 = 法律记录.风险等级 === '诉讼' ? 0.4 : 0.7;
      if (Math.random() < 成功率) {
        const 已解决: 法律记录 = { ...法律记录, 是否已解决: true, 解决方式: 方式 };
        描述列表.push('和解成功，风险解除');
        return { 成功: true, 新资源状态: 新资源, 新法律记录: 已解决, 描述: 描述列表.join('，') };
      }
      新资源 = 修改声誉(新资源, -10, '和解失败').新状态;
      描述列表.push('和解破裂，声誉下降');
      break;
    }
    case '公开道歉': {
      新资源 = 修改声誉(新资源, 法律记录.风险等级 === '诉讼' ? -5 : 5, '公开道歉').新状态;
      const 已解决: 法律记录 = { ...法律记录, 是否已解决: true, 解决方式: 方式 };
      描述列表.push(法律记录.风险等级 === '诉讼' ? '道歉被无视' : '道歉获得谅解');
      return { 成功: true, 新资源状态: 新资源, 新法律记录: 已解决, 描述: 描述列表.join('，') };
    }
    case '反击指控': {
      const 成功率 = 法律记录.风险等级 === '诉讼' ? 0.3 : 0.5;
      if (Math.random() < 成功率) {
        const 已解决: 法律记录 = { ...法律记录, 是否已解决: true, 解决方式: 方式 };
        新资源 = 修改声誉(新资源, 15, '反击成功').新状态;
        描述列表.push('反击成功，声誉提升');
        return { 成功: true, 新资源状态: 新资源, 新法律记录: 已解决, 描述: 描述列表.join('，') };
      }
      新资源 = 修改声誉(新资源, -20, '反击失败').新状态;
      描述列表.push('反击失败，声誉大幅下降');
      break;
    }
    case '沉默应对': {
      if (Math.random() < 0.4) {
        新资源 = { ...新资源, 亲密度代币: 新资源.亲密度代币 + 5 };
        描述列表.push('沉默使事件自然冷却，少量代币奖励');
        const 已解决: 法律记录 = { ...法律记录, 是否已解决: true, 解决方式: 方式 };
        return { 成功: true, 新资源状态: 新资源, 新法律记录: 已解决, 描述: 描述列表.join('，') };
      }
      新资源 = 修改声誉(新资源, -10, '沉默导致默认').新状态;
      描述列表.push('沉默被解读为默认，声誉下降');
      break;
    }
  }

  return { 成功: false, 新资源状态: 新资源, 描述: 描述列表.join('，') };
}

export function 道德修复(
  资源状态: NSFW资源状态,
  道德记录: 道德记录,
  修复方式: '善行' | '公开' | '补偿' | '时间'
): { 新资源状态: NSFW资源状态; 新道德记录: 道德记录; 描述: string } {
  let 新资源 = { ...资源状态 };
  const 描述列表: string[] = [];

  switch (修复方式) {
    case '善行': {
      const 恢复值 = Math.round(5 + 资源状态.声誉值 * 0.05);
      const 新记录 = 更新道德记录(道德记录, '行善积德', 恢复值);
      新资源 = 修改声誉(新资源, 恢复值, '善行').新状态;
      描述列表.push(`通过善行恢复 ${恢复值} 道德值`);
      return { 新资源状态: 新资源, 新道德记录: 新记录, 描述: 描述列表.join('，') };
    }
    case '公开': {
      if (资源状态.声誉值 >= 50) {
        const 新记录 = 更新道德记录(道德记录, '公开澄清', 10);
        描述列表.push('声誉良好，公开澄清有效');
        return { 新资源状态: 新资源, 新道德记录: 新记录, 描述: 描述列表.join('，') };
      }
      描述列表.push('声誉不足，公开澄清效果有限');
      return { 新资源状态: 新资源, 新道德记录: 道德记录, 描述: 描述列表.join('，') };
    }
    case '补偿': {
      const 成本 = 15;
      if (资源状态.亲密度代币 >= 成本) {
        新资源 = { ...新资源, 亲密度代币: 新资源.亲密度代币 - 成本 };
        const 新记录 = 更新道德记录(道德记录, '补偿受害者', 12);
        描述列表.push('通过补偿修复道德形象');
        return { 新资源状态: 新资源, 新道德记录: 新记录, 描述: 描述列表.join('，') };
      }
      描述列表.push('代币不足，无法补偿');
      return { 新资源状态: 新资源, 新道德记录: 道德记录, 描述: 描述列表.join('，') };
    }
    case '时间': {
      const 自然恢复 = 3;
      const 新记录 = 更新道德记录(道德记录, '时间淡化', 自然恢复);
      描述列表.push(`时间自然恢复 ${自然恢复} 道德值`);
      return { 新资源状态: 新资源, 新道德记录: 新记录, 描述: 描述列表.join('，') };
    }
  }
}

export function 初始化道德记录(初始声望: number = 50): 道德记录 {
  return { 当前评判: '清白', 历史事件: [], 社区声望: 初始声望 };
}
