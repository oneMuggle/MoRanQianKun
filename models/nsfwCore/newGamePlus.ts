// ==================== 核心函数 ====================

export function 创建新周目(
  上周目成就: 成就进度,
  上周目代币: number
): 周目配置 {
  const 周目数 = 计算周目数(上周目成就) + 1;

  return {
    当前周目: 周目数,
    已继承项目: [],
    剩余代币: 上周目代币,
    新周目加成: {
      性癖发现率: 0,
      初始信任度: 0,
      精力上限: 0,
    },
  };
}

function 计算周目数(成就: 成就进度): number {
  const 周目成就 = 成就.已解锁.filter(id => id.startsWith('ngp_'));
  return 周目成就.length;
}

export function 选择继承项目(
  配置: 周目配置,
  项目Id: string
): { 新配置: 周目配置; 成功: boolean; 错误?: string } {
  if (配置.已继承项目.includes(项目Id)) {
    return { 新配置: 配置, 成功: false, 错误: '该项目已继承' };
  }

  const 项目 = 可继承项目.find(p => p.id === 项目Id);
  if (!项目) {
    return { 新配置: 配置, 成功: false, 错误: '项目不存在' };
  }

  if (配置.剩余代币 < 项目.消耗代币) {
    return { 新配置: 配置, 成功: false, 错误: '代币不足' };
  }

  const 新配置: 周目配置 = {
    ...配置,
    已继承项目: [...配置.已继承项目, 项目Id],
    剩余代币: 配置.剩余代币 - 项目.消耗代币,
    新周目加成: { ...配置.新周目加成 },
  };

  switch (项目.效果.类型) {
    case '性癖发现率提升':
      新配置.新周目加成.性癖发现率 += 项目.效果.值;
      break;
    case '初始属性加成':
      if (项目.id === 'inherit_energy') {
        新配置.新周目加成.精力上限 += 项目.效果.值;
      } else if (项目.id === 'inherit_reputation') {
        新配置.新周目加成.初始信任度 += 项目.效果.值;
      }
      break;
  }

  return { 新配置, 成功: true };
}

export function 获取可继承列表(
  配置: 周目配置
): { 项目: 继承项目; 已选择: boolean; 可购买: boolean }[] {
  return 可继承项目.map(项目 => ({
    项目,
    已选择: 配置.已继承项目.includes(项目.id),
    可购买:
      !配置.已继承项目.includes(项目.id) &&
      配置.剩余代币 >= 项目.消耗代币,
  }));
}

export function 计算新周目初始值(
  配置: 周目配置,
  基础值: { 精力: number; 声誉: number; 信任度: number }
): { 精力: number; 声誉: number; 信任度: number } {
  return {
    精力: 基础值.精力 + 配置.新周目加成.精力上限,
    声誉: 基础值.声誉,
    信任度: 基础值.信任度 + 配置.新周目加成.初始信任度,
  };
}
