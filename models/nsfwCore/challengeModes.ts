// ==================== 类型定义 ====================

export type 挑战类型 = '纯爱路线' | '极限挑战' | '速通';

export interface 挑战规则 {
  类型: 挑战类型;
  名称: string;
  描述: string;
  限制: string[];
  目标: string;
  奖励倍率: number;
}

export interface 挑战进度 {
  当前挑战: 挑战类型 | null;
  开始时间: number;
  里程碑: Record<string, boolean>;
  违规次数: number;
  已完成: boolean;
}

export interface 挑战记录 {
  挑战类型: 挑战类型;
  完成时间: number;
  违规次数: number;
  获得奖励: number;
}

