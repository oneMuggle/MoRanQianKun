// ==================== 核心函数 ====================

export function 创建挑战(
  类型: 挑战类型
): 挑战进度 {
  return {
    当前挑战: 类型,
    开始时间: Date.now(),
    里程碑: {},
    违规次数: 0,
    已完成: false,
  };
}

export function 检查挑战违规(
  进度: 挑战进度,
  事件: { 类型: string; 严重程度: number }
): 挑战进度 {
  if (!进度.当前挑战) return 进度;

  const 新进度 = { ...进度 };

  switch (进度.当前挑战) {
    case '纯爱路线':
      if (事件.类型 === '暴露风险' || 事件.类型 === '越界行为') {
        新进度.违规次数++;
      }
      break;
    case '极限挑战':
      break;
    case '速通':
      break;
  }

  return 新进度;
}

export function 检查挑战完成(
  进度: 挑战进度,
  资源状态: NSFW资源状态,
  成就已解锁数: number,
  信任度: number,
  当前回合?: number
): { 已完成: boolean; 描述: string } {
  if (!进度.当前挑战) return { 已完成: false, 描述: '未开始挑战' };

  switch (进度.当前挑战) {
    case '纯爱路线':
      if (进度.违规次数 > 0) {
        return { 已完成: false, 描述: '违反纯爱规则' };
      }
      if (信任度 >= 100) {
        return { 已完成: true, 描述: '达成灵魂伴侣' };
      }
      break;
    case '极限挑战':
      if (资源状态.声誉值 >= 30 && 成就已解锁数 >= 5) {
        return { 已完成: true, 描述: '完成极限挑战' };
      }
      if (资源状态.声誉值 < 0) {
        return { 已完成: false, 描述: '声誉过低' };
      }
      break;
    case '速通':
      if (成就已解锁数 >= 3) {
        return { 已完成: true, 描述: '速通成功' };
      }
      if (当前回合 && 当前回合 > 20) {
        return { 已完成: false, 描述: '超过回合限制' };
      }
      break;
  }

  return { 已完成: false, 描述: '进行中' };
}

export function 计算挑战奖励(
  进度: 挑战进度,
  基础代币: number
): number {
  if (!进度.当前挑战 || !进度.已完成) return 0;
  const 规则 = 挑战规则库[进度.当前挑战];
  return Math.round(基础代币 * 规则.奖励倍率);
}

export function 获取挑战排行榜(
  记录列表: 挑战记录[]
): 挑战记录[] {
  return [...记录列表].sort((a, b) => {
    const 倍率A = 挑战规则库[a.挑战类型].奖励倍率;
    const 倍率B = 挑战规则库[b.挑战类型].奖励倍率;
    return (b.获得奖励 * 倍率B) - (a.获得奖励 * 倍率A);
  });
}
