/**
 * 计算后宫等级
 */
export function 计算后宫等级(成员数: number, 平均亲密度: number, 和谐度: number): 后宫等级类型 {
  if (成员数 >= 5 && 平均亲密度 >= 80 && 和谐度 >= 85) return '鼎盛';
  if (成员数 >= 4 && 平均亲密度 >= 70 && 和谐度 >= 70) return '繁荣';
  if (成员数 >= 3 && 平均亲密度 >= 60 && 和谐度 >= 55) return '稳定';
  if (成员数 >= 2 && 平均亲密度 >= 40) return '发展中';
  return '新建立';
}

/**
 * 计算后宫称号
 */
export function 计算后宫称号(成员数: number, 和谐度: number, 低嫉妒成员数: number): string {
  if (成员数 >= 5 && 低嫉妒成员数 >= 5) return '后宫之主';
  if (成员数 >= 3 && 和谐度 >= 70) return '平衡大师';
  if (成员数 >= 3) return '三人成众';
  if (成员数 >= 1) return '初建茅庐';
  return '';
}
