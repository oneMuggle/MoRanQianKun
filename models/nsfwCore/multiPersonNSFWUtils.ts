/**
 * 计算嫉妒之夜安抚成功率
 */
export function 计算安抚成功率(成员: 嫉妒之夜参数['成员']): number {
  let 基础率 = 80;
  基础率 -= (成员.嫉妒值 - 70) * 1;  // 嫉妒越高越难
  基础率 += 成员.信任度 * 0.3;        // 信任度加成
  基础率 += 成员.亲密度等级 * 3;      // 亲密度加成

  if (成员.欲望阶段 === '沉沦' || 成员.欲望阶段 === '支配') {
    基础率 += 10;
  }

  return Math.max(30, Math.min(95, Math.round(基础率)));
}

