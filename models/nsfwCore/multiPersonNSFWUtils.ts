/**
 * 生成随机属性奖励
 */
function 生成随机属性奖励(数量: number): { 属性类型: string; 数值: number }[] {
  const 属性列表 = ['力量', '敏捷', '体质', '根骨', '悟性', '福源'];
  const 奖励: { 属性类型: string; 数值: number }[] = [];
  for (let i = 0; i < 数量; i++) {
    奖励.push({
      属性类型: 属性列表[Math.floor(Math.random() * 属性列表.length)],
      数值: Math.floor(Math.random() * 3) + 1,  // 1-3
    });
  }
  return 奖励;
}

/**
 * 计算多人双修收益
 */
export function 计算多人双修收益(
  参数: 双修大会参数
): { 基础属性: { 属性类型: string; 数值: number }[]; 专属组合: string | null; 和谐度修正: number } {
  const 人数 = 参数.参与人数;
  const 平均和谐度 = 参数.平均和谐度;

  // 基础收益: 人数 × (1~3)
  const 基础属性数量 = 人数 * (Math.floor(Math.random() * 3) + 1);
  const 基础属性 = 生成随机属性奖励(基础属性数量);

  // 和谐度加成
  let 额外属性数量 = 0;
  if (平均和谐度 >= 80) {
    额外属性数量 = 2;
  } else if (平均和谐度 >= 60) {
    额外属性数量 = 1;
  } else if (平均和谐度 < 40) {
    额外属性数量 = -1;
  }

  // 位分加成: 正室参与 +1
  const 有正室 = 参数.参与成员.some(m => m.位分 === '正室');
  if (有正室) {
    额外属性数量 += 1;
  }

  // 侧室以上 >= 2: "同心"效果
  const 侧室以上 = 参数.参与成员.filter(m => m.位分等级 >= 4).length;
  if (侧室以上 >= 2) {
    额外属性数量 += 1;
  }

  if (额外属性数量 > 0) {
    基础属性.push(...生成随机属性奖励(额外属性数量));
  } else if (额外属性数量 < 0) {
    // 和谐度低时减少属性
    for (let i = 0; i < Math.abs(额外属性数量); i++) {
      if (基础属性.length > 0) {
        基础属性.pop();
      }
    }
  }

  // 和谐度修正（影响后宫全局和谐度）
  let 和谐度修正 = 0;
  if (平均和谐度 >= 70) {
    和谐度修正 = 5;
  } else if (平均和谐度 >= 50) {
    和谐度修正 = 0;
  } else {
    和谐度修正 = -10;
  }

  return { 基础属性, 专属组合: null, 和谐度修正 };
}

/**
 * 计算多人双修风险
 */
export function 计算多人双修风险(参与人数: number, 平均和谐度: number): { 风险值: number; 风险等级: '低' | '中' | '高' } {
  const 基础风险 = 参与人数 * 10;
  const 和谐度修正 = (100 - 平均和谐度) / 10;
  const 风险值 = Math.round(基础风险 + 和谐度修正);
  const 风险等级 = 风险值 >= 60 ? '高' : 风险值 >= 35 ? '中' : '低';
  return { 风险值, 风险等级 };
}

