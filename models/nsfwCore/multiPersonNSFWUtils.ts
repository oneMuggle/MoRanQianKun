/**
 * 验证双修大会参与条件
 */
export function 验证双修大会条件(
  成员: 双修大会成员摘要[],
  后宫等级: 后宫等级类型
): { 通过: boolean; 失败原因: string | null } {
  const 人数 = 成员.length as 双修参与人数;
  if (人数 < 2 || 人数 > 5 || ![2, 3, 4, 5].includes(人数)) {
    return { 通过: false, 失败原因: `参与人数必须为 2-5 人，当前 ${人数} 人` };
  }

  const 门槛 = 双修大会门槛[人数];
  if (!门槛) {
    return { 通过: false, 失败原因: '无效的参与人数' };
  }

  // 检查后宫等级
  const 等级顺序: 后宫等级类型[] = ['新建立', '发展中', '稳定', '繁荣', '鼎盛'];
  const 当前等级Index = 等级顺序.indexOf(后宫等级);
  const 需求等级Index = 等级顺序.indexOf(门槛.最低后宫等级);
  if (当前等级Index < 需求等级Index) {
    return { 通过: false, 失败原因: `后宫等级不足，需要 "${门槛.最低后宫等级}"，当前 "${后宫等级}"` };
  }

  // 检查所有成员亲密度等级 >= 5
  for (const m of 成员) {
    if (m.亲密度等级 < 5) {
      return { 通过: false, 失败原因: `${m.姓名} 亲密度等级不足（需要 Lv5，当前 Lv${m.亲密度等级}）` };
    }
  }

  // 检查平均和谐度
  const 平均和谐度 = 成员.reduce((sum, m) => sum + m.对其他人和谐度, 0) / 成员.length;
  if (平均和谐度 < 门槛.最低和谐度) {
    return { 通过: false, 失败原因: `平均和谐度不足（需要 ${门槛.最低和谐度}，当前 ${Math.round(平均和谐度)}）` };
  }

  return { 通过: true, 失败原因: null };
}

