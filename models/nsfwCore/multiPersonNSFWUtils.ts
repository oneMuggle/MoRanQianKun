/**
 * 计算争宠事件结果
 */
export function 计算争宠事件结果(
  参数: 争宠事件参数,
  选择: 争宠事件选择,
  后宫和谐度: number
): 争宠事件结果 {
  const 亲密度变化: Record<string, number> = {};
  const 嫉妒变化: Record<string, number> = {};
  let 和谐度变化 = 0;
  let 是否成功 = true;
  let 描述 = '';

  if (选择.类型 === '选择成员') {
    // 选中的成员获得亲密，其他人嫉妒
    for (const 成员 of 参数.邀约成员) {
      if (成员.npcId === 选择.目标NpcId) {
        亲密度变化[成员.npcId] = 5;
        描述 += `${成员.姓名} 亲密度 +5，`;
      } else {
        嫉妒变化[成员.npcId] = 20;
        描述 += `${成员.姓名} 嫉妒 +20，`;
      }
    }
  } else if (选择.类型 === '全部拒绝') {
    // 全员嫉妒 +10，和谐度 +5
    for (const 成员 of 参数.邀约成员) {
      嫉妒变化[成员.npcId] = 10;
    }
    和谐度变化 = 5;
    描述 = '你选择了公平对待所有人，虽然略有不满，但和谐度提升了';
  } else if (选择.类型 === '说服一起') {
    const 成功率 = 计算说服一起成功率({ 和谐度: 后宫和谐度, 成员列表: [], 后宫等级: '发展中' });
    是否成功 = Math.random() * 100 < 成功率;

    if (是否成功) {
      和谐度变化 = 15;
      for (const 成员 of 参数.邀约成员) {
        亲密度变化[成员.npcId] = 2;
      }
      描述 = `成功说服所有人一起活动！和谐度 +15，每人亲密度 +2`;
    } else {
      for (const 成员 of 参数.邀约成员) {
        嫉妒变化[成员.npcId] = 15;
      }
      描述 = '说服失败，所有人都不满意，嫉妒值上升';
    }
  }

  return { 选择, 是否成功, 亲密度变化, 嫉妒变化, 和谐度变化, 描述 };
}

