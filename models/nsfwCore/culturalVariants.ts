// ==================== 核心函数 ====================

export function 初始化文化配置(
  起始文化: 文化背景 = '现代都市'
): 文化配置 {
  return {
    当前文化: 起始文化,
    已解锁文化: [起始文化],
    文化适应度: {
      '中国古风': 0,
      '日本和风': 0,
      '现代都市': 50,
      '西方中世纪': 0,
    },
  };
}

export function 计算文化适应(
  配置: 文化配置
): 文化适应结果 {
  const 规则 = 文化规则库[配置.当前文化];
  const 适应度 = 配置.文化适应度[配置.当前文化] || 0;
  const 适应修正 = 适应度 / 100;

  return {
    风险修正: 1 - (适应修正 * 0.3),
    道德修正: 适应修正 * 10,
    场景过滤: 规则.可用场景,
    性癖发现率修正: 1 + (适应修正 * 0.2),
  };
}

export function 切换文化(
  配置: 文化配置,
  目标文化: 文化背景
): { 新配置: 文化配置; 成功: boolean; 错误?: string } {
  if (!配置.已解锁文化.includes(目标文化)) {
    return { 新配置: 配置, 成功: false, 错误: '该文化未解锁' };
  }

  if (目标文化 === 配置.当前文化) {
    return { 新配置: 配置, 成功: false, 错误: '已在该文化中' };
  }

  return {
    新配置: {
      ...配置,
      当前文化: 目标文化,
    },
    成功: true,
  };
}

export function 解锁文化(
  配置: 文化配置,
  新文化: 文化背景
): 文化配置 {
  if (配置.已解锁文化.includes(新文化)) return 配置;

  return {
    ...配置,
    已解锁文化: [...配置.已解锁文化, 新文化],
  };
}

export function 积累文化适应度(
  配置: 文化配置,
  增量: number
): 文化配置 {
  const 新适应度 = { ...配置.文化适应度 };
  新适应度[配置.当前文化] = Math.min(
    100,
    (新适应度[配置.当前文化] || 0) + 增量
  );

  return {
    ...配置,
    文化适应度: 新适应度,
  };
}

export function 获取可用场景(
  配置: 文化配置,
  所有场景: { id: string; 名称: string; 文化?: 文化背景 }[]
): { id: string; 名称: string }[] {
  const 规则 = 文化规则库[配置.当前文化];
  return 所有场景.filter(场景 => {
    if (!场景.文化) return true;
    return 场景.文化 === 配置.当前文化 || 规则.可用场景.includes(场景.名称);
  });
}

export function 应用文化风险修正(
  配置: 文化配置,
  基础风险: number
): number {
  const 规则 = 文化规则库[配置.当前文化];
  const 适应 = 计算文化适应(配置);
  return Math.round(基础风险 * 规则.风险计算倍率 * 适应.风险修正);
}

export function 应用文化性癖调整(
  配置: 文化配置,
  基础发现率: number,
  性癖标签: string
): number {
  const 规则 = 文化规则库[配置.当前文化];
  const 适应 = 计算文化适应(配置);
  const 标签调整 = 规则.性癖偏好调整[性癖标签] || 1.0;
  return Math.round(基础发现率 * 标签调整 * 适应.性癖发现率修正);
}
