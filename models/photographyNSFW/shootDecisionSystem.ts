/**
 * 拍摄回合决策
 */
export function 拍摄回合决策(
  决策: 回合决策,
  项目: 拍摄项目状态,
  模特: 模特核心状态,
  摄影师: 摄影师核心状态
): 回合结果 {
  const 系数 = 决策系数[决策];
  const 当前尺度Idx = 尺度索引(项目.实际尺度);
  const 最大尺度Idx = 尺度序列.length - 1;

  // 模特同意概率计算
  const 同意概率 = 计算同意概率(决策, 模特, 项目);

  // 越界判定
  const 越界 = 决策 === '推进尺度' && Math.random() < 系数.越界风险 * (1 - 模特.安全感 / 100);

  let 新尺度 = 项目.实际尺度;
  if (决策 === '推进尺度') {
    if (Math.random() <= 同意概率 && 当前尺度Idx < 最大尺度Idx) {
      新尺度 = 尺度序列[当前尺度Idx + 1];
      项目.实际尺度 = 新尺度;
      项目.尺度变更历史.push({
        旧尺度: 尺度序列[当前尺度Idx],
        新尺度,
        回合: 项目.当前回合,
        模特是否同意: true,
      });
    }
  }

  const 情绪变化 = Math.round(系数.情绪影响 * (1 + (模特.信任度 - 50) / 100));
  const 信任变化 = Math.round(系数.信任影响 * (1 + (模特.安全感 - 50) / 100));
  const 亲密度变化 = 决策 === '推进尺度' ? 3 : 决策 === '保持当前' ? 1 : 决策 === '放缓节奏' ? 2 : 0;
  const 收益变化 = Math.round(50 * 系数.收益倍率 * (摄影师.技术水平 / 100));

  return {
    成功: true,
    模特情绪变化: 情绪变化,
    信任度变化: 信任变化,
    亲密度变化,
    收益变化,
    描述: get决策描述(决策, 模特, 新尺度),
    是否越界: 越界,
  };
}

/**
 * 模特情绪管理
 */
export function 模特情绪管理(
  方式: 情绪调节方式,
  _模特: 模特核心状态,
  _项目: 拍摄项目状态
): { 情绪变化: number; 信任变化: number; 成本: number; 描述: string } {
  const 系数 = 情绪调节系数[方式];

  const 随机因子 = 0.8 + Math.random() * 0.4;
  const 情绪变化 = Math.round(系数.情绪恢复 * 随机因子);
  const 信任变化 = Math.round(系数.信任恢复 * 随机因子);

  return {
    情绪变化,
    信任变化,
    成本: 系数.成本,
    描述: get情绪调节描述(方式),
  };
}

/**
 * 信任度博弈 — 评估当前信任状态下的最优策略
 */
export function 信任度博弈评估(
  模特: 模特核心状态,
  项目: 拍摄项目状态
): { 推荐策略: 回合决策; 风险评估: '低' | '中' | '高'; 描述: string } {
  const 信任度 = 模特.信任度;
  const 安全感 = 模特.安全感;
  const 当前尺度Idx = 尺度索引(项目.实际尺度);
  const 最大尺度Idx = 尺度序列.length - 1;

  if (信任度 < 30 || 安全感 < 20) {
    return { 推荐策略: '停止拍摄', 风险评估: '高', 描述: '信任度极低，继续拍摄可能导致关系破裂' };
  }

  if (信任度 < 50) {
    return { 推荐策略: '放缓节奏', 风险评估: '中', 描述: '信任度偏低，建议先建立信任再推进' };
  }

  if (当前尺度Idx >= 最大尺度Idx - 1) {
    return { 推荐策略: '保持当前', 风险评估: '高', 描述: '已接近最大尺度，推进风险极高' };
  }

  if (信任度 > 70 && 安全感 > 60) {
    return { 推荐策略: '推进尺度', 风险评估: '低', 描述: '信任度和安全感都较高，可以适度推进' };
  }

  return { 推荐策略: '保持当前', 风险评估: '中', 描述: '当前状态平稳，保持节奏是最佳选择' };
}

/**
 * 拍摄技巧解锁判定
 */
export function 拍摄技巧解锁(
  当前状态: 技巧解锁状态,
  _项目: 拍摄项目状态
): { 新解锁技巧: 拍摄技巧[]; 描述: string } {
  const 新经验值 = 当前状态.经验值 + 10;
  const 新等级 = Math.floor(新经验值 / 50) + 1;

  if (新等级 <= 当前状态.等级) {
    return { 新解锁技巧: [], 描述: '经验不足，未解锁新技巧' };
  }

  const 全部技巧: 拍摄技巧[] = ['引导技巧', '氛围营造', '光线掌控', '情感共鸣', '后期处理'];
  const 新解锁: 拍摄技巧[] = [];

  for (const 技巧 of 全部技巧) {
    if (!当前状态.已解锁技巧.includes(技巧)) {
      const 解锁阈值 = get技巧解锁阈值(技巧);
      if (新等级 >= 解锁阈值) {
        新解锁.push(技巧);
      }
    }
  }

  return {
    新解锁技巧: 新解锁,
    描述: 新解锁.length > 0
      ? `升级至等级 ${新等级}，解锁技巧：${新解锁.join('、')}`
      : `升级至等级 ${新等级}，暂无新技巧解锁`,
  };
}

/**
 * 计算回合收益
 */
export function 计算回合收益(
  决策: 回合决策,
  摄影师: 摄影师核心状态,
  模特: 模特核心状态
): number {
  const 基础收益 = 100;
  const 技术水平系数 = 摄影师.技术水平 / 100;
  const 信任度系数 = 模特.信任度 / 100;
  const 尺度加成 = 尺度索引(模特.当前底线) * 0.2;
  const 决策倍率 = 决策系数[决策].收益倍率;

  return Math.round(基础收益 * 技术水平系数 * (0.5 + 信任度系数 * 0.5 + 尺度加成) * 决策倍率);
}

// ==================== 内部辅助函数 ====================

function 计算同意概率(决策: 回合决策, 模特: 模特核心状态, 项目: 拍摄项目状态): number {
  if (决策 !== '推进尺度') return 1;

  const 基础概率 = 0.6;
  const 信任度加成 = (模特.信任度 / 100) * 0.3;
  const 安全感加成 = (模特.安全感 / 100) * 0.1;
  const 尺度惩罚 = 尺度索引(项目.实际尺度) * 0.1;
  const 保护意识惩罚 = (() => {
    switch (模特.保护意识) {
      case '极度保护': return 0.2;
      case '适度保护': return 0.1;
      case '开放型': return 0;
      case '商业导向': return -0.05;
    }
  })();

  return Math.max(0.1, Math.min(0.9,
    基础概率 + 信任度加成 + 安全感加成 - 尺度惩罚 - 保护意识惩罚
  ));
}

