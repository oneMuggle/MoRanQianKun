export interface 战斗敌方信息 {
    名字: string;
    境界: string;
    简介: string;
    技能: string[];
    战斗力: number;
    防御力: number;
    当前血量: number;
    最大血量: number;
    当前精力: number;
    最大精力: number;
    当前内力: number;
    最大内力: number;
    // ── 2026-06-15 U4 战斗模型扩展（借鉴 yishijie）──
    // 全部 optional + default 0，向后兼容旧存档
    /** 暴击率 0-100，命中时附加伤害的判定概率 */
    暴击率?: number;
    /** 闪避率 0-100，受击时完全规避的概率 */
    闪避率?: number;
    /** 最大连击次数，每次普通攻击可触发的连击上限 */
    最大连击?: number;
    /** 物理抗性 0-100，减免物理伤害的百分比 */
    物理抗性?: number;
    /** 内力抗性 0-100，减免内力/法术伤害的百分比 */
    内力抗性?: number;
}

export interface 战斗状态结构 {
    是否战斗中: boolean;
    敌方: 战斗敌方信息[];
}

/**
 * 规范化战斗敌方信息：补齐所有 U4 扩展字段的默认值 0
 *
 * 设计目标：旧存档（无新字段）加载时不报错，所有读取方都能拿到完整结构
 * 不可变：返回新对象，不修改入参
 */
export function 规范化战斗敌方信息(raw: 战斗敌方信息): 战斗敌方信息 {
    return {
        ...raw,
        技能: [...raw.技能],
        暴击率: raw.暴击率 ?? 0,
        闪避率: raw.闪避率 ?? 0,
        最大连击: raw.最大连击 ?? 0,
        物理抗性: raw.物理抗性 ?? 0,
        内力抗性: raw.内力抗性 ?? 0,
    };
}
