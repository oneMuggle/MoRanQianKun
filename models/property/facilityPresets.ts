// ─── 按时代获取设施预设 ───

export const 按时代获取设施预设 = (时代: string): 设施预设结构[] => {
    switch (时代) {
        case '古代':
        case '近代':
            return 古代设施预设;
        case '现代':
            return 现代设施预设;
        case '近未来':
        case '未来':
            return 未来设施预设;
        default:
            return 古代设施预设;
    }
};

// ─── 按设施ID查找预设 ───

export const 按ID查找设施预设 = (设施ID: string, 时代: string = '古代'): 设施预设结构 | undefined => {
    const presets = 按时代获取设施预设(时代);
    return presets.find(p => p.设施ID === 设施ID);
};

