/**
 * 运动伤害联动 — NSFW行为导致注意力分散，增加运动伤害风险
 */
export function 计算运动伤害(
  运动类型: string,
  当前体力: number,
  NSFW注意力分散: boolean,
  天气: 天气状况
): 伤害联动结果 {
  const 基础概率 = 运动伤害概率[运动类型] ?? 0.03;
  const 体力修正 = 当前体力 < 30 ? 2.0 : 当前体力 < 50 ? 1.5 : 当前体力 < 70 ? 1.2 : 1.0;
  const NSFW修正 = NSFW注意力分散 ? 1.8 : 1.0;
  const 天气修正 = 天气 === '极端' ? 2.0 : 天气 === '雨' || 天气 === '雪' ? 1.5 : 1.0;

  const 伤害概率 = 基础概率 * 体力修正 * NSFW修正 * 天气修正;

  if (Math.random() > 伤害概率) {
    return {
      伤害类型: '无',
      严重程度: '轻微',
      亲密度变化: 0,
      信任变化: 0,
      描述: '一切安全，没有受伤',
    };
  }

  // 确定伤害类型和严重程度
  const 伤害列表: 运动伤害类型[] = ['擦伤', '扭伤', '骨折', '坠落'];
  const 严重程度 = 伤害概率 > 0.15 ? '严重' : 伤害概率 > 0.08 ? '中等' : '轻微';
  const 伤害类型 = 伤害列表[Math.floor(Math.random() * 伤害列表.length)];
  const 影响 = 伤害影响[严重程度];

  return {
    伤害类型,
    严重程度,
    亲密度变化: 影响.亲密度变化,
    信任变化: 影响.信任变化,
    描述: get伤害描述(伤害类型, 严重程度),
  };
}

/**
 * 户外泄露 — 户外运动更容易被路人看到/拍照
 */
export function 户外泄露判定(
  活动: 活动项目状态,
  参与者: 参与者核心状态,
  _当前尺度: string,
  NSFW行为发生: boolean
): 户外泄露结果 {
  if (!NSFW行为发生) {
    return { 泄露类型: '意外泄露', 传播范围: '未传播', 严重等级: 0, 描述: '没有发生泄露' };
  }

  // 计算泄露概率
  const 场所风险 = (() => {
    switch (活动.活动场所) {
      case '国家公园': return 0.3;
      case '野山野水': return 0.15;
      case '无人区': return 0.05;
      case '专业场地': return 0.4;
      default: return 0.2;
    }
  })();

  // 参与者安全意识影响
  const 安全意识修正 = (100 - 参与者.安全意识) / 100;
  const 泄露概率 = 场所风险 * 安全意识修正;

  if (Math.random() > 泄露概率) {
    return { 泄露类型: '意外泄露', 传播范围: '未传播', 严重等级: 0, 描述: '虽然有风险行为，但暂时没有被发现' };
  }

  // 判定泄露类型
  const 泄露类型: 泄露类型 = 活动.记录设备.length > 0 ? '内部人员泄露' : '意外泄露';

  // 判定传播范围
  const 传播范围: 传播范围 = (() => {
    if (活动.信号状况 === '满格') return '社交媒体';
    if (活动.信号状况 === '较差') return '小范围';
    return '户外论坛';
  })();

  const 严重等级 = 活动.环境危险度;

  return {
    泄露类型,
    传播范围,
    严重等级,
    描述: get泄露描述(泄露类型, 传播范围),
  };
}

/**
 * 社交媒体传播 — 户外运动照片更容易在社交媒体传播
 */
export function 社交媒体传播模拟(
  初始泄露: 泄露事件状态,
  _活动: 活动项目状态,
  回合数: number
): 社交媒体传播结果[] {
  const 结果: 社交媒体传播结果[] = [];

  for (let i = 0; i < Math.min(回合数, 5); i++) {
    // 随机选择传播平台
    const 平台列表 = Object.entries(社交媒体平台);
    const [平台, 配置] = 平台列表[Math.floor(Math.random() * 平台列表.length)];

    const 基础传播 = 初始泄露.泄露照片数 * 10;
    const 传播速度 = Math.round(基础传播 * 配置.传播倍率 * (1 + i * 0.5));

    结果.push({
      平台,
      传播速度,
      影响范围: 配置.影响范围,
      描述: `第${i + 1}回合传播至${平台}，新增观看${传播速度}次`,
    });
  }

  return 结果;
}

