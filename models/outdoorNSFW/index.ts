// ==================== 叙事尺度指数导出 ====================
export function 获取尺度指数(尺度: 极限叙事尺度): number {
  const 定义 = 尺度分级定义[尺度];
  return 定义?.尺度指数 ?? 10;
}

export function 获取下一尺度(当前尺度: 极限叙事尺度): 极限叙事尺度 | null {
  const 顺序: 极限叙事尺度[] = ['正常', 'PG-13', 'R级', 'NC-17', 'XXX'];
  const 当前索引 = 顺序.indexOf(当前尺度);
  if (当前索引 === -1 || 当前索引 >= 顺序.length - 1) return null;
  return 顺序[当前索引 + 1];
}

// ==================== 环境风险计算 ====================
export function 计算环境风险(
  场所: keyof typeof 环境风险因素.场所风险,
  私密程度: keyof typeof 环境风险因素.私密程度风险,
  信号状况: '满格' | '较差' | '无信号',
  住宿类型: keyof typeof 环境风险因素.住宿类型风险
): number {
  let 总风险 = 0;

  总风险 += 环境风险因素.场所风险[场所] || 0;
  总风险 += 环境风险因素.私密程度风险[私密程度] || 0;
  总风险 += 环境风险因素.信号状况风险[信号状况] || 0;
  总风险 += 环境风险因素.住宿类型风险[住宿类型] || 0;

  return Math.min(100, 总风险 / 4); // 返回平均值，最高100
}
