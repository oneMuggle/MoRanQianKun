// ==================== 设备风险评估 ====================

export function 评估设备风险(
  设备类型: 记录设备类型,
  归属: 设备归属,
  授权: 设备授权,
  拍摄方式参数: 拍摄方式
): number {
  let 风险值 = 0;

  // 设备类型风险
  const 设备风险: Record<记录设备类型, number> = {
    '运动相机': 10,
    '无人机': 30,
    '手机': 15,
    '专业摄像机': 25,
    '手表相机': 50,
    '隐蔽摄像头': 90,
  };
  风险值 += 设备风险[设备类型] * 0.3;

  // 归属风险
  const 归属风险: Record<设备归属, number> = {
    '官方设备': 5,
    '教练领队设备': 40,
    '参与者设备': 20,
    '第三方设备': 60,
    '无名设备': 80,
  };
  风险值 += 归属风险[归属] * 0.3;

  // 授权状态风险
  const 授权风险: Record<设备授权['授权状态'], number> = {
    '已确认': 5,
    '部分授权': 30,
    '未确认': 50,
    '拒绝拍摄': 0,
  };
  风险值 += 授权风险[授权.授权状态] * 0.2;

  // 拍摄方式风险
  const 拍摄风险: Record<拍摄方式, number> = {
    '公开记录': 5,
    '固定机位': 15,
    '跟拍': 20,
    '航拍': 25,
    '水下拍摄': 35,
    '隐蔽拍摄': 90,
  };
  风险值 += 拍摄风险[拍摄方式参数] * 0.2;

  return Math.min(100, Math.round(风险值));
}

// ==================== 检测可疑设备 ====================

export function 检测可疑设备(
  设备列表: 装备记录[],
  场景描述?: string
): 装备记录[] {
  const 可疑设备: 装备记录[] = [];

  for (const 设备 of 设备列表) {
    // 隐蔽摄像头必定可疑
    if (设备.类型 === '隐蔽摄像头') {
      可疑设备.push(设备);
      continue;
    }

    // 手表相机在私密场景下可疑
    if (设备.类型 === '手表相机' && 场景描述?.includes('帐篷')) {
      可疑设备.push(设备);
      continue;
    }

    // 无名设备可疑
    if (设备.归属 === '无名设备') {
      可疑设备.push(设备);
      continue;
    }

    // 第三方设备在无授权情况下可疑
    if (设备.归属 === '第三方设备' && 设备.授权状态.授权状态 !== '已确认') {
      可疑设备.push(设备);
    }
  }

  return 可疑设备;
}

// ==================== 设备扫描提示 ====================

export function 生成设备扫描提示(场景: string): string[] {
  const 提示: string[] = [];

  if (场景.includes('帐篷') || 场景.includes('露营')) {
    提示.push("检查帐篷内部是否有可疑小孔或装置");
    提示.push("注意手表、运动眼镜等穿戴设备");
    提示.push("检查背包侧袋和装备包");
  }

  if (场景.includes('更换') || 场景.includes('更衣')) {
    提示.push("确保有安全的更衣环境");
    提示.push("检查是否有镜头对准更衣区域");
    提示.push("优先选择有隔断的更衣区");
  }

  if (场景.includes('休息') || 场景.includes('睡眠')) {
    提示.push("休息前确认帐篷拉链是否牢固");
    提示.push("不要将贵重物品暴露在镜头前");
    提示.push("尽量与信任的人相邻而居");
  }

  if (场景.includes('水上') || 场景.includes('游泳') || 场景.includes('潜水')) {
    提示.push("下水前确认周围是否有可疑设备");
    提示.push("湿衣状态下注意保护隐私");
    提示.push("出水后尽快遮挡身体");
  }

  return 提示;
}
