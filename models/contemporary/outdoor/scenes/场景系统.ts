// ==================== 越界触发检测 ====================

export function 检测越界触发(
  场景: 野外场景定义,
  教练: 教练领队核心状态,
  参与者: 参与者核心状态,
  已有越界行为: 越界行为类型[]
): 越界行为类型 | null {
  // 教练的隐藏倾向决定了触发概率
  const 总越界倾向 = (教练.越界倾向 + 教练.偷拍倾向 + 教练.猎艳倾向) / 3;
  
  // 场景风险越高，越界触发概率越高
  const 触发概率 = (总越界倾向 * 场景.风险系数) / 100;
  
  // 随机触发
  if (Math.random() > 触发概率) {
    return null;
  }

  // 根据教练动机选择最可能的越界类型
  const 可能越界行为 = 场景.典型触发;
  
  // 基于教练动机权重
  const 动机权重: Record<string, number> = {
    '纯教学': 0.1,
    '商业盈利': 0.2,
    '泡妞': 0.6,
    '收集': 0.7,
    '传播': 0.8,
    '猎艳': 0.9,
  };

  // 返回最可能的越界行为
  const topChoice = 可能越界行为.sort((a, b) => {
    const weightA = 动机权重[教练.动机类型] || 0.5;
    const weightB = 动机权重[教练.动机类型] || 0.5;
    return weightB - weightA;
  })[0];

  return topChoice || null;
}
