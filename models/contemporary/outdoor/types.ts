// ==================== 核心状态接口 ====================

// 户外运动者状态
export interface 户外运动者状态 {
  ID: string;
  化名: string;
  本名?: string;
  性别: '男' | '女';
  年龄: number;

  // 运动信息
  主要运动: 极限运动类型[];
  执照等级: 执照等级;
  野外经验: number;          // 0-100
  体能等级: number;           // 0-100
  装备价值: number;           // 拥有的装备价值

  // 社交
  队友关系: Record<string, number>;  // 队友ID -> 亲密度
  圈内人脉: number;            // 0-100
  活动参与次数: number;        // 参加野外活动的次数

  // 经历
  野外亲密次数: number;        // 在野外发生的亲密接触次数
  帐篷独处次数: number;        // 在帐篷里独处的次数
  篝火夜话次数: number;        // 篝火旁交心的次数

  // 心理
  野外心理: number;            // 0-100，在野外的心理状态
  冒险欲望: number;            // 0-100，追求刺激的程度
  信任倾向: number;            // 0-100，在野外对队友的信任

  // 安全
  事故经历: number;            // 事故经历次数
  迷路经历: number;            // 迷路经历次数
  野外自我保护: number;        // 0-100

  // 吸引力
  运动魅力: number;            // 0-100，运动时的魅力
  危险气息: number;            // 0-100，让人觉得危险的程度
}

// 活动状态
export interface 野外活动状态 {
  活动ID: string;
  活动类型: 野外活动类型;
  地点: string;
  时间: string;
  时长: number;                // 天数

  // 参与者
  参与人数: number;
  参与者ID: string[];
  领队ID: string;

  // 场景
  场景类型: 野外场景;
  场景描述: string;
  私密程度: number;            // 0-100，私密程度

  // 状态
  当前阶段: '集合' | '出发' | '进行中' | '休息' | '营地' | '结束';
  暧昧指数: number;             // 0-100
  酒精程度: number;             // 0-100，饮用酒的程度

  // 氛围
  氛围指数: number;             // 0-100
  信任建立: number;             // 0-100，队友间的信任
  暧昧触发: boolean;            // 是否触发暧昧
}

// 装备状态
export interface 野外装备状态 {
  装备ID: string;
  装备名称: string;
  类型: 极限运动类型;
  价值: number;
  新旧程度: number;             // 0-100
  保养状态: number;             // 0-100
}
