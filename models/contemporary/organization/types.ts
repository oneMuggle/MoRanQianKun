// ==================== 核心状态接口 ====================

// 家居服务者状态
export interface 家居服务者状态 {
  ID: string;
  化名: string;
  本名?: string;
  性别: '男' | '女';
  年龄: number;

  // 职业
  家居职业: 家居职业类型;
  服务等级: 服务等级;
  服务客户数: number;

  // 工作
  进入私人空间次数: number;       // 进入客户家的次数
  发现秘密数量: number;           // 发现客户秘密数
  边界模糊次数: number;           // 越界次数
  收入: number;                   // 月收入

  // 客户
  客户类型分布: Record<家居客户类型, number>;

  // 安全
  骚扰经历: number;               // 被骚扰次数
  投诉: number;                   // 被投诉次数

  // 心理
  职业倦怠: number;                // 0-100
  工作满意度: number;              // 0-100
  羞耻度: number;                  // 0-100

  // 安全意识
  安全意识: number;                // 0-100
  防范意识: number;                // 0-100
}

// 客户状态
export interface 家居客户状态 {
  ID: string;
  客户名: string;
  性别: '男' | '女';
  年龄: number;

  // 客户类型
  客户类型: 家居客户类型;
  居住类型: '公寓' | '别墅' | '普通住宅';

  // 订单
  下单频率: number;               // 每月次数
  购买服务: 服务等级[];

  // 安全
  防范意识: number;                // 0-100
  隐私保护: number;                // 0-100
  对服务人员评价: Record<string, number>;

  // 心理
  孤独感: number;                  // 0-100
  对服务依赖: number;              // 0-100
}
