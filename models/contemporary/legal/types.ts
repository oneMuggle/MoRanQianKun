// ==================== 核心状态接口 ====================

// 咨询从业者状态
export interface 咨询从业者状态 {
  ID: string;
  化名: string;
  本名?: string;
  性别: '男' | '女';
  年龄: number;

  // 职业
  咨询职业: 咨询职业类型;
  所属机构?: string;
  从业年限: number;

  // 客户
  客户类型: 咨询客户类型[];
  成功案例: number;
  月收入: number;

  // 工作
  工作压力: number;                 // 0-100
  职业倦怠: number;                 // 0-100
  客户信任度: number;               // 0-100

  // 潜规则
  潜规则边界模糊次数: number;       // 越界次数
  被投诉: number;                   // 被投诉次数

  // 心理
  职业认同: number;                 // 0-100
  情感投入: number;                 // 0-100，对客户的情感投入
  收入满意度: number;               // 0-100

  // 安全
  法律风险: number;                  // 0-100
  行业名声: number;                  // 0-100
  隐私保护: number;                  // 0-100
}

// 客户状态
export interface 咨询客户状态 {
  ID: string;
  客户名: string;
  性别: '男' | '女';
  年龄: number;

  // 客户类型
  客户类型: 咨询客户类型;
  问题类型: string;

  // 关系
  咨询师ID?: string;
  信任度: number;                    // 0-100
  依赖度: number;                    // 0-100
  倾诉深度: number;                  // 0-100，倾诉了多少隐私

  // 经历
  咨询次数: number;
  花费金额: number;
  被建议越界: boolean;               // 是否被建议过做越界的事

  // 心理
  心理状态: number;                  // 0-100
  倾诉欲: number;                    // 0-100
}
