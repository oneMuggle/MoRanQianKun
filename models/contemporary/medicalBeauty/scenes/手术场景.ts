// ==================== 手术室场景 ====================

export function 生成手术场景(
  整形者: 整形者核心状态,
  机构: 医美机构状态,
  项目: 手术类型,
  级别: 手术级别,
  医生资质: 医生资质
): 手术场景结果 {
  const 级别信息 = 手术级别配置[级别];
  const 资质信息 = 资质安全系数[医生资质];
  
  // 模拟手术结果
  const 失败事件 = 模拟手术结果(项目, 级别, 机构);
  
  // 生成主刀医生姓名
  const 医生姓名 = 医生资质 === '无证游医' ? '张老师' : 
    ['王主任', '李医生', '张教授', '陈院长'][Math.floor(Math.random() * 4)];
  
  // 计算费用
  const 基础费用 = parseInt(级别信息.费用范围.split('-')[1]);
  const 实际费用 = Math.round(基础费用 * 资质信息.价格系数);

  let 场景描述 = `【医美机构 - 手术室】

手术项目：${项目}
手术级别：${级别}
${级别信息.名称}

`;

  if (失败事件) {
    switch (失败事件.类型) {
      case '效果不满意':
        场景描述 += `【手术中】
你躺在手术台上，局部麻醉让你意识清醒但感觉不到疼痛...
手术过程中你听到医生说了一句"这里有点不太好处理"...

【手术后】
你被推出手术室，脸部肿胀，眼睛还没能完全睁开。
护士给你镜子让你看效果，你发现两边不太对称...
`;
        break;
      case '并发症':
        场景描述 += `【手术中】
麻醉后你渐渐失去意识...
不知过了多久，你被一阵剧烈的疼痛惊醒！

【手术后】
你发现手术部位出现异常肿胀和发红，
医生说是正常的并发症反应，需要住院观察...
`;
        break;
      case '功能性损伤':
        场景描述 += `【手术中】
手术进行了很久，期间你多次被疼痛打断...
医生似乎遇到了什么麻烦，临时叫来了其他人...

【手术后】
你发现眼睛闭不上了 / 鼻子歪了 / 嘴唇变形了...
医生说这是功能性损伤，需要很长时间恢复...
`;
        break;
      case '毁容':
        场景描述 += `【手术中】
你被全麻后沉沉睡去...
中间似乎被疼痛唤醒，但又被加强麻醉...

【手术后】
你被推出来，脸上缠满了绷带。
换药时你看到了自己的脸——疤痕狰狞，组织坏死...
`;
        break;
      case '死亡':
        场景描述 += `【手术中】
麻醉起效后你失去意识...
手术室外传来一阵骚动...

【后续】
（此场景需要特殊处理）
`;
        break;
    }
  } else {
    场景描述 += `【手术中】
你躺在手术台上，麻醉让你感觉昏昏沉沉...
医生和护士在旁边轻声交谈，你听到手术器械的声音...

【手术后】
手术顺利完成，你被推到恢复室。
护士给你冰敷，告诉你一些术后注意事项。
`;

    if (整形者.成瘾程度 > 60) {
      场景描述 += `
但你已经迫不及待想看最终效果了...
`;
    }
  }

  // 医生描述
  if (医生资质 === '无证游医') {
    场景描述 += `
【关于医生】
这个"医生"是朋友介绍的，据说"手艺很好"。
手术室简陋，器械看起来也不太正规。
你没有看到任何执业资格证书。
`;
  } else if (资质信息.风险系数 > 1) {
    场景描述 += `
【关于医生】
普通执业医师，缺乏大型手术经验。
但手术费相对便宜，机构说"效果也不会差"。
`;
  }

  return {
    场景描述,
    手术信息: {
      项目,
      级别,
      时长: 级别信息.时长,
      麻醉: 级别信息.麻醉方式,
      费用: 实际费用,
      主刀医生: 医生姓名,
    },
    手术结果: 失败事件 
      ? (失败事件.严重程度 === '极重' ? '严重失败' : '失败')
      : '成功',
    失败详情: 失败事件?.描述,
    术后状态: 失败事件 ? '焦虑' : '期待',
    恢复期: 级别信息.恢复期,
    关键选择: [
      '询问医生术后护理注意事项',
      '要求查看术前术后对比照片',
      '询问如果效果不好怎么办',
      '向护士要联系方式以便随时咨询',
      '要求提前安排拆线时间',
    ],
    下一步: 失败事件 ? '修复' : '恢复期',
  };
}

// ==================== 恢复期场景 ====================

export function 生成恢复场景(
  整形者: 整形者核心状态,
  机构: 医美机构状态,
  术后天数: number,
  级别: 手术级别
): 恢复场景结果 {
  const 级别信息 = 手术级别配置[级别];
  
  // 判断恢复状态
  let 恢复状态: 恢复场景结果['恢复状态'];
  let 异常描述: string | undefined;
  
  // 根据术后天数和随机因素判断
  const 随机 = Math.random();
  const 基础异常率 = 0.1 + (机构.事故率 / 100) * 0.3;
  
  if (术后天数 < 7) {
    // 早期恢复期
    if (随机 < 0.7) {
      恢复状态 = '正常';
    } else if (随机 < 0.9) {
      恢复状态 = '异常';
      异常描述 = '肿胀比预期严重，可能需要延长恢复期';
    } else {
      恢复状态 = '并发症';
      异常描述 = '出现感染迹象，需要立即处理';
    }
  } else if (术后天数 < 30) {
    // 中期恢复期
    if (随机 < 0.75) {
      恢复状态 = '良好';
    } else if (随机 < 0.9) {
      恢复状态 = '正常';
    } else if (随机 < 0.98) {
      恢复状态 = '异常';
      异常描述 = '恢复速度不如预期，可能存在轻度感染';
    } else {
      恢复状态 = '并发症';
      异常描述 = '伤口愈合不良，需要修复手术';
    }
  } else {
    // 后期恢复期
    if (随机 < 0.8) {
      恢复状态 = '良好';
    } else if (随机 < 0.95) {
      恢复状态 = '正常';
    } else {
      恢复状态 = '异常';
      异常描述 = '疤痕明显，可能需要激光治疗';
    }
  }

  // 生成场景描述
  let 场景描述 = `【整形者住所 - 术后第${术后天数}天】

`;

  if (恢复状态 === '良好') {
    场景描述 += `你对着镜子仔细观察，恢复得比预期要好。
肿胀已经消退大半，能看到初步的效果了。
`;
  } else if (恢复状态 === '正常') {
    场景描述 += `恢复期正常进行中，还有些肿胀但可以接受。
你每天都在照镜子，期待最终效果。
`;
  } else if (恢复状态 === '异常') {
    场景描述 += `恢复情况不太理想...
${异常描述}
你给机构打电话，咨询师说是正常现象让你再观察。
`;
  } else if (恢复状态 === '并发症') {
    场景描述 += `并发症出现了！
${异常描述}
机构那边态度开始变得不耐烦...
你开始担心会不会出问题。
`;
  }

  // 焦虑和满意度变化
  let 焦虑变化 = 0;
  let 满意度变化 = 0;

  if (恢复状态 === '良好') {
    焦虑变化 = -10;
    满意度变化 = +10;
    场景描述 += `
你心里的大石头终于落地了一些。
`;
  } else if (恢复状态 === '正常') {
    焦虑变化 = 0;
    满意度变化 = +5;
  } else if (恢复状态 === '异常') {
    焦虑变化 = +15;
    满意度变化 = -10;
    场景描述 += `
你的焦虑感越来越强，晚上都睡不好觉。
`;
  } else if (恢复状态 === '并发症') {
    焦虑变化 = +25;
    满意度变化 = -20;
    场景描述 += `
你开始后悔做这个手术了...
要是早知道会这样，打死也不会来。
`;
  }

  // 整形成瘾相关
  if (整形者.成瘾程度 > 50 && 恢复状态 === '良好') {
    场景描述 += `
但你已经开始在想要不要做下一个项目了...
`;
    满意度变化 += 5; // 对下一次更期待
  }

  return {
    场景描述,
    恢复状态,
    异常描述,
    焦虑变化,
    满意度变化,
    关键选择: [
      '给机构客服打电话询问恢复情况',
      '上网搜索类似情况是否正常',
      '在整形群里咨询其他人的经验',
      '预约提前复诊',
      '继续观察，等待自然恢复',
      '联系家人/朋友寻求支持',
    ],
    下一步: 恢复状态 === '并发症' ? '修复' : '拆线',
  };
}

