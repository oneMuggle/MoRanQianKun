/**
 * 生成拆线/复诊场景
 */
export function 生成拆线场景(
  整形者: 整形者核心状态,
  机构: 医美机构状态,
  手术结果: '成功' | '部分成功' | '失败' | '严重失败'
): 拆线场景结果 {
  let 最终效果: 拆线场景Result['最终效果'];
  let 效果描述: string;
  let 满意度: number;

  switch (手术结果) {
    case '成功':
      if (Math.random() > 0.3) {
        最终效果 = '完美';
        效果描述 = '效果超出预期，你对这次手术非常满意';
        满意度 = 95;
      } else {
        最终效果 = '满意';
        效果描述 = '效果还不错，达到预期';
        满意度 = 80;
      }
      break;
    case '部分成功':
      最终效果 = '一般';
      效果描述 = '效果马马虎虎，有的地方不太满意';
      满意度 = 55;
      break;
    case '失败':
      最终效果 = '失望';
      效果描述 = '效果不理想，和承诺的差太远了';
      满意度 = 30;
      break;
    case '严重失败':
      最终效果 = '毁容';
      效果描述 = '情况很糟糕，简直是毁容';
      满意度 = 5;
      break;
  }

  let 场景描述 = `【医美机构 - 拆线室】

术后恢复期已过，今天是复诊的日子。

`;

  if (最终效果 === '完美' || 最终效果 === '满意') {
    场景描述 += `咨询师热情地迎接你："恢复得很好哦！"
镜子里的你确实变美了，五官更加精致。
${整形者.成瘾程度 > 50 ? '你内心已经在盘算下一个项目了...' : ''}
`;
  } else if (最终效果 === '一般') {
    场景描述 += `咨询师看了看说："恢复得还可以，每个人体质不同。"
但你自己觉得效果一般，有一边有点不对称...
`;
  } else if (最终效果 === '失望') {
    场景描述 += `咨询师看了一眼就开始推卸责任：
"这是你恢复期没护理好"、"正常现象"、"再等等看"...

你看着镜子里的自己，心情跌到谷底。
这根本不是自己想要的效果！
`;
  } else if (最终效果 === '毁容') {
    场景描述 += `拆开绷带的瞬间，你崩溃了。
疤痕狰狞、鼻子歪斜、眼睛闭不上...
咨询师面无表情地说："签了合同就表示认可手术结果。"

你想哭却哭不出来。
`;
  }

  // 机构态度
  if (最终效果 === '失望' || 最终效果 === '毁容') {
    场景描述 += `
【机构态度】
机构推卸责任，说是"正常现象"或个人体质问题。
如果要修复，机构表示要额外收费。
甚至有护士在旁边说："当初是你们自己要求做的..."
`;

    if (机构.宰客程度 > 60) {
      场景描述 += `
你感觉到这家机构只关心钱，根本不在乎客户的结果。
`;
    }
  }

  return {
    场景描述,
    最终效果,
    效果描述,
    满意度,
    关键选择: 最终效果 === '完美' || 最终效果 === '满意'
      ? [
          '感谢医生，期待完全恢复',
          '询问维持效果需要多久',
          '咨询下一个想做的项目',
          '回家好好庆祝一下',
        ]
      : 最终效果 === '一般'
      ? [
          '勉强接受这个结果',
          '要求机构给个说法',
          '考虑要不要做修复',
          '在网络上分享经历',
        ]
      : [
          '要求机构免费修复',
          '收集证据准备维权',
          '向消费者协会投诉',
          '找媒体曝光',
          '接受现实，攒钱去其他地方修复',
        ],
    下一步: 最终效果 === '完美' || 最终效果 === '满意'
      ? '继续整形'
      : 最终效果 === '一般'
      ? '结束'
      : '维权',
  };
}

