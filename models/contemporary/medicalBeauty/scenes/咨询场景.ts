// ==================== 场景生成 ====================

export interface 面诊场景结果 {
  场景描述: string;
  咨询师话术: string;
  整形者反应: string;
  关键选择: string[];
  下一步: '离开' | '交定金' | '继续咨询' | '贷款签约';
}

/**
 * 生成面诊场景
 */
export function 生成面诊场景(
  整形者: 整形者核心状态,
  机构: 医美机构状态
): 面诊场景结果 {
  const 配置 = 面诊场景配置表[机构.档次];
  let 话术: 咨询师话术;

  switch (配置.咨询师风格) {
    case '专业型':
      话术 = 专业型咨询师话术;
      break;
    case '恐吓型':
      话术 = 恐吓型咨询师话术;
      break;
    default:
      话术 = 推销型咨询师话术;
  }

  const 随机话术 = {
    开场白: 话术.开场白[Math.floor(Math.random() * 话术.开场白.length)],
    放大焦虑: 话术.放大焦虑[Math.floor(Math.random() * 话术.放大焦虑.length)],
    推荐项目: 话术.推荐项目[Math.floor(Math.random() * 话术.推荐项目.length)],
    价格策略: 话术.价格策略[Math.floor(Math.random() * 话术.价格策略.length)],
    促成成交: 话术.促成成交[Math.floor(Math.random() * 话术.促成成交.length)],
  };

  const 场景描述 = `【医美机构 - 面诊室】

${机构.机构名称}

${机构.档次 === '黑机构' ? '机构藏在一栋老旧居民楼里，装修简陋但挂满了"韩国专家"的宣传海报。' : ''}
${机构.档次 === '顶级' ? '机构位于市中心顶级写字楼，环境奢华，有专人接待。' : ''}
${机构.档次 === '中端' || 机构.档次 === '低端' ? '机构位于商业广场，装修中等，有接待员引导。' : ''}

${随机话术.开场白}

${随机话术.放大焦虑}

${随机话术.推荐项目}

${机构.宰客程度 > 50 ? `价格报得很高：${Math.floor(Math.random() * 5 + 3)}万` : `价格报价合理：${Math.floor(Math.random() * 3 + 1)}万`}

${配置.是否推荐贷款 ? `咨询师开始推荐分期：${随机话术.价格策略}` : ''}`;

  return {
    场景描述,
    咨询师话术: `${随机话术.放大焦虑} ${随机话术.推荐项目}`,
    整形者反应: '你的反应...',
    关键选择: [
      '相信咨询师的判断，接受推荐的项目',
      '觉得太贵，询问更便宜的方案',
      '对贷款感兴趣，了解分期细节',
      '觉得不对劲，决定再看看其他机构',
      '被咨询师说服，当场支付定金',
    ],
    下一步: '待选择',
  };
}

// ==================== 贷款签约场景 ====================

export interface 贷款签约场景结果 {
  场景描述: string;
  贷款套路: string[];
  关键选择: string[];
  是否签约: boolean;
}

/**
 * 生成贷款签约场景
 */
export function 生成贷款签约场景(
  整形者: 整形者核心状态,
  产品名称: string,
  贷款金额: number,
  期数: number
): 贷款签约场景Result {
  const 实际年化 = 期数 > 24 ? 36 : 24;
  const 月还款 = Math.round((贷款金额 * (1 + 实际年化 / 100)) / 期数);
  const 总还款 = 月还款 * 期数;

  const 场景描述 = `【医美机构 - 贷款签约】

${产品名称}分期付款

${产品名称.includes('0利息') ? '利息已经被算进手术费' : ''}
表面月供：${月还款}元
实际年化利率：约${实际年化}%
总还款：${总还款}元
${!产品名称.includes('正规') ? '这是高利贷产品！' : ''}

签约合同：
密密麻麻的条款，你被催促着快点签字...
${!产品名称.includes('正规') ? '咨询师不断催促，不给你细看的时间' : ''}

`;
  const 套路: string[] = [];
  if (产品名称.includes('0利息')) {
    套路.push('0利息陷阱：利息被算进手术费');
  }
  if (实际年化 > 24) {
    套路.push(`高利息：实际年化${实际年化}%，超过法律保护范围`);
  }
  if (!产品名称.includes('正规')) {
    套路.push('非正规平台：存在砍头息、暴力催收风险');
  }

  return {
    场景描述,
    贷款套路: 套路,
    关键选择: [
      '仔细看合同，了解所有条款',
      '被催促，直接签字',
      '觉得不对劲，拒绝贷款',
      '要求修改条款或降低额度',
    ],
    是否签约: false,
  };
}

type 贷款签约场景Result = {
  场景描述: string;
  贷款套路: string[];
  关键选择: string[];
  是否签约: boolean;
};
