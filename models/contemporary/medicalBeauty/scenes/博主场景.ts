// ==================== 辅助函数 ====================

function 生成随机中介(): 医美中介状态 {
  const 风格 = Math.random() > 0.6 ? '普通' : Math.random() > 0.5 ? '黑心' : '良心';
  
  const 中介: 医美中介状态 = {
    中介ID: `中介_${Date.now()}`,
    昵称: 风格 === '良心' ? '阿雪' : 风格 === '普通' ? '小丽' : '阿强',
    性别: 风格 === '黑心' ? '男' : '女',
    年龄: 25 + Math.floor(Math.random() * 15),
    合作医院: ['医院A', '医院B', '医院C'].slice(0, Math.floor(Math.random() * 3) + 1),
    客源数量: Math.floor(Math.random() * 100),
    成交率: 50 + Math.floor(Math.random() * 40),
    中介抽成: 风格 === '良心' ? 30 : 风格 === '普通' ? 50 : 70,
    月收入: 风格 === '良心' ? 15000 : 风格 === '普通' ? 30000 : 50000,
    累计收入: 0,
    推销力度: 风格 === '良心' ? 40 : 风格 === '普通' ? 70 : 90,
    良心程度: 风格 === '良心' ? 80 : 风格 === '普通' ? 40 : 10,
    承诺水分: 风格 === '良心' ? 10 : 风格 === '普通' ? 30 : 60,
    坑蒙拐骗: 风格 === '良心' ? 5 : 风格 === '普通' ? 20 : 60,
    虚假承诺: 风格 === '良心' ? 10 : 风格 === '普通' ? 30 : 70,
  };

  中介.累计收入 = 中介.月收入 * (10 + Math.floor(Math.random() * 20));

  return 中介;
}

function 博主InfoTypeToChinese(type: 博主类型): string {
  const map: Record<博主类型, string> = {
    '真实分享型': '真实分享',
    '商业推广型': '商业推广',
    '虚假案例型': '虚假案例',
    '医托型': '医托引流',
  };
  return map[type];
}
