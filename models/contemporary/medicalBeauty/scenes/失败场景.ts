// ==================== 维权场景 ====================

export interface 维权场景结果 {
  场景描述: string;
  维权选项: {
    选项: string;
    成功率: number;
    成本: number;
    风险: string[];
  }[];
  选择结果?: {
    成功: boolean;
    获得赔偿: number;
    描述: string;
  };
  后续选项: string[];
}

export function 生成维权场景(
  整形者: 整形者核心状态,
  机构: 医美机构状态,
  失败类型: '效果不满意' | '并发症' | '毁容' | '死亡',
  是否已尝试协商: boolean = false
): 维权场景结果 {
  const 维权选项列表 = 获取维权选项(失败类型);
  
  let 场景描述 = `【维权场景】

你在${机构.机构名称}遭遇了${失败TypeTo描述(失败类型)}。
现在你要决定如何维权。

`;

  if (!是否已尝试协商) {
    场景描述 += `【可选维权路径】

1. 协商解决（已失败）
你已经尝试和机构协商，但对方推卸责任、态度恶劣。

2. 投诉渠道
- 向当地消费者协会投诉
- 向卫生健康委员会举报
- 向市场监督管理局投诉

3. 法律途径
- 委托律师发送律师函
- 向法院提起民事诉讼
- 构成刑事犯罪的可以报案

4. 舆论压力
- 在网络平台发帖曝光
- 向媒体提供线索
- 短视频平台曝光

`;
  }

  // 根据失败类型添加特定描述
  if (失败类型 === '效果不满意') {
    场景描述 += `【效果不满意的具体情况】
你发现手术后的效果和当初承诺的差太远：
- 双眼皮不对称
- 鼻子歪斜
- 下巴弧度不对

机构说这是"正常审美差异"，拒绝承担责任。
`;
  } else if (失败类型 === '并发症') {
    场景描述 += `【并发症的具体情况】
手术后出现了严重并发症：
- 伤口感染
- 持续红肿不退
- 组织坏死

机构说是你"体质问题"，拖延不肯处理。
`;
  } else if (失败类型 === '毁容') {
    场景描述 += `【毁容的严重后果】
手术造成了永久性伤害：
- 面部疤痕明显
- 组织缺损
- 功能障碍

机构的态度是"签了合同就要认"，甚至威胁你"闹事就报警"。
`;
  } else if (失败类型 === '死亡') {
    场景描述 += `【最严重的后果】
手术意外导致...

（此场景涉及严重后果，需要特殊处理）
`;
  }

  // 添加维权难度描述
  场景描述 += `
【维权难度分析】
`;

  if (机构.类型 === '工作室') {
    场景描述 += `- 机构是工作室性质，可能无正规资质
`;
  }
  if (机构.虚假宣传 > 60) {
    场景描述 += `- 机构存在虚假宣传，但举证困难
`;
  }
  if (机构.宰客程度 > 60) {
    场景描述 += `- 机构背景复杂，可能有"保护伞"
`;
  }
  if (机构.事故率 > 20) {
    场景描述 += `- 机构有大量投诉记录，但似乎都能摆平
`;
  }

  return {
    场景描述,
    维权选项: 维权选项列表.map(opt => ({
      选项: opt.选项,
      成功率: Math.round(opt.成功率 * 100),
      成本: opt.成本,
      风险: opt.风险,
    })),
    后续选项: [
      '收集证据（病历、照片、录音）',
      '咨询专业律师',
      '联系其他受害者一起维权',
      '在网络平台分享经历',
      '尝试与机构高层谈判',
      '放弃维权，自认倒霉',
    ],
  };
}

function 失败TypeTo描述(type: string): string {
  const map: Record<string, string> = {
    '效果不满意': '手术效果不满意',
    '并发症': '手术后出现并发症',
    '毁容': '手术导致毁容',
    '死亡': '手术意外导致死亡',
  };
  return map[type] || type;
}

// ==================== 隐私泄露场景 ====================

export interface 隐私泄露场景结果 {
  场景描述: string;
  泄露渠道: string;
  影响评估: {
    隐私: number;
    名誉: number;
    关系: number;
    工作: number;
  };
  应对选项: string[];
  后续: '维权' | '危机公关' | '沉默' | '报警';
}

export function 生成隐私泄露场景(
  整形者: 整形者核心状态,
  机构: 医美机构状态,
  泄露类型: '术前照泄露' | '术后照泄露' | '手术过程视频泄露'
): 隐私泄露场景Result {
  let 场景描述 = `【隐私泄露场景】

`;

  if (泄露类型 === '术前照泄露') {
    场景描述 += `你收到朋友的截图："这个人长得好像你啊"
点开一看，是你在某医美论坛的帖子，
配图是你的术前素颜照，还写着"这是我手术前的照片"...

`;
  } else if (泄露类型 === '术后照泄露') {
    场景描述 += `你的术后恢复照被发到了网上！
照片清晰地显示了手术部位和你的面部特征...
评论区有人在问"这是哪家做的"...

`;
  } else if (泄露类型 === '手术过程视频泄露') {
    场景描述 += `一段手术室的视频在群里传播...
虽然打了码，但认识你的人一眼就能认出是你。
视频标题写着："XX医美手术全程记录"...

`;
  }

  // 判断泄露渠道
  let 泄露渠道: string;
  const 随机 = Math.random();
  
  if (机构.虚假宣传 > 50) {
    泄露渠道 = '机构用你的照片做案例宣传';
  } else if (机构.渠道抽成 > 50) {
    泄露渠道 = '可能是中介或渠道商泄露';
  } else {
    泄露渠道 = '机构内部人员无意/故意泄露';
  }

  场景描述 += `【泄露渠道分析】
${泄露渠道}

`;

  // 评估影响
  let 影响评估 = {
    隐私: 80,
    名誉: 60,
    关系: 40,
    工作: 30,
  };

  if (整形者.家人知道) {
    影响评估.关系 += 20;
    场景描述 += `【家人】
你的家人已经知道你去整形了...
他们看到这些照片会有什么反应？
`;
  } else {
    场景描述 += `【家人】
你一直瞒着家人去整形...
如果被家人发现，后果难以想象。
`;
  }

  if (整形者['男朋友/老公知道']) {
    影响评估.关系 += 15;
  }

  if (泄露类型 === '手术过程视频泄露') {
    影响评估.名誉 += 20;
    影响评估.隐私 += 15;
  }

  场景描述 += `
【应对选择】

`;

  const 应对选项 = [
    '立即联系机构要求删除',
    '报警处理（侵犯隐私权）',
    '委托律师发律师函',
    '在平台举报要求删帖',
    '联系发布者协商删除',
    '发声明澄清是自己整形，与机构无关',
    '忍气吞声，希望事情平息',
  ];

  场景描述 += 应对选项.map((opt, i) => `${i + 1}. ${opt}`).join('\n');

  return {
    场景描述,
    泄露渠道,
    影响评估,
    应对选项,
    后续: '维权',
  };
}

type 隐私泄露场景Result = {
  场景描述: string;
  泄露渠道: string;
  影响评估: {
    隐私: number;
    名誉: number;
    关系: number;
    工作: number;
  };
  应对选项: string[];
  后续: '维权' | '危机公关' | '沉默' | '报警';
};

// ==================== 贷后催债场景 ====================

export interface 贷后场景结果 {
  场景描述: string;
  还款状态: '正常' | '逾期' | '严重逾期' | '已违约';
  催收情况: {
    方式: string;
    频率: string;
    威胁程度: '温和' | '正常' | '频繁' | '暴力';
  };
  对生活影响: {
    征信: boolean;
    通讯录: boolean;
    工作: boolean;
    关系: boolean;
  };
  建议: string[];
  可选行动: string[];
}

export function 生成贷后场景(
  整形者: 整形者核心状态,
  已逾期天数: number = 0
): 贷后场景Result {
  const 还款压力评估 = 评估还款压力(整形者);
  const 逾期危机评估 = 评估逾期危机(整形者, 已逾期天数);

  let 场景描述 = `【贷后场景】

`;

  if (已逾期天数 === 0) {
    场景描述 += `你按时还款中，但每个月还完款后生活费就所剩无几了...

【当前还款状况】
- 月还款：${整形者.月还款}元
- 还款压力等级：${还款压力评估.等级}
- 还款占收入比：${还款压力评估.还款占比}%

`;

    if (还款压力评估.等级 === '危险' || 还款压力评估.等级 === '崩溃') {
      场景描述 += `你感到喘不过气来...
为了还贷，你不得不：
- 减少伙食费
- 刷信用卡以贷养贷
- 向朋友借钱

你开始担心这样下去会不会崩盘...
`;
    }
  } else {
    场景描述 += `你已经逾期${已逾期天数}天了！

【逾期危机】
`;

    if (逾期危机评估.催收强度 === '温柔') {
      场景描述 += `贷款平台开始打电话催收，语气还算客气：
"您好，您的贷款已经逾期，请尽快处理..."
`;
    } else if (逾期危机评估.催收强度 === '正常') {
      场景描述 += `催收电话越来越频繁：
"再不还钱我们要走法律程序了！"
每天都收到好几通电话，你不敢接。
`;
    } else if (逾期危机评估.催收强度 === '频繁') {
      场景描述 += `催收已经开始骚扰你的通讯录了！
你的朋友和家人收到了催收电话：
"你朋友在我们平台借了钱，现在联系不上..."
`;
    } else if (逾期危机评估.催收强度 === '暴力') {
      场景描述 += `暴力催收开始了！
- 各种威胁短信和电话
- PS你的照片到处传播
- 威胁要上门找你家人
`;
    }

    场景描述 += `
【对你的影响】
`;

    if (逾期危机评估.影响.征信) {
      场景描述 += `- 征信受损，以后贷款买房买车都受影响
`;
    }
    if (逾期危机评估.影响.通讯录) {
      场景描述 += `- 通讯录被爆，朋友家人都知道了
`;
    }
    if (逾期危机评估.影响.工作) {
      场景描述 += `- 工作受到影响，领导找你谈话
`;
    }
  }

  // 整形者心理状态
  场景描述 += `
【心理状态】
`;

  if (整形者.成瘾程度 > 60) {
    场景描述 += `虽然债务缠身，但你还是忍不住在想：
下一次整形做什么好呢？
`;
  }

  if (整形者.羞耻程度 > 60) {
    场景描述 += `你感到非常羞耻，不敢告诉任何人。
`;
  }

  return {
    场景描述,
    还款状态: 已逾期天数 === 0 
      ? '正常' 
      : 已逾期天数 < 30 
      ? '逾期' 
      : 已逾期天数 < 90 
      ? '严重逾期' 
      : '已违约',
    催收情况: {
      方式: 逾期危机评估.催收强度 === '暴力' ? '威胁短信+电话+爆通讯录' : '电话催收',
      频率: 逾期危机评估.催收强度 === '温柔' ? '偶尔' : 逾期危机评估.催收强度 === '正常' ? '每天几次' : '频繁',
      威胁程度: 逾期危机评估.催收强度,
    },
    对生活影响: 逾期危机评估.影响,
    建议: [
     逾期危机评估.建议,
      '寻求家人帮助',
      '向金融监管部门投诉',
      '保留催收威胁的证据',
      '考虑协商还款或延期',
    ],
    可选行动: [
      '向家人坦白寻求帮助',
      '协商还款计划',
      '向监管部门投诉暴力催收',
      '报警处理威胁恐吓',
      '继续以贷养贷',
      '放弃，直接不还了',
    ],
  };
}

type 贷后场景Result = {
  场景描述: string;
  还款状态: '正常' | '逾期' | '严重逾期' | '已违约';
  催收情况: {
    方式: string;
    频率: string;
    威胁程度: '温和' | '正常' | '频繁' | '暴力';
  };
  对生活影响: {
    征信: boolean;
    通讯录: boolean;
    工作: boolean;
    关系: boolean;
  };
  建议: string[];
  可选行动: string[];
};

// ==================== 危机场景总入口 ====================

export type 危机场景类型 = '维权' | '隐私泄露' | '贷后催债';

export function 生成危机场景(
  类型: 危机场景类型,
  整形者: 整形者核心状态,
  机构: 医美机构状态,
  具体参数?: {
    失败类型?: '效果不满意' | '并发症' | '毁容' | '死亡';
    泄露类型?: '术前照泄露' | '术后照泄露' | '手术过程视频泄露';
    逾期天数?: number;
  }
): 维权场景Result | 隐私泄露场景Result | 贷后场景Result {
  switch (类型) {
    case '维权':
      return 生成维权场景(
        整形者, 
        机构, 
        具体参数?.失败类型 || '效果不满意'
      ) as 维权场景Result | 隐私泄露场景Result | 贷后场景Result;
    
    case '隐私泄露':
      return 生成隐私泄露场景(
        整形者, 
        机构, 
        具体参数?.泄露类型 || '术后照泄露'
      ) as 维权场景Result | 隐私泄露场景Result | 贷后场景Result;
    
    case '贷后催债':
      return 生成贷后场景(
        整形者, 
        具体参数?.逾期天数 || 0
      ) as 维权场景Result | 隐私泄露场景Result | 贷后场景Result;
  }
}

type 维权场景Result = {
  场景描述: string;
  维权选项: { 选项: string; 成功率: number; 成本: number; 风险: string[]; }[];
  选择结果?: { 成功: boolean; 获得赔偿: number; 描述: string; };
  后续选项: string[];
};
