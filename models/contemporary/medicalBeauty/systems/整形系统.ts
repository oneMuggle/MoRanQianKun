// ==================== 状态更新 ====================

import { 更新整形者状态, 进行手术 } from '../states/整形者状态';

export function 完成整形手术(
  state: 整形者核心状态,
  项目: 手术类型,
  费用: number,
  效果: '成功' | '部分成功' | '失败'
): 整形者核心状态 {
  let newState = 进行手术(state, 项目, 费用);

  if (效果 === '成功') {
    newState = 更新整形者状态(newState, {
      当前颜值: Math.min(10, state.当前颜值 + 1),
      满意度: 90,
      焦虑程度: Math.max(0, state.焦虑程度 - 20),
    });
  } else if (效果 === '部分成功') {
    newState = 更新整形者状态(newState, {
      满意度: 50,
      焦虑程度: state.焦虑程度 + 10,
    });
  } else {
    newState = 更新整形者状态(newState, {
      满意度: 20,
      焦虑程度: Math.min(100, state.焦虑程度 + 30),
      手术失败次数: state.手术失败次数 + 1,
    });
  }

  return newState;
}


