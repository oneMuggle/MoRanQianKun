// ==================== 机构推荐 ====================

export interface 机构推荐 {
  机构: 医美机构状态;
  推荐指数: number;
  适合项目: 手术类型[];
  风险提示: string[];
}

export function 推荐机构(
  项目: 手术类型,
  消费能力: 消费能力
): 机构推荐[] {
  // 根据消费能力确定适合的机构档次
  let 适合档次: 机构档次[];
  
  switch (消费能力) {
    case '学生党':
      适合档次 = ['中端', '低端'];
      break;
    case '普通白领':
      适合档次 = ['中端', '高端'];
      break;
    case '中产阶层':
      适合档次 = ['高端', '中端'];
      break;
    case '有钱任性':
      适合档次 = ['高端', '顶级'];
      break;
    case '网红富婆':
      适合档次 = ['顶级', '高端'];
      break;
  }

  const 项目级别 = 整形项目列表.find(p => p.类型 === 项目)?.级别 || '二级';

  // 生成推荐
  const 推荐列表: 机构推荐[] = [];

  for (const 档次 of 适合档次) {
    const 机构 = 根据档次创建机构(档次);
    
    let 推荐指数 = 70;
    const 风险提示: string[] = [];

    // 根据项目级别调整推荐
    if (项目级别 === '三级' || 项目级别 === '四级') {
      if (机构.档次 === '低端' || 机构.档次 === '黑机构') {
        推荐指数 = 0;
        风险提示.push('此类大型手术不能在低级别机构进行！');
      }
    }

    // 机构风险
    if (机构.事故率 > 10) {
      推荐指数 -= 20;
      风险提示.push('机构事故率偏高');
    }
    if (机构.无证医生 > 0) {
      推荐指数 -= 30;
      风险提示.push('存在无证医生');
    }
    if (机构.宰客程度 > 60) {
      推荐指数 -= 15;
      风险提示.push('宰客严重');
    }

    推荐指数 = Math.max(0, 推荐指数);

    推荐列表.push({
      机构,
      推荐指数,
      适合项目: [项目],
      风险提示,
    });
  }

  return 推荐列表.sort((a, b) => b.推荐指数 - a.推荐指数);
}

// ==================== 费用计算 ====================

export interface 费用明细 {
  手术费: number;
  麻醉费: number;
  住院费: number;
  药费: number;
  复查费: number;
  其他费: number;
  总计: number;
}

export function 计算费用明细(
  项目: 手术类型,
  机构: 医美机构状态,
  医生资质: 医生资质,
  消费能力: 消费能力
): 费用明细 {
  const 项目信息 = 整形项目列表.find(p => p.类型 === 项目)!;
  const 资质系数 = 资质安全系数[医生资质];

  // 基础手术费
  const 基础手术费 = 项目信息.费用[消费能力];

  // 机构加价
  const 机构加价率 = 1 + (机构.宰客程度 / 100) * 0.5;

  // 医生加价
  const 医生加价率 = 资质系数.价格系数;

  // 计算各项费用
  const 手术费 = Math.round(基础手术费 * 机构加价率 * 医生加价率);
  const 麻醉费 = Math.round(手术费 * 0.15);
  const 住院费 = 项目.级别 === '三级' || 项目.级别 === '四级' ? 2000 : 500;
  const 药费 = Math.round(手术费 * 0.05);
  const 复查费 = 项目.级别 === '三级' || 项目.级别 === '四级' ? 2000 : 500;
  const 其他费 = Math.round(手术费 * 0.08);

  const 总计 = 手术费 + 麻醉费 + 住院费 + 药费 + 复查费 + 其他费;

  return {
    手术费,
    麻醉费,
    住院费,
    药费,
    复查费,
    其他费,
    总计,
  };
}

