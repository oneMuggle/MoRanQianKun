// ==================== 项目推荐 ====================

export interface 项目推荐 {
  项目: 整形项目;
  推荐理由: string;
  适合人群: string[];
  费用适配度: number;  // 0-100
  风险评估: number;    // 0-100，越高越危险
}

export function 推荐项目(
  整形者: 整形者核心状态,
  机构: 医美机构状态
): 项目推荐[] {
  const 消费能力 = 整形者.消费能力;
  const 动机 = 整形者.整形动机;
  const 年龄 = 整形者.年龄;

  // 根据消费能力和动机筛选项目
  const 筛选后的项目 = 整形项目列表.map(项目 => {
    const 费用 = 项目.费用[消费能力];
    const 费用适配度 = 计算费用适配度(费用, 消费能力);
    const 风险评估 = 计算项目风险(项目, 机构);
    
    let 推荐理由 = '';
    const 适合人群: string[] = [];

    // 根据动机推荐
    if (动机 === '职业需求') {
      if (项目.类型 === '眼部整形') {
        推荐理由 = '职业需求，眼部整形提升精神面貌';
        适合人群.push('主播', '模特', '演员');
      }
    } else if (动机 === '婚姻需求') {
      if (项目.类型 === '眼部整形' || 项目.类型 === '鼻部整形') {
        推荐理由 = '婚姻需求，面部改善提升自信';
        适合人群.push('准备结婚');
      }
    } else if (动机 === '自信提升') {
      if (项目.级别 === '一级') {
        推荐理由 = '先从风险小的项目开始建立信心';
        适合人群.push('初整形者');
      }
    } else if (动机 === '跟风从众') {
      推荐理由 = '跟风热门项目，需谨慎评估';
      适合人群.push('关注流行审美');
    } else if (动机 === '上瘾沉迷') {
      推荐理由 = '建议先冷静，考虑心理健康';
      适合人群.push('需心理评估');
    }

    // 根据年龄调整
    if (年龄 < 25 && 项目.级别 !== '一级') {
      推荐理由 += '（25岁前建议谨慎大型手术）';
    }

    return {
      项目,
      推荐理由: 推荐理由 || '可咨询了解',
      适合人群,
      费用适配度,
      风险评估,
    };
  });

  // 按风险低优先排序
  return 筛选后的项目.sort((a, b) => a.风险评估 - b.风险评估);
}

function 计算费用适配度(费用: number, 消费能力: 消费能力): number {
  const 消费阈值: Record<消费能力, number> = {
    '学生党': 15000,
    '普通白领': 40000,
    '中产阶层': 100000,
    '有钱任性': 300000,
    '网红富婆': 800000,
  };

  const 阈值 = 消费阈值[消费能力];
  
  if (费用 <= 阈值 * 0.5) return 100;
  if (费用 <= 阈值) return 80;
  if (费用 <= 阈值 * 1.5) return 50;
  if (费用 <= 阈值 * 2) return 30;
  return 10;
}

function 计算项目风险(项目: 整形项目, 机构: 医美机构状态): number {
  // 基础风险
  const 级别风险: Record<手术级别, number> = {
    '一级': 10,
    '二级': 30,
    '三级': 60,
    '四级': 85,
  };

  let 风险 = 级别风险[项目.级别];

  // 机构加成
  if (机构.事故率 > 10) 风险 += 15;
  if (机构.感染率 > 5) 风险 += 10;
  if (机构.无证医生 > 0) 风险 += 20;

  return Math.min(100, 风险);
}

