/**
 * 识别黑机构
 */
export function 识别黑机构(机构: 医美机构状态): {
  是黑机构: boolean;
  危险信号: string[];
  风险评分: number;
} {
  const 危险信号: string[] = [];
  let 风险评分 = 0;

  // 机构类型
  if (机构.类型 === '工作室') {
    危险信号.push('场所为工作室，无正规资质');
    风险评分 += 30;
  }

  // 无证医生
  if (机构.无证医生 > 0) {
    危险信号.push(`存在${机构.无证医生}名无证医生`);
    风险评分 += 25;
  }

  // 事故率
  if (机构.事故率 > 20) {
    危险信号.push('事故率极高');
    风险评分 += 20;
  }

  // 死亡率
  if (机构.死亡率 > 5) {
    危险信号.push('有手术死亡记录');
    风险评分 += 30;
  }

  // 宰客程度
  if (机构.宰客程度 > 70) {
    危险信号.push('严重宰客');
    风险评分 += 15;
  }

  // 虚假宣传
  if (机构.虚假宣传 > 60) {
    危险信号.push('虚假宣传严重');
    风险评分 += 15;
  }

  // 水军比例
  if (机构.水军比例 > 60) {
    危险信号.push('好评疑似造假');
    风险评分 += 10;
  }

  // 低好评率
  if (机构.好评率 < 40) {
    危险信号.push('好评率极低');
    风险评分 += 15;
  }

  return {
    是黑机构: 风险评分 >= 50,
    危险信号,
    风险评分: Math.min(100, 风险评分),
  };
}
