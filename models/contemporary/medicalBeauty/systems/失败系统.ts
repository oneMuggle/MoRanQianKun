/**
 * 获取维权选项
 */
export function 获取维权选项(
  失败类型: string
): 维权选项[] {
  return 维权选项列表[失败类型] || 维权选项列表['效果不满意'];
}

/**
 * 执行维权
 */
export function 执行维权(
  state: 整形者核心状态,
  机构: 医美机构状态,
  失败类型: string,
  选择: string
): {
  成功: boolean;
  获得赔偿: number;
  维权后状态: 整形者核心状态;
  维权后机构: 医美机构状态;
  描述: string;
} {
  const 选项列表 = 获取维权选项(失败类型);
  const 选项 = 选项列表.find(o => o.选项 === 选择);

  if (!选项) {
    return {
      成功: false,
      获得赔偿: 0,
      维权后状态: state,
      维权后机构: 机构,
      描述: '无效选择',
    };
  }

  const 成功 = Math.random() < 选项.成功率;
  let 获得赔偿 = 0;

  const 新状态 = 更新整形者状态(state, {
    维权次数: state.维权次数 + 1,
  });

  const 新机构 = 更新机构状态(机构, {
    投诉率: Math.min(100, 机构.投诉率 + (成功 ? 5 : 2)),
  });

  if (成功) {
    // 成功赔偿
    switch (失败类型) {
      case '效果不满意':
        获得赔偿 = 选项.成本 + 5000;
        break;
      case '并发症':
        获得赔偿 = 选项.成本 + 20000;
        break;
      case '毁容':
        获得赔偿 = 选项.成本 + 100000;
        break;
      case '死亡':
        获得赔偿 = 选项.成本 + 500000;
        break;
    }
  }

  return {
    成功,
    获得赔偿,
    维权后状态: 新状态,
    维权后机构: 新机构,
    描述: 成功 
      ? `维权成功，获得赔偿${获得赔偿}元` 
      : `维权失败，${选项.风险.join('，')}`,
  };
}

