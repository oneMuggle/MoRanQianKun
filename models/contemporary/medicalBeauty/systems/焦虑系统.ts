// ==================== 焦虑计算 ====================

/**
 * 计算基础外貌焦虑
 */
export function 计算基础外貌焦虑(
  原生颜值: number,
  性别: '男' | '女',
  年龄: number
): number {
  // 颜值越高，焦虑越低
  let 焦虑 = (10 - 原生颜值) * 8;

  // 年轻人更容易焦虑
  if (年龄 < 25) {
    焦虑 += 10;
  } else if (年龄 > 35) {
    焦虑 -= 5;
  }

  // 女性更容易焦虑
  if (性别 === '女') {
    焦虑 += 10;
  }

  return Math.max(0, Math.min(100, 焦虑));
}

/**
 * 触发焦虑事件
 */
export function 触发焦虑事件(
  state: 整形者核心状态,
  焦虑源: 焦虑触发因素
): 整形者核心状态 {
  let 新焦虑 = state.外貌焦虑;
  let 新自信 = state.自信程度;

  // 焦虑增加
  新焦虑 += 焦虑源.焦虑增量;

  // 自信下降
  新自信 -= Math.round(焦虑源.焦虑增量 / 2);

  // 如果本来就焦虑，增量会放大
  if (state.外貌焦虑 > 60) {
    新焦虑 += Math.round(焦虑源.焦虑增量 * 0.5);
  }

  // 社交媒体影响持久
  if (焦虑源.来源 === '社交媒体') {
    // 后续还会持续影响
    新焦虑 += 5;
  }

  return {
    ...state,
    外貌焦虑: Math.min(100, 新焦虑),
    自信程度: Math.max(0, 新自信),
  };
}

/**
 * 焦虑导致的行为
 */
export function 焦虑驱动行为(
  state: 整形者核心状态
): {
  可能行为: '继续刷整形内容' | '开始咨询' | '下单整形' | '与朋友比较' | '暂时放下';
  紧迫度: number;
} {
  const { 外貌焦虑, 成瘾程度, 整形阶段 } = state;

  // 高焦虑 + 高成瘾 = 高紧迫
  if (外貌焦虑 > 70 || 成瘾程度 > 70) {
    return { 可能行为: '下单整形', 紧迫度: 90 };
  }

  // 中度焦虑
  if (外貌焦虑 > 50) {
    if (整形阶段 === '观望') {
      return { 可能行为: '开始咨询', 紧迫度: 60 };
    }
    return { 可能行为: '继续刷整形内容', 紧迫度: 50 };
  }

  // 低焦虑
  if (外貌焦虑 > 30) {
    return { 可能行为: '与朋友比较', 紧迫度: 30 };
  }

  return { 可能行为: '暂时放下', 紧迫度: 10 };
}

// ==================== 焦虑缓解 ====================

/**
 * 缓解焦虑的行为
 */
export function 缓解焦虑行为(
  state: 整形者核心状态,
  行为: '卸载整形APP' | '和朋友聚会' | '运动健身' | '心理咨询' | '整形成功'
): 整形者核心状态 {
  let 焦虑减少 = 0;
  let 自信增加 = 0;

  switch (行为) {
    case '卸载整形APP':
      焦虑减少 = 15;
      自信增加 = 5;
      break;
    case '和朋友聚会':
      焦虑减少 = 10;
      自信增加 = 8;
      break;
    case '运动健身':
      焦虑减少 = 20;
      自信增加 = 15;
      break;
    case '心理咨询':
      焦虑减少 = 25;
      自信增加 = 10;
      break;
    case '整形成功':
      焦虑减少 = 40;
      自信增加 = 30;
      break;
  }

  return {
    ...state,
    外貌焦虑: Math.max(0, state.外貌焦虑 - 焦虑减少),
    自信程度: Math.min(100, state.自信程度 + 自信增加),
  };
}

// ==================== 颜值评估 ====================

/**
 * 评估当前颜值分数
 */
export function 评估颜值(
  基础颜值: number,
  做过项目: string[],
  项目效果: Record<string, number> = {}
): { 评分: number; 变化: string[] } {
  let 当前评分 = 基础颜值;
  const 变化: string[] = [];

  // 每个项目的效果
  const 默认效果: Record<string, number> = {
    '眼部整形': 1.5,
    '鼻部整形': 2,
    '面部轮廓': 2,
    '胸部整形': 1.5,
    '身体塑形': 1,
    '唇部整形': 0.8,
    '抗衰老': 1,
  };

  for (const 项目 of 做过项目) {
    const 效果 = 项目效果[项目] ?? 默认效果[项目] ?? 1;
    当前评分 += 效果;
    变化.push(`+${项目}: ${效果 > 0 ? '+' : ''}${效果}`);
  }

  return {
    评分: Math.min(10, Math.round(当前评分 * 10) / 10),
    变化,
  };
}

/**
 * 计算与标准的差距
 */
export function 计算与标准差距(
  当前颜值: number,
  性别: '男' | '女'
): { 差距: number; 达标度: number } {
  const 标准分数 = 8; // 社会普遍认为8分以上才是\"漂亮\"
  
  const 差距 = 标准分数 - 当前颜值;
  const 达标度 = Math.round((当前颜值 / 标准分数) * 100);

  return { 差距: Math.max(0, 差距), 达标度: Math.min(100, 达标度) };
}
