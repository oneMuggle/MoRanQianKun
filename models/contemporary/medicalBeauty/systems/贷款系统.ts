// ==================== 贷款产品分析 ====================

export interface 贷款分析结果 {
  产品: 医美贷产品;
  月还款: number;
  总还款: number;
  实际年化: number;
  隐藏费用总额: number;
  套路程度: '正常' | '一般' | '高利息' | '高利贷';
}

/**
 * 分析贷款产品
 */
export function 分析贷款产品(
  产品: 医美贷产品,
  贷款金额: number,
  期数: number
): 贷款分析结果 {
  // 计算月还款（简化计算）
  const 月利率 = 产品.表面利率 / 100 / 12;
  const 月还款 = (贷款金额 * (1 + 月利率 * 期数)) / 期数;
  const 总还款 = 月还款 * 期数;
  
  // 隐藏费用
  const 隐藏费用总额 = 贷款金额 * 0.1; // 简化估算

  // 判断套路程度
  let 套路程度: 套路程度;
  if (产品.实际年化 === 0 || 产品.实际年化 <= 8) {
    套路程度 = '正常';
  } else if (产品.实际年化 <= 20) {
    套路程度 = '一般';
  } else if (产品.实际年化 <= 36) {
    套路程度 = '高利息';
  } else {
    套路程度 = '高利贷';
  }

  return {
    产品,
    月还款: Math.round(月还款),
    总还款: Math.round(总还款),
    实际年化: 产品.实际年化,
    隐藏费用总额,
    套路程度,
  };
}

/**
 * 推荐合适的贷款产品
 */
export function 推荐贷款产品(
  贷款金额: number,
  期数: number,
  消费者类型: '学生党' | '普通白领' | '中产阶层' | '有钱任性' | '网红富婆'
): 贷款分析结果[] {
  const 分析结果 = 医美贷产品列表.map(产品 => 
    分析贷款产品(产品, 贷款金额, 期数)
  );

  // 按实际年化排序
  分析结果.sort((a, b) => a.实际年化 - b.实际年化);

  return 分析结果;
}

// ==================== 还款压力评估 ====================

export type 还款压力等级 = '轻松' | '可承受' | '吃力' | '危险' | '崩溃';

export interface 还款压力评估 {
  等级: 还款压力等级;
  月还款: number;
  月收入: number;
  还款占比: number;
  风险描述: string;
  建议: string;
}

/**
 * 评估还款压力
 */
export function 评估还款压力(
  state: 整形者核心状态
): 还款压力评估 {
  const { 月还款, 消费能力 } = state;

  // 估算月收入
  let 月收入: number;
  switch (消费能力) {
    case '学生党': 月收入 = 2000; break;
    case '普通白领': 月收入 = 8000; break;
    case '中产阶层': 月收入 = 20000; break;
    case '有钱任性': 月收入 = 50000; break;
    case '网红富婆': 月收入 = 100000; break;
  }

  const 还款占比 = Math.round((月还款 / 月收入) * 100);

  let 等级: 还款压力等级;
  let 风险描述: string;
  let 建议: string;

  if (还款占比 < 20) {
    等级 = '轻松';
    风险描述 = '还款压力不大，可以正常生活';
    建议 = '保持正常还款即可';
  } else if (还款占比 < 40) {
    等级 = '可承受';
    风险描述 = '需要节省一些开支，但还能应付';
    建议 = '注意控制其他消费';
  } else if (还款占比 < 60) {
    等级 = '吃力';
    风险描述 = '还款压力大，影响生活质量';
    建议 = '考虑增加收入或提前还款';
  } else if (还款占比 < 80) {
    等级 = '危险';
    风险描述 = '还款压力极大，逾期风险高';
    建议 = '务必寻求家人帮助或协商还款';
  } else {
    等级 = '崩溃';
    风险描述 = '入不敷出，以贷养贷风险极高';
    建议 = '立即停止借贷，寻求专业法律援助';
  }

  return {
    等级,
    月还款,
    月收入,
    还款占比,
    风险描述,
    建议,
  };
}

// ==================== 贷款套路识别 ====================

export interface 套路识别结果 {
  是否套路: boolean;
  套路类型: string[];
  防范建议: string[];
}

/**
 * 识别贷款套路
 */
export function 识别贷款套路(
  产品: 医美贷产品,
  宣传话术: string
): 套路识别结果 {
  const 套路类型: string[] = [];
  const 防范建议: string[] = [];

  // 无息诱惑
  if (产品.表面利率 === 0 && 产品.实际年化 > 0) {
    套路类型.push('0利息陷阱（实际有隐藏费用）');
    防范建议.push('要求书面说明所有费用明细');
  }

  // 低月供诱导
  if (宣传话术.includes('每月只要') || 宣传话术.includes('轻松月付')) {
    套路类型.push('低月供诱导（隐瞒总利息）');
    防范建议.push('计算实际总还款金额');
  }

  // 高实际利率
  if (产品.实际年化 > 36) {
    套路类型.push('高利贷（超过法律保护利率）');
    防范建议.push('拒绝并报警或向金融监管部门投诉');
  }

  // 隐藏费用
  if (产品.隐藏费用.length > 0) {
    套路类型.push(`存在隐藏费用：${产品.隐藏费用.join('、')}`);
    防范建议.push('签约前要求完整费用清单');
  }

  // 不正规平台
  if (!产品.是否正规) {
    套路类型.push('非正规金融平台');
    防范建议.push('选择银行或正规消费金融公司');
  }

  return {
    是否套路: 套路类型.length > 0,
    套路类型,
    防范建议,
  };
}

// ==================== 贷款申请流程 ====================

export interface 贷款申请结果 {
  成功: boolean;
  产品: 医美贷产品;
  批款金额: number;
  实际到账: number;
  月还款: number;
  期数: number;
  总利息: number;
}

/**
 * 模拟申请贷款
 */
export function 申请医美贷(
  state: 整形者核心状态,
  申请金额: number,
  产品名称: string,
  期数: number
): {
  结果: 贷款申请结果;
  新状态: 整形者核心状态;
} {
  const 产品 = 医美贷产品列表.find(p => p.名称 === 产品名称);
  
  if (!产品) {
    return {
      结果: {
        成功: false,
        产品: 医美贷产品列表[0],
        批款金额: 0,
        实际到账: 0,
        月还款: 0,
        期数: 0,
        总利息: 0,
      },
      新状态: state,
    };
  }

  // 实际到账金额（扣除砍头息等）
  const 砍头息 = 产品.是否正规 ? 0 : 申请金额 * 0.1;
  const 实际到账 = 申请金额 - 砍头息;

  // 月利率
  const 月利率 = 产品.实际年化 / 100 / 12;
  const 月还款 = (申请金额 * (1 + 月利率 * 期数)) / 期数;
  const 总利息 = 月还款 * 期数 - 申请金额;

  // 更新状态
  const 新状态 = 申请整形贷(state, 申请金额, 月利率, 期数);

  return {
    结果: {
      成功: true,
      产品,
      批款金额: 申请金额,
      实际到账,
      月还款: Math.round(月还款),
      期数,
      总利息: Math.round(总利息),
    },
    新状态,
  };
}

// ==================== 逾期危机 ====================

export interface 逾期危机评估 {
  是否逾期: boolean;
  逾期天数: number;
  催收强度: '温柔' | '正常' | '频繁' | '暴力';
  影响: {
    征信: boolean;
    通讯录: boolean;
    工作: boolean;
  };
  建议: string;
}

/**
 * 评估逾期危机
 */
export function 评估逾期危机(
  state: 整形者核心状态,
  逾期天数: number = 0
): 逾期危机评估 {
  const { 贷款金额, 月还款, 消费能力 } = state;

  if (逾期天数 === 0) {
    return {
      是否逾期: false,
      逾期天数: 0,
      催收强度: '正常',
      影响: { 征信: false, 通讯录: false, 工作: false },
      建议: '保持正常还款',
    };
  }

  // 催收强度
  let 催收强度: 逾期危机评估['催收强度'] = '温柔';
  if (逾期天数 > 30) 催收强度 = '频繁';
  else if (逾期天数 > 7) 催收强度 = '正常';

  // 是否暴力催收
  if (逾期天数 > 60 && !state.贷款金额) {
    催收强度 = '暴力';
  }

  // 通讯录被爆
  const 通讯录危险 = 逾期天数 > 7 && !state.贷款金额;

  return {
    是否逾期: true,
    逾期天数,
    催收强度,
    影响: {
      征信: 逾期天数 > 30,
      通讯录: 通讯录危险,
      工作: 逾期天数 > 90,
    },
    建议: 逾期天数 > 30 ? '建议寻求法律援助' : '尽量协商还款',
  };
}
