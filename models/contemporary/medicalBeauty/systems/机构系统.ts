/**
 * 获取机构详细信息
 */
export function 获取机构详细信息(机构: 医美机构状态): 机构详细信息 {
  const 安全评估 = 评估机构安全性(机构);
  
  const 风险提示: string[] = [];
  if (机构.无证医生 > 0) 风险提示.push('存在无证医生');
  if (机构.事故率 > 10) 风险提示.push('事故率偏高');
  if (机构.死亡率 > 5) 风险提示.push('有死亡案例');
  if (机构.感染率 > 5) 风险提示.push('感染率偏高');
  if (机构.虚假宣传 > 50) 风险提示.push('存在虚假宣传');
  if (机构.水军比例 > 50) 风险提示.push('好评疑似造假');

  const 适合项目: string[] = [];
  if (机构.档次 === '顶级' || 机构.档次 === '高端') {
    适合项目.push('面部轮廓', '胸部整形', '身体塑形', '抗衰老');
  }
  if (机构.档次 !== '黑机构') {
    适合项目.push('眼部整形', '鼻部整形', '唇部整形');
  }
  if (机构.档次 === '顶级') {
    适合项目.push('私密整形');
  }

  return {
    基本信息: {
      名称: 机构.机构名称,
      类型: 机构.类型,
      档次: 机构.档次,
      地址: '某市中心商务区', // 简化，实际应该关联位置
    },
    资质: {
      资质证书: 机构.资质证书,
      医生数量: 机构.医生数量,
      主任医师: 机构.主任医师,
      副主任医师: 0,
      主治医师: 0,
      执业医师: 机构.医生数量 - 机构.主任医师 - 机构.无证医生,
      无证医生: 机构.无证医生,
    },
    安全: {
      事故率: 机构.事故率,
      死亡率: 机构.死亡率,
      感染率: 机构.感染率,
      投诉率: 机构.投诉率,
      安全评分: 安全评估.安全评分,
    },
    经营: {
      宰客程度: 机构.宰客程度,
      虚假宣传: 机构.虚假宣传,
      渠道抽成: 机构.渠道抽成,
      好评率: 机构.好评率,
      真实好评率: 机构.真实好评率,
      水军比例: 机构.水军比例,
    },
    风险提示,
    适合项目,
  };
}

// ==================== 机构评级 ====================

export type 机构评级 = 'A+' | 'A' | 'B+' | 'B' | 'C' | 'D';

export function 评级机构(机构: 医美机构状态): {
  评级: 机构评级;
  综合评分: number;
  安全评分: number;
  服务评分: number;
  价格评分: number;
} {
  const 安全评估 = 评估机构安全性(机构);
  
  // 服务评分（基于好评率和投诉率）
  const 服务评分 = Math.round(
    机构.好评率 * 0.7 +
    (100 - 机构.投诉率) * 0.3
  );

  // 价格评分（基于宰客程度和渠道抽成）
  const 价格评分 = Math.round(
    (100 - 机构.宰客程度) * 0.6 +
    (100 - 机构.渠道抽成) * 0.4
  );

  // 综合评分
  const 综合评分 = Math.round(
    安全评估.安全评分 * 0.5 +
    服务评分 * 0.3 +
    价格评分 * 0.2
  );

  // 评级
  let 评级: 机构评级;
  if (综合评分 >= 90) 评级 = 'A+';
  else if (综合评分 >= 80) 评级 = 'A';
  else if (综合评分 >= 70) 评级 = 'B+';
  else if (综合评分 >= 60) 评级 = 'B';
  else if (综合评分 >= 40) 评级 = 'C';
  else 评级 = 'D';

  return {
    评级,
    综合评分,
    安全评分: 安全评估.安全评分,
    服务评分,
    价格评分,
  };
}

// ==================== 机构分布统计 ====================

export interface 机构分布统计 {
  总数: number;
  各档次数量: Record<机构档次, number>;
  各类型数量: Record<机构类型, number>;
  平均安全评分: number;
  平均好评率: number;
  黑机构比例: number;
}

/**
 * 统计机构分布
 */
export function 统计机构分布(所有机构: 医美机构状态[]): 机构分布统计 {
  const 各档次数量: Record<string, number> = {
    '顶级': 0,
    '高端': 0,
    '中端': 0,
    '低端': 0,
    '黑机构': 0,
  };

  const 各类型数量: Record<string, number> = {
    '三甲医院整形科': 0,
    '大型连锁机构': 0,
    '专科医院': 0,
    '美容诊所': 0,
    '工作室': 0,
    '渠道医院': 0,
  };

  let 总安全评分 = 0;
  let 总好评率 = 0;
  let 黑机构数 = 0;

  for (const 机构 of 所有机构) {
    各档次数量[机构.档次]++;
    各类型数量[机构.类型]++;
    总安全评分 += 评估机构安全性(机构).安全评分;
    总好评率 += 机构.好评率;
    if (机构.档次 === '黑机构') 黑机构数++;
  }

  return {
    总数: 所有机构.length,
    各档次数量: 各档次数量 as Record<机构档次, number>,
    各类型数量: 各类型数量 as Record<机构类型, number>,
    平均安全评分: Math.round(总安全评分 / 所有机构.length),
    平均好评率: Math.round(总好评率 / 所有机构.length),
    黑机构比例: Math.round((黑机构数 / 所有机构.length) * 100),
  };
}

