/**
 * 模拟机构背景调查
 */
export function 背景调查机构(机构: 医美机构状态): 背景调查结果 {
  const 机构名称 = 机构.机构名称;
  
  // 模拟网络评价生成
  const 正面: string[] = [];
  const 负面: string[] = [];
  const 中性: string[] = [];

  if (机构.好评率 > 70) {
    正面.push('环境好', '服务态度不错', '效果还可以');
  }
  if (机构.真实好评率 > 50) {
    正面.push('网友评价真实');
  }
  if (机构.宰客程度 > 60) {
    负面.push('价格虚高', '咨询师推销厉害');
  }
  if (机构.事故率 > 10) {
    负面.push('有失败案例', '术后出问题');
  }
  if (机构.水军比例 > 40) {
    负面.push('怀疑水军刷好评');
  }
  if (机构.虚假宣传 > 50) {
    负面.push('宣传与实际不符');
  }

  // 违规记录
  const 违规记录: string[] = [];
  if (机构.无证医生 > 0) {
    违规记录.push('使用无证医生');
  }
  if (机构.虚假宣传 > 60) {
    违规记录.push('虚假宣传');
  }

  // 危机预警
  const 危机预警: string[] = [];
  if (机构.死亡率 > 5) {
    危机预警.push('有死亡案例，需高度警惕');
  }
  if (机构.宰客程度 > 80) {
    危机预警.push('宰客严重，曾被投诉');
  }
  if (违规记录.length > 0) {
    危机预警.push('存在违规行为');
  }

  // 综合建议
  let 综合建议: string;
  if (机构.档次 === '黑机构') {
    综合建议 = '强烈不建议选择此类机构';
  } else if (危机预警.length > 2) {
    综合建议 = '存在较多风险，建议谨慎';
  } else if (机构.真实好评率 > 60) {
    综合建议 = '口碑较好，可以考虑';
  } else {
    综合建议 = '建议多做比较，实地考察';
  }

  return {
    机构名称,
    网络评价: { 正面, 负面, 中性 },
    资质核查: {
      有无证: 机构.无证医生 === 0,
      执业信息: 机构.无证医生 === 0 ? '资质合规' : `存在${机构.无证医生}名无证医生`,
      违规记录,
    },
    舆论分析: {
      真实好评率: 机构.真实好评率,
      水军比例: 机构.水军比例,
      危机预警,
    },
    综合建议,
  };
}
