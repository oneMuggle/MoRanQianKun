// ==================== 机构比较 ====================

export interface 机构比较结果 {
  机构: 医美机构状态;
  综合评分: number;
  安全评分: number;
  性价比评分: number;
  推荐指数: number;
  优势: string[];
  劣势: string[];
}

/**
 * 比较多个机构
 */
export function 比较机构(...机构列表: 医美机构状态[]): 机构比较结果[] {
  return 机构列表.map(机构 => {
    const 安全评估 = 评估机构安全性(机构);
    
    // 计算性价比（考虑价格和安全）
    const 性价比评分 = Math.round(
      (100 - 机构.宰客程度) * 0.4 + 
      (100 - 机构.渠道抽成) * 0.3 +
      (100 - 机构.虚假宣传) * 0.3
    );

    // 综合评分
    const 综合评分 = Math.round(
      安全评估.安全评分 * 0.5 +
      性价比评分 * 0.3 +
      机构.好评率 * 0.2
    );

    // 优势
    const 优势: string[] = [];
    if (安全评估.安全评分 > 80) 优势.push('安全性高');
    if (机构.主任医师 > 0) 优势.push('有主任医师');
    if (机构.好评率 > 80) 优势.push('口碑好');
    if (机构.宰客程度 < 30) 优势.push('价格合理');
    if (机构.虚假宣传 < 20) 优势.push('宣传真实');

    // 劣势
    const 劣势: string[] = [];
    if (机构.无证医生 > 0) 劣势.push('有无证医生');
    if (机构.事故率 > 10) 劣势.push('事故率偏高');
    if (机构.死亡率 > 5) 劣势.push('有死亡案例');
    if (机构.宰客程度 > 60) 劣势.push('宰客严重');
    if (机构.水军比例 > 50) 劣势.push('好评疑似造假');

    return {
      机构,
      综合评分,
      安全评分: 安全评估.安全评分,
      性价比评分,
      推荐指数: Math.round((综合评分 + 安全评估.安全评分) / 2),
      优势,
      劣势,
    };
  }).sort((a, b) => b.推荐指数 - a.推荐指数);
}

// ==================== 机构搜索 ====================

export interface 机构搜索条件 {
  类型?: 机构类型;
  档次?: 机构档次;
  最高价格?: number;
  最大事故率?: number;
  最大距离?: number;  // 公里
  排序方式?: '综合' | '安全' | '价格' | '好评';
}

/**
 * 筛选符合条件的机构
 */
export function 筛选机构(
  所有机构: 医美机构状态[],
  条件: 机构搜索条件
): 医美机构状态[] {
  return 所有机构.filter(机构 => {
    // 类型筛选
    if (条件.类型 && 机构.类型 !== 条件.类型) {
      return false;
    }

    // 档次筛选
    if (条件.档次 && 机构.档次 !== 条件.档次) {
      return false;
    }

    // 事故率筛选
    if (条件.最大事故率 && 机构.事故率 > 条件.最大事故率) {
      return false;
    }

    return true;
  }).map(机构 => ({ 机构, 评分: 评估机构安全性(机构).安全评分 }))
    .filter(item => 条件.最大事故率 ? item.评分 >= (100 - 条件.最大事故率) : true)
    .map(item => item.机构);
}

