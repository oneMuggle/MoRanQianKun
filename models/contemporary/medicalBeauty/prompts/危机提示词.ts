/**
 * 危机场景提示词
 * 
 * 生成手术失败、维权纠纷等危机场景的提示词
 */

import type { 整形危机, 整形者核心状态, 医美机构状态 } from '../types';

/**
 * 生成危机场景提示词
 */
export function 生成危机提示词(params: {
  危机类型: 整形危机;
  整形者: 整形者核心状态;
  机构: 医美机构状态;
  详细描述: string;
}): string {
  const { 危机类型, 整形者, 机构, 详细描述 } = params;

  let 危机级别 = '';
  let 整形者态度 = '';
  let 机构态度 = '';
  let 监管部门态度 = '';

  switch (危机类型) {
    case '手术失败':
      危机级别 = '中';
      整形者态度 = '愤怒、后悔、要求赔偿';
      机构态度 = '推卸责任、拖延时间、建议修复';
      监管部门态度 = '需要投诉才会介入';
      break;
    case '并发症':
      危机级别 = '中高';
      整形者态度 = '恐慌、担心、要求治疗';
      机构态度 = '推卸、延误、可能跑路';
      监管部门态度 = '需要医疗鉴定';
      break;
    case '感染':
      危机级别 = '高';
      整形者态度 = '恐惧、愤怒、要求解释';
      机构态度 = '推卸、甩锅、威胁封口';
      监管部门态度 = '可能吊销执照';
      break;
    case '毁容':
      危机级别 = '极高';
      整形者态度 = '崩溃、绝望、可能抑郁';
      机构态度 = '推卸、威胁、可能跑路';
      监管部门态度 = '涉及刑事犯罪';
      break;
    case '死亡':
      危机级别 = '极';
      整形者态度 = '家属极度悲痛、愤怒';
      机构态度 = '可能转移责任、毁灭证据';
      监管部门态度 = '刑事调查';
      break;
    case '贷款纠纷':
      危机级别 = '中';
      整形者态度 = '无力还款、焦虑、可能被催收';
      机构态度 = '不介入、与己无关';
      监管部门态度 = '需要提供证据';
      break;
    case '隐私泄露':
      危机级别 = '中高';
      整形者态度 = '羞耻、愤怒、担心传播';
      机构态度 = '推卸、删除评论';
      监管部门态度 = '涉及侵犯隐私权';
      break;
    case '曝光':
      危机级别 = '中';
      整形者态度 = '羞耻、尴尬、担心影响';
      机构态度 = '可能以此要挟';
      监管部门态度 = '无直接关联';
      break;
  }

  return `【系统角色】
你是${危机类型}危机场景。

【危机详情】
危机类型：${危机类型}
危机级别：${危机级别}
${详细描述}

【整形者信息】
- 昵称：${整形者.昵称}
- 性别：${整形者.性别}
- 年龄：${整形者.年龄}岁
- 整形次数：${整形者.整形次数}
- 累计花费：${整形者.累计花费}元
- 贷款金额：${整形者.贷款金额}元

【机构信息】
- 机构名称：${机构.机构名称}
- 机构类型：${机构.类型}
- 机构档次：${机构.档次}
- 机构宰客程度：${机构.宰客程度}/100
- 机构虚假宣传：${机构.虚假宣传}/100

【各方态度】
- 整形者：${整形者态度}
- 机构：${机构态度}
- 监管部门：${监管部门态度}

【回复要求】
- 体现危机的严重性
- 体现维权的困难
- 体现医美行业的阴暗面
- 暧昧内容回避
- 留有后续空间`;
}

/**
 * 生成维权场景提示词
 */
export function 生成维权提示词(params: {
  失败类型: string;
  整形者: 整形者核心状态;
  机构: 医美机构状态;
  维权方式: string;
}): string {
  const { 失败类型, 整形者, 机构, 维权方式 } = params;

  return `【特殊场景】
维权纠纷场景

【事件概述】
${整形者.昵称}在${机构.机构名称}进行${失败类型 === '效果不满意' ? '整形手术' : '手术'}后出现问题。

【整形者诉求】
- 要求机构承担责任
- 要求赔偿损失
- ${失败类型 === '毁容' || 失败类型 === '死亡' ? '要求追究刑事责任' : '寻求合理解决方案'}

【机构态度】
- ${机构.宰客程度 > 50 ? '推卸责任、百般抵赖' : '配合协商但赔偿有限'}
- ${机构.虚假宣传 > 50 ? '存在虚假宣传行为' : '宣传相对规范'}

【维权方式】
${维权方式}

【可选应对】
1. 搜集证据（病历、照片、录音）
2. 咨询律师
3. 向消协投诉
4. 向卫健委举报
5. 通过媒体曝光
6. 直接诉讼

【回复要求】
- 体现维权过程的艰辛
- 体现机构与消费者的不对等关系
- 暧昧内容回避`;
}
