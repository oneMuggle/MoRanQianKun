// 泄露受害者状态
export interface 泄露受害者状态 {
  ID: string;
  化名: string;
  本名?: string;
  性别: '男' | '女';
  年龄: number;
  职业: 受害者身份;

  // 泄露信息
  泄露内容类型: 泄露类型[];
  泄露源头: 泄露源头;
  泄露时间?: string;

  // 传播状态
  当前传播阶段: 传播阶段;
  当前传播范围: number;      // 估算传播人数
  主要传播渠道: 传播渠道[];
  热搜排名?: number;         // 如果上了热搜

  // 心理状态
  心理创伤程度: number;      // 0-100
  羞耻度: number;            // 0-100
  愤怒度: number;            // 0-100
  恐惧度: number;            // 0-100
  后悔度: number;            // 0-100
  抑郁程度: number;          // 0-100

  // 危机公关
  危机公关状态: 危机公关状态;
  已删帖数量: number;
  律师函数量: number;
  报警状态: boolean;

  // 影响评估
  名声损失: number;          // 0-100
  工作影响: number;          // 0-100
  感情影响: number;          // 0-100
  经济损失: number;          // 预估经济损失
  关系影响列表: string[];     // 受影响的关系ID

  // 历史
  之前是否有过泄露: boolean;
  之前泄露经历次数: number;
}

// 泄露事件状态
export interface 泄露事件状态 {
  事件ID: string;
  泄露类型: 泄露类型;
  内容描述: string;
  源头角色ID: string;
  
  // 传播状态
  传播阶段: 传播阶段;
  当前传播范围: number;
  传播渠道: 传播渠道[];
  首次发现时间: string;
  开始传播时间?: string;

  // 证据
  证据状态: '有完整证据' | '部分证据' | '无证据' | '证据已销毁';
  截图保存数量: number;
  证据可信度: number;        // 0-100

  // 危机公关
  危机公关状态: 危机公关状态;
  处理进程: string;
  已删除链接数: number;
  仍在传播链接数: number;

  // 受害者状态
  受害者ID: string;
  对受害者影响: {
    名声损失: number;
    心理创伤: number;
    经济损失: number;
    关系影响: string[];
  };

  // 参与者
  传播者ID: string[];        // 已知的传播者
  获利者ID: string[];        // 借此获利的人
}

// 传播链状态
export interface 传播链状态 {
  链ID: string;
  事件ID: string;

  // 传播路径
  传播路径: {
    传播者ID: string;
    传播渠道: 传播渠道;
    传播时间: string;
    传播范围: number;        // 传播给了多少人
    是否有获利: boolean;    // 是否借此获利
  }[];

  // 关键节点
  关键传播节点: {
    节点ID: string;
    节点名称: string;
    传播量: number;
    影响范围: '全网' | '某个平台' | '某个地区' | '某个群体';
  }[];

  // 裂变分析
  裂变次数: number;          // 被转发了多少次
  传播速度: number;          // 0-100
}

