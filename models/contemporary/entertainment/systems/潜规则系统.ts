/**
 * 更新状态
 */
function 更新状态应对后果(
  对象: 艺人核心状态 | 练习生核心状态,
  后果: 潜规则后果,
  应对方式: 潜规则应对,
  规则类型: 潜规则类型
): typeof 对象 {
  if ('生涯阶段' in 对象) {
    // 艺人的情况
    return {
      ...对象,
      心理创伤度: Math.max(0, Math.min(100, 对象.心理创伤度 + 后果.长期影响.心理创伤变化)),
      职业倦怠度: Math.max(0, Math.min(100, 对象.职业倦怠度 + 后果.长期影响.职业倦怠变化)),
      业内口碑: Math.max(0, Math.min(100, 对象.业内口碑 + 后果.长期影响.业内口碑变化)),
      公众形象: Math.max(0, Math.min(100, 对象.公众形象 + 后果.长期影响.公众形象变化)),
      潜规则程度: 获取潜规则程度变化(应对方式, 对象.潜规则程度),
      潜规则类型经历: [...对象.潜规则类型经历, 规则类型],
      潜规则应对历史: [...对象.潜规则应对历史, 应对方式],
    } as typeof 对象;
  } else {
    // 练习生的情况
    return {
      ...对象,
      潜规则压力程度: Math.max(0, Math.min(100, 对象.潜规则压力程度 + 后果.长期影响.心理创伤变化)),
      潜规则暗示次数: 对象.潜规则暗示次数 + 1,
    } as typeof 对象;
  }
}

/**
 * 获取潜规则程度变化
 */
function 获取潜规则程度变化(应对: 潜规则应对, 当前: 潜规则程度): 潜规则程度 {
  const 程度序列: 潜规则程度[] = ['无', '暗示', '轻度', '中度', '重度', '受害'];

  if (应对 === '接受') {
    const 当前索引 = 程度序列.indexOf(当前);
    return 程度序列[Math.min(程度序列.length - 1, 当前索引 + 1)];
  }
  return 当前;
}

/**
 * 获取规则类型描述
 */
function 获取规则类型描述(type: 潜规则类型): string {
  const 描述表: Record<潜规则类型, string> = {
    '资源交换': '资源交换',
    '陪酒': '陪酒',
    '陪玩': '陪玩',
    '亲密接触': '亲密接触',
    '录音录像': '录音录像',
    '情人关系': '情人关系',
  };
  return 描述表[type] || type;
}
