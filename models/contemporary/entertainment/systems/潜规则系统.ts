/**
 * 潜规则系统
 * 
 * 负责娱乐圈潜规则的触发、应对、后果处理
 */

import type { 艺人核心状态 } from '../states/艺人状态';
import type { 练习生核心状态 } from '../states/练习生状态';
import type {
  潜规则类型,
  潜规则程度,
  潜规则应对,
  潜规则应对 as 潜规则应对类型,
} from '../types';

/**
 * 潜规则触发条件
 */
export interface 潜规则触发条件 {
  对象身份: '练习生' | '新人艺人' | '成熟艺人' | '顶流明星' | '过气艺人';
  对方身份: '投资人' | '导演' | '制片人' | '高层' | '经纪人' | '前辈';
  场合: '酒局' | '包厢' | '私下约见' | '剧组' | '酒店' | '其他私下场合';
  对方权力大小: number;  // 0-100，对象对对方的依赖程度
}

/**
 * 潜规则触发概率
 */
export function 计算潜规则触发概率(
  条件: 潜规则触发条件
): number {
  const 身份风险表: Record<string, number> = {
    '练习生': 70,
    '新人艺人': 50,
    '成熟艺人': 25,
    '顶流明星': 10,
    '过气艺人': 60,
  };

  const 场合风险表: Record<string, number> = {
    '酒局': 30,
    '包厢': 35,
    '私下约见': 40,
    '剧组': 15,
    '酒店': 45,
    '其他私下场合': 25,
  };

  const 身份风险 = 身份风险表[条件.对象身份] || 30;
  const 场合风险 = 场合风险表[条件.场合] || 20;
  const 权力依赖 = 条件.对方权力大小;

  // 综合风险 = 身份风险 * 0.4 + 场合风险 * 0.3 + 权力依赖 * 0.3
  return Math.min(100, Math.round(身份风险 * 0.4 + 场合风险 * 0.3 + 权力依赖 * 0.3));
}

/**
 * 潜规则后果
 */
export interface 潜规则后果 {
  短期结果: '获得资源' | '失去机会' | '保持关系' | '关系恶化' | '不确定';
  长期影响: {
    心理创伤变化: number;     // 正数表示增加，负数表示减少
    职业倦怠变化: number;
    业内口碑变化: number;
    公众形象变化: number;
  };
  资源变化: number;           // 获得或失去的资源机会
  后续影响描述: string;
}

/**
 * 应对潜规则
 */
export function 应对潜规则(
  对象: 艺人核心状态 | 练习生核心状态,
  规则类型: 潜规则类型,
  应对方式: 潜规则应对类型,
  对方权力大小: number = 50
): { 新状态: typeof 对象; 后果: 潜规则后果 } {
  let 后果: 潜规则后果;

  switch (应对方式) {
    case '接受':
      后果 = 处理接受后果(对象, 规则类型, 对方权力大小);
      break;
    case '拒绝':
      后果 = 处理拒绝后果(对象, 规则类型, 对方权力大小);
      break;
    case '周旋':
      后果 = 处理周旋后果(对象, 规则类型, 对方权力大小);
      break;
    case '反抗':
      后果 = 处理反抗后果(对象, 规则类型, 对方权力大小);
      break;
    case '交易':
      后果 = 处理交易后果(对象, 规则类型, 对方权力大小);
      break;
    case '转嫁':
      后果 = 处理转嫁后果(对象, 规则类型, 对方权力大小);
      break;
    default:
      后果 = {
        短期结果: '不确定',
        长期影响: { 心理创伤变化: 0, 职业倦怠变化: 0, 业内口碑变化: 0, 公众形象变化: 0 },
        资源变化: 0,
        后续影响描述: '无法确定的复杂后果',
      };
  }

  // 根据后果更新对象状态
  const 新状态 = 更新状态应对后果(对象, 后果, 应对方式, 规则类型);

  return { 新状态, 后果 };
}

/**
 * 处理接受后果
 */
function 处理接受后果(
  对象: 艺人核心状态 | 练习生核心状态,
  规则类型: 潜规则类型,
  对方权力大小: number
): 潜规则后果 {
  const 资源获得 = Math.round((对方权力大小 / 100) * 30);
  const 心理创伤 = Math.round(对方权力大小 / 100 * 20);

  return {
    短期结果: '获得资源',
    长期影响: {
      心理创伤变化: 心理创伤,
      职业倦怠变化: 10,
      业内口碑变化: 5,
      公众形象变化: 0,
    },
    资源变化: 资源获得,
    后续影响描述: `${获取规则类型描述(规则类型)}に応じて、要求が越来越多くなる可能性があります。この関係を維持する必要があります。`,
  };
}

/**
 * 处理拒绝后果
 */
function 处理拒绝后果(
  对象: 艺人核心状态 | 练习生核心状态,
  规则类型: 潜规则类型,
  对方权力大小: number
): 潜规则后果 {
  const 机会失去 = Math.round((对方权力大小 / 100) * 40);
  const 心理创伤 = -5;  // 拒绝可能带来一定的释放感

  return {
    短期结果: '失去机会',
    长期影响: {
      心理创伤变化: 心理创伤,
      职业倦怠变化: 5,
      业内口碑变化: -10,
      公众形象变化: 0,
    },
    资源变化: -机会失去,
    后续影响描述: '拒绝可能导致对方不满，未来的机会可能会受到影响。但如果坚持底线，长期来看可能获得尊重。',
  };
}

/**
 * 处理周旋后果
 */
function 处理周旋后果(
  对象: 艺人核心状态 | 练习生核心状态,
  规则类型: 潜规则类型,
  对方权力大小: number
): 潜规则后果 {
  const 周旋成功率 = 50 + (100 - 对方权力大小) / 2;

  if (周旋成功率 > 60) {
    return {
      短期结果: '保持关系',
      长期影响: {
        心理创伤变化: 5,
        职业倦怠变化: 3,
        业内口碑变化: 5,
        公众形象变化: 0,
      },
      资源变化: 5,
      后续影响描述: '成功周旋，保持了关系且没有得罪对方。但对方可能仍在观察。',
    };
  } else {
    return {
      短期结果: '关系恶化',
      长期影响: {
        心理创伤变化: 10,
        职业倦怠变化: 8,
        业内口碑变化: -5,
        公众形象变化: 0,
      },
      资源变化: -15,
      后续影响描述: '周旋失败，对方可能认为你在"装"，关系可能恶化。',
    };
  }
}

/**
 * 处理反抗后果
 */
function 处理反抗后果(
  对象: 艺人核心状态 | 练习生核心状态,
  规则类型: 潜规则类型,
  对方权力大小: number
): 潜规则后果 {
  const 风险 = 对方权力大小 > 70 ? '高风险' : 对方权力大小 > 40 ? '中风险' : '低风险';

  return {
    短期结果: '关系恶化',
    长期影响: {
      心理创伤变化: -10,  // 反抗可能带来一定的释放感
      职业倦怠变化: 15,
      业内口碑变化: 风险 === '低风险' ? 15 : -15,
      公众形象变化: 风险 === '低风险' ? 10 : -5,
    },
    资源变化: 风险 === '低风险' ? 10 : -30,
    后续影响描述: `反抗${风险}。${风险 === '低风险' ? '如果成功，可能获得公众支持。' : '如果对方权力很大，可能遭到封杀或报复。'}选择反抗需要证据和靠山。`,
  };
}

