/**
 * 粉丝行为影响计算
 */
export function 计算粉丝行为影响(
  粉丝状态: 粉丝状态,
  行为: keyof 粉丝行为影响['正面行为'] | keyof 粉丝行为影响['负面行为']
): { 艺人变化: Partial<艺人核心状态>; 粉丝变化: Partial<粉丝状态> } {
  const 影响值 = 正面行为影响表[行为] || 负面行为影响表[行为];
  
  if (!影响值) {
    return { 艺人变化: {}, 粉丝变化: {} };
  }

  // 根据粉丝疯狂程度放大影响
  const 放大系数 = 1 + (粉丝状态.疯狂程度 / 100);

  if (行为 in 正面行为影响表) {
    const 正面影响 = 影响值 as 粉丝行为影响['正面行为']['打投'];
    return {
      艺人变化: {
        粉丝数量: (正面影响 as any).艺人热度 || 0,
        公众形象: (正面影响 as any).艺人形象 || 0,
      } as Partial<艺人核心状态>,
      粉丝变化: {
        投入金额: 行为 === '氪金' ? (正面影响 as any).艺人收入 || 0 : 0,
        疯狂程度: 5,
      } as Partial<粉丝状态>,
    };
  } else {
    const 负面影响 = 影响值 as 粉丝行为影响['负面行为']['攻击其他艺人'];
    return {
      艺人变化: {
        公众形象: (负面影响 as any).艺人形象 || 0,
        负面新闻数: 1,
      } as Partial<艺人核心状态>,
      粉丝变化: {
        疯狂程度: (行为 === '私生行为' || 行为 === '过度跟踪') ? 20 : 10,
      } as Partial<粉丝状态>,
    };
  }
}

/**
 * 粉丝类型升级判断
 */
export function 是否可以升级为粉头(状态: 粉丝状态): boolean {
  return (
    状态.粉丝类型 === '大粉' &&
    状态.投入金额 >= 10000 &&
    状态.投入时间 >= 500 &&
    状态.疯狂程度 >= 60
  );
}

/**
 * 是否可能转为私生
 */
export function 是否可能转为私生(状态: 粉丝状态): boolean {
  return (
    (状态.粉丝类型 === '大粉' || 状态.粉丝类型 === '粉头') &&
    状态.疯狂程度 >= 70 &&
    状态.与偶像关系认知 !== '普通粉丝' &&
    (状态.与偶像关系认知 === '老婆/老公' || 状态.与偶像关系认知 === '命运伴侣')
  );
}

/**
 * 是否可能转为anti
 */
export function 是否可能转为Anti(状态: 粉丝状态): boolean {
  return (
    (状态.粉丝类型 === '大粉' || 状态.粉丝类型 === '粉头') &&
    状态.疯狂程度 >= 50 &&
    状态.幻想程度 < 30
  );
}

/**
 * 爬墙概率计算
 */
export function 计算爬墙概率(状态: 粉丝状态): number {
  const 基础概率 = 20;
  const 追星时长因子 = 状态.追星时长 > 3 ? -10 : 0;  // 追得越久越不容易爬墙
  const 疯狂程度因子 = (100 - 状态.疯狂程度) * 0.2;
  const 现实社交因子 = 状态.现实社交能力 > 70 ? 15 : 0;
  const 依赖度因子 = 状态.追星依赖度 > 70 ? -10 : 0;

  return Math.max(0, Math.min(100, 基础概率 + 追星时长因子 + 疯狂程度因子 + 现实社交因子 + 依赖度因子));
}

