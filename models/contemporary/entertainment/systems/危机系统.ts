/**
 * 演化危机阶段
 */
export function 演化危机阶段(危机: 艺人危机): 艺人危机 {
  const 配置 = 危机演化配置表[危机.当前阶段];
  const 讨论量变化 = 配置.讨论量增长;
  const 是否反转 = Math.random() * 100 < 配置.反转概率;

  let 新阶段: 危机阶段;
  
  if (是否反转) {
    新阶段 = '舆论反转';
  } else {
    const 阶段顺序: 危机阶段[] = ['苗头', '发酵', '爆发', '热搜', '平息'];
    const 当前索引 = 阶段顺序.indexOf(危机.当前阶段 as any);
    
    if (当前索引 < 阶段顺序.length - 1) {
      新阶段 = 阶段顺序[当前索引 + 1];
    } else {
      新阶段 = '平息';
    }
  }

  return {
    ...危机,
    当前阶段: 新阶段,
    讨论量: Math.max(0, 危机.讨论量 + 讨论量变化),
    热搜排名: 新阶段 === '热搜' ? Math.floor(Math.random() * 20) + 1 : 危机.热搜排名,
  };
}

/**
 * 应用危机影响
 */
export function 应用危机影响(
  状态: 艺人核心状态,
  处理结果: 危机处理效果
): 艺人核心状态 {
  const 粉丝流失 = Math.round(状态.粉丝数量 * (处理结果.长期影响.粉丝流失率 / 100));

  return {
    ...状态,
    粉丝数量: Math.max(0, 状态.粉丝数量 - 粉丝流失),
    公众形象: Math.max(0, Math.min(100, 状态.公众形象 + 处理结果.长期影响.公众形象变化)),
    舆论敏感度: Math.min(100, 状态.舆论敏感度 + 10),
  };
}

/**
 * 生成危机报告
 */
export function 生成危机报告(危机: 艺人危机, 状态: 艺人核心状态): string {
  const 配置 = 危机演化配置表[危机.当前阶段];

  let 报告 = `【危机事件报告】\n\n`;
  报告 += `危机ID：${危机.危机ID}\n`;
  报告 += `危机类型：${危机.危机类型} - ${危机类型描述表[危机.危机类型]}\n`;
  报告 += `爆发时间：${危机.爆发时间}\n`;
  报告 += `当前阶段：${危机.当前阶段}\n`;
  报告 += `阶段持续：${配置.持续时间}\n`;
  报告 += `当前讨论量：${危机.讨论量.toLocaleString()}\n`;
  
  if (危机.热搜排名) {
    报告 += `热搜排名：第${危机.热搜排名}名\n`;
  }
  
  if (危机.处理方式) {
    报告 += `处理方式：${危机.处理方式}\n`;
    const 效果 = 评估危机处理效果(危机, 危机.处理方式);
    报告 += `平息概率：${效果.成功概率}%\n`;
  }

  报告 += `\n【对事业影响】\n`;
  报告 += `预估粉丝流失：${危机.对事业影响.粉丝流失}\n`;
  报告 += `预估商务损失：${危机.对事业影响.商务损失.toLocaleString()}元\n`;
  报告 += `资源损失：${危机.对事业影响.资源损失}\n`;

  return 报告;
}
