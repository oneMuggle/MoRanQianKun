/**
 * 粉丝互动场景配置
 */
export interface 粉丝互动场景配置 {
  场景类型: '粉丝见面会' | '私生跟踪' | '机场接送' | '后台互动';
  粉丝类型?: 粉丝类型;
  风险等级?: number;
}

/**
 * 生成粉丝见面会场景描述
 */
export function 生成粉丝见面会场景描述(
  艺人: 艺人核心状态,
  粉丝: 粉丝核心状态
): string {
  const 活动类型 = ['签售会', '握手会', '合照会', '生日会'][Math.floor(Math.random() * 4)];

  let 描述 = `[粉丝见面会 - 酒店会议厅]\n\n`;
  描述 += `${艺人.艺名}的${活动类型}正在进行...\n\n`;
  描述 += `【活动信息】\n`;
  描述 += `- 活动类型：${活动类型}\n`;
  描述 += `- 参与粉丝：约${Math.floor(Math.random() * 200) + 100}人\n`;
  描述 += `- 排队时间：约${Math.floor(Math.random() * 3) + 1}小时\n\n`;
  描述 += `【粉丝信息】\n`;
  描述 += `- ${粉丝.昵称}\n`;
  描述 += `- 粉丝类型：${粉丝.粉丝类型}\n`;
  描述 += `- 追星时长：${粉丝.追星时长}年\n`;
  描述 += `- 疯狂程度：${粉丝.疯狂程度}%\n\n`;

  if (粉丝.粉丝类型 === '大粉' || 粉丝.粉丝类型 === '粉头') {
    描述 += `【特殊机会】\n`;
    描述 += `作为${粉丝.粉丝类型}，可能被邀请参加"后台见面"\n`;
  }

  return 描述;
}

/**
 * 生成机场/行程跟踪场景描述
 */
export function 生成行程跟踪场景描述(艺人: 艺人核心状态): string {
  const 地点 = ['机场', '酒店', '公寓楼下', '剧组现场'][Math.floor(Math.random() * 4)];

  let 描述 = `[${地点}]\n\n`;
  描述 += `${艺人.艺名}的行程被泄露...\n\n`;
  描述 += `【现场状况】\n`;

  switch (地点) {
    case '机场':
      描述 += `- 大量粉丝聚集\n`;
      描述 += `- 有人试图跟机\n`;
      描述 += `- 安保人员正在维持秩序\n`;
      break;
    case '酒店':
      描述 += `- 有粉丝在酒店大堂蹲守\n`;
      描述 += `- 有人在楼层走廊徘徊\n`;
      描述 += `- 隐私泄露风险高\n`;
      break;
    case '公寓楼下':
      描述 += `- 有人在家门口徘徊\n`;
      描述 += `- 疑似有人在安装偷拍设备\n`;
      描述 += `- 安全威胁明显\n`;
      break;
    case '剧组现场':
      描述 += `- 有人试图进入拍摄区域\n`;
      描述 += `- 有人偷拍路透照\n`;
      break;
  }

  描述 += `\n【当前骚扰等级】${艺人.私生骚扰等级}/10\n`;
  描述 += `【被跟踪次数】${艺人.被跟踪次数}次\n`;

  return 描述;
}

