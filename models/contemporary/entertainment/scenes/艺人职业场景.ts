/**
 * 艺人职业场景配置
 */
export interface 艺人职业场景配置 {
  场景类型: '试镜' | '综艺录制' | '颁奖典礼' | '粉丝见面会' | '媒体采访';
  场合描述: string;
  潜规则风险?: boolean;
}

/**
 * 生成试镜场景描述
 */
export function 生成试镜场景描述(
  艺人: 艺人核心状态,
  是否有潜规则风险: boolean = false
): string {
  let 描述 = `[剧组筹备组 - 酒店房间]\n\n`;
  描述 += `${艺人.艺名}来参加一部重要角色的试镜...\n`;
  描述 += `导演/制片人约你"单独聊聊剧本"\n\n`;

  if (是否有潜规则风险) {
    描述 += `【场景要素】\n`;
    描述 += `- 昏暗的灯光\n`;
    描述 += `- 单独相处的空间\n`;
    描述 += `- 暗示性的话语\n`;
    描述 += `- 肢体接触的试探\n`;
  }

  描述 += `\n【角色信息】\n`;
  描述 += `试镜角色：女主角/男主角\n`;
  描述 += `剧组等级：大型制作\n`;
  描述 += `竞争人数：${Math.floor(Math.random() * 50) + 20}人\n`;

  return 描述;
}

