/**
 * 生成颁奖典礼场景描述
 */
export function 生成颁奖典礼场景描述(艺人: 艺人核心状态): string {
  let 描述 = `[颁奖典礼红毯+后台]\n\n`;
  描述 += `${艺人.艺名}走上红毯，闪光灯不断闪烁...\n\n`;
  描述 += `【红毯环节】\n`;
  描述 += `- 穿着打扮（可能被嘲或被夸）\n`;
  描述 += `- 拍照姿势\n`;
  描述 += `- 接受采访（可能问到敏感问题）\n\n`;
  描述 += `【后台环节】\n`;
  描述 += `- 遇到其他艺人（合作/竞争）\n`;
  描述 += `- 遇到投资人/高层（社交机会）\n`;
  描述 += `- 躲过镜头的私密交流\n`;

  if (艺人.私生骚扰等级 > 5) {
    描述 += `\n⚠️ 后台有可疑人物徘徊...\n`;
  }

  return 描述;
}

/**
 * 生成媒体采访场景描述
 */
export function 生成媒体采访场景描述(
  艺人: 艺人核心状态,
  敏感话题?: string
): string {
  let 描述 = `[媒体采访室]\n\n`;
  描述 += `${艺人.艺名}正在接受媒体采访...\n\n`;
  描述 += `【当前人设】${艺人.对外人设}\n`;
  描述 += `【真实性格】${艺人.真实性格 || '未知'}\n`;
  描述 += `【人设崩塌度】${艺人.人设崩塌度}%\n\n`;

  if (敏感话题) {
    描述 += `【敏感话题】\n`;
    描述 += `${敏感话题}是当前舆论关注点，需要谨慎回应\n`;
  }

  return 描述;
}
