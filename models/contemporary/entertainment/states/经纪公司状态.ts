/**
 * 经纪公司核心状态
 */

import type {
  经纪公司等级,
  运营风格,
  粉丝运营策略,
} from '../types';

export interface 经纪公司状态 {
  公司ID: string;
  公司名称: string;
  公司等级: 经纪公司等级;

  // 资源与能力
  平台资源: number;           // 0-100，与平台的关系
  媒体资源: number;           // 0-100，媒体关系
  商务资源: number;           // 0-100，广告、商务资源
  培训资源: number;           // 0-100，培训体系完善度

  // 旗下艺人
  艺人数量: number;
  头部艺人ID?: string;        // 公司主推艺人
  潜力新人ID?: string[];      // 预备出道艺人

  // 运营风格
  运营风格: 运营风格;
  粉丝运营策略: 粉丝运营策略;
  潜规则容忍度: number;       // 0-100，公司对潜规则的容忍度

  // 负面记录
  潜规则举报次数: number;
  艺人解约纠纷: number;
  税务问题记录: number;
}

/**
 * 创建默认经纪公司状态
 */
export function 创建默认经纪公司状态(
  公司ID: string,
  公司名称: string,
  公司等级: 经纪公司等级
): 经纪公司状态 {
  return {
    公司ID,
    公司名称,
    公司等级,
    平台资源: 50,
    媒体资源: 50,
    商务资源: 50,
    培训资源: 50,
    艺人数量: 0,
    运营风格: '正常',
    粉丝运营策略: '正常',
    潜规则容忍度: 30,
    潜规则举报次数: 0,
    艺人解约纠纷: 0,
    税务问题记录: 0,
  };
}

/**
 * 增加旗下艺人
 */
export function 增加旗下艺人(
  状态: 经纪公司状态,
  艺人ID: string,
  是否头部: boolean = false
): 经纪公司状态 {
  return {
    ...状态,
    艺人数量: 状态.艺人数量 + 1,
    头部艺人ID: 是否头部 ? 艺人ID : 状态.头部艺人ID,
    潜力新人ID: [...(状态.潜力新人ID || []), 艺人ID],
  };
}

/**
 * 增加负面记录
 */
export function 增加负面记录(
  状态: 经纪公司状态,
  类型: '潜规则举报' | '解约纠纷' | '税务问题'
): 经纪公司状态 {
  switch (类型) {
    case '潜规则举报':
      return {
        ...状态,
        潜规则举报次数: 状态.潜规则举报次数 + 1,
      };
    case '解约纠纷':
      return {
        ...状态,
        艺人解约纠纷: 状态.艺人解约纠纷 + 1,
      };
    case '税务问题':
      return {
        ...状态,
        税务问题记录: 状态.税务问题记录 + 1,
      };
  }
}

/**
 * 调整潜规则容忍度
 */
export function 调整潜规则容忍度(
  状态: 经纪公司状态,
  变化: number
): 经纪公司状态 {
  return {
    ...状态,
    潜规则容忍度: Math.max(0, Math.min(100, 状态.潜规则容忍度 + 变化)),
  };
}
