// ==================== 购买合同检查 ====================

export interface 合同检查结果 {
  有效: boolean;
  缺失条款: string[];
  风险条款: string[];
  建议: string[];
}

export function 检查购买合同(合同内容: string): 合同检查结果 {
  const 缺失条款: string[] = [];
  const 风险条款: string[] = [];
  const 建议: string[] = [];

  const 必要条款 = [
    '健康保障',
    '退换政策',
    '售后服务',
    '血统证书',
    '疫苗记录',
  ];

  for (const 条款 of 必要条款) {
    if (!合同内容.includes(条款)) {
      缺失条款.push(`缺少【${条款}】条款`);
    }
  }

  if (风险条款.length > 0) {
    建议.push('合同存在风险条款，建议不要签署');
    建议.push('要求补充缺失条款后再购买');
  } else {
    建议.push('合同条款基本完善');
  }

  return {
    有效: 缺失条款.length === 0 && 风险条款.length === 0,
    缺失条款,
    风险条款,
    建议,
  };
}
