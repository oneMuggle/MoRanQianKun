// ==================== 购买决策 ====================

export interface 购买结果 {
  成功: boolean;
  宠物?: 宠物核心状态;
  花费: number;
  风险评估: ReturnType<typeof 星期狗风险评估>;
  警告: string[];
  建议: string[];
}

// 执行购买
export function 执行购买(
  渠道: string,
  类型: 宠物类型,
  品种: 宠物品种,
  期望价格?: number
): 购买结果 {
  const 渠道配置 = 购买渠道列表[渠道];
  const 警告: string[] = [];
  const 建议: string[] = [];

  if (!渠道配置) {
    return {
      成功: false,
      花费: 0,
      风险评估: {
        是否高风险: false,
        风险等级: '低',
        可能症状: [],
        建议: [],
      },
      警告: ['无效的购买渠道'],
      建议: ['请选择有效的购买渠道'],
    };
  }

  // 风险警告
  if (渠道配置.风险 > 0.5) {
    警告.push(`【警告】${渠道}风险较高（${渠道配置.风险 * 100}%），购买需谨慎`);
  }

  // 创建宠物
  const 宠物 = 根据类型创建宠物(类型, 品种);
  
  // 估算实际价格
  let 实际花费 = 期望价格;
  if (!实际花费) {
    switch (渠道) {
      case '正规猫舍':
        实际花费 = 品种 === '赛级' ? 30000 : 品种 === '纯种' ? 15000 : 5000;
        break;
      case '宠物店':
        实际花费 = 品种 === '纯种' ? 3000 : 1000;
        break;
      case '路边摊':
        实际花费 = 200;
        break;
      case '领养':
        实际花费 = 300;
        break;
      case '朋友赠送':
        实际花费 = 0;
        break;
      default:
        实际花费 = 1000;
    }
  }

  // 星期狗风险评估
  const 风险评估 = 星期狗风险评估(宠物);

  // 根据渠道调整宠物信息
  switch (渠道) {
    case '正规猫舍':
      宠物.血统证书 = true;
      宠物.芯片植入 = true;
      宠物.免疫记录 = ['猫三联', '狂犬'];
      宠物.来源 = '正规猫舍/犬舍';
      break;
    case '宠物店':
      宠物.血统证书 = 品种 === '纯种' || 品种 === '赛级';
      宠物.来源 = '宠物店购买';
      break;
    case '路边摊':
      宠物.血统证书 = false;
      宠物.芯片植入 = false;
      宠物.来源 = '路边购买';
      警告.push('路边购买的宠物风险极高，可能买到"星期狗"');
      建议.push('建议购买后尽快到正规医院进行健康检查');
      break;
    case '领养':
      宠物.来源 = '领养';
      宠物.血统证书 = false;
      建议.push('领养的宠物通常已绝育、驱虫，更健康');
      break;
    case '朋友赠送':
      宠物.来源 = '朋友赠送';
      警告.push('来源不明的宠物可能存在健康或行为问题');
      建议.push('建议先进行健康检查和行为评估');
      break;
  }

  宠物.购买价格 = 实际花费;
  宠物.当前价值 = Math.round(实际花费 * (渠道 === '正规猫舍' ? 0.8 : 0.5));

  // 高风险渠道额外警告
  if (渠道配置.风险 > 0.5) {
    建议.push('建议购买宠物保险以防万一');
    建议.push('保留购买凭证和合同');
  }

  return {
    成功: true,
    宠物,
    花费: 实际花费,
    风险评估,
    警告,
    建议,
  };
}

// 推荐购买渠道
export function 推荐购买渠道(
  类型: 宠物类型,
  品种: 宠物品种,
  预算: number
): string[] {
  const 推荐: { 渠道: string; 分数: number }[] = [];

  for (const [渠道, config] of Object.entries(购买渠道列表)) {
    let 分数 = 50;

    // 风险折扣
    分数 -= config.风险 * 40;

    // 预算匹配
    if (渠道 === '正规猫舍' && 预算 < 5000) {
      分数 -= 30;
    }
    if (渠道 === '路边摊' && 预算 > 5000) {
      分数 -= 20;
    }

    // 品种匹配
    if ((品种 === '赛级' || 品种 === '纯种') && 渠道 === '正规猫舍') {
      分数 += 20;
    }
    if (品种 === '土猫/土狗' && 渠道 === '领养') {
      分数 += 30;
    }

    推荐.push({ 渠道, 分数 });
  }

  return 推荐
    .filter(r => r.分数 > 30)
    .sort((a, b) => b.分数 - a.分数)
    .slice(0, 3)
    .map(r => r.渠道);
}

