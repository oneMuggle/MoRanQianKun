// ==================== 过度医疗检测 ====================

export interface 过度医疗检测 {
  是否过度: boolean;
  过度类型: string[];
  合理检查: string[];
  不必要检查: string[];
  节省建议: string[];
}

export function 检测过度医疗(
  建议检查: string[],
  宠物症状: string
): 过度医疗检测 {
  const 不必要检查: string[] = [];
  const 合理检查: string[] = [];
  const 过度类型: string[] = [];

  // 小病大治检测
  const 轻微症状 = ['轻微咳嗽', '轻度腹泻', '食欲不振', '轻微外伤'];
  if (轻微症状.some(s => 宠物症状.includes(s))) {
    if (建议检查.includes('全身检查') || 建议检查.includes('CT') || 建议检查.includes('MRI')) {
      不必要检查.push('全身检查/高级影像检查');
      过度类型.push('小病大治');
    }
  }

  // 重复检查检测
  if (建议检查.includes('血常规') && 建议检查.includes('全套生化')) {
    const 检查数量 = 建议检查.filter(d => 
      ['血常规', '生化', 'B超', 'X光'].includes(d)
    ).length;
    if (检查数量 > 2) {
      过度类型.push('重复检查');
    }
  }

  // 不必要手术检测
  if (建议检查.includes('超声刀') && !宠物症状.includes('肿瘤')) {
    不必要检查.push('超声刀手术');
    过度类型.push('不必要的手术');
  }

  // 进口药过度使用
  if (建议检查.includes('进口药') && !宠物症状.includes('严重')) {
    过度类型.push('进口药过度使用');
  }

  // 检查套餐拆分
  if (建议检查.includes('套餐检查')) {
    过度类型.push('套餐检查包含无关项目');
  }

  // 合理检查
  const 必要检查Map: Record<string, string[]> = {
    '咳嗽': ['听诊', 'X光胸片', '血常规'],
    '腹泻': ['便检', '血常规'],
    '呕吐': ['腹部B超', '血常规'],
    '外伤': ['清创缝合', 'X光'],
    '骨折': ['X光', 'CT', '骨科手术'],
    '皮肤病': ['皮肤刮片', '伍德灯检查'],
  };

  for (const [症状, 检查列表] of Object.entries(必要检查Map)) {
    if (宠物症状.includes(症状)) {
      合理检查.push(...检查列表);
    }
  }

  // 节省建议
  const 节省建议: string[] = [];
  if (不必要检查.length > 0) {
    节省建议.push('可以先做基础检查，明确问题后再做进一步检查');
    节省建议.push('问医生每项检查的必要性');
  }
  if (过度类型.includes('进口药过度使用')) {
    节省建议.push('国产药同样有效，不必追求进口');
  }

  return {
    是否过度: 过度类型.length > 0,
    过度类型,
    合理检查: Array.from(new Set(合理检查)),
    不必要检查,
    节省建议,
  };
}

// ==================== 医疗费用计算 ====================

export interface 医疗费用明细 {
  项目: string;
  费用: number;
  必要性: '必要' | '可选' | '过度';
}

export function 计算医疗费用(
  医疗类型: 医疗类型,
  宠物体重: number,
  是否进口药: boolean
): {
  总费用: number;
  明细: 医疗费用明细[];
  是否合理: boolean;
  建议: string[];
} {
  const 明细: 医疗费用明细[] = [];
  let 总费用 = 0;

  // 基础费用估算
  switch (医疗类型) {
    case '基础保健':
      明细.push(
        { 项目: '挂号费', 费用: 50, 必要性: '必要' },
        { 项目: '疫苗', 费用: 是否进口药 ? 300 : 150, 必要性: '必要' },
        { 项目: '驱虫', 费用: 是否进口药 ? 200 : 100, 必要性: '必要' },
      );
      总费用 = 是否进口药 ? 550 : 300;
      break;

    case '常规诊疗':
      明细.push(
        { 项目: '挂号费', 费用: 100, 必要性: '必要' },
        { 项目: '门诊费', 费用: 200, 必要性: '必要' },
        { 项目: '化验费', 费用: 300, 必要性: '可选' },
        { 项目: '药费', 费用: 是否进口药 ? 500 : 200, 必要性: '必要' },
      );
      总费用 = 是否进口药 ? 1100 : 800;
      break;

    case '手术':
      // 绝育手术
      明细.push(
        { 项目: '术前检查', 费用: 300, 必要性: '必要' },
        { 项目: '麻醉费', 费用: 宠物体重 > 10 ? 500 : 300, 必要性: '必要' },
        { 项目: '手术费', 费用: 800, 必要性: '必要' },
        { 项目: '术后消炎', 费用: 200, 必要性: '必要' },
      );
      总费用 = 1600 + (宠物体重 > 10 ? 200 : 0);
      break;

    case '急诊':
      明细.push(
        { 项目: '急诊费', 费用: 300, 必要性: '必要' },
        { 项目: '夜间附加', 费用: 200, 必要性: '必要' },
        { 项目: '抢救费', 费用: 1000, 必要性: '必要' },
        { 项目: 'ICU', 费用: 800, 必要性: '可选' },
      );
      总费用 = 2300;
      break;

    case '专科':
      明细.push(
        { 项目: '专科挂号', 费用: 200, 必要性: '必要' },
        { 项目: '专科检查', 费用: 500, 必要性: '必要' },
        { 项目: '治疗费', 费用: 是否进口药 ? 2000 : 1000, 必要性: '必要' },
      );
      总费用 = 是否进口药 ? 2700 : 1700;
      break;

    case '整形':
      明细.push(
        { 项目: '麻醉费', 费用: 500, 必要性: '必要' },
        { 项目: '整形费', 费用: 2000, 必要性: '可选' },
        { 项目: '术后护理', 费用: 300, 必要性: '必要' },
      );
      总费用 = 2800;
      break;

    case '牙科':
      明细.push(
        { 项目: '检查费', 费用: 100, 必要性: '必要' },
        { 项目: '洗牙费', 费用: 300, 必要性: '必要' },
        { 项目: '麻醉费', 费用: 200, 必要性: '必要' },
      );
      总费用 = 600;
      break;
  }

  const 是否合理 = 总费用 < 5000;

  return {
    总费用,
    明细,
    是否合理,
    建议: 是否合理 ? [] : ['费用较高，建议咨询多家医院后再决定'],
  };
}

// ==================== 医疗风险评估 ====================

export interface 医疗风险评估 {
  风险等级: '低' | '中' | '高';
  风险因素: string[];
  建议: string[];
}

export function 评估医疗风险(
  医院评价: number,
  医生资质: string,
  手术类型: string
): 医疗风险评估 {
  const 风险因素: string[] = [];
  const 建议: string[] = [];

  if (医院评价 < 70) {
    风险因素.push('医院评价较低');
    建议.push('建议选择评价更高的医院');
  }

  if (医生资质 === '无证') {
    风险因素.push('医生无资质认证');
    建议.push('务必选择有资质的兽医');
  }

  if (['骨科手术', '肿瘤手术', '剖腹产'].includes(手术类型)) {
    风险因素.push('高风险手术类型');
    建议.push('建议选择大型正规宠物医院');
    建议.push('术前充分了解手术风险');
  }

  let 风险等级: '低' | '中' | '高' = '低';
  if (风险因素.length >= 2) {
    风险等级 = '高';
  } else if (风险因素.length >= 1) {
    风险等级 = '中';
  }

  return {
    风险等级,
    风险因素,
    建议,
  };
}
