// ==================== 美容风险评估 ====================

export interface 美容风险评估 {
  风险等级: '低' | '中' | '高';
  主要风险: string[];
  预防建议: string[];
}

export function 评估美容风险(
  宠物: 宠物核心状态,
  美容类型: 美容类型,
  店铺评价: number
): 美容风险评估 {
  const 主要风险: string[] = [];
  const 预防建议: string[] = [];

  // 宠物性格风险
  if (宠物.攻击性 > 60) {
    主要风险.push('宠物有攻击倾向，可能在美容过程中伤人');
    预防建议.push('建议选择有宠物保定经验的美容师');
    预防建议.push('考虑在美容前使用镇静剂');
  }

  if (宠物.攻击性 > 80) {
    主要风险.push('宠物攻击性极高');
    预防建议.push('强烈建议选择专业医疗机构进行美容');
  }

  // 心脏问题风险
  if (宠物.健康值 < 50) {
    主要风险.push('宠物健康状况欠佳');
    预防建议.push('美容前建议进行全面体检');
    预防建议.push('美容过程中注意观察宠物状态');
  }

  // 店铺风险
  if (店铺评价 < 70) {
    主要风险.push('店铺评价较低');
    预防建议.push('建议选择评价更高的店铺');
  }

  // 美容类型风险
  if (美容类型 === '赛级美容' || 美容类型 === '创意造型') {
    主要风险.push('高端美容服务时间长，风险增加');
    预防建议.push('选择有资质的高级美容师');
    预防建议.push('美容过程中要求定期休息');
  }

  // 应激风险
  if (宠物.粘人度 > 80) {
    预防建议.push('高粘人度宠物可能分离焦虑，建议主人陪伴');
  }

  let 风险等级: '低' | '中' | '高' = '低';
  if (主要风险.length >= 2) {
    风险等级 = '高';
  } else if (主要风险.length >= 1) {
    风险等级 = '中';
  }

  return {
    风险等级,
    主要风险,
    预防建议,
  };
}

// ==================== 美容价格计算 ====================

export interface 美容价格明细 {
  服务: string;
  价格: number;
}

export function 计算美容价格(
  美容类型: 美容类型,
  宠物大小: '小型' | '中型' | '大型',
  是否名贵品种: boolean
): {
  总价: number;
  明细: 美容价格明细[];
  优惠: string[];
} {
  const config = 美容服务配置表[美容类型];
  const 明细: 美容价格明细[] = [];
  const 优惠: string[] = [];

  // 基础价格
  let 基础价格 = 0;
  switch (美容类型) {
    case '基础洗澡':
      基础价格 = 宠物大小 === '小型' ? 80 : 宠物大小 === '中型' ? 120 : 150;
      break;
    case '精致美容':
      基础价格 = 宠物大小 === '小型' ? 280 : 宠物大小 === '中型' ? 380 : 480;
      break;
    case '赛级美容':
      基础价格 = 宠物大小 === '小型' ? 800 : 宠物大小 === '中型' ? 1200 : 1800;
      break;
    case '创意造型':
      基础价格 = 宠物大小 === '小型' ? 500 : 宠物大小 === '中型' ? 700 : 1000;
      break;
    case '局部修剪':
      基础价格 = 50;
      break;
  }

  // 名贵品种加价
  if (是否名贵品种) {
    基础价格 *= 1.5;
  }

  明细.push({
    服务: `${config.类型}（${宠物大小}型${是否名贵品种 ? '名贵' : '普通'}品种）`,
    价格: 基础价格,
  });

  // 优惠
  if (基础价格 > 1000) {
    优惠.push('会员卡可享受9折优惠');
  }
  if (基础价格 > 500) {
    优惠.push('办理季度套餐可享8.5折');
  }

  return {
    总价: Math.round(基础价格),
    明细,
    优惠,
  };
}

