// ==================== 纠纷升级评估 ====================

export interface 纠纷升级评估 {
  是否升级: boolean;
  升级风险: number;
  触发条件: string[];
  预防升级建议: string[];
}

export function 评估纠纷升级风险(
  当前状态: '协商中' | '投诉中' | '调解中' | '诉讼中',
  对方态度: '积极' | '配合' | '推诿' | '拒绝',
  时间已过长: boolean
): 纠纷升级评估 {
  const 触发条件: string[] = [];
  const 预防升级建议: string[] = [];
  let 升级风险 = 30;

  if (对方态度 === '拒绝') {
    升级风险 += 40;
    触发条件.push('对方拒绝协商');
  }

  if (对方态度 === '推诿') {
    升级风险 += 20;
    触发条件.push('对方持续推诿');
  }

  if (时间已过长) {
    升级风险 += 15;
    触发条件.push('纠纷持续时间过长');
  }

  if (当前状态 === '协商中' && 升级风险 > 50) {
    预防升级建议.push('建议升级到向相关部门投诉');
  }

  if (当前状态 === '投诉中' && 升级风险 > 70) {
    预防升级建议.push('建议考虑法律途径');
  }

  if (触发条件.length > 0) {
    预防升级建议.push('注意保存所有证据');
    预防升级建议.push('可以考虑律师函警告');
  }

  return {
    是否升级: 升级风险 > 60,
    升级风险,
    触发条件,
    预防升级建议,
  };
}
