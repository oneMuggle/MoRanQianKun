// ==================== 纠纷处理 ====================

export interface 纠纷处理结果 {
  解决: boolean;
  赔偿金额: number;
  耗时: string;
  满意度: number;
  建议: string[];
}

export function 协商解决纠纷(
  纠纷类型: 纠纷类型,
  宠物价值: number,
  损失金额: number,
  对方态度: '积极' | '配合' | '推诿' | '拒绝'
): 纠纷处理结果 {
  const 建议: string[] = [];
  let 赔偿金额 = 0;
  let 满意度 = 50;

  switch (对方态度) {
    case '积极':
      赔偿金额 = Math.min(损失金额, 宠物价值 * 1.5);
      满意度 = 90;
      建议.push('对方态度积极，可争取合理赔偿');
      break;
    case '配合':
      赔偿金额 = Math.min(损失金额 * 0.8, 宠物价值);
      满意度 = 70;
      建议.push('对方愿意配合，可以协商解决');
      break;
    case '推诿':
      赔偿金额 = 损失金额 * 0.3;
      满意度 = 40;
      建议.push('对方推诿，建议收集证据后投诉');
      建议.push('可以向消费者协会或农业农村部门投诉');
      break;
    case '拒绝':
      赔偿金额 = 0;
      满意度 = 10;
      建议.push('对方拒绝赔偿，建议走法律途径');
      建议.push('保留好所有证据，包括聊天记录、照片、视频、医疗单据等');
      break;
  }

  return {
    解决: 对方态度 === '积极' || 对方态度 === '配合',
    赔偿金额,
    耗时: 对方态度 === '积极' ? '1-3天' : '1-4周',
    满意度,
    建议,
  };
}

