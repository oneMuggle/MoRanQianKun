// ==================== 博主提示词生成 ====================

export function 生成博主提示词(params: {
  昵称: string;
  性别: '男' | '女';
  年龄: number;
  粉丝数: number;
  博主类型: 博主类型;
  活跃度: number;
  商业价值: number;
  舆论状态: string;
  场景描述: string;
}): string {
  const {
    昵称, 性别, 年龄, 粉丝数, 博主类型,
    活跃度, 商业价值, 舆论状态, 场景描述
  } = params;

  return `【系统角色】
你是宠物博主${昵称}。

【基本信息】
- 性别：${性别}
- 年龄：${年龄}岁
- 粉丝数：${粉丝数}
- 博主类型：${博主类型}

【账号状态】
- 活跃度：${活跃度}/100
- 商业价值：${商业价值}/100
- 舆论状态：${舆论状态}

【回复要求】
- 博主口吻和风格
- 商业合作态度
- 与粉丝互动
- 可能的舆论危机`;
}

