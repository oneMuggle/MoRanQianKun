// ==================== 宠物美容场景 ====================

export interface 美容场景 {
  场景描述: string;
  过程描述: string[];
  暧昧触发点: string[];
  选择点: string[];
}

export function 触发美容场景(
  美容类型: 美容类型,
  美容师性别: '男' | '女'
): 美容场景 {
  const config = 美容服务配置表[美容类型];
  const 过程: string[] = [];

  if (美容类型 === '基础洗澡') {
    过程.push('美容师轻轻地将猫咪放入浴池');
    过程.push('温热的水流过毛发，猫咪发出舒服的呼噜声');
    过程.push('美容师的手指轻柔地按摩着毛发');
  } else if (美容类型 === '精致美容') {
    过程.push('美容师仔细地修剪着毛发');
    过程.push('每剪一刀都会轻声安抚');
    过程.push('吹干时猫咪靠在美容师怀里');
  } else if (美容类型 === '赛级美容') {
    过程.push('美容师进行专业级的美容流程');
    过程.push('每一个细节都精心处理');
    过程.push('需要2-3小时的专业服务');
  }

  return {
    场景描述: `[宠物美容店 - ${config.时间}]
带宠物来做${config.内容.join('、')}...
美容师${美容师性别 === '男' ? '小哥' : '小姐姐'}热情地接待...
`,
    过程描述: 过程,
    暧昧触发点: [
      '美容师整理毛发时的近距离接触',
      '美容过程中等待的空闲时间',
      '关于宠物护理的交流',
      '美容师对宠物的赞美',
    ],
    选择点: [
      '是否在店里等待？',
      '是否和美容师多聊聊？',
      '是否询问联系方式？',
      '是否选择更高端的服务？',
    ],
  };
}

