// ==================== 预约管理器 ====================

export class 预约管理器 {
  private 预约列表: Map<string, 预约状态> = new Map();

  /**
   * 创建新预约
   */
  创建预约(params: {
    会员ID: string;
    会所ID: string;
    疗程类型: 服务类型;
    房间类型: 房间类型;
    预约时间: string;
    指定技师ID?: string;
    特殊需求?: string;
    健康声明?: string;
  }): 预约状态 {
    const 预约ID = this.生成ID();
    const 疗程 = 疗程列表.find(t => t.类型 === params.疗程类型) || 疗程列表[0];
    
    const 预约: 预约状态 = {
      预约ID,
      会员ID: params.会员ID,
      会所ID: params.会所ID,
      技师ID: params.指定技师ID,
      预约时间: params.预约时间,
      疗程类型: params.疗程类型,
      房间类型: params.房间类型,
      时长: 疗程.时长,
      状态: '待确认',
      订金: Math.round(疗程.价格 * 预约规则.订金比例),
      总费用: 疗程.价格,
      特殊需求: params.特殊需求,
      健康声明: params.健康声明,
    };

    this.预约列表.set(预约ID, 预约);
    return 预约;
  }

  /**
   * 计算取消扣款
   */
  计算取消扣款(预约: 预约状态, 当前时间: Date): {
    扣款比例: number;
    扣款金额: number;
    退还金额: number;
    说明: string;
  } {
    const 预约时间 = new Date(预约.预约时间);
    const 小时差 = (预约时间.getTime() - 当前时间.getTime()) / (1000 * 60 * 60);

    let 扣款规则;
    if (小时差 >= 24) {
      扣款规则 = 预约规则.取消政策['提前24小时'];
    } else if (小时差 >= 12) {
      扣款规则 = 预约规则.取消政策['提前12小时'];
    } else if (小时差 >= 6) {
      扣款规则 = 预约规则.取消政策['提前6小时'];
    } else {
      扣款规则 = 预约规则.取消政策['未按时到店'];
    }

    const 扣款金额 = Math.round(预约.订金 * 扣款规则.扣款比例);
    const 退还金额 = 预约.订金 - 扣款金额;

    return {
      扣款比例: 扣款规则.扣款比例,
      扣款金额,
      退还金额,
      说明: 扣款规则.说明,
    };
  }

  /**
   * 检查会员预约权限
   */
  检查预约权限(会员: 会员核心状态, 疗程价格: number): {
    可以预约: boolean;
    原因?: string;
    建议?: string;
  } {
    // 检查余额
    if (会员.会员卡余额 < 疗程价格 * 预约规则.订金比例) {
      return {
        可以预约: false,
        原因: '会员卡余额不足，建议先充值',
        建议: '请联系会所客服了解充值优惠',
      };
    }

    // 检查隐私设置
    if (!会员.隐身模式) {
      // 未开启隐身模式，提示可能显示动态
      return {
        可以预约: true,
        原因: '预约信息将对其他会员可见（隐身模式未开启）',
      };
    }

    return { 可以预约: true };
  }

  /**
   * 获取会员可预约时段
   */
  获取可预约时段(
    会所: 会所状态,
    日期: string,
    已预约时段: string[]
  ): string[] {
    const 所有时段 = Object.keys(预约规则.时段);
    
    // 过滤掉已满的时段
    return 所有时段.filter(时段 => {
      const 已预约数 = 已预约时段.filter(t => t.startsWith(时段)).length;
      return 已预约数 < 会所.在职技师;
    });
  }

  /**
   * 验证健康声明
   */
  验证健康声明(健康声明: string): {
    有效: boolean;
    风险提示?: string[];
  } {
    const 风险关键词 = ['心脏病', '高血压', '糖尿病', '皮肤病', '传染病', '怀孕'];
    const 风险提示 = 风险关键词.filter(k => 健康声明.includes(k));

    if (风险提示.length > 0) {
      return {
        有效: true,
        风险提示: [
          `检测到以下健康状况：${风险提示.join('、')}`,
          '建议在接受服务前咨询专业医生意见',
          '部分疗程可能不适合您',
        ],
      };
    }

    return { 有效: true };
  }

  /**
   * 计算疗程最终价格
   */
  计算最终价格(
    基础价格: number,
    会员等级: 会员等级,
    是否升房: boolean = false,
    是否指定技师: boolean = false
  ): {
    原价: number;
    折扣: number;
    折扣后价格: number;
    升房费: number;
    技师费: number;
    总价: number;
  } {
    const 权益 = 会员等级权益[会员等级];
    const 折扣 = 权益.折扣;
    const 折扣后价格 = Math.round(基础价格 * 折扣);

    // 升房费（如果有）
    const 升房费 = 是否升房 ? Math.round(折扣后价格 * 0.5) : 0;

    // 指定技师费
    const 技师费 = 是否指定技师 ? 200 : 0;

    return {
      原价: 基础价格,
      折扣,
      折扣后价格,
      升房费,
      技师费,
      总价: 折扣后价格 + 升房费 + 技师费,
    };
  }

  private 生成ID(): string {
    return `APPT_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;
  }
}

// ==================== 房间管理器 ====================

export class 房间管理器 {
  private 房间状态: Map<string, {
    房间ID: string;
    状态: '空闲' | '已预订' | '服务中' | '消杀中';
    下一个可用时间?: string;
  }> = new Map();

  /**
   * 初始化房间
   */
  初始化房间(房间数量: number): void {
    for (let i = 1; i <= 房间数量; i++) {
      const 房间ID = `ROOM_${i}`;
      this.房间状态.set(房间ID, {
        房间ID,
        状态: '空闲',
      });
    }
  }

  /**
   * 预订房间
   */
  预订房间(
    房间类型: 房间类型,
    开始时间: string,
    时长: string
  ): { 成功: boolean; 房间ID?: string; 下一个可用时间?: string } {
    // 找到第一个匹配类型的空闲房间
    for (const [房间ID, 房间] of Array.from(this.房间状态.entries())) {
      if (房间.状态 === '空闲') {
        房间.状态 = '已预订';
        
        // 计算结束时间
        const 开始 = new Date(开始时间);
        const 时长分钟 = parseInt(时长) || 90;
        const 结束 = new Date(开始.getTime() + 时长分钟 * 60 * 1000);
        
        // 加上消杀缓冲
        const 可用 = new Date(结束.getTime() + 预约规则.消杀缓冲 * 60 * 1000);
        
        return {
          成功: true,
          房间ID,
          下一个可用时间: 可用.toISOString(),
        };
      }
    }

    return { 成功: false };
  }

  /**
   * 释放房间
   */
  释放房间(房间ID: string): void {
    const 房间 = this.房间状态.get(房间ID);
    if (房间) {
      房间.状态 = '消杀中';
      
      // 消杀完成后变为空闲
      setTimeout(() => {
        房间.状态 = '空闲';
      }, 预约规则.消杀缓冲 * 60 * 1000);
    }
  }

  /**
   * 获取房间状态
   */
  获取房间状态(): Map<string, { 状态: string; 下一个可用时间?: string }> {
    return new Map(
      Array.from(this.房间状态.entries()).map(([id, room]) => [
        id,
        { 状态: room.状态, 下一个可用时间: room.下一个可用时间 },
      ])
    );
  }
}
