/**
 * 会员心理状态描述
 */
export function 描述会员心理(会员: 会员核心状态): string {
  const 心理元素: string[] = [];

  // 防范意识
  if (会员.防范意识 > 80) {
    心理元素.push('极高的防范意识，对隐私和安全非常重视');
  } else if (会员.防范意识 < 30) {
    心理元素.push('防范意识较弱，可能忽视潜在风险');
  }

  // 酒后乱性风险
  if (会员.酒后乱性风险 > 70) {
    心理元素.push('酒后容易失控，需要注意');
  }

  // 隐私保护
  if (会员.隐私保护 > 80) {
    心理元素.push('极度重视隐私保护，选择隐身模式');
  }

  // 社交目的
  if (会员.社交目的 === '猎艳') {
    心理元素.push('抱着猎艳心态前来');
  } else if (会员.社交目的 === '释放压力') {
    心理元素.push('单纯想放松一下');
  } else if (会员.社交目的 === '商务接待') {
    心理元素.push('有商务接待需求');
  }

  // 暧昧进展
  if (会员.暧昧对象) {
    心理元素.push(`正在追求：${会员.暧昧对象}，进展${会员.关系进展}%`);
  }

  return 心理元素.length > 0
    ? 心理元素.join('；')
    : '心理状态平静';
}

