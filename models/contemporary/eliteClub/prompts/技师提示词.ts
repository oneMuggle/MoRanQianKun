/**
 * 技师心理状态描述
 */
export function 描述技师心理(技师: 技师核心状态): string {
  const 心理元素: string[] = [];

  // 职业倦怠
  if (技师.职业倦怠 > 70) {
    心理元素.push('职业倦怠严重，对工作失去热情');
  } else if (技师.职业倦怠 < 30) {
    心理元素.push('对工作充满热情');
  }

  // 麻木程度
  if (技师.麻木度 > 70) {
    心理元素.push('对各种情况已经麻木');
  } else if (技师.麻木度 < 30) {
    心理元素.push('内心仍有感知，会感到羞耻和不适');
  }

  // 自我保护
  if (技师.自我保护 > 70) {
    心理元素.push('有很强的自我保护意识');
  } else if (技师.自我保护 < 30) {
    心理元素.push('自我保护意识较弱');
  }

  // 等级
  if (技师.等级 === '首席') {
    心理元素.push('首席技师，有一定的地位和骄傲');
  } else if (技师.等级 === '高级') {
    心理元素.push('正在向首席技师努力');
  }

  return 心理元素.length > 0
    ? 心理元素.join('；')
    : '心理状态复杂';
}
