/**
 * 技师代号风格
 */
export function 获取技师代号风格(代号: string): string {
  // 检查代号关键词来确定风格
  if (代号.includes('清风') || 代号.includes('明月') || 代号.includes('翠玉')) {
    return '清新婉约型 - 温柔体贴，善于倾听';
  } else if (代号.includes('烈焰') || 代号.includes('寒冰') || 代号.includes('破军')) {
    return '刚毅冷峻型 - 气场强大，专业度高';
  } else if (代号.includes('星河流转') || 代号.includes('烟雨江南')) {
    return '文艺气质型 - 有内涵，擅长高端客户服务';
  } else if (代号.includes('出水芙蓉') || 代号.includes('月下瑶池')) {
    return '古典美人型 - 优雅端庄，擅长高端服务';
  }
  return '普通专业型';
}

