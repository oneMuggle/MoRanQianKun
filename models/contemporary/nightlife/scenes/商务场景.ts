/**
 * 生成商务接待场景描述
 */
export function 生成商务接待描述(params: {
  场所: 夜场场所状态;
  客户等级: '普通客户' | '重要客户' | 'VIP客户';
  消费金额: number;
}): string {
  const { 场所, 客户等级, 消费金额 } = params;

  const 场所档次数: Record<string, string> = {
    '高档型': '高档豪华',
    '豪华型': '顶级奢华',
    '顶级私人': '至尊私密',
  };

  const 场所描述 = 场所档次数[场所.档次] || '高档';
  
  return `[${场所.场所名称} - ${场所描述}${场所.类型}]\n\n${客户等级}到访，您安排了${消费金额}元的消费。\n\n妈咪带着公主们鱼贯而入，${场所.场所名称}的夜晚正式开始...\n\n${场所.涉黄程度 > 50 ? '这里的公主都很open，气氛暧昧' : '服务正规但不失热情'}。\n`;
}

/**
 * 商务接待结果评价
 */
export function 评价商务接待结果(params: {
  消费金额: number;
  客户满意度: number;
  是否达成目的: boolean;
  后续影响: 'positive' | 'neutral' | 'negative';
}): {
  评价: string;
  影响描述: string[];
} {
  const { 消费金额, 客户满意度, 是否达成目的, 后续影响 } = params;

  let 评价 = '';
  const 影响描述: string[] = [];

  if (后续影响 === 'positive') {
    评价 = '圆满成功';
    影响描述.push('客户关系更加稳固');
    影响描述.push('未来合作可期');
    影响描述.push('建立了信任基础');
  } else if (后续影响 === 'neutral') {
    评价 = '基本满意';
    影响描述.push('关系维持现状');
    影响描述.push('需要继续跟进');
  } else {
    评价 = '效果不佳';
    影响描述.push('可能影响后续合作');
    影响描述.push('需要想办法补救');
  }

  if (消费金额 > 20000) {
    影响描述.push('高消费展示了实力');
  }

  if (客户满意度 > 80) {
    影响描述.push('客户非常满意');
  }

  return { 评价, 影响描述 };
}
