/**
 * 服务人员心理状态
 */
export function 描述服务人员心理(状态: 服务人员核心状态): string {
  const 心理描述: string[] = [];

  // 麻木度
  if (状态.麻木度 > 70) {
    心理描述.push('对这一切已经麻木');
  } else if (状态.麻木度 < 30) {
    心理描述.push('内心还有羞耻感');
  }

  // 赚钱欲望
  if (状态.赚钱欲望 > 70) {
    心理描述.push('非常想赚钱');
  }

  // 酒精依赖
  if (状态.酒精依赖 > 60) {
    心理描述.push('对酒精有依赖');
  }

  // 转型意愿
  if (状态.计划转型) {
    心理描述.push(`想要转型：${状态.转型方向}`);
  }

  // 债务
  if (状态.债务 > 0) {
    心理描述.push(`欠债${状态.债务}元`);
  }

  return 心理描述.join('；') || '心理状态正常';
}
