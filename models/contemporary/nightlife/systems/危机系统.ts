/**
 * 越界行为应对
 */
export function 应对越界行为(params: {
  越界类型: 越界行为;
  服务人员自我保护: number;
  场所安保水平: '弱' | '中' | '强';
  是否有朋友在场: boolean;
}): {
  能否抵抗: boolean;
  建议行动: string;
  成功概率: number;
} {
  const { 越界类型, 服务人员自我保护, 场所安保水平, 是否有朋友在场 } = params;

  const 越界强度表: Record<越界行为, number> = {
    '言语调戏': 10,
    '肢体接触': 25,
    '强迫喝酒': 35,
    '强行搂抱': 50,
    '言语威胁': 70,
    '动手动脚': 60,
    '强行带走': 85,
  };

  const 强度 = 越界强度表[越界类型];

  // 抵抗能力计算
  let 抵抗能力 = 服务人员自我保护;
  
  if (场所安保水平 === '强') 抵抗能力 += 30;
  else if (场所安保水平 === '中') 抵抗能力 += 15;

  if (是否有朋友在场) 抵抗能力 += 20;

  const 成功概率 = Math.max(0, Math.min(100, 抵抗能力 - 强度 * 0.5));
  const 能否抵抗 = Math.random() * 100 < 成功概率;

  let 建议行动: string;
  if (能否抵抗) {
    建议行动 = '大声呵斥并反抗，寻找机会逃离';
  } else {
    建议行动 = '不要激怒对方，保命优先，记住对方特征事后报警';
  }

  return { 能否抵抗, 建议行动, 成功概率 };
}

