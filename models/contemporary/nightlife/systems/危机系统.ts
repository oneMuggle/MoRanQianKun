/**
 * 评估危机发生概率
 */
export function 评估危机概率(params: {
  危机类型: 危机事件;
  醉酒值: number;
  场所档次: '经济型' | '中档型' | '高档型' | '豪华型' | '顶级私人';
  防范意识: number;
  是否独处: boolean;
}): { 概率: number; 风险等级: string } {
  const { 危机类型, 醉酒值, 场所档次, 防范意识, 是否独处 } = params;

  const 配置 = 危机事件配置[危机类型];
  let 概率 = 配置.发生概率;

  // 醉酒加成
  if (醉酒值 > 60) {
    概率 += 25;
  } else if (醉酒值 > 40) {
    概率 += 10;
  }

  // 场所档次影响
  if (场所档次 === '经济型') {
    概率 += 15;
  } else if (场所档次 === '顶级私人') {
    概率 -= 10;
  }

  // 防范意识
  概率 -= (防范意识 - 50) * 0.3;

  // 独处风险
  if (是否独处) {
    概率 += 20;
  }

  概率 = Math.max(0, Math.min(100, 概率));

  let 风险等级: string;
  if (概率 < 20) 风险等级 = '低';
  else if (概率 < 40) 风险等级 = '中';
  else if (概率 < 60) 风险等级 = '高';
  else 风险等级 = '极高';

  return { 概率, 风险等级 };
}

