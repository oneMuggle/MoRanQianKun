/**
 * 暧昧升级计算
 */
export function 计算暧昧升级(params: {
  当前暧昧值: number;
  场景: 暧昧场景;
  醉酒程度: 醉酒程度;
  是否主动: boolean; // 是否主动参与
}): { 新暧昧值: number; 触发事件?: string } {
  const { 当前暧昧值, 场景, 醉酒程度, 是否主动 } = params;

  const 场景配置 = 暧昧场景触发表[场景];
  
  // 基础暧昧值
  let 暧昧增量 = 场景配置.暧昧程度;

  // 醉酒加成
  const 醉酒加成 = 醉酒程度 === '大醉' || 醉酒程度 === '烂醉' 
    ? 场景配置.醉酒加成 
    : 醉酒程度 === '上头'
      ? Math.floor(场景配置.醉酒加成 * 0.6)
      : 0;

  // 主动加成
  if (是否主动) {
    暧昧增量 += 10;
  }

  // 醉酒程度额外加成
  const 醉酒程度加成表: Record<醉酒程度, number> = {
    '清醒': 0,
    '微醺': 5,
    '上头': 15,
    '大醉': 30,
    '烂醉': 50,
  };

  const 总加成 = 暧昧增量 + 醉酒加成 + 醉酒程度加成表[醉酒程度];
  const 新暧昧值 = Math.min(100, 当前暧昧值 + 总加成);

  // 判断是否触发特殊事件
  let 触发事件: string | undefined;
  if (新暧昧值 >= 80 && 当前暧昧值 < 80) {
    触发事件 = '关系急剧升温';
  } else if (新暧昧值 >= 50 && 当前暧昧值 < 50) {
    触发事件 = '暧昧关系确立';
  }

  return { 新暧昧值, 触发事件 };
}

/**
 * 越界行为风险评估
 */
export function 评估越界风险(params: {
  越界类型: 越界行为;
  服务人员麻木度: number;
  服务人员自我保护: number;
  消费者醉酒程度: 醉酒程度;
  消费者冲动程度: number;
}): { 风险值: number; 是否会抵抗: boolean } {
  const { 越界类型, 服务人员麻木度, 服务人员自我保护, 消费者醉酒程度, 消费者冲动程度 } = params;

  // 基础风险值
  const 越界风险表: Record<越界行为, number> = {
    '言语调戏': 10,
    '肢体接触': 25,
    '强迫喝酒': 35,
    '强行搂抱': 50,
    '言语威胁': 70,
    '动手动脚': 60,
    '强行带走': 85,
  };

  let 风险值 = 越界风险表[越界类型];

  // 麻木度降低风险感知
  风险值 -= (服务人员麻木度 - 50) * 0.2;

  // 自我保护能力
  if (服务人员自我保护 > 60) {
    风险值 -= 20;
  }

  // 醉酒程度影响
  const 醉酒影响表: Record<醉酒程度, number> = {
    '清醒': -10,
    '微醺': 0,
    '上头': 20,
    '大醉': 40,
    '烂醉': 60,
  };
  风险值 += 醉酒影响表[消费者醉酒程度];

  // 冲动程度
  风险值 += (消费者冲动程度 - 50) * 0.2;

  风险值 = Math.max(0, Math.min(100, 风险值));

  // 是否抵抗
  const 是否会抵抗 = Math.random() * 100 > 风险值;

  return { 风险值, 是否会抵抗 };
}

