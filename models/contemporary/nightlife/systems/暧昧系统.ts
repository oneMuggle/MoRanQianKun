/**
 * 暧昧升温事件判定
 */
export function 判定暧昧升温(params: {
  当前暧昧值: number;
  醉酒值: number;
  气氛值: number; // 0-100
  是否有肢体接触: boolean;
}): { 是否升温: boolean; 升温幅度: number; 触发事件?: string } {
  const { 当前暧昧值, 醉酒值, 气氛值, 是否有肢体接触 } = params;

  // 基础升温概率
  let 升温概率 = 20;

  // 醉酒加成
  if (醉酒值 > 60) 升温概率 += 25;
  else if (醉酒值 > 40) 升温概率 += 15;

  // 气氛加成
  if (气氛值 > 70) 升温概率 += 20;
  else if (气氛值 > 50) 升温概率 += 10;

  // 肢体接触加成
  if (是否有肢体接触) 升温概率 += 15;

  // 暧昧值已经很高时更容易升温
  if (当前暧昧值 > 60) 升温概率 += 10;

  const 是否升温 = Math.random() * 100 < 升温概率;

  if (!是否升温) {
    return { 是否升温: false, 升温幅度: 0 };
  }

  // 计算升温幅度
  let 升温幅度 = 5 + Math.floor(Math.random() * 15);
  
  if (醉酒值 > 60) 升温幅度 += 10;
  if (气氛值 > 70) 升温幅度 += 5;
  if (是否有肢体接触) 升温幅度 += 5;

  // 判断触发事件
  let 触发事件: string | undefined;
  const 新暧昧值 = 当前暧昧值 + 升温幅度;
  
  if (新暧昧值 >= 90 && 当前暧昧值 < 90) {
    触发事件 = '即将越界';
  } else if (新暧昧值 >= 70 && 当前暧昧值 < 70) {
    触发事件 = '正式确立暧昧关系';
  }

  return { 是否升温: true, 升温幅度, 触发事件 };
}
