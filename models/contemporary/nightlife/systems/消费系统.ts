/**
 * 计算包厢费用
 */
export function 计算包厢费用(
  场所类型: 夜场类型,
  包厢大小: '小包' | '中包' | '大包' | '豪华包' | '总统包',
  时长: number // 小时
): number {
  const 费用表 = 场所费用表[场所类型];
  
  if (!费用表 || !('包厢费' in 费用表)) {
    // 蹦迪酒吧、静吧等按其他方式计费
    return 0;
  }

  const 单价 = (费用表.包厢费 as Record<string, number>)[包厢大小] || 0;
  return 单价 * 时长;
}

/**
 * 计算酒水消费
 */
export function 计算酒水消费(
  酒水: 酒水类型,
  档次: 酒水档次,
  数量: number
): { 总价: number; 利润: number } {
  const 价格表 = 酒水价格表[酒水];
  
  if (!价格表) {
    return { 总价: 0, 利润: 0 };
  }

  const 档次价格 = (价格表 as Record<string, { 进价: number; 售价: number }>)[档次];
  
  if (!档次价格) {
    return { 总价: 0, 利润: 0 };
  }

  const 进价 = 档次价格.进价 * 数量;
  const 售价 = 档次价格.售价 * 数量;
  
  return {
    总价: 售价,
    利润: 售价 - 进价,
  };
}

/**
 * 计算陪酒服务费
 */
export function 计算陪酒服务费(
  场所类型: 夜场类型,
  服务类型: '公主' | '少爷' | '模特',
  时长: number
): number {
  const 费用表 = 场所费用表[场所类型];
  
  if (!费用表) return 0;

  let 单价 = 0;
  if (服务类型 === '公主' && '公主费' in 费用表) {
    单价 = 费用表.公主费;
  } else if (服务类型 === '少爷' && '少爷费' in 费用表) {
    单价 = 费用表.少爷费;
  } else if (服务类型 === '模特' && '模特费' in 费用表) {
    单价 = 费用表.模特费;
  }

  return 单价 * 时长;
}

/**
 * 计算小费
 */
export function 计算小费(
  服务质量: number, // 0-100
  客人大方程度: '吝啬' | '普通' | '大方' | '挥金如土',
  场所档次: 场所档次
): number {
  const 基础小费表: Record<场所档次, number> = {
    '经济型': 100,
    '中档型': 300,
    '高档型': 500,
    '豪华型': 1000,
    '顶级私人': 2000,
  };

  const 大方系数表 = {
    '吝啬': 0.5,
    '普通': 1,
    '大方': 1.5,
    '挥金如土': 2.5,
  };

  const 基础小费 = 基础小费表[场所档次];
  const 大方系数 = 大方系数表[客人大方程度];
  const 服务系数 = 0.5 + (服务质量 / 100) * 1;

  return Math.floor(基础小费 * 大方系数 * 服务系数);
}

/**
 * 计算出台费
 */
export function 计算出台费(
  服务人员级别: '普通' | '高级' | '头牌',
  客人魅力: number, // 1-10
  场所档次: 场所档次
): number {
  const 基础出台费表: Record<'普通' | '高级' | '头牌', number> = {
    '普通': 1000,
    '高级': 3000,
    '头牌': 5000,
  };

  const 档次系数表: Record<场所档次, number> = {
    '经济型': 0.8,
    '中档型': 1,
    '高档型': 1.5,
    '豪华型': 2,
    '顶级私人': 3,
  };

  const 基础出台费 = 基础出台费表[服务人员级别];
  const 档次系数 = 档次系数表[场所档次];
  const 魅力系数 = 0.8 + (客人魅力 / 10) * 0.4;

  return Math.floor(基础出台费 * 档次系数 * 魅力系数);
}

/**
 * 计算总账单
 */
export interface 账单明细 {
  包厢费: number;
  酒水费: number;
  陪酒服务费: number;
  果盘零食: number;
  小费: number;
  出台费: number;
  其他: number;
  总计: number;
}

export function 计算总账单(params: {
  场所类型: 夜场类型;
  包厢大小: '小包' | '中包' | '大包' | '豪华包' | '总统包';
  时长: number;
  酒水列表: Array<{ 类型: 酒水类型; 档次: 酒水档次; 数量: number }>;
  公主数量: number;
  少爷数量: number;
  模特数量: number;
  果盘费: number;
  零食费: number;
  小费总额: number;
  出台次数: number;
}): 账单明细 {
  const {
    场所类型,
    包厢大小,
    时长,
    酒水列表,
    公主数量,
    少爷数量,
    模特数量,
    果盘费,
    零食费,
    小费总额,
    出台次数,
  } = params;

  // 包厢费
  const 包厢费 = 计算包厢费用(场所类型, 包厢大小, 时长);

  // 酒水费
  let 酒水费 = 0;
  for (const 酒水 of 酒水列表) {
    const 消费 = 计算酒水消费(酒水.类型, 酒水.档次, 酒水.数量);
    酒水费 += 消费.总价;
  }

  // 陪酒服务费
  const 公主费 = 计算陪酒服务费(场所类型, '公主', 时长) * 公主数量;
  const 少爷费 = 计算陪酒服务费(场所类型, '少爷', 时长) * 少爷数量;
  const 模特费 = 计算陪酒服务费(场所类型, '模特', 时长) * 模特数量;
  const 陪酒服务费 = 公主费 + 少爷费 + 模特费;

  // 出台费（简化计算）
  const 出台费 = 出台次数 * 2000;

  const 总计 = 包厢费 + 酒水费 + 陪酒服务费 + 果盘费 + 零食费 + 小费总额 + 出台费;

  return {
    包厢费,
    酒水费,
    陪酒服务费,
    果盘零食: 果盘费 + 零食费,
    小费: 小费总额,
    出台费,
    其他: 0,
    总计,
  };
}

