import type { 消费者核心状态, 醉酒程度, 醉酒影响因素 } from '../types';
import { 醉酒演变表, 醉酒影响因素表, 获取醉酒程度 } from '../states/消费者状态';

/**
 * 醉酒状态管理器
 */
export class 醉酒系统 {
  /**
   * 计算饮酒后的醉酒值
   */
  static 计算醉酒值(
    当前醉酒值: number,
    饮酒量: number,
    影响因素: keyof 醉酒影响因素 = '体质'
  ): number {
    const 影响值 = 醉酒影响因素表[影响因素].影响;
    return Math.min(100, 当前醉酒值 + 饮酒量 * 影响值);
  }

  /**
   * 获取醉酒状态描述
   */
  static 获取醉酒状态描述(醉酒值: number): {
    程度: 醉酒程度;
    理智程度: number;
    冲动程度: number;
    表现: string;
    风险等级: string;
  } {
    const 程度 = 获取醉酒程度(醉酒值);
    const 演变 = 醉酒演变表[程度];
    return {
      程度,
      理智程度: 演变.理智程度,
      冲动程度: 演变.冲动程度,
      表现: 演变.表现,
      风险等级: 演变.风险,
    };
  }

  /**
   * 计算醒酒时间（小时）
   */
  static 计算醒酒时间(醉酒值: number): number {
    // 平均每小时醒酒 15-20%
    return 醉酒值 / 18;
  }

  /**
   * 醉酒状态下的越界概率
   */
  static 计算越界概率(醉酒程度: 醉酒程度): number {
    const 概率表: Record<醉酒程度, number> = {
      '清醒': 0.1,
      '微醺': 0.3,
      '上头': 0.6,
      '大醉': 0.9,
      '烂醉': 1.0,
    };
    return 概率表[醉酒程度];
  }

  /**
   * 醉酒状态下的自我保护能力
   */
  static 计算自我保护能力(醉酒程度: 醉酒程度): number {
    const 能力表: Record<醉酒程度, number> = {
      '清醒': 80,
      '微醺': 50,
      '上头': 30,
      '大醉': 10,
      '烂醉': 0,
    };
    return 能力表[醉酒程度];
  }

  /**
   * 更新消费者醉酒状态
   */
  static 更新醉酒状态(
    状态: 消费者核心状态,
    饮酒量: number,
    影响因素: keyof 醉酒影响因素 = '体质'
  ): 消费者核心状态 {
    const 新醉酒值 = this.计算醉酒值(状态.醉酒值, 饮酒量, 影响因素);
    const 醉酒信息 = this.获取醉酒状态描述(新醉酒值);

    return {
      ...状态,
      醉酒值: 新醉酒值,
      醉酒程度: 醉酒信息.程度,
      理智程度: 醉酒信息.理智程度,
      冲动程度: 醉酒信息.冲动程度,
    };
  }

  /**
   * 模拟时间流逝（醒酒）
   */
  static 醒酒(状态: 消费者核心状态, 经过小时数: number): 消费者核心状态 {
    const 醒酒量 = 经过小时数 * 18; // 每小时醒酒 18%
    const 新醉酒值 = Math.max(0, 状态.醉酒值 - 醒酒量);
    const 醉酒信息 = this.获取醉酒状态描述(新醉酒值);

    return {
      ...状态,
      醉酒值: 新醉酒值,
      醉酒程度: 醉酒信息.程度,
      理智程度: 醉酒信息.理智程度,
      冲动程度: 醉酒信息.冲动程度,
      兴奋程度: Math.max(10, 状态.兴奋程度 - 经过小时数 * 10),
    };
  }

  /**
   * 醉酒状态下的是否会答应出台
   */
  static 会同意出台(状态: 消费者核心状态, 诱惑程度: number): boolean {
    const 越界概率 = this.计算越界概率(状态.醉酒程度);
    const 理智因子 = 状态.理智程度 / 100;
    const 冲动因子 = 状态.冲动程度 / 100;
    const 诱惑因子 = 诱惑程度 / 100;

    // 综合判断
    const 同意概率 = 越界概率 * 0.4 + (1 - 理智因子) * 0.3 + 冲动因子 * 0.2 + 诱惑因子 * 0.1;
    
    return Math.random() < 同意概率;
  }
}

