/**
 * 根据消费能力获取钱包厚度
 */
function 获取钱包厚度(消费能力: 消费能力): number {
  const 钱包表: Record<消费能力, number> = {
    '平民': 1000,
    '中产': 10000,
    '富豪': 50000,
    '挥金如土': 200000,
  };
  return 钱包表[消费能力];
}

/**
 * 醉酒程度映射
 */
export function 获取醉酒程度(醉酒值: number): 醉酒程度 {
  if (醉酒值 < 20) return '清醒';
  if (醉酒值 < 40) return '微醺';
  if (醉酒值 < 60) return '上头';
  if (醉酒值 < 80) return '大醉';
  return '烂醉';
}

