/**
 * 更新消费者醉酒状态
 */
export function 更新醉酒状态(
  状态: 消费者核心状态,
  喝酒量: number,
  因素: keyof typeof 醉酒影响因素表 = '体质'
): 消费者核心状态 {
  const 影响值 = 醉酒影响因素表[因素].影响;
  const 新醉酒值 = Math.min(100, 状态.醉酒值 + 喝酒量 * 影响值);
  const 新醉酒程度 = 获取醉酒程度(新醉酒值);
  const 演变 = 醉酒演变表[新醉酒程度];

  return {
    ...状态,
    醉酒值: 新醉酒值,
    醉酒程度: 新醉酒程度,
    理智程度: 演变.理智程度,
    冲动程度: 演变.冲动程度,
    兴奋程度: Math.min(100, 状态.兴奋程度 + 喝酒量 * 0.5),
  };
}

/**
 * 计算越界风险
 */
export function 计算越界风险(状态: 消费者核心状态): number {
  const 醉酒风险表 = {
    '清醒': 0.1,
    '微醺': 0.3,
    '上头': 0.6,
    '大醉': 0.9,
    '烂醉': 1.0,
  };

  const 醉酒风险 = 醉酒风险表[状态.醉酒程度];
  const 理智因子 = (100 - 状态.理智程度) / 100;
  const 冲动因子 = 状态.冲动程度 / 100;
  const 防范因子 = (100 - 状态.防范意识) / 100;

  return Math.min(1, 醉酒风险 * 0.4 + 理智因子 * 0.2 + 冲动因子 * 0.2 + 防范因子 * 0.2);
}
