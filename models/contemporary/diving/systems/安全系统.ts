/**
 * 计算事故风险指数
 */
export function 计算事故风险指数(
  潜水员经验: number,
  装备状况: number,
  环境条件: number,  // 0-100, 越高越危险
  深度: number,
  水流强度: '无' | '弱' | '中' | '强'
): number {
  let 风险 = 20;  // 基础风险

  // 经验不足增加风险
  if (潜水员经验 < 10) 风险 += 30;
  else if (潜水员经验 < 50) 风险 += 15;
  else if (潜水员经验 >= 100) 风险 -= 10;

  // 装备状况差增加风险
  风险 += (100 - 装备状况) / 4;

  // 环境条件差增加风险
  风险 += 环境条件 / 4;

  // 深度超过限制增加风险
  if (深度 > 30) 风险 += 20;
  else if (深度 > 18) 风险 += 10;

  // 水流强度增加风险
  const 水流加成 = { '无': 0, '弱': 5, '中': 15, '强': 30 };
  风险 += 水流加成[水流强度];

  return Math.max(0, Math.min(100, Math.round(风险)));
}

/**
 * 判断是否需要紧急上升
 */
export function 需要紧急上升(
  气源剩余: number,  // 百分比
  紧急情况: string[],
  深度: number
): {
  需要紧急上升: boolean;
  原因: string;
  建议: string;
} {
  // 气源极低
  if (气源剩余 < 20) {
    return {
      需要紧急上升: true,
      原因: '气源严重不足',
      建议: '立即开始紧急上升程序',
    };
  }

  // 有严重紧急情况
  const 严重情况 = ['潜水疾病', '恐慌发作', '紧急上升'];
  for (const 情况 of 紧急情况) {
    const 配置 = 紧急情况配置[情况];
    if (配置 && 配置.严重程度 === '严重') {
      return {
        需要紧急上升: true,
        原因: 配置.名称,
        建议: 配置.需要行动.join(' -> '),
      };
    }
  }

  // 深度超过安全范围且有问题
  if (深度 > 40) {
    return {
      需要紧急上升: true,
      原因: '深度超过安全范围',
      建议: '立即上升至安全深度',
    };
  }

  return {
    需要紧急上升: false,
    原因: '',
    建议: '继续正常潜水',
  };
}

/**
 * 隐私风险评估
 */
export function 评估隐私风险(
  场所私密等级: number,
  监控覆盖: number,
  参与者数量: number,
  酒精程度: number,
  是否有摄影师: boolean
): {
  风险等级: '低' | '中' | '高';
  风险点: string[];
  建议: string[];
} {
  let 风险 = 0;
  const 风险点: string[] = [];
  const 建议: string[] = [];

  // 私密等级低
  if (场所私密等级 < 50) {
    风险 += 30;
    风险点.push('场所私密性低');
    建议.push('注意言行，避免敏感场景');
  }

  // 监控覆盖高
  if (监控覆盖 > 50) {
    风险 += 25;
    风险点.push('存在监控设备');
    建议.push('假设所有行为都会被记录');
  }

  // 参与者多
  if (参与者数量 > 20) {
    风险 += 15;
    风险点.push('人多眼杂');
    建议.push('敏感行为可能被围观');
  }

  // 酒精影响
  if (酒精程度 > 7) {
    风险 += 20;
    风险点.push('酒精导致判断力下降');
    建议.push('适量饮酒，保持清醒');
  }

  // 有摄影师
  if (是否有摄影师) {
    风险 += 30;
    风险点.push('存在摄影师');
    建议.push('假设所有行为都可能被拍摄');
  }

  // 判断风险等级
  let 风险等级: '低' | '中' | '高';
  if (风险 < 30) 风险等级 = '低';
  else if (风险 < 60) 风险等级 = '中';
  else 风险等级 = '高';

  return { 风险等级, 风险点, 建议 };
}
