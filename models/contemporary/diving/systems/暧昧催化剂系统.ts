/**
 * 计算当前暧昧指数
 */
export function 计算暧昧指数(
  当前阶段: 暧昧升级,
  催化剂数量: number,
  催化剂加成: number,
  环境加成: number = 0
): number {
  const 阶段配置 = 暧昧升级配置[当前阶段];
  const 基础指数 = 阶段配置.身体接触程度;

  // 催化剂积累
  const 催化剂指数 = 催化剂数量 * 8;

  // 催化剂效果加成
  const 效果加成 = 催化剂加成;

  // 环境加成
  const 总指数 = 基础指数 + 催化剂指数 + 效果加成 + 环境加成;

  return Math.max(0, Math.min(100, Math.round(总指数)));
}

/**
 * 判断是否可以升级
 */
export function 可以升级到下一阶段(
  当前阶段: 暧昧升级,
  催化剂数量: number,
  心理接受度: number
): {
  可以升级: boolean;
  所需催化剂: number;
  缺少催化剂: number;
  提示: string;
} {
  const 当前配置 = 暧昧升级配置[当前阶段];
  const 等级顺序: 暧昧升级[] = ['眼神接触', '身体接触', '语言暧昧', '单独约会', '亲密接触', '确认关系'];
  const 当前索引 = 等级顺序.indexOf(当前阶段);

  if (当前索引 === 等级顺序.length - 1) {
    return {
      可以升级: false,
      所需催化剂: 0,
      缺少催化剂: 0,
      提示: '已经到达最高阶段',
    };
  }

  const 下一阶段 = 等级顺序[当前索引 + 1];
  const 下一配置 = 暧昧升级配置[下一阶段];

  const 催化剂满足 = 催化剂数量 >= 下一配置.所需催化剂数;
  const 心理满足 = 心理接受度 >= 下一配置.心理接受度;

  if (催化剂满足 && 心理满足) {
    return {
      可以升级: true,
      所需催化剂: 下一配置.所需催化剂数,
      缺少催化剂: 0,
      提示: `可以升级到「${下一配置.名称}」`,
    };
  }

  return {
    可以升级: false,
    所需催化剂: 下一配置.所需催化剂数,
    缺少催化剂: Math.max(0, 下一配置.所需催化剂数 - 催化剂数量),
    提示: `还需要${Math.max(0, 下一配置.所需催化剂数 - 催化剂数量)}个催化剂才能升级`,
  };
}

/**
 * 获取下一个阶段
 */
export function 获取下一阶段(当前阶段: 暧昧升级): 暧昧升级 | null {
  const 等级顺序: 暧昧升级[] = ['眼神接触', '身体接触', '语言暧昧', '单独约会', '亲密接触', '确认关系'];
  const 当前索引 = 等级顺序.indexOf(当前阶段);
  
  if (当前索引 === 等级顺序.length - 1) {
    return null;
  }

  return 等级顺序[当前索引 + 1];
}
