/**
 * 计算别墅暧昧指数
 */
export function 计算别墅暧昧指数(
  激活场景数: number,
  氛围数量: number,
  空间设施加分: number,
  酒精程度: number
): number {
  let 指数 = 20;  // 基础指数

  // 场景加成
  指数 += 激活场景数 * 12;

  // 氛围加成
  指数 += 氛围数量 * 8;

  // 空间设施加成
  指数 += 空间设施加分;

  // 酒精加成
  指数 += (酒精程度 - 5) * 2;

  return Math.max(0, Math.min(100, Math.round(指数)));
}

/**
 * 判断别墅私密性
 */
export function 判断别墅私密性(
  管家服务: '随叫随到' | '定时' | '不打扰',
  监控覆盖: number,
  进入方式: '水路' | '陆路' | '两者',
  周边别墅距离: '很近' | '较近' | '很远',
): {
  私密等级: '极高' | '高' | '中' | '低';
  风险点: string[];
} {
  let 等级: '极高' | '高' | '中' | '低' = '高';
  const 风险点: string[] = [];

  // 管家服务影响
  if (管家服务 === '随叫随到') {
    等级 = '中';
    风险点.push('管家可能随时出现');
  } else if (管家服务 === '不打扰') {
    等级 = '极高';
  }

  // 监控影响
  if (监控覆盖 > 50) {
    风险点.push('可能有监控');
    if (等级 === '极高') 等级 = '高';
  }

  // 进入方式
  if (进入方式 === '陆路') {
    风险点.push('可能有人路过');
  }

  // 周边距离
  if (周边别墅距离 === '很近') {
    风险点.push('隔音可能不好');
  }

  return { 私密等级: 等级, 风险点 };
}
