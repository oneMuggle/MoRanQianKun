/**
 * 计算潜伴互助暧昧指数
 */
export function 计算潜伴暧昧指数(
  场景: string,
  教练暧昧倾向: number,
  学员放松程度: number,
  是否有异性: boolean
): number {
  const 场景配置 = 潜伴互助场景配置[场景];
  if (!场景配置) return 0;

  let 指数 = 场景配置.暧昧指数;

  // 教练暧昧倾向加成
  指数 += (教练暧昧倾向 - 50) / 2;

  // 学员放松程度加成
  指数 += (学员放松程度 - 50) / 4;

  // 异性加成
  if (是否有异性) {
    指数 += 15;
  }

  return Math.max(0, Math.min(100, Math.round(指数)));
}

/**
 * 判断潜伴关系是否正常
 */
export function 判断潜伴关系正常(
  教练暧昧倾向: number,
  学员水下焦虑度: number
): {
  正常: boolean;
  警告: string | null;
  风险等级: '低' | '中' | '高';
} {
  // 教练暧昧倾向过高且学员焦虑低 = 可能存在问题
  if (教练暧昧倾向 > 70 && 学员水下焦虑度 < 30) {
    return {
      正常: false,
      警告: '教练行为可能超出正常教学范围',
      风险等级: '高',
    };
  }

  // 教练暧昧倾向中等且学员焦虑高 = 可能需要关注
  if (教练暧昧倾向 > 50 && 学员水下焦虑度 > 60) {
    return {
      正常: false,
      警告: '学员可能在焦虑状态下被利用',
      风险等级: '中',
    };
  }

  // 正常情况
  return {
    正常: true,
    警告: null,
    风险等级: '低',
  };
}
