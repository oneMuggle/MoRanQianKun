/**
 * 计算派对整体暧昧指数
 */
export function 计算派对暧昧指数(
  派对类型: string,
  酒精摄入程度: number,
  催化剂激活数量: number,
  参与人数: number
): number {
  const 类型配置 = 派对类型配置[派对类型];
  if (!类型配置) return 0;

  let 指数 = 类型配置.暧昧指数;

  // 酒精加成
  指数 += (酒精摄入程度 - 5) * 3;  // 假设10分制

  // 催化剂加成
  指数 += 催化剂激活数量 * 8;

  // 人数影响（人太少没氛围，人太多没重点）
  if (参与人数 >= 5 && 参与人数 <= 20) {
    指数 += 10;
  } else if (参与人数 > 50) {
    指数 -= 20;
  }

  return Math.max(0, Math.min(100, Math.round(指数)));
}
