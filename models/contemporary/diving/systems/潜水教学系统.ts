/**
 * 计算潜水等级
 */
export function 计算潜水等级(总潜水次数: number, 总潜水时长: number): 潜水等级 {
  if (总潜水次数 >= 200 && 总潜水时长 >= 150) return '教练';
  if (总潜水次数 >= 100 && 总潜水时长 >= 80) return 'DM';
  if (总潜水次数 >= 50 && 总潜水时长 >= 40) return '救援潜水员';
  if (总潜水次数 >= 20 && 总潜水时长 >= 15) return 'AOW';
  return 'OW';
}

/**
 * 获取升级进度
 */
export function 获取升级进度(当前等级: 潜水等级, 总潜水次数: number, 总潜水时长: number): {
  下一等级: 潜水等级 | null;
  进度: number;
  差距次数: number;
  差距时长: number;
} {
  const 等级顺序: 潜水等级[] = ['OW', 'AOW', '救援潜水员', 'DM', '教练'];
  const 当前索引 = 等级顺序.indexOf(当前等级);
  
  if (当前索引 === 等级顺序.length - 1) {
    return { 下一等级: null, 进度: 100, 差距次数: 0, 差距时长: 0 };
  }

  const 下一等级 = 等级顺序[当前索引 + 1];
  const 下一条件 = 潜水等级系统[下一等级].升级条件;

  const 次数进度 = Math.min(1, 总潜水次数 / 下一条件.总潜水次数);
  const 时长进度 = Math.min(1, 总潜水时长 / 下一条件.总潜水时长);
  const 进度 = Math.round(((次数进度 + 时长进度) / 2) * 100);

  return {
    下一等级,
    进度,
    差距次数: Math.max(0, 下一条件.总潜水次数 - 总潜水次数),
    差距时长: Math.max(0, 下一条件.总潜水时长 - 总潜水时长),
  };
}

/**
 * 判断是否可以升级
 */
export function 可以升级(当前等级: 潜水等级, 总潜水次数: number, 总潜水时长: number): boolean {
  const 等级顺序: 潜水等级[] = ['OW', 'AOW', '救援潜水员', 'DM', '教练'];
  const 当前索引 = 等级顺序.indexOf(当前等级);
  
  if (当前索引 === 等级顺序.length - 1) {
    return false;
  }

  const 下一等级 = 等级顺序[当前索引 + 1];
  const 条件 = 潜水等级系统[下一等级].升级条件;

  return 总潜水次数 >= 条件.总潜水次数 && 总潜水时长 >= 条件.总潜水时长;
}
