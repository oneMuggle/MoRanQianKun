/**
 * 获取别墅场景描述
 */
export function 获取别墅场景描述(
  别墅类型: string,
  时间: string,
  地点: string,
  空间设施: string[],
  氛围: string[]
): string {
  const 场景 = 水上别墅场景配置[别墅类型] || 水上别墅场景配置['水上别墅'];
  const 时间氛围 = 别墅时间段氛围[时间] || 别墅时间段氛围['傍晚'];
  
  return `
【${场景.场景名称}】
时间: ${时间} - ${时间氛围.氛围}
地点: ${地点}

${场景.场景描述}

空间设施:
${空间设施.map(s => `- ${s}`).join('\n')}

氛围:
${氛围.map(a => `- ${a}`).join('\n')}

暧昧指数: ${场景.暧昧指数}%
私密性: ${场景.私密性}
消费水平: ${场景.消费水平}

推荐活动: ${时间氛围.活动建议}
`;
}

