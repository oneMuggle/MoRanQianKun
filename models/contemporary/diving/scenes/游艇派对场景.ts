/**
 * 获取派对场景描述
 */
export function 获取游艇派对描述(
  派对类型: string,
  时间: string,
  地点: string,
  激活催化剂: string[]
): string {
  const 场景 = 游艇派对场景配置[派对类型] || 游艇派对场景配置['私人派对'];
  
  return `
【${场景.场景名称}】
时间: ${时间}
地点: ${地点}

${场景.场景描述}

暧昧指数: ${场景.暧昧指数}%
私密性: ${场景.私密性}
消费水平: ${场景.消费水平}

激活的催化剂:
${激活催化剂.map(c => `- ${c}`).join('\n')}
`;
}
