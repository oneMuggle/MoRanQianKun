/**
 * 计算项目暧昧指数
 */
export function 计算项目暧昧指数(状态: 潜水项目状态): number {
  const 类型配置 = 潜水类型配置[状态.类型];
  let 指数 = 类型配置.暧昧指数;

  // 催化剂加成
  指数 += 状态.暧昧催化剂.length * 10;

  // 环境条件加成
  if (状态.天气 === '阴') 指数 += 5;
  if (状态.水流 === '强') 指数 += 15;
  if (状态.深度 > 20) 指数 += 10;

  // 参与者加成
  if (状态.参与者列表.length === 2) 指数 += 10;

  return Math.min(100, 指数);
}
