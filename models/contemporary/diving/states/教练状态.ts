    本名: partial.本名,
    性别: partial.性别 || '男',
    年龄: partial.年龄 || 30,
    国籍: partial.国籍 || '中国',

    // 资质
    教练等级: partial.教练等级 || '教练',
    所属机构: partial.所属机构 || 'PADI',
    教学经验: partial.教学经验 || 3,
