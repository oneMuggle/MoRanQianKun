/**
 * 判断粉丝是否危险
 */
export function 判断粉丝是否危险(状态: 粉丝核心状态): {
  是否危险: boolean;
  危险类型: string[];
  危险等级: '安全' | '注意' | '警告' | '危险';
} {
  const 危险类型: string[] = [];
  let 危险等级: '安全' | '注意' | '警告' | '危险' = '安全';

  if (状态.骚扰倾向 > 50) {
    危险类型.push('有跟踪骚扰倾向');
    危险等级 = 危险等级 === '安全' ? '警告' : 危险等级 === '注意' ? '危险' : 危险等级;
  }

  if (状态.妄想程度 > 70) {
    危险类型.push('妄想程度严重，认为与创作者有特殊关系');
    危险等级 = (危险等级 as string) === '安全' ? '警告' : (危险等级 as string) === '注意' ? '危险' : 危险等级;
  }

  if (状态.退款纠纷 > 50) {
    危险类型.push('频繁要求退款');
    危险等级 = 危险等级 === '安全' ? '注意' : 危险等级;
  }

  if (状态.边界认知 < 30) {
    危险类型.push('对虚拟/现实边界认知模糊');
    危险等级 = 危险等级 === '安全' ? '注意' : 危险等级;
  }

  if (状态.正常社交 < 30) {
    危险类型.push('现实社交能力严重不足');
    危险等级 = 危险等级 === '安全' ? '注意' : 危险等级;
  }

  return {
    是否危险: 危险等级 === '警告' || 危险等级 === '危险',
    危险类型,
    危险等级,
  };
}

/**
 * 计算粉丝价值
 */
export function 计算粉丝价值(状态: 粉丝核心状态): {
  经济价值: number;
  社交价值: number;
  风险价值: number;
  综合评分: number;
} {
  const { 消费能力, 互动频率, 沉迷程度, 骚扰倾向, 退款纠纷 } = 状态;

  // 经济价值 = 消费能力 * 0.8 + 互动频率 * 2
  const 经济价值 = Math.min(100, 消费能力 * 0.8 + 互动频率 * 2);

  // 社交价值 = 沉迷程度 * 0.6 - 退款纠纷 * 0.4
  const 社交价值 = Math.max(0, Math.min(100, 沉迷程度 * 0.6 - 退款纠纷 * 0.4));

  // 风险价值 = 骚扰倾向 * 0.5 + 退款纠纷 * 0.3 + (100 - 边界认知) * 0.2
  const 风险价值 = Math.max(0, Math.min(100,
    骚扰倾向 * 0.5 + 退款纠纷 * 0.3 + (100 - 状态.边界认知) * 0.2
  ));

  // 综合评分 = 经济价值 * 0.6 + 社交价值 * 0.3 - 风险价值 * 0.5
  const 综合评分 = Math.max(0, Math.min(100,
    经济价值 * 0.6 + 社交价值 * 0.3 - 风险价值 * 0.5
  ));

  return { 经济价值, 社交价值, 风险价值, 综合评分 };
}

