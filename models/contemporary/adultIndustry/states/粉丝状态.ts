/**
 * 判断粉丝是否可以升级
 */
export function 判断是否可以升级(状态: 粉丝核心状态): {
  可以升级: boolean;
  目标类型?: 粉丝类型;
  升级条件?: string;
} {
  const 目标类型 = 粉丝升级条件[状态.类型];

  if (!目标类型) {
    return { 可以升级: false };
  }

  // 升级条件检查
  switch (目标类型) {
    case '订阅粉丝':
      if (状态.消费能力 < 50) {
        return { 可以升级: false, 目标类型, 升级条件: '月消费需达到50元以上' };
      }
      break;
    case '高付费粉丝':
      if (状态.消费能力 < 500) {
        return { 可以升级: false, 目标类型, 升级条件: '月消费需达到500元以上' };
      }
      break;
    case '定制客户':
      if (状态.消费能力 < 300 || 状态.互动频率 < 20) {
        return { 可以升级: false, 目标类型, 升级条件: '月消费需达到300元以上，月互动20次以上' };
      }
      break;
    case '私粉':
      if (状态.消费能力 < 200 || 状态.互动频率 < 30) {
        return { 可以升级: false, 目标类型, 升级条件: '月消费需达到200元以上，月互动30次以上' };
      }
      break;
    case '私生饭':
      if (状态.骚扰倾向 < 30 || 状态.妄想程度 < 50) {
        return { 可以升级: false, 目标类型, 升级条件: '骚扰倾向需达到30以上，妄想程度需达到50以上' };
      }
      break;
  }

  return { 可以升级: true, 目标类型 };
}
