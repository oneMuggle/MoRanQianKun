/**
 * 判断平台是否对创作者友好
 */
export function 判断平台友好度(平台: 平台状态): {
  友好度: '不友好' | '一般' | '友好' | '非常友好';
  创作者支持度: number;
  收入保障度: number;
} {
  let 创作者支持度 = 50;
  let 收入保障度 = 50;

  // 根据审核严格度调整
  if (平台.审核严格度 === '宽松') {
    创作者支持度 += 20;
    收入保障度 += 10;
  } else if (平台.审核严格度 === '严格') {
    创作者支持度 -= 20;
    收入保障度 -= 10;
  }

  // 根据平台规模调整
  if (平台.日活用户 > 1000000) {
    创作者支持度 += 15;
    收入保障度 += 15;
  } else if (平台.日活用户 > 100000) {
    创作者支持度 += 10;
    收入保障度 += 10;
  }

  // 根据分成比例（创作者数量/付费用户）调整
  if (平台.创作者数量 > 0) {
    const 创作者竞争度 = 平台.创作者数量 / Math.max(1, 平台.付费用户);
    if (创作者竞争度 < 0.01) {
      创作者支持度 += 10;
    } else if (创作者竞争度 > 0.1) {
      创作者支持度 -= 10;
    }
  }

  // 根据历史表现调整
  创作者支持度 -= 平台.历史上封禁事件 * 2;
  收入保障度 -= 平台.法律诉讼 * 5;

  创作者支持度 = Math.max(0, Math.min(100, 创作者支持度));
  收入保障度 = Math.max(0, Math.min(100, 收入保障度));

  const 综合 = (创作者支持度 + 收入保障度) / 2;

  let 友好度: '不友好' | '一般' | '友好' | '非常友好' = '一般';
  if (综合 >= 80) {
    友好度 = '非常友好';
  } else if (综合 >= 60) {
    友好度 = '友好';
  } else if (综合 <= 30) {
    友好度 = '不友好';
  }

  return { 友好度, 创作者支持度, 收入保障度 };
}

/**
 * 计算平台审核风险
 */
export function 计算审核风险(平台: 平台状态, 内容分级: string): {
  风险等级: '低' | '中' | '高' | '极高';
  通过概率: number;
  建议分级: string;
} {
  let 基础风险: '低' | '中' | '高' | '极高' = '中';
  let 通过概率 = 0.7;

  // 内容分级影响
  switch (内容分级) {
    case 'G级':
      基础风险 = '低';
      通过概率 = 0.95;
      break;
    case 'R级':
      基础风险 = '中';
      通过概率 = 0.7;
      break;
    case 'NC-17级':
      基础风险 = '高';
      通过概率 = 0.4;
      break;
    case 'XXX级':
      基础风险 = '极高';
      通过概率 = 0.1;
      break;
  }

  // 平台严格度调整
  if (平台.审核严格度 === '宽松') {
    通过概率 *= 1.2;
  } else if (平台.审核严格度 === '严格') {
    通过概率 *= 0.6;
  }

  // AI审核能力调整
  if (平台.AI审核能力 > 80) {
    通过概率 *= 0.8; // AI太强，高风险内容更难通过
  } else if (平台.AI审核能力 < 40) {
    通过概率 *= 1.1; // AI较弱，有机会钻空子
  }

  通过概率 = Math.max(0.01, Math.min(0.99, 通过概率));

  // 确定最终风险等级
  let 风险等级: '低' | '中' | '高' | '极高' = '低';
  if (通过概率 >= 0.8) {
    风险等级 = '低';
  } else if (通过概率 >= 0.5) {
    风险等级 = '中';
  } else if (通过概率 >= 0.2) {
    风险等级 = '高';
  } else {
    风险等级 = '极高';
  }

  // 建议分级
  let 建议分级 = 内容分级;
  if (平台.审核严格度 === '严格' && 内容分级 === 'NC-17级') {
    建议分级 = 'R级';
  } else if (平台.审核严格度 === '宽松' && 内容分级 === 'R级') {
    建议分级 = 'NC-17级';
  }

  return { 风险等级, 通过概率: Math.round(通过概率 * 100), 建议分级 };
}
