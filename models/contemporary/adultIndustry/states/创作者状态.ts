/**
 * 计算心理状态综合评分
 */
export function 计算心理状态综合评分(状态: 创作者核心状态): {
  身心健康: number;
  职业发展: number;
  安全风险: number;
} {
  const {
    职业认同度,
    羞耻度,
    焦虑度,
    幸福度,
    疲劳度,
    曝光风险,
    内容泄露风险,
    恶意粉丝风险,
  } = 状态;

  // 身心健康 = 幸福度 - 羞耻度/2 - 焦虑度/2 - 疲劳度/2
  const 身心健康 = Math.max(0, Math.min(100,
    幸福度 - 羞耻度 / 2 - 焦虑度 / 2 - 疲劳度 / 2
  ));

  // 职业发展 = 职业认同度 * (1 - 疲劳度/200)
  const 职业发展 = Math.max(0, Math.min(100,
    职业认同度 * (1 - 疲劳度 / 200)
  ));

  // 安全风险 = 曝光风险 * 0.5 + 内容泄露风险 * 0.3 + 恶意粉丝风险 * 0.2
  const 安全风险 = Math.max(0, Math.min(100,
    曝光风险 * 0.5 + 内容泄露风险 * 0.3 + 恶意粉丝风险 * 0.2
  ));

  return { 身心健康, 职业发展, 安全风险 };
}

/**
 * 判断创作者是否处于危机状态
 */
export function 判断危机状态(状态: 创作者核心状态): {
  是否危机: boolean;
  危机等级: '无' | '轻微' | '中等' | '严重';
  危机原因: string[];
} {
  const 危机原因: string[] = [];
  let 危机等级: '无' | '轻微' | '中等' | '严重' = '无';

  if (状态.账号状态 === '封禁') {
    危机原因.push('账号被封禁');
    危机等级 = '中等';
  }

  if (状态.账号状态 === '限流') {
    危机原因.push('账号被限流');
    危机等级 = 危机等级 === '无' ? '轻微' : 危机等级;
  }

  if (状态.曝光风险 > 70) {
    危机原因.push('身份曝光风险极高');
    危机等级 = 危机等级 === '轻微' ? '中等' : 危机等级 === '中等' ? '严重' : 危机等级;
  }

  if (状态.焦虑度 > 80) {
    危机原因.push('焦虑度过高');
    危机等级 = 危机等级 === '轻微' ? '中等' : 危机等级;
  }

  if (状态.疲劳度 > 90) {
    危机原因.push('创作疲劳严重');
    危机等级 = 危机等级 === '轻微' ? '轻微' : 危机等级;
  }

  if (状态.月收入 < 1000 && 状态.职业阶段 !== '新人') {
    危机原因.push('收入过低');
    危机等级 = 危机等级 === '无' ? '轻微' : 危机等级;
  }

  return {
    是否危机: 危机等级 !== '无',
    危机等级,
    危机原因,
  };
}
