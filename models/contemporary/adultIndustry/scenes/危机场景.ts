/**
 * 获取危机场景提示词
 */
export function 获取危机场景提示词(params: {
  危机类型: 危机类型;
  危机等级: 危机等级;
  创作者化名: string;
  氛围描述?: string;
}): string {
  const { 危机类型, 危机等级, 创作者化名, 氛围描述 } = params;

  let 危机详情: string;
  let 各方态度: { 创作者: string; 平台: string; 粉丝: string };

  switch (危机类型) {
    case '账号被封':
      危机详情 = '账号被平台永久封禁，所有内容无法访问，收入来源中断。';
      各方态度 = {
        创作者: '焦急万分，紧急寻找解决方案',
        平台: '按规则处理，等待申诉',
        粉丝: '等待消息，部分粉丝开始流失',
      };
      break;
    case '隐私曝光':
      危机详情 = '真实身份被曝光，面临社会舆论压力和潜在的法律风险。';
      各方态度 = {
        创作者: '极度恐慌，需要做出重大决定',
        平台: '可能下架内容，配合调查',
        粉丝: '反应不一，有人支持有人离开',
      };
      break;
    case '内容泄露':
      危机详情 = '作品被非法传播，收入受到影响。';
      各方态度 = {
        创作者: '愤怒但无奈，需要维权',
        平台: '收到投诉，审核处理中',
        粉丝: '部分同情，部分趁机索取免费内容',
      };
      break;
    case '恶意投诉':
      危机详情 = '被恶意举报，账号受到限制。';
      各方态度 = {
        创作者: '愤怒委屈，收集证据维权',
        平台: '审核中，需要更多证据',
        粉丝: '观望中，部分粉丝支持',
      };
      break;
    default:
      危机详情 = '面临危机事件，需要妥善处理。';
      各方态度 = {
        创作者: '冷静处理，评估损失',
        平台: '按规则处理',
        粉丝: '等待官方消息',
      };
  }

  return `【系统角色】
你是${危机类型}危机场景。

【危机详情】
${危机详情}

【危机等级】
${危机等级}

【各方态度】
- 创作者：${各方态度.创作者}
- 平台：${各方态度.平台}
- 粉丝：${各方态度.粉丝}

【场景氛围】
${氛围描述 || '气氛紧张，各方都在等待事态发展'}

【回复要求】
- 展现危机的严重性
- 各方的心理博弈
- 暧昧内容回避
- 留有后续发展空间`;
}

/**
 * 危机恢复场景提示词
 */
export function 获取危机恢复提示词(params: {
  创作者化名: string;
  危机类型: 危机类型;
  处理结果: '成功' | '部分成功' | '失败';
}): string {
  const { 创作者化名, 危机类型, 处理结果 } = params;

  let 结果描述: string;
  let 后续建议: string;

  if (处理结果 === '成功') {
    结果描述 = '危机得到妥善处理，创作者恢复正常运营';
    后续建议 = '总结经验教训，加强预防措施';
  } else if (处理结果 === '部分成功') {
    结果描述 = '危机得到部分缓解，但仍有一定影响';
    后续建议 = '继续努力修复，制定长期计划';
  } else {
    结果描述 = '危机未能有效解决，需要重新评估策略';
    后续建议 = '考虑转型或暂时退出，寻求新的发展方向';
  }

  return `【系统角色】
你是经历了${危机类型}危机的创作者${创作者化名}。

【危机处理结果】
${结果描述}

【后续建议】
${后续建议}

【回复要求】
- 体现危机后的心理状态
- 展现对未来的规划
- 保持积极的应对态度
- 注意内容适当性`;
}
