/**
 * 判定违规类型
 */
export function 判定违规类型(params: {
  裸露程度: number;      // 0-100
  暗示程度: number;       // 0-100
  是否含敏感词: boolean;
  封面暴露度: number;    // 0-100
  内容分级: 内容分级;
}): {
  违规类型: '无' | '轻微' | '中度' | '严重' | '致命';
  违规描述: string[];
  风险评分: number;      // 0-100
} {
  const { 裸露程度, 暗示程度, 是否含敏感词, 封面暴露度, 内容分级 } = params;

  const 违规描述: string[] = [];
  let 风险评分 = 0;

  // 根据裸露程度判定
  if (裸露程度 >= 90) {
    违规描述.push('高清露点内容');
    风险评分 += 50;
  } else if (裸露程度 >= 60) {
    违规描述.push('明确成人内容');
    风险评分 += 30;
  } else if (裸露程度 >= 30) {
    违规描述.push('边缘内容');
    风险评分 += 15;
  }

  // 根据暗示程度判定
  if (暗示程度 >= 70) {
    违规描述.push('高度暗示内容');
    风险评分 += 20;
  } else if (暗示程度 >= 40) {
    违规描述.push('轻度暗示内容');
    风险评分 += 10;
  }

  // 根据敏感词判定
  if (是否含敏感词) {
    违规描述.push('包含敏感词');
    风险评分 += 15;
  }

  // 根据封面暴露度判定
  if (封面暴露度 >= 80) {
    违规描述.push('封面过度暴露');
    风险评分 += 15;
  } else if (封面暴露度 >= 50) {
    违规描述.push('封面暴露');
    风险评分 += 5;
  }

  // 根据内容分级调整
  if (内容分级 === 'XXX级') {
    风险评分 = Math.min(100, 风险评分 * 1.5);
  } else if (内容分级 === 'NC-17级') {
    风险评分 = Math.min(100, 风险评分 * 1.2);
  }

  风险评分 = Math.round(风险评分);

  // 判定违规类型
  let 违规类型: '无' | '轻微' | '中度' | '严重' | '致命' = '无';
  if (风险评分 >= 60) {
    违规类型 = '致命';
  } else if (风险评分 >= 40) {
    违规类型 = '严重';
  } else if (风险评分 >= 20) {
    违规类型 = '中度';
  } else if (风险评分 >= 10) {
    违规类型 = '轻微';
  }

  return { 违规类型, 违规描述, 风险评分 };
}

/**
 * 判定处罚结果
 */
export function 判定处罚结果(params: {
  违规类型: '无' | '轻微' | '中度' | '严重' | '致命';
  违规次数: number;
}): {
  处罚结果: string;
  处罚天数: number;
  是否永久封禁: boolean;
  是否法律追诉: boolean;
} {
  const { 违规类型, 违规次数 } = params;

  if (违规类型 === '无') {
    return {
      处罚结果: '无违规',
      处罚天数: 0,
      是否永久封禁: false,
      是否法律追诉: false,
    };
  }

  const 配置 = 违规处罚配置[
    违规类型 === '致命' ? '高清露点内容' :
    违规类型 === '严重' ? '明确成人内容' :
    违规类型 === '中度' ? '边缘内容' :
    '封面暴露'
  ];

  let 处罚结果: string;
  let 处罚天数 = 0;
  let 是否永久封禁 = false;
  let 是否法律追诉 = false;

  if (违规次数 === 1) {
    处罚结果 = 配置.第一次;
  } else if (违规次数 === 2) {
    处罚结果 = 配置.第二次;
  } else {
    处罚结果 = 配置.第三次;
  }

  // 解析处罚结果
  if (处罚结果.includes('警告')) {
    处罚天数 = 0;
  } else if (处罚结果.includes('限流')) {
    处罚天数 = parseInt(处罚结果.match(/\d+/)?.[0] || '7');
  } else if (处罚结果.includes('封禁30天')) {
    处罚天数 = 30;
    是否永久封禁 = false;
  } else if (处罚结果.includes('永久封禁')) {
    处罚天数 = -1; // 表示永久
    是否永久封禁 = true;
  }

  if (处罚结果.includes('法律追诉')) {
    是否法律追诉 = true;
  }

  return { 处罚结果, 处罚天数, 是否永久封禁, 是否法律追诉 };
}

/**
 * 计算平台审核风险
 */
export function 计算平台审核风险(params: {
  审核严格度: 审核严格度;
  内容分级: 内容分级;
  创作者历史违规次数: number;
}): {
  推荐分级: 内容分级;
  通过概率: number;
  建议: string;
} {
  const { 审核严格度, 内容分级, 创作者历史违规次数 } = params;

  const 阈值 = 审核严格度阈值[审核严格度];

  // 根据审核严格度和内容分级计算通过概率
  let 通过概率: number;

  if (内容分级 === 'G级') {
    通过概率 = 95;
  } else if (内容分级 === 'R级') {
    if (审核严格度 === '宽松') {
      通过概率 = 85;
    } else if (审核严格度 === '中等') {
      通过概率 = 70;
    } else {
      通过概率 = 50;
    }
  } else if (内容分级 === 'NC-17级') {
    if (审核严格度 === '宽松') {
      通过概率 = 60;
    } else if (审核严格度 === '中等') {
      通过概率 = 35;
    } else {
      通过概率 = 15;
    }
  } else {
    // XXX级
    if (审核严格度 === '宽松') {
      通过概率 = 30;
    } else if (审核严格度 === '中等') {
      通过概率 = 10;
    } else {
      通过概率 = 2;
    }
  }

  // 根据历史违规次数调整
  通过概率 -= 创作者历史违规次数 * 10;
  通过概率 = Math.max(1, Math.min(99, 通过概率));

  // 推荐分级
  let 推荐分级: 内容分级 = 内容分级;
  let 建议: string;

  if (通过概率 < 30 && 审核严格度 === '严格') {
    推荐分级 = 'R级';
    建议 = '建议降低内容分级至R级，或更换至宽松平台';
  } else if (通过概率 < 50 && 审核严格度 === '中等') {
    推荐分级 = 'R级';
    建议 = '建议降低内容分级至R级以提高通过率';
  } else if (通过概率 >= 70) {
    建议 = '当前分级与平台匹配度较好';
  } else {
    建议 = '内容分级与平台存在一定风险，建议谨慎操作';
  }

  return { 推荐分级, 通过概率, 建议 };
}
