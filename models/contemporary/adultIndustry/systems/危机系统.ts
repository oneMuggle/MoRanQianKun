/**
 * 计算危机触发概率
 */
export function 计算危机触发概率(params: {
  危机类型: 危机类型;
  安全状态: {
    曝光风险: number;
    内容泄露风险: number;
    恶意粉丝风险: number;
    账号状态: string;
  };
  内容分级: string;
}): {
  月触发概率: number;
  风险因素: string[];
} {
  const { 危机类型, 安全状态, 内容分级 } = params;
  const 配置 = 危机类型配置[危机类型];

  let 月触发概率 = 配置.触发概率基数;
  const 风险因素: string[] = [];

  // 安全状态影响
  if (安全状态.曝光风险 > 70) {
    月触发概率 += 15;
    风险因素.push('身份曝光风险高');
  }

  if (安全状态.内容泄露风险 > 50) {
    月触发概率 += 10;
    风险因素.push('内容泄露风险高');
  }

  if (安全状态.恶意粉丝风险 > 50) {
    月触发概率 += 10;
    风险因素.push('恶意粉丝风险');
  }

  if (安全状态.账号状态 === '限流') {
    月触发概率 += 5;
    风险因素.push('账号已被限流');
  }

  // 内容分级影响
  if (内容分级 === 'XXX级') {
    月触发概率 += 20;
    风险因素.push('内容分级为XXX级');
  } else if (内容分级 === 'NC-17级') {
    月触发概率 += 10;
    风险因素.push('内容分级为NC-17级');
  }

  月触发概率 = Math.min(100, Math.max(0, 月触发概率));

  return { 月触发概率, 风险因素 };
}

/**
 * 评估危机处理策略
 */
export function 评估危机处理策略(params: {
  危机类型: 危机类型;
  危机等级: 危机等级;
  资源状态: {
    经济资源: number;     // 可用资金
    粉丝基础: number;     // 当前粉丝数
    平台关系: number;     // 0-100
    法律支援: boolean;    // 是否有法律支援
  };
}): {
  推荐策略: string[];
  备选策略: string[];
  预期成本: { min: number; max: number };
  成功率: number;
} {
  const { 危机类型, 危机等级, 资源状态 } = params;
  const 影响 = 危机等级影响[危机等级];

  const 推荐策略: string[] = [];
  const 备选策略: string[] = [];

  // 根据危机类型添加策略
  const 类型策略 = 危机类型配置[危机类型].典型应对;
  推荐策略.push(...类型策略.slice(0, 2));
  备选策略.push(...类型策略.slice(2));

  // 根据危机等级调整策略
  if (危机等级 === '致命') {
    if (!资源状态.法律支援) {
      推荐策略.push('紧急寻求法律支援');
    }
    推荐策略.push('评估永久退出可能性');
  }

  // 根据资源状态调整
  let 预期成本 = { min: 0, max: 0 };

  if (危机等级 === '轻微') {
    预期成本 = { min: 0, max: 1000 };
  } else if (危机等级 === '中等') {
    预期成本 = { min: 1000, max: 10000 };
  } else if (危机等级 === '严重') {
    预期成本 = { min: 10000, max: 50000 };
  } else {
    预期成本 = { min: 50000, max: 200000 };
  }

  // 计算成功率
  let 成功率 = 80;

  if (资源状态.平台关系 < 30) {
    成功率 -= 20;
  }

  if (!资源状态.法律支援 && (危机类型 === '隐私曝光' || 危机类型 === '法律纠纷')) {
    成功率 -= 30;
  }

  if (资源状态.经济资源 < 预期成本.min) {
    成功率 -= 20;
  }

  成功率 = Math.max(5, Math.min(95, 成功率));

  return { 推荐策略, 备选策略, 预期成本, 成功率 };
}

/**
 * 危机恢复时间估算
 */
export function 估算危机恢复时间(params: {
  危机等级: 危机等级;
  是否及时处理: boolean;
  处理策略正确性: number; // 0-100
}): {
  恢复时间: { min: number; max: number }; // 天
  完全恢复概率: number;
} {
  const { 危机等级, 是否及时处理, 处理策略正确性 } = params;

  let 基础恢复时间: { min: number; max: number };

  if (危机等级 === '轻微') {
    基础恢复时间 = { min: 1, max: 7 };
  } else if (危机等级 === '中等') {
    基础恢复时间 = { min: 7, max: 30 };
  } else if (危机等级 === '严重') {
    基础恢复时间 = { min: 30, max: 90 };
  } else {
    基础恢复时间 = { min: 90, max: 365 };
  }

  // 调整恢复时间
  let 恢复乘数 = 1.0;

  if (是否及时处理) {
    恢复乘数 *= 0.7;
  }

  恢复乘数 *= (1 - 处理策略正确性 / 200);

  const 恢复时间 = {
    min: Math.round(基础恢复时间.min * 恢复乘数),
    max: Math.round(基础恢复时间.max * 恢复乘数),
  };

  // 完全恢复概率
  let 完全恢复概率 = 90;

  if (危机等级 === '严重') {
    完全恢复概率 = 60;
  } else if (危机等级 === '致命') {
    完全恢复概率 = 20;
  }

  if (!是否及时处理) {
    完全恢复概率 -= 20;
  }

  完全恢复概率 = Math.max(5, Math.min(95, 完全恢复概率));

  return { 恢复时间, 完全恢复概率 };
}
