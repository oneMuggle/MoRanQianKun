/**
 * 计算最优变现模式组合
 */
export function 计算最优变现组合(params: {
  职业阶段: '新人' | '成长期' | '稳定期' | '瓶颈期' | '转型期';
  粉丝基数: number;
  活跃粉丝比例: number;
  内容分级: string;
}): {
  推荐组合: string[];
  预期月收入: { min: number; max: number };
  风险评估: string;
} {
  const { 职业阶段, 粉丝基数, 活跃粉丝比例, 内容分级 } = params;

  // 根据职业阶段选择推荐组合
  let 推荐组合: string[];
  if (职业阶段 === '新人') {
    推荐组合 = 粉丝基数 > 500 ? ['订阅制', '按次付费'] : ['按次付费', '打赏制'];
  } else if (职业阶段 === '成长期') {
    推荐组合 = 活跃粉丝比例 > 0.3 ? ['订阅制', '打赏制', '私聊收费'] : ['订阅制', '按次付费'];
  } else if (职业阶段 === '稳定期') {
    推荐组合 = 粉丝基数 > 10000 ? ['定制内容', '线下服务', '私聊收费', '订阅制'] : ['订阅制', '打赏制', '私聊收费'];
  } else if (职业阶段 === '瓶颈期') {
    推荐组合 = ['订阅制', '广告分成', '定制内容'];
  } else {
    推荐组合 = ['订阅制', '按次付费']; // 转型期保守策略
  }

  // 计算预期收入
  let 基础月收入: number;
  if (内容分级 === 'G级') {
    基础月收入 = 粉丝基数 * 0.5;
  } else if (内容分级 === 'R级') {
    基础月收入 = 粉丝基数 * 1.5;
  } else if (内容分级 === 'NC-17级') {
    基础月收入 = 粉丝基数 * 3;
  } else {
    基础月收入 = 粉丝基数 * 5;
  }

  const 活跃粉丝数 = Math.round(粉丝基数 * 活跃粉丝比例);
  const 预期月收入 = Math.round(基础月收入 * (0.5 + 活跃粉丝比例));

  // 风险评估
  let 风险评估: string;
  if (推荐组合.length >= 4) {
    风险评估 = '多元化组合，风险分散较好';
  } else if (推荐组合.includes('线下服务') || 推荐组合.includes('定制内容')) {
    风险评估 = '收入潜力大但风险较高，需注意安全';
  } else if (推荐组合.includes('打赏制')) {
    风险评估 = '收入不稳定，依赖直播效果';
  } else {
    风险评估 = '稳定但增长有限';
  }

  return {
    推荐组合,
    预期月收入: {
      min: Math.round(预期月收入 * 0.3),
      max: Math.round(预期月收入 * 2),
    },
    风险评估,
  };
}

/**
 * 收入来源比例计算
 */
export function 计算收入来源比例(变现模式列表: 变现模式[]): Record<变现模式, number> {
  const 初始比例: Record<变现模式, number> = {
    '订阅制': 0,
    '按次付费': 0,
    '打赏制': 0,
    '定制内容': 0,
    '私聊收费': 0,
    '线下服务': 0,
    '广告分成': 0,
  };

  if (变现模式列表.length === 0) {
    return 初始比例;
  }

  // 根据模式分配权重
  const 权重: Record<变现模式, number> = {
    '订阅制': 25,
    '按次付费': 20,
    '打赏制': 15,
    '定制内容': 20,
    '私聊收费': 10,
    '线下服务': 5,
    '广告分成': 5,
  };

  let 总权重 = 0;
  for (const 模式 of 变现模式列表) {
    总权重 += 权重[模式];
  }

  for (const 模式 of 变现模式列表) {
    初始比例[模式] = Math.round((权重[模式] / 总权重) * 100);
  }

  return 初始比例;
}
