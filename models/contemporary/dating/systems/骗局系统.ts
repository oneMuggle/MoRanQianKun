// ==================== 被骗风险评估 ====================

export interface 被骗风险评估 {
  风险等级: '低' | '中' | '高' | '极高';
  主要风险: string[];
  建议: string[];
}

/**
 * 评估玩家的被骗风险
 */
export function 评估被骗风险(
  相亲者: 相亲者核心状态,
  对象: 相亲对象核心状态
): 被骗风险评估 {
  const 主要风险: string[] = [];
  let 风险值 = 0;

  // 相亲次数少容易被骗
  if (相亲者.相亲次数 < 3) {
    风险值 += 20;
    主要风险.push('相亲经验不足');
  }

  // 骗子识别能力低
  if (相亲者.骗子识别能力 < 40) {
    风险值 += 25;
    主要风险.push('防骗意识薄弱');
  }

  // 年龄焦虑高容易冲动
  if (相亲者.年龄焦虑度 > 70) {
    风险值 += 20;
    主要风险.push('急于脱单容易冲动');
  }

  // 物质焦虑高容易贪图对方条件
  if (相亲者.物质焦虑度 > 70) {
    风险值 += 15;
    主要风险.push('对物质过于看重');
  }

  // 被骗过一次更容易再被骗
  if (相亲者.被骗次数 > 0) {
    风险值 += 15;
    主要风险.push('有被骗史');
  }

  // 对象是骗子
  if (对象.是骗子) {
    风险值 += 对象.欺骗能力;
  }

  // 条件太好要警惕
  if (对象.外貌 === '帅气/漂亮' && 对象.收入 > 40) {
    风险值 += 15;
    主要风险.push('对方条件过于完美');
  }

  风险值 = Math.min(100, 风险值);

  let 风险等级: '低' | '中' | '高' | '极高';
  let 建议: string[];

  if (风险值 >= 80) {
    风险等级 = '极高';
    建议 = [
      '立即停止与对方交往',
      '不要有任何金钱往来',
      '如有财产损失立即报警',
      '可以向平台举报该用户',
    ];
  } else if (风险值 >= 60) {
    风险等级 = '高';
    建议 = [
      '保持警惕，不要轻信对方',
      '拒绝任何金钱要求',
      '不要单独约见',
      '多与朋友商量',
    ];
  } else if (风险值 >= 40) {
    风险等级 = '中';
    建议 = [
      '继续观察，不要急于确定关系',
      '多了解对方背景',
      '保护好个人财产',
    ];
  } else {
    风险等级 = '低';
    建议 = [
      '继续保持正常交往节奏',
      '注意保护个人隐私',
    ];
  }

  return {
    风险等级,
    主要风险,
    建议,
  };
}

// ==================== 应对策略 ====================

/**
 * 生成反诈建议
 */
export function 生成反诈建议(对象: 相亲对象核心状态): string[] {
  const 建议: string[] = [];

  if (对象.是骗子) {
    建议.push('不要继续与对方交往');
    建议.push('拉黑并举报');
    建议.push('如有损失立即报警');
    建议.push('不要删除聊天记录作为证据');
    return 建议;
  }

  // 针对不同骗术的预防建议
  建议.push('不要轻易见面');
  建议.push('不要透露财务状况');
  建议.push('不要借出钱款');
  建议.push('不要裸聊或拍私密照片');
  建议.push('多与朋友家人商量');
  建议.push('选择公共场所见面');
  建议.push('注意保护个人隐私');

  return 建议;
}
