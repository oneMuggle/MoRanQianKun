import type { 相亲者核心状态, 相亲对象核心状态 } from '../types';

// ==================== 离婚原因 ====================

export type 离婚原因 =
  | '出轨'
  | '家暴'
  | '性格不合'
  | '家庭矛盾'
  | '经济纠纷'
  | '长期分居'
  | '赌博/吸毒'
  | '骗婚曝光';

export interface 离婚事件 {
  原因: 离婚原因;
  发生时间: string;
  详细描述: string;
  证据: string[];
}

// ==================== 财产分割 ====================

export interface 财产分割方案 {
  房产: {
    归属: '男方' | '女方' | '平分';
    折价款: number;
    剩余贷款: number;
  };
  存款: {
    总额: number;
    分割比例: number;
    女方可得: number;
  };
  车辆: {
    归属: '男方' | '女方' | '折价';
    折价款: number;
  };
  彩礼: {
    是否返还: boolean;
    返还比例: number;
    理由: string;
  };
  债务: {
    总额: number;
    分担比例: number;
    女方承担: number;
  };
  其他资产: {
    名称: string;
    价值: number;
    归属: '男方' | '女方' | '平分';
  }[];
}

/**
 * 计算财产分割方案
 */
export function 计算财产分割方案(
  丈夫: 相亲者核心状态,
  妻子: 相亲对象核心状态,
  是否有小孩: boolean,
  离婚原因: 离婚原因,
  结婚年数: number
): 财产分割方案 {
  const 彩礼金额 = 10; // 假设彩礼10万（简化处理）

  // 房产分割
  let 房产归属: '男方' | '女方' | '平分' = '平分';
  let 折价款 = 0;
  let 剩余贷款 = 0;

  if (丈夫.房产 === '有贷款') {
    剩余贷款 = 100; // 假设剩余贷款100万
    if (结婚年数 < 3) {
      // 短婚，倾向男方
      房产归属 = '男方';
      折价款 = 50;
    } else if (结婚年数 < 7) {
      房产归属 = '平分';
      折价款 = (500 - 剩余贷款) / 2; // 假设房产价值500万
    } else {
      // 长婚，照顾女方和子女
      房产归属 = 是否有小孩 ? '女方' : '平分';
      折价款 = 是否有小孩 ? 300 : (500 - 剩余贷款) / 2;
    }
  } else if (丈夫.房产 === '无贷款') {
    if (结婚年数 < 3) {
      房产归属 = '男方';
      折价款 = 0;
    } else {
      房产归属 = '平分';
      折价款 = 250; // 假设房产价值500万
    }
  }

  // 存款分割
  const 存款总额 = 丈夫.存款;
  let 分割比例 = 0.5;

  // 无过错方可能获得更多
  if (离婚原因 === '出轨' || 离婚原因 === '家暴') {
    if (丈夫.隐藏属性?.includes('出轨')) {
      分割比例 = 0.6; // 出轨方少分
    }
  }

  // 短婚倾向于返还彩礼
  let 彩礼是否返还 = false;
  let 返还比例 = 0;
  let 返还理由 = '';

  if (结婚年数 < 2 && (离婚原因 === '性格不合' || 离婚原因 === '骗婚曝光')) {
    彩礼是否返还 = true;
    返还比例 = 0.7;
    返还理由 = '结婚时间短，可主张返还彩礼';
  } else if (结婚年数 < 5) {
    彩礼是否返还 = true;
    返还比例 = 0.3;
    返还理由 = '根据实际情况返还部分彩礼';
  }

  // 车辆分割
  const 车辆归属 = 丈夫.车子 === '有/无' || 丈夫.车子 === '普通' ? '男方' : '折价';

  // 债务分割（婚姻期间共同债务）
  const 债务总额 = 丈夫.房贷 ? 100 : 0; // 简化处理
  const 债务分担比例 = 0.5;

  return {
    房产: {
      归属: 房产归属,
      折价款: Math.round(折价款),
      剩余贷款,
    },
    存款: {
      总额: 存款总额,
      分割比例,
      女方可得: Math.round(存款总额 * 分割比例),
    },
    车辆: {
      归属: 车辆归属,
      折价款: 车辆归属 === '折价' ? 10 : 0,
    },
    彩礼: {
      是否返还: 彩礼是否返还,
      返还比例,
      理由: 返还理由,
    },
    债务: {
      总额: 债务总额,
      分担比例: 债务分担比例,
      女方承担: Math.round(债务总额 * 债务分担比例),
    },
    其他资产: [],
  };
}

// ==================== 子女抚养 ====================

export interface 子女抚养方案 {
  子女数量: number;
  抚养权归属: '男方' | '女方';
  抚养费: {
    每月金额: number;
    支付方式: '一次性' | '按月';
    支付到: number; // 支付到几岁
  };
  探视权: {
    频率: string;
    方式: string;
  };
}

/**
 * 计算子女抚养方案
 */
export function 计算子女抚养方案(
  丈夫: 相亲者核心状态,
  妻子: 相亲对象核心状态,
  子女年龄: number
): 子女抚养方案 {
  // 2岁以下原则上跟母亲
  // 2-8岁看双方条件
  // 8岁以上考虑子女意愿

  let 抚养权归属: '男方' | '女方';

  if (子女年龄 < 2) {
    抚养权归属 = '女方';
  } else if (子女年龄 < 8) {
    // 看经济能力和抚养意愿
    if (丈夫.收入 > 妻子.收入 * 1.5) {
      抚养权归属 = '男方';
    } else {
      抚养权归属 = '女方';
    }
  } else {
    // 8岁以上考虑子女意愿，这里简化处理
    抚养权归属 = Math.random() > 0.5 ? '男方' : '女方';
  }

  // 抚养费通常为收入的20-30%
  const 抚养人收入 = 抚养权归属 === '男方' ? 丈夫.收入 : 妻子.收入;
  const 抚养费基数 = 抚养人收入 * 10000 / 12; // 月收入
  const 每月抚养费 = Math.round(抚养费基数 * 0.25);

  return {
    子女数量: 1,
    抚养权归属,
    抚养费: {
      每月金额: Math.max(1000, 每月抚养费),
      支付方式: '按月',
      支付到: 18,
    },
    探视权: {
      频率: '每周末',
      方式: '单方接送',
    },
  };
}

// ==================== 离婚流程 ====================

export interface 离婚流程 {
  方式: '协议离婚' | '诉讼离婚';
  预计时间: string;
  所需材料: string[];
  关键步骤: string[];
  难点: string[];
}

/**
 * 评估离婚流程
 */
export function 评估离婚流程(
  双方是否同意: boolean,
  是否有财产纠纷: boolean,
  是否有子女: boolean,
  是否有家暴证据: boolean
): 离婚流程 {
  const 方式: '协议离婚' | '诉讼离婚' = 双方是否同意 && !是否有财产纠纷 && !是否有子女
    ? '协议离婚'
    : '诉讼离婚';

  const 所需材料 = [
    '身份证',
    '户口本',
    '结婚证',
    '离婚协议书',
  ];

  if (是否有财产纠纷) {
    所需材料.push('财产证明');
    所需材料.push('房产证');
  }

  if (是否有子女) {
    所需材料.push('子女出生证明');
  }

  if (是否有家暴证据) {
    所需材料.push('家暴证据');
  }

  const 关键步骤: string[] = [];
  const 难点: string[] = [];
  let 预计时间 = '';

  if (方式 === '协议离婚') {
    关键步骤.push('双方协商一致');
    关键步骤.push('签订离婚协议书');
    关键步骤.push('到民政局办理');
    预计时间 = '1个月';
  } else {
    关键步骤.push('收集证据');
    关键步骤.push('写起诉书');
    关键步骤.push('法院立案');
    关键步骤.push('庭前调解');
    关键步骤.push('开庭审理');
    关键步骤.push('判决');
    预计时间 = '6-12个月';

    if (是否有财产纠纷) {
      难点.push('财产分割争议');
    }
    if (是否有子女) {
      难点.push('子女抚养权争议');
    }
    if (是否有家暴证据) {
      难点.push('家暴认定');
    }
  }

  return {
    方式,
    预计时间,
    所需材料,
    关键步骤,
    难点,
  };
}

// ==================== 离婚决策 ====================

export interface 离婚决策分析 {
  是否值得离婚: '值得' | '考虑' | '不建议';
  理由: string[];
  风险: string[];
  建议: string[];
}

/**
 * 分析离婚决策
 */
export function 分析离婚决策(
  婚姻满意度: number,
  离婚原因: 离婚原因,
  是否有子女: boolean,
  子女年龄: number,
  经济状况: '好' | '一般' | '差'
): 离婚决策分析 {
  const 理由: string[] = [];
  const 风险: string[] = [];
  let 是否值得离婚: '值得' | '考虑' | '不建议' = '考虑';

  // 家暴和出轨通常是值得离婚的理由
  if (离婚原因 === '家暴') {
    是否值得离婚 = '值得';
    理由.push('家暴是原则问题');
    风险.push('可能面临报复');
  } else if (离婚原因 === '出轨') {
    是否值得离婚 = '值得';
    理由.push('感情破裂难以修复');
    风险.push('可能影响子女心理');
  } else if (离婚原因 === '骗婚曝光') {
    是否值得离婚 = '值得';
    理由.push('基础就是欺骗');
  }

  // 有子女需要更慎重
  if (是否有子女) {
    if (子女年龄 < 3) {
      风险.push('子女年幼需要完整家庭');
    }
    风险.push('子女心理可能受影响');
    风险.push('抚养权争夺');
  }

  // 经济状况差的时候离婚风险更大
  if (经济状况 === '差') {
    风险.push('经济上可能陷入困境');
    风险.push('生活水平可能下降');
  }

  // 婚姻满意度评估
  if (婚姻满意度 > 50) {
    是否值得离婚 = '不建议';
    理由.push('婚姻还有挽回余地');
    理由.push('离婚需要慎重考虑');
  } else if (婚姻满意度 > 30) {
    是否值得离婚 = '考虑';
    理由.push('建议先尝试婚姻咨询');
  }

  const 建议: string[] = [];
  if (是否值得离婚 === '值得') {
    建议.push('收集必要的证据');
    建议.push('保护好个人财产');
    建议.push('必要时申请人身保护令');
    建议.push('寻求专业法律帮助');
  } else if (是否值得离婚 === '考虑') {
    建议.push('先尝试沟通和婚姻咨询');
    建议.push('给彼此一个冷静期');
    建议.push('分析离婚后的生活安排');
  } else {
    建议.push('尝试修复关系');
    建议.push('找出问题的根源');
    建议.push('可以考虑暂时分居');
  }

  return {
    是否值得离婚,
    理由,
    风险,
    建议,
  };
}
