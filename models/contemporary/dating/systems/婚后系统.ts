// ==================== 出轨识别 ====================

export interface 出轨迹象 {
  类型: '行为' | '态度' | '消费' | '社交';
  表现: string;
  严重程度: number;  // 0-100
}

/**
 * 检测出轨迹象
 */
export function 检测出轨迹象(
  配偶: 对象核心状态
): {
  存在: boolean;
  迹象: 出轨迹象[];
  风险等级: '低' | '中' | '高' | '极高';
  建议: string[];
} {
  const 迹象: 出轨迹象[] = [];

  // 行为变化检测
  const 行为变化概率 = Math.random() * 100;
  if (行为变化概率 < 30) {
    迹象.push({
      类型: '行为',
      表现: '手机不离身，经常偷偷看',
      严重程度: 30 + Math.floor(Math.random() * 20),
    });
  }
  if (行为变化概率 < 20) {
    迹象.push({
      类型: '行为',
      表现: '回家时间变晚，借口多',
      严重程度: 40 + Math.floor(Math.random() * 20),
    });
  }
  if (行为变化概率 < 15) {
    迹象.push({
      类型: '行为',
      表现: '行踪不明，经常联系不上',
      严重程度: 60 + Math.floor(Math.random() * 20),
    });
  }

  // 态度变化检测
  const 态度变化概率 = Math.random() * 100;
  if (态度变化概率 < 35) {
    迹象.push({
      类型: '态度',
      表现: '突然对你变得冷淡',
      严重程度: 30 + Math.floor(Math.random() * 20),
    });
  }
  if (态度变化概率 < 25) {
    迹象.push({
      类型: '态度',
      表现: '突然变得挑剔',
      严重程度: 20 + Math.floor(Math.random() * 20),
    });
  }
  if (态度变化概率 < 20) {
    迹象.push({
      类型: '态度',
      表现: '经常拒绝亲密',
      严重程度: 50 + Math.floor(Math.random() * 20),
    });
  }

  // 消费变化检测
  const 消费变化概率 = Math.random() * 100;
  if (消费变化概率 < 20) {
    迹象.push({
      类型: '消费',
      表现: '信用卡账单有不明支出',
      严重程度: 40 + Math.floor(Math.random() * 20),
    });
  }
  if (消费变化概率 < 15) {
    迹象.push({
      类型: '消费',
      表现: '现金提取频繁',
      严重程度: 30 + Math.floor(Math.random() * 20),
    });
  }

  // 社交变化检测
  const 社交变化概率 = Math.random() * 100;
  if (社交变化概率 < 25) {
    迹象.push({
      类型: '社交',
      表现: '突然有频繁联系的异性',
      严重程度: 50 + Math.floor(Math.random() * 30),
    });
  }
  if (社交变化概率 < 15) {
    迹象.push({
      类型: '社交',
      表现: '社交媒体互动异常',
      严重程度: 30 + Math.floor(Math.random() * 20),
    });
  }

  // 计算风险
  const 总严重程度 = 迹象.reduce((sum, i) => sum + i.严重程度, 0);
  const 平均严重程度 = 迹象.length > 0 ? 总严重程度 / 迹象.length : 0;

  let 风险等级: '低' | '中' | '高' | '极高';
  let 建议: string[];

  if (平均严重程度 >= 70 || 迹象.length >= 4) {
    风险等级 = '极高';
    建议 = [
      '收集证据',
      '考虑咨询律师',
      '必要时报警',
      '考虑婚姻是否继续',
    ];
  } else if (平均严重程度 >= 50 || 迹象.length >= 3) {
    风险等级 = '高';
    建议 = [
      '尝试沟通',
      '注意收集证据',
      '保护好个人财产',
      '向可信赖的朋友倾诉',
    ];
  } else if (平均严重程度 >= 30 || 迹象.length >= 2) {
    风险等级 = '中';
    建议 = [
      '留意观察',
      '尝试沟通了解情况',
      '多关心对方',
    ];
  } else {
    风险等级 = '低';
    建议 = [
      '可能是工作压力大',
      '多关心对方',
      '保持正常沟通',
    ];
  }

  return {
    存在: 迹象.length > 0,
    迹象,
    风险等级,
    建议,
  };
}

// ==================== 家暴评估 ====================

export interface 家暴风险 {
  存在风险: boolean;
  暴力类型: string[];
  触发场景: string[];
  风险等级: '低' | '中' | '高' | '极高';
  建议: string[];
}

/**
 * 评估家暴风险
 */
export function 评估家暴风险(
  配偶: 对象核心状态
): 家暴风险 {
  // 简化评估：基于隐藏属性和关系状态
  const 暴力类型: string[] = [];
  const 触发场景: string[] = [];
  let 风险值 = 20; // 基础风险

  // 检查隐藏属性中是否有暴力倾向
  if (配偶.隐藏属性.includes('易怒')) {
    风险值 += 30;
    暴力类型.push('语言暴力');
    触发场景.push('争吵时失控');
  }
  if (配偶.隐藏属性.includes('酗酒')) {
    风险值 += 25;
    暴力类型.push('身体暴力');
    触发场景.push('酗酒后');
  }
  if (配偶.隐藏属性.includes('控制欲强')) {
    风险值 += 20;
    暴力类型.push('经济控制');
    触发场景.push('管控你的消费');
  }

  // 关系状态影响
  if (配偶.关系状态 === '已婚') {
    风险值 += 10;
  }

  // 信任度低可能预示暴力
  if (配偶.信任度 < 40) {
    风险值 += 15;
  }

  风险值 = Math.min(100, 风险值);

  let 风险等级: '低' | '中' | '高' | '极高';
  let 建议: string[];

  if (风险值 >= 70) {
    风险等级 = '极高';
    建议 = [
      '立即寻求帮助',
      '联系妇联热线',
      '必要时报警',
      '考虑分居或离婚',
      '保护好重要证件和财产',
    ];
  } else if (风险值 >= 50) {
    风险等级 = '高';
    建议 = [
      '注意安全',
      '不要激化矛盾',
      '记录家暴证据',
      '告诉可信赖的人',
      '准备紧急联系方案',
    ];
  } else if (风险值 >= 30) {
    风险等级 = '中';
    建议 = [
      '留意预警信号',
      '避免单独相处时激化矛盾',
      '与朋友家人保持联系',
    ];
  } else {
    风险等级 = '低';
    建议 = [
      '保持正常沟通',
      '建立平等尊重的关系',
    ];
  }

  return {
    存在风险: 风险值 >= 30,
    暴力类型: [...new Set(暴力类型)],
    触发场景: [...new Set(触发场景)],
    风险等级,
    建议,
  };
}

// ==================== 婆媳矛盾 ====================

export interface 婆媳矛盾评估 {
  矛盾程度: '无' | '轻微' | '中度' | '严重';
  主要问题: string[];
  建议: string[];
}

/**
 * 评估婆媳矛盾
 */
export function 评估婆媳矛盾(
  妻子: 对象核心状态,
  婆婆态度: '开明' | '一般' | '保守' | '挑剔'
): 婆媳矛盾评估 {
  const 主要问题: string[] = [];
  let 矛盾值 = 0;

  // 婆婆态度影响
  switch (婆婆态度) {
    case '挑剔':
      矛盾值 += 50;
      主要问题.push('婆婆过于挑剔');
      break;
    case '保守':
      矛盾值 += 30;
      主要问题.push('婆婆观念保守');
      break;
    case '一般':
      矛盾值 += 15;
      break;
    case '开明':
      矛盾值 += 0;
      break;
  }

  // 妻子性格影响
  if (妻子.隐藏属性.includes('倔强')) {
    矛盾值 += 20;
    主要问题.push('妻子性格倔强');
  }
  if (妻子.隐藏属性.includes('敏感')) {
    矛盾值 += 15;
    主要问题.push('妻子比较敏感');
  }

  let 矛盾程度: '无' | '轻微' | '中度' | '严重';
  let 建议: string[];

  if (矛盾值 >= 60) {
    矛盾程度 = '严重';
    建议 = [
      '分开居住',
      '减少接触',
      '丈夫做好调解工作',
      '必要时寻求外部帮助',
    ];
  } else if (矛盾值 >= 40) {
    矛盾程度 = '中度';
    建议 = [
      '保持距离',
      '少管对方的事',
      '让丈夫在中间调解',
    ];
  } else if (矛盾值 >= 20) {
    矛盾程度 = '轻微';
    建议 = [
      '多沟通',
      '互相理解',
      '丈夫做好桥梁',
    ];
  } else {
    矛盾程度 = '无';
    建议 = [
      '继续保持',
      '相互尊重',
    ];
  }

  return {
    矛盾程度,
    主要问题,
    建议,
  };
}

// ==================== 婚姻满意度 ====================

/**
 * 计算婚姻满意度
 */
export function 计算婚姻满意度(
  丈夫: 相亲者核心状态,
  妻子: 对象核心状态
): {
  满意度: number;
  主要影响因素: { 因素: string; 影响: '正面' | '负面' }[];
  建议: string[];
} {
  let 满意度 = 60; // 基础满意度
  const 主要影响因素: { 因素: string; 影响: '正面' | '负面' }[] = [];

  // 经济状况影响
  if (丈夫.收入 > 30) {
    满意度 += 10;
    主要影响因素.push({ 因素: '经济条件较好', 影响: '正面' });
  } else if (丈夫.收入 < 10) {
    满意度 -= 15;
    主要影响因素.push({ 因素: '经济压力', 影响: '负面' });
  }

  // 信任度影响
  if (妻子.信任度 > 70) {
    满意度 += 15;
    主要影响因素.push({ 因素: '相互信任', 影响: '正面' });
  } else if (妻子.信任度 < 40) {
    满意度 -= 20;
    主要影响因素.push({ 因素: '缺乏信任', 影响: '负面' });
  }

  // 亲密程度影响
  if (妻子.投入度 > 70) {
    满意度 += 10;
    主要影响因素.push({ 因素: '感情投入', 影响: '正面' });
  } else if (妻子.投入度 < 30) {
    满意度 -= 15;
    主要影响因素.push({ 因素: '感情淡漠', 影响: '负面' });
  }

  // 物质要求与实际的差距
  if (妻子.物质要求 > 80 && 丈夫.收入 < 20) {
    满意度 -= 15;
    主要影响因素.push({ 因素: '物质期望不匹配', 影响: '负面' });
  }

  满意度 = Math.min(100, Math.max(0, 满意度));

  const 建议: string[] = [];
  if (满意度 >= 70) {
    建议.push('继续保持');
    建议.push('注意维护感情');
  } else if (满意度 >= 50) {
    建议.push('多沟通');
    建议.push('注意对方需求');
  } else if (满意度 >= 30) {
    建议.push('考虑婚姻咨询');
    建议.push('找出问题根源');
  } else {
    建议.push('考虑是否继续这段婚姻');
    建议.push('寻求专业帮助');
  }

  return {
    满意度,
    主要影响因素,
    建议,
  };
}
