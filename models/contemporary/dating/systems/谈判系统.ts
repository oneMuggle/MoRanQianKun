// ==================== 谈判态势 ====================

export interface 谈判态势 {
  主动方: '男方' | '女方' | '势均力敌';
  优势方: string[];
  劣势方: string[];
  建议策略: string;
}

/**
 * 分析彩礼谈判态势
 */
export function 分析谈判态势(
  男方: 相亲者核心状态,
  女方: 相亲对象核心状态
): 谈判态势 {
  const 男方综合 = 计算个人综合条件(男方);
  const 女方综合 = 评估女方条件(女方);
  const 差距 = 男方综合 - 女方综合;

  const 优势方: string[] = [];
  const 劣势方: string[] = [];

  // 收入对比
  if (男方.收入 > 女方.收入 * 1.5) {
    优势方.push('男方收入优势');
  } else if (女方.收入 > 男方.收入 * 1.5) {
    优势方.push('女方收入优势');
  }

  // 房产对比
  if (男方.房产 === '无贷款' && 女方.房产 === '无') {
    优势方.push('男方房产优势');
  } else if (女方.房产 === '无贷款' && 男方.房产 === '无') {
    优势方.push('女方房产优势');
  }

  // 年龄对比
  if (男方.性别 === '男' && 男方.年龄 > 35) {
    劣势方.push('男方年龄偏大');
  }
  if (女方.性别 === '女' && 女方.年龄 > 32) {
    劣势方.push('女方年龄偏大');
  }

  // 急切度对比
  if (男方.急切度 > 70 && 女方.投入度 < 50) {
    优势方.push('男方更急切');
  } else if (女方.投入度 > 70 && 男方.急切度 < 50) {
    优势方.push('女方更急切');
  }

  // 判断主动方
  let 主动方: '男方' | '女方' | '势均力敌';
  let 建议策略: string;

  if (优势方.length > 劣势方.length + 1) {
    主动方 = '男方';
    建议策略 = '可以适当压低彩礼要求';
  } else if (劣势方.length > 优势方.length + 1) {
    主动方 = '女方';
    建议策略 = '可能需要接受较高彩礼要求';
  } else if (优势方.length === 劣势方.length && 优势方.length > 0) {
    主动方 = '势均力敌';
    建议策略 = '建议协商解决';
  } else {
    主动方 = '势均力敌';
    建议策略 = '按当地习俗和双方家庭情况协商';
  }

  return {
    主动方,
    优势方,
    劣势方,
    建议策略,
  };
}

/**
 * 评估女方综合条件
 */
function 评估女方条件(女方: 相亲对象核心状态): number {
  let score = 50;

  // 年龄
  if (女方.年龄 < 25) score += 15;
  else if (女方.年龄 < 28) score += 10;
  else if (女方.年龄 < 32) score += 5;
  else if (女方.年龄 > 35) score -= 15;

  // 外貌
  const 外貌评分: Record<string, number> = {
    '帅气/漂亮': 15, '普通': 8, '一般': 3, '难看': -10,
  };
  score += 外貌评分[女方.外貌] || 0;

  // 收入
  if (女方.收入 > 20) score += 10;
  else if (女方.收入 > 10) score += 5;

  // 学历
  if (女方.学历 === '博士') score += 10;
  else if (女方.学历 === '硕士') score += 7;
  else if (女方.学历 === '本科') score += 3;

  return Math.min(100, Math.max(0, score));
}

// ==================== 谈判过程 ====================

export interface 彩礼谈判进度 {
  当前彩礼: number;
  期望彩礼: number;
  底价彩礼: number;
  谈判轮次: number;
  分歧点: string[];
  情绪状态: '友好' | '紧张' | '僵持' | '破裂边缘';
}

/**
 * 初始化谈判
 */
export function 初始化彩礼谈判(
  男方: 相亲者核心状态,
  女方: 相亲对象核心状态,
  地区: '农村' | '县城' | '地级市' | '省会' | '一线城市'
): 彩礼谈判进度 {
  const { 建议彩礼, 范围 } = 计算彩礼(男方.收入, 女方.收入, 地区);

  // 期望彩礼：女方通常会抬高
  const 期望彩礼 = Math.round(范围.max * (1 + 女方.物质要求 / 100));

  // 底价彩礼：男方能接受的最低
  const 底价彩礼 = Math.round(范围.min * 0.8);

  return {
    当前彩礼: 建议彩礼,
    期望彩礼,
    底价彩礼,
    谈判轮次: 0,
    分歧点: [],
    情绪状态: '友好',
  };
}

/**
 * 进行一轮谈判
 */
export function 进行谈判轮次(
  进度: 彩礼谈判进度,
  出价: number,
  态度: '强硬' | '妥协' | '折中'
): {
  新进度: 彩礼谈判进度;
  结果: '接受' | '继续' | '破裂';
  说明: string;
} {
  const 新进度 = { ...进度, 谈判轮次: 进度.谈判轮次 + 1 };

  // 检查是否在范围内
  if (出价 < 进度.底价彩礼 * 0.5) {
    新进度.情绪状态 = '破裂边缘';
    新进度.分歧点.push('差距过大无法弥合');
    return {
      新进度,
      结果: '破裂',
      说明: '彩礼差距太大，谈判破裂',
    };
  }

  if (出价 >= 进度.期望彩礼 * 0.9) {
    // 接近期望，接受
    新进度.当前彩礼 = 出价;
    新进度.情绪状态 = '友好';
    return {
      新进度,
      结果: '接受',
      说明: '达成一致！',
    };
  }

  if (出价 >= 进度.当前彩礼 * 0.8) {
    // 在当前基础上有商量空间
    switch (态度) {
      case '强硬':
        新进度.情绪状态 = '紧张';
        新进度.分歧点.push('男方态度强硬');
        return {
          新进度,
          结果: '继续',
          说明: '继续谈判',
        };
      case '妥协':
        新进度.当前彩礼 = 出价;
        新进度.情绪状态 = '友好';
        return {
          新进度,
          结果: '继续',
          说明: '达成初步共识',
        };
      case '折中':
        const 折中价 = Math.round((进度.当前彩礼 + 出价) / 2);
        新进度.当前彩礼 = 折中价;
        return {
          新进度,
          结果: '继续',
          说明: '提出折中方案',
        };
    }
  }

  // 差距太大
  新进度.情绪状态 = '僵持';
  新进度.分歧点.push(`在${出价}和${进度.期望彩礼}之间存在分歧`);

  return {
    新进度,
    结果: '继续',
    说明: '分歧较大，需要进一步协商',
  };
}

// ==================== 嫁妆计算 ====================

export interface 嫁妆方案 {
  嫁妆金额: number;
  嫁妆内容: string[];
  说明: string;
}

/**
 * 生成嫁妆方案
 */
export function 生成嫁妆方案(
  彩礼: number,
  女方家庭背景: '城市中产' | '城市底层' | '农村' | '富裕'
): 嫁妆方案 {
  let 模式: '传统' | '现代' | '极端' | '谈判';
  let 说明: string;

  switch (女方家庭背景) {
    case '富裕':
      // 富裕家庭通常会厚嫁妆
      模式 = '现代';
      说明 = '富裕家庭，嫁妆丰厚';
      break;
    case '城市中产':
      模式 = '传统';
      说明 = '中产家庭，按传统习俗来';
      break;
    case '城市底层':
      模式 = '极端';
      说明 = '家庭条件有限，嫁妆较少';
      break;
    case '农村':
      模式 = '极端';
      说明 = '农村家庭，可能需要商量';
      break;
  }

  const { 嫁妆 } = 计算嫁妆(彩礼, 模式);

  const 嫁妆内容: string[] = [];
  if (嫁妆 >= 彩礼) {
    嫁妆内容.push('家电全套');
    嫁妆内容.push('汽车');
    嫁妆内容.push('装修费');
  } else if (嫁妆 >= 彩礼 * 0.5) {
    嫁妆内容.push('家电');
    嫁妆内容.push('部分装修费');
  } else {
    嫁妆内容.push('少量现金');
    嫁妆内容.push('床上用品');
  }

  return {
    嫁妆金额: 嫁妆,
    嫁妆内容,
    说明,
  };
}

