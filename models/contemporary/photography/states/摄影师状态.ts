// ==================== 摄影师工厂函数 ====================

export function 创建摄影师(params: {
  摄影师ID: string;
  艺名: string;
  性别: '男' | '女';
  年龄: number;
  摄影师类型: 摄影师类型;
  动机类型: 摄影师动机;
}): 摄影师核心状态 {
  const {
    摄影师ID,
    艺名,
    性别,
    年龄,
    摄影师类型,
    动机类型,
  } = params;

  return {
    摄影师ID,
    艺名,
    性别,
    年龄,
    从业年限: 0,

    // 职业信息
    摄影师类型,
    动机类型,
    信誉等级: '普通摄影师',

    // 专业能力
    技术水平: 50,
    艺术造诣: 50,
    后期能力: 50,
    设备等级: '入门',

    // 作品风格
    擅长风格: [],
    主要拍摄尺度: 'PG-13',
    作品发布平台: [],

    // 模特资源
    合作模特数量: 0,
    固定合作模特: [],
    新模特获取能力: 30,

    // 商业信息
    月均收入: 0,
    拍摄报价: 500,
    主收入来源: '其他',

    // 口碑与投诉
    被投诉次数: 0,
    越界投诉次数: 0,
    圈内警告次数: 0,

    // 隐藏属性
    真实动机: 动机类型,
    越界倾向: 计算基础越界倾向(动机类型),
    偷拍倾向: 计算基础偷拍倾向(动机类型),
    传播倾向: 计算基础传播倾向(动机类型),
  };
}

// ==================== 隐藏属性计算 ====================

