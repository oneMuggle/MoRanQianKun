// ==================== 状态更新函数 ====================

export function 更新摄影师投诉(
  摄影师: 摄影师核心状态,
  类型: '普通' | '越界' | '偷拍'
): 摄影师核心状态 {
  const 更新后 = { ...摄影师 };

  switch (类型) {
    case '普通':
      更新后.被投诉次数 += 1;
      break;
    case '越界':
      更新后.被投诉次数 += 1;
      更新后.越界投诉次数 += 1;
      更新后.越界倾向 = Math.min(100, 更新后.越界倾向 + 10);
      break;
    case '偷拍':
      更新后.被投诉次数 += 1;
      更新后.偷拍投诉次数 = (更新后.偷拍投诉次数 ?? 0) + 1;
      更新后.偷拍倾向 = Math.min(100, 更新后.偷拍倾向 + 15);
      break;
  }

  // 根据投诉更新信誉
  更新后.信誉等级 = 计算信誉等级(更新后);

  return 更新后;
}

function 计算信誉等级(摄影师: 摄影师核心状态): 摄影师信誉 {
  const 总投诉 = 摄影师.被投诉次数;
  const 越界投诉 = 摄影师.越界投诉次数;
  const 偷拍投诉 = 摄影师.偷拍投诉次数 ?? 0;
  const 圈内警告 = 摄影师.圈内警告次数;

  if (偷拍投诉 >= 2 || 越界投诉 >= 5) return '惯犯';
  if (偷拍投诉 >= 1 || 越界投诉 >= 3 || 圈内警告 >= 3) return '名声较差';
  if (越界投诉 >= 1 || 圈内警告 >= 1 || 总投诉 >= 3) return '有争议';
  if (摄影师.从业年限 >= 10 && 总投诉 === 0) return '业界大佬';
  if (摄影师.从业年限 >= 5 && 总投诉 <= 1) return '资深摄影师';
  return '普通摄影师';
}

export function 添加合作模特(
  摄影师: 摄影师核心状态,
  模特ID: string
): 摄影师核心状态 {
  if (摄影师.固定合作模特.includes(模特ID)) {
    return 摄影师;
  }

  return {
    ...摄影师,
    合作模特数量: 摄影师.合作模特数量 + 1,
    固定合作模特: [...摄影师.固定合作模特, 模特ID],
  };
}

export function 记录被拒绝(
  摄影师: 摄影师核心状态
): 摄影师核心状态 {
  return {
    ...摄影师,
    被拒绝次数: 摄影师.被拒绝次数 + 1,
  };
}

export function 更新技能水平(
  摄影师: 摄影师核心状态,
  变化: {
    技术水平?: number;
    艺术造诣?: number;
    后期能力?: number;
  }
): 摄影师核心状态 {
  return {
    ...摄影师,
    技术水平: Math.max(0, Math.min(100, 摄影师.技术水平 + (变化.技术水平 ?? 0))),
    艺术造诣: Math.max(0, Math.min(100, 摄影师.艺术造诣 + (变化.艺术造诣 ?? 0))),
    后期能力: Math.max(0, Math.min(100, 摄影师.后期能力 + (变化.后期能力 ?? 0))),
  };
}

export function 标记圈内警告(
  摄影师: 摄影师核心状态
): 摄影师核心状态 {
  return {
    ...摄影师,
    圈内警告次数: 摄影师.圈内警告次数 + 1,
    信誉等级: 计算信誉等级({
      ...摄影师,
      圈内警告次数: 摄影师.圈内警告次数 + 1,
    }),
  };
}

// ==================== 判断函数 ====================

export function 是否危险摄影师(
  摄影师: 摄影师核心状态
): boolean {
  return (
    摄影师.偷拍投诉次数 !== undefined && 摄影师.偷拍投诉次数 >= 1 ||
    摄影师.越界投诉次数 >= 3 ||
    摄影师.真实动机 === '偷拍' ||
    摄影师.真实动机 === '传播'
  );
}

export function 是否值得信赖(
  摄影师: 摄影师核心状态
): boolean {
  return (
    摄影师.信誉等级 === '业界大佬' ||
    摄影师.信誉等级 === '资深摄影师'
  ) && 摄影师.被投诉次数 === 0;
}

export function 获取摄影师风险评级(
  摄影师: 摄影师核心状态
): '安全' | '一般' | '危险' | '高危' {
  if (摄影师.真实动机 === '偷拍' || 摄影师.真实动机 === '传播') return '高危';
  if (摄影师.偷拍投诉次数 && 摄影师.偷拍投诉次数 >= 1) return '高危';
  if (摄影师.越界投诉次数 >= 3) return '危险';
  if (摄影师.越界投诉次数 >= 1 || 摄影师.圈内警告次数 >= 1) return '一般';
  return '安全';
}

// ==================== 报价相关 ====================

export function 是否报价异常(
  摄影师: 摄影师核心状态,
  市场均价: number
): boolean {
  const 报价比例 = 摄影师.拍摄报价 / 市场均价;
  return 报价比例 < 0.3 || 报价比例 > 3;
}

export function 获取报价合理性(
  摄影师: 摄影师核心状态,
  市场均价: number
): '过低' | '合理' | '过高' {
  const 报价比例 = 摄影师.拍摄报价 / 市场均价;
  if (报价比例 < 0.5) return '过低';
  if (报价比例 > 2) return '过高';
  return '合理';
}
