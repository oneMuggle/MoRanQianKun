// ==================== 判断函数 ====================

export function 是否已控制(
  事件: 泄露事件状态
): boolean {
  return 事件.影响范围控制 >= 70;
}

export function 是否严重(
  事件: 泄露事件状态
): boolean {
  const { 心理创伤, 名誉损失, 职业影响, 现实生活影响 } = 事件.对模特影响;
  return 心理创伤 >= 60 || 名誉损失 >= 60 || 职业影响 >= 60 || 现实生活影响 >= 60;
}

export function 获取严重程度(
  事件: 泄露事件状态
): '轻微' | '中等' | '严重' | '极严重' {
  const { 心理创伤, 名誉损失, 职业影响 } = 事件.对模特影响;
  const 总分 = 心理创伤 + 名誉损失 + 职业影响;

  if (总分 < 100) return '轻微';
  if (总分 < 200) return '中等';
  if (总分 < 300) return '严重';
  return '极严重';
}

export function 是否需要报警(
  事件: 泄露事件状态
): boolean {
  return 事件.内容尺度 === 'NC-17' || 事件.内容尺度 === 'XXX';
}
