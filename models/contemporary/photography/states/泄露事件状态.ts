import type {
  泄露事件状态,
  泄露类型,
  传播范围,
  拍摄尺度,
} from '../types';

// ==================== 泄露事件工厂函数 ====================

export function 创建泄露事件(params: {
  事件ID: string;
  拍摄项目ID: string;
  模特ID: string;
  摄影师ID: string;
  泄露类型: 泄露类型;
  内容尺度: 拍摄尺度;
  涉及人数: number;
}): 泄露事件状态 {
  const {
    事件ID,
    拍摄项目ID,
    模特ID,
    摄影师ID,
    泄露类型,
    内容尺度,
    涉及人数,
  } = params;

  return {
    事件ID,
    拍摄项目ID,
    模特ID,
    摄影师ID,

    // 泄露详情
    泄露照片数: 0,
    泄露视频数: 0,
    泄露类型,
    内容尺度,
    涉及人数,
    模特ID列表: [模特ID],

    // 传播信息
    首次发现时间: new Date().toISOString(),
    传播范围: '未传播',
    当前传播范围: '未传播',
    当前传播平台: [],
    传播速度: '静止',
    传播平台: [],
    传播源头追溯: '未知',

    // 维权状态
    发现后处理: '未处理',
    删除成功率: 0,
    影响范围控制: 0,

    // 影响评估
    对模特影响: {
      心理创伤: 0,
      名誉损失: 0,
      职业影响: 0,
      现实生活影响: 0,
    },

    // 后续发展
    是否有二次传播: false,
    是否被存档: false,
    是否被用于牟利: false,
  };
}

// ==================== 传播追踪 ====================

export function 更新传播范围(
  事件: 泄露事件状态,
  新范围: 传播范围
): 泄露事件状态 {
  return {
    ...事件,
    当前传播范围: 新范围,
    传播范围: 新范围,
  };
}

export function 添加传播平台(
  事件: 泄露事件状态,
  平台: string
): 泄露事件状态 {
  if ((事件.传播平台 ?? []).includes(平台)) {
    return 事件;
  }

  return {
    ...事件,
    传播平台: [...(事件.传播平台 ?? []), 平台],
  };
}

export function 更新传播速度(
  事件: 泄露事件状态,
  速度: '静止' | '缓慢' | '快速' | '爆发'
): 泄露事件状态 {
  return {
    ...事件,
    传播速度: 速度,
  };
}

// ==================== 维权处理 ====================

export function 开始维权处理(
  事件: 泄露事件状态,
  处理方式: 泄露事件状态['发现后处理']
): 泄露事件状态 {
  return {
    ...事件,
    发现后处理: 处理方式,
    是否已处理: true,
    更新时间: Date.now(),
  };
}

export function 更新处理结果(
  事件: 泄露事件状态,
  结果: {
    删除成功率?: number;
    影响范围控制?: number;
    处理结果?: string;
  }
): 泄露事件状态 {
  return {
    ...事件,
    删除成功率: 结果.删除成功率 ?? 事件.删除成功率,
    影响范围控制: 结果.影响范围控制 ?? 事件.影响范围控制,
    处理结果: 结果.处理结果,
    更新时间: Date.now(),
  };
}

// ==================== 影响评估更新 ====================

export function 更新影响评估(
  事件: 泄露事件状态,
  影响: Partial<泄露事件状态['对模特影响']>
): 泄露事件状态 {
  return {
    ...事件,
    对模特影响: {
      ...事件.对模特影响,
      ...影响,
    },
    更新时间: Date.now(),
  };
}

