/**
 * 泄露应对选择
 */
export function 生成泄露应对选择(params: {
  模特: 模特核心状态;
  泄露类型: 泄露类型;
  传播范围: 传播范围;
}): string {
  const { 模特, 泄露类型, 传播范围 } = params;

  return `【泄露应对选择】

模特${模特.姓名}面临以下选择：

【选项A：平台投诉】
联系传播平台要求删除照片
- 优点：直接、相对快速
- 缺点：需要提供身份证明，照片可能已被备份

【选项B：报警处理】
向公安机关报案
- 优点：法律威慑、可追究刑责
- 缺点：程序繁琐、可能需要公开身份

【选项C：委托律师】
请律师发律师函
- 优点：专业、正式
- 缺点：费用高、需要时间

【选项D：发声澄清】
在社交媒体公开发声
- 优点：掌握舆论主动权
- 缺点：可能引发更多关注

【选项E：冷处理】
暂时不理会，等待热度下降
- 优点：避免二次传播
- 缺点：照片持续存在

【选项F：私下解决】
联系泄露者谈判
- 优点：可能快速解决
- 缺点：可能助长对方气焰

【${模特.姓名}的处境】
- 泄露类型：${泄露类型}
- 传播范围：${传播范围}
- 心理状态：${模特.心理阴影程度}/100
`;
}

/**
 * 泄露后心理变化
 */
export function 生成泄露心理变化(params: {
  模特: 模特核心状态;
  传播范围: 传播范围;
  处理方式: string;
}): string {
  const { 模特, 传播范围, 处理方式 } = params;

  const 心理阶段: string[] = [
    `【第一阶段：震惊】
"这不可能...一定是搞错了..."
否认事实，难以接受...`,

    `【第二阶段：愤怒】
"是谁干的！我要让他付出代价！"
愤怒情绪爆发，寻找发泄口...`,

    `【第三阶段：恐惧】
"会不会被更多人看到..."
"我的家人朋友会不会知道..."
恐惧与焦虑笼罩...`,

    `【第四阶段：自责】
"是不是我的错..."
"如果当初不拍就好了..."
自我否定与自责...`,

    `【第五阶段：纠结】
"要不要报警..."
"要不要发声..."
反复权衡利弊，难以决定...`,

    处理方式 === '发声澄清' 
      ? `【第六阶段：发声】
她决定站出来发声，
掌握舆论主动权...
虽然可能引发更多讨论，
但她选择直面这一切...`
      : 处理方式 === '报警'
      ? `【第六阶段：报警】
她选择了报警，
警方已介入调查...
虽然程序繁琐，但她相信法律...`
      : `【第六阶段：等待】
她选择先冷处理...
希望时间能冲淡一切...
但心里始终有一根刺...`,
  ];

  return `【泄露心理变化轨迹】

模特${模特.姓名}的心理历程：

${心理阶段.join('\n\n')}

【当前心理阴影程度】
${模特.心理阴影程度}/100 → ${Math.min(100, 模特.心理阴影程度 + getPsychologicalImpact(传播范围))}/100
`;
}

function getPsychologicalImpact(传播范围: 传播范围): number {
  const 影响表: Record<传播范围, number> = {
    '未传播': 5,
    '小范围': 20,
    '论坛传播': 35,
    '社交媒体': 50,
    '图片网站': 60,
    '成人网站': 80,
    '全网扩散': 95,
  };
  return 影响表[传播范围] || 20;
}

/**
 * 泄露事件演化
 */
export function 生成泄露演化结果(params: {
  当前传播范围: 传播范围;
  传播速度: '静止' | '缓慢' | '快速' | '爆发';
  是否处理: boolean;
}): string {
  const { 当前传播范围, 传播速度, 是否处理 } = params;

  const 演化结果: Record<string, string> = {
    '静止+处理': '照片热度逐渐下降，被平台删除后逐渐被遗忘。',
    '静止+不处理': '照片继续存在，但传播范围没有扩大。',
    '缓慢+处理': '在平台投诉下，传播速度放缓，最终被控制在小范围。',
    '缓慢+不处理': '照片缓慢扩散，几个月后传播范围翻倍。',
    '快速+处理': '虽然投诉努力，但传播速度太快，难以完全控制。',
    '快速+不处理': '照片迅速扩散，一周内传遍全网。',
    '爆发+处理': '已经爆发式传播，即使投诉也难以完全删除。',
    '爆发+不处理': '灾难性传播，个人信息被扒出，事件失控。',
  };

  const key = `${传播速度}+${是否处理 ? '处理' : '不处理'}`;
  return 演化结果[key] || '演化结果未知...';
}

