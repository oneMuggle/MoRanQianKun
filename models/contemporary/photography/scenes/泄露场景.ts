/**
 * 泄露场景
 * 
 * 照片泄露事件的发生、发展与处理
 * 模特面临的心理压力与应对选择
 */

import type { 
  模特核心状态, 
  摄影师核心状态,
  泄露类型,
  传播范围,
} from '../types';

/**
 * 泄露发现场景
 */
export function 生成泄露发现场景(params: {
  模特: 模特核心状态;
  摄影师?: 摄影师核心状态;
  泄露类型: 泄露类型;
  传播范围: 传播范围;
}): string {
  const { 模特, 摄影师, 泄露类型, 传播范围 } = params;

  const 发现渠道: Record<传播范围, string> = {
    '未传播': '暂无外部迹象...',
    '小范围': '朋友发来消息："有人在传你的照片"',
    '论坛传播': '摄影论坛有人发帖讨论你的照片',
    '社交媒体': '微博/小红书上有人@你："这是你吗？"',
    '图片网站': '陌生人在图片网站上看到了你的照片',
    '成人网站': '成人网站上出现了你的照片...',
    '全网扩散': '你的照片在各大平台疯传...',
  };

  return `【泄露发现】

模特${模特.姓名}收到了朋友的消息...

${发现渠道[传播范围]}

【泄露信息】
- 泄露类型：${泄露类型}
- 传播范围：${传播范围}
- 发现时间：刚刚

【模特状态】
- 当前心理阴影程度：${模特.心理阴影程度}/100
- ${摄影师 ? `摄影师：${摄影师.姓名}` : '摄影师：未知'}

【${模特.姓名}的第一反应】
"怎么可能...是谁泄露的..."
"我该怎么办...？"

她的脑子一片空白，不知道如何应对...`;
}

