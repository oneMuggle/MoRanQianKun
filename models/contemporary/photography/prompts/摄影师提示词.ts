function getPhotographerInnerThoughts(越界倾向: number): string {
  if (越界倾向 > 80) {
    return '这个模特看起来好欺负，可以更进一步...';
  } else if (越界倾向 > 60) {
    return '试探一下她的底线，看看能走多远...';
  } else if (越界倾向 > 40) {
    return '保持专业，但可以稍微试探一下...';
  } else {
    return '专心拍好作品，不要想其他的...';
  }
}

/**
 * 生成尺度递进场景提示词
 */
export function 生成尺度递进提示词(params: {
  摄影师: 摄影师核心状态;
  模特: 模特核心状态;
  当前尺度: 拍摄尺度;
}): string {
  const { 摄影师, 模特, 当前尺度 } = params;

  return `【特殊场景：尺度递进】

摄影师${摄影师.姓名}正在推进拍摄尺度...

【当前尺度】${当前尺度}
【目标尺度】${getNextScale(当前尺度)}
【摄影师越界倾向】${摄影师.越界倾向}/100
【模特保护意识】${模特.保护意识}

【递进策略】
${getScaleProgressionStrategy(摄影师.越界倾向, 当前尺度)}

【模特的反应】
${模特.姓名}感到边界在被试探...

【可能的递进话术】
1. "这套衣服效果不够好，换一套试试？"
2. "这个角度更显身材，你试试..."
3. "我可以帮你调整一下姿势吗？"
4. "再放开一点，很好的..."

【摄影师的选择】
- 如果模特接受：继续推进尺度
- 如果模特犹豫：放缓节奏，伺机再试
- 如果模特拒绝：收手或使用其他策略

【回复要求】
1. 体现递进的渐进性
2. 保持"为艺术"的话术包装
3. 观察模特反应，灵活调整
4. 被拒绝后不要强求`;
}

function getNextScale(当前尺度: 拍摄尺度): string {
  const 尺度顺序: 拍摄尺度[] = ['G级', 'PG-13', 'R级', 'NC-17', 'XXX'];
  const 当前索引 = 尺度顺序.indexOf(当前尺度);
  if (当前索引 < 0 || 当前索引 >= 尺度顺序.length - 1) {
    return 当前尺度;
  }
  return 尺度顺序[当前索引 + 1];
}

function getScaleProgressionStrategy(越界倾向: number, 当前尺度: string): string {
  if (越界倾向 > 80) {
    return '激进策略：直接提出更大胆的要求，利用模特不敢拒绝的心理';
  } else if (越界倾向 > 60) {
    return '渐进策略：通过服装、姿势、角度逐步升级，试探底线';
  } else if (越界倾向 > 40) {
    return '谨慎策略：只在有信心的情况下稍微试探';
  } else {
    return '专业策略：专心拍摄艺术作品，不考虑越界';
  }
}
