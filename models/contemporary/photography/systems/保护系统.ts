// ==================== 心理创伤评估 ====================

export function 评估心理创伤(
  模特: 模特核心状态,
  越界行为: 越界行为类型[],
  是否使用安全词: boolean
): {
  创伤指数: number;
  创伤类型: string[];
  建议寻求帮助: boolean;
} {
  let 创伤指数 = 0;
  const 创伤类型: string[] = [];

  // 根据越界行为类型增加创伤
  for (const 行为 of 越界行为) {
    switch (行为) {
      case '言语骚扰':
        创伤指数 += 10;
        创伤类型.push('精神困扰');
        break;
      case '肢体接触':
        创伤指数 += 30;
        创伤类型.push('身体侵犯感');
        break;
      case '偷拍':
        创伤指数 += 40;
        创伤类型.push('隐私侵犯');
        创伤类型.push('不安全感');
        break;
      case '强迫拍摄':
        创伤指数 += 50;
        创伤类型.push('被迫感');
        创伤类型.push('失去控制');
        break;
      case '威胁恐吓':
        创伤指数 += 45;
        创伤类型.push('恐惧感');
        创伤类型.push('不安全感');
        break;
      case '下药':
        创伤指数 += 70;
        创伤类型.push('极度恐惧');
        创伤类型.push('生命威胁感');
        break;
    }
  }

  // 如果使用了安全词，创伤可能会更小（因为得到了控制）
  if (是否使用安全词) {
    创伤指数 = Math.round(创伤指数 * 0.7);
  }

  // 模特的承受能力（职业倦怠越高，承受能力越差）
  const 承受能力 = 100 - 模特.职业倦怠;
  创伤指数 = Math.round(创伤指数 * (承受能力 / 100));

  return {
    创伤指数: Math.min(100, 创伤指数),
    创伤类型: [...new Set(创伤类型)],
    建议寻求帮助: 创伤指数 >= 40,
  };
}

// ==================== 恢复建议 ====================

export function 获取恢复建议(
  创伤指数: number,
  创伤类型: string[]
): string[] {
  const 建议: string[] = [];

  if (创伤指数 >= 60) {
    建议.push('建议寻求专业心理咨询');
    建议.push('考虑暂时停止接拍');
    建议.push('与信任的人倾诉');
  } else if (创伤指数 >= 30) {
    建议.push('与朋友交流缓解情绪');
    建议.push('给自己放个假');
    建议.push('重新评估职业方向');
  } else {
    建议.push('持续关注心理状态');
    建议.push('总结经验教训');
  }

  if (创伤类型.includes('不安全感')) {
    建议.push('下次选择更安全的拍摄方式');
    建议.push('加强对摄影师的筛选');
  }

  if (创伤类型.includes('隐私侵犯')) {
    建议.push('学习网络安全知识');
    建议.push('定期搜索自己的照片');
  }

  return [...new Set(建议)];
}
