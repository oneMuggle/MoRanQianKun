// ==================== 保护机制评估 ====================

export interface 保护机制完整性 {
  拍摄前: { 完成数: number; 总数: number; 完整度: number };
  拍摄中: { 完成数: number; 总数: number; 完整度: number };
  拍摄后: { 完成数: number; 总数: number; 完整度: number };
  总完整度: number;
  风险等级: '安全' | '较安全' | '有风险' | '高风险';
}

export function 评估保护机制完整性(
  已完成检查项: string[]
): 保护机制完整性 {
  const 拍摄前检查项 = 安全检查点列表.filter(s => s.时机.includes('拍摄前'));
  const 拍摄中检查项 = 安全检查点列表.filter(s => s.时机.includes('拍摄中'));
  const 拍摄后检查项 = 安全检查点列表.filter(s => s.时机.includes('拍摄后'));

  const 拍摄前完成 = 拍摄前检查项.filter(s => 已完成检查项.includes(s.检查项));
  const 拍摄中完成 = 拍摄中检查项.filter(s => 已完成检查项.includes(s.检查项));
  const 拍摄后完成 = 拍摄后检查项.filter(s => 已完成检查项.includes(s.检查项));

  const 拍摄前完整度 = 拍摄前检查项.length > 0
    ? Math.round((拍摄前完成.length / 拍摄前检查项.length) * 100)
    : 0;
  const 拍摄中完整度 = 拍摄中检查项.length > 0
    ? Math.round((拍摄中完成.length / 拍摄中检查项.length) * 100)
    : 0;
  const 拍摄后完整度 = 拍摄后检查项.length > 0
    ? Math.round((拍摄后完成.length / 拍摄后检查项.length) * 100)
    : 0;

  const 总完整度 = Math.round((拍摄前完整度 + 拍摄中完整度 + 拍摄后完整度) / 3);

  let 风险等级: '安全' | '较安全' | '有风险' | '高风险';
  if (总完整度 >= 80) {
    风险等级 = '安全';
  } else if (总完整度 >= 60) {
    风险等级 = '较安全';
  } else if (总完整度 >= 40) {
    风险等级 = '有风险';
  } else {
    风险等级 = '高风险';
  }

  return {
    拍摄前: { 完成数: 拍摄前完成.length, 总数: 拍摄前检查项.length, 完整度: 拍摄前完整度 },
    拍摄中: { 完成数: 拍摄中完成.length, 总数: 拍摄中检查项.length, 完整度: 拍摄中完整度 },
    拍摄后: { 完成数: 拍摄后完成.length, 总数: 拍摄后检查项.length, 完整度: 拍摄后完整度 },
    总完整度,
    风险等级,
  };
}

