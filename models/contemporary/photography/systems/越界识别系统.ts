// ==================== 识别函数 ====================

export function 识别言语越界(言语: string): boolean {
  return 言语越界信号列表.some(
    信号 => 言语.toLowerCase().includes(信号.toLowerCase())
  );
}

export function 识别行为越界(行为: string): boolean {
  return 行为越界信号列表.some(
    信号 => 行为.toLowerCase().includes(信号.toLowerCase())
  );
}

export function 获取越界信号类型(
  言语?: string,
  行为?: string
): 越界行为类型[] {
  const 结果: 越界行为类型[] = [];

  if (言语 && 识别言语越界(言语)) {
    结果.push('言语骚扰');
  }

  if (行为 && 识别行为越界(行为)) {
    结果.push('肢体接触');
  }

  return 结果;
}

