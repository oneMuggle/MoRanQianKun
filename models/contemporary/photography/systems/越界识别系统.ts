export function 检测越界场景(
  当前场景: string,
  摄影师行为: string[]
): 越界场景事件 | null {
  const 场景匹配 = 越界场景列表.find(s =>
    当前场景.includes(s.场景) || s.场景.includes(当前场景)
  );

  if (!场景匹配) return null;

  // 检查是否有触发条件被触发
  const 触发数量 = 摄影师行为.filter(行为 =>
    场景匹配.触发条件.some(条件 => 行为.includes(条件))
  ).length;

  if (触发数量 > 0) {
    return 场景匹配;
  }

  return null;
}

// ==================== 越界预防建议 ====================

export function 获取越界预防建议(
  模特: 模特核心状态,
  摄影师: 摄影师核心状态,
  拍摄场所: string
): string[] {
  const 建议: string[] = [];

  // 场所相关建议
  if (拍摄场所.includes('酒店') || 拍摄场所.includes('民宿')) {
    建议.push('选择正规酒店，避免偏僻民宿');
    建议.push('提前告知朋友拍摄地点');
    建议.push('保持房门敞开');
  }

  if (拍摄场所.includes('个人住所') || 拍摄场所.includes('公寓')) {
    建议.push('首次约拍避免选择个人住所');
    建议.push('确认其他人在附近');
    建议.push('携带防护用品');
  }

  // 摄影师相关建议
  if (摄影师.越界投诉次数 > 0) {
    建议.push('该摄影师有越界记录，需格外警惕');
  }

  if (摄影师.偷拍投诉次数 && 摄影师.偷拍投诉次数 > 0) {
    建议.push('该摄影师有偷拍投诉，绝对不要合作');
  }

  // 模特保护意识相关
  if (模特.保护意识 === '极度保护') {
    建议.push('坚持自己的底线，不轻易妥协');
    建议.push('感觉不适立即停止拍摄');
  }

  if (模特.保护意识 === '适度保护') {
    建议.push('明确告知自己的底线');
    建议.push('准备好安全词');
  }

  // 通用建议
  建议.push('拍摄前确认合同内容');
  建议.push('告知朋友预计结束时间');
  建议.push('保持手机畅通');

  return [...new Set(建议)]; // 去重
}
