// ==================== 越界概率评估 ====================

export function 评估越界概率(
  摄影师: 摄影师核心状态,
  项目: 拍摄项目状态,
  场所私密程度: '公开' | '半公开' | '私密'
): {
  越界概率: number;
  最可能越界类型: 越界行为类型[];
  风险等级: '低' | '中' | '高' | '极高';
} {
  // 基础概率来自摄影师的隐藏属性
  const 基础越界概率 = (摄影师.越界倾向 + 摄影师.偷拍倾向 + 摄影师.传播倾向) / 3;

  // 场所调整
  const 场所系数: Record<string, number> = {
    '公开': 0.5,      // 影棚、外景
    '半公开': 1.0,    // 民宿、更衣室
    '私密': 1.5,      // 酒店、个人住所、野外
  };

  // 项目进度调整（拍摄中后期风险更高）
  const 进度系数 = 项目.当前回合 / Math.max(1, 项目.总回合数);

  // 计算最终概率
  const 越界概率 = Math.min(100, 基础越界概率 * 场所系数[场所私密程度] * (0.8 + 进度系数 * 0.4));

  // 确定最可能的越界类型
  const 可能越界: { 类型: 越界行为类型; 概率: number }[] = [
    { 类型: '言语骚扰', 概率: 摄影师.越界倾向 },
    { 类型: '要求加拍', 概率: 摄影师.越界倾向 * 0.8 },
    { 类型: '服装要求', 概率: 摄影师.越界倾向 * 0.7 },
    { 类型: '肢体接触', 概率: 摄影师.越界倾向 * 0.6 },
    { 类型: '偷拍', 概率: 摄影师.偷拍倾向 },
    { 类型: '威胁恐吓', 概率: 摄影师.越界倾向 * 0.3 },
    { 类型: '下药', 概率: 摄影师.偷拍倾向 * 0.2 },
  ];

  // 排序并取前两个
  可能越界.sort((a, b) => b.概率 - a.概率);
  const 最可能越界类型 = 可能越界.slice(0, 2).map(p => p.类型);

  // 风险等级
  let 风险等级: '低' | '中' | '高' | '极高';
  if (越界概率 < 20) {
    风险等级 = '低';
  } else if (越界概率 < 40) {
    风险等级 = '中';
  } else if (越界概率 < 70) {
    风险等级 = '高';
  } else {
    风险等级 = '极高';
  }

  return { 越界概率: Math.round(越界概率), 最可能越界类型, 风险等级 };
}

