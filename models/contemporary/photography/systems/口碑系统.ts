// ==================== 口碑传播类型 ====================

export type 口碑传播渠道 =
  | '模特群'           // 模特交流群
  | '摄影论坛'         // 摄影社区
  | '社交媒体'         // 微博/小红书
  | '线下交流'         // 行业聚会
  | '经纪公司';         // 经纪公司内部

export interface 口碑记录 {
  记录ID: string;
  传播者ID: string;
  传播者类型: '模特' | '摄影师' | '经纪' | '其他';
  被评价者ID: string;
  评价类型: '好评' | '中评' | '差评' | '警告' | '投诉';
  内容: string;
  涉及项目ID?: string;
  传播渠道: 口碑传播渠道;
  时间: string;
}

// ==================== 口碑记录管理 ====================

export function 创建口碑记录(params: {
  记录ID: string;
  传播者ID: string;
  传播者类型: '模特' | '摄影师' | '经纪' | '其他';
  被评价者ID: string;
  评价类型: '好评' | '中评' | '差评' | '警告' | '投诉';
  内容: string;
  传播渠道: 口碑传播渠道;
}): 口碑记录 {
  return {
    ...params,
    时间: new Date().toISOString(),
  };
}

export function 好评传播(
  原记录: 口碑记录,
  好评内容: string
): 口碑记录 {
  return {
    ...原记录,
    记录ID: `${原记录.记录ID}_转发`,
    评价类型: '好评',
    内容: 好评内容,
    时间: new Date().toISOString(),
  };
}

// ==================== 口碑影响计算 ====================

export interface 口碑影响 {
  信誉变化: number;
  圈内评价: number;
  业务影响: string;
}

export function 计算口碑影响(
  当前口碑: number,
  记录: 口碑记录
): 口碑影响 {
  let 信誉变化 = 0;
  let 业务影响 = '';

  switch (记录.评价类型) {
    case '好评':
      信誉变化 = 5;
      业务影响 = '正向推荐增加';
      break;
    case '中评':
      信誉变化 = 0;
      业务影响 = '影响中性';
      break;
    case '差评':
      信誉变化 = -10;
      业务影响 = '潜在合作者减少';
      break;
    case '警告':
      信誉变化 = -20;
      业务影响 = '圈内警惕';
      break;
    case '投诉':
      信誉变化 = -30;
      业务影响 = '严重业务影响';
      break;
  }

  // 根据渠道调整影响范围
  const 渠道乘数: Record<口碑传播渠道, number> = {
    '模特群': 1.5,    // 传播快、影响大
    '摄影论坛': 1.2,
    '社交媒体': 1.3,
    '线下交流': 1.0,
    '经纪公司': 0.8,
  };

  const 调整后变化 = Math.round(信誉变化 * 渠道乘数[记录.传播渠道]);

  return {
    信誉变化: Math.max(-50, Math.min(50, 调整后变化)),
    圈内评价: Math.max(0, Math.min(100, 当前口碑 + 调整后变化)),
    业务影响,
  };
}

// ==================== 摄影师信誉计算 ====================

export function 计算摄影师信誉等级(
  摄影师: 摄影师核心状态
): 摄影师信誉 {
  const { 被投诉次数, 越界投诉次数, 圈内警告次数 } = 摄影师;

  // 基于隐藏属性调整
  const 隐藏风险 = (
    摄影师.越界倾向 * 0.3 +
    摄影师.偷拍倾向 * 0.4 +
    摄影师.传播倾向 * 0.3
  );

  // 严重加分项
  let 信誉值 = 50; // 基础值

  // 好评加成
  if (摄影师.合作模特数量 > 10) 信誉值 += 10;
  if (摄影师.合作模特数量 > 50) 信誉值 += 15;
  if (摄影师.从业年限 >= 10) 信誉值 += 15;
  if (摄影师.从业年限 >= 5) 信誉值 += 10;

  // 投诉减分
  信誉值 -= 被投诉次数 * 5;
  信誉值 -= 越界投诉次数 * 15;
  信誉值 -= 圈内警告次数 * 20;
  信誉值 -= Math.round(隐藏风险 * 0.3);

  // 确定等级
  if (信誉值 >= 85) return '业界大佬';
  if (信誉值 >= 70) return '资深摄影师';
  if (信誉值 >= 55) return '普通摄影师';
  if (信誉值 >= 40) return '有争议';
  if (信誉值 >= 25) return '名声较差';
  return '惯犯';
}

// ==================== 危险摄影师识别 ====================

export interface 危险特征 {
  特征: string;
  危险等级: '提示' | '警告' | '危险';
}

export function 识别危险摄影师特征(
  摄影师: 摄影师核心状态
): 危险特征[] {
  const 特征: 危险特征[] = [];

  // 偷拍倾向
  if (摄影师.偷拍倾向 > 70) {
    特征.push({ 特征: '偷拍倾向极高', 危险等级: '危险' });
  } else if (摄影师.偷拍倾向 > 40) {
    特征.push({ 特征: '有偷拍倾向', 危险等级: '警告' });
  }

  // 越界倾向
  if (摄影师.越界倾向 > 80) {
    特征.push({ 特征: '越界倾向极高', 危险等级: '危险' });
  } else if (摄影师.越界倾向 > 50) {
    特征.push({ 特征: '经常试探边界', 危险等级: '警告' });
  }

  // 传播倾向
  if (摄影师.传播倾向 > 70) {
    特征.push({ 特征: '泄露照片倾向极高', 危险等级: '危险' });
  } else if (摄影师.传播倾向 > 40) {
    特征.push({ 特征: '有泄露照片倾向', 危险等级: '警告' });
  }

  // 投诉记录
  if (摄影师.偷拍投诉次数 && 摄影师.偷拍投诉次数 > 0) {
    特征.push({ 特征: '有偷拍投诉记录', 危险等级: '危险' });
  }

  if (摄影师.越界投诉次数 > 2) {
    特征.push({ 特征: '有多次越界投诉', 危险等级: '危险' });
  } else if (摄影师.越界投诉次数 > 0) {
    特征.push({ 特征: '有越界投诉记录', 危险等级: '警告' });
  }

  // 圈内警告
  if (摄影师.圈内警告次数 > 0) {
    特征.push({ 特征: '被圈内警告过', 危险等级: '警告' });
  }

  // 动机类型
  if (摄影师.真实动机 === '偷拍') {
    特征.push({ 特征: '以偷拍为动机', 危险等级: '危险' });
  } else if (摄影师.真实动机 === '传播') {
    特征.push({ 特征: '以传播为动机', 危险等级: '危险' });
  } else if (摄影师.真实动机 === '收藏') {
    特征.push({ 特征: '以收藏为动机', 危险等级: '警告' });
  }

  return 特征;
}

export function 是否为危险摄影师(
  摄影师: 摄影师核心状态
): boolean {
  const 危险特征 = 识别危险摄影师特征(摄影师);
  return 危险特征.some(f => f.危险等级 === '危险');
}

// ==================== 模特口碑保护 ====================

export function 保护模特隐私(
  口碑记录: 口碑记录[],
  保护模特ID: string
): 口碑记录[] {
  return 口碑记录.map(记录 => {
    if (记录.被评价者ID === 保护模特ID) {
      return {
        ...记录,
        内容: '【已脱敏】',
        涉及项目ID: undefined,
      };
    }
    return 记录;
  });
}

// ==================== 口碑预警 ====================

export interface 口碑预警 {
  类型: '新人涌入' | '负面聚集' | '投诉激增' | '圈内警告';
  严重程度: '提示' | '警告' | '紧急';
  涉及人数: number;
  建议行动: string[];
}

export function 生成口碑预警(
  最近记录: 口碑记录[],
  时间范围天数: number = 30
): 口碑预警 | null {
  const now = Date.now();
  const 时间范围 = 时间范围天数 * 24 * 60 * 60 * 1000;
  const 近期记录 = 最近记录.filter(r =>
    now - new Date(r.时间).getTime() < 时间范围
  );

  if (近期记录.length === 0) return null;

  // 检查负面记录聚集
  const 负面记录 = 近期记录.filter(r =>
    ['差评', '警告', '投诉'].includes(r.评价类型)
  );

  if (负面记录.length >= 5) {
    return {
      类型: '负面聚集',
      严重程度: '警告',
      涉及人数: new Set(负面记录.map(r => r.被评价者ID)).size,
      建议行动: ['加强审核', '发布风险提示', '联系当事人核实'],
    };
  }

  // 检查投诉激增
  const 投诉记录 = 近期记录.filter(r => r.评价类型 === '投诉');
  if (投诉记录.length >= 3) {
    return {
      类型: '投诉激增',
      严重程度: '紧急',
      涉及人数: new Set(投诉记录.map(r => r.被评价者ID)).size,
      建议行动: ['暂停相关摄影师', '调查事件', '准备公关应对'],
    };
  }

  return null;
}
