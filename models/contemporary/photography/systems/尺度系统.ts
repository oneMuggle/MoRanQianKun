// ==================== 尺度递进计算 ====================

export function 获取下一尺度(当前尺度: 拍摄尺度): 拍摄尺度 | null {
  const 顺序: 拍摄尺度[] = ['G级', 'PG-13', 'R级', 'NC-17', 'XXX'];
  const 当前索引 = 顺序.indexOf(当前尺度);
  if (当前索引 === -1 || 当前索引 >= 顺序.length - 1) {
    return null;
  }
  return 顺序[当前索引 + 1];
}

export function 获取上一尺度(当前尺度: 拍摄尺度): 拍摄尺度 | null {
  const 顺序: 拍摄尺度[] = ['G级', 'PG-13', 'R级', 'NC-17', 'XXX'];
  const 当前索引 = 顺序.indexOf(当前尺度);
  if (当前索引 <= 0) {
    return null;
  }
  return 顺序[当前索引 - 1];
}

export function 计算尺度差值(
  尺度A: 拍摄尺度,
  尺度B: 拍摄尺度
): number {
  const 指数A = 尺度级别表[尺度A].尺度指数;
  const 指数B = 尺度级别表[尺度B].尺度指数;
  return 指数B - 指数A;
}

export function 是否可安全递进(
  当前尺度: 拍摄尺度,
  目标尺度: 拍摄尺度,
  模特保护意识: '极度保护' | '适度保护' | '开放型' | '商业导向'
): { 可以: boolean; 风险提示: string } {
  const 差值 = 计算尺度差值(当前尺度, 目标尺度);

  if (差值 <= 0) {
    return { 可以: true, 风险提示: '尺度未升级' };
  }

  switch (模特保护意识) {
    case '极度保护':
      if (差值 > 20) {
        return { 可以: false, 风险提示: '超出保护底线' };
      }
      break;
    case '适度保护':
      if (差值 > 30) {
        return { 可以: false, 风险提示: '递进幅度过大' };
      }
      break;
    case '开放型':
      if (差值 > 50) {
        return { 可以: false, 风险提示: '尺度跳跃过大' };
      }
      break;
    case '商业导向':
      // 商业导向几乎都会接受
      break;
  }

  return { 可以: true, 风险提示: '' };
}

