// 疗愈师状态
export interface 疗愈师状态 {
  ID: string;
  化名: string;
  本名?: string;
  性别: '男' | '女';
  年龄: number;

  // 职业信息
  疗愈方式: 疗愈方式类型[];
  疗愈师等级: 疗愈师等级;
  客户数量: number;
  资深客户数: number;          // 长期客户数
  月收入: number;

  // 专业能力
  专业技能: number;             // 0-100
  催眠能力: number;            // 0-100，催眠技能
  沟通能力: number;             // 0-100
  神秘感营造: number;           // 0-100

  // 身体接触
  身体接触接受度: number;       // 0-100
  身体接触尺度: number;         // 0-100，允许的尺度
  服务类型: '纯疗愈' | '擦边' | '明确越界';

  // 心理
  职业倦怠: number;             // 0-100
  羞耻度: number;                // 0-100
  自我认同: number;              // 0-100，对职业的认同
  收入满意度: number;            // 0-100

  // 客户关系
  客户依赖指数: number;          // 0-100，客户对疗愈师的依赖程度
  师徒关系数量: number;          // 形成的师徒关系数
  暧昧关系数量: number;          // 暧昧关系数

  // 安全
  骚扰经历: number;              // 被骚扰次数
  越界要求次数: number;          // 被要求越界的次数
  投诉经历: number;              // 被投诉次数
}

