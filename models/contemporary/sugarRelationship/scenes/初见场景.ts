// ==================== 场景选择建议 ====================

// 糖宝选择场景
export function 推荐初见场景(
  糖宝: 糖宝核心状态,
  糖爹: 糖爹核心状态
): 初见场景[] {
  const 推荐: { 场景: 初见场景; 分数: number }[] = [];

  for (const [场景, config] of Object.entries(初见场景配置)) {
    let 分数 = 50;

    // 私密性考虑
    if (糖宝.风险感知 > 70) {
      分数 += config.私密性 === '极高' ? 30 : config.私密性 === '高' ? 20 : 0;
    }

    // 糖宝偏好
    if (糖宝.外貌类型 === '清纯学生' || 糖宝.身份类型 === '在校学生') {
      分数 += config.消费水平 === '高' || config.消费水平 === '奢华' ? 15 : 0;
    }

    // 糖爹财富匹配
    if (糖爹.财富等级 === '普通干爹' && config.消费水平 === '奢华') {
      分数 -= 30;
    }
    if (糖爹.财富等级 === '传奇干爹' && config.消费水平 !== '奢华') {
      分数 -= 20;
    }

    // 风险考虑
    分数 -= config.风险等级 * 0.5;

    推荐.push({ 场景: 场景 as 初见场景, 分数 });
  }

  return 推荐
    .sort((a, b) => b.分数 - a.分数)
    .slice(0, 3)
    .map(r => r.场景);
}

// ==================== 场景执行 ====================

// 初见场景执行
export function 执行初见场景(
  糖宝: 糖宝核心状态,
  糖爹: 糖爹核心状态,
  场景: 初见场景,
  期望零花钱?: number
): {
  场景结果: '成功' | '继续了解' | '失败';
  匹配度: number;
  问题: string[];
  建议: string[];
  关系?: 糖关系状态完整;
  糖宝?: 糖宝核心状态;
  糖爹?: 糖爹核心状态;
} {
  const config = 初见场景配置[场景];
  let 问题: string[] = [];
  let 建议: string[] = [];

  // 计算第一印象匹配度
  let 匹配度 = 50;

  // 年龄匹配
  if (糖爹.年龄 > 55) {
    匹配度 -= 15;
    问题.push('年龄差距较大');
  }

  // 外观第一印象
  if (糖宝.外貌类型 === '清纯学生' && config.消费水平 === '奢华') {
    匹配度 += 10;
  }

  // 气场匹配
  if (糖宝.身份类型 === '职场新人' || 糖宝.身份类型 === '全职被包养') {
    匹配度 += (config.私密性 === '高' || config.私密性 === '极高') ? 15 : -10;
  }

  // 零花钱期望
  const 实际零花 = 期望零花钱 || Math.round(10000 + Math.random() * 40000);
  if (实际零花 < 10000 && 糖爹.财富等级 !== '普通干爹') {
    建议.push('可以尝试争取更高零花钱');
  }

  // 场景特定问题
  if (场景 === '游艇派对' && !糖宝.身份类型 === '网红/主播') {
    问题.push('游艇派对可能有其他认识的人');
  }

  if (场景 === '海外度假') {
    问题.push('海外度假意味着更快的进展');
    建议.push('确保了解对方真实意图后再决定');
  }

  // 判断结果
  let 场景结果: '成功' | '继续了解' | '失败';
  if (匹配度 >= 70) {
    场景结果 = '成功';
  } else if (匹配度 >= 45) {
    场景结果 = '继续了解';
  } else {
    场景结果 = '失败';
  }

  // 如果成功，建立关系
  if (场景结果 === '成功') {
    const { 关系, 糖宝: newSugarBaby, 糖爹: newSugarDaddy } = 建立关系(
      糖宝,
      糖爹,
      实际零花
    );

    return {
      场景结果,
      匹配度,
      问题,
      建议,
      关系,
      糖宝: newSugarBaby,
      糖爹: newSugarDaddy,
    };
  }

  return {
    场景结果,
    匹配度,
    问题,
    建议,
  };
}

// ==================== 见面后评估 ====================

// 评估第一次见面后的感受
export function 评估初见感受(
  糖宝: 糖宝核心状态,
  糖爹: 糖爹核心状态,
  场景: 初见场景,
  糖宝满意: boolean,
  糖爹满意: boolean
): {
  是否继续: boolean;
  进入下一阶段概率: number;
  糖宝反馈: string;
  糖爹反馈: string;
} {
  let 进入下一阶段概率 = 50;

  // 双向满意
  if (糖宝满意 && 糖爹满意) {
    进入下一阶段概率 = 85;
  } else if (糖宝满意 || 糖爹满意) {
    进入下一阶段概率 = 45;
  } else {
    进入下一阶段概率 = 10;
  }

  // 场景调整
  if (场景 === '线上视频') {
    进入下一阶段概率 *= 0.8; // 线上见面需要更多了解
  } else if (场景 === '私人会所' || 场景 === '海外度假') {
    进入下一阶段概率 *= 1.2; // 高端场合表示认真
  }

  const 糖宝反馈 = 糖宝满意
    ? `对${糖爹.化名}的谈吐和气场印象深刻，愿意继续了解`
    : `感觉一般，需要更多时间考虑`;

  const 糖爹反馈 = 糖爹满意
    ? `对${糖宝.化名}的外貌和气质满意，希望进一步发展`
    : `觉得还需要再看看其他选择`;

  return {
    是否继续: Math.random() < 进入下一阶段概率 / 100,
    进入下一阶段概率: Math.round(进入下一阶段概率),
    糖宝反馈,
    糖爹反馈,
  };
}
