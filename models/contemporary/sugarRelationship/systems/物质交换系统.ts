// 发放零花钱
export function 发放零花钱(
  关系: 糖关系状态完整,
  糖宝: 糖宝核心状态,
  糖爹: 糖爹核心状态,
  金额?: number
): {
  关系: 糖关系状态完整;
  糖宝: 糖宝核心状态;
  糖爹: 糖爹核心状态;
  金额: number;
} {
  const 实际金额 = 金额 ?? 关系.月零花钱;

  // 记录礼物
  let newRelation = 添加礼物记录(
    关系,
    `月度零花钱`,
    实际金额,
    '转账'
  );

  // 更新糖宝
  let newSugarBaby = 糖宝获得收益(糖宝, 实际金额, '转账');
  newSugarBaby.物质依赖度 = Math.min(100, 糖宝.物质依赖度 + 实际金额 / 50000);
  newSugarBaby.幸福度 = Math.min(100, 糖宝.幸福度 + Math.min(10, 实际金额 / 10000));

  // 更新糖爹
  let newSugarDaddy = 糖爹花费(糖爹, 实际金额);

  return {
    关系: newRelation,
    糖宝: newSugarBaby,
    糖爹: newSugarDaddy,
    金额: 实际金额,
  };
}

// 发放周零花钱
export function 发放周零花钱(
  关系: 糖关系状态完整,
  糖宝: 糖宝核心状态,
  糖爹: 糖爹核心状态
): {
  关系: 糖关系状态完整;
  糖宝: 糖宝核心状态;
  糖爹: 糖爹核心状态;
  金额: number;
} {
  const 周零花 = Math.round(关系.月零花钱 / 4);

  return 发放零花钱(关系, 糖宝, 糖爹, 周零花);
}

// ==================== 礼物操作 ====================

// 赠送礼物
export function 赠送礼物(
  关系: 糖关系状态完整,
  糖宝: 糖宝核心状态,
  糖爹: 糖爹核心状态,
  礼物类型: keyof typeof 礼物等级,
  自定义内容?: string
): {
  关系: 糖关系状态完整;
  糖宝: 糖宝核心状态;
  糖爹: 糖爹核心状态;
  礼物价值: number;
  礼物内容: string;
} {
  const 等级配置 = 礼物等级[礼物类型];
  const 价值范围 = 等级配置.范围.split('-').map(s => {
    const match = s.match(/(\d+)/);
    return match ? parseInt(match[1]) * (s.includes('万') ? 10000 : 1) : 0;
  });

  // 随机在范围内取值
  const 礼物价值 = 价值范围.length === 2
    ? Math.round(价值范围[0] + Math.random() * (价值范围[1] - 价值范围[0]))
    : 价值范围[0] || 10000;

  const 礼物内容 = 自定义内容 || 等级配置.典型礼物[Math.floor(Math.random() * 等级配置.典型礼物.length)];

  // 记录礼物
  let newRelation = 添加礼物记录(
    关系,
    礼物内容,
    礼物价值,
    礼物价值 > 100000 ? '房产' : '奢侈品'
  );

  // 更新糖宝
  let newSugarBaby = 糖宝获得收益(糖宝, 礼物价值, 礼物类型 === '日常零花' ? '现金' : '奢侈品');
  newSugarBaby.奢侈品数量 = 礼物类型 !== '日常零花' ? 糖宝.奢侈品数量 + 1 : 糖宝.奢侈品数量;
  newSugarBaby.幸福度 = Math.min(100, 糖宝.幸福度 + Math.min(15, 礼物价值 / 10000));
  newSugarBaby.情感依赖度 = Math.min(100, 糖宝.情感依赖度 + 礼物价值 / 50000);

  // 更新糖爹
  let newSugarDaddy = 糖爹花费(糖爹, 礼物价值);
  newSugarDaddy.慷慨度 = Math.min(100, 糖爹.慷慨度 + 礼物价值 / 100000);

  return {
    关系: newRelation,
    糖宝: newSugarBaby,
    糖爹: newSugarDaddy,
    礼物价值,
    礼物内容,
  };
}

// 生日/纪念日大额礼物
export function 赠送大额礼物(
  关系: 糖关系状态完整,
  糖宝: 糖宝核心状态,
  糖爹: 糖爹核心状态,
  节日类型: '生日' | '纪念日' | '特殊节日'
): {
  关系: 糖关系状态完整;
  糖宝: 糖宝核心状态;
  糖爹: 糖爹核心状态;
  礼物价值: number;
  礼物内容: string;
} {
  // 大额礼物范围 20-100万
  const 礼物价值 = Math.round(200000 + Math.random() * 800000);
  const 礼物内容 = `${节日类型}礼物`;

  let newRelation = 添加礼物记录(
    关系,
    礼物内容,
    礼物价值,
    礼物价值 > 500000 ? '房产' : '奢侈品'
  );

  let newSugarBaby = 糖宝获得收益(糖宝, 礼物价值, '奢侈品');
  newSugarBaby.奢侈品数量 = 糖宝.奢侈品数量 + 1;
  newSugarBaby.幸福度 = Math.min(100, 糖宝.幸福度 + 20);
  newSugarBaby.情感依赖度 = Math.min(100, 糖宝.情感依赖度 + 15);

  let newSugarDaddy = 糖爹花费(糖爹, 礼物价值);
  newSugarDaddy.慷慨度 = Math.min(100, 糖爹.慷慨度 + 5);

  return {
    关系: newRelation,
    糖宝: newSugarBaby,
    糖爹: newSugarDaddy,
    礼物价值,
    礼物内容,
  };
}

// ==================== 谈判与调整 ====================

// 糖宝要求加零花钱
export function 要求加零花钱(
  关系: 糖关系状态完整,
  糖宝: 糖宝核心状态,
  糖爹: 糖爹核心状态,
  要求金额: number
): {
  成功: boolean;
  实际金额: number;
  关系: 糖关系状态完整;
  糖宝: 糖宝核心状态;
  糖爹: 糖爹核心状态;
  原因?: string;
} {
  const 当前金额 = 关系.月零花钱;
  const 涨幅 = (要求金额 - 当前金额) / 当前金额;

  // 涨幅超过50%很难成功
  if (涨幅 > 0.5) {
    return {
      成功: false,
      实际金额: 当前金额,
      关系,
      糖宝,
      糖爹,
      原因: '涨幅过大，糖爹难以接受',
    };
  }

  // 糖宝满意度高、成功率高
  const 成功率 = (关系.糖宝满意度 / 100) * (糖爹.慷慨度 / 100) * (1 - 涨幅 * 0.5);

  if (Math.random() < 成功率) {
    return {
      成功: true,
      实际金额: 要求金额,
      关系: { ...关系, 月零花钱: 要求金额 },
      糖宝,
      糖爹,
    };
  } else {
    // 谈判失败，可能影响关系
    return {
      成功: false,
      实际金额: 当前金额,
      关系: {
        ...关系,
        糖宝满意度: Math.max(0, 关系.糖宝满意度 - 10),
        糖宝投入度: Math.max(0, 关系.糖宝投入度 - 5),
      },
      糖宝: {
        ...糖宝,
        幸福度: Math.max(0, 糖宝.幸福度 - 5),
      },
      糖爹,
      原因: '糖爹拒绝，关系受到影响',
    };
  }
}

// ==================== 经济统计 ====================

// 计算糖宝总收入
export function 计算糖宝总收入(糖宝: 糖宝核心状态): {
  月收入: number;
  年收入: number;
  名下资产: number;
  净资产: number;
} {
  return {
    月收入: 糖宝.月零花钱,
    年收入: 糖宝.月零花钱 * 12,
    名下资产: 糖宝.名下资产,
    净资产: 糖宝.名下资产 + 糖宝.累计获得 - 糖宝.债务,
  };
}

// 计算糖爹总支出
export function 计算糖爹总支出(糖爹: 糖爹核心状态): {
  月支出: number;
  年支出: number;
  累计支出: number;
  支出占比: number;
} {
  return {
    月支出: 糖爹.同时糖宝数 > 0 ? 糖爹.累计花费 / Math.max(1, 糖爹.历任糖宝数) : 0,
    年支出: (糖爹 as any).月支出 * 12,
    累计支出: 糖爹.累计花费,
    支出占比: 糖爹.年收入 > 0 ? (糖爹.累计花费 / 糖爹.年收入) * 100 : 0,
  };
}
