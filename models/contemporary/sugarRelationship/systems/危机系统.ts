// 评估当前曝光风险
export function 评估曝光风险(
  糖宝: 糖宝核心状态,
  糖爹: 糖爹核心状态,
  关系: 糖关系状态完整
): 曝光风险评估 {
  const 风险来源 = {
    '网络痕迹': Math.min(100, 风险来源配置['网络痕迹'].基础风险 + (糖宝.曝光风险 - 20)),
    '熟人泄露': Math.min(100, 风险来源配置['熟人泄露'].基础风险 + (糖宝.入行次数 - 1) * 5),
    '原配调查': Math.min(100, 风险来源配置['原配调查'].基础风险 + (糖爹.曝光风险 - 15)),
    '照片泄露': Math.min(100, 风险来源配置['照片泄露'].基础风险),
    '经济异常': Math.min(100, 风险来源配置['经济异常'].基础风险 + (糖宝.奢侈品数量 - 3) * 3),
    '竞争对手': Math.min(100, 风险来源配置['竞争对手'].基础风险),
  };

  const 当前风险值 = Math.round(
    (风险来源['网络痕迹'] * 0.15 +
      风险来源['熟人泄露'] * 0.25 +
      风险来源['原配调查'] * 0.25 +
      风险来源['照片泄露'] * 0.15 +
      风险来源['经济异常'] * 0.1 +
      风险来源['竞争对手'] * 0.1)
  );

  const 风险升级触发: string[] = [];
  if (糖宝.同时维持 && 糖宝.备胎数量 > 0) {
    风险升级触发.push('同时维持多个关系');
  }
  if (糖宝.奢侈品数量 > 10) {
    风险升级触发.push('奢侈品过多引起关注');
  }
  if (糖爹.婚姻状态 === '已婚' && (糖爹 as any).配偶不知情) {
    风险升级触发.push('原配可能发现');
  }
  if (关系.持续时间 > 24) {
    风险升级触发.push('长期关系风险累积');
  }

  let 当前风险阶段: 曝光风险评估['当前风险阶段'] = '安全';
  if (当前风险值 >= 80) 当前风险阶段 = '极危';
  else if (当前风险值 >= 60) 当前风险阶段 = '危险';
  else if (当前风险值 >= 40) 当前风险阶段 = '警告';
  else if (当前风险值 >= 20) 当前风险阶段 = '注意';

  return {
    当前风险值,
    风险来源,
    风险升级触发,
    当前风险阶段,
  };
}

// 触发危机事件
export function 触发危机事件(
  关系: 糖关系状态完整,
  糖宝: 糖宝核心状态,
  糖爹: 糖爹核心状态,
  事件: 危机事件
): {
  关系: 糖关系状态完整;
  糖宝: 糖宝核心状态;
  糖爹: 糖爹核心状态;
  影响: {
    糖宝焦虑度变化: number;
    糖宝曝光风险变化: number;
    糖爹曝光风险变化: number;
    关系状态变化: '糖宝满意度下降' | '危机期' | '关系结束' | '无变化';
  };
} {
  let newRelation = 添加危机事件(关系, 事件);

  let 糖宝焦虑度变化 = 0;
  let 糖宝曝光风险变化 = 0;
  let 糖爹曝光风险变化 = 0;
  let 关系状态变化: '糖宝满意度下降' | '危机期' | '关系结束' | '无变化' = '无变化';

  switch (事件) {
    case '原配找上门':
      糖宝焦虑度变化 = 30;
      糖宝曝光风险变化 = 40;
      糖爹曝光风险变化 = 30;
      关系状态变化 = '危机期';
      break;
    case '家人发现':
      糖宝焦虑度变化 = 25;
      糖宝曝光风险变化 = 20;
      关系状态变化 = '糖宝满意度下降';
      break;
    case '照片泄露':
      糖宝曝光风险变化 = 35;
      关系状态变化 = '危机期';
      break;
    case '聊天记录泄露':
      糖宝曝光风险变化 = 25;
      break;
    case '被跟踪':
      糖宝焦虑度变化 = 20;
      糖宝曝光风险变化 = 15;
      break;
    case '被举报':
      糖宝曝光风险变化 = 30;
      糖爹曝光风险变化 = 25;
      关系状态变化 = '危机期';
      break;
    case '网络曝光':
      糖宝曝光风险变化 = 50;
      关系状态变化 = '关系结束';
      break;
    case '竞争对手抹黑':
      糖宝曝光风险变化 = 20;
      break;
    case '仙人跳':
      糖宝焦虑度变化 = 35;
      糖宝曝光风险变化 = 25;
      break;
    case '染病风险':
      糖宝焦虑度变化 = 25;
      break;
    case '意外怀孕':
      糖宝焦虑度变化 = 30;
      关系状态变化 = '危机期';
      break;
    case '原配威胁':
      糖宝焦虑度变化 = 25;
      关系状态变化 = '危机期';
      break;
  }

  const newSugarBaby = 更新糖宝状态(糖宝, {
    焦虑度: Math.min(100, 糖宝.焦虑度 + 糖宝焦虑度变化),
    曝光风险: Math.min(100, 糖宝.曝光风险 + 糖宝曝光风险变化),
  });

  const newSugarDaddy = 更新糖爹状态(糖爹, {
    曝光风险: Math.min(100, 糖爹.曝光风险 + 糖爹曝光风险变化),
  });

  return {
    关系: newRelation,
    糖宝: newSugarBaby,
    糖爹: newSugarDaddy,
    影响: {
      糖宝焦虑度变化,
      糖宝曝光风险变化,
      糖爹曝光风险变化,
      关系状态变化,
    },
  };
}

