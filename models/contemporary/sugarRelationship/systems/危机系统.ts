// 执行危机应对
export function 执行危机应对(
  关系: 糖关系状态完整,
  糖宝: 糖宝核心状态,
  糖爹: 糖爹核心状态,
  事件: 危机事件,
  选择: string
): {
  成功: boolean;
  关系: 糖关系状态完整;
  糖宝: 糖宝核心状态;
  糖爹: 糖爹核心状态;
  结果描述: string;
} {
  const 选项列表 = 获取危机应对选项(事件, 糖宝, 糖爹);
  const 选择项 = 选项列表.find(o => o.选项 === 选择);

  if (!选择项) {
    return {
      成功: false,
      关系,
      糖宝,
      糖爹,
      结果描述: '无效的选择',
    };
  }

  const 成功 = Math.random() < 选择项.成功率;

  let newSugarBaby = { ...糖宝 };
  let newSugarDaddy = { ...糖爹 };
  let newRelation = { ...关系 };

  if (成功) {
    // 成功化解
    newSugarBaby.焦虑度 = Math.max(0, 糖宝.焦虑度 - 20);
  } else {
    // 应对失败
    newSugarBaby.焦虑度 = Math.min(100, 糖宝.焦虑度 + 15);
    newSugarBaby.曝光风险 = Math.min(100, 糖宝.曝光风险 + 10);
  }

  // 如果选择项有成本
  if (选择项.成本 > 0 && 选择项.成功率 > 0.5) {
    newSugarBaby.累计获得 = 糖宝.累计获得 - 选择项.成本;
    newSugarDaddy.累计花费 = 糖爹.累计花费 + 选择项.成本;
  }

  const 结果描述 = 成功
    ? `成功${选择}，危机化解`
    : `${选择}失败，需要进一步处理`;

  return {
    成功,
    关系: newRelation,
    糖宝: newSugarBaby,
    糖爹: newSugarDaddy,
    结果描述,
  };
}
