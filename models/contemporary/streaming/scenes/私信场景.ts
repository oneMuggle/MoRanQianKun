// ==================== 私信场景生成 ====================

export interface 私信场景生成配置 {
  主播昵称: string;
  粉丝昵称: string;
  粉丝等级: 粉丝等级;
  当前关系: 粉丝关系;
  互动次数: number;
  消息内容?: string;
}

/**
 * 生成私信场景
 */
export function 生成私信场景(config: 私信场景生成配置): {
  主播回复: string;
  关系变化: 粉丝关系 | null;
  特殊事件: string | null;
} {
  const { 主播昵称, 粉丝昵称, 粉丝等级, 当前关系, 互动次数, 消息内容 } = config;
  
  // 检查暧昧升级触发
  for (const 触发 of 暧昧升级触发配置) {
    let 匹配 = true;
    
    if (触发.条件.关系 && 触发.条件.关系 !== 当前关系) {
      匹配 = false;
    }
    if (触发.条件.等级 && 触发.条件.等级 !== 粉丝等级) {
      匹配 = false;
    }
    if (触发.条件.互动次数 && 触发.条件.互动次数 < 互动次数) {
      匹配 = false;
    }
    
    if (匹配) {
      const 话题 = 触发.话题[Math.floor(Math.random() * 触发.话题.length)];
      const 反应 = 触发.反应[当前关系] || 触发.反应['陌生'];
      
      // 检查是否触发关系升级
      let 关系变化: 粉丝关系 | null = null;
      if (当前关系 === '陌生' && 互动次数 >= 5) {
        关系变化 = '熟悉';
      } else if (当前关系 === '熟悉' && 粉丝等级 === '守护') {
        关系变化 = '暧昧';
      } else if (当前关系 === '暧昧' && 粉丝等级 === '榜一') {
        关系变化 = '金主';
      }
      
      return {
        主播回复: `${粉丝昵称}：${话题}\n${主播昵称}：${反应}`,
        关系变化,
        特殊事件: 关系变化 ? `关系升级为${关系变化}` : null,
      };
    }
  }
  
  // 默认回复
  const 默认话题 = 获取私信话题(当前关系);
  return {
    主播回复: `${粉丝昵称}：${消息内容 || '嗨~'}\n${主播昵称}：${默认话题}`,
    关系变化: null,
    特殊事件: null,
  };
}

// ==================== 线下见面场景 ====================

export interface 线下见面场景 {
  见面类型: '纯粹感谢' | '暧昧试探' | '明确目的';
  场景描述: string;
  可能结局: string[];
  关系影响: '升温' | '降温' | '维持';
}

/**
 * 生成线下见面场景
 */
export function 生成线下见面场景(
  粉丝等级: 粉丝等级,
  之前关系: 粉丝关系
): 线下见面场景 {
  const 见面类型概率 = Math.random();
  let 见面类型: '纯粹感谢' | '暧昧试探' | '明确目的';
  
  if (粉丝等级 === '皇帝') {
    见面类型 = 见面类型概率 < 0.3 ? '纯粹感谢' : 
                见面类型概率 < 0.7 ? '暧昧试探' : '明确目的';
  } else if (粉丝等级 === '榜一') {
    见面类型 = 见面类型概率 < 0.5 ? '暧昧试探' : '明确目的';
  } else {
    见面类型 = '暧昧试探';
  }
  
  const 场景描述表: Record<string, string[]> = {
    '纯粹感谢': [
      `${粉丝等级}级粉丝${粉丝等级}约见面，只是为了当面说声谢谢...`,
      '粉丝准备了小礼物，感谢一直以来的支持...',
    ],
    '暧昧试探': [
      '粉丝的眼神中带着某种期待...',
      '气氛变得微妙，双方都在试探...',
    ],
    '明确目的': [
      '粉丝表达了想要更深层次关系的意愿...',
      '这次见面似乎不只是普通的朋友约会...',
    ],
  };
  
  const 结局表: Record<string, string[]> = {
    '纯粹感谢': ['成为好朋友', '保持现状', '关系更进一步'],
    '暧昧试探': ['关系升级', '维持暧昧', '发现不合适'],
    '明确目的': ['确立特殊关系', '被拒绝但保持联系', '发生某些事情后分开'],
  };
  
  return {
    见面类型,
    场景描述: 场景描述表[见面类型][Math.floor(Math.random() * 场景描述表[见面类型].length)],
    可能结局: 结局表[见面类型],
    关系影响: 见面类型 === '明确目的' ? '升温' : '维持',
  };
}
