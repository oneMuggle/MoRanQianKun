/**
 * 检查PK暧昧升级
 */
export function 检查PK暧昧升级(
  惩罚尺度: 内容尺度,
  弹幕密度: number,
  粉丝参与度: number
): { 升级: boolean; 描述: string; 效果: string } {
  // 检查各升级条件
  for (const config of PK暧昧升级配置) {
    const { 触发条件 } = config;
    
    if (触发条件.惩罚尺度 && 触发条件.惩罚尺度 === 惩罚尺度) {
      return {
        升级: true,
        描述: config.描述[Math.floor(Math.random() * config.描述.length)],
        效果: config.升级效果,
      };
    }
    
    if (触发条件.弹幕暗示 && 弹幕密度 > 50) {
      return {
        升级: true,
        描述: config.描述[Math.floor(Math.random() * config.描述.length)],
        效果: config.升级效果,
      };
    }
  }
  
  return { 升级: false, 描述: '', 效果: '' };
}
