/**
 * 生成PK发起描述
 */
export function 生成PK发起描述(
  主播人设: 主播人设,
  对手人设: 主播人设,
  是否有暧昧: boolean
): string {
  let 类型: '同风格' | '跨风格' | '暧昧' = '跨风格';
  
  if (是否有暧昧) {
    类型 = '暧昧';
  } else if (主播人设 === 对手人设) {
    类型 = '同风格';
  }
  
  const 话术池 = PK发起话术[类型];
  return 话术池[Math.floor(Math.random() * 话术池.length)];
}

