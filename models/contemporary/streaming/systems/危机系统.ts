/**
 * 评估危机影响范围
 */
export function 评估危机影响(事件类型: 危机事件类型, 主播粉丝数: number): number {
  const 基础影响表: Record<危机事件类型, number> = {
    '黑料曝光': 50000,
    '竞争对手抹黑': 10000,
    '粉丝撕逼': 5000,
    '平台封禁': 100000,
    '私照泄露': 30000,
    '线下纠纷': 20000,
  };

  const 基础影响 = 基础影响表[事件类型] || 10000;
  // 粉丝数越多，影响范围越大（对数增长）
  const 放大系数 = Math.log10(Math.max(主播粉丝数, 1)) + 1;
  return Math.floor(基础影响 * 放大系数);
}

/**
 * 计算舆论演化
 */
export function 计算舆论演化(
  当前状态: 舆论状态
): { 下一状态: 舆论状态; 持续时间: number } | null {
  const 配置 = 舆论演化配置[当前状态];
  if (配置.下一状态.length === 0) return null;

  const 随机 = Math.random() * 100;
  let 累计概率 = 0;

  for (let i = 0; i < 配置.下一状态.length; i++) {
    累计概率 += 配置.概率[i];
    if (随机 < 累计概率) {
      return {
        下一状态: 配置.下一状态[i],
        持续时间: 配置.持续时间,
      };
    }
  }

  return null;
}

/**
 * 选择最佳公关策略
 */
export function 选择公关策略(
  预算: number,
  事件严重程度: '轻微' | '中等' | '严重' | '毁灭'
): 公关策略 {
  const 有效策略 = Object.entries(危机处理策略效果)
    .filter(([_, 效果]) => 效果.成本 <= 预算)
    .sort((a, b) => b[1].成功率 - a[1].成功率);

  if (有效策略.length === 0) return '冷处理';

  // 根据严重程度调整选择
  if (事件严重程度 === '毁灭') {
    // 严重情况优先选择高成功率策略
    return 有效策略[0][0] as 公关策略;
  } else if (事件严重程度 === '严重') {
    // 严重情况避免低成功率策略
    const 中高成功率 = 有效策略.filter(([_, 效果]) => 效果.成功率 >= 50);
    if (中高成功率.length > 0) {
      return 中高成功率[0][0] as 公关策略;
    }
  }

  return 有效策略[0][0] as 公关策略;
}

/**
 * 处理危机事件
 */
export function 处理危机(
  事件: 危机事件,
  策略: 公关策略,
  预算: number
): { 成功: boolean; 处理状态: '待处理' | '处理中' | '已平息' | '失控'; 处理方案: string } {
  const 效果 = 危机处理策略效果[策略];
  const 成功 = Math.random() * 100 < 效果.成功率;

  return {
    成功,
    处理状态: 成功 ? '已平息' : '处理中',
    处理方案: `采取${策略}，投入${预算}元`,
  };
}
