import {
  危机事件,
  危机事件类型,
  舆论状态,
  主播核心状态,
} from '../types';

/**
 * 创建危机事件
 */
export function 创建危机事件(
  事件类型: 危机事件类型,
  影响范围: number = 1000
): 危机事件 {
  return {
    事件ID: `危机_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`,
    事件类型,
    发生时间: new Date().toISOString(),
    发酵程度: '小范围发酵',
    影响范围,
    处理状态: '待处理',
  };
}

/**
 * 舆论发酵升级
 */
export function 舆论升级(当前状态: 舆论状态): 舆论状态 {
  const 升级路径: Record<舆论状态, 舆论状态> = {
    '无危机': '无危机',
    '小范围发酵': '大范围扩散',
    '大范围扩散': '大范围扩散', // 可能上热搜
    '舆论反转': '平息',
    '平息': '平息',
  };
  
  return 升级路径[当前状态];
}

/**
 * 舆论平息
 */
export function 舆论平息(当前状态: 舆论状态): 舆论状态 {
  return '平息';
}

/**
 * 危机事件添加到主播状态
 */
export function 添加危机事件(
  主播状态: 主播核心状态,
  事件: 危机事件
): 主播核心状态 {
  return {
    ...主播状态,
    当前舆论状态: 事件.发酵程度,
    危机事件列表: [...主播状态.危机事件列表, 事件],
    历史危机次数: 主播状态.历史危机次数 + 1,
  };
}

/**
 * 危机处理状态更新
 */
export function 更新危机处理状态(
  事件: 危机事件,
  新状态: '待处理' | '处理中' | '已平息' | '失控',
  处理方案?: string
): 危机事件 {
  return {
    ...事件,
    处理状态: 新状态,
    处理方案: 处理方案 || 事件.处理方案,
  };
}

/**
 * 检查网络热度触发危机阈值
 */
export function 检查危机阈值(网络热度: number): boolean {
  // 高热度主播更容易触发危机
  return 网络热度 > 70 && Math.random() < 网络热度 / 200;
}

/**
 * 危机影响计算
 */
export function 计算危机影响(
  事件: 危机事件,
  主播状态: 主播核心状态
): { 粉丝流失: number; 收入影响: number; 信誉损失: number } {
  const 基础影响 = 事件.影响范围 / 100;
  
  const 粉丝流失 = Math.floor(基础影响 * (1 + 主播状态.历史危机次数 * 0.1));
  const 收入影响 = Math.floor(主播状态.月收入 * 0.1 * 基础影响);
  const 信誉损失 = Math.floor(基础影响 * 10);
  
  return { 粉丝流失, 收入影响, 信誉损失 };
}

/**
 * 应用危机影响
 */
export function 应用危机影响(
  主播状态: 主播核心状态,
  事件: 危机事件
): 主播核心状态 {
  const { 粉丝流失, 收入影响, 信誉损失 } = 计算危机影响(事件, 主播状态);
  
  return {
    ...主播状态,
    粉丝数量: Math.max(0, 主播状态.粉丝数量 - 粉丝流失),
    月收入: Math.max(0, 主播状态.月收入 - 收入影响),
    网络热度: Math.max(0, 主播状态.网络热度 - 信誉损失),
    当前舆论状态: 事件.发酵程度,
  };
}

/**
 * 危机公关处理
 */
export function 危机公关处理(
  主播状态: 主播核心状态,
  事件: 危机事件,
  处理方案: string
): 主播核心状态 {
  const 处理后事件 = 更新危机处理状态(事件, '处理中', 处理方案);
  
  // 简化判断：大部分公关处理能平息危机
  const 公关成功率 = 0.7;
  const 成功 = Math.random() < 公关成功率;
  
  if (成功) {
    const 平息后事件 = 更新危机处理状态(处理后事件, '已平息');
    
    return {
      ...主播状态,
      危机事件列表: 主播状态.危机事件列表.map(e =>
        e.事件ID === 事件.事件ID ? 平息后事件 : e
      ),
      当前舆论状态: '平息',
    };
  } else {
    const 失控后事件 = 更新危机处理状态(处理后事件, '失控');
    const 升级后事件 = { ...失控后事件, 发酵程度: 舆论升级(失控后事件.发酵程度) };
    
    return {
      ...主播状态,
      危机事件列表: 主播状态.危机事件列表.map(e =>
        e.事件ID === 事件.事件ID ? 升级后事件 : e
      ),
      当前舆论状态: 升级后事件.发酵程度,
    };
  }
}

/**
 * 黑料曝光处理
 */
export function 处理黑料曝光(
  主播状态: 主播核心状态,
  黑料内容: string
): 主播核心状态 {
  const 事件 = 创建危机事件('黑料曝光');
  
  // 检查是否是已泄露的秘密
  if (主播状态.已泄露秘密.includes(黑料内容)) {
    // 旧料新炒，影响较小
    事件.影响范围 = Math.floor(事件.影响范围 * 0.3);
  } else {
    // 新料，影响较大
    事件.影响范围 = Math.floor(事件.影响范围 * 1.5);
    // 添加到已泄露秘密
    return {
      ...添加危机事件(主播状态, 事件),
      已泄露秘密: [...主播状态.已泄露秘密, 黑料内容],
    };
  }
  
  return 添加危机事件(主播状态, 事件);
}

/**
 * 竞争对手抹黑处理
 */
export function 处理竞争对手抹黑(
  主播状态: 主播核心状态,
  抹黑内容: string
): 主播核心状态 {
  const 事件 = 创建危机事件('竞争对手抹黑');
  
  // 检查主播的粉丝基础（铁粉多则影响小）
  const 铁粉抵抗 = 主播状态.粉丝数量 / 10000; // 最多减少50%影响
  事件.影响范围 = Math.floor(事件.影响范围 * (1 - Math.min(0.5, 铁粉抵抗)));
  
  return 添加危机事件(主播状态, 事件);
}

/**
 * 平台封禁处理
 */
export function 处理平台封禁(
  主播状态: 主播核心状态,
  封禁时长: number // 天数
): 主播核心状态 {
  if (封禁时长 > 30) {
    return {
      ...主播状态,
      主播状态: '封禁' as any,
      当前直播状态: '被封禁' as any,
    };
  }
  
  // 临时封禁
  return {
    ...主播状态,
    当前直播状态: '被封禁' as any,
  };
}

/**
 * 私照泄露处理
 */
export function 处理私照泄露(
  主播状态: 主播核心状态
): 主播核心状态 {
  const 事件 = 创建危机事件('私照泄露');
  
  // 对"纯情玉女"人设影响最大
  const 人设影响系数 = 主播状态.人设 === '纯情玉女' ? 1.5 : 1.0;
  事件.影响范围 = Math.floor(事件.影响范围 * 人设影响系数);
  
  return 添加危机事件(主播状态, 事件);
}
