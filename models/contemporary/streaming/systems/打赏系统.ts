// ==================== 权益判断函数 ====================

/**
 * 检查粉丝是否解锁了特定权限
 */
export function 检查权限(粉丝: 粉丝核心状态, 权限: string): boolean {
  const 配置 = 粉丝等级配置[粉丝.等级];
  return 配置.解锁权限.includes(权限);
}

/**
 * 获取粉丝的下一个等级信息
 */
export function 获取下一等级信息(当前等级: 粉丝等级): { 等级: 粉丝等级; 所需金额: number } | null {
  const 等级顺序: 粉丝等级[] = ['潜水', '过客', '粉丝', '铁粉', '守护', '榜一', '皇帝'];
  const 当前索引 = 等级顺序.indexOf(当前等级);
  if (当前索引 >= 等级顺序.length - 1) return null;
  
  const 下一等级 = 等级顺序[当前索引 + 1];
  const 所需金额 = 粉丝等级配置[下一等级].累计打赏门槛 - 粉丝等级配置[当前等级].累计打赏门槛;
  return { 等级: 下一等级, 所需金额 };
}

/**
 * 计算升级到下一级还需要多少打赏
 */
export function 计算升级差距(粉丝: 粉丝核心状态): number {
  const 当前配置 = 粉丝等级配置[粉丝.等级];
  const 下一信息 = 获取下一等级信息(粉丝.等级);
  if (!下一信息) return 0;
  return 下一信息.所需金额 - (粉丝.累计打赏 - 当前配置.累计打赏门槛);
}

/**
 * 处理打赏并更新粉丝状态
 */
export function 处理打赏(粉丝: 粉丝核心状态, 打赏金额: number): {
  粉丝: 粉丝核心状态;
  升级了: boolean;
  旧等级: 粉丝等级;
  新等级: 粉丝等级;
} {
  const 旧等级 = 计算粉丝等级(粉丝.累计打赏);
  粉丝.累计打赏 += 打赏金额;
  粉丝.月打赏 += 打赏金额;
  const 新等级 = 计算粉丝等级(粉丝.累计打赏);
  const 升级了 = 旧等级 !== 新等级;

  // 更新等级相关状态
  if (升级了) {
    粉丝.等级 = 新等级;
    // 解锁私下互动权限（榜一及以上）
    if (新等级 === '榜一' || 新等级 === '皇帝') {
      粉丝.已解锁私下互动 = true;
    }
  }

  return { 粉丝, 升级了, 旧等级, 新等级 };
}
