/**
 * 检查是否解锁特定粉丝团权益
 */
export function 检查粉丝团权益(等级: number, 权益等级: number): boolean {
  return 等级 >= 权益等级;
}

/**
 * 获取已解锁的权益列表
 */
export function 获取已解锁权益(等级: number): string[] {
  const 权益: string[] = [];
  for (let i = 1; i <= 等级; i++) {
    if (粉丝团等级权益[i]) {
      权益.push(`${i}级：${粉丝团等级权益[i]}`);
    }
  }
  return 权益;
}
