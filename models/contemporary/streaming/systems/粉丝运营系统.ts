// ==================== 经验值计算 ====================

/**
 * 获取升级所需经验
 */
export function 获取升级经验(当前等级: number): number {
  return 粉丝团升级经验[当前等级 + 1]?.所需经验 || 0;
}

/**
 * 计算粉丝团经验获取（基于打赏）
 */
export function 计算粉丝团经验(打赏金额: number): number {
  // 每1元打赏 = 1点粉丝团经验
  return 打赏金额;
}

/**
 * 处理粉丝团升级
 */
export function 处理粉丝团升级(
  当前等级: number,
  获得经验: number
): { 升级了: boolean; 新等级: number; 剩余经验: number } {
  let 等级 = 当前等级;
  let 经验 = 获得经验;

  while (等级 < 10) {
    const 所需 = 获取升级经验(等级);
    if (经验 >= 所需) {
      经验 -= 所需;
      等级++;
    } else {
      break;
    }
  }

  return {
    升级了: 等级 > 当前等级,
    新等级: 等级,
    剩余经验: 经验,
  };
}

