/**
 * PK票数计算（礼物价值转票数）
 */
export function 礼物价值转票数(礼物价值: number): number {
  // 假设1元 = 1票
  return Math.floor(礼物价值);
}

/**
 * PK结果判定
 */
export function 判定PK结果(
  我方票数: number,
  对方票数: number
): '胜利' | '失败' | '平局' {
  if (我方票数 > 对方票数) return '胜利';
  if (我方票数 < 对方票数) return '失败';
  return '平局';
}

/**
 * 获取惩罚池（根据尺度）
 */
export function 获取惩罚池(尺度: 内容尺度): 惩罚池项[] {
  return 惩罚池[尺度] || [];
}

/**
 * 根据尺度获取随机惩罚
 */
export function 随机抽取惩罚(尺度: 内容尺度): 惩罚池项 | null {
  const 惩罚列表 = 获取惩罚池(尺度);
  if (惩罚列表.length === 0) return null;
  
  const 随机索引 = Math.floor(Math.random() * 惩罚列表.length);
  return 惩罚列表[随机索引];
}

/**
 * 粉丝投票选择惩罚
 */
export function 粉丝投票惩罚(
  惩罚选项: 惩罚池项[],
  票数统计: Record<string, number>
): 惩罚池项 | null {
  if (惩罚选项.length === 0) return null;
  if (惩罚选项.length === 1) return 惩罚选项[0];
  
  // 找出最高票
  let 最高票惩罚 = 惩罚选项[0];
  let 最高票数 = 0;
  
  for (const 惩罚 of 惩罚选项) {
    const 票数 = 票数统计[惩罚.描述] || 0;
    if (票数 > 最高票数) {
      最高票数 = 票数;
      最高票惩罚 = 惩罚;
    }
  }
  
  return 最高票惩罚;
}

/**
 * 检查惩罚是否在主播底线内
 */
export function 惩罚在底线内(
  惩罚: 惩罚池项,
  主播底线: 内容尺度
): boolean {
  const 尺度顺序: 内容尺度[] = ['纯净', '软色情', '中等', '大胆', '极限'];
  const 惩罚尺度Index = 尺度顺序.indexOf(惩罚.尺度);
  const 底线Index = 尺度顺序.indexOf(主播底线);
  
  return 惩罚尺度Index <= 底线Index;
}

/**
 * 过滤出主播能接受的惩罚
 */
export function 过滤可接受惩罚(
  惩罚选项: 惩罚池项[],
  主播底线: 内容尺度
): 惩罚池项[] {
  return 惩罚选项.filter(p => 惩罚在底线内(p, 主播底线));
}

/**
 * PK状态更新
 */
export function 更新PK状态(
  直播间状态: 直播间状态,
  我方票数: number,
  对方票数: number,
  惩罚内容: PK惩罚类型
): 直播间状态 {
  return {
    ...直播间状态,
    连麦状态: 'PK中',
    PK状态: {
      对手ID: 直播间状态.连麦对象ID || '',
      双方票数: [我方票数, 对方票数],
      惩罚内容,
      惩罚对象: 判定PK结果(我方票数, 对方票数) === '失败' ? '自己' : '对方',
    },
  };
}

/**
 * PK结束重置状态
 */
export function 重置PK状态(直播间状态: 直播间状态): 直播间状态 {
  return {
    ...直播间状态,
    连麦状态: '无',
    连麦对象ID: undefined,
    PK状态: undefined,
  };
}

/**
 * 计算PK对主播的影响
 */
export function 计算PK影响(
  主播状态: 主播核心状态,
  PK结果: '胜利' | '失败' | '平局',
  输了接受惩罚: boolean
): 主播核心状态 {
  let 新状态 = { ...主播状态 };
  
  if (PK结果 === '胜利') {
    // 胜利：自信增加，可能获得新粉丝
    新状态.自信心 = Math.min(100, 新状态.自信心 + 5);
    新状态.网络热度 = Math.min(100, 新状态.网络热度 + 3);
  } else if (PK结果 === '失败' && 输了接受惩罚) {
    // 失败接受惩罚：职业倦怠可能增加
    新状态.职业倦怠度 = Math.min(100, 新状态.职业倦怠度 + 3);
    // 但如果惩罚尺度较高，可能提高自信（觉得自己大胆）
    新状态.自信心 = Math.min(100, 新状态.自信心 + 2);
  }
  
  return 新状态;
}

