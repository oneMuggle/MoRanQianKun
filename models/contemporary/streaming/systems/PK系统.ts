// ==================== PK流程函数 ====================

/**
 * 获取给定尺度下可用的惩罚类型
 */
export function 获取可用惩罚(尺度: 内容尺度): PK惩罚类型[] {
  return 惩罚池配置[尺度] || [];
}

/**
 * 获取惩罚的详细描述
 */
export function 获取惩罚描述(惩罚类型: PK惩罚类型, 尺度: 内容尺度): string[] {
  return 惩罚内容描述[惩罚类型]?.[尺度] || [];
}

/**
 * 检查人设是否支持该尺度
 */
export function 人设支持尺度(人设: 主播人设, 尺度: 内容尺度): boolean {
  const 支持列表 = 人设尺度兼容矩阵[人设];
  return 支持列表.includes(尺度);
}

/**
 * 获取人设推荐的尺度
 */
export function 获取人设推荐尺度(人设: 主播人设): 内容尺度 {
  const 支持列表 = 人设尺度兼容矩阵[人设];
  // 返回中等偏上的尺度作为推荐
  if (支持列表.includes('中等')) return '中等';
  if (支持列表.includes('软色情')) return '软色情';
  return 支持列表[0] || '纯净';
}

/**
 * 根据尺度筛选惩罚池
 */
export function 筛选惩罚池(尺度: 内容尺度): { 惩罚类型: PK惩罚类型; 描述: string }[] {
  const 可用惩罚 = 获取可用惩罚(尺度);
  const 结果: { 惩罚类型: PK惩罚类型; 描述: string }[] = [];
  
  for (const 惩罚 of 可用惩罚) {
    const 描述列表 = 获取惩罚描述(惩罚, 尺度);
    for (const 描述 of 描述列表) {
      结果.push({ 惩罚类型: 惩罚, 描述 });
    }
  }
  
  return 结果;
}
