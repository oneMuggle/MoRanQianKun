/**
 * 检查主播是否完成最低要求
 */
export function 检查最低要求(
  主播状态: 主播核心状态,
  合同: MCN合同条款
): { 完成: boolean; 违规项: string[] } {
  const 违规项: string[] = [];
  
  if (主播状态.直播时长 < 合同.最低要求.每月直播时长) {
    违规项.push(`直播时长不足: ${主播状态.直播时长}/${合同.最低要求.每月开播天数}小时`);
  }
  
  return {
    完成: 违规项.length === 0,
    违规项,
  };
}

/**
 * 计算违约赔偿金
 */
export function 计算违约赔偿金(
  主播状态: 主播核心状态,
  合同: MCN合同条款
): number {
  // 基础违约金
  let 赔偿金 = 合同.违约金.提前解约;
  
  // 如果有未完成的最低要求，增加赔偿
  const { 完成, 违规项 } = 检查最低要求(主播状态, 合同);
  if (!完成) {
    赔偿金 += 违规项.length * 50000;
  }
  
  // 如果违反竞业禁止条款
  if (合同.违约金.违反竞业) {
    赔偿金 += 合同.违约金.违反竞业;
  }
  
  return 赔偿金;
}

/**
 * 检查内容是否在合同允许范围内
 */
export function 内容在合同允许范围(
  目标尺度: 内容尺度,
  合同: MCN合同条款
): boolean {
  const 尺度顺序: 内容尺度[] = ['纯净', '软色情', '中等', '大胆', '极限'];
  const 目标Index = 尺度顺序.indexOf(目标尺度);
  const 最低Index = 尺度顺序.indexOf(合同.内容限制.最低尺度);
  const 最高Index = 尺度顺序.indexOf(合同.内容限制.最高尺度);
  
  return 目标Index >= 最低Index && 目标Index <= 最高Index;
}

/**
 * 获取主播实际分成比例
 */
export function 获取实际分成比例(合同: MCN合同条款): number {
  return 合同.分成比例.主播;
}

/**
 * 计算公会收益
 */
export function 计算公会收益(总收入: number, 合同: MCN合同条款): number {
  return Math.floor(总收入 * 合同.分成比例.公会);
}

/**
 * 计算平台收益
 */
export function 计算平台收益(总收入: number, 合同: MCN合同条款): number {
  return Math.floor(总收入 * 合同.分成比例.平台);
}

/**
 * 创建公会状态
 */
export function 创建公会(
  公会名称: string,
  公会等级: '小公会' | '中等公会' | '大型公会' | '顶级公会' = '小公会'
): MCN公会状态 {
  const 资源配置 = {
    '小公会': { 流量扶持: 20, 运营支持: '基础' as const, 培训体系: false },
    '中等公会': { 流量扶持: 40, 运营支持: '专业' as const, 培训体系: true },
    '大型公会': { 流量扶持: 70, 运营支持: '专业' as const, 培训体系: true },
    '顶级公会': { 流量扶持: 90, 运营支持: '全方位' as const, 培训体系: true },
  };
  
  const config = 资源配置[公会等级];
  
  return {
    公会ID: `公会_${Date.now()}`,
    公会名称,
    公会等级,
    合同期限: 12,
    分成比例: 40,
    最低直播时长: 150,
    违约金条款: '按合同规定执行',
    竞业禁止条款: true,
    流量扶持: config.流量扶持,
    运营支持: config.运营支持,
    培训体系: config.培训体系,
    主播数量: 0,
    主播列表: [],
  };
}

/**
 * 主播签约公会
 */
export function 签约公会(
  主播状态: 主播核心状态,
  公会状态: MCN公会状态
): 主播核心状态 {
  return {
    ...主播状态,
    MCN合同状态: '合约期内',
    公会ID: 公会状态.公会ID,
  };
}

/**
 * 主播解约
 */
export function 申请解约(
  主播状态: 主播核心状态,
  合同: MCN合同条款
): { 主播状态: 主播核心状态; 违约金: number; 成功: boolean } {
  const 违约金 = 计算违约赔偿金(主播状态, 合同);
  
  // 检查是否能够支付违约金（简化判断：累计收入 > 违约金的50%）
  const 能支付 = 主播状态.累计收入 > 违约金 * 0.5;
  
  if (能支付) {
    return {
      主播状态: {
        ...主播状态,
        MCN合同状态: '自由身',
        公会ID: undefined,
        累计收入: 主播状态.累计收入 - 违约金,
      },
      违约金,
      成功: true,
    };
  }
  
  return {
    主播状态: {
      ...主播状态,
      MCN合同状态: '违约中',
    },
    违约金,
    成功: false,
  };
}

/**
 * 公会添加主播
 */
export function 公会添加主播(
  公会状态: MCN公会状态,
  主播ID: string
): MCN公会状态 {
  return {
    ...公会状态,
    主播数量: 公会状态.主播数量 + 1,
    主播列表: [...公会状态.主播列表, 主播ID],
  };
}

/**
 * 公会移除主播
 */
export function 公会移除主播(
  公会状态: MCN公会状态,
  主播ID: string
): MCN公会状态 {
  return {
    ...公会状态,
    主播数量: Math.max(0, 公会状态.主播数量 - 1),
    主播列表: 公会状态.主播列表.filter(id => id !== 主播ID),
  };
}
