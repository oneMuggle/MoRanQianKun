export interface MCN合同信息 {
  公会名称: string;
  合同期限: number;         // 月数
  分成比例: {
    平台: number;
    公会: number;
    主播: number;
  };
  最低直播时长: number;     // 每月小时数
  违约金: number;           // 万元
  内容限制: {
    禁止内容: string[];
    最低尺度: 内容尺度;
    最高尺度: 内容尺度;
  };
  竞业禁止: boolean;
  流量扶持: boolean;
  运营支持: '无' | '基础' | '专业' | '全方位';
}

/**
 * 生成随机合同条款
 */
export function 生成随机合同(公会等级: '小公会' | '中等公会' | '大型公会' | '顶级公会'): MCN合同信息 {
  const 分成Map = {
    '小公会': { 平台: 40, 公会: 30, 主播: 30 },
    '中等公会': { 平台: 35, 公会: 25, 主播: 40 },
    '大型公会': { 平台: 35, 公会: 20, 主播: 45 },
    '顶级公会': { 平台: 30, 公会: 20, 主播: 50 },
  };

  const 时长Map = {
    '小公会': 100,
    '中等公会': 120,
    '大型公会': 150,
    '顶级公会': 200,
  };

  const 扶持Map = {
    '小公会': 20,
    '中等公会': 40,
    '大型公会': 60,
    '顶级公会': 80,
  };

  const 支持Map: Record<string, '无' | '基础' | '专业' | '全方位'> = {
    '小公会': '无',
    '中等公会': '基础',
    '大型公会': '专业',
    '顶级公会': '全方位',
  };

  return {
    公会名称: `${['星火', '璀璨', '王者', '传奇'][Math.floor(Math.random() * 4)]}公会`,
    合同期限: [12, 24, 36][Math.floor(Math.random() * 3)],
    分成比例: 分成Map[公会等级],
    最低直播时长: 时长Map[公会等级],
    违约金: { '小公会': 10, '中等公会': 20, '大型公会': 30, '顶级公会': 50 }[公会等级],
    内容限制: {
      禁止内容: ['露出', '政治敏感', '违规话题'],
      最低尺度: '软色情',
      最高尺度: '大胆',
    },
    竞业禁止: 公会等级 === '大型公会' || 公会等级 === '顶级公会',
    流量扶持: 扶持Map[公会等级] > 0,
    运营支持: 支持Map[公会等级],
  };
}

/**
 * 计算主播实际收入
 */
export function 计算主播收入(
  总礼物收入: number,
  合同分成: { 平台: number; 公会: number; 主播: number }
): {
  平台抽成: number;
  公会抽成: number;
  主播实得: number;
  税前收入: number;
} {
  const 平台抽成 = Math.floor(总礼物收入 * (合同分成.平台 / 100));
  const 公会抽成 = Math.floor((总礼物收入 - 平台抽成) * (合同分成.公会 / 100));
  const 主播实得 = 总礼物收入 - 平台抽成 - 公会抽成;
  
  return {
    平台抽成,
    公会抽成,
    主播实得,
    税前收入: 主播实得,
  };
}

/**
 * 检查是否违约
 */
export function 检查违约(
  主播数据: { 直播时长: number; 内容尺度: 内容尺度; MCN合同状态: MCN合同状态 },
  合同要求: { 最低直播时长: number; 内容限制: { 最低尺度: 内容尺度; 最高尺度: 内容尺度 } }
): { 违约: boolean; 原因: string[] } {
  const 原因: string[] = [];

  if (主播数据.直播时长 < 合同要求.最低直播时长) {
    原因.push(`直播时长不足：${主播数据.直播时长}/${合同要求.最低直播时长}小时`);
  }

  const 尺度等级 = ['纯净', '软色情', '中等', '大胆', '极限'];
  const 当前索引 = 尺度等级.indexOf(主播数据.内容尺度);
  const 最低索引 = 尺度等级.indexOf(合同要求.内容限制.最低尺度);
  const 最高索引 = 尺度等级.indexOf(合同要求.内容限制.最高尺度);

  if (当前索引 < 最低索引 || 当前索引 > 最高索引) {
    原因.push(`内容尺度超出范围：${主播数据.内容尺度}，要求：${合同要求.内容限制.最低尺度}-${合同要求.内容限制.最高尺度}`);
  }

  return {
    违约: 原因.length > 0,
    原因,
  };
}

/**
 * 计算违约赔偿金
 */
export function 计算违约赔偿金(违约金条款: string, 已履行月数: number, 合同总月数: number): number {
  // 解析违约金条款，假设格式为 "提前解约需赔偿X万元"
  const 匹配 = 违约金条款.match(/(\d+)/);
  if (!匹配) return 0;
  
  const 总违约金 = parseInt(匹配[1]) * 10000;
  const 未履行比例 = (合同总月数 - 已履行月数) / 合同总月数;
  
  return Math.floor(总违约金 * 未履行比例);
}
