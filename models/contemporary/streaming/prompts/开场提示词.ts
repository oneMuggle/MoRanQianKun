/**
 * 私信互动提示词
 */

import type { 粉丝关系, 粉丝等级 } from '../types';

/**
 * 生成私信互动提示词
 */
export function 生成私信提示词(params: {
  主播名: string;
  粉丝名: string;
  关系阶段: 粉丝关系;
  粉丝等级: 粉丝等级;
  累计打赏: number;
  紧密度: number;
}): string {
  const { 主播名, 粉丝名, 关系阶段, 粉丝等级, 累计打赏, 紧密度 } = params;

  return `【系统角色】
你是主播${主播名}，正在与粉丝${粉丝名}私信交流。

【关系阶段】
你们的关系目前处于：${关系阶段}
- ${获取关系描述(关系阶段)}

【粉丝信息】
- 等级：${粉丝等级}
- 累计打赏：${累计打赏}元
- 关系紧密度：${紧密度}/100

【交流要点】
- 保持粉丝粘性
- 适当暧昧但不过分
- 引导粉丝继续支持
- 维护"偶像"与"粉丝"边界
${关系阶段 === '暧昧' || 关系阶段 === '恋人' || 关系阶段 === '金主' ? '- 关系较深，可以适当增加暧昧内容\n- 注意保护个人隐私\n- 避免过于直接的表达' : ''}

【私信场景】
请生成一段私信聊天的内容，包括：
1. 主播的开场消息
2. 粉丝的回应
3. 话题的走向
4. 暧昧升级的可能性

请用私聊的语气来呈现，保持人设一致性。`;
}

/**
 * 获取关系描述
 */
function 获取关系描述(关系: 粉丝关系): string {
  const 描述表: Record<粉丝关系, string> = {
    '陌生': '只限于直播间的互动，没有私下交流过',
    '熟悉': '有偶尔的私下聊天，对对方有一定了解',
    '暧昧': '超出普通粉丝关系，有情感上的暧昧',
    '恋人': '已经在线下确定关系',
    '金主': '持续大额打赏的暧昧关系，有一定利益交换',
  };

  return 描述表[关系] || '';
}

/**
 * 生成榜一大哥私信提示词
 */
export function 生成榜一大哥私信提示词(params: {
  主播名: string;
  粉丝名: string;
  连续月数: number;
}): string {
  const { 主播名, 粉丝名, 连续月数 } = params;

  return `【特殊场景】
榜一大哥专属私信

【主播名】${主播名}
【粉丝名】${粉丝名}
【榜一时间】连续${连续月数}个月

【榜一特权】
- 每月可提出一次"暧昧请求"（尺度由主播设定上限）
- 可与主播私下视频通话
- 生日/节日可获得"专属内容"
- ${连续月数 >= 3 ? '连续三个月榜一，获得"永久守护"称号' : `${3 - 连续月数}个月后可获得"永久守护"称号`}

【私信氛围】
榜一大哥发来消息，气氛变得微妙...
主播的回复既要注意维护关系，又要保持一定的矜持

【可能的话题】
- 粉丝提出"暧昧请求"
- 主播分享近期的"私密"内容
- 讨论线下见面的事宜
- 粉丝表达更深的感情

请生成一段榜一大哥私信的场景。`;
}

