/**
 * 主播核心状态
 */

import type {
  主播人设,
  主播状态 as 主播状态类型,
  内容尺度,
  MCN合同状态,
  直播平台,
  直播状态,
  舆论状态,
} from '../types';
import type { 危机事件 } from './危机事件';

export interface 主播核心状态 {
  // 基础信息
  主播ID: string;
  昵称: string;
  真实姓名?: string;        // 隐藏信息
  人设: 主播人设;
  性别: '男' | '女';

  // 平台信息
  平台: 直播平台;
  主播状态: 主播状态类型;
  内容尺度: 内容尺度;

  // MCN 合同
  MCN合同状态: MCN合同状态;
  公会ID?: string;
  合同到期日?: string;
  违约金: number;           // 如果违约需要赔偿

  // 数据指标
  粉丝数量: number;
  关注数量: number;
  直播时长: number;         // 累计直播小时数
  作品数量: number;         // 短视频数量
  网络热度: number;         // 0-100，影响事件触发

  // 收入相关
  月收入: number;
  累计收入: number;
  税前收入: number;

  // 关系网络
  榜一大哥ID?: string;      // 当前榜一粉丝ID
  暧昧对象列表: string[];   // 有暧昧关系的粉丝ID列表
  黑名单: string[];         // 拉黑的粉丝ID

  // 直播状态
  当前直播状态: 直播状态;
  当前观众数: number;
  当前收入: number;        // 本场直播收入

  // 危机与舆论
  当前舆论状态: 舆论状态;
  危机事件列表: 危机事件[];
  历史危机次数: number;

  // 心理状态
  职业倦怠度: number;       // 0-100，过高影响直播质量
  自信心: number;          // 0-100，影响内容尺度
  匿名羞耻度: number;       // 0-100，对NSFW内容的羞耻感

  // 秘密与底线
  已泄露秘密: string[];    // 已经暴露的秘密列表
  内容底线: 内容尺度;       // 主播给自己设定的底线
  红线条款: string[];      // 绝对不能做的事情
}

/**
 * 创建默认主播状态
 */
export function 创建默认主播状态(主播ID: string, 昵称: string, 人设: 主播人设, 性别: '男' | '女'): 主播核心状态 {
  return {
    主播ID,
    昵称,
    人设,
    性别,
    平台: '抖音',
    主播状态: '路人',
    内容尺度: '纯净',
    MCN合同状态: '未签约',
    违约金: 0,
    粉丝数量: 0,
    关注数量: 0,
    直播时长: 0,
    作品数量: 0,
    网络热度: 0,
    月收入: 0,
    累计收入: 0,
    税前收入: 0,
    暧昧对象列表: [],
    黑名单: [],
    当前直播状态: '下播',
    当前观众数: 0,
    当前收入: 0,
    当前舆论状态: '无危机',
    危机事件列表: [],
    历史危机次数: 0,
    职业倦怠度: 0,
    自信心: 50,
    匿名羞耻度: 50,
    已泄露秘密: [],
    内容底线: '纯净',
    红线条款: [],
  };
}
