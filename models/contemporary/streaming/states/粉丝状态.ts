/**
 * 粉丝核心状态
 */

import type { 粉丝等级, 粉丝关系, 粉丝身份 } from '../types';

export interface 粉丝核心状态 {
  // 基础信息
  粉丝ID: string;
  昵称: string;
  性别: '男' | '女';
  年龄: number;

  // 打赏数据
  累计打赏: number;
  月打赏: number;
  等级: 粉丝等级;

  // 与主播关系
  已绑定主播ID?: string;    // 守护的主播
  关系: 粉丝关系;
  私下互动次数: number;     // 线下见面、私下聊天次数

  // 权限
  已解锁私下互动: boolean;  // 达到榜一后解锁
  粉丝团职位: '普通粉丝' | '粉丝团长' | '管理组';
  专属徽章?: string;        // 守护专属徽章

  // 行为记录
  骚扰记录: number;         // 被举报次数
  恶意行为: string[];       // 具体恶意行为
  异常发言: string[];        // 被过滤的敏感发言

  // 心理画像
  消费能力: number;         // 0-100，预估可消费金额
  粘性: number;              // 0-100，对主播的忠诚度
  攻击性: number;            // 0-100，对其他粉丝的攻击性
}

/**
 * 创建默认粉丝状态
 */
export function 创建默认粉丝状态(粉丝ID: string, 昵称: string): 粉丝核心状态 {
  return {
    粉丝ID,
    昵称,
    性别: '男',
    年龄: 25,
    累计打赏: 0,
    月打赏: 0,
    等级: '潜水',
    关系: '陌生',
    私下互动次数: 0,
    已解锁私下互动: false,
    粉丝团职位: '普通粉丝',
    骚扰记录: 0,
    恶意行为: [],
    异常发言: [],
    消费能力: 50,
    粘性: 0,
    攻击性: 0,
  };
}

/**
 * 根据累计打赏计算粉丝等级
 */
export function 计算粉丝等级(累计打赏: number): 粉丝等级 {
  if (累计打赏 >= 50000) return '皇帝';
  if (累计打赏 >= 10000) return '榜一';
  if (累计打赏 >= 5000) return '守护';
  if (累计打赏 >= 1000) return '铁粉';
  if (累计打赏 >= 100) return '粉丝';
  if (累计打赏 >= 10) return '过客';
  return '潜水';
}
