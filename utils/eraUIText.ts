type 监听器 = (文案: 时代主题UI文案) => void;
const 监听器列表: Set<监听器> = new Set();

export const 获取UI文案 = (): 时代主题UI文案 => ({ ...当前文案 });

export const 设置时代UI文案 = (方案: 时代主题方案) => {
    当前文案 = { ...当前文案, ...方案.界面文案 };
    通知变更();
};

export const 订阅UI文案变更 = (监听器: 监听器) => {
    监听器列表.add(监听器);
    return () => { 监听器列表.delete(监听器); };
};

const 通知变更 = () => {
    const 快照 = { ...当前文案 };
    监听器列表.forEach((fn) => { fn(快照); });
};
