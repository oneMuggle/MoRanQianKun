/**
 * 根据ID查询妖象功法
 */
export function getYaoxiangById(id: string): 妖象功法 | undefined {
  return 妖象功法列表.find(f => f.id === id);
}

/**
 * 根据门派筛选功法
 */
export function getYaoxiangBySect(门派: 妖象功法门派): 妖象功法[] {
  return 妖象功法列表.filter(f => f.门派 === 门派);
}

/**
 * 根据品级筛选功法
 */
export function getYaoxiangByGrade(品级: 妖象功法品级): 妖象功法[] {
  return 妖象功法列表.filter(f => f.品级 === 品级);
}
