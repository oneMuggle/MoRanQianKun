import type { 气运属性类型, 气运效果类型 } from './types';

export { 气运属性类型, 气运效果类型 };

export type 气运类别 =
    | '真·气运'
    | '限制版气运'
    | '因果律'
    | '天道规则'
    | '绝对无敌'
    | '脑洞破防'
    | '法则扭曲'
    | '白嫖躺赢'
    | '怠惰降维'
    | '精神暴击'
    | '合欢秘辛';

export type 气运稀有度 = '传说' | '稀有' | '普通';

export interface 气运数据 {
    名称: string;
    类别: 气运类别;
    描述: string;
    效果: Array<{
        类型: 气运效果类型;
        属性?: 气运属性类型;
        修正值?: number;
        描述?: string;
    }>;
    稀有度: 气运稀有度;
    代价?: string;
    nsfw等级?: 0 | 1 | 2;
    能力类型?: '战斗' | '生存' | '社交' | '谋略' | '特殊' | '辅助';
    适用境界?: [number, number];
    /** 适配的时代背景大类，不填则全时代可见 */
    时代适配?: string[];
    /** 适配的具体子纪元 ID，优先级高于时代适配，不填则回退到大类匹配 */
    子纪元适配?: string[];
}
