import type { 叙事块, 验证结果, 验证错误 } from '../../../models/narrativeGrammar';

export function 验证叙事格式(叙事块: 叙事块): 验证结果 {
  const 错误列表: 验证错误[] = [];
  if (叙事块.正文.length === 0) {
    错误列表.push({ 行号: 0, 原始行: '', 错误类型: '缺少必填字段', 错误信息: '正文内容为空' });
  }
  叙事块.正文.forEach((行, 索引) => {
    if (行.类型 === '旁白' && !行.内容.trim()) {
      错误列表.push({ 行号: 索引 + 1, 原始行: 行.原始行, 错误类型: '缺少必填字段', 错误信息: '旁白内容为空' });
    } else if (行.类型 === '角色台词') {
      if (!行.角色名.trim()) 错误列表.push({ 行号: 索引 + 1, 原始行: 行.原始行, 错误类型: '缺少必填字段', 错误信息: '角色名为空' });
      if (!行.内容.trim()) 错误列表.push({ 行号: 索引 + 1, 原始行: 行.原始行, 错误类型: '缺少必填字段', 错误信息: '台词内容为空' });
    } else if (行.类型 === '判定') {
      if (!行.判定类型) 错误列表.push({ 行号: 索引 + 1, 原始行: 行.原始行, 错误类型: '缺少必填字段', 错误信息: '判定类型缺失' });
      if (isNaN(行.判定值) || isNaN(行.难度)) 错误列表.push({ 行号: 索引 + 1, 原始行: 行.原始行, 错误类型: '值类型错误', 错误信息: '判定值或难度不是有效数字' });
      if (!行.结果) 错误列表.push({ 行号: 索引 + 1, 原始行: 行.原始行, 错误类型: '缺少必填字段', 错误信息: '判定结果缺失' });
    }
  });
  return { 有效: 错误列表.length === 0, 错误列表 };
}

export function 验证标签结构(文本: string): 验证结果 {
  const 错误列表: 验证错误[] = [];
  if (!/<正文>[\s\S]*<\/正文>/.test(文本)) 错误列表.push({ 行号: 0, 原始行: '', 错误类型: '标签错误', 错误信息: '缺少必填标签 <正文>' });
  if (!/<短期记忆>[\s\S]*<\/短期记忆>/.test(文本)) 错误列表.push({ 行号: 0, 原始行: '', 错误类型: '标签错误', 错误信息: '缺少必填标签 <短期记忆>' });
  const 标签对 = [['<正文>', '</正文>'], ['<变量规划>', '</变量规划>'], ['<剧情规划>', '</剧情规划>'], ['<短期记忆>', '</短期记忆>'], ['<thinking>', '</thinking>'], ['<judge>', '</judge>'], ['<行动选项>', '</行动选项>'], ['<disclaimer>', '</disclaimer>']];
  标签对.forEach(([开始, 结束]) => {
    const 开始数 = (文本.match(new RegExp(开始, 'g')) || []).length;
    const 结束数 = (文本.match(new RegExp(结束, 'g')) || []).length;
    if (开始数 !== 结束数) 错误列表.push({ 行号: 0, 原始行: '', 错误类型: '标签错误', 错误信息: `标签 ${开始} 和 ${结束} 数量不匹配` });
  });
  ['<正文>', '<变量规划>', '<剧情规划>', '<短期记忆>', '<thinking>'].forEach(标签 => {
    const 计数 = (文本.match(new RegExp(标签, 'g')) || []).length;
    if (计数 > 1) 错误列表.push({ 行号: 0, 原始行: '', 错误类型: '标签错误', 错误信息: `顶层标签 ${标签} 出现多次（最多1次）` });
  });
  return { 有效: 错误列表.length === 0, 错误列表 };
}
