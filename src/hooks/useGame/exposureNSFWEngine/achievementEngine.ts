/** 获取所有预定义成就 */
export function 获取所有成就(): 露出成就[] {
  return 预定义成就.map(a => ({ ...a, 已达成: false }));
}

/** 检查并返回新达成的成就 */
export function 检查成就(上下文: 成就检查上下文, 已有成就: 露出成就[]): 新达成成就[] {
  const 新成就: 新达成成就[] = [];

  for (const 成就模板 of 预定义成就) {
    const 已有 = 已有成就.find(a => a.id === 成就模板.id && a.已达成);
    if (已有) continue;

    let 已达成 = false;
    let 原因 = '';

    switch (成就模板.条件) {
      case '首次露出':
        已达成 = 上下文.是否首次;
        原因 = '完成了第一次露出';
        break;
      case '首次成功':
        已达成 = 上下文.是否首次成功;
        原因 = '第一次成功完成露出';
        break;
      case '首次失败':
        已达成 = 上下文.是否首次失败;
        原因 = '第一次露出失败';
        break;
      case '等级达到':
        已达成 = 上下文.当前露出等级 >= 成就模板.条件值;
        原因 = `露出等级达到 ${成就模板.条件值}`;
        break;
      case '紧张度超过':
        已达成 = 上下文.当前紧张度 >= 成就模板.条件值;
        原因 = `紧张度达到 ${上下文.当前紧张度}`;
        break;
      case '旁观者数量':
        已达成 = 上下文.旁观者数量 >= 成就模板.条件值;
        原因 = `有 ${上下文.旁观者数量} 个旁观者在场`;
        break;
      case '网络流言等级':
        已达成 = 上下文.网络流言等级 >= 成就模板.条件值;
        原因 = `网络流言等级达到 ${上下文.网络流言等级}`;
        break;
      case '社会性死亡':
        已达成 = 上下文.是否社会性死亡;
        原因 = '名誉扫地，社会性死亡';
        break;
      case '完美脱身':
        已达成 = 上下文.是否完美脱身;
        原因 = '在高紧张度下全身而退';
        break;
      case '连续成功':
        已达成 = 上下文.连续成功次数 >= 成就模板.条件值;
        原因 = `连续 ${成就模板.条件值} 次成功`;
        break;
    }

    if (已达成) {
      新成就.push({
        成就: { ...成就模板, 已达成: true, 达成时间: new Date().toISOString() },
        原因,
      });
    }
  }

  return 新成就;
}

/** 获取指定分类的成就 */
export function 获取分类成就(分类: 露出成就分类, 已有成就: 露出成就[]): 露出成就[] {
  return 已有成就.filter(a => a.分类 === 分类);
}

/** 计算成就完成度 */
export function 计算成就完成度(已有成就: 露出成就[]): { 总数: number; 已达成: number; 百分比: number } {
  const 总数 = 已有成就.length;
  const 已达成 = 已有成就.filter(a => a.已达成).length;
  return { 总数, 已达成, 百分比: 总数 > 0 ? Math.round((已达成 / 总数) * 100) : 0 };
}
