/**
 * eventTrigger.test.ts
 * 事件触发器单元测试
 */

import {
    检查到期事件,
    计算触发回合,
    构建事件注入提示词,
    解析事件更新信号,
    计算事件新状态,
    批量更新事件状态,
    创建回合偏移事件,
    创建绝对回合事件,
    创建条件事件,
    获取事件描述,
    求值增强条件,
    检查周期性触发,
    获取下一触发回合,
    查找链式触发事件,
    清理已过期事件,
    处理事件组互斥,
    获取分组待触发事件,
    更新周期触发计数,
    检查事件过期,
} from './eventTrigger';
import type { 游戏事件, 增强条件 } from '../../models/eventTrigger';

describe('eventTrigger', () => {
    // ==================== 检查到期事件 ====================

    describe('检查到期事件', () => {
        it('应返回空数组当事件列表为空', () => {
            expect(检查到期事件([], 10)).toEqual([]);
            expect(检查到期事件(undefined, 10)).toEqual([]);
        });

        it('应过滤掉非待触发状态的事件', () => {
            const 事件列表: 游戏事件[] = [
                {
                    id: '1',
                    类型: '预约',
                    名称: '测试事件',
                    触发条件: { kind: '回合偏移', 偏移量: 5 },
                    事件数据: {},
                    状态: '已触发',
                    创建回合: 10,
                },
            ];
            expect(检查到期事件(事件列表, 20)).toEqual([]);
        });

        it('应返回已到期的回合偏移事件', () => {
            const 事件列表: 游戏事件[] = [
                {
                    id: '1',
                    类型: '预约',
                    名称: '测试事件',
                    触发条件: { kind: '回合偏移', 偏移量: 5 },
                    事件数据: {},
                    状态: '待触发',
                    创建回合: 10,
                },
            ];
            expect(检查到期事件(事件列表, 15)).toHaveLength(1);
            expect(检查到期事件(事件列表, 16)).toHaveLength(1);
        });

        it('不应返回未到期的回合偏移事件', () => {
            const 事件列表: 游戏事件[] = [
                {
                    id: '1',
                    类型: '预约',
                    名称: '测试事件',
                    触发条件: { kind: '回合偏移', 偏移量: 10 },
                    事件数据: {},
                    状态: '待触发',
                    创建回合: 10,
                },
            ];
            expect(检查到期事件(事件列表, 15)).toEqual([]);
        });

        it('应返回已到期的绝对回合事件', () => {
            const 事件列表: 游戏事件[] = [
                {
                    id: '1',
                    类型: '定时',
                    名称: '定时事件',
                    触发条件: { kind: '回合绝对', 目标回合: 20 },
                    事件数据: {},
                    状态: '待触发',
                    创建回合: 5,
                },
            ];
            expect(检查到期事件(事件列表, 20)).toHaveLength(1);
            expect(检查到期事件(事件列表, 21)).toHaveLength(1);
        });

        it('应按优先级排序', () => {
            const 事件列表: 游戏事件[] = [
                {
                    id: '1',
                    类型: '预约',
                    名称: '低优先级',
                    触发条件: { kind: '回合偏移', 偏移量: 5 },
                    事件数据: {},
                    状态: '待触发',
                    创建回合: 10,
                    优先级: 1,
                },
                {
                    id: '2',
                    类型: '预约',
                    名称: '高优先级',
                    触发条件: { kind: '回合偏移', 偏移量: 5 },
                    事件数据: {},
                    状态: '待触发',
                    创建回合: 10,
                    优先级: 10,
                },
            ];
            const 结果 = 检查到期事件(事件列表, 15);
            expect(结果[0].名称).toBe('高优先级');
        });

        it('应过滤已过期事件', () => {
            const 事件列表: 游戏事件[] = [
                {
                    id: '1',
                    类型: '预约',
                    名称: '过期事件',
                    触发条件: { kind: '回合偏移', 偏移量: 5 },
                    事件数据: {},
                    状态: '待触发',
                    创建回合: 10,
                    过期回合: 18,
                },
            ];
            expect(检查到期事件(事件列表, 19)).toEqual([]);
            expect(检查到期事件(事件列表, 17)).toHaveLength(1);
        });
    });

    // ==================== 计算触发回合 ====================

    describe('计算触发回合', () => {
        it('应正确计算回合偏移', () => {
            const 事件: 游戏事件 = {
                id: '1',
                类型: '预约',
                名称: '测试',
                触发条件: { kind: '回合偏移', 偏移量: 10 },
                事件数据: {},
                状态: '待触发',
                创建回合: 5,
            };
            expect(计算触发回合(事件)).toBe(15);
        });

        it('应正确返回绝对回合', () => {
            const 事件: 游戏事件 = {
                id: '1',
                类型: '定时',
                名称: '测试',
                触发条件: { kind: '回合绝对', 目标回合: 100 },
                事件数据: {},
                状态: '待触发',
                创建回合: 5,
            };
            expect(计算触发回合(事件)).toBe(100);
        });

        it('条件表达式应返回 null', () => {
            const 事件: 游戏事件 = {
                id: '1',
                类型: '条件',
                名称: '测试',
                触发条件: { kind: '条件表达式', 表达式: '服从度 > 50' },
                事件数据: {},
                状态: '待触发',
                创建回合: 5,
            };
            expect(计算触发回合(事件)).toBeNull();
        });
    });

    // ==================== 构建事件注入提示词 ====================

    describe('构建事件注入提示词', () => {
        it('应生成包含事件名称的提示词', () => {
            const 事件: 游戏事件 = {
                id: 'test-1',
                类型: '预约',
                名称: '见面预约',
                描述: '与 NPC 见面',
                触发条件: { kind: '回合偏移', 偏移量: 5 },
                事件数据: { npcId: 'npc-1', npcName: '小红' },
                状态: '待触发',
                创建回合: 10,
            };
            const 提示词 = 构建事件注入提示词(事件);
            expect(提示词).toContain('见面预约');
            expect(提示词).toContain('事件触发');
            expect(提示词).toContain('触发回合: 15');
        });

        it('应包含事件数据', () => {
            const 事件: 游戏事件 = {
                id: 'test-1',
                类型: '预约',
                名称: '测试事件',
                触发条件: { kind: '回合绝对', 目标回合: 50 },
                事件数据: { key: 'value' },
                状态: '待触发',
                创建回合: 0,
            };
            const 提示词 = 构建事件注入提示词(事件);
            expect(提示词).toContain('key');
            expect(提示词).toContain('value');
        });

        it('应包含事件更新标签', () => {
            const 事件: 游戏事件 = {
                id: 'test-123',
                类型: '预约',
                名称: '测试',
                触发条件: { kind: '回合偏移', 偏移量: 5 },
                事件数据: {},
                状态: '待触发',
                创建回合: 0,
            };
            const 提示词 = 构建事件注入提示词(事件);
            expect(提示词).toContain('<事件更新>');
            expect(提示词).toContain('test-123');
            expect(提示词).toContain('</事件更新>');
        });
    });

    // ==================== 解析事件更新信号 ====================

    describe('解析事件更新信号', () => {
        it('应正确解析有效的事件更新标签', () => {
            const 响应 = `一些剧情内容...
<事件更新>
{"id": "event-1", "新状态": "已触发"}
</事件更新>
更多内容`;
            const 结果 = 解析事件更新信号(响应);
            expect(结果).not.toBeNull();
            expect(结果?.事件ID).toBe('event-1');
            expect(结果?.新状态).toBe('已触发');
        });

        it('应返回 null 当无事件更新标签', () => {
            expect(解析事件更新信号('没有标签的内容')).toBeNull();
        });

        it('应处理额外数据', () => {
            const 响应 = `<事件更新>
{"id": "e1", "新状态": "已完成", "额外数据": {"score": 100}}
</事件更新>`;
            const 结果 = 解析事件更新信号(响应);
            expect(结果?.额外数据).toEqual({ score: 100 });
        });
    });

    // ==================== 事件状态更新 ====================

    describe('计算事件新状态', () => {
        it('应正确更新状态', () => {
            const 原始事件: 游戏事件 = {
                id: '1',
                类型: '预约',
                名称: '测试',
                触发条件: { kind: '回合偏移', 偏移量: 5 },
                事件数据: { a: 1 },
                状态: '待触发',
                创建回合: 10,
            };
            const 更新后 = 计算事件新状态(原始事件, '已触发');
            expect(更新后.状态).toBe('已触发');
            expect(更新后.触发回合).toBe(15);
        });

        it('应合并额外数据', () => {
            const 原始事件: 游戏事件 = {
                id: '1',
                类型: '预约',
                名称: '测试',
                触发条件: { kind: '回合偏移', 偏移量: 5 },
                事件数据: { a: 1 },
                状态: '待触发',
                创建回合: 10,
            };
            const 更新后 = 计算事件新状态(原始事件, '已触发', { b: 2 });
            expect(更新后.事件数据).toEqual({ a: 1, b: 2 });
        });
    });

    describe('批量更新事件状态', () => {
        it('应批量更新指定事件', () => {
            const 事件列表: 游戏事件[] = [
                {
                    id: '1',
                    类型: '预约',
                    名称: '事件1',
                    触发条件: { kind: '回合偏移', 偏移量: 5 },
                    事件数据: {},
                    状态: '待触发',
                    创建回合: 0,
                },
                {
                    id: '2',
                    类型: '预约',
                    名称: '事件2',
                    触发条件: { kind: '回合偏移', 偏移量: 10 },
                    事件数据: {},
                    状态: '待触发',
                    创建回合: 0,
                },
            ];
            const 更新映射 = { '1': '已触发' as const };
            const 结果 = 批量更新事件状态(事件列表, 更新映射);
            expect(结果[0].状态).toBe('已触发');
            expect(结果[1].状态).toBe('待触发');
        });
    });

    // ==================== 事件创建辅助 ====================

    describe('创建事件辅助函数', () => {
        it('创建回合偏移事件应正确设置字段', () => {
            const 事件 = 创建回合偏移事件('e1', '测试', 5, { npcId: 'n1' }, {
                描述: '测试描述',
                优先级: 3,
            });
            expect(事件.id).toBe('e1');
            expect(事件.名称).toBe('测试');
            expect(事件.类型).toBe('预约');
            expect(事件.触发条件).toEqual({ kind: '回合偏移', 偏移量: 5 });
            expect(事件.事件数据).toEqual({ npcId: 'n1' });
            expect(事件.描述).toBe('测试描述');
            expect(事件.优先级).toBe(3);
        });

        it('创建绝对回合事件应正确设置字段', () => {
            const 事件 = 创建绝对回合事件('e2', '定时', 100);
            expect(事件.类型).toBe('定时');
            expect(事件.触发条件).toEqual({ kind: '回合绝对', 目标回合: 100 });
        });

        it('创建条件事件应正确设置字段', () => {
            const 事件 = 创建条件事件('e3', '条件', '服从度 > 80');
            expect(事件.类型).toBe('条件');
            expect(事件.触发条件).toEqual({ kind: '条件表达式', 表达式: '服从度 > 80' });
        });
    });

    // ==================== 获取事件描述 ====================

    describe('获取事件描述', () => {
        it('应生成回合偏移事件描述', () => {
            const 事件: 游戏事件 = {
                id: '1',
                类型: '预约',
                名称: '见面',
                触发条件: { kind: '回合偏移', 偏移量: 5 },
                事件数据: {},
                状态: '待触发',
                创建回合: 10,
            };
            const 描述 = 获取事件描述(事件);
            expect(描述).toContain('见面');
            expect(描述).toContain('10 + 5 = 15');
        });

        it('应为非待触发事件添加状态标记', () => {
            const 事件: 游戏事件 = {
                id: '1',
                类型: '预约',
                名称: '已结束',
                触发条件: { kind: '回合偏移', 偏移量: 5 },
                事件数据: {},
                状态: '已触发',
                创建回合: 10,
            };
            const 描述 = 获取事件描述(事件);
            expect(描述).toContain('[已触发]');
        });
    });
});
