/**
 * 校园纪元 NPC 关系系统 — 与 NSFW 引擎的集成
 * 处理关系变化对欲望档案、BDSM 系统的影响
 */

import type { NPC关系数据 } from '../../../models/campusNSFW';
import type { NPC欲望档案 } from '../../../models/campusNSFW';

/**
 * 处理关系变化对欲望档案的影响
 * 当 NPC 关系升级时，同步更新欲望档案中的关系阶段
 */
export function 处理关系影响(
  欲望档案: NPC欲望档案,
  关系数据: NPC关系数据
): NPC欲望档案 {
  const 更新后 = { ...欲望档案 };

  // 关系亲密阶段推进时，同步欲望档案中的关系阶段
  const 关系阶段映射: Record<string, string> = {
    '陌生': '初识',
    '相识': '相识',
    '好感': '好感',
    '亲密': '亲密',
    '恋人': '恋人',
    '挚友': '恋人',
  };

  const 欲望关系阶段 = 关系阶段映射[关系数据.关系类型];
  if (欲望关系阶段 && (更新后 as any).关系阶段 !== 欲望关系阶段) {
    (更新后 as any).关系阶段 = 欲望关系阶段;
  }

  // 高亲密度关系可能影响暴露风险阈值（关系越深，秘密越容易暴露）
  if (关系数据.亲密度 >= 50 && (更新后 as any).暴露风险阈值) {
    // 亲密关系下适当降低暴露风险容忍度（更容易紧张）
    (更新后 as any).暴露风险阈值 = Math.max(1, (更新后 as any).暴露风险阈值 - 5);
  }

  return 更新后;
}

/**
 * 处理关系冲突对欲望档案的影响
 * 关系倒退时，欲望阶段可能受到影响
 */
export function 处理关系冲突影响(
  欲望档案: NPC欲望档案,
  变化值: { 信任度: number; 好感度: number }
): NPC欲望档案 {
  const 更新后 = { ...欲望档案 };

  // 信任度大幅下降时，可能影响 NPC 的开放程度
  if (变化值.信任度 <= -10) {
    // 信任破裂可能导致 NPC 欲望阶段回退
    const 欲望阶段列表 = ['初识', '相识', '好感', '亲密', '恋人'];
    const 当前索引 = 欲望阶段列表.indexOf((更新后 as any).关系阶段 || '初识');
    if (当前索引 > 0) {
      // 回退一个阶段
      (更新后 as any).关系阶段 = 欲望阶段列表[当前索引 - 1];
    }
  }

  return 更新后;
}

/**
 * 检查关系是否满足 BDSM 系统解锁条件
 * 关系达到"亲密"阶段后可建立 BDSM 关系
 */
export function 检查BDSM解锁条件(关系数据: NPC关系数据): boolean {
  return (
    关系数据.亲密度 >= 50 &&
    关系数据.信任度 >= 40 &&
    (关系数据.关系类型 === '亲密' || 关系数据.关系类型 === '恋人')
  );
}
