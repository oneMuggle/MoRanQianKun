import type { NPC结构 } from '../../../models/social';
import type { NPC欲望档案, 亲密里程碑, 后果记录, 里程碑类型 } from '../../../models/campusNSFW';
import type { 校园亲密互动类型 } from '../../../models/intimacy';
import { 计算欲望阶段推进, 检查欲望阶段升级, 计算互动冷却, 计算暴露风险, 判定后果, 判定关系轨道, 生成里程碑 } from './desireStateMachine';
import { 计算露出偏好推进 } from './exposureSystem';

export function 处理NSFW互动(参数: {
  npc: NPC结构; 欲望档案: NPC欲望档案; 互动类型: 校园亲密互动类型;
  地点暴露风险: string; 时间段: string;
  选择倾向: '保守' | '中性' | '激进' | '冒险暴露' | '安全隐蔽';
  周围人数: number; 多角NPC数: number; 里程碑列表: 亲密里程碑[];
}): { 更新后档案: NPC欲望档案; 后果?: 后果记录; 新里程碑?: 亲密里程碑; 暴露风险值: number; 应升级阶段: boolean } {
  const { npc, 欲望档案, 互动类型, 地点暴露风险, 时间段, 选择倾向, 多角NPC数, 里程碑列表 } = 参数;
  const 新阶段进度 = 计算欲望阶段推进(欲望档案, 互动类型, 地点暴露风险, 选择倾向);
  const 更新后档案: NPC欲望档案 = { ...欲望档案, 阶段进度: 新阶段进度 };
  const { 升级: 应升级阶段, 新阶段 } = 检查欲望阶段升级(更新后档案);
  if (应升级阶段) { 更新后档案.当前阶段 = 新阶段; 更新后档案.阶段进度 = 0; }
  更新后档案.互动冷却期 = 计算互动冷却(互动类型, 更新后档案.当前阶段);
  const 暴露风险值 = 计算暴露风险(地点暴露风险, 时间段, 欲望档案.流言等级);
  更新后档案.暴露风险值 = Math.min(100, 欲望档案.暴露风险值 + Math.floor(暴露风险值 / 10));
  if (里程碑列表.length > 0 && 里程碑列表.length % 5 === 0) {
    const { 轨道 } = 判定关系轨道(里程碑列表, 更新后档案.当前阶段, 多角NPC数);
    更新后档案.关系轨道 = 轨道;
  }
  const 后果判定 = 判定后果(暴露风险值, 欲望档案.学业影响, 多角NPC数, 更新后档案.当前阶段, 欲望档案.流言等级);
  let 后果: 后果记录 | undefined;
  if (后果判定.触发 && 后果判定.类型) {
    后果 = { id: `consequence_${Date.now()}_${Math.random().toString(36).slice(2, 8)}`, 触发时间: new Date().toISOString(), 类型: 后果判定.类型, 关联NPC: npc.id, 严重程度: 后果判定.严重程度 ?? '轻微', 描述: `${后果判定.类型}事件触发`, 持续回合: 后果判定.严重程度 === '严重' ? 5 : 后果判定.严重程度 === '毁灭' ? 10 : 3, 是否已解决: false };
  }
  const 里程碑映射: Partial<Record<校园亲密互动类型, 里程碑类型>> = { '牵手': '首次牵手', '拥抱': '首次拥抱', '轻吻': '首次亲吻', '深吻': '首次亲吻', '独处过夜': '首次独处', '完全亲密': '首次亲密' };
  const 里程碑类型 = 里程碑映射[互动类型];
  let 新里程碑: 亲密里程碑 | undefined;
  if (里程碑类型 && !里程碑列表.some(m => m.里程碑类型 === 里程碑类型 && m.NPC姓名 === npc.姓名)) {
    新里程碑 = 生成里程碑(npc.id, npc.姓名, 里程碑类型, `与${npc.姓名}的${互动类型}`, 选择倾向 === '保守' ? '保守' : 选择倾向 === '激进' || 选择倾向 === '冒险暴露' ? '激进' : '中性', '未知', '关系进入新阶段');
  }
  更新后档案.最后一次互动时间 = new Date().toISOString();
  const npc性格外向 = npc.核心性格特征 ? ['外向', '开朗', '活泼', '大胆', '热情'].some(t => npc.核心性格特征?.includes(t)) : false;
  const 之前发生过暴露 = 更新后档案.暴露风险值 >= 70;
  const 露出推进 = 计算露出偏好推进(欲望档案, 更新后档案.当前阶段, 选择倾向, 更新后档案.暴露风险值, npc性格外向, 之前发生过暴露);
  更新后档案.露出状态 = { 当前等级: 露出推进.新等级, 等级进度: 露出推进.新进度, 最后一次露出尝试: 更新后档案.露出状态?.最后一次露出尝试 ?? new Date().toISOString(), 成功露出次数: 更新后档案.露出状态?.成功露出次数 ?? 0, 暴露失败次数: 更新后档案.露出状态?.暴露失败次数 ?? 0, 最大紧张度记录: 更新后档案.露出状态?.最大紧张度记录 ?? 0 };
  return { 更新后档案, 后果, 新里程碑, 暴露风险值, 应升级阶段 };
}
