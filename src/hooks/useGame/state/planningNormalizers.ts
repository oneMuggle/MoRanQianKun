const 取文本 = (value: any, fallback = ''): string => (typeof value === 'string' ? value.trim() : fallback);
const 取数字 = (value: any, fallback = 0): number => { const n = Number(value); return Number.isFinite(n) ? n : fallback; };
const 取字符串数组 = (value: any): string[] => (Array.isArray(value) ? value.map((item) => 取文本(item)).filter(Boolean) : []);
const 取数字数组 = (value: any): number[] => (Array.isArray(value) ? value.map((item) => Number(item)).filter((item) => Number.isFinite(item)) : []);

