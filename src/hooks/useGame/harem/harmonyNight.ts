/**
 * 和谐之夜引擎
 * 处理和谐之夜的触发、活动选择、效果计算
 */

import type { NSFW资源状态 } from '../../../models/nsfwCore/resources';
import type { 后宫系统状态, 和谐之夜活动类型 } from '../../../models/nsfwCore/multiPersonNSFW';
import {
  和谐之夜参数,
  和谐之夜结果,
  多人NSFW场景参数,
  和谐之夜活动配置,
} from '../../../models/nsfwCore/multiPersonNSFW';
import {
  计算和谐之夜效果,
  生成多人NSFW叙事约束,
} from '../../../models/nsfwCore/multiPersonNSFWUtils';

/**
 * 检查是否可触发和谐之夜
 */
export function 检查和谐之夜可触发(
  后宫系统: 后宫系统状态
): boolean {
  return 后宫系统.和谐度 >= 80 && 后宫系统.成员列表.length >= 2;
}

/**
 * 初始化和谐之夜
 */
export function 初始化和谐之夜(
  活动类型: 和谐之夜活动类型,
  后宫系统: 后宫系统状态
): { 参数: 和谐之夜参数 | null; 错误: string | null } {
  if (后宫系统.成员列表.length < 2) {
    return { 参数: null, 错误: '成员不足，至少需要 2 名成员' };
  }

  const 参与成员 = 后宫系统.成员列表.map(m => ({
    npcId: m.npcId,
    姓名: m.姓名,
    亲密度等级: m.亲密度等级,
    信任度: m.信任度,
  }));

  const 配置 = 和谐之夜活动配置[活动类型];

  return {
    参数: {
      参与成员,
      活动类型,
      当前和谐度: 后宫系统.和谐度,
      精力消耗: 配置.精力消耗,
    },
    错误: null,
  };
}

/**
 * 处理和谐之夜
 */
export function 处理和谐之夜(
  参数: 和谐之夜参数,
  后宫系统: 后宫系统状态,
  资源状态: NSFW资源状态,
  时代ID: string,
  基础叙事约束: string
): {
  成功: boolean;
  结果: 和谐之夜结果;
  新资源状态: NSFW资源状态;
  错误: string | null;
} {
  // 检查精力
  if (资源状态.精力值 < 参数.精力消耗) {
    return {
      成功: false,
      结果: {} as 和谐之夜结果,
      新资源状态: 资源状态,
      错误: `精力不足！需要 ${参数.精力消耗} 精力，当前只有 ${资源状态.精力值}`,
    };
  }

  // 计算效果
  const 结果 = 计算和谐之夜效果(参数);

  // 消耗精力
  const 新资源状态: NSFW资源状态 = {
    ...资源状态,
    精力值: 资源状态.精力值 - 参数.精力消耗,
  };

  // 生成叙事提示词（覆盖计算函数生成的简易版本）
  const 配置 = 和谐之夜活动配置[参数.活动类型];
  const 叙事参数: 多人NSFW场景参数 = {
    场景类型: '和谐之夜',
    参与成员: 参数.参与成员.map(m => ({
      姓名: m.姓名,
      位分: 后宫系统.成员列表.find(hm => hm.npcId === m.npcId)?.位分 ?? '',
      亲密度等级: m.亲密度等级,
      嫉妒值: 后宫系统.成员列表.find(hm => hm.npcId === m.npcId)?.嫉妒值 ?? 0,
      和谐度: 后宫系统.和谐度,
      欲望阶段: '克制' as any,
      人格特征: '',
    })),
    当前互动成员: 参数.参与成员[0]?.姓名 ?? '',
    是否多人场景: true,
    成员间关系摘要: '',
    平均和谐度: 参数.当前和谐度,
    内容强度: 配置.NSFW档位,
    时代ID,
  };
  结果.叙事提示词 = 生成多人NSFW叙事约束(叙事参数, 基础叙事约束);

  return { 成功: true, 结果, 新资源状态, 错误: null };
}

/**
 * 应用和谐之夜结果到游戏状态
 */
export function 应用和谐之夜结果(
  后宫系统: 后宫系统状态,
  结果: 和谐之夜结果
): 后宫系统状态 {
  const 新和谐度 = Math.max(0, Math.min(100, 后宫系统.和谐度 + 结果.和谐度变化));

  const 新成员列表 = 后宫系统.成员列表.map(成员 => ({
    ...成员,
    亲密度等级: 成员.亲密度等级 + 结果.每人亲密度变化,
    信任度: Math.max(0, Math.min(100, 成员.信任度 + 结果.每人信任变化)),
  }));

  const 新事件历史 = [
    ...后宫系统.事件历史,
    {
      类型: '和谐之夜' as const,
      描述: 结果.描述,
      时间: new Date().toISOString(),
      参与成员: 后宫系统.成员列表.map(m => m.npcId),
    },
  ];

  return {
    ...后宫系统,
    成员列表: 新成员列表,
    和谐度: 新和谐度,
    事件历史: 新事件历史,
  };
}
