/**
 * 嫉妒之夜引擎
 * 处理嫉妒之夜的触发、安抚选项生成、结果计算
 */

import type { NSFW资源状态 } from '../../../models/nsfwCore/resources';
import type { 后宫系统状态 } from '../../../models/nsfwCore/multiPersonNSFW';
import {
  嫉妒之夜参数,
  嫉妒之夜结果,
  安抚类型,
  多人NSFW场景参数,
} from '../../../models/nsfwCore/multiPersonNSFW';
import {
  生成嫉妒之夜安抚选项,
  生成多人NSFW叙事约束,
} from '../../../models/nsfwCore/multiPersonNSFWUtils';

/**
 * 检查是否触发嫉妒之夜
 */
export function 检查嫉妒之夜触发(
  后宫系统: 后宫系统状态
): { npcId: string; 姓名: string; 嫉妒值: number }[] {
  const 阈值 = 70;
  return 后宫系统.成员列表
    .filter(m => m.嫉妒值 >= 阈值 && m.亲密度等级 >= 3)
    .sort((a, b) => b.嫉妒值 - a.嫉妒值)
    .slice(0, 1)  // 每回合最多触发 1 次
    .map(m => ({ npcId: m.npcId, 姓名: m.姓名, 嫉妒值: m.嫉妒值 }));
}

/**
 * 初始化嫉妒之夜
 */
export function 初始化嫉妒之夜(
  成员: 后宫系统状态['成员列表'][number],
  嫉妒原因: string
): 嫉妒之夜参数 {
  return {
    成员: {
      npcId: 成员.npcId,
      姓名: 成员.姓名,
      嫉妒值: 成员.嫉妒值,
      信任度: 成员.信任度,
      亲密度等级: 成员.亲密度等级,
      欲望阶段: '克制' as any,
    },
    嫉妒原因,
  };
}

/**
 * 处理嫉妒之夜安抚选择
 */
export function 处理嫉妒之夜安抚(
  参数: 嫉妒之夜参数,
  选择: 安抚类型,
  后宫系统: 后宫系统状态,
  资源状态: NSFW资源状态,
  时代ID: string,
  基础叙事约束: string
): {
  成功: boolean;
  结果: 嫉妒之夜结果;
  新资源状态: NSFW资源状态;
  错误: string | null;
} {
  const 选项列表 = 生成嫉妒之夜安抚选项(参数.成员);
  const 选中选项 = 选项列表.find(o => o.类型 === 选择);

  if (!选中选项) {
    return {
      成功: false,
      结果: {} as 嫉妒之夜结果,
      新资源状态: 资源状态,
      错误: '无效的安抚选项',
    };
  }

  // 检查精力
  if (资源状态.精力值 < 选中选项.精力消耗) {
    return {
      成功: false,
      结果: {} as 嫉妒之夜结果,
      新资源状态: 资源状态,
      错误: `精力不足！需要 ${选中选项.精力消耗} 精力，当前只有 ${资源状态.精力值}`,
    };
  }

  // "暂不回应"直接返回失败效果
  if (选择 === '暂不回应') {
    const 新资源状态 = { ...资源状态 };
    const 结果: 嫉妒之夜结果 = {
      安抚类型: '暂不回应',
      是否成功: false,
      嫉妒变化: 选中选项.失败效果.嫉妒变化,
      信任变化: 选中选项.失败效果.信任变化,
      亲密度变化: 0,
      其他成员嫉妒变化: {},
      触发后续事件: 选中选项.失败效果.触发后续事件 ?? '冷战',
      描述: '你选择了暂不回应，关系进一步恶化',
      叙事提示词: '',
    };
    return { 成功: true, 结果, 新资源状态, 错误: null };
  }

  // 判定成功/失败
  const 是否成功 = Math.random() * 100 < 选中选项.成功率;

  // 消耗精力
  const 新资源状态: NSFW资源状态 = {
    ...资源状态,
    精力值: 资源状态.精力值 - 选中选项.精力消耗,
  };

  // 生成叙事提示词
  const 叙事参数: 多人NSFW场景参数 = {
    场景类型: '嫉妒之夜',
    参与成员: [{
      姓名: 参数.成员.姓名,
      位分: 后宫系统.成员列表.find(m => m.npcId === 参数.成员.npcId)?.位分 ?? '',
      亲密度等级: 参数.成员.亲密度等级,
      嫉妒值: 参数.成员.嫉妒值,
      和谐度: 后宫系统.成员列表.find(m => m.npcId === 参数.成员.npcId)?.和谐度 ?? 0,
      欲望阶段: 参数.成员.欲望阶段,
      人格特征: '',
    }],
    当前互动成员: 参数.成员.姓名,
    是否多人场景: false,
    成员间关系摘要: '',
    平均和谐度: 后宫系统.和谐度,
    内容强度: '适度展开',
    时代ID,
  };
  const 叙事提示词 = 生成多人NSFW叙事约束(叙事参数, 基础叙事约束);

  let 结果: 嫉妒之夜结果;

  if (是否成功) {
    const 效果 = 选中选项.成功效果;
    const 其他成员嫉妒变化: Record<string, number> = {};
    if (效果.其他成员嫉妒变化) {
      for (const 成员 of 后宫系统.成员列表) {
        if (成员.npcId !== 参数.成员.npcId) {
          其他成员嫉妒变化[成员.npcId] = 效果.其他成员嫉妒变化!;
        }
      }
    }

    结果 = {
      安抚类型: 选择,
      是否成功: true,
      嫉妒变化: 效果.嫉妒变化,
      信任变化: 效果.信任变化,
      亲密度变化: 效果.亲密度变化,
      其他成员嫉妒变化,
      触发后续事件: null,
      描述: `${选择}: 安抚成功`,
      叙事提示词,
    };
  } else {
    const 效果 = 选中选项.失败效果;
    结果 = {
      安抚类型: 选择,
      是否成功: false,
      嫉妒变化: 效果.嫉妒变化,
      信任变化: 效果.信任变化,
      亲密度变化: 0,
      其他成员嫉妒变化: {},
      触发后续事件: 效果.触发后续事件 ?? null,
      描述: `${选择}: 安抚失败`,
      叙事提示词,
    };
  }

  return { 成功: true, 结果, 新资源状态, 错误: null };
}

/**
 * 应用嫉妒之夜结果到游戏状态
 */
export function 应用嫉妒之夜结果(
  后宫系统: 后宫系统状态,
  结果: 嫉妒之夜结果,
  目标NpcId: string
): 后宫系统状态 {
  const 新成员列表 = 后宫系统.成员列表.map(成员 => {
    if (成员.npcId === 目标NpcId) {
      return {
        ...成员,
        嫉妒值: Math.max(0, Math.min(100, 成员.嫉妒值 + 结果.嫉妒变化)),
        信任度: Math.max(0, Math.min(100, 成员.信任度 + 结果.信任变化)),
      };
    }
    const 嫉妒增量 = 结果.其他成员嫉妒变化[成员.npcId] ?? 0;
    if (嫉妒增量 !== 0) {
      return {
        ...成员,
        嫉妒值: Math.max(0, Math.min(100, 成员.嫉妒值 + 嫉妒增量)),
      };
    }
    return 成员;
  });

  const 新事件历史 = [
    ...后宫系统.事件历史,
    {
      类型: '嫉妒之夜' as const,
      描述: 结果.描述,
      时间: new Date().toISOString(),
      参与成员: [目标NpcId],
    },
  ];

  const 新活跃冲突 = 结果.触发后续事件
    ? [...后宫系统.活跃冲突, `${结果.触发后续事件}: ${后宫系统.成员列表.find(m => m.npcId === 目标NpcId)?.姓名}`]
    : 后宫系统.活跃冲突;

  return {
    ...后宫系统,
    成员列表: 新成员列表,
    事件历史: 新事件历史,
    活跃冲突: 新活跃冲突,
  };
}
