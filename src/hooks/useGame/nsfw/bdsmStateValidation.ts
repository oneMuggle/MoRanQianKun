/**
 * bdsmStateValidation.ts
 * BDSM 关系状态数据校验
 *
 * 类似关系模块的 `验证关系数据`，对 BDSM 状态进行完整性和合法性校验。
 * 在存档加载和 NPC 创建时调用，确保数据一致性。
 */

import type {
  BDSM关系状态,
  BDSM调教任务,
  BDSM日常指令,
  BDSM里程碑,
  契约记录,
  关系阶段,
  BDSM任务状态,
  BDSM评价等级,
  契约状态,
} from '../../../models/campusNSFW/sm';

// ==================== 常量 ====================

const 合法的关系阶段: 关系阶段[] = ['初识', '试探', '确立', '深入', '固化'];
const 合法的任务状态: BDSM任务状态[] = ['待接受', '进行中', '已完成', '已失败', '已放弃'];
const 合法的评价等级: BDSM评价等级[] = ['完美服从', '优秀', '良好', '勉强', '失败', '拒绝'];
const 合法的契约状态: 契约状态[] = ['未缔结', '口头约定', '书面契约', '信物交换', '已解除', '已违约'];
const 合法的任务类型 = ['服从测试', '忠诚考验', '技能训练', '心理建设', '公开挑战', '日常指令', '信物任务', '契约履行'];
const 合法的难度 = ['入门', '初级', '中级', '高级', '极限'];

// ==================== 类型 ====================

export interface BDSM校验结果 {
  有效: boolean;
  错误列表: string[];
  警告列表: string[];
}

export interface 任务校验结果 {
  有效: boolean;
  错误列表: string[];
  警告列表: string[];
}

// ==================== 校验函数 ====================

/**
 * 校验 BDSM 关系状态数据的完整性和合法性
 */
export function 验证BDSM状态数据(关系状态: Partial<BDSM关系状态>, npcId: string): BDSM校验结果 {
  const 错误列表: string[] = [];
  const 警告列表: string[] = [];

  // 检查必要字段
  if (关系状态.阶段 === undefined) {
    错误列表.push(`[${npcId}] BDSM关系状态缺少阶段字段`);
  } else if (!合法的关系阶段.includes(关系状态.阶段)) {
    错误列表.push(`[${npcId}] BDSM关系阶段值不合法: ${关系状态.阶段}`);
  }

  if (typeof 关系状态.服从度 !== 'number') {
    错误列表.push(`[${npcId}] BDSM关系状态缺少服从度字段`);
  } else {
    if (关系状态.服从度 < 0 || 关系状态.服从度 > 100) {
      错误列表.push(`[${npcId}] 服从度值超出范围 [0, 100]: ${关系状态.服从度}`);
    }
  }

  if (typeof 关系状态.权力天平 !== 'number') {
    错误列表.push(`[${npcId}] BDSM关系状态缺少权力天平字段`);
  } else {
    if (关系状态.权力天平 < -50 || 关系状态.权力天平 > 50) {
      错误列表.push(`[${npcId}] 权力天平值超出范围 [-50, 50]: ${关系状态.权力天平}`);
    }
  }

  if (typeof 关系状态.安全词 !== 'string' || 关系状态.安全词.length === 0) {
    警告列表.push(`[${npcId}] 安全词为空或未设置`);
  }

  // 校验契约记录
  if (Array.isArray(关系状态.契约记录)) {
    关系状态.契约记录.forEach((契约, idx) => {
      const 契约错误 = 校验单条契约记录(契约, `${npcId}.契约记录[${idx}]`);
      错误列表.push(...契约错误.错误列表);
      警告列表.push(...契约错误.警告列表);
    });
  }

  // 校验任务历史
  if (Array.isArray(关系状态.任务历史)) {
    关系状态.任务历史.forEach((任务, idx) => {
      const 任务错误 = 校验单条任务记录(任务, `${npcId}.任务历史[${idx}]`);
      错误列表.push(...任务错误.错误列表);
      警告列表.push(...任务错误.警告列表);
    });
  }

  // 校验日常指令
  if (Array.isArray(关系状态.日常指令)) {
    关系状态.日常指令.forEach((指令, idx) => {
      const 指令错误 = 校验单条日常指令(指令, `${npcId}.日常指令[${idx}]`);
      错误列表.push(...指令错误.错误列表);
      警告列表.push(...指令错误.警告列表);
    });
  }

  // 校验里程碑
  if (Array.isArray(关系状态.里程碑)) {
    关系状态.里程碑.forEach((里程碑, idx) => {
      const 里程碑错误 = 校验单条里程碑(里程碑, `${npcId}.里程碑[${idx}]`);
      错误列表.push(...里程碑错误.错误列表);
      警告列表.push(...里程碑错误.警告列表);
    });
  }

  // 校验底线列表
  if (!Array.isArray(关系状态.底线列表)) {
    警告列表.push(`[${npcId}] 底线列表不是数组`);
  }

  return {
    有效: 错误列表.length === 0,
    错误列表,
    警告列表,
  };
}

/**
 * 校验单条契约记录
 */
function 校验单条契约记录(契约: Partial<契约记录>, 路径: string): { 错误列表: string[]; 警告列表: string[] } {
  const 错误列表: string[] = [];
  const 警告列表: string[] = [];

  if (!契约.id) {
    错误列表.push(`[${路径}] 契约记录缺少 id`);
  }

  if (契约.状态 && !合法的契约状态.includes(契约.状态)) {
    错误列表.push(`[${路径}] 契约状态值不合法: ${契约.状态}`);
  }

  if (!契约.缔结时间) {
    警告列表.push(`[${路径}] 契约记录缺少缔结时间`);
  }

  if (!Array.isArray(契约.条款列表) || 契约.条款列表.length === 0) {
    警告列表.push(`[${路径}] 契约条款列表为空`);
  }

  return { 错误列表, 警告列表 };
}

/**
 * 校验单条任务记录
 */
function 校验单条任务记录(任务: Partial<BDSM调教任务>, 路径: string): { 错误列表: string[]; 警告列表: string[] } {
  const 错误列表: string[] = [];
  const 警告列表: string[] = [];

  if (!任务.id) {
    错误列表.push(`[${路径}] 任务记录缺少 id`);
  }

  if (任务.类型 && !合法的任务类型.includes(任务.类型)) {
    错误列表.push(`[${路径}] 任务类型值不合法: ${任务.类型}`);
  }

  if (任务.状态 && !合法的任务状态.includes(任务.状态)) {
    错误列表.push(`[${路径}] 任务状态值不合法: ${任务.状态}`);
  }

  if (任务.难度 && !合法的难度.includes(任务.难度)) {
    错误列表.push(`[${路径}] 任务难度值不合法: ${任务.难度}`);
  }

  if (任务.评价 && !合法的评价等级.includes(任务.评价)) {
    错误列表.push(`[${路径}] 任务评价等级值不合法: ${任务.评价}`);
  }

  // 任务完成但没有评价
  if (任务.状态 === '已完成' && !任务.评价) {
    警告列表.push(`[${路径}] 任务已完成但缺少评价`);
  }

  // 任务已拒绝但有服从度变化
  if (任务.评价 === '拒绝' && 任务.服从度变化 >= 0) {
    警告列表.push(`[${路径}] 拒绝评价但服从度变化非负`);
  }

  return { 错误列表, 警告列表 };
}

/**
 * 校验单条日常指令
 */
function 校验单条日常指令(指令: Partial<BDSM日常指令>, 路径: string): { 错误列表: string[]; 警告列表: string[] } {
  const 错误列表: string[] = [];
  const 警告列表: string[] = [];

  if (!指令.内容) {
    错误列表.push(`[${路径}] 日常指令缺少内容`);
  }

  if (指令.分类 && !['称呼', '穿着', '行为', '报告', '任务'].includes(指令.分类)) {
    错误列表.push(`[${路径}] 日常指令分类不合法: ${指令.分类}`);
  }

  if (typeof 指令.是否完成 !== 'boolean') {
    警告列表.push(`[${路径}] 日常指令是否完成字段类型错误`);
  }

  return { 错误列表, 警告列表 };
}

/**
 * 校验单条里程碑
 */
function 校验单条里程碑(里程碑: Partial<BDSM里程碑>, 路径: string): { 错误列表: string[]; 警告列表: string[] } {
  const 错误列表: string[] = [];
  const 警告列表: string[] = [];

  if (!里程碑.描述) {
    错误列表.push(`[${路径}] 里程碑缺少描述`);
  }

  if (!里程碑.时间) {
    警告列表.push(`[${路径}] 里程碑缺少时间`);
  }

  if (!里程碑.类型) {
    警告列表.push(`[${路径}] 里程碑缺少类型`);
  }

  return { 错误列表, 警告列表 };
}

/**
 * 校验并修复 BDSM 状态数据（尝试自动修复可识别的问题）
 */
export function 校验并修复BDSM状态数据(关系状态: BDSM关系状态, npcId: string): {
  修复后状态: BDSM关系状态;
  校验结果: BDSM校验结果;
} {
  const 校验结果 = 验证BDSM状态数据(关系状态, npcId);

  // 创建修复后的状态副本
  const 修复后状态: BDSM关系状态 = { ...关系状态 };

  // 自动修复：确保必要字段有默认值
  if (修复后状态.阶段 === undefined) {
    修复后状态.阶段 = '初识';
  }
  if (typeof 修复后状态.服从度 !== 'number') {
    修复后状态.服从度 = 0;
  }
  if (typeof 修复后状态.权力天平 !== 'number') {
    修复后状态.权力天平 = 0;
  }
  if (!修复后状态.安全词) {
    修复后状态.安全词 = '月光';
  }
  if (!Array.isArray(修复后状态.底线列表)) {
    修复后状态.底线列表 = [];
  }
  if (!Array.isArray(修复后状态.契约记录)) {
    修复后状态.契约记录 = [];
  }
  if (!Array.isArray(修复后状态.任务历史)) {
    修复后状态.任务历史 = [];
  }
  if (!Array.isArray(修复后状态.日常指令)) {
    修复后状态.日常指令 = [];
  }
  if (!Array.isArray(修复后状态.里程碑)) {
    修复后状态.里程碑 = [];
  }

  // 修复契约记录
  修复后状态.契约记录 = 修复后状态.契约记录.map(契约 => ({
    ...契约,
    状态: 合法的契约状态.includes(契约.状态 as 契约状态) ? 契约.状态 : '未缔结',
    违约次数: typeof 契约.违约次数 === 'number' ? 契约.违约次数 : 0,
    条款列表: Array.isArray(契约.条款列表) ? 契约.条款列表 : [],
  }));

  // 修复任务历史
  修复后状态.任务历史 = 修复后状态.任务历史.map(任务 => ({
    ...任务,
    状态: 合法的任务状态.includes(任务.状态 as BDSM任务状态) ? 任务.状态 : '待接受',
    难度: 合法的难度.includes(任务.难度 as any) ? 任务.难度 : '入门',
    服从度变化: typeof 任务.服从度变化 === 'number' ? 任务.服从度变化 : 0,
  }));

  // 修复日常指令
  修复后状态.日常指令 = 修复后状态.日常指令.map(指令 => ({
    ...指令,
    分类: ['称呼', '穿着', '行为', '报告', '任务'].includes(指令.分类) ? 指令.分类 : '行为',
    是否完成: typeof 指令.是否完成 === 'boolean' ? 指令.是否完成 : false,
  }));

  return { 修复后状态, 校验结果 };
}
