// ==================== 复合情感初始化 ====================

function 获取人格情感系数(人格标签?: string): Partial<Record<情感维度, number>> {
  if (!人格标签) return {};
  const 系数: Partial<Record<情感维度, number>> = {};
  if (人格标签.includes('傲娇') || 人格标签.includes('大小姐')) {
    系数.嫉妒 = 1.3; 系数.不安 = 1.2;
  }
  if (人格标签.includes('温柔') || 人格标签.includes('贤妻')) {
    系数.爱慕 = 1.3; 系数.内疚 = 1.2;
  }
  if (人格标签.includes('高冷') || 人格标签.includes('清冷')) {
    系数.兴奋 = 0.7; 系数.内疚 = 0.8;
  }
  if (人格标签.includes('元气') || 人格标签.includes('活泼')) {
    系数.兴奋 = 1.3; 系数.爱慕 = 1.2;
  }
  if (人格标签.includes('病娇') || 人格标签.includes('偏执')) {
    系数.嫉妒 = 1.5; 系数.不安 = 1.3; 系数.爱慕 = 1.4;
  }
  return 系数;
}

export function 初始化复合情感(人格标签?: string, 好感度?: number): 复合情感状态 {
  const 人格系数 = 获取人格情感系数(人格标签);
  const 基础值 = 好感度 ? 好感度 * 0.3 : 10;
  const 情感列表: 情感维度[] = ['爱慕', '嫉妒', '不安', '兴奋', '内疚'];
  const 情感维度值: Record<情感维度, number> = {} as any;

  for (const 维度 of 情感列表) {
    情感维度值[维度] = Math.max(0, Math.min(100, 基础值 + (Math.random() * 20 - 10) * (人格系数[维度] ?? 1.0)));
  }

  const 主导情感 = 情感列表.reduce((a, b) => 情感维度值[a] > 情感维度值[b] ? a : b);

  return {
    基础心情: 基础值,
    情感维度: 情感维度值,
    主导情感,
    情感冲突: [],
    最后更新时间: new Date().toISOString(),
  };
}

// ==================== 情感冲突检测 ====================

const 情感冲突对: { A: 情感维度; B: 情感维度; 行为表现: string }[] = [
  { A: '爱慕', B: '内疚', 行为表现: '渴望亲近却又自责不安' },
  { A: '嫉妒', B: '内疚', 行为表现: '因嫉妒而自责，但又无法控制' },
  { A: '兴奋', B: '不安', 行为表现: '既期待又忐忑的矛盾心理' },
  { A: '爱慕', B: '嫉妒', 行为表现: '深爱却又因他人而嫉妒' },
];

export function 检测情感冲突(情感: 复合情感状态): 情感冲突条目[] {
  const 冲突列表: 情感冲突条目[] = [];

  for (const { A, B, 行为表现 } of 情感冲突对) {
    const 冲突强度 = Math.min(情感.情感维度[A], 情感.情感维度[B]);
    if (冲突强度 > 30) {
      冲突列表.push({
        维度A: A,
        维度B: B,
        冲突强度: Math.round(冲突强度),
        行为表现,
      });
    }
  }

  return 冲突列表;
}

