// ==================== NSFW深化状态确保初始化 ====================

interface NPC人格参考 {
  核心性格特征?: string;
  好感度: number;
  完整演化状态?: import('../../../models/npcNSFWEnhancement/types').完整演化状态;
}

export function 确保NSFW深化状态初始化(npc: NPC人格参考): NPC人格参考 {
  if (!npc.完整演化状态) {
    npc.完整演化状态 = {} as any;
  }

  if (!npc.完整演化状态.复合情感) {
    npc.完整演化状态.复合情感 = 初始化复合情感(npc.核心性格特征, npc.好感度);
  }

  if (!npc.完整演化状态.内在动机) {
    npc.完整演化状态.内在动机 = 初始化内在动机({ 人格类型: npc.核心性格特征, 好感度: npc.好感度 });
  }

  if (!npc.完整演化状态.节奏状态) {
    npc.完整演化状态.节奏状态 = 初始化节奏状态();
  }

  return npc;
}

// ==================== 主循环集成：回合预处理 ====================

export interface 回合预处理结果 {
  更新后的NPC: NPC人格参考;
  提示词注入: string;
  自主行为: 自主行为回合结果[];
  前戏阶段提示?: string;
}

/**
 * 执行NSFW回合预处理（每回合调用一次）
 * 1. 衰减欲望满足度
 * 2. 检测情感冲突
 * 3. 计算自主行为
 * 4. 推进前戏阶段
 * 5. 生成提示词注入
 */
export function 执行NSFW回合预处理(
  npc: NPC人格参考,
  在场NPC列表: NPC主动行为上下文[] = [],
  最近互动类型?: string,
  亲密度等级?: number
): 回合预处理结果 {
  const 更新NPC = 确保NSFW深化状态初始化(npc);
  const 演化状态 = 更新NPC.完整演化状态!;

  // 1. 衰减欲望
  if (演化状态.内在动机) {
    const 新动机 = 衰减欲望满足度(演化状态.内在动机 as Autonomy内在动机);
    演化状态.内在动机 = 新动机 as typeof 演化状态.内在动机;
  }

  // 2. 检测情感冲突
  if (演化状态.复合情感) {
    演化状态.复合情感.情感冲突 = 检测情感冲突(演化状态.复合情感);
    演化状态.复合情感.最后更新时间 = new Date().toISOString();
  }

  // 3. 推进前戏阶段
  let 前戏阶段提示 = '';
  if (演化状态.节奏状态 && 最近互动类型) {
    const 情感维度值 = 演化状态.复合情感?.情感维度;
    演化状态.节奏状态 = 推进前戏阶段(
      演化状态.节奏状态,
      最近互动类型,
      亲密度等级 ?? 0,
      情感维度值
    );
    前戏阶段提示 = 生成前戏阶段提示(演化状态.节奏状态);
  }

  // 4. 计算自主行为
  const 自主行为结果 = 计算回合自主行为(在场NPC列表);
  const 提示词注入 = 自主行为提示词注入(自主行为结果);

  // 合并前戏阶段提示
  const 总提示 = [提示词注入, 前戏阶段提示].filter(Boolean).join('\n');

  return {
    更新后的NPC: 更新NPC,
    提示词注入: 总提示,
    自主行为: 自主行为结果,
    前戏阶段提示,
  };
}
