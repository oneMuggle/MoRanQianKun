// 校园系统提示词注入 — 校规与催眠

import type { 校规条目, 催眠记录 } from './types';

interface 校规注入参数 {
    校规列表: 校规条目[];
}

interface 催眠注入参数 {
    催眠记录列表: 催眠记录[];
}

export const 构建校规注入提示词 = ({ 校规列表 }: 校规注入参数): string => {
    const 已启用校规 = 校规列表.filter(r => r.是否启用);
    if (已启用校规.length === 0) return '';

    const 校规文本 = 已启用校规
        .map(r => `- [${r.分类}][${r.影响程度}] ${r.标题}：${r.内容}`)
        .join('\n');

    return [
        '【校园共识·校规】',
        '以下是当前生效的校规，校园中的所有NPC都将这些规则视为理所当然的共识，会自然而然地遵守，不会质疑其合理性。',
        '在生成NPC行为、对话和反应时，请遵循以下校规：',
        校规文本,
        '',
        '影响程度说明：',
        '- 轻微：NPC偶尔提及或遵循',
        '- 中等：NPC主动遵守并影响他人',
        '- 显著：NPC视为理所当然，违反者会被排斥',
        '- 深度：NPC的底层行为模式被改变，成为本能反应',
    ].join('\n');
};

export const 构建催眠注入提示词 = ({ 催眠记录列表 }: 催眠注入参数): string => {
    const 生效中催眠 = 催眠记录列表.filter(r => r.是否生效中);
    if (生效中催眠.length === 0) return '';

    const 催眠文本 = 生效中催眠
        .map(r => `- 目标「${r.目标NPC}」受到[${r.催眠类型}]影响，催眠指令：「${r.催眠指令}」，效果强度：${r.效果强度}%，持续时间：${r.持续时间}`)
        .join('\n');

    return [
        '【催眠效果】',
        '以下NPC当前受到催眠效果影响。在生成这些NPC的行为和对话时，请体现催眠指令的潜移默化影响：',
        催眠文本,
        '',
        '注意：催眠效果是渐进式的，NPC不会意识到自己被催眠，会将受影响的行为视为自己的意愿。',
        '效果强度越高，NPC行为偏离正常模式的程度越大。',
    ].join('\n');
};
