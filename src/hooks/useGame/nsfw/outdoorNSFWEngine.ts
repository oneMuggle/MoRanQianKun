/**
 * 创建户外活动
 */
export function 创建户外活动(
  运动类型值: 运动类型,
  活动性质值: 活动性质,
  参与者: 参与者核心状态,
  教练: 教练领队核心状态,
  上下文: { 场所: 活动场所; 天气: 天气状况; 住宿: 住宿类型; 私密程度: 私密程度 }
): 活动创建结果 {
  const 强度 = 运动强度系数[运动类型值];
  const 天气修正值 = 天气修正[上下文.天气];

  const 场所风险 = 环境风险因素.场所风险[上下文.场所];
  const 私密风险 = 环境风险因素.私密程度风险[上下文.私密程度];
  const 住宿风险 = 环境风险因素.住宿类型风险[上下文.住宿];
  const 初始风险 = Math.round(
    (强度.风险基数 * 0.4 + 场所风险 * 0.3 + 私密风险 * 0.2 + 住宿风险 * 0.1)
    * 天气修正值.风险倍率
  );

  const 活动: 活动项目状态 = {
    项目ID: `outdoor_${Date.now()}_${Math.random().toString(36).slice(2, 6)}`,
    组织者ID: 教练.教练ID,
    教练ID: 教练.教练ID,
    参与者ID列表: [参与者.参与者ID],
    活动类型: 运动类型值,
    活动性质: 活动性质值,
    活动时长: 1,
    活动强度: 强度.基础体力消耗 > 35 ? '极高' : 强度.基础体力消耗 > 25 ? '较高' : 强度.基础体力消耗 > 15 ? '中等' : '休闲',
    活动场所: 上下文.场所,
    私密程度: 上下文.私密程度,
    信号状况: 上下文.场所 === '无人区' ? '无信号' : 上下文.场所 === '野山野水' ? '较差' : '满格',
    住宿类型: 上下文.住宿,
    男女分住: 上下文.住宿 !== '多人混帐',
    记录设备: ['运动相机'],
    设备归属: ['教练领队设备'],
    拍摄授权: false,
    约定内容: `${运动类型值}活动，${活动性质值}`,
    约定尺度: '正常',
    实际内容: '',
    实际尺度: '正常',
    越界行为: [],
    投诉记录: false,
    原片风险: Math.min(100, 初始风险),
    天气状况: 上下文.天气,
    环境危险度: Math.round((场所风险 + 私密风险) / 2),
  };

  return { 成功: true, 活动, 初始风险, 描述: `创建了${运动类型值}活动，初始风险: ${初始风险}` };
}

/**
 * 运动强度系统
 */
export function 计算运动强度(
  运动类型值: 运动类型,
  当前体力: number,
  天气: 天气状况,
  参与者状态: 参与者核心状态
): 运动强度结果 {
  const 强度 = 运动强度系数[运动类型值];
  const 天气修正值 = 天气修正[天气];

  const 基础消耗 = 强度.基础体力消耗 * 天气修正值.体力消耗倍率;
  const 体能修正 = (100 - 参与者状态.体能水平) / 100 * 0.3;
  const 体力消耗 = Math.round(基础消耗 * (1 + 体能修正));

  const 新体力 = Math.max(0, 当前体力 - 体力消耗);
  const 判断力变化 = 新体力 < 30 ? -25 : 新体力 < 50 ? -15 : 新体力 < 70 ? -5 : 0;
  const 抵抗力变化 = 新体力 < 40 ? -20 : 新体力 < 60 ? -10 : 0;

  let 描述 = '';
  if (新体力 < 30) 描述 = '体力严重不足，判断力大幅下降';
  else if (新体力 < 50) 描述 = '体力较低，判断力和边界感有所模糊';
  else if (新体力 < 70) 描述 = '体力适中，判断力正常';
  else 描述 = '体力充沛，判断力清晰';

  return { 体力消耗, 判断力变化, 抵抗力变化, 描述 };
}

/**
 * 户外暴露风险计算
 */
export function 计算户外暴露风险(
  活动: 活动项目状态,
  当前尺度: 极限叙事尺度,
  上下文: 户外活动上下文
): number {
  const 强度 = 运动强度系数[活动.活动类型];
  const 天气修正值 = 天气修正[上下文.天气];

  const 场所风险 = 环境风险因素.场所风险[活动.活动场所];
  const 私密风险 = 环境风险因素.私密程度风险[活动.私密程度];
  const 住宿风险 = 环境风险因素.住宿类型风险[活动.住宿类型];

  const 尺度指数 = 尺度分级定义[当前尺度].尺度指数;
  const 尺度加成 = 尺度指数 / 100;

  const 体力影响 = 上下文.当前体力 < 30 ? 1.3 : 上下文.当前体力 < 60 ? 1.1 : 1.0;
  const 人数影响 = 1 - (上下文.参与人数 - 1) * 0.05;

  const 风险 = Math.round(
    (场所风险 * 0.3 + 私密风险 * 0.3 + 住宿风险 * 0.2 + 强度.风险基数 * 尺度加成 * 0.2)
    * 天气修正值.风险倍率
    * 体力影响
    * Math.max(0.5, 人数影响)
  );

  return Math.max(0, Math.min(100, 风险));
}

/**
 * 同伴互动系统
 */
export function 同伴互动(
  互动类型: '互助保护' | '单独指导' | '休息交流' | '紧急救援' | '深夜谈话',
  上下文: 户外活动上下文,
  教练: 教练领队核心状态
): 同伴互动结果 {
  const 强度 = 运动强度系数[上下文.运动类型];
  const 基础亲密度 = 强度.亲密度提升倍率;

  const 互动系数: Record<string, { 亲密倍率: number; 信任影响: number; 风险影响: number }> = {
    '互助保护': { 亲密倍率: 0.8, 信任影响: 10, 风险影响: -5 },
    '单独指导': { 亲密倍率: 1.5, 信任影响: 5, 风险影响: 10 },
    '休息交流': { 亲密倍率: 0.6, 信任影响: 3, 风险影响: 0 },
    '紧急救援': { 亲密倍率: 2.0, 信任影响: 20, 风险影响: -10 },
    '深夜谈话': { 亲密倍率: 1.2, 信任影响: 8, 风险影响: 5 },
  };

  const 系数 = 互动系数[互动类型];

  const 动机修正 = (() => {
    switch (教练.动机类型) {
      case '纯教学': return { 信任: 5, 风险: -5 };
      case '泡妞': return { 信任: -3, 风险: 10 };
      case '猎艳': return { 信任: -5, 风险: 15 };
      default: return { 信任: 0, 风险: 0 };
    }
  })();

  const 亲密度变化 = Math.round(基础亲密度 * 系数.亲密倍率 * (0.8 + Math.random() * 0.4));
  const 信任变化 = 系数.信任影响 + 动机修正.信任;
  const 体力变化 = 互动类型 === '休息交流' ? 10 : 互动类型 === '紧急救援' ? -15 : -5;
  const 风险变化 = 系数.风险影响 + 动机修正.风险;

  return {
    成功: true,
    亲密度变化,
    信任变化,
    体力变化,
    风险变化,
    描述: get同伴互动描述(互动类型, 亲密度变化),
  };
}

