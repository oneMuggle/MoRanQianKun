import type { 游戏事件 } from '../../../models/eventTrigger';
import { 计算触发回合 } from './core';

export function 获取事件描述(事件: 游戏事件): string {
  const 状态标记 = 事件.状态 !== '待触发' ? `[${事件.状态}]` : '';
  const 优先级标记 = 事件.优先级 !== undefined ? `⚡${事件.优先级}` : '';
  const 触发回合 = 计算触发回合(事件);
  let 触发条件描述 = '';
  switch (事件.触发条件.kind) {
    case '回合偏移': 触发条件描述 = `${事件.创建回合 || 0} + ${事件.触发条件.偏移量} = ${触发回合}`; break;
    case '回合绝对': 触发条件描述 = `回合 ${事件.触发条件.目标回合}`; break;
    case '条件表达式': 触发条件描述 = `条件: ${事件.触发条件.表达式}`; break;
  }
  return `${状态标记}${优先级标记}${事件.名称} (${触发条件描述})`;
}
