/**
 * 获取指定运动类型的场景列表
 */
export function 获取运动场景(运动类型值: 运动类型): 运动场景[] {
  return 运动场景库[运动类型值] ?? [];
}

/**
 * 获取当前条件下可用的场景
 */
export function 获取可用场景(
  运动类型值: 运动类型,
  当前体力: number,
  当前亲密度: number,
  _当前尺度: 极限叙事尺度
): 运动场景[] {
  const 场景列表 = 获取运动场景(运动类型值);
  return 场景列表.filter(场景 => {
    if (场景.触发条件.最低体力 && 当前体力 < 场景.触发条件.最低体力) return false;
    if (场景.触发条件.最低亲密度 && 当前亲密度 < 场景.触发条件.最低亲密度) return false;
    return true;
  });
}

/**
 * 执行场景互动
 */
export function 执行场景互动(
  场景: 运动场景,
  上下文: { 体力: number; 亲密度: number; 天气?: string }
): 场景互动结果 {
  const 天气修正 = 上下文.天气 === '雨' || 上下文.天气 === '雪' ? 0.8 : 1.0;
  const 成功率 = Math.min(0.9, 0.5 + (上下文.体力 - 30) / 100 + (上下文.亲密度 - 20) / 100);

  const 成功 = Math.random() < 成功率;

  const 描述列表 = 成功 ? 场景.成功描述 : 场景.失败描述;
  const 描述 = 描述列表[Math.floor(Math.random() * 描述列表.length)];

  return {
    成功,
    风险变化: Math.round(场景.风险变化 * (成功 ? 0.8 : 1.2) * 天气修正),
    亲密度变化: Math.round(场景.亲密度变化 * (成功 ? 1.0 : 0.5)),
    体力变化: 成功 ? -5 : -10,
    描述,
  };
}

/**
 * 应用装备效果
 */
export function 应用装备(装备: 运动装备): 装备应用结果 {
  return {
    风险减少: 装备.风险减少,
    体力加成: 装备.体力加成,
    描述: `装备了${装备.名称}，${装备.描述}`,
  };
}

/**
 * 获取适用于指定运动类型的装备列表
 */
export function 获取可用装备(运动类型值: 运动类型): 运动装备[] {
  return 运动装备表.filter(装备 =>
    装备.适用运动.includes(运动类型值)
  );
}
