/**
 * 暴露风险 — 多人暴露场景
 * 多名 NPC 同时处于暴露风险中，群体动态、连带责任
 */

import type { NSFW资源状态 } from '../nsfwCore/resources';
import { 修改声誉 } from '../nsfwCore/resources';

// ==================== 类型定义 ====================

export type 性格倾向 = '大胆' | '谨慎' | '从众' | '领袖';

export interface 群体暴露参与者 {
  NPCID: string;
  NPC名称: string;
  性格倾向: 性格倾向;
  紧张度: number;
  配合度: number;
  暴露风险: number;
  是否被发现: boolean;
}

export interface 群体暴露会话 {
  id: string;
  参与者: 群体暴露参与者[];
  场景名称: string;
  当前回合: number;
  最大回合: number;
  群体紧张度: number;
  被发现人数: number;
  群体行为: '各自为战' | '互相掩护' | '争先恐后' | '恐慌';
}

export interface 群体动态事件 {
  类型: '掩护' | '出卖' | '跟随' | '带头' | '恐慌';
  触发者: string;
  影响对象: string[];
  描述: string;
}

export interface 群体回合结果 {
  新会话: 群体暴露会话;
  事件: 群体动态事件 | null;
  资源变化: NSFW资源状态;
  描述: string;
}

// ==================== 常量 ====================

const 性格系数: Record<性格倾向, { 风险倍率: number; 紧张增速: number; 配合倾向: number }> = {
  '大胆': { 风险倍率: 1.3, 紧张增速: 0.5, 配合倾向: 0.3 },
  '谨慎': { 风险倍率: 0.7, 紧张增速: 1.2, 配合倾向: 0.8 },
  '从众': { 风险倍率: 1.0, 紧张增速: 0.8, 配合倾向: 0.9 },
  '领袖': { 风险倍率: 1.1, 紧张增速: 0.6, 配合倾向: 0.5 },
};

// ==================== 核心函数 ====================

export function 创建群体暴露会话(
  场景名称: string,
  参与者列表: { ID: string; 名称: string; 性格: 性格倾向 }[]
): 群体暴露会话 {
  return {
    id: `group_exposure_${Date.now()}`,
    参与者: 参与者列表.map(p => ({
      NPCID: p.ID,
      NPC名称: p.名称,
      性格倾向: p.性格,
      紧张度: 20,
      配合度: 60,
      暴露风险: 30,
      是否被发现: false,
    })),
    场景名称,
    当前回合: 0,
    最大回合: 5,
    群体紧张度: 20,
    被发现人数: 0,
    群体行为: '各自为战' as const,
  };
}

export function 计算群体行为(会话: 群体暴露会话): 群体暴露会话['群体行为'] {
  const 平均紧张 = 会话.参与者.reduce((sum, p) => sum + p.紧张度, 0) / 会话.参与者.length;
  const 平均配合 = 会话.参与者.reduce((sum, p) => sum + p.配合度, 0) / 会话.参与者.length;
  const 已发现 = 会话.参与者.filter(p => p.是否被发现).length;

  if (平均紧张 >= 80 || 已发现 >= 2) return '恐慌';
  if (平均配合 >= 70 && 平均紧张 < 50) return '互相掩护';
  if (平均紧张 >= 50) return '争先恐后';
  return '各自为战';
}

function 检测群体动态事件(会话: 群体暴露会话): 群体动态事件 | null {
  const 行为 = 会话.群体行为;

  if (行为 === '恐慌') {
    const 未暴露 = 会话.参与者.filter(p => !p.是否被发现);
    if (未暴露.length > 0) {
      const 触发者 = 未暴露[Math.floor(Math.random() * 未暴露.length)];
      return {
        类型: '恐慌',
        触发者: 触发者.NPCID,
        影响对象: 未暴露.map(p => p.NPCID),
        描述: `${触发者.NPC名称} 惊慌失措，导致更多人暴露`,
      };
    }
  }

  if (行为 === '互相掩护') {
    const 未暴露 = 会话.参与者.filter(p => !p.是否被发现);
    if (未暴露.length >= 2) {
      return {
        类型: '掩护',
        触发者: 未暴露[0].NPCID,
        影响对象: 未暴露.slice(1).map(p => p.NPCID),
        描述: `${未暴露[0].NPC名称} 主动掩护其他人`,
      };
    }
  }

  const 领袖 = 会话.参与者.find(p => p.性格倾向 === '领袖' && !p.是否被发现);
  if (领袖 && Math.random() < 0.3) {
    return {
      类型: '带头',
      触发者: 领袖.NPCID,
      影响对象: 会话.参与者.filter(p => p.NPCID !== 领袖.NPCID).map(p => p.NPCID),
      描述: `${领袖.NPC名称} 带头行动`,
    };
  }

  return null;
}

export function 执行群体回合(
  会话: 群体暴露会话,
  策略: '分散行动' | '集体行动' | '原地等待' | '互相掩护',
  资源状态: NSFW资源状态
): 群体回合结果 {
  if (会话.当前回合 >= 会话.最大回合) {
    return { 新会话: 会话, 事件: null, 资源变化: 资源状态, 描述: '暴露回合结束' };
  }

  const 新参与者 = 会话.参与者.map(p => ({ ...p }));
  let 新资源 = { ...资源状态 };
  const 描述列表: string[] = [`第 ${会话.当前回合 + 1} 回合`];

  for (const 参与者 of 新参与者) {
    const 系数 = 性格系数[参与者.性格倾向];

    switch (策略) {
      case '分散行动':
        参与者.暴露风险 = Math.round(参与者.暴露风险 * 系数.风险倍率 * 0.7);
        参与者.紧张度 = Math.min(100, 参与者.紧张度 + Math.round(10 * 系数.紧张增速));
        break;
      case '集体行动':
        参与者.暴露风险 = Math.round(参与者.暴露风险 * 系数.风险倍率 * 1.3);
        参与者.紧张度 = Math.min(100, 参与者.紧张度 + Math.round(8 * 系数.紧张增速));
        参与者.配合度 = Math.min(100, 参与者.配合度 + 5);
        break;
      case '原地等待':
        参与者.紧张度 = Math.min(100, 参与者.紧张度 + 15);
        参与者.暴露风险 = Math.max(0, 参与者.暴露风险 - 5);
        break;
      case '互相掩护':
        if (参与者.配合度 >= 50) {
          参与者.暴露风险 = Math.round(参与者.暴露风险 * 0.8);
          参与者.配合度 = Math.min(100, 参与者.配合度 + 8);
        } else {
          参与者.暴露风险 = Math.round(参与者.暴露风险 * 1.1);
        }
        参与者.紧张度 = Math.min(100, 参与者.紧张度 + 5);
        break;
    }

    if (!参与者.是否被发现 && 参与者.暴露风险 > Math.random() * 100) {
      参与者.是否被发现 = true;
      描述列表.push(`${参与者.NPC名称} 被发现！`);
    }
  }

  会话.群体紧张度 = Math.round(
    新参与者.reduce((sum, p) => sum + p.紧张度, 0) / 新参与者.length
  );
  会话.被发现人数 = 新参与者.filter(p => p.是否被发现).length;
  会话.群体行为 = 计算群体行为({ ...会话, 参与者: 新参与者 });

  const 事件 = 检测群体动态事件({ ...会话, 参与者: 新参与者 });
  if (事件) {
    for (const id of 事件.影响对象) {
      const 参与者 = 新参与者.find(p => p.NPCID === id);
      if (参与者) {
        switch (事件.类型) {
          case '掩护':
            参与者.暴露风险 = Math.max(0, 参与者.暴露风险 - 10);
            break;
          case '恐慌':
            参与者.暴露风险 = Math.min(100, 参与者.暴露风险 + 15);
            break;
          case '带头':
            参与者.配合度 = Math.min(100, 参与者.配合度 + 10);
            break;
        }
      }
    }
    描述列表.push(事件.描述);
  }

  会话.参与者 = 新参与者;
  会话.当前回合++;

  if (事件 && 事件.类型 === '恐慌') {
    新资源 = 修改声誉(新资源, -会话.被发现人数 * 3, '群体暴露恐慌').新状态;
  }

  return { 新会话: 会话, 事件, 资源变化: 新资源, 描述: 描述列表.join('，') };
}

export function 结算群体暴露(
  会话: 群体暴露会话,
  资源状态: NSFW资源状态
): { 新资源状态: NSFW资源状态; 描述: string } {
  let 新资源 = { ...资源状态 };
  const 描述列表: string[] = ['群体暴露结算'];

  const 全员安全 = 会话.参与者.every(p => !p.是否被发现);
  const 半数暴露 = 会话.被发现人数 >= Math.ceil(会话.参与者.length / 2);

  if (全员安全) {
    新资源 = 修改声誉(新资源, 5, '全员安全撤离').新状态;
    描述列表.push('所有人安全撤离，声誉+5');
  } else if (半数暴露) {
    新资源 = 修改声誉(新资源, -15, '半数以上暴露').新状态;
    描述列表.push(`${会话.被发现人数}/${会话.参与者.length} 被发现，声誉-15`);
  } else if (会话.被发现人数 > 0) {
    新资源 = 修改声誉(新资源, -会话.被发现人数 * 5, '部分暴露').新状态;
    描述列表.push(`${会话.被发现人数} 人被发现，声誉-${会话.被发现人数 * 5}`);
  }

  return { 新资源状态: 新资源, 描述: 描述列表.join('，') };
}
