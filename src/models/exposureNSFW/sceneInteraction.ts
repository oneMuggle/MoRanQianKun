// ==================== 核心引擎函数 ====================

/**
 * 获取场景互动选项
 */
export function 获取场景互动选项(场景类型: string): 场景互动选项[] {
  return 场景互动模板[场景类型] ?? 场景互动模板.default;
}

/**
 * 执行场景互动
 */
export function 执行场景互动(
  选项Id: string,
  场景类型: string
): 场景互动结果 {
  const 选项 = 获取场景互动选项(场景类型).find(o => o.id === 选项Id);
  if (!选项) {
    return { 成功: false, 风险变化: 0, 紧张度变化: 0, 描述: '无效的互动选项' };
  }

  // 基于风险变化计算成功概率
  const 基础成功率 = 0.6;
  const 风险加成 = Math.abs(选项.风险变化) / 100;
  const 成功率 = Math.max(0.2, Math.min(0.9, 基础成功率 + 风险加成));

  const 成功 = Math.random() <= 成功率;
  const 描述列表 = 成功 ? 选项.成功描述 : 选项.失败描述;

  return {
    成功,
    风险变化: 成功 ? 选项.风险变化 : Math.abs(选项.风险变化) * 0.5,
    紧张度变化: 选项.紧张度变化,
    描述: 描述列表[Math.floor(Math.random() * 描述列表.length)],
  };
}

/**
 * 环境利用 — 利用场景特性降低风险
 */
export function 环境利用(
  利用方式: 环境利用,
  _场景: 露出场景模板,
  上下文: { 天气?: string; 时间段?: string }
): { 风险降低: number; 描述: string } {
  let 降低值 = 0;
  let 描述 = '';

  switch (利用方式) {
    case '寻找掩体':
      降低值 = 10 + Math.round(Math.random() * 10);
      描述 = '找到了合适的遮挡物';
      break;
    case '利用光线':
      if (上下文.时间段 === '夜晚' || 上下文.时间段 === '黄昏') {
        降低值 = 15 + Math.round(Math.random() * 10);
        描述 = '昏暗的光线提供了天然的掩护';
      } else {
        降低值 = 5;
        描述 = '光线条件一般，掩护效果有限';
      }
      break;
    case '借助噪音':
      降低值 = 8 + Math.round(Math.random() * 7);
      描述 = '环境噪音掩盖了声响';
      break;
    case '选择死角':
      降低值 = 12 + Math.round(Math.random() * 8);
      描述 = '视野死角降低了被发现的概率';
      break;
  }

  // 天气修正
  if (上下文.天气 === '雨天' && 利用方式 === '借助噪音') {
    降低值 += 5;
    描述 += '，雨声进一步掩盖了异常';
  }

  return { 风险降低: 降低值, 描述 };
}

/**
 * 道具系统 — 携带道具改变暴露风险
 */
export function 应用道具(
  道具: 道具类型,
  当前风险: number
): { 新风险: number; 效果: 道具效果; 描述: string } {
  const 效果 = 道具效果表[道具];
  const 新风险 = Math.max(0, 当前风险 - 效果.风险降低);

  return {
    新风险,
    效果,
    描述: 效果.描述,
  };
}

/**
 * 获取可用道具列表（基于场景合理性）
 */
export function 获取可用道具(场景类型: string, 天气?: string): 道具类型[] {
  const 基础道具: 道具类型[] = ['外套', '墨镜', '围巾'];

  if (天气 === '雨天' || 天气 === '雪天') {
    return [...基础道具, '雨伞'];
  }

  if (场景类型 === '派对' || 场景类型 === '晚会') {
    return [...基础道具, '面具'];
  }

  return 基础道具;
}
