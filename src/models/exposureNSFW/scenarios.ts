/** 所有时代场景汇总（key 使用游戏实际 eraId） */
const 全部场景: Record<string, 露出场景模板[]> = {
  // 古代东方
  'ancient_eastern_wuxia': 武侠时代场景,
  'ancient_eastern_xianxia': 修仙时代场景,
  'ancient_eastern_strange': 志怪时代场景,
  // 现代西方
  'modern_western_victorian': 维多利亚时代场景,
  // 当代
  'contemporary_urban': 校园时代场景,
  'contemporary_campus': 校园时代场景,
  'contemporary_campus_urban': 校园时代场景,
  'contemporary_rural': 校园时代场景,
  // 民国（谍战风格）
  'modern_eastern_republic': 谍战时代场景,
};

/** 时代别名映射：简化 ID → 实际 eraId */
const 时代别名: Record<string, string> = {
  wuxia: 'ancient_eastern_wuxia',
  xiuxian: 'ancient_eastern_xianxia',
  zhiguai: 'ancient_eastern_strange',
  victorian: 'modern_western_victorian',
  spywar: 'modern_eastern_republic',
  campus: 'contemporary_campus',
};

/**
 * 根据时代 ID 获取场景列表（支持别名映射）
 */
export function 获取时代场景(时代Id: string): 露出场景模板[] {
  // 先尝试直接匹配
  if (全部场景[时代Id]) return 全部场景[时代Id];
  // 再尝试别名映射
  const 实际EraId = 时代别名[时代Id];
  if (实际EraId && 全部场景[实际EraId]) return 全部场景[实际EraId];
  // 默认返回武侠场景作为 fallback
  return 全部场景['ancient_eastern_wuxia'] ?? [];
}

/**
 * 根据场景 ID 获取单个场景模板
 */
export function 获取场景ById(场景Id: string): 露出场景模板 | null {
  for (const 场景列表 of Object.values(全部场景)) {
    const 匹配 = 场景列表.find(s => s.id === 场景Id);
    if (匹配) return 匹配;
  }
  return null;
}

/**
 * 获取所有已注册的时代 ID
 */
export function 获取已注册时代(): string[] {
  return Object.keys(全部场景);
}

/**
 * 根据场所类型过滤场景
 */
export function 按场所类型过滤(
  时代Id: string,
  场所类型: '半私密' | '半公开' | '公开'
): 露出场景模板[] {
  return 获取时代场景(时代Id).filter(s => s.场所类型 === 场所类型);
}
