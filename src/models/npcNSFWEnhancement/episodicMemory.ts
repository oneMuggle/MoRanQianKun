/**
 * 情景记忆系统 — Episodic Memory
 * NPC 记住与玩家共同经历的具体情境（时间、地点、氛围、情感烙印）
 * 当当前情境与记忆相似时自动触发回忆
 */

// ==================== 情境标签 ====================

export type 情境维度 = '时间' | '地点' | '氛围' | '关系' | '行为';

export type 时间标签 = '清晨' | '午后' | '黄昏' | '夜晚' | '深夜' | '午夜';
export type 地点标签 = '室内' | '室外' | '私密' | '公共' | '自然' | '建筑';
export type 氛围标签 = '温馨' | '紧张' | '暧昧' | '激烈' | '羞涩' | '放纵';
export type 关系标签 = '初次' | '熟悉' | '亲密' | '依赖' | '冲突' | '和解';
export type 行为标签 = '约会' | '谈心' | '肢体接触' | '亲密行为' | '突破界限' | '事后护理';

export interface 情境标签组 {
  时间: 时间标签[];
  地点: 地点标签[];
  氛围: 氛围标签[];
  关系: 关系标签[];
  行为: 行为标签[];
}

// ==================== 情感烙印 ====================

export interface 情感烙印 {
  情感类型: string;
  强度: number;
  持久度: number;
}

// ==================== 情景记忆条目 ====================

export interface 情景记忆条目 {
  id: string;
  标题: string;
  简述: string;
  详细描述: string;
  发生时间: string;
  情境标签: 情境标签组;
  情感烙印: 情感烙印[];
  重要度: number;
  回忆次数: number;
  最后回忆时间?: string;
  衰减率: number;
  当前强度: number;
}

// ==================== 情景记忆库 ====================

export interface 情景记忆库 {
  记忆列表: 情景记忆条目[];
  最大容量: number;
  最后更新时间: string;
}

// ==================== 核心函数 ====================

export function 创建情景记忆库(最大容量: number = 20): 情景记忆库 {
  return { 记忆列表: [], 最大容量, 最后更新时间: new Date().toISOString() };
}

export function 添加情景记忆(
  记忆库: 情景记忆库,
  新记忆: Omit<情景记忆条目, 'id' | '回忆次数' | '衰减率' | '当前强度'>
): 情景记忆库 {
  const 完整记忆: 情景记忆条目 = {
    ...新记忆,
    id: `ep_${Date.now()}_${Math.random().toString(36).substring(2, 8)}`,
    回忆次数: 0,
    衰减率: 0.1,
    当前强度: 100,
  };

  const 新列表 = [...记忆库.记忆列表, 完整记忆];
  if (新列表.length > 记忆库.最大容量) {
    新列表.sort((a, b) => {
      const a得分 = a.当前强度 * 0.4 + a.重要度 * 10 + a.回忆次数 * 5;
      const b得分 = b.当前强度 * 0.4 + b.重要度 * 10 + b.回忆次数 * 5;
      return a得分 - b得分;
    });
    新列表.shift();
  }

  return { ...记忆库, 记忆列表: 新列表, 最后更新时间: new Date().toISOString() };
}

function 计算情境匹配度(记忆: 情景记忆条目, 当前情境: 情境标签组): number {
  let 匹配度 = 0;
  let 总标签数 = 0;
  const 维度: (keyof 情境标签组)[] = ['时间', '地点', '氛围', '关系', '行为'];
  for (const 维度名 of 维度) {
    const 记忆标签 = 记忆.情境标签[维度名];
    const 当前标签 = 当前情境[维度名];
    总标签数 += 当前标签.length;
    for (const 标签 of 当前标签) {
      if ((记忆标签 as string[]).includes(标签)) 匹配度 += 1;
    }
  }
  return 总标签数 > 0 ? 匹配度 / 总标签数 : 0;
}

export function 情境匹配检索(
  记忆库: 情景记忆库,
  当前情境: Partial<情境标签组>,
  最大条目数: number = 3
): { 记忆: 情景记忆条目; 匹配度: number }[] {
  const 完整情境: 情境标签组 = {
    时间: 当前情境.时间 ?? [],
    地点: 当前情境.地点 ?? [],
    氛围: 当前情境.氛围 ?? [],
    关系: 当前情境.关系 ?? [],
    行为: 当前情境.行为 ?? [],
  };

  return 记忆库.记忆列表
    .filter(m => m.当前强度 > 10)
    .map(记忆 => ({ 记忆, 匹配度: 计算情境匹配度(记忆, 完整情境) }))
    .filter(r => r.匹配度 > 0)
    .sort((a, b) => {
      const a分 = a.匹配度 * 0.6 + (a.记忆.当前强度 / 100) * 0.25 + (a.记忆.重要度 / 10) * 0.15;
      const b分 = b.匹配度 * 0.6 + (b.记忆.当前强度 / 100) * 0.25 + (b.记忆.重要度 / 10) * 0.15;
      return b分 - a分;
    })
    .slice(0, 最大条目数);
}

export function 衰减情景记忆(记忆库: 情景记忆库, 回合数: number = 1): 情景记忆库 {
  const 新列表 = 记忆库.记忆列表
    .map(记忆 => {
      const 平均持久度 = 记忆.情感烙印.length > 0
        ? 记忆.情感烙印.reduce((sum, e) => sum + e.持久度, 0) / 记忆.情感烙印.length
        : 50;
      return {
        ...记忆,
        当前强度: Math.max(0, 记忆.当前强度 - 记忆.衰减率 * 回合数 * (1 - 平均持久度 / 200)),
      };
    })
    .filter(m => m.当前强度 > 5);

  return { ...记忆库, 记忆列表: 新列表, 最后更新时间: new Date().toISOString() };
}

export function 强化记忆(记忆库: 情景记忆库, 记忆Id: string): 情景记忆库 {
  return {
    ...记忆库,
    记忆列表: 记忆库.记忆列表.map(记忆 =>
      记忆.id !== 记忆Id ? 记忆 : {
        ...记忆,
        当前强度: Math.min(100, 记忆.当前强度 + 15),
        回忆次数: 记忆.回忆次数 + 1,
        最后回忆时间: new Date().toISOString(),
      }
    ),
  };
}

export function 生成情景记忆摘要(
  记忆库: 情景记忆库,
  当前情境: Partial<情境标签组>,
  最大条目数: number = 2
): string {
  const 匹配结果 = 情境匹配检索(记忆库, 当前情境, 最大条目数);
  if (匹配结果.length === 0) return '无相关情景记忆';

  return '情景记忆：' + 匹配结果.map(({ 记忆, 匹配度 }) => {
    const 强度标记 = 记忆.当前强度 > 70 ? '深刻' : 记忆.当前强度 > 40 ? '模糊' : '隐约';
    return `[${强度标记}]${记忆.标题}：${记忆.简述}（${Math.round(匹配度 * 100)}%匹配）`;
  }).join('；');
}

export function 快速创建情景记忆(
  标题: string,
  简述: string,
  详细描述: string,
  情境: Partial<情境标签组>,
  情感: { 类型: string; 强度: number; 持久度: number }[],
  重要度: number = 5
): Omit<情景记忆条目, 'id' | '回忆次数' | '衰减率' | '当前强度'> {
  return {
    标题, 简述, 详细描述,
    发生时间: new Date().toISOString(),
    情境标签: {
      时间: 情境.时间 ?? [],
      地点: 情境.地点 ?? [],
      氛围: 情境.氛围 ?? [],
      关系: 情境.关系 ?? [],
      行为: 情境.行为 ?? [],
    },
    情感烙印: 情感.map(e => ({ 情感类型: e.类型, 强度: e.强度, 持久度: e.持久度 })),
    重要度,
  };
}
