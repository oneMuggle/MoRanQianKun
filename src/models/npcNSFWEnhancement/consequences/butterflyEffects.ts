export function 检测蝴蝶效应(
  状态: 后果系统状态,
  事件类型: string,
  严重程度: number,
  游戏时间: string
): 蝴蝶效应[] {
  const 新效应: 蝴蝶效应[] = [];

  for (const 规则 of 蝴蝶效应规则库) {
    if (事件类型 !== 规则.触发事件) continue;
    if (严重程度 < 规则.最小严重程度) continue;
    const 已存在 = 状态.蝴蝶效应.some(e => e.原始事件 === 事件类型 && !e.已触发);
    if (已存在) continue;

    const 效应: 蝴蝶效应 = {
      id: 生成ID(),
      原始事件: 事件类型,
      描述: 规则.描述模板,
      影响范围: 规则.影响范围,
      激活条件: 规则.激活条件,
      已触发: false,
      创建时间: 游戏时间,
      关联后果: [],
    };

    状态.蝴蝶效应.push(效应);
    新效应.push(效应);
  }

  状态.最后更新时间 = 游戏时间;
  return 新效应;
}

export function 检查蝴蝶效应触发(
  状态: 后果系统状态,
  游戏时间: string,
  当前回合: number
): 蝴蝶效应[] {
  const 已触发: 蝴蝶效应[] = [];

  for (const 效应 of 状态.蝴蝶效应) {
    if (效应.已触发) continue;
    const 规则 = 蝴蝶效应规则库.find(r => r.触发事件 === 效应.原始事件);
    if (!规则) continue;

    const 创建回合 = 解析回合(效应.创建时间);
    if (当前回合 - 创建回合 >= 规则.延迟回合) {
      效应.已触发 = true;
      效应.触发时间 = 游戏时间;
      已触发.push(效应);
    }
  }

  return 已触发;
}

export function 蝴蝶效应转后果(
  状态: 后果系统状态,
  效应: 蝴蝶效应,
  游戏时间: string,
  涉及NPC: string[]
): 后果条目 | null {
  if (!效应.已触发) return null;

  const 规则 = 蝴蝶效应规则库.find(r => r.触发事件 === 效应.原始事件);
  if (!规则) return null;

  const 后果: 后果条目 = {
    id: 生成ID(),
    类型: '关系',
    描述: 效应.描述,
    严重程度: 规则.影响范围 === '全局' ? '严重' : '中等',
    持续时间: '中期',
    生效时间: 游戏时间,
    剩余回合: 15,
    涉及NPC,
    来源事件: `蝴蝶效应: ${效应.原始事件}`,
  };

  状态.后果列表.push(后果);
  效应.关联后果.push(后果.id);
  return 后果;
}

export function 获取活跃蝴蝶效应(状态: 后果系统状态): 蝴蝶效应[] {
  return 状态.蝴蝶效应.filter(e => !e.已触发);
}

export function 生成蝴蝶效应摘要(状态: 后果系统状态): string | null {
  const 活跃 = 获取活跃蝴蝶效应(状态);
  if (活跃.length === 0) return null;

  const 文本 = 活跃
    .map(e => `${e.描述}（范围：${e.影响范围}）`)
    .join('；');

  return `潜在蝴蝶效应：${文本}`;
}

function 解析回合(时间串: string): number {
  const parts = 时间串.split(':');
  if (parts.length < 5) return 0;
  const 数字 = parts.map(Number);
  if (数字.some(n => isNaN(n))) return 0;
  const [年, 月, 日, 时, 分] = 数字;
  return (((年 * 12 + 月) * 30 + 日) * 24 + 时) * 2 + Math.floor(分 / 30);
}
