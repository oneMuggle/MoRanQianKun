// ==================== 纯函数 ====================

/**
 * 达成里程碑
 */
export function 达成里程碑(
  当前羁绊树: 情感羁绊树,
  里程碑Id: string,
  备注?: string
): 情感羁绊树 {
  const 里程碑 = 预定义里程碑[里程碑Id];
  if (!里程碑) return 当前羁绊树;

  if (当前羁绊树.已达成里程碑.some(m => m.里程碑Id === 里程碑Id)) {
    return 当前羁绊树;
  }

  const 新已达成 = [
    ...当前羁绊树.已达成里程碑,
    { 里程碑Id, 达成时间: new Date().toISOString(), 备注 },
  ];

  const 新羁绊值 = clamp(当前羁绊树.羁绊值 + 里程碑.羁绊值变化, 0, 100);

  const 新结局倾向 = { ...当前羁绊树.结局倾向 };
  for (const [结局, 变化] of Object.entries(里程碑.结局倾向变化)) {
    新结局倾向[结局 as 关系结局] = clamp(
      (新结局倾向[结局 as 关系结局] ?? 0) + 变化,
      0,
      100
    );
  }

  return {
    ...当前羁绊树,
    已达成里程碑: 新已达成,
    羁绊值: 新羁绊值,
    结局倾向: 新结局倾向,
    最后更新时间: new Date().toISOString(),
  };
}

/**
 * 获取当前主导结局
 */
export function 获取主导结局(羁绊树: 情感羁绊树): 关系结局 {
  let 最高结局: 关系结局 = '陌路';
  let 最高值 = 0;

  for (const [结局, 值] of Object.entries(羁绊树.结局倾向)) {
    if (值 > 最高值) {
      最高值 = 值;
      最高结局 = 结局 as 关系结局;
    }
  }

  return 最高结局;
}

/**
 * 羁绊值自然衰减
 */
export function 计算羁绊衰减(当前羁绊树: 情感羁绊树): 情感羁绊树 {
  const 衰减值 = 1;
  const 新羁绊值 = Math.max(0, 当前羁绊树.羁绊值 - 衰减值);

  const 新结局倾向 = { ...当前羁绊树.结局倾向 };
  for (const 结局 of Object.keys(新结局倾向)) {
    新结局倾向[结局 as 关系结局] = Math.max(
      0,
      (新结局倾向[结局 as 关系结局] ?? 0) - 0.5
    );
  }

  return {
    ...当前羁绊树,
    羁绊值: 新羁绊值,
    结局倾向: 新结局倾向,
    最后更新时间: new Date().toISOString(),
  };
}

