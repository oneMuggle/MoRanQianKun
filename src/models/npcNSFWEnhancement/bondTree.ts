// ==================== 情感羁绊 ====================

export type 关系结局 = '恋人' | '炮友' | '主从' | '陌路' | '知己' | '挚友';

export type 里程碑类别 =
  | '首次'
  | '关系进展'
  | 'NSFW突破'
  | '情感深化'
  | '特殊事件'
  | '日常温馨';

export type 里程碑稀有度 = '普通' | '稀有' | '史诗' | '传说';

export interface 情感里程碑 {
  id: string;
  名称: string;
  类别: 里程碑类别;
  稀有度: 里程碑稀有度;
  描述: string;
  达成时间?: string;
  羁绊值变化: number;
  结局倾向变化: Record<关系结局, number>;
  解锁内容?: string[];
}

export interface 已达成里程碑 {
  里程碑Id: string;
  达成时间: string;
  备注?: string;
}

// ==================== 羁绊树状态 ====================

export interface 情感羁绊树 {
  已达成里程碑: 已达成里程碑[];
  当前分支: 剧情分支 | null;
  结局倾向: Record<关系结局, number>;
  羁绊值: number;  // 0-100
  最后更新时间: string;
}

export interface 剧情分支 {
  id: string;
  名称: string;
  描述: string;
  开启时间: string;
  当前进度: number;  // 0-100
  选项记录: 分支选项[];
}

export interface 分支选项 {
  选项描述: string;
  选择时间: string;
  影响: {
    羁绊值变化: number;
    结局倾向变化?: Record<关系结局, number>;
  };
}

