// ==================== 核心函数 ====================

export function 创建初始日常状态(): 日常行为状态 {
  const 初始权重: Record<string, number> = {};
  for (const 模板 of 默认行为模板) {
    初始权重[模板.id] = 模板.权重;
  }

  return {
    今日行为: [],
    偏好权重: 初始权重,
    行为倾向: '正常',
    最后更新日期: new Date().toISOString(),
  };
}

export function 选择日常行为(
  当前时间段: '清晨' | '上午' | '午后' | '黄昏' | '夜晚' | '深夜',
  性格标签: string[] = [],
  当前心情: number = 50,
  日常状态: 日常行为状态,
  已执行行为Ids: string[] = []
): 日常行为模板 | null {
  const 候选 = 默认行为模板.filter(t =>
    t.时间段.includes(当前时间段) &&
    !已执行行为Ids.includes(t.id)
  );

  if (候选.length === 0) return null;

  const 得分列表 = 候选.map(模板 => {
    let 得分 = 日常状态.偏好权重[模板.id] ?? 模板.权重;

    const 匹配标签 = 模板.性格标签偏好.filter(t =>
      性格标签.some(c => c.includes(t) || t.includes(c))
    );
    得分 += 匹配标签.length * 0.3;

    if (当前心情 < 30 && 模板.心情影响 > 0) 得分 += 0.5;
    if (当前心情 > 80 && 模板.类别 === '社交') 得分 += 0.3;

    switch (日常状态.行为倾向) {
      case '活跃':
        if (模板.体力消耗 > 15) 得分 += 0.3;
        break;
      case '消极':
        if (模板.体力消耗 < 10) 得分 += 0.3;
        break;
      case '自闭':
        if (模板.类别 === '独处' || 模板.类别 === '特殊') 得分 += 0.5;
        if (模板.类别 === '社交') 得分 -= 0.5;
        break;
    }

    return { 模板, 得分 };
  });

  const 总得分 = 得分列表.reduce((s, d) => s + Math.max(0, d.得分), 0);
  if (总得分 <= 0) return 候选[0] ?? null;

  let 随机值 = Math.random() * 总得分;
  for (const { 模板, 得分 } of 得分列表) {
    随机值 -= Math.max(0, 得分);
    if (随机值 <= 0) return 模板;
  }

  return 得分列表[得分列表.length - 1].模板;
}

export function 记录日常行为(
  状态: 日常行为状态,
  行为: 日常行为模板,
  地点: string,
  完成度: number = 100,
  被打断: boolean = false
): 日常行为状态 {
  const 新记录: 日常行为记录 = {
    行为Id: 行为.id,
    行为名称: 行为.名称,
    执行时间: new Date().toISOString(),
    地点,
    完成度,
    被打断,
  };

  const 新权重 = { ...状态.偏好权重 };
  const 当前权重 = 新权重[行为.id] ?? 行为.权重;
  新权重[行为.id] = 当前权重 + (完成度 / 100) * 0.1 - (被打断 ? 0.05 : 0);

  const 新倾向 = 计算行为倾向(状态.今日行为, 新记录);

  return {
    ...状态,
    今日行为: [...状态.今日行为, 新记录].slice(-10),
    偏好权重: 新权重,
    行为倾向: 新倾向,
    最后更新日期: new Date().toISOString(),
  };
}

function 计算行为倾向(
  历史记录: 日常行为记录[],
  新记录: 日常行为记录
): 日常行为倾向 {
  const 全部记录 = [...历史记录, 新记录].slice(-5);
  const 平均完成度 = 全部记录.reduce((s, r) => s + r.完成度, 0) / 全部记录.length;
  const 打断率 = 全部记录.filter(r => r.被打断).length / 全部记录.length;

  if (打断率 > 0.6) return '自闭';
  if (平均完成度 < 30) return '消极';
  if (平均完成度 > 80 && 打断率 < 0.1) return '活跃';
  return '正常';
}

export function 生成日常行为提示(日常状态: 日常行为状态): string {
  if (日常状态.今日行为.length === 0) return '';

  const 最近行为 = 日常状态.今日行为[日常状态.今日行为.length - 1];
  const 组件: string[] = [];

  组件.push(`【日常活动】${最近行为.行为名称}（完成度${最近行为.完成度}%）`);

  if (最近行为.被打断) {
    组件.push('注意：该NPC的日常活动被打断，可能心情受到轻微影响。');
  }

  if (日常状态.行为倾向 !== '正常') {
    const 倾向描述 = {
      '活跃': '精力充沛，倾向高强度活动',
      '消极': '缺乏动力，倾向低消耗活动',
      '自闭': '回避社交，倾向独处',
    };
    组件.push(`行为倾向：${日常状态.行为倾向}（${倾向描述[日常状态.行为倾向]}）`);
  }

  return 组件.join('\n');
}

export function 获取今日行为Ids(日常状态: 日常行为状态): string[] {
  return 日常状态.今日行为.map(r => r.行为Id);
}
