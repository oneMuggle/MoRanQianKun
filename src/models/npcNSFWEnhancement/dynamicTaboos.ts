export function 计算动态禁忌(
  性癖档案: 性癖档案,
  心理防线: number,
  羁绊值: number,
  历史事件: string[] = []
): 动态禁忌 {
  const 软化原因: Record<string, string> = {};
  const 当前禁忌 = [...性癖档案.绝对禁忌];
  const 软化禁忌: string[] = [];
  const 可尝试: string[] = [];

  for (const 禁忌 of 当前禁忌) {
    const 匹配规则 = 默认软化规则.find(r => 禁忌.includes(r.禁忌关键词));
    if (!匹配规则) continue;

    const 防线达标 = 心理防线 <= 匹配规则.防线阈值;
    const 羁绊达标 = 羁绊值 >= 匹配规则.羁绊阈值;
    const 事件满足 = !匹配规则.需要历史事件 ||
      匹配规则.需要历史事件.every(e => 历史事件.includes(e));

    if (防线达标 && 羁绊达标 && 事件满足) {
      可尝试.push(禁忌);
      软化原因[禁忌] = `心理防线(${心理防线})低于${匹配规则.防线阈值}，羁绊值(${羁绊值})高于${匹配规则.羁绊阈值}`;
    } else if (防线达标 || 羁绊达标) {
      软化禁忌.push(禁忌);
      软化原因[禁忌] = 防线达标
        ? `心理防线(${心理防线})较低，但羁绊值(${羁绊值})未达${匹配规则.羁绊阈值}`
        : `羁绊值(${羁绊值})足够，但心理防线(${心理防线})仍高于${匹配规则.防线阈值}`;
    }
  }

  const 剩余禁忌 = 当前禁忌.filter(t => !可尝试.includes(t));

  return { 当前禁忌: 剩余禁忌, 软化禁忌, 可尝试, 软化原因 };
}

export function 更新禁忌列表(
  性癖档案: 性癖档案,
  心理防线: number,
  羁绊值: number,
  历史事件: string[] = []
): 性癖档案 {
  const 动态 = 计算动态禁忌(性癖档案, 心理防线, 羁绊值, 历史事件);

  const 新增可协商 = 动态.可尝试.filter(t =>
    !性癖档案.可协商.includes(t) && !性癖档案.绝对禁忌.includes(t)
  );

  if (新增可协商.length === 0) return 性癖档案;

  return {
    ...性癖档案,
    绝对禁忌: 动态.当前禁忌,
    可协商: [...性癖档案.可协商, ...新增可协商],
    倾向摘要: 生成禁忌摘要(动态),
  };
}

export function 生成禁忌摘要(动态: 动态禁忌): string {
  const 组件: string[] = [];

  if (动态.当前禁忌.length > 0) {
    组件.push(`绝对禁忌：${动态.当前禁忌.join('、')}`);
  }

  if (动态.软化禁忌.length > 0) {
    组件.push(`注意：以下禁忌正在软化中，随着关系深入可能逐渐接受：${动态.软化禁忌.slice(0, 3).join('、')}`);
  }

  if (动态.可尝试.length > 0) {
    组件.push(`可协商：${动态.可尝试.join('、')}（当前心理防线较低且羁绊值足够，这些禁忌已可以逐步尝试）`);
  }

  return 组件.join('\n');
}

export function 预测禁忌软化(
  禁忌: string,
  当前心理防线: number,
  当前羁绊值: number,
  历史事件: string[] = []
): { 可能软化: boolean; 距离: string } | null {
  const 匹配规则 = 默认软化规则.find(r => 禁忌.includes(r.禁忌关键词));
  if (!匹配规则) return null;

  const 防线差 = 当前心理防线 - 匹配规则.防线阈值;
  const 羁绊差 = 匹配规则.羁绊阈值 - 当前羁绊值;
  const 事件缺 = 匹配规则.需要历史事件?.filter(e => !历史事件.includes(e)) ?? [];

  if (防线差 <= 0 && 羁绊差 <= 0 && 事件缺.length === 0) {
    return { 可能软化: true, 距离: '已满足所有软化条件' };
  }

  const 距离描述: string[] = [];
  if (防线差 > 0) 距离描述.push(`心理防线还需降低${防线差}`);
  if (羁绊差 > 0) 距离描述.push(`羁绊值还需提升${羁绊差}`);
  if (事件缺.length > 0) 距离描述.push(`需先体验：${事件缺.join('、')}`);

  return { 可能软化: false, 距离: 距离描述.join('，') };
}
