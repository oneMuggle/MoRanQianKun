import type { 性癖档案 } from './types';

export interface 动态禁忌 {
  当前禁忌: string[];
  软化禁忌: string[];
  可尝试: string[];
  软化原因: Record<string, string>;
}

interface 软化规则 {
  禁忌关键词: string;
  防线阈值: number;
  羁绊阈值: number;
  需要历史事件?: string[];
  软化后标签: string;
}

