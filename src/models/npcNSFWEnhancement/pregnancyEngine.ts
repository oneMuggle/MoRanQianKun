import { NPC结构 } from '../social';
import type { 孕产变化日志, 妊娠阶段, 孕产演化状态 } from './types';

// ==================== 常量配置 ====================

const 妊娠月份上限 = 9;
const 默认受孕率 = 30;
const 妊娠期分钟数 = 90 * 24 * 60;
const 产后恢复回合数 = 5;

// ==================== 初始化 ====================

export function 初始化孕产状态(npc: NPC结构): 孕产演化状态 | null {
  if (npc.性别 !== '女' || !npc.子宫) return null;
  if (!npc.完整演化状态) return null;
  if (!npc.完整演化状态.孕产演化) {
    npc.完整演化状态.孕产演化 = {
      当前阶段: '未受孕',
      变化日志: [],
    };
  }
  return npc.完整演化状态.孕产演化;
}

// ==================== 核心操作 ====================

export function 记录内射(
  npc: NPC结构,
  描述: string,
  游戏时间: string
): void {
  if (!npc.子宫) return;

  const 判定日 = 计算怀孕判定日(游戏时间);
  npc.子宫.内射记录.push({
    日期: 游戏时间,
    描述,
    怀孕判定日: 判定日,
  });
}

function 计算怀孕判定日(当前时间: string): string {
  const 分钟 = 解析游戏时间到分钟(当前时间);
  if (分钟 === 0) return 当前时间;
  const 判定分钟 = 分钟 + 4320; // 3天后
  return 分钟到游戏时间(判定分钟);
}

export function 检查受孕判定(
  npc: NPC结构,
  游戏时间: string,
  启用孕产系统: boolean = false
): { 有变化: boolean; 变化日志: 孕产变化日志[] } {
  if (!启用孕产系统) return { 有变化: false, 变化日志: [] };

  const 孕产状态 = 初始化孕产状态(npc);
  if (!孕产状态 || !npc.子宫) return { 有变化: false, 变化日志: [] };
  if (孕产状态.当前阶段 !== '未受孕') return { 有变化: false, 变化日志: [] };

  const 当前分钟 = 解析游戏时间到分钟(游戏时间);
  const 待判定记录 = npc.子宫.内射记录.filter(r => {
    const 判定分钟 = 解析游戏时间到分钟(r.怀孕判定日);
    return 判定分钟 > 0 && 当前分钟 >= 判定分钟;
  });

  if (待判定记录.length === 0) return { 有变化: false, 变化日志: [] };

  const 日志: 孕产变化日志[] = [];

  for (const 记录 of 待判定记录) {
    const 受孕率 = 孕产状态.受孕率 ?? 默认受孕率;
    const 种子 = 解析游戏时间到分钟(游戏时间) % 100;
    const 受孕成功 = 种子 < 受孕率;

    if (受孕成功) {
      const 旧阶段 = 孕产状态.当前阶段;
      孕产状态.当前阶段 = '妊娠一月';
      孕产状态.妊娠开始时间 = 游戏时间;
      孕产状态.预产期 = 分钟到游戏时间(当前分钟 + 妊娠期分钟数);
      npc.子宫.状态 = '妊娠一月';

      日志.push({
        时间: 游戏时间,
        变化类型: '受孕成功',
        旧阶段,
        新阶段: '妊娠一月',
        触发原因: `内射记录受孕判定（概率${受孕率}%）`,
        备注: 记录.描述,
      });
    } else {
      日志.push({
        时间: 游戏时间,
        变化类型: '受孕失败',
        旧阶段: 孕产状态.当前阶段,
        新阶段: '未受孕',
        触发原因: '内射记录受孕判定未通过',
      });
    }
  }

  npc.子宫.内射记录 = npc.子宫.内射记录.filter(r => {
    const 判定分钟 = 解析游戏时间到分钟(r.怀孕判定日);
    return 判定分钟 > 当前分钟;
  });

  if (日志.length > 0) {
    孕产状态.变化日志.push(...日志.slice(-10));
  }

  return { 有变化: 日志.length > 0, 变化日志: 日志 };
}

export function 推进妊娠进程(
  npc: NPC结构,
  游戏时间: string,
  启用孕产系统: boolean = false
): { 有变化: boolean; 变化日志: 孕产变化日志[] } {
  if (!启用孕产系统) return { 有变化: false, 变化日志: [] };

  const 孕产状态 = 初始化孕产状态(npc);
  if (!孕产状态 || !npc.子宫) return { 有变化: false, 变化日志: [] };

  const 当前阶段 = 孕产状态.当前阶段;
  const 妊娠月份 = 获取妊娠月份(当前阶段);
  if (妊娠月份 === 0 || 妊娠月份 >= 妊娠月份上限) return { 有变化: false, 变化日志: [] };

  const 预产期分钟 = 孕产状态.预产期 ? 解析游戏时间到分钟(孕产状态.预产期) : 0;
  const 当前分钟 = 解析游戏时间到分钟(游戏时间);
  if (预产期分钟 === 0 || 当前分钟 <= 0) return { 有变化: false, 变化日志: [] };

  const 已过分钟 = 预产期分钟 - 当前分钟;
  const 每月份分钟 = 妊娠期分钟数 / 妊娠月份上限;
  const 应达月份 = Math.min(妊娠月份上限, Math.floor((妊娠期分钟数 - 已过分钟) / 每月份分钟) + 1);

  if (应达月份 > 妊娠月份) {
    const 日志: 孕产变化日志[] = [];

    for (let 月 = 妊娠月份 + 1; 月 <= 应达月份; 月++) {
      const 新阶段 = 月份到妊娠阶段(月);
      const 旧阶段 = 月 === 妊娠月份 + 1 ? 当前阶段 : 月份到妊娠阶段(月 - 1);
      孕产状态.当前阶段 = 新阶段;
      npc.子宫.状态 = 新阶段;

      日志.push({
        时间: 游戏时间,
        变化类型: '妊娠推进',
        旧阶段,
        新阶段,
        触发原因: `妊娠自然推进至${新阶段}`,
      });

      if (月 === 妊娠月份上限) {
        孕产状态.当前阶段 = '分娩中';
        npc.子宫.状态 = '分娩中';
        日志.push({
          时间: 游戏时间,
          变化类型: '分娩完成',
          旧阶段: '妊娠九月',
          新阶段: '分娩中',
          触发原因: '妊娠期满，分娩触发',
        });

        孕产状态.当前阶段 = '产后恢复';
        npc.子宫.状态 = '产后恢复';
        npc.子宫.宫口状态 = '松弛';
        日志.push({
          时间: 游戏时间,
          变化类型: '产后恢复',
          旧阶段: '分娩中',
          新阶段: '产后恢复',
          触发原因: '分娩完成，进入产后恢复期',
        });
      }
    }

    if (日志.length > 0) {
      孕产状态.变化日志.push(...日志.slice(-10));
    }

    return { 有变化: true, 变化日志: 日志 };
  }

  return { 有变化: false, 变化日志: [] };
}

export function 应用产后恢复(
  npc: NPC结构,
  当前游戏时间: string,
  回合数: number,
  启用孕产系统: boolean = false
): { 有变化: boolean; 变化日志: 孕产变化日志[] } {
  if (!启用孕产系统) return { 有变化: false, 变化日志: [] };

  const 孕产状态 = 初始化孕产状态(npc);
  if (!孕产状态 || !npc.子宫) return { 有变化: false, 变化日志: [] };
  if (孕产状态.当前阶段 !== '产后恢复') return { 有变化: false, 变化日志: [] };

  if (回合数 >= 产后恢复回合数) {
    const 旧阶段 = 孕产状态.当前阶段;
    孕产状态.当前阶段 = '未受孕';
    npc.子宫.状态 = '未受孕';
    npc.子宫.宫口状态 = '紧致';

    const 日志: 孕产变化日志 = {
      时间: 当前游戏时间,
      变化类型: '产后恢复',
      旧阶段,
      新阶段: '未受孕',
      触发原因: `产后恢复完成（${回合数}回合）`,
    };

    孕产状态.变化日志.push(日志);
    return { 有变化: true, 变化日志: [日志] };
  }

  return { 有变化: false, 变化日志: [] };
}

// ==================== 辅助函数 ====================

function 获取妊娠阶段索引(阶段: 妊娠阶段): number {
  const 索引表: Record<妊娠阶段, number> = {
    '未受孕': 0, '受孕判定中': 0,
    '妊娠一月': 1, '妊娠二月': 2, '妊娠三月': 3,
    '妊娠四月': 4, '妊娠五月': 5, '妊娠六月': 6,
    '妊娠七月': 7, '妊娠八月': 8, '妊娠九月': 9,
    '分娩中': 10, '产后恢复': 11,
  };
  return 索引表[阶段] ?? 0;
}

function 获取妊娠月份(阶段: 妊娠阶段): number {
  return 获取妊娠阶段索引(阶段);
}

function 月份到妊娠阶段(月份: number): 妊娠阶段 {
  const 映射: Record<number, 妊娠阶段> = {
    1: '妊娠一月', 2: '妊娠二月', 3: '妊娠三月',
    4: '妊娠四月', 5: '妊娠五月', 6: '妊娠六月',
    7: '妊娠七月', 8: '妊娠八月', 9: '妊娠九月',
  };
  return 映射[月份] ?? '未受孕';
}

function 解析游戏时间到分钟(时间串: string): number {
  const parts = 时间串.split(':');
  if (parts.length < 5) return 0;
  const 数字部分 = parts.map(Number);
  if (数字部分.some(n => isNaN(n))) return 0;
  const [年, 月, 日, 时, 分] = 数字部分;
  return ((((年 * 12 + 月) * 30 + 日) * 24 + 时) * 60 + 分);
}

function 分钟到游戏时间(总分钟: number): string {
  const 分 = 总分钟 % 60;
  const 总小时 = Math.floor(总分钟 / 60);
  const 时 = 总小时 % 24;
  const 总日 = Math.floor(总小时 / 24);
  const 日 = 总日 % 30;
  const 总月 = Math.floor(总日 / 30);
  const 月 = 总月 % 12;
  const 年 = Math.floor(总月 / 12);
  return `${年}:${月}:${日}:${时}:${分}`;
}
