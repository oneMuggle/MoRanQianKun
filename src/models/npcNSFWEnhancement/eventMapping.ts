/**
 * 获取事件映射配置
 */
export function 获取事件映射(事件: 性癖触发事件): 事件映射配置 | null {
  return 事件性癖映射[事件] ?? null;
}

/**
 * 获取所有可用事件列表（供 prompt 注入使用）
 */
export function 获取所有触发事件(): 性癖触发事件[] {
  return Object.keys(事件性癖映射) as 性癖触发事件[];
}

/**
 * 生成触发事件列表文本（用于 prompt 注入）
 */
export function 生成触发事件列表(): string {
  const 事件列表 = 获取所有触发事件();
  const 映射摘要 = 事件列表.map(e => {
    const m = 事件性癖映射[e];
    return `${e}→${m.触发类别}(${m.触发子类型.join('/')}, 累积${m.累积阈值}次)`;
  });
  return 映射摘要.join('；');
}
