/**
 * NPC NSFW 增强模块 — 偏好漂移算法
 * 基于滑动窗口的事件频率分析，检测性癖偏好漂移趋势
 */

import { NPC结构 } from '../social';
import type { 性癖大类, 完整演化状态 } from './types';
import type { 性癖触发事件 } from './eventMapping';
import { 获取事件映射 } from './eventMapping';

const 漂移窗口大小 = 20;
const 漂移认定阈值 = 0.6;

/**
 * 初始化偏好漂移状态
 */
export function 初始化偏好漂移(npc: NPC结构): void {
  const 完整状态 = ensure完整状态(npc);
  if (完整状态.偏好漂移) return;

  完整状态.偏好漂移 = {
    事件频率统计: {},
    最近事件序列: [],
    漂移方向: null,
    漂移强度: 0,
    最后计算时间: '',
  };
}

/**
 * 记录事件用于漂移分析
 */
export function 记录漂移事件(
  npc: NPC结构,
  事件: 性癖触发事件,
  _游戏时间: string
): void {
  const 完整状态 = ensure完整状态(npc);
  if (!完整状态.偏好漂移) {
    初始化偏好漂移(npc);
  }

  const 漂移 = 完整状态.偏好漂移!;

  // 更新频率统计
  漂移.事件频率统计[事件] = (漂移.事件频率统计[事件] || 0) + 1;

  // 推入滑动窗口
  漂移.最近事件序列.push(事件);
  if (漂移.最近事件序列.length > 漂移窗口大小) {
    漂移.最近事件序列 = 漂移.最近事件序列.slice(-漂移窗口大小);
  }
}

/**
 * 计算漂移方向和强度
 */
export function 计算偏好漂移(
  npc: NPC结构
): { 漂移方向: 性癖大类 | null; 漂移强度: number } {
  const 完整状态 = ensure完整状态(npc);
  if (!完整状态.偏好漂移) return { 漂移方向: null, 漂移强度: 0 };

  const 漂移 = 完整状态.偏好漂移;
  const 窗口 = 漂移.最近事件序列;

  if (窗口.length < 5) {
    return { 漂移方向: null, 漂移强度: 0 };
  }

  // 统计窗口内各性癖大类的出现次数
  const 大类计数: Record<性癖大类, number> = {
    '主导控制': 0,
    '角色扮演': 0,
    '感官刺激': 0,
    '暴露窥视': 0,
    '拘束限制': 0,
    '语言调教': 0,
    '公共冒险': 0,
    '情感依赖': 0,
    '特殊偏好': 0,
  };

  for (const 事件 of 窗口) {
    const 映射 = 获取事件映射(事件 as 性癖触发事件);
    if (映射) {
      大类计数[映射.触发类别] = (大类计数[映射.触发类别] || 0) + 1;
    }
  }

  const 窗口总事件数 = 窗口.length;
  let 最大类别: 性癖大类 | null = null;
  let 最大计数 = 0;

  for (const [类别, 计数] of Object.entries(大类计数)) {
    if (计数 > 最大计数) {
      最大计数 = 计数;
      最大类别 = 类别 as 性癖大类;
    }
  }

  if (!最大类别) return { 漂移方向: null, 漂移强度: 0 };

  const 占比 = 最大计数 / 窗口总事件数;
  if (占比 >= 漂移认定阈值) {
    漂移.漂移方向 = 最大类别;
    漂移.漂移强度 = Math.round(占比 * 100) / 100;
  } else {
    漂移.漂移方向 = null;
    漂移.漂移强度 = 0;
  }

  return { 漂移方向: 漂移.漂移方向, 漂移强度: 漂移.漂移强度 };
}

/**
 * 应用漂移：根据漂移方向提升对应性癖潜能
 */
export function 应用偏好漂移(
  npc: NPC结构,
  当前游戏时间: string
): { 有变化: boolean; 变化描述: string } {
  const 完整状态 = ensure完整状态(npc);
  if (!完整状态.偏好漂移) return { 有变化: false, 变化描述: '' };

  const { 漂移方向, 漂移强度 } = 计算偏好漂移(npc);
  完整状态.偏好漂移.最后计算时间 = 当前游戏时间;

  if (!漂移方向 || 漂移强度 < 漂移认定阈值) {
    return { 有变化: false, 变化描述: '' };
  }

  // 提升潜能池中对应类别的潜能值
  const 潜能池 = 完整状态.潜能池 ?? [];
  let 有变化 = false;

  for (const 潜能 of 潜能池) {
    if (潜能.类别 === 漂移方向 && !潜能.已觉醒) {
      潜能.潜能值 = Math.min(10, 潜能.潜能值 + Math.round(漂移强度 * 2));
      有变化 = true;
    }
  }

  const 变化描述 = 有变化
    ? `偏好漂移至"${漂移方向}"（强度${Math.round(漂移强度 * 100)}%），相关潜能提升`
    : '';

  return { 有变化, 变化描述 };
}

// ==================== 辅助函数 ====================

function ensure完整状态(npc: NPC结构): 完整演化状态 {
  if (!npc.完整演化状态) {
    npc.完整演化状态 = {
      演化日志: npc.性癖演化状态?.演化日志 ?? [],
      事件计数器: npc.性癖演化状态?.事件计数器 ?? {},
      最后演化时间: npc.性癖演化状态?.最后演化时间 ?? '',
    };
  }
  return npc.完整演化状态;
}
