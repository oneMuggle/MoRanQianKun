/**
 * NPC NSFW 增强模块 — 敏感点主动探索引擎
 * 玩家选择部位和方式主动探索敏感点，解锁技巧并组合使用
 */

import { NPC结构 } from '../../social';
import type { 敏感点条目, 敏感点开发程度, 完整演化状态 } from './types';
import { 全时代通用敏感点 } from '../sensitiveZones';

// ==================== 类型定义 ====================

export type 探索方式 = '轻抚' | '轻咬' | '温热' | '按压' | '指尖划过';

export interface 探索反馈 {
  反馈类型: '正面' | '负面' | '中性';
  反应强度: number;
  反应描述: string;
  亲密度变化: number;
  开发程度变化?: 敏感点开发程度;
}

export interface 敏感点技巧 {
  敏感点名称: string;
  探索方式: 探索方式;
  效果描述: string;
  协同效果?: string[];
}

export interface 探索结果 {
  成功: boolean;
  反馈: 探索反馈;
  新发现?: 敏感点条目;
  新技巧?: 敏感点技巧;
  描述: string;
}

// ==================== 方式效果系数 ====================

const 方式效果系数: Record<探索方式, number> = {
  '轻抚': 1.0,
  '轻咬': 1.2,
  '温热': 0.9,
  '按压': 0.8,
  '指尖划过': 1.1,
};

// ==================== 核心引擎函数 ====================

/**
 * 初始化敏感点探索系统
 */
export function 初始化敏感点探索(npc: NPC结构): void {
  const 完整状态 = ensure完整状态(npc);
  if (!完整状态.敏感点探索) {
    完整状态.敏感点探索 = {
      已发现技巧: [],
      探索记录: [],
      当前探索部位: null,
      最后探索时间: '',
    };
  }
}

/**
 * 探索敏感点 — 玩家选择部位和方式
 */
export function 探索敏感点(
  npc: NPC结构,
  部位: string,
  方式: 探索方式,
  游戏时间: string
): 探索结果 {
  const 完整状态 = ensure完整状态(npc);
  if (!完整状态.敏感点探索) {
    初始化敏感点探索(npc);
  }

  const 目标 = 查找敏感点(npc, 部位);
  if (!目标) {
    return {
      成功: false,
      反馈: { 反馈类型: '中性', 反应强度: 1, 反应描述: '没有找到对应的部位', 亲密度变化: 0 },
      描述: `未找到名为"${部位}"的敏感点`,
    };
  }

  if (目标.发现状态 === '未发觉') {
    const 发现概率 = 计算发现概率(npc, 目标);
    if (Math.random() < 发现概率) {
      目标.发现状态 = '已发现';
      目标.开发程度 = '未开发';
      记录探索历史(npc, 游戏时间, `发现了新敏感点：${部位}`, '发现');
      return {
        成功: true,
        反馈: { 反馈类型: '正面', 反应强度: 2, 反应描述: 目标.反应描述, 亲密度变化: 2 },
        新发现: 目标,
        描述: `意外发现了新的敏感点：${部位}！${目标.反应描述}`,
      };
    }
    return {
      成功: false,
      反馈: { 反馈类型: '中性', 反应强度: 1, 反应描述: '对方没有明显反应', 亲密度变化: 0 },
      描述: `尝试探索"${部位}"，但对方没有明显反应`,
    };
  }

  const 反馈 = 计算探索反馈(npc, 目标, 方式);
  if (反馈.反馈类型 === '正面' && 反馈.开发程度变化) {
    目标.开发程度 = 反馈.开发程度变化;
    记录探索历史(npc, 游戏时间, `${部位} 开发至 ${反馈.开发程度变化}`, '开发');
  }

  let 新技巧: 敏感点技巧 | undefined;
  if (反馈.反馈类型 === '正面' && Math.random() < 0.3) {
    新技巧 = {
      敏感点名称: 目标.名称,
      探索方式: 方式,
      效果描述: `${目标.名称}对${方式}特别敏感，${目标.反应描述}`,
      协同效果: 获取协同技巧(目标, 方式),
    };
    完整状态.敏感点探索!.已发现技巧.push(新技巧);
  }

  记录探索历史(npc, 游戏时间, `${部位} + ${方式}`, '探索');

  return {
    成功: 反馈.反馈类型 === '正面',
    反馈,
    新技巧,
    描述: 生成探索描述(目标, 方式, 反馈),
  };
}

/**
 * 组合技巧使用
 */
export function 组合技巧(
  npc: NPC结构,
  技巧1: 敏感点技巧,
  技巧2: 敏感点技巧,
  游戏时间: string
): 探索结果 {
  const 完整状态 = ensure完整状态(npc);

  const 技巧1存在 = 完整状态.敏感点探索?.已发现技巧.some(t =>
    t.敏感点名称 === 技巧1.敏感点名称 && t.探索方式 === 技巧1.探索方式
  );
  const 技巧2存在 = 完整状态.敏感点探索?.已发现技巧.some(t =>
    t.敏感点名称 === 技巧2.敏感点名称 && t.探索方式 === 技巧2.探索方式
  );

  if (!技巧1存在 || !技巧2存在) {
    return {
      成功: false,
      反馈: { 反馈类型: '中性', 反应强度: 1, 反应描述: '技巧尚未解锁', 亲密度变化: 0 },
      描述: '组合技巧失败：其中一个技巧尚未被发现',
    };
  }

  const 有协同 = 技巧1.协同效果?.includes(`${技巧2.敏感点名称}:${技巧2.探索方式}`) ||
                 技巧2.协同效果?.includes(`${技巧1.敏感点名称}:${技巧1.探索方式}`);

  if (有协同) {
    const 协同反馈: 探索反馈 = {
      反馈类型: '正面',
      反应强度: 5,
      反应描述: `「${技巧1.效果描述}」与「${技巧2.效果描述}」产生协同效应`,
      亲密度变化: 6,
    };
    记录探索历史(npc, 游戏时间, `技巧协同：${技巧1.敏感点名称}+${技巧2.敏感点名称}`, '协同');
    return { 成功: true, 反馈: 协同反馈, 描述: `技巧协同成功！${协同反馈.反应描述}` };
  }

  const 基础反馈: 探索反馈 = {
    反馈类型: '正面',
    反应强度: 3,
    反应描述: `组合使用「${技巧1.敏感点名称}」和「${技巧2.敏感点名称}」`,
    亲密度变化: 3,
  };
  记录探索历史(npc, 游戏时间, `技巧组合：${技巧1.敏感点名称}+${技巧2.敏感点名称}`, '组合');
  return { 成功: true, 反馈: 基础反馈, 描述: `技巧组合生效：${基础反馈.反应描述}` };
}

/**
 * 获取已发现的技巧列表
 */
export function 获取已发现技巧(npc: NPC结构): 敏感点技巧[] {
  const 完整状态 = ensure完整状态(npc);
  return 完整状态.敏感点探索?.已发现技巧 ?? [];
}

/**
 * 生成敏感点探索摘要 — 用于 prompt 注入
 */
export function 生成敏感点探索摘要(npc: NPC结构): string {
  const 完整状态 = ensure完整状态(npc);
  const 探索状态 = 完整状态.敏感点探索;
  if (!探索状态 || 探索状态.已发现技巧.length === 0) {
    return '尚未开始敏感点探索';
  }

  const 技巧数 = 探索状态.已发现技巧.length;
  const 主要敏感点 = npc.敏感点档案?.主要敏感点 ?? [];
  const 已开发 = 主要敏感点.filter(p => p.开发程度 && p.开发程度 !== '未开发');
  const 最高程度 = 获取最高开发程度(已开发);

  return `已发现 ${技巧数} 个敏感点技巧，${已开发.length} 个敏感点已被开发（最高：${最高程度}）`;
}

// ==================== 内部辅助函数 ====================

function ensure完整状态(npc: NPC结构): 完整演化状态 & { 敏感点探索?: 敏感点探索状态 } {
  if (!npc.完整演化状态) {
    npc.完整演化状态 = {
      演化日志: npc.性癖演化状态?.演化日志 ?? [],
      事件计数器: npc.性癖演化状态?.事件计数器 ?? {},
      最后演化时间: npc.性癖演化状态?.最后演化时间 ?? '',
    };
  }
  return npc.完整演化状态 as 完整演化状态 & { 敏感点探索?: 敏感点探索状态 };
}

function 查找敏感点(npc: NPC结构, 部位: string): 敏感点条目 | undefined {
  const 主要 = npc.敏感点档案?.主要敏感点.find(p => p.名称 === 部位 || p.时代名称 === 部位);
  if (主要) return 主要;
  const 隐藏 = npc.敏感点档案?.隐藏敏感点.find(p => p.名称 === 部位 || p.时代名称 === 部位);
  if (隐藏) return 隐藏;
  return 全时代通用敏感点.find(p => p.名称 === 部位);
}

function 计算发现概率(_npc: NPC结构, 目标: 敏感点条目): number {
  return Math.max(0.1, 0.4 - (目标.敏感度 - 1) * 0.05);
}

function 计算探索反馈(
  _npc: NPC结构,
  目标: 敏感点条目,
  方式: 探索方式
): 探索反馈 {
  const 基础系数 = 方式效果系数[方式];
  const 敏感度系数 = 目标.敏感度 / 3;
  const 开发加成 = 获取开发程度加成(目标.开发程度);
  const 随机因子 = 0.7 + Math.random() * 0.6;

  const 综合评分 = 基础系数 * 敏感度系数 * 开发加成 * 随机因子;

  if (综合评分 >= 1.5) {
    const 新开发程度 = 提升开发程度(目标.开发程度);
    return {
      反馈类型: '正面',
      反应强度: Math.min(5, Math.round(综合评分)),
      反应描述: 目标.反应描述,
      亲密度变化: Math.round(综合评分 * 1.5),
      开发程度变化: 新开发程度 !== 目标.开发程度 ? 新开发程度 : undefined,
    };
  } else if (综合评分 >= 0.8) {
    return {
      反馈类型: '正面',
      反应强度: Math.max(1, Math.round(综合评分)),
      反应描述: `${目标.名称}有轻微反应`,
      亲密度变化: 1,
    };
  }
  return {
    反馈类型: '中性',
    反应强度: 1,
    反应描述: `${目标.名称}似乎对这种方式不太敏感`,
    亲密度变化: 0,
  };
}

function 获取开发程度加成(开发程度?: 敏感点开发程度): number {
  const 加成表: Record<敏感点开发程度, number> = {
    '未开发': 0.8, '初步探索': 1.0, '渐入佳境': 1.2, '深度开发': 1.4, '完全开发': 1.6,
  };
  return 开发程度 ? (加成表[开发程度] ?? 0.8) : 0.8;
}

function 提升开发程度(当前?: 敏感点开发程度): 敏感点开发程度 {
  const 序列: 敏感点开发程度[] = ['未开发', '初步探索', '渐入佳境', '深度开发', '完全开发'];
  if (!当前) return '初步探索';
  const 索引 = 序列.indexOf(当前);
  if (索引 < 0 || 索引 >= 序列.length - 1) return 当前;
  return 序列[索引 + 1];
}

function 获取最高开发程度(已开发: 敏感点条目[]): 敏感点开发程度 {
  const 序列: 敏感点开发程度[] = ['未开发', '初步探索', '渐入佳境', '深度开发', '完全开发'];
  let 最高 = '未开发' as 敏感点开发程度;
  for (const p of 已开发) {
    if (p.开发程度 && 序列.indexOf(p.开发程度) > 序列.indexOf(最高)) {
      最高 = p.开发程度;
    }
  }
  return 最高;
}

function 获取协同技巧(目标: 敏感点条目, _方式: 探索方式): string[] {
  const 协同表: Record<string, string[]> = {
    '头颈区': ['耳垂:轻咬', '后颈:温热', '太阳穴:按压'],
    '胸胸区': ['锁骨:指尖划过', '胸口:温热'],
    '腰腹区': ['腰侧:轻抚', '小腹:温热'],
    '四肢区': ['大腿内侧:轻抚', '手心:按压', '膝盖后:指尖划过'],
    '背部区': ['脊柱线:指尖划过', '肩胛骨:温热'],
    '特殊区': ['发丝:轻抚', '脚踝:轻抚'],
  };
  return 协同表[目标.区域] ?? [];
}

function 生成探索描述(目标: 敏感点条目, 方式: 探索方式, 反馈: 探索反馈): string {
  if (反馈.反馈类型 === '正面' && 反馈.反应强度 >= 4) {
    return `${方式}${目标.名称}时，对方产生了强烈反应！${目标.反应描述}（亲密度 +${反馈.亲密度变化}）`;
  }
  if (反馈.反馈类型 === '正面') {
    return `${方式}${目标.名称}时，对方有了${反馈.反应强度 >= 3 ? '明显' : '轻微'}反应。${反馈.反应描述}（亲密度 +${反馈.亲密度变化}）`;
  }
  return `${方式}${目标.名称}时，对方反应平淡。${反馈.反应描述}`;
}

function 记录探索历史(
  npc: NPC结构,
  游戏时间: string,
  描述: string,
  类型: '发现' | '开发' | '探索' | '协同' | '组合'
): void {
  const 完整状态 = ensure完整状态(npc);
  if (!完整状态.敏感点探索) return;

  完整状态.敏感点探索.探索记录.push({ 时间: 游戏时间, 类型, 描述 });
  if (完整状态.敏感点探索.探索记录.length > 30) {
    完整状态.敏感点探索.探索记录 = 完整状态.敏感点探索.探索记录.slice(-30);
  }
  完整状态.敏感点探索.最后探索时间 = 游戏时间;
}

