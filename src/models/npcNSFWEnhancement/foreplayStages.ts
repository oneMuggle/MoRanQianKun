// ==================== 核心函数 ====================

/**
 * 创建初始节奏状态
 */
export function 创建初始节奏状态(): 互动节奏状态 {
  return {
    当前阶段: '氛围营造',
    阶段持续时间: 0,
    阶段满意度: 50,
    节奏评价: '适中',
    最后更新时间: new Date().toISOString(),
  };
}

/**
 * 评估互动对当前阶段的评分
 */
export function 评估互动(
  当前阶段: 前戏阶段,
  互动类型: string
): 互动评分 {
  const 评分 = 互动类型评分表[互动类型]?.[当前阶段];
  if (评分) return 评分;

  // 默认评分：中性互动
  return {
    阶段匹配度: 30,
    满意度增量: 0,
    推进意愿: 20,
  };
}

/**
 * 推进前戏阶段
 * 根据互动类型、满意度和亲密度判断是否进入下一阶段
 */
export function 推进前戏阶段(
  当前状态: 互动节奏状态,
  互动类型: string,
  亲密度等级: number,
  情感状态?: Record<情感维度, number>
): 互动节奏状态 {
  const 当前阶段索引 = 阶段顺序.indexOf(当前状态.当前阶段);
  if (当前阶段索引 === -1) return 当前状态;

  const 互动评分 = 评估互动(当前状态.当前阶段, 互动类型);
  const 新持续时间 = 当前状态.阶段持续时间 + 1;

  // 计算新满意度
  let 新满意度 = 当前状态.阶段满意度 + 互动评分.满意度增量;

  // 亲密度加成：高亲密度更容易满意
  if (亲密度等级 >= 4) 新满意度 += 5;
  else if (亲密度等级 >= 2) 新满意度 += 3;

  // 情感状态影响
  if (情感状态) {
    const 爱慕值 = 情感状态['爱慕'] ?? 50;
    if (爱慕值 > 70) 新满意度 += 8;
    else if (爱慕值 > 50) 新满意度 += 4;

    const 不安值 = 情感状态['不安'] ?? 0;
    if (不安值 > 60) 新满意度 -= 10;
    else if (不安值 > 40) 新满意度 -= 5;
  }

  新满意度 = Math.max(0, Math.min(100, 新满意度));

  // 判断是否可以推进
  const 已满足最低回合 = 新持续时间 >= 阶段最低回合数[当前状态.当前阶段];
  const 满意度达标 = 新满意度 >= 阶段满意度阈值[当前状态.当前阶段];
  const 意愿足够 = 互动评分.推进意愿 > 40;
  const 非最后阶段 = 当前阶段索引 < 阶段顺序.length - 1;

  let 新阶段 = 当前状态.当前阶段;
  let 新持续 = 新持续时间;

  if (非最后阶段 && 已满足最低回合 && 满意度达标 && 意愿足够) {
    新阶段 = 阶段顺序[当前阶段索引 + 1];
    新持续 = 0;
    新满意度 = 45 + Math.min(10, Math.floor(亲密度等级 * 2));
  }

  const 节奏评价 = 计算节奏评价(新阶段, 新满意度, 新持续);

  return {
    当前阶段: 新阶段,
    阶段持续时间: 新持续,
    阶段满意度: Math.round(新满意度),
    节奏评价,
    最后更新时间: new Date().toISOString(),
  };
}

/**
 * 计算节奏评价
 */
function 计算节奏评价(
  阶段: 前戏阶段,
  满意度: number,
  持续时间: number
): 节奏评价 {
  const 最低回合 = 阶段最低回合数[阶段];
  const 阈值 = 阶段满意度阈值[阶段];

  if (持续时间 > 最低回合 * 2 && 满意度 < 阈值 - 20) {
    return '过慢';
  }

  if (满意度 > 阈值 + 20 && 持续时间 > 最低回合 + 2) {
    return '过快';
  }

  if (满意度 >= 阈值 + 10 && 持续时间 >= 最低回合 && 持续时间 <= 最低回合 + 1) {
    return '完美';
  }

  return '适中';
}

/**
 * 评估阶段满意度（综合情感维度）
 */
export function 评估阶段满意度(
  当前状态: 互动节奏状态,
  情感状态: Record<情感维度, number>
): number {
  let 满意度 = 当前状态.阶段满意度;

  const 爱慕值 = 情感状态['爱慕'] ?? 50;
  满意度 += Math.floor((爱慕值 - 50) * 0.2);

  const 兴奋值 = 情感状态['兴奋'] ?? 0;
  满意度 += Math.floor(兴奋值 * 0.1);

  const 不安值 = 情感状态['不安'] ?? 0;
  满意度 -= Math.floor(不安值 * 0.15);

  const 内疚值 = 情感状态['内疚'] ?? 0;
  满意度 -= Math.floor(内疚值 * 0.05);

  return Math.max(0, Math.min(100, Math.round(满意度)));
}

/**
 * 强制变更阶段（用于NPC主动推进、玩家强制跳过等特殊情况）
 */
export function 强制变更阶段(
  当前状态: 互动节奏状态,
  目标阶段: 前戏阶段
): 互动节奏状态 {
  const 目标索引 = 阶段顺序.indexOf(目标阶段);
  if (目标索引 === -1) return 当前状态;

  const 当前索引 = 阶段顺序.indexOf(当前状态.当前阶段);

  return {
    当前阶段: 目标阶段,
    阶段持续时间: 0,
    阶段满意度: 45,
    节奏评价: 目标索引 > 当前索引 ? '过快' : '适中',
    最后更新时间: new Date().toISOString(),
  };
}

/**
 * 生成前戏阶段提示词注入
 */
export function 生成前戏阶段提示(当前状态: 互动节奏状态): string {
  const 组件: string[] = [];

  组件.push(`前戏阶段：${当前状态.当前阶段}（第${当前状态.阶段持续时间 + 1}回合）`);
  组件.push(`满意度：${当前状态.阶段满意度}%（${当前状态.节奏评价}）`);

  if (当前状态.节奏评价 === '过快') {
    组件.push('注意：节奏过快，建议增加前戏描写，放慢推进速度。');
  } else if (当前状态.节奏评价 === '过慢') {
    组件.push('注意：节奏过慢，互动缺乏进展，建议适当推进。');
  } else if (当前状态.节奏评价 === '完美') {
    组件.push('注意：节奏完美，请保持当前的互动节奏。');
  }

  const 当前索引 = 阶段顺序.indexOf(当前状态.当前阶段);
  if (当前索引 < 阶段顺序.length - 1) {
    const 下一阶段 = 阶段顺序[当前索引 + 1];
    const 最低回合 = 阶段最低回合数[当前状态.当前阶段];
    const 阈值 = 阶段满意度阈值[当前状态.当前阶段];

    const 还差回合 = Math.max(0, 最低回合 - 当前状态.阶段持续时间);
    const 还差满意度 = Math.max(0, 阈值 - 当前状态.阶段满意度);

    if (还差回合 > 0 || 还差满意度 > 0) {
      const 条件: string[] = [];
      if (还差回合 > 0) 条件.push(`还需${还差回合}回合`);
      if (还差满意度 > 0) 条件.push(`满意度还需提升${还差满意度}%`);
      组件.push(`距离进入「${下一阶段}」：${条件.join('，')}`);
    } else {
      组件.push(`已满足进入「${下一阶段}」的条件，下次互动可以自然推进。`);
    }
  }

  return 组件.join('\n');
}
