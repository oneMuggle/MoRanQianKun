// ==================== 查询函数 ====================

/** 获取敏感点的视觉配置 */
export function 获取敏感点视觉配置(名称: string): 敏感点视觉配置 | undefined {
  return 敏感点视觉映射[名称];
}

/** 根据身体区域获取视觉配置 */
export function 获取区域视觉配置(区域: 身体区域分类): 身体区域视觉映射 | undefined {
  return 身体区域视觉配置.find(c => c.区域 === 区域);
}

/** 获取敏感点在当前视图下的坐标 */
export function 获取敏感点坐标(
  名称: string,
  视图: '正面' | '背面'
): { x: number; y: number } | null {
  const config = 敏感点视觉映射[名称];
  if (!config) return null;

  if (视图 === '正面') {
    return config.正面坐标 || null;
  }

  return config.背面坐标 || config.正面坐标 || null;
}

/** 检查敏感点在指定视图下是否可见 */
export function 敏感点是否可见(名称: string, 视图: '正面' | '背面'): boolean {
  const config = 敏感点视觉映射[名称];
  if (!config) return false;

  if (视图 === '正面') return !!config.正面坐标;
  return !!(config.背面坐标 || config.正面坐标);
}
