function get评级等级(分数: number): 作品评级结果['评级'] {
  if (分数 >= 90) return 'S';
  if (分数 >= 75) return 'A';
  if (分数 >= 55) return 'B';
  if (分数 >= 35) return 'C';
  return 'D';
}

