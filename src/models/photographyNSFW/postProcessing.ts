/**
 * 写真 NSFW 增强模块 — 后期处理小游戏
 * 后期风格选择影响泄露后果，修图决策影响传播风险
 */

import type { 拍摄项目状态, 模特核心状态, 摄影师核心状态 } from './states';

// ==================== 类型定义 ====================

export type 后期风格 = '自然' | '精修' | 'AI增强' | '艺术处理';

export type 修图决策 = '保留敏感内容' | '适度遮掩' | '完全删除敏感内容';

export interface 作品评级结果 {
  评级: 'S' | 'A' | 'B' | 'C' | 'D';
  质量分数: number;        // 0-100
  模特满意度: number;      // 0-100
  描述: string;
}

export interface 后期处理结果 {
  风格: 后期风格;
  修图决策: 修图决策;
  泄露风险变化: number;    // 负数表示降低
  作品评级: 作品评级结果;
  收益变化: number;
  描述: string;
}

// ==================== 风格系数 ====================

const 风格系数: Record<后期风格, {
  质量加成: number;
  泄露风险倍率: number;
  模特满意度影响: number;
  成本: number;
}> = {
  '自然': { 质量加成: 0, 泄露风险倍率: 0.8, 模特满意度影响: 5, 成本: 0 },
  '精修': { 质量加成: 20, 泄露风险倍率: 1.2, 模特满意度影响: 15, 成本: 100 },
  'AI增强': { 质量加成: 30, 泄露风险倍率: 1.8, 模特满意度影响: 0, 成本: 200 },
  '艺术处理': { 质量加成: 25, 泄露风险倍率: 0.9, 模特满意度影响: 10, 成本: 150 },
};

const 修图决策系数: Record<修图决策, {
  泄露风险变化: number;
  模特满意度影响: number;
  商业价值影响: number;
}> = {
  '保留敏感内容': { 泄露风险变化: 30, 模特满意度影响: -20, 商业价值影响: 15 },
  '适度遮掩': { 泄露风险变化: 0, 模特满意度影响: 5, 商业价值影响: 5 },
  '完全删除敏感内容': { 泄露风险变化: -40, 模特满意度影响: 10, 商业价值影响: -10 },
};

// ==================== 核心引擎函数 ====================

/**
 * 选择后期风格
 */
export function 选择后期风格(
  风格: 后期风格,
  项目: 拍摄项目状态,
  摄影师: 摄影师核心状态,
  _模特: 模特核心状态
): { 风格: 后期风格; 质量提升: number; 泄露风险变化: number; 成本: number; 描述: string } {
  const 系数 = 风格系数[风格];
  const 技术水平系数 = 摄影师.技术水平 / 100;
  const 质量提升 = Math.round(系数.质量加成 * 技术水平系数);
  const 泄露风险变化 = Math.round((系数.泄露风险倍率 - 1) * 项目.泄露风险评分);

  return {
    风格,
    质量提升,
    泄露风险变化,
    成本: 系数.成本,
    描述: get后期风格描述(风格),
  };
}

/**
 * 修图决策 — 是否保留敏感内容
 */
export function 修图决策(
  决策: 修图决策,
  _项目: 拍摄项目状态,
  _模特: 模特核心状态
): { 泄露风险变化: number; 模特满意度变化: number; 商业价值变化: number; 描述: string } {
  const 系数 = 修图决策系数[决策];
  const 随机因子 = 0.9 + Math.random() * 0.2;

  return {
    泄露风险变化: Math.round(系数.泄露风险变化 * 随机因子),
    模特满意度变化: Math.round(系数.模特满意度影响 * 随机因子),
    商业价值变化: Math.round(系数.商业价值影响 * 随机因子),
    描述: get修图决策描述(决策),
  };
}

/**
 * 作品评级 — 基于多维度因素计算作品质量
 */
export function 作品评级(
  _项目: 拍摄项目状态,
  摄影师: 摄影师核心状态,
  模特: 模特核心状态,
  风格: 后期风格,
  修图: 修图决策
): 作品评级结果 {
  // 技术质量分 (0-40)
  const 技术分 = Math.round(摄影师.技术水平 * 0.4);

  // 模特表现分 (0-30)
  const 信任度系数 = 模特.信任度 / 100;
  const 安全感系数 = 模特.安全感 / 100;
  const 表现分 = Math.round(30 * (信任度系数 * 0.6 + 安全感系数 * 0.4));

  // 风格加成 (0-20)
  const 风格加成 = 风格系数[风格].质量加成 * 0.2;

  // 修图惩罚/加成 (0-10)
  const 修图影响 = 修图决策系数[修图].商业价值影响 * 0.1;

  const 总分 = Math.max(0, Math.min(100,
    技术分 + 表现分 + Math.round(风格加成) + Math.round(修图影响)
  ));

  const 评级 = get评级等级(总分);
  const 满意度 = Math.round(
    (模特.信任度 * 0.3 + 总分 * 0.5 + 风格系数[风格].模特满意度影响) *
    (0.8 + Math.random() * 0.4)
  );

  return {
    评级,
    质量分数: 总分,
    模特满意度: Math.max(0, Math.min(100, 满意度)),
    描述: get评级描述(评级, 总分),
  };
}

/**
 * 完整后期处理流程
 */
export function 执行后期处理(
  风格: 后期风格,
  决策: 修图决策,
  项目: 拍摄项目状态,
  摄影师: 摄影师核心状态,
  模特: 模特核心状态
): 后期处理结果 {
  const 风格结果 = 选择后期风格(风格, 项目, 摄影师, 模特);
  const 修图结果 = 修图决策(决策, 项目, 模特);
  const 评级结果 = 作品评级(项目, 摄影师, 模特, 风格, 决策);

  // 收益计算
  const 基础收益 = 200;
  const 质量加成 = 评级结果.质量分数 / 100;
  const 风格成本 = 风格结果.成本;
  const 收益变化 = Math.round(基础收益 * 质量加成 - 风格成本);

  return {
    风格,
    修图决策: 决策,
    泄露风险变化: 风格结果.泄露风险变化 + 修图结果.泄露风险变化,
    作品评级: 评级结果,
    收益变化,
    描述: `${风格结果.描述}${修图结果.描述} | 评级：${评级结果.评级}(${评级结果.质量分数}分)`,
  };
}

