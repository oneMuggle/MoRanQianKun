/**
 * 写真 NSFW 增强模块 — 泄露应急响应
 * 泄露不是被动挨打，而是有应急操作空间
 */

import type { 拍摄项目状态, 泄露事件状态, 模特核心状态 } from './states';
import type { 泄露类型, 传播范围 } from './types';

// ==================== 类型定义 ====================

export type 应急响应 = '删除源文件' | '法律手段' | '公开承认' | '否认' | '追回';

export interface 泄露检测结果 {
  是否发现泄露: boolean;
  泄露类型: 泄露类型 | '未知';
  传播范围: 传播范围;
  发现时间: number;
  描述: string;
}

export interface 应急响应结果 {
  成功: boolean;
  传播范围变化: 传播范围;
  声誉变化: number;
  法律风险变化: number;
  成本: number;
  描述: string;
}

// ==================== 响应系数 ====================

const 应急响应系数: Record<应急响应, {
  基础成功率: number;
  声誉影响: number;
  法律风险影响: number;
  成本: number;
}> = {
  '删除源文件': { 基础成功率: 0.7, 声誉影响: 0, 法律风险影响: -5, 成本: 0 },
  '法律手段': { 基础成功率: 0.6, 声誉影响: 5, 法律风险影响: -15, 成本: 500 },
  '公开承认': { 基础成功率: 0.5, 声誉影响: -10, 法律风险影响: -10, 成本: 0 },
  '否认': { 基础成功率: 0.4, 声誉影响: -15, 法律风险影响: 5, 成本: 0 },
  '追回': { 基础成功率: 0.3, 声誉影响: 5, 法律风险影响: -5, 成本: 300 },
};

const 传播范围序列: 传播范围[] = ['未传播', '小范围', '论坛传播', '社交媒体', '图片网站', '成人网站', '全网扩散'];

function 传播索引(范围: 传播范围): number {
  return 传播范围序列.indexOf(范围);
}

// ==================== 核心引擎函数 ====================

/**
 * 泄露检测 — 第一时间发现泄露
 */
export function 泄露检测(
  项目: 拍摄项目状态,
  _模特: 模特核心状态,
  当前时间: number
): 泄露检测结果 {
  const 泄露风险 = 项目.泄露风险评分;

  // 基于泄露风险评分进行随机检测
  const 发现概率 = Math.max(0.05, 泄露风险 / 100);

  if (Math.random() > 发现概率) {
    return {
      是否发现泄露: false,
      泄露类型: '未知',
      传播范围: '未传播',
      发现时间: 0,
      描述: '暂未发现异常泄露',
    };
  }

  // 根据项目状态推断泄露类型和传播范围
  const 泄露类型 = 推断泄露类型(项目);
  const 传播范围 = 推断传播范围(项目);

  return {
    是否发现泄露: true,
    泄露类型,
    传播范围,
    发现时间: 当前时间,
    描述: `检测到${泄露类型}，传播范围：${传播范围}`,
  };
}

/**
 * 应急响应选项
 */
export function 执行应急响应(
  响应: 应急响应,
  当前泄露: 泄露事件状态,
  上下文: { 持有金钱?: number; 法律风险?: number; 声誉?: number },
  _游戏时间: string
): 应急响应结果 {
  const 配置 = 应急响应系数[响应];

  // 资源加成
  const 资源加成 = (() => {
    switch (响应) {
      case '法律手段':
        return Math.min(0.2, (上下文.持有金钱 ?? 0) / 1000 * 0.2);
      case '追回':
        return Math.min(0.15, (上下文.声誉 ?? 0) / 100 * 0.15);
      default:
        return 0;
    }
  })();

  // 传播范围惩罚 — 传播越广越难控制
  const 当前传播Idx = 传播索引(当前泄露.传播范围);
  const 传播惩罚 = 当前传播Idx * 0.08;

  const 成功率 = Math.max(0.1, Math.min(0.9,
    (配置.基础成功率 + 资源加成) * (1 - 传播惩罚)
  ));

  if (Math.random() <= 成功率) {
    const 新传播范围 = 控制传播范围(当前泄露.传播范围, 响应);
    const 声誉变化 = Math.round(配置.声誉影响 * (0.8 + Math.random() * 0.4));

    return {
      成功: true,
      传播范围变化: 新传播范围,
      声誉变化,
      法律风险变化: 配置.法律风险影响,
      成本: 配置.成本,
      描述: get应急响应成功描述(响应, 新传播范围),
    };
  }

  // 响应失败 — 传播可能进一步扩大
  const 恶化范围 = 恶化传播范围(当前泄露.传播范围);

  return {
    成功: false,
    传播范围变化: 恶化范围,
    声誉变化: Math.round(配置.声誉影响 * 1.5),
    法律风险变化: Math.round(配置.法律风险影响 * 0.5),
    成本: 配置.成本,
    描述: get应急响应失败描述(响应, 恶化范围),
  };
}

/**
 * 泄露后果最小化 — 通过快速响应降低传播范围
 */
export function 泄露后果最小化(
  响应时间: number,  // 从发现到响应的时间（回合数）
  当前泄露: 泄露事件状态,
  _项目: 拍摄项目状态
): { 传播范围变化: 传播范围; 额外损失: number; 描述: string } {
  // 响应越快效果越好
  const 时间系数 = 响应时间 <= 1 ? 0.9 : 响应时间 <= 3 ? 0.7 : 响应时间 <= 5 ? 0.5 : 0.3;

  const 当前传播Idx = 传播索引(当前泄露.传播范围);
  const 可降低等级 = Math.round(当前传播Idx * 时间系数);
  const 新传播Idx = Math.max(0, 当前传播Idx - 可降低等级);
  const 新传播范围 = 传播范围序列[新传播Idx];

  const 额外损失 = 当前传播Idx * (1 - 时间系数) * 10;

  return {
    传播范围变化: 新传播范围,
    额外损失: Math.round(额外损失),
    描述: 响应时间 <= 1
      ? '立即响应，有效控制了传播范围'
      : 响应时间 <= 3
        ? '较快响应，部分遏制了传播'
        : '响应较晚，传播已经形成一定规模',
  };
}

// ==================== 内部辅助函数 ====================

function 推断泄露类型(项目: 拍摄项目状态): 泄露类型 {
  if (项目.越界行为记录.length > 0) {
    return '盗取传播';
  }
  if (项目.交付方式 === '直接交付') {
    return '二手传播';
  }
  return '意外泄露';
}

function 推断传播范围(项目: 拍摄项目状态): 传播范围 {
  const 风险评分 = 项目.泄露风险评分;
  if (风险评分 > 80) return '社交媒体';
  if (风险评分 > 60) return '论坛传播';
  if (风险评分 > 40) return '小范围';
  return '未传播';
}

function 控制传播范围(当前: 传播范围, 响应: 应急响应): 传播范围 {
  const idx = 传播索引(当前);
  const 降低等级 = 响应 === '删除源文件' ? 2 :
                   响应 === '法律手段' ? 1 :
                   响应 === '追回' ? 2 :
                   响应 === '公开承认' ? 0 :
                   0; // 否认可能无效
  const 新Idx = Math.max(0, idx - 降低等级);
  return 传播范围序列[新Idx];
}

function 恶化传播范围(当前: 传播范围): 传播范围 {
  const idx = 传播索引(当前);
  const 最大Idx = 传播范围序列.length - 1;
  // 失败时可能传播范围扩大 0-1 级
  const 恶化 = Math.random() < 0.5 ? 1 : 0;
  return 传播范围序列[Math.min(最大Idx, idx + 恶化)];
}

