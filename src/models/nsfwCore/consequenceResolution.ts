// ==================== 核心函数 ====================

export function 计算应对成功率(
  策略: 应对策略,
  资源状态: NSFW资源状态,
  后果: 后果记录
): number {
  let 成功率 = 策略.基础成功率;

  if (资源状态.声誉值 >= 70) 成功率 += 0.1;
  if (资源状态.声誉值 <= 20) 成功率 -= 0.15;

  switch (后果.严重程度) {
    case '轻微': 成功率 += 0.1; break;
    case '中等': break;
    case '严重': 成功率 -= 0.1; break;
    case '毁灭': 成功率 -= 0.2; break;
  }

  return Math.max(0.1, Math.min(0.95, 成功率));
}

export function 检查策略可用(
  策略: 应对策略,
  资源状态: NSFW资源状态
): boolean {
  if (策略.消耗资源.代币 && 资源状态.亲密度代币 < 策略.消耗资源.代币) return false;
  if (策略.消耗资源.风险预算 && 资源状态.风险预算 < 策略.消耗资源.风险预算) return false;
  if (策略.消耗资源.最低声誉 && 资源状态.声誉值 < 策略.消耗资源.最低声誉) return false;
  return true;
}

export function 执行应对策略(
  策略: 应对策略,
  资源状态: NSFW资源状态,
  后果: 后果记录,
  随机数: number = Math.random()
): 应对结果 {
  const 成功率 = 计算应对成功率(策略, 资源状态, 后果);
  let 新资源 = { ...资源状态 };

  if (策略.消耗资源.代币) {
    const r = 消费代币(新资源, 策略.消耗资源.代币);
    新资源 = r.新状态;
  }
  if (策略.消耗资源.风险预算) {
    新资源.风险预算 = Math.max(0, 新资源.风险预算 - 策略.消耗资源.风险预算!);
  }

  const 成功 = 随机数 < 成功率;

  if (成功) {
    if (策略.成功效果.声誉恢复) {
      新资源 = 修改声誉(新资源, 策略.成功效果.声誉恢复!, '应对成功，声誉恢复').新状态;
    }
    if (策略.成功效果.代币奖励) {
      新资源 = 获得代币(新资源, 策略.成功效果.代币奖励!).新状态;
    }

    const 新后果: 后果记录 = {
      ...后果,
      是否已解决: true,
      描述: `${后果.描述}（已通过「${策略.名称}」解决）`,
    };

    return {
      成功: true,
      新资源状态: 新资源,
      新后果记录: 新后果,
      描述: `应对成功！${策略.成功效果.后果消除 ? '后果已消除' : ''}`,
    };
  }

  新资源 = 修改声誉(新资源, -策略.失败效果.声誉额外损失, `应对失败: ${策略.名称}`).新状态;

  const 新后果: 后果记录 = 策略.失败效果.后果升级
    ? { ...后果, 严重程度: 升级严重程度(后果.严重程度), 持续回合: 后果.持续回合 + 2 }
    : 后果;

  return {
    成功: false,
    新资源状态: 新资源,
    新后果记录: 新后果,
    描述: `应对失败：${策略.失败效果.描述}`,
  };
}

function 修改声誉(
  资源: NSFW资源状态,
  变化量: number,
  _原因: string
): { 新状态: NSFW资源状态; 描述: string } {
  const 新状态 = { ...资源, 声誉值: Math.max(0, Math.min(100, 资源.声誉值 + 变化量)) };
  return { 新状态, 描述: `声誉 ${变化量 > 0 ? '+' : ''}${变化量}` };
}

function 消费代币(资源: NSFW资源状态, 数量: number): { 新状态: NSFW资源状态; 描述: string } {
  const 新状态 = { ...资源, 亲密度代币: Math.max(0, 资源.亲密度代币 - 数量) };
  return { 新状态, 描述: `消费 ${数量} 代币` };
}

function 获得代币(资源: NSFW资源状态, 数量: number): { 新状态: NSFW资源状态; 描述: string } {
  const 新状态 = { ...资源, 亲密度代币: 资源.亲密度代币 + 数量 };
  return { 新状态, 描述: `获得 ${数量} 代币` };
}

function 升级严重程度(当前: 后果记录['严重程度']): 后果记录['严重程度'] {
  const 等级 = ['轻微', '中等', '严重', '毁灭'] as const;
  const 索引 = 等级.indexOf(当前);
  return 索引 < 等级.length - 1 ? 等级[索引 + 1] : 当前;
}

export function 获取可用策略(
  资源状态: NSFW资源状态,
  后果: 后果记录
): 策略推荐[] {
  return 应对策略库
    .filter(策略 => 检查策略可用(策略, 资源状态))
    .map(策略 => {
      const 成功率 = 计算应对成功率(策略, 资源状态, 后果);
      let 推荐度 = Math.round(成功率 * 100);

      if (后果.严重程度 === '毁灭' && 策略.id === '法律手段') 推荐度 += 15;
      if (后果.严重程度 === '轻微' && 策略.id === '转移注意力') 推荐度 += 10;
      if (策略.成功效果.代币奖励 && 资源状态.亲密度代币 < 20) 推荐度 += 10;

      推荐度 = Math.min(100, 推荐度);

      let 推荐理由 = '';
      if (推荐度 >= 75) 推荐理由 = '推荐！成功率较高';
      else if (推荐度 >= 50) 推荐理由 = '可以尝试';
      else if (推荐度 >= 30) 推荐理由 = '风险较高，谨慎选择';
      else 推荐理由 = '成功率很低，不建议';

      return { 策略, 推荐度, 推荐理由 };
    })
    .sort((a, b) => b.推荐度 - a.推荐度);
}
