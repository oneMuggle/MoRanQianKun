// ==================== 核心函数 ====================

export function 检查组合可用(
  组合: 场景组合,
  已解锁场景: 场景定义[]
): boolean {
  const 已解锁来源 = new Set(已解锁场景.map(s => s.来源));
  return 组合.组合场景.every(cs => 已解锁来源.has(cs.来源));
}

export function 计算组合风险收益(
  组合: 场景组合,
  基础场景: 场景定义[]
): { 总风险: number; 总收益: number; 协同加成: string } {
  const 基础风险总和 = 基础场景.reduce((sum, s) => sum + s.基础风险, 0);
  const 基础收益总和 = 基础场景.reduce((sum, s) => sum + s.基础收益, 0);

  const 总风险 = Math.round(基础风险总和 * 组合.风险协同系数);
  const 总收益 = Math.round(基础收益总和 * 组合.协同倍率);
  const 协同加成 = `风险×${组合.风险协同系数.toFixed(1)} | 收益×${组合.协同倍率.toFixed(1)}`;

  return { 总风险, 总收益, 协同加成 };
}

export function 执行场景组合(
  组合: 场景组合,
  资源状态: NSFW资源状态,
  基础场景: 场景定义[]
): 组合执行结果 {
  const { 总风险, 总收益, 协同加成 } = 计算组合风险收益(组合, 基础场景);
  let 新资源 = { ...资源状态 };
  const 描述列表: string[] = [`执行组合「${组合.名称}」`];

  if (总收益 > 0) {
    新资源 = 获得亲密度代币(新资源, 总收益, `场景组合收益: ${组合.名称}`).新状态;
    描述列表.push(`获得 ${总收益} 代币`);
  }

  if (组合.额外奖励) {
    if (组合.额外奖励.代币) {
      新资源 = 获得亲密度代币(新资源, 组合.额外奖励.代币, '组合额外奖励').新状态;
      描述列表.push(`额外获得 ${组合.额外奖励.代币} 代币`);
    }
    if (组合.额外奖励.声誉变化) {
      新资源 = 修改声誉(新资源, 组合.额外奖励.声誉变化, `组合: ${组合.名称}`).新状态;
    }
  }

  新资源.风险预算 = Math.max(0, 新资源.风险预算 - Math.round(总风险 * 0.5));

  return {
    成功: true,
    新资源状态: 新资源,
    总风险,
    总收益,
    协同加成,
    描述: 描述列表.join('，'),
  };
}

export function 获取推荐组合(
  已解锁场景: 场景定义[],
  上下文: {
    当前声誉: number;
    风险预算: number;
  }
): 推荐组合[] {
  return 场景组合库
    .filter(组合 => 检查组合可用(组合, 已解锁场景))
    .map(组合 => {
      let 匹配度 = 60;
      匹配度 += 上下文.风险预算 * 0.2;

      if (组合.风险协同系数 > 1 && 上下文.当前声誉 < 40) {
        匹配度 -= 20;
      }
      if (组合.额外奖励?.声誉变化 && 组合.额外奖励.声誉变化 < 0 && 上下文.当前声誉 < 30) {
        匹配度 -= 15;
      }

      匹配度 = Math.max(0, Math.min(100, 匹配度));

      let 推荐理由 = '';
      if (匹配度 >= 80) 推荐理由 = '强烈推荐！当前状态非常适合此组合';
      else if (匹配度 >= 60) 推荐理由 = '可以尝试，状态基本满足要求';
      else if (匹配度 >= 40) 推荐理由 = '风险偏高，建议谨慎';
      else 推荐理由 = '当前状态不推荐此组合';

      return { 组合, 匹配度: Math.round(匹配度), 推荐理由 };
    })
    .sort((a, b) => b.匹配度 - a.匹配度);
}

export function 获取可用组合(已解锁场景: 场景定义[]): 场景组合[] {
  return 场景组合库.filter(组合 => 检查组合可用(组合, 已解锁场景));
}
