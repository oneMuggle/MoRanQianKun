// ==================== 资源操作函数 ====================

/**
 * 初始化资源状态
 */
export function 初始化NSFW资源(): NSFW资源状态 {
  return {
    精力值: 100,
    声誉值: 70,
    亲密度代币: 0,
    风险预算: 100,
  };
}

/**
 * 消耗精力
 */
export function 消耗精力(
  当前状态: NSFW资源状态,
  行为类型: keyof 精力消耗配置,
  配置: 精力消耗配置 = 默认精力消耗配置
): { 新状态: NSFW资源状态; 消耗量: number; 精力不足: boolean; 描述: string } {
  const 消耗量 = 配置[行为类型];
  const 精力不足 = 当前状态.精力值 < 消耗量;
  const 新精力 = Math.max(0, 当前状态.精力值 - 消耗量);

  const 描述 = 精力不足
    ? `精力不足！需要 ${消耗量} 精力，当前只有 ${当前状态.精力值}`
    : `消耗了 ${消耗量} 精力，剩余 ${新精力}`;

  return {
    新状态: { ...当前状态, 精力值: 新精力 },
    消耗量,
    精力不足,
    描述,
  };
}

/**
 * 恢复精力
 */
export function 恢复精力(
  当前状态: NSFW资源状态,
  恢复量: number,
  上限: number = 100
): { 新状态: NSFW资源状态; 实际恢复: number; 描述: string } {
  const 实际恢复 = Math.min(恢复量, 上限 - 当前状态.精力值);
  const 新精力 = 当前状态.精力值 + 实际恢复;

  const 描述 = 实际恢复 > 0
    ? `恢复了 ${实际恢复} 精力，当前 ${新精力}`
    : '精力已满，无需恢复';

  return {
    新状态: { ...当前状态, 精力值: 新精力 },
    实际恢复,
    描述,
  };
}

/**
 * 消耗风险预算
 */
export function 消耗风险预算(
  当前状态: NSFW资源状态,
  行为类型: keyof 风险预算配置,
  配置: 风险预算配置 = 默认风险预算配置
): { 新状态: NSFW资源状态; 消耗量: number; 预算不足: boolean; 描述: string } {
  const 消耗量 = 配置[行为类型];
  const 预算不足 = 当前状态.风险预算 < 消耗量;
  const 新预算 = Math.max(0, 当前状态.风险预算 - 消耗量);

  const 描述 = 预算不足
    ? `风险预算不足！需要 ${消耗量}，当前只有 ${当前状态.风险预算}`
    : `消耗了 ${消耗量} 风险预算，剩余 ${新预算}`;

  return {
    新状态: { ...当前状态, 风险预算: 新预算 },
    消耗量,
    预算不足,
    描述,
  };
}

/**
 * 恢复风险预算（每回合自然恢复）
 */
export function 恢复风险预算(
  当前状态: NSFW资源状态,
  恢复量: number = 15,
  上限: number = 100
): NSFW资源状态 {
  return {
    ...当前状态,
    风险预算: Math.min(上限, 当前状态.风险预算 + 恢复量),
  };
}

/**
 * 获得亲密度代币
 */
export function 获得亲密度代币(
  当前状态: NSFW资源状态,
  数量: number,
  原因: string = ''
): { 新状态: NSFW资源状态; 描述: string } {
  const 新代币 = 当前状态.亲密度代币 + 数量;
  const 描述 = 原因
    ? `获得 ${数量} 亲密度代币（${原因}），当前 ${新代币}`
    : `获得 ${数量} 亲密度代币，当前 ${新代币}`;

  return {
    新状态: { ...当前状态, 亲密度代币: 新代币 },
    描述,
  };
}

/**
 * 消费亲密度代币
 */
export function 消费亲密度代币(
  当前状态: NSFW资源状态,
  数量: number,
  用途: string = ''
): { 新状态: NSFW资源状态; 成功: boolean; 描述: string } {
  const 代币不足 = 当前状态.亲密度代币 < 数量;
  if (代币不足) {
    return {
      新状态: 当前状态,
      成功: false,
      描述: `代币不足！需要 ${数量}，当前只有 ${当前状态.亲密度代币}`,
    };
  }

  const 新代币 = 当前状态.亲密度代币 - 数量;
  const 描述 = 用途
    ? `消费 ${数量} 亲密度代币（${用途}），剩余 ${新代币}`
    : `消费 ${数量} 亲密度代币，剩余 ${新代币}`;

  return {
    新状态: { ...当前状态, 亲密度代币: 新代币 },
    成功: true,
    描述,
  };
}

/**
 * 修改声誉值
 */
export function 修改声誉(
  当前状态: NSFW资源状态,
  变化量: number,
  原因: string = ''
): { 新状态: NSFW资源状态; 描述: string } {
  const 新声誉 = Math.max(0, Math.min(100, 当前状态.声誉值 + 变化量));
  const 变化描述 = 变化量 > 0 ? '提升' : '下降';
  const 描述 = 原因
    ? `声誉${变化描述} ${Math.abs(变化量)}（${原因}），当前 ${新声誉}`
    : `声誉${变化描述} ${Math.abs(变化量)}，当前 ${新声誉}`;

  return {
    新状态: { ...当前状态, 声誉值: 新声誉 },
    描述,
  };
}

/**
 * 精力不足惩罚计算
 */
export function 精力不足惩罚(当前精力: number): {
  判断力下降: number;
  风险增加: number;
  NPC反应变差: number;
} {
  if (当前精力 < 10) {
    return { 判断力下降: 30, 风险增加: 25, NPC反应变差: 20 };
  }
  if (当前精力 < 25) {
    return { 判断力下降: 20, 风险增加: 15, NPC反应变差: 10 };
  }
  if (当前精力 < 50) {
    return { 判断力下降: 10, 风险增加: 5, NPC反应变差: 5 };
  }
  return { 判断力下降: 0, 风险增加: 0, NPC反应变差: 0 };
}

/**
 * 获取资源状态摘要（用于 UI 显示）
 */
export function 获取资源摘要(状态: NSFW资源状态): string {
  const 精力状态 = 状态.精力值 < 25 ? '精力不足' : 状态.精力值 < 50 ? '精力一般' : '精力充沛';
  const 声誉状态 = 状态.声誉值 < 30 ? '声誉危机' : 状态.声誉值 < 60 ? '声誉一般' : '声誉良好';

  return `精力: ${状态.精力值}/100 (${精力状态}) | 声誉: ${状态.声誉值}/100 (${声誉状态}) | 代币: ${状态.亲密度代币} | 风险预算: ${状态.风险预算}/100`;
}
