// ==================== 商店函数 ====================

/**
 * 初始化玩家库存
 */
export function 初始化库存(): 玩家库存 {
  return {
    已购道具: [],
    已购数量: {},
    已解锁场景: [],
    已解锁技能: [],
  };
}

/**
 * 获取可购买商品列表
 */
export function 获取可购买商品(库存: 玩家库存): 商店道具[] {
  return 商店道具列表.filter(道具 => {
    if (!道具.限购) return true;
    const 已购次数 = 库存.已购数量[道具.id] ?? 0;
    return 已购次数 < 道具.限购;
  });
}

/**
 * 购买道具
 */
export function 购买道具(
  资源状态: NSFW资源状态,
  库存: 玩家库存,
  道具ID: string
): 购买结果 {
  const 道具 = 商店道具列表.find(d => d.id === 道具ID);
  if (!道具) {
    return { 成功: false, 新资源状态: 资源状态, 道具: {} as 商店道具, 描述: '道具不存在' };
  }

  if (道具.限购) {
    const 已购次数 = 库存.已购数量[道具ID] ?? 0;
    if (已购次数 >= 道具.限购) {
      return { 成功: false, 新资源状态: 资源状态, 道具, 描述: '已达到购买上限' };
    }
  }

  const 代币结果 = 消费亲密度代币(资源状态, 道具.价格, `购买${道具.名称}`);
  if (!代币结果.成功) {
    return { 成功: false, 新资源状态: 代币结果.新状态, 道具, 描述: 代币结果.描述 };
  }

  const 新库存 = {
    ...库存,
    已购道具: [...库存.已购道具, 道具ID],
    已购数量: { ...库存.已购数量, [道具ID]: (库存.已购数量[道具ID] ?? 0) + 1 },
  };
  if (道具.效果.场景解锁) {
    新库存.已解锁场景 = [...库存.已解锁场景, 道具.效果.场景解锁!];
  }
  if (道具.效果.技能解锁) {
    新库存.已解锁技能 = [...库存.已解锁技能, 道具.效果.技能解锁!];
  }

  return {
    成功: true,
    新资源状态: 代币结果.新状态,
    道具,
    描述: `成功购买 ${道具.名称}！${道具.描述}`,
  };
}

/**
 * 使用道具
 */
export function 使用道具(
  资源状态: NSFW资源状态,
  道具: 商店道具
): { 新资源状态: NSFW资源状态; 描述: string } {
  let 新状态 = { ...资源状态 };
  const 效果 = 道具.效果;
  const 描述列表: string[] = [];

  if (效果.信任变化) {
    描述列表.push(`信任度 ${效果.信任变化 > 0 ? '+' : ''}${效果.信任变化}`);
  }
  if (效果.风险变化) {
    描述列表.push(`风险 ${效果.风险变化 > 0 ? '+' : ''}${效果.风险变化}`);
  }
  if (效果.情绪变化) {
    描述列表.push(`情绪 ${效果.情绪变化 > 0 ? '+' : ''}${效果.情绪变化}`);
  }
  if (效果.精力恢复) {
    新状态.精力值 = Math.min(100, 新状态.精力值 + 效果.精力恢复);
    描述列表.push(`精力 +${效果.精力恢复}`);
  }
  if (效果.声誉变化) {
    const 声誉结果 = 修改声誉(新状态, 效果.声誉变化, 道具.名称);
    新状态 = 声誉结果.新状态;
    描述列表.push(声誉结果.描述);
  }

  return {
    新资源状态: 新状态,
    描述: `使用了 ${道具.名称}：${描述列表.join('，')}`,
  };
}
