/**
 * 检查事件链是否满足触发条件
 */
export function 检查事件链触发(
  链: 事件链定义,
  上下文: {
    活跃子系统: 子系统类型[];
    平均亲密度: number;
    平均信任度: number;
    当前声誉: number;
    已解锁场景: string[];
  }
): boolean {
  const 节点 = 链.节点[0];
  const 条件 = 节点.触发条件;

  if (条件.所需子系统 && !上下文.活跃子系统.includes(条件.所需子系统)) return false;
  if (条件.最低亲密度 && 上下文.平均亲密度 < 条件.最低亲密度) return false;
  if (条件.最低信任度 && 上下文.平均信任度 < 条件.最低信任度) return false;
  if (条件.最低声誉 && 上下文.当前声誉 < 条件.最低声誉) return false;
  if (条件.所需场景解锁 && !上下文.已解锁场景.includes(条件.所需场景解锁)) return false;

  return true;
}

/**
 * 检查当前节点是否可执行
 */
export function 检查节点可执行(
  链: 事件链定义,
  进度: 事件链进度,
  上下文: {
    平均亲密度: number;
    平均信任度: number;
    当前声誉: number;
  }
): boolean {
  if (进度.阶段 !== '进行中') return false;
  if (进度.当前节点索引 >= 链.节点.length) return false;

  const 节点 = 链.节点[进度.当前节点索引];
  const 条件 = 节点.触发条件;

  if (条件.最低亲密度 && 上下文.平均亲密度 < 条件.最低亲密度) return false;
  if (条件.最低信任度 && 上下文.平均信任度 < 条件.最低信任度) return false;
  if (条件.最低声誉 && 上下文.当前声誉 < 条件.最低声誉) return false;

  return true;
}

/**
 * 推进事件链到下一个节点
 */
export function 推进事件链(
  链: 事件链定义,
  进度: 事件链进度,
  资源状态: NSFW资源状态,
  路径选择?: '安全' | '激进'
): 事件链结果 {
  if (进度.阶段 !== '进行中') {
    return { 成功: false, 描述: '事件链未进行中' };
  }

  const 当前节点 = 链.节点[进度.当前节点索引];
  const 新路径选择: Record<string, '安全' | '激进'> = { ...进度.路径选择 };

  if (路径选择 && 当前节点.分支选项) {
    新路径选择[当前节点.id] = 路径选择;
  }

  let 新资源 = { ...资源状态 };
  const 描述列表: string[] = [`完成「${当前节点.名称}」`];

  if (当前节点.奖励) {
    if (当前节点.奖励.代币) {
      const 代币结果 = 获得亲密度代币(新资源, 当前节点.奖励.代币, `事件链奖励: ${当前节点.名称}`);
      新资源 = 代币结果.新状态;
      描述列表.push(`获得 ${当前节点.奖励.代币} 代币`);
    }
    if (当前节点.奖励.声誉变化) {
      const 声誉结果 = 修改声誉(新资源, 当前节点.奖励.声誉变化, 当前节点.名称);
      新资源 = 声誉结果.新状态;
      描述列表.push(声誉结果.描述);
    }
  }

  const 是否完成 = 进度.当前节点索引 >= 链.节点.length - 1;

  if (是否完成) {
    if (链.总奖励.代币) {
      const 代币结果 = 获得亲密度代币(新资源, 链.总奖励.代币, `事件链完成奖励: ${链.名称}`);
      新资源 = 代币结果.新状态;
      描述列表.push(`事件链完成！获得 ${链.总奖励.代币} 代币`);
    }
    if (链.总奖励.声誉变化) {
      const 声誉结果 = 修改声誉(新资源, 链.总奖励.声誉变化, `事件链完成: ${链.名称}`);
      新资源 = 声誉结果.新状态;
    }

    return {
      成功: true,
      新资源状态: 新资源,
      新场景解锁: 链.总奖励.成就解锁,
      描述: 描述列表.join('，'),
    };
  }

  return {
    成功: true,
    新资源状态: 新资源,
    描述: 描述列表.join('，'),
  };
}

/**
 * 创建新事件链进度
 */
export function 创建事件链进度(链ID: string): 事件链进度 {
  return {
    链ID,
    阶段: '进行中',
    当前节点索引: 0,
    路径选择: {},
    开始时间: new Date().toISOString(),
  };
}

/**
 * 获取可触发的事件链列表
 */
export function 获取可触发事件链(
  上下文: {
    活跃子系统: 子系统类型[];
    平均亲密度: number;
    平均信任度: number;
    当前声誉: number;
    已解锁场景: string[];
  },
  已有进度: 事件链进度[]
): 事件链定义[] {
  const 已有链ID = new Set(已有进度.map(p => p.链ID));
  return 事件链库.filter(链 => !已有链ID.has(链.id) && 检查事件链触发(链, 上下文));
}

/**
 * 获取活跃事件链列表（含进度）
 */
export function 获取活跃事件链(
  进度列表: 事件链进度[]
): { 链: 事件链定义; 进度: 事件链进度; 当前节点: 事件链节点; 完成百分比: number }[] {
  return 进度列表
    .filter(p => p.阶段 === '进行中')
    .map(进度 => {
      const 链 = 事件链库.find(c => c.id === 进度.链ID)!;
      const 当前节点 = 链.节点[进度.当前节点索引];
      const 完成百分比 = Math.round((进度.当前节点索引 / 链.节点.length) * 100);
      return { 链, 进度, 当前节点, 完成百分比 };
    });
}
