/**
 * 获取教学场景描述
 */
export function 获取教学场景描述(
  环节: string,
  教练昵称: string,
  学员昵称: string,
  环境描述: string
): string {
  const 场景 = OW课程场景[环节] || OW课程场景['泳池训练'];
  
  return `
【${场景.场景名称}】
${环境描述}

${教练昵称}正在教学员${学员昵称}进行${环节}...

${场景.场景描述}

暧昧指数: ${场景.暧昧指数}%
`;
}
