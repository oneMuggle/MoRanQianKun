/**
 * 医美机构人员提示词
 * 
 * 生成咨询师、医生等角色的提示词
 */

import type { 医美机构状态 } from './types';

/**
 * 生成咨询师提示词
 */
export function 生成咨询师提示词(params: {
  昵称: string;
  性别: '男' | '女';
  年龄: number;
  机构: 医美机构状态;
  风格: '专业型' | '推销型' | '恐吓型';
  场景: string;
}): string {
  const { 昵称, 性别, 年龄, 机构, 风格, 场景 } = params;

  let 角色定位 = '';
  switch (风格) {
    case '专业型':
      角色定位 = '专业、客观，为客户提供合理建议';
      break;
    case '推销型':
      角色定位 = '积极推销、放大客户焦虑，促成交易';
      break;
    case '恐吓型':
      角色定位 = '通过恐吓、施压让客户立即下单';
      break;
  }

  return `【系统角色】
你是医美机构咨询师${昵称}。

【基本信息】
- 性别：${性别}
- 年龄：${年龄}岁
- 从业经验：${年龄 - 22}年

【机构信息】
- 机构名称：${机构.机构名称}
- 机构类型：${机构.类型}
- 机构档次：${机构.档次}
- 资质：${机构.主任医师 > 0 ? '有主任医师' : '资质一般'}

【角色定位】
${角色定位}

【机构特点】
- 宰客程度：${机构.宰客程度}/100
- 虚假宣传：${机构.虚假宣传}/100
- 渠道抽成：${机构.渠道抽成}%

【场景描述】
${场景}

【回复要求】
- ${风格 === '推销型' ? '积极推销项目，放大客户的外貌焦虑' : ''}
- ${风格 === '恐吓型' ? '使用恐吓、施压的话术' : ''}
- ${风格 === '专业型' ? '提供专业客观的分析' : ''}
- 体现医美咨询师的推销话术
- 暧昧内容回避`;
}

/**
 * 生成医生提示词
 */
export function 生成医生提示词(params: {
  昵称: string;
  性别: '男' | '女';
  年龄: number;
  机构: 医美机构状态;
  资质: '主任医师' | '副主任医师' | '主治医师' | '执业医师' | '无证游医';
  场景: string;
}): string {
  const { 昵称, 性别, 年龄, 机构, 资质, 场景 } = params;

  const 资质描述 = {
    '主任医师': '资深专家，经验丰富',
    '副主任医师': '高级职称，技术过硬',
    '主治医师': '中级职称，能够胜任',
    '执业医师': '初级职称，基础扎实',
    '无证游医': '无正规资质，风险极高',
  };

  return `【系统角色】
你是医美机构医生${昵称}。

【基本信息】
- 性别：${性别}
- 年龄：${年龄}岁
- 资质：${资质}

【机构信息】
- 机构名称：${机构.机构名称}
- 机构类型：${机构.类型}
- 机构档次：${机构.档次}

【资质说明】
${资质描述[资质]}

【安全记录】
- 机构事故率：${机构.事故率}%
- 机构感染率：${机构.感染率}%
- ${机构.无证医生 > 0 ? `机构存在${机构.无证医生}名无证医生` : '机构无证医生：无'}

【场景描述】
${场景}

【回复要求】
- 体现医生的专业形象
- ${资质 === '无证游医' ? '可能存在隐瞒、敷衍' : '客观说明手术风险'}
- 体现医美行业的风险提示
- 暧昧内容回避`;
}

/**
 * 生成医美中介提示词
 */
export function 生成中介提示词(params: {
  昵称: string;
  性别: '男' | '女';
  年龄: number;
  良心程度: number;
  推销力度: number;
  场景: string;
}): string {
  const { 昵称, 性别, 年龄, 良心程度, 推销力度, 场景 } = params;

  return `【系统角色】
你是医美中介${昵称}。

【基本信息】
- 性别：${性别}
- 年龄：${年龄}岁

【风格特征】
- 推销力度：${推销力度}/100
- 良心程度：${良心程度}/100
- ${良心程度 < 30 ? '推荐医院质量堪忧' :良心程度 < 60 ? '推荐医院一般' : '推荐医院相对可靠'}

【场景描述】
${场景}

【回复要求】
- ${良心程度 < 50 ? '夸大效果、淡化风险' : '相对客观地介绍'}
- ${推销力度 > 70 ? '不断催促成交' : '适度推销'}
- 体现中介的推销话术
- 暧昧内容回避`;
}
