// ==================== 博主成长系统 ====================

export interface 成长结果 {
  新增粉丝: number;
  掉粉: number;
  收益变化: number;
  舆论变化: number;
}

export function 计算博主成长(
  当前状态: 博主状态,
  发布内容质量: number,
  是否争议内容: boolean
): 成长结果 {
  let 新增粉丝 = 0;
  let 掉粉 = 0;
  let 收益变化 = 0;
  let 舆论变化 = 0;

  // 内容质量影响
  if (发布内容质量 >= 80) {
    新增粉丝 = Math.floor(Math.random() * 1000) + 500;
    收益变化 = Math.floor(Math.random() * 1000) + 500;
  } else if (发布内容质量 >= 60) {
    新增粉丝 = Math.floor(Math.random() * 300) + 100;
    收益变化 = Math.floor(Math.random() * 500) + 100;
  } else {
    掉粉 = Math.floor(Math.random() * 100) + 50;
    舆论变化 = 5;
  }

  // 争议内容影响
  if (是否争议内容) {
    新增粉丝 = Math.floor(新增粉丝 * 1.5); // 争议带来流量
    掉粉 = Math.floor(掉粉 + 新增粉丝 * 0.2); // 但也掉粉
    舆论变化 += 20;
  }

  return {
    新增粉丝,
    掉粉,
    收益变化,
    舆论变化,
  };
}

// ==================== 商业合作评估 ====================

export interface 商业合作评估 {
  是否接受: boolean;
  推荐程度: '强烈推荐' | '可以考虑' | '建议拒绝';
  收益: number;
  舆论风险: number;
  理由: string[];
}

export function 评估商业合作(
  博主: 博主状态,
  合作类型: '品牌广告' | '带货' | '联名' | 'PR赠送',
  产品口碑: '好' | '中' | '差'
): 商业合作评估 {
  const 理由: string[] = [];
  let 推荐程度: '强烈推荐' | '可以考虑' | '建议拒绝' = '可以考虑';
  let 收益 = 0;
  let 舆论风险 = 0;

  // 基础收益
  switch (合作类型) {
    case 'PR赠送':
      收益 = 0;
      break;
    case '品牌广告':
      收益 = Math.floor(博主.粉丝数 * 0.1);
      break;
    case '带货':
      收益 = Math.floor(博主.粉丝数 * 0.2);
      break;
    case '联名':
      收益 = Math.floor(博主.粉丝数 * 0.5);
      break;
  }

  // 产品口碑影响
  if (产品口碑 === '差') {
    舆论风险 += 30;
    推荐程度 = '建议拒绝';
    理由.push('产品口碑差会损害博主信誉');
  } else if (产品口碑 === '中') {
    舆论风险 += 10;
    推荐程度 = '可以考虑';
    理由.push('需要谨慎评估产品质量');
  } else {
    推荐程度 = '强烈推荐';
    理由.push('好产品值得推荐');
  }

  // 博主状态影响
  if (博主.舆论状态 === '危机') {
    推荐程度 = '建议拒绝';
    舆论风险 += 20;
    理由.push('当前舆论状态不佳，不宜接广告');
  } else if (博主.舆论状态 === '大争议') {
    推荐程度 = '建议拒绝';
    理由.push('有大争议时接广告风险太高');
  }

  // 收益太低
  if (收益 < 1000 && 合作类型 !== 'PR赠送') {
    推荐程度 = '建议拒绝';
    理由.push('收益太低，不值得冒险');
  }

  return {
    是否接受: 推荐程度 === '强烈推荐',
    推荐程度,
    收益,
    舆论风险,
    理由,
  };
}

// ==================== 舆论危机处理 ====================

export interface 危机处理结果 {
  成功: boolean;
  舆论变化: number;
  粉丝变化: number;
  后续建议: string[];
}

export function 处理舆论危机(
  当前状态: 博主状态,
  处理方式: '沉默' | '澄清' | '道歉' | '起诉'
): 危机处理结果 {
  let 成功 = false;
  let 舆论变化 = 0;
  let 粉丝变化 = 0;
  const 后续建议: string[] = [];

  switch (处理方式) {
    case '沉默':
      舆论变化 = -10;
      粉丝变化 = -Math.floor(Math.random() * 500);
      后续建议.push('保持沉默可能让舆论发酵');
      后续建议.push('准备后续澄清方案');
      break;

    case '澄清':
      舆论变化 = Math.random() > 0.5 ? 20 : -10;
      粉丝变化 = 舆论变化 > 0 ? Math.floor(Math.random() * 200) : -Math.floor(Math.random() * 300);
      成功 = 舆论变化 > 0;
      后续建议.push('提供证据支持澄清');
      后续建议.push('争取粉丝理解');
      break;

    case '道歉':
      舆论变化 = Math.random() > 0.3 ? 15 : -15;
      粉丝变化 = 舆论变化 > 0 ? Math.floor(Math.random() * 100) : -Math.floor(Math.random() * 500);
      成功 = 舆论变化 > 0;
      后续建议.push('态度要诚恳');
      后续建议.push('提出具体改进措施');
      break;

    case '起诉':
      舆论变化 = Math.random() > 0.4 ? 25 : -5;
      粉丝变化 = 舆论变化 > 0 ? Math.floor(Math.random() * 300) : -Math.floor(Math.random() * 200);
      成功 = 舆论变化 > 0;
      后续建议.push('使用法律武器保护自己');
      后续建议.push('必要时可以发律师函');
      break;
  }

  return {
    成功,
    舆论变化,
    粉丝变化,
    后续建议,
  };
}

// ==================== 宠物走网红路线评估 ====================

export interface 网红潜力评估 {
  是否适合: boolean;
  潜力等级: '低' | '中' | '高' | '爆款';
  适合类型: 博主类型[];
  建议: string[];
}

export function 评估网红潜力(
  宠物: 宠物核心状态
): 网红潜力评估 {
  const 建议: string[] = [];
  let 潜力分数 = 50;

  // 品种因素
  if (宠物.品种 === '赛级') {
    潜力分数 += 30;
    建议.push('赛级品种有天然流量优势');
  } else if (宠物.品种 === '纯种') {
    潜力分数 += 15;
  } else if (宠物.品种 === '串串') {
    潜力分数 += 10; // 串串反而可能更吸睛
    建议.push('串串的独特性可能成为卖点');
  }

  // 外观因素
  if (宠物.外观值 > 80) {
    潜力分数 += 20;
    建议.push('高外观值适合走颜值路线');
  }

  // 性格因素
  if (宠物.亲人度 > 80) {
    潜力分数 += 15;
    建议.push('亲人性格更容易拍摄');
  }

  if (宠物.攻击性 > 50) {
    潜力分数 -= 10;
    建议.push('攻击性可能影响拍摄安全');
  }

  // 名字因素
  if (宠物.名称 && ['奶茶', '布丁', '团子', '豆豆'].some(n => 宠物.名称.includes(n))) {
    潜力分数 += 10;
    建议.push('可爱名字更容易传播');
  }

  let 潜力等级: '低' | '中' | '高' | '爆款' = '低';
  if (潜力分数 >= 90) 潜力等级 = '爆款';
  else if (潜力分数 >= 70) 潜力等级 = '高';
  else if (潜力分数 >= 50) 潜力等级 = '中';

  const 适合类型: 博主类型[] = 潜力等级 === '爆款' 
    ? ['搞笑剧情型', '才艺表演型', '日常分享型']
    : 潜力等级 === '高'
    ? ['日常分享型', '才艺表演型']
    : 潜力等级 === '中'
    ? ['日常分享型']
    : [];

  return {
    是否适合: 潜力分数 >= 50,
    潜力等级,
    适合类型,
    建议,
  };
}
