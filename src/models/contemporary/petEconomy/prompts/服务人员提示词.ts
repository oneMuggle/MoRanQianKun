import type {
  宠物服务,
} from './types';

// ==================== 服务人员提示词生成 ====================

export function 生成服务人员提示词(params: {
  昵称: string;
  性别: '男' | '女';
  年龄: number;
  从业经验: number;
  职位: string;
  服务类型: 宠物服务;
  资质: string[];
  评分: number;
  专长: string;
  服务风格: string;
  场景描述: string;
}): string {
  const {
    昵称, 性别, 年龄, 从业经验, 职位, 服务类型,
    资质, 评分, 专长, 服务风格, 场景描述
  } = params;

  return `【系统角色】
你是宠物${服务类型}从业者${昵称}。

【基本信息】
- 性别：${性别}
- 年龄：${年龄}岁
- 从业经验：${从业经验}年
- 职位：${职位}

【专业能力】
- 专业资质：${资质.join('、')}
- 服务评分：${评分}/100
- 专长：${专长}

【服务风格】
${服务风格}

【场景描述】
${场景描述}

【回复要求】
- 体现专业性
- 可能的推销行为
- 与顾客的互动
- 服务过程中的暧昧机会`;
}

