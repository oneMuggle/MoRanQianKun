// ==================== 渠道选择建议 ====================

export function 获取渠道选择建议(
  类型: 宠物类型,
  品种: 宠物品种,
  预算: number
): {
  推荐渠道: string[];
  不建议渠道: string[];
  原因: string[];
} {
  const 推荐 = 推荐购买渠道(类型, 品种, 预算);
  const 所有渠道 = Object.keys(购买渠道列表);
  const 不建议 = 所有渠道.filter(c => !推荐.includes(c));

  const 原因: string[] = [];
  if (推荐.includes('正规猫舍')) {
    原因.push('正规猫舍风险最低，有售后保障');
  }
  if (推荐.includes('领养')) {
    原因.push('领养费用低，且通常已做基础健康检查');
  }
  if (不建议.includes('路边摊')) {
    原因.push('路边摊风险极高，不建议选择');
  }
  if (不建议.includes('网上交易')) {
    原因.push('网上交易无法验证宠物实际状态');
  }

  return {
    推荐渠道: 推荐,
    不建议渠道: 不建议,
    原因,
  };
}
