// ==================== 过度医疗识别 ====================

export function 识别过度医疗(
  建议内容: string[],
  症状: string
): {
  是过度医疗: boolean;
  问题类型: string[];
  合理建议: string[];
  节省金额: number;
  应对话术: string[];
} {
  const 检测结果 = 检测过度医疗(建议内容, 症状);

  // 估算可能节省的金额
  let 节省金额 = 0;
  if (检测结果.不必要检查.includes('全身检查/高级影像检查')) {
    节省金额 += 1500;
  }
  if (检测结果.过度类型.includes('进口药过度使用')) {
    节省金额 += 300;
  }

  // 应对话术
  const 应对话术 = [
    '这项检查是必要的吗？不做会怎样？',
    '有没有更便宜但同样有效的方案？',
    '能先做基础检查，明确问题后再决定吗？',
    '国产药和进口药有什么区别？',
  ];

  return {
    是过度医疗: 检测结果.是否过度,
    问题类型: 检测结果.过度类型,
    合理建议: 检测结果.合理检查,
    节省金额,
    应对话术,
  };
}

// ==================== 医疗费用评估 ====================

export function 评估医疗方案(
  医疗类型: 医疗类型,
  宠物体重: number,
  是否有医保: boolean
): {
  总费用: number;
  费用明细: string;
  自付费用: number;
  合理评价: '合理' | '偏高' | '离谱';
  节省建议: string[];
} {
  const 费用 = 计算医疗费用(医疗类型, 宠物体重, false);

  // 假设宠物医保报销30%
  const 医保报销 = 是否有医保 ? Math.floor(费用.总费用 * 0.3) : 0;
  const 自付费用 = 费用.总费用 - 医保报销;

  let 合理评价: '合理' | '偏高' | '离谱' = '合理';
  if (费用.总费用 > 10000) {
    合理评价 = '离谱';
  } else if (费用.总费用 > 5000) {
    合理评价 = '偏高';
  }

  return {
    总费用: 费用.总费用,
    费用明细: 费用.明细.map(d => `${d.项目}：${d.费用}元`).join('；'),
    自付费用,
    合理评价,
    节省建议: 费用.建议,
  };
}

