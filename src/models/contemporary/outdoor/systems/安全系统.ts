// ==================== 教练红线和评分 ====================

export interface 教练红线指标 {
  有偷拍投诉: boolean;
  有越界记录: boolean;
  无资质: boolean;
  场所偏僻: boolean;
  拒绝签合同: boolean;
}

export function 评估教练可信度(
  教练: 教练领队核心状态,
  红线: 教练红线指标
): {
  可信度评分: number;
  建议: '强烈推荐' | '可以接受' | '谨慎选择' | '强烈不推荐';
  风险提示: string[];
} {
  const 风险提示: string[] = [];
  let 可信度评分 = 100;

  // 红线检查
  if (红线.有偷拍投诉) {
    可信度评分 -= 50;
    风险提示.push("该教练有偷拍投诉记录");
  }

  if (红线.有越界记录) {
    可信度评分 -= 30;
    风险提示.push("该教练有越界骚扰记录");
  }

  if (红线.无资质) {
    可信度评分 -= 20;
    风险提示.push("该教练无专业资质");
  }

  if (红线.场所偏僻) {
    可信度评分 -= 15;
    风险提示.push("活动场所过于偏僻，存在风险");
  }

  if (红线.拒绝签合同) {
    可信度评分 -= 20;
    风险提示.push("该教练拒绝签订合同/协议");
  }

  // 口碑调整
  if (教练.被投诉次数 > 0) {
    可信度评分 -= Math.min(30, 教练.被投诉次数 * 5);
    风险提示.push(`该教练有${教练.被投诉次数}次投诉记录`);
  }

  if (教练.越界投诉次数 > 0) {
    可信度评分 -= Math.min(40, 教练.越界投诉次数 * 10);
    风险提示.push(`该教练有${教练.越界投诉次数}次越界投诉`);
  }

  if (教练.圈内警告次数 > 0) {
    可信度评分 -= Math.min(30, 教练.圈内警告次数 * 10);
    风险提示.push(`该教练在圈内被警告过${教练.圈内警告次数}次`);
  }

  // 资质加成
  if (教练.资质等级 === '国际认证') {
    可信度评分 += 10;
  } else if (教练.资质等级 === '高级') {
    可信度评分 += 5;
  }

  可信度评分 = Math.max(0, Math.min(100, 可信度评分));

  let 建议: '强烈推荐' | '可以接受' | '谨慎选择' | '强烈不推荐';
  if (可信度评分 >= 80) {
    建议 = '强烈推荐';
  } else if (可信度评分 >= 60) {
    建议 = '可以接受';
  } else if (可信度评分 >= 40) {
    建议 = '谨慎选择';
  } else {
    建议 = '强烈不推荐';
  }

  return { 可信度评分, 建议, 风险提示 };
}

