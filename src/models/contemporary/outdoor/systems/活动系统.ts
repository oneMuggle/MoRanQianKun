export function 处理尺度递进(
  当前活动: 活动项目状态,
  触发条件: string
): {
  是否递进: boolean;
  新尺度: 活动尺度;
  递进事件?: 尺度递进事件;
} {
  const 递进项 = 尺度递进场景.find(s => s.触发条件 === 触发条件);
  
  if (!递进项) {
    return { 是否递进: false, 新尺度: 当前活动.实际尺度 };
  }

  // 检查是否已经超过允许的最大尺度
  if (当前活动.约定尺度 === '正常' && 递进项.新尺度 !== '正常') {
    return { 
      是否递进: true, 
      新尺度: 递进项.新尺度,
      递进事件: {
        ...递进项,
        参与者反应: '犹豫',
      },
    };
  }

  return { 
    是否递进: true, 
    新尺度: 递进项.新尺度,
    递进事件: 递进项,
  };
}

// ==================== 肾上腺素影响计算 ====================

export function 计算肾上腺素影响(
  状态: '挑战成功' | '濒临危险' | '极限状态',
  基础值: number
): {
  判断力下降: number;
  边界模糊: number;
  羞耻心: number;
  自我保护意识: number;
  新值: number;
} {
  const 影响表 = {
    '挑战成功': {
      判断力下降: 20,
      边界模糊: 15,
      羞耻心: -10,
      自我保护意识: -5,
    },
    '濒临危险': {
      判断力下降: 40,
      边界模糊: 25,
      羞耻心: -15,
      自我保护意识: -20,
    },
    '极限状态': {
      判断力下降: 30,
      边界模糊: 20,
      羞耻心: -20,
      自我保护意识: -30,
    },
  };

  const 影响 = 影响表[状态];
  
  return {
    ...影响,
    新值: Math.max(0, Math.min(100, 基础值 + 影响.判断力下降)),
  };
}

// ==================== 活动阶段更新 ====================

export type 活动阶段 = '集合' | '出发' | '进行中' | '休息' | '营地' | '结束';

export function 获取下一阶段(当前阶段: 活动阶段): 活动阶段 {
  const 阶段顺序: 活动阶段[] = ['集合', '出发', '进行中', '休息', '营地', '结束'];
  const 当前索引 = 阶段顺序.indexOf(当前阶段);
  if (当前索引 === -1 || 当前索引 === 阶段顺序.length - 1) {
    return '结束';
  }
  return 阶段顺序[当前索引 + 1];
}

export function 是否应该进入危险阶段(当前阶段: 活动阶段): boolean {
  return ['休息', '营地'].includes(当前阶段);
}
