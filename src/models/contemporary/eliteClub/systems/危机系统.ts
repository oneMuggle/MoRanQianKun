// ==================== 危机评估器 ====================

export class 危机评估器 {
  /**
   * 评估危机等级
   */
  评估危机等级(
    类型: 危机类型,
    涉及金额?: number,
    证据完备度?: number
  ): 危机等级 {
    switch (类型) {
      case '隐私泄露':
        if (证据完备度 && 证据完备度 > 80) return '严重';
        return '一般';

      case '仙人跳':
        if (涉及金额 && 涉及金额 > 10000) return '极度严重';
        return '严重';

      case '敲诈勒索':
        if (涉及金额 && 涉及金额 > 50000) return '极度严重';
        return '严重';

      case '服务纠纷':
        return '一般';

      case '财务纠纷':
        if (涉及金额 && 涉及金额 > 5000) return '严重';
        return '轻微';

      case '人身安全':
        return '严重';

      case '技师被骚扰':
        return '严重';

      case '场所被查':
        return '极度严重';

      default:
        return '一般';
    }
  }

  /**
   * 评估会员风险值
   */
  评估会员风险(会员: 会员核心状态): {
    综合风险: number;
    最高风险: string;
    风险分析: string[];
  } {
    const 风险项: { 名称: string; 值: number }[] = [
      { 名称: '酒后乱性风险', 值: 会员.酒后乱性风险 },
      { 名称: '被仙人跳风险', 值: 会员.被仙人跳风险 },
      { 名称: '隐私保护不足', 值: 100 - 会员.隐私保护 },
      { 名称: '防范意识不足', 值: 100 - 会员.防范意识 },
    ];

    const 风险分析 = 风险项
      .filter(r => r.值 > 60)
      .map(r => `${r.名称}较高：${r.值}/100`);

    const 综合风险 = Math.round(
      风险项.reduce((sum, r) => sum + r.值, 0) / 风险项.length
    );

    const 最高风险项 = 风险项.reduce((max, r) => (r.值 > max.值 ? r : max));

    return {
      综合风险,
      最高风险: 最高风险项.名称,
      风险分析,
    };
  }

  /**
   * 检查是否触发危机
   */
  检查是否触发危机(
    会员: 会员核心状态,
    会所: 会所状态,
    当前时间: Date
  ): {
    是否触发: boolean;
    危机类型?: 危机类型;
    触发概率?: number;
    警告信息?: string;
  } {
    // 深夜时段风险增加
    const 小时 = 当前时间.getHours();
    const 是深夜时段 = 小时 >= 22 || 小时 < 4;

    // 醉酒状态风险增加
    const 醉酒风险 = 会员.酒后乱性风险;

    // 会所安全等级
    const 安全风险 = 100 - 会所.隐私保护;

    // 综合风险计算
    let 综合风险 = (醉酒风险 + 安全风险) / 2;
    if (是深夜时段) 综合风险 *= 1.3;

    // 触发危机的阈值
    if (综合风险 > 80) {
      return {
        是否触发: true,
        危机类型: '仙人跳',
        触发概率: 综合风险,
        警告信息: '检测到较高风险，建议谨慎行动',
      };
    }

    if (综合风险 > 60) {
      return {
        是否触发: true,
        危机类型: '服务纠纷',
        触发概率: 综合风险,
        警告信息: '存在一定风险，请注意安全',
      };
    }

    return { 是否触发: false };
  }
}

// ==================== 危机处理 ====================

export class 危机处理器 {
  private 危机记录列表: 危机事件记录[] = [];

  /**
   * 记录新危机
   */
  记录危机(
    类型: 危机类型,
    等级: 危机等级,
    涉及方: ('会员' | '技师' | '会所')[],
    描述: string
  ): 危机事件记录 {
    const 危机: 危机事件记录 = {
      危机ID: `CRISIS_${Date.now()}_${Math.random().toString(36).substr(2, 6)}`,
      类型,
      等级,
      涉及方,
      发生时间: new Date().toISOString(),
      描述,
      处理状态: '待处理',
    };

    this.危机记录列表.push(危机);
    return 危机;
  }

  /**
   * 更新危机状态
   */
  更新危机状态(
    危机ID: string,
    新状态: 危机事件记录['处理状态'],
    解决方案?: string
  ): boolean {
    const 危机 = this.危机记录列表.find(c => c.危机ID === 危机ID);
    if (危机) {
      危机.处理状态 = 新状态;
      if (解决方案) 危机.解决方案 = 解决方案;
      return true;
    }
    return false;
  }

  /**
   * 获取危机历史
   */
  获取危机历史(): 危机事件记录[] {
    return [...this.危机记录列表];
  }

  /**
   * 生成危机应对建议
   */
  生成应对建议(危机: 危机事件记录): string {
    const 场景配置 = 危机场景库[危机.类型];
    if (!场景配置 || 场景配置.length === 0) {
      return '请谨慎处理，必要时报警或寻求法律帮助。';
    }

    const 配置 = 场景配置[0];
    return `【${危机.类型}应对指南】

1. 保持冷静，不要恐慌
2. ${配置.应对策略[0] || '立即收集证据'}
3. ${配置.应对策略[1] || '考虑报警'}
4. ${配置.应对策略[2] || '寻求法律帮助'}

如有需要，可联系会所客服协助处理。`;
  }
}
