/**
 * 艺人核心状态
 */

import type {
  艺人生涯阶段,
  艺人类型,
  经纪公司等级,
  合同状态,
  恋情状态,
  潜规则程度,
  潜规则类型,
  潜规则应对,
  艺人危机,
  私生事件,
  艺人危机类型,
  危机阶段,
  危机处理方式,
} from './types';

export interface 艺人核心状态 {
  // 基础信息
  艺人ID: string;
  艺名: string;
  本名?: string;               // 隐藏信息
  性别: '男' | '女';
  年龄: number;
  身高: number;
  体重: number;

  // 职业生涯
  生涯阶段: 艺人生涯阶段;
  艺人类型: 艺人类型[];
  出道时间?: string;
  代表作品?: string[];
  粉丝数量: number;
  微博超话排名: number;

  // 经纪公司
  所属公司ID?: string;
  公司等级: 经纪公司等级;
  合同状态: 合同状态;
  合同到期日?: string;
  违约金: number;

  // 人设与形象
  对外人设: string;            // 官方人设描述
  真实性格?: string;           // 实际性格
  人设崩塌度: number;          // 0-100，人设与真实差距
  完美度要求: number;          // 公司对形象的要求程度
  恋情状态: 恋情状态;

  // 潜规则经历
  潜规则程度: 潜规则程度;
  潜规则类型经历: 潜规则类型[];
  潜规则应对历史: 潜规则应对[];
  心理创伤度: number;          // 0-100
  职业倦怠度: number;          // 0-100

  // 资源与机会
  资源获取能力: number;        // 0-100，能拿到好资源的能力
  机会数量: number;           // 近期获得的机会
  被替换风险: number;          // 0-100，被新人替代的风险

  // 舆论与名声
  公众形象: number;            // 0-100，正面形象
  业内口碑: number;            // 0-100，业内评价
  黑粉数量: number;
  负面新闻数: number;

  // 危机状态
  当前危机?: 艺人危机;
  历史危机次数: number;
  舆论敏感度: number;          // 0-100，对负面新闻的敏感程度

  // 私生饭骚扰
  私生骚扰等级: number;        // 0-10，当前被私生骚扰程度
  私生行为记录: 私生事件[];
  被跟踪次数: number;

  // 心理状态
  自信度: number;              // 0-100
  压抑度: number;              // 0-100，表达真实自我的压抑
  孤独感: number;              // 0-100
  匿名表达欲: number;          // 0-100，想说真话的冲动
}

