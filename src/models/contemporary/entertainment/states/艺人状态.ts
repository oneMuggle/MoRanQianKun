/**
 * 更新艺人危机状态
 */
export function 更新危机状态(
  状态: 艺人核心状态,
  危机类型: 艺人危机类型,
  处理方式: 危机处理方式
): 艺人核心状态 {
  const 新危机: 艺人危机 = {
    危机ID: `危机_${Date.now()}`,
    危机类型,
    爆发时间: new Date().toISOString(),
    当前阶段: '苗头' as const,
    讨论量: 0,
    处理方式,
    对事业影响: {
      粉丝流失: 0,
      商务损失: 0,
      资源损失: 0,
    },
  };

  return {
    ...状态,
    当前危机: 新危机,
    历史危机次数: 状态.历史危机次数 + 1,
  };
}

/**
 * 添加潜规则经历
 */
export function 添加潜规则经历(
  状态: 艺人核心状态,
  类型: 潜规则类型,
  应对: 潜规则应对
): 艺人核心状态 {
  return {
    ...状态,
    潜规则类型经历: [...状态.潜规则类型经历, 类型],
    潜规则应对历史: [...状态.潜规则应对历史, 应对],
    心理创伤度: Math.min(100, 状态.心理创伤度 + (应对 === '接受' ? 15 : 应对 === '反抗' ? 5 : 10)),
  };
}

/**
 * 添加私生事件
 */
export function 添加私生事件(
  状态: 艺人核心状态,
  事件: 私生事件
): 艺人核心状态 {
  return {
    ...状态,
    私生行为记录: [...状态.私生行为记录, 事件],
    被跟踪次数: 状态.被跟踪次数 + 1,
    私生骚扰等级: Math.min(10, 状态.私生骚扰等级 + 1),
    压抑度: Math.min(100, 状态.压抑度 + 10),
  };
}
