/**
 * 粉丝核心状态
 */

import type {
  粉丝类型,
  私生行为等级,
  与偶像关系认知,
} from './types';

export interface 粉丝核心状态 {
  粉丝ID: string;
  昵称: string;
  性别: '男' | '女';
  年龄: number;

  // 追星状态
  追星时长: number;           // 年数
  投入金额: number;           // 累计投入金额
  投入时间: number;           // 累计投入时间（小时）
  粉的艺人数量: number;       // 同时粉几人

  // 粉丝类型
  粉丝类型: 粉丝类型;
  疯狂程度: number;           // 0-100，追星疯狂程度

  // 与偶像关系认知
  与偶像关系认知: 与偶像关系认知;
  幻想程度: number;           // 0-100，对与偶像关系的幻想程度

  // 行为记录
  私生行为: 私生行为等级;
  跟踪记录?: string[];        // 具体跟踪行为
  网络攻击记录?: string[];    // 攻击其他粉丝/艺人的行为
  控评经历?: string[];        // 控评经历

  // 心理状态
  追星依赖度: number;         // 0-100，对追星的依赖
  情绪受偶像影响度: number;   // 0-100，偶像事件对情绪的影响
  现实社交能力: number;       // 0-100，现实社交能力

  // 经济状况
  月可支配收入: number;
  追星占比: number;           // 收入中用于追星的比例
}

/**
 * 创建默认粉丝状态
 */
export function 创建默认粉丝状态(
  粉丝ID: string,
  昵称: string,
  性别: '男' | '女',
  年龄: number
): 粉丝核心状态 {
  return {
    粉丝ID,
    昵称,
    性别,
    年龄,
    追星时长: 0,
    投入金额: 0,
    投入时间: 0,
    粉的艺人数量: 1,
    粉丝类型: '普通粉丝',
    疯狂程度: 30,
    与偶像关系认知: '普通粉丝',
    幻想程度: 30,
    私生行为: '正常追星',
    追星依赖度: 30,
    情绪受偶像影响度: 50,
    现实社交能力: 60,
    月可支配收入: 5000,
    追星占比: 10,
  };
}

/**
 * 增加投入
 */
export function 增加粉丝投入(
  状态: 粉丝核心状态,
  金额: number,
  时间: number
): 粉丝核心状态 {
  return {
    ...状态,
    投入金额: 状态.投入金额 + 金额,
    投入时间: 状态.投入时间 + 时间,
  };
}

/**
 * 升级粉丝类型
 */
export function 升级粉丝类型(状态: 粉丝核心状态): 粉丝核心状态 {
  const 类型升级表: Record<粉丝类型, 粉丝类型> = {
    '普通粉丝': '核心粉丝',
    '核心粉丝': '大粉',
    '大粉': '粉头',
    '粉头': '粉头',  // 粉头是组织者，不再升级
    '私生饭': '私生饭',
    'anti粉': 'anti粉',
  };

  const 新类型 = 类型升级表[状态.粉丝类型];

  return {
    ...状态,
    粉丝类型: 新类型,
    疯狂程度: Math.min(100, 状态.疯狂程度 + 10),
  };
}

/**
 * 记录私生行为
 */
export function 记录私生行为(
  状态: 粉丝核心状态,
  行为描述: string
): 粉丝核心状态 {
  const 行为等级升级表: Record<私生行为等级, 私生行为等级> = {
    '正常追星': '过度关注',
    '过度关注': '轻微越界',
    '轻微越界': '严重越界',
    '严重越界': '犯罪行为',
    '犯罪行为': '犯罪行为',
  };

  return {
    ...状态,
    粉丝类型: '私生饭',
    私生行为: 行为等级升级表[状态.私生行为],
    跟踪记录: [...(状态.跟踪记录 || []), 行为描述],
    疯狂程度: Math.min(100, 状态.疯狂程度 + 20),
  };
}

/**
 * 添加控评经历
 */
export function 添加控评经历(
  状态: 粉丝核心状态,
  控评描述: string
): 粉丝核心状态 {
  return {
    ...状态,
    控评经历: [...(状态.控评经历 || []), 控评描述],
  };
}

/**
 * 转为anti粉
 */
export function 转为Anti粉(状态: 粉丝核心状态): 粉丝核心状态 {
  return {
    ...状态,
    粉丝类型: 'anti粉',
    与偶像关系认知: '普通粉丝',
    幻想程度: 0,
  };
}
