/**
 * 骚扰等级计算
 */
export function 计算骚扰等级(状态: 艺人核心状态): number {
  const 基础等级 = 状态.私生骚扰等级;
  const 跟踪次数加权 = Math.min(状态.被跟踪次数 * 0.5, 5);
  const 负面新闻加权 = 状态.负面新闻数 * 0.3;
  
  return Math.min(10, Math.round(基础等级 + 跟踪次数加权 + 负面新闻加权));
}

/**
 * 行为升级判断
 */
export function 是否会升级行为(粉丝状态: 粉丝核心状态): boolean {
  const 升级概率 = (粉丝状态.疯狂程度 / 100) * 0.3;
  const 当前等级 = 私生行为分级配置表[粉丝状态.私生行为].行为等级;
  
  if (当前等级 === '犯罪行为') return false;
  
  return Math.random() < 升级概率;
}

/**
 * 创建私生事件
 */
export function 创建私生事件(
  艺人ID: string,
  类型: 私生饭类型,
  方式: 跟踪方式,
  严重程度: number
): 私生事件 {
  return {
    事件ID: `私生_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`,
    发生时间: new Date().toISOString(),
    私生类型: 类型,
    跟踪方式: 方式,
    严重程度: Math.min(100, 严重程度),
  };
}

/**
 * 艺人被私生骚扰后的状态更新
 */
export function 处理私生骚扰事件(
  艺人状态: 艺人核心状态,
  事件: 私生事件
): 艺人核心状态 {
  const 跟踪配置 = 跟踪方式配置表[事件.跟踪方式];
  const 行为配置 = 私生行为分级配置表[艺人状态.私生骚扰等级 >= 7 ? '严重越界' : 
                    艺人状态.私生骚扰等级 >= 4 ? '轻微越界' : '过度关注'];

  return {
    ...艺人状态,
    私生骚扰等级: Math.min(10, 艺人状态.私生骚扰等级 + 1),
    被跟踪次数: 艺人状态.被跟踪次数 + 1,
    压抑度: Math.min(100, 艺人状态.压抑度 + 跟踪配置.风险等级 * 0.5),
    孤独感: Math.min(100, 艺人状态.孤独感 + 10),
    匿名表达欲: Math.min(100, 艺人状态.匿名表达欲 + 5),
    私生行为记录: [...艺人状态.私生行为记录, 事件],
  };
}

/**
 * 粉丝私生行为后果
 */
export function 粉丝私生行为后果(粉丝状态: 粉丝核心状态): {
  被抓概率: number;
  法律后果: string;
  心理变化: Partial<粉丝核心状态>;
} {
  const 当前行为配置 = 私生行为分级配置表[粉丝状态.私生行为];
  
  // 被抓概率受粉丝疯狂程度和是否有前科影响
  const 基础被抓概率 = 当前行为配置.被抓概率;
  const 疯狂程度加成 = 粉丝状态.疯狂程度 > 80 ? 20 : 粉丝状态.疯狂程度 > 60 ? 10 : 0;
  const 前科加成 = (粉丝状态.跟踪记录?.length || 0) > 3 ? 15 : 0;
  
  return {
    被抓概率: Math.min(95, 基础被抓概率 + 疯狂程度加成 + 前科加成),
    法律后果: 跟踪方式配置表[(粉丝状态.跟踪记录?.[粉丝状态.跟踪记录?.length - 1]) as 跟踪方式 || '行程跟踪']?.法律后果 || '未知',
    心理变化: {
      追星依赖度: Math.min(100, 粉丝状态.追星依赖度 + 5),
      现实社交能力: Math.max(0, 粉丝状态.现实社交能力 - 10),
    },
  };
}

/**
 * 生成私生事件描述
 */
export function 生成私生事件描述(事件: 私生事件): string {
  const 类型描述 = 私生饭类型描述表[事件.私生类型];
  const 方式描述 = 跟踪方式配置表[事件.跟踪方式];

  return `【私生事件】\n` +
    `事件ID：${事件.事件ID}\n` +
    `发生时间：${事件.发生时间}\n` +
    `私生饭类型：${事件.私生类型}\n` +
    `行为描述：${类型描述}\n` +
    `跟踪方式：${事件.跟踪方式}\n` +
    `风险等级：${方式描述.风险等级}\n` +
    `严重程度：${事件.严重程度}\n` +
    `法律后果：${方式描述.法律后果}\n`;
}
