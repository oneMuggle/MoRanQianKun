/**
 * 执行月度培训
 */
export function 执行月度培训(状态: 练习生核心状态): 练习生核心状态 {
  const 配置 = 培训阶段配置表[状态.培训等级];
  
  return {
    ...状态,
    培训时长: 状态.培训时长 + 1,
    培训费用: 状态.培训费用 + 配置.费用,
    唱功: Math.min(100, 状态.唱功 + 配置.能力提升.唱功),
    舞蹈: Math.min(100, 状态.舞蹈 + 配置.能力提升.舞蹈),
    表现力: Math.min(100, 状态.表现力 + 配置.能力提升.表现力),
    外貌: Math.min(100, 状态.外貌 + 配置.能力提升.外貌),
    粉丝缘: Math.min(100, 状态.粉丝缘 + 配置.能力提升.粉丝缘),
    焦虑度: Math.min(100, 状态.焦虑度 + 2),
  };
}

/**
 * 评估培训等级
 */
export function 评估培训等级(状态: 练习生核心状态): 培训等级 {
  const 总分 = 状态.唱功 + 状态.舞蹈 + 状态.表现力 + 状态.外貌;
  const 平均分 = 总分 / 4;

  if (状态.培训时长 < 6) {
    return '低级';
  } else if (状态.培训时长 < 18) {
    return 平均分 >= 60 ? '中级' : '低级';
  } else if (状态.培训时长 < 36) {
    return 平均分 >= 70 ? '高级' : '中级';
  } else {
    return '出道预备';
  }
}

/**
 * 计算出道预测
 */
export function 计算出道预测(状态: 练习生核心状态): number {
  const 能力总分 = 状态.唱功 + 状态.舞蹈 + 状态.表现力 + 状态.外貌 + 状态.粉丝缘;
  const 心理总分 = 状态.出道渴望度 + 状态.坚持底线意愿;
  const 支持度 = 状态.公司支持度;

  // 综合评分：能力占60%，心理占20%，公司支持占20%
  const 综合评分 = (能力总分 / 5) * 0.6 + (心理总分 / 2) * 0.2 + 支持度 * 0.2;

  // 转换为出道预测排名（越低越好，0表示不确定）
  if (综合评分 >= 85) return 1;
  if (综合评分 >= 75) return 2;
  if (综合评分 >= 65) return 3;
  if (综合评分 >= 55) return 5;
  if (综合评分 >= 45) return 8;
  return 0;  // 不确定
}

/**
 * 选秀排名概率
 */
export function 计算选秀排名概率(
  练习生状态: 练习生核心状态,
  选秀总人数: number,
  出道名额: number
): number {
  const 能力评估 = (练习生状态.唱功 + 练习生状态.舞蹈 + 练习生状态.表现力 + 练习生状态.外貌 + 练习生状态.粉丝缘) / 5;
  
  // 能力、粉丝缘、培训等级综合计算
  const 出道概率 = (能力评估 * 0.4 + 练习生状态.粉丝缘 * 0.3 + (练习生状态.培训时长 / 48) * 30);
  
  return Math.min(100, Math.max(0, 出道概率));
}

/**
 * 检查是否可以出道
 */
export function 可以出道(状态: 练习生核心状态): boolean {
  const 最低要求 = {
    唱功: 60,
    舞蹈: 60,
    表现力: 60,
    外貌: 65,
    粉丝缘: 50,
  };

  return (
    状态.唱功 >= 最低要求.唱功 &&
    状态.舞蹈 >= 最低要求.舞蹈 &&
    状态.表现力 >= 最低要求.表现力 &&
    状态.外貌 >= 最低要求.外貌 &&
    状态.粉丝缘 >= 最低要求.粉丝缘 &&
    状态.培训时长 >= 24
  );
}

/**
 * 生成月度考核评价
 */
export function 生成月度考核评价(状态: 练习生核心状态): string {
  const 能力列表 = [
    { name: '唱功', value: 状态.唱功 },
    { name: '舞蹈', value: 状态.舞蹈 },
    { name: '表现力', value: 状态.表现力 },
    { name: '外貌', value: 状态.外貌 },
    { name: '粉丝缘', value: 状态.粉丝缘 },
  ];

  const 排序能力 = [...能力列表].sort((a, b) => b.value - a.value);
  const 最高 = 排序能力[0];
  const 最低 = 排序能力[排序能力.length - 1];

  let 评价 = `【${状态.本名 || 状态.艺名意向 || '练习生'} 月度考核】\n\n`;
  评价 += `培训时长：${状态.培训时长}个月（${状态.培训等级}）\n`;
  评价 += `综合评分：${Math.round((状态.唱功 + 状态.舞蹈 + 状态.表现力 + 状态.外貌 + 状态.粉丝缘) / 5)}/100\n\n`;
  
  评价 += `【能力评估】\n`;
  for (const 能力 of 能力列表) {
    const bar = '█'.repeat(Math.floor(能力.value / 10)) + '░'.repeat(10 - Math.floor(能力.value / 10));
    评价 += `${能力.name}：${bar} ${能力.value}\n`;
  }

  评价 += `\n最强项：${最高.name}（${最高.value}）\n`;
  评价 += `需加强：${最低.name}（${最低.value}）\n`;

  if (可以出道(状态)) {
    评价 += `\n✨ 已达到出道标准！\n`;
  } else {
    评价 += `\n📋 距离出道还需继续努力\n`;
  }

  return 评价;
}
