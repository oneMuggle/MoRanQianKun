/**
 * 黑幕触发概率
 */
export function 计算黑幕触发概率(
  练习生: 练习生核心状态,
  黑幕类型: 选秀黑幕类型,
  公司等级: number = 50
): number {
  const 基础概率: Record<选秀黑幕类型, number> = {
    '排名内定': 30,
    '投票造假': 25,
    '镜头分配': 40,
    '恶剪': 20,
    '选手交易': 15,
    '资本干预': 35,
    '潜规则置换': 10,
  };

  // 无公司背景的练习生更容易成为受害者
  const 公司加成 = 公司等级 < 30 ? -20 : 公司等级 > 70 ? 20 : 0;
  
  // 外貌和粉丝缘好的练习生可能获得更多镜头
  const 自身条件 = (练习生.外貌 / 100) * 10 + (练习生.粉丝缘 / 100) * 5;

  return Math.max(0, Math.min(100, 基础概率[黑幕类型] + 公司加成 + 自身条件));
}

/**
 * 选秀排名变动
 */
export interface 排名变动 {
  练习生ID: string;
  原排名: number;
  新排名: number;
  变动原因: string;
}

/**
 * 计算选秀排名
 */
export function 计算选秀排名(
  练习生列表: 练习生核心状态[],
  出道名额: number = 9,
  黑幕因素?: { 练习生ID: string; 黑幕类型: 选秀黑幕类型; 效果: number }[]
): 练习生核心状态[] {
  // 按综合评分排序
  const 评分列表 = 练习生列表.map(练习生 => ({
    练习生,
    综合评分: 计算综合评分(练习生),
  }));

  // 应用黑幕因素
  if (黑幕因素) {
    for (const 黑幕 of 黑幕因素) {
      const 目标 = 评分列表.find(s => s.练习生.练习生ID === 黑幕.练习生ID);
      if (目标) {
        目标.综合评分 += 黑幕.效果;
      }
    }
  }

  // 按评分排序
  评分列表.sort((a, b) => b.综合评分 - a.综合评分);

  // 更新排名
  return 评分列表.map((item, index) => ({
    ...item.练习生,
    出道排名预测: index + 1,
    被淘汰风险: 计算淘汰风险(index + 1, 练习生列表.length, 出道名额),
  }));
}

/**
 * 计算练习生综合评分
 */
function 计算综合评分(练习生: 练习生核心状态): number {
  // 能力占50%，心理状态占30%，公司支持占20%
  const 能力评分 = (练习生.唱功 + 练习生.舞蹈 + 练习生.表现力 + 练习生.外貌 + 练习生.粉丝缘) / 5;
  const 心理评分 = (练习生.出道渴望度 + 练习生.坚持底线意愿) / 2;
  const 公司支持 = 练习生.公司支持度;

  return 能力评分 * 0.5 + 心理评分 * 0.3 + 公司支持 * 0.2;
}

/**
 * 计算淘汰风险
 */
function 计算淘汰风险(排名: number, 总人数: number, 出道名额: number): number {
  if (排名 <= 出道名额) return 0;
  
  const 淘汰区人数 = 总人数 - 出道名额;
  const 距离出道位 = 排名 - 出道名额;
  
  return Math.round((距离出道位 / 淘汰区人数) * 100);
}

/**
 * 选秀结果判定
 */
export function 判定选秀结果(
  练习生: 练习生核心状态,
  出道名额: number = 9
): 选秀结果 {
  const 排名 = 练习生.出道排名预测 || 99;
  const 风险 = 练习生.被淘汰风险;

  if (排名 === 0) return '淘汰';  // 被淘汰
  if (排名 <= 出道名额) {
    return 风险 > 30 ? '出道但被拖累' : '出道';
  }
  if (风险 >= 80) return '被退赛';
  if (风险 >= 50) return '淘汰';
  return '退赛';
}

/**
 * 生成选秀阶段描述
 */
export function 生成选秀阶段描述(
  阶段: 选秀阶段,
  选手人数: number
): string {
  const 配置 = 选秀阶段配置表[阶段];
  const 通过人数 = Math.round(选手人数 * 配置.通过率);

  let 描述 = `【${阶段}】\n`;
  描述 += `持续时间：${配置.持续时间}\n`;
  描述 += `选手人数：${选手人数}\n`;
  描述 += `预计通过人数：约${通过人数}人\n`;
  描述 += `可操控环节：\n`;
  
  for (const 黑幕 of 配置.可操控环节) {
    描述 += `  - ${黑幕}：${黑幕类型描述表[黑幕]}\n`;
  }

  return 描述;
}

/**
 * 检查是否可能遭遇黑幕
 */
export function 是否可能遭遇黑幕(
  练习生: 练习生核心状态,
  阶段: 选秀阶段
): { 可能遭遇: boolean; 黑幕类型: 选秀黑幕类型[] } {
  const 配置 = 选秀阶段配置表[阶段];
  const 可能遭遇的黑幕: 选秀黑幕类型[] = [];

  for (const 黑幕 of 配置.可操控环节) {
    const 概率 = 计算黑幕触发概率(练习生, 黑幕);
    if (概率 > 30) {
      可能遭遇的黑幕.push(黑幕);
    }
  }

  return {
    可能遭遇: 可能遭遇的黑幕.length > 0,
    黑幕类型: 可能遭遇的黑幕,
  };
}
