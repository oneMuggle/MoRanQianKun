/**
 * 创建婚介所信息
 */
export function 创建婚介所(类型: 婚介所类型): 婚介所信息 {
  const 类型信息 = 婚介所类型表[类型];
  
  return {
    名称: 类型信息.名称示例[Math.floor(Math.random() * 类型信息.名称示例.length)],
    类型,
    位置: 类型 === '高端婚介' ? '市中心高档写字楼' : '商业街或居民楼',
    装修档次: 类型 === '高端婚介' ? '高档' : 类型 === '黑婚介' ? '简陋' : '中档',
    接待态度: 类型 === '黑婚介' ? '热情' : '专业',
  };
}

