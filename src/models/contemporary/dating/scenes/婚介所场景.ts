// ==================== 场景生成 ====================

export interface 婚介所场景 {
  标题: string;
  场景描写: string;
  婚介套路: string[];
  关键选择: string[];
  建议行动: string[];
}

/**
 * 生成婚介所场景
 */
export function 生成婚介所场景(
  相亲者: 相亲者核心状态,
  婚介所类型: 婚介所类型 = '黑婚介'
): 婚介所场景 {
  const 婚介所 = 创建婚介所(婚介所类型);
  const 套路列表 = 获取婚介所套路(婚介所类型);

  let 场景描写 = '';

  switch (婚介所类型) {
    case '正规婚介':
      场景描写 = `${婚介所.名称}

位于${婚介所.位置}，环境整洁。
前台接待员态度专业，正在核实你的身份信息。
墙上挂着营业执照和成功案例。
看起来是正规经营。`;
      break;
    case '黑婚介':
      场景描写 = `${婚介所.名称}

位于${婚介所.位置}，装修${婚介所.装修档次}。
一进门就有工作人员热情迎接。
\"现在正好有活动，只要999就能成为会员！\"
墙上挂满了\"成功案例\"和锦旗，但总觉得哪里怪怪的...`;
      break;
    case '高端婚介':
      场景描写 = `${婚介所.名称}

位于${婚介所.位置}，装修奢华。
一进门就有专人接待，送上茶水。
\"我们的会员都是精英阶层，成功率80%以上...\"
\"VIP服务有不同的套餐，从10万到50万不等...\"
`;
      break;
    case '相亲角组织者':
      场景描写 = `${婚介所.名称}

在公园相亲角旁边的一个小亭子里。
\"先生/女士，来登记一下吧，现在优惠只要50元...\"
\"登记后可以在我们这展示一个月，还推荐你进VIP群...\"
`;
      break;
  }

  return {
    标题: `【婚介所 - ${婚介所.名称}】`,
    场景描写,
    婚介套路: 套路列表.map(s => s.描述),
    关键选择: [
      '配合登记个人信息',
      '询问服务内容和费用',
      '对高价会员犹豫',
      '找个借口先离开',
      '当场签约享受优惠',
    ],
    建议行动: [
      '不要被低价诱惑',
      '确认婚介所资质',
      '仔细阅读合同',
      '保留所有凭证',
      '谨慎对待会员推销',
    ],
  };
}
