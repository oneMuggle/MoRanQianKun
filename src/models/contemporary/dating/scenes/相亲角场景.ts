// ==================== 相亲角人物类型 ====================

export interface 相亲角人物 {
  类型: '为子女相亲的父母' | '本人征婚' | '婚介所的人' | '路过打酱油';
  条件描述: string;
  真实程度: number; // 0-100
  关注点: string[];
  搭话方式: string;
}

/**
 * 生成相亲角人物
 */
export function 生成相亲角人物(
  相亲者: 相亲者核心状态
): 相亲角人物[] {
  const 人物类型 = [
    {
      类型: '为子女相亲的父母' as const,
      条件描述: `${相亲者.性别 === '男' ? '儿子' : '女儿'}：${相亲者.年龄 - 5}岁，${相亲者.职业}，有房有车`,
      真实程度: 70 + Math.floor(Math.random() * 20),
      关注点: ['对方工作', '对方家庭', '对方房产', '对方颜值'],
      搭话方式: '直接询问家庭条件',
    },
    {
      类型: '本人征婚' as const,
      条件描述: `${相亲者.昵称}，${相亲者.年龄}岁，${相亲者.职业}，年收入${相亲者.收入}万`,
      真实程度: 60 + Math.floor(Math.random() * 30),
      关注点: ['眼缘', '聊不聊得来', '对方诚意'],
      搭话方式: '先聊聊看',
    },
    {
      类型: '婚介所的人' as const,
      条件描述: '我是正规婚介所的，有大量优质资源...',
      真实程度: 30 + Math.floor(Math.random() * 30),
      关注点: ['你的预算', '你能付多少介绍费'],
      搭话方式: '热情推销',
    },
    {
      类型: '路过打酱油' as const,
      条件描述: '随便看看，不着急',
      真实程度: 100,
      关注点: ['随便聊聊'],
      搭话方式: '随意闲聊',
    },
  ];

  // 随机返回1-3个人物
  const numPeople = 1 + Math.floor(Math.random() * 3);
  const shuffled = [...人物类型].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, numPeople);
}

// ==================== 相亲角场景描述 ====================

export interface 相亲角场景描述 {
  标题: string;
  时间地点: string;
  场景描写: string;
  氛围: string;
  可能的收获: string[];
  潜在风险: string[];
}

/**
 * 生成相亲角场景
 */
export function 生成相亲角场景(
  相亲者: 相亲者核心状态,
  地点: string = '一般城市公园'
): 相亲角场景描述 {
  const 配置 = 相亲角配置表[地点] || 相亲角配置表['一般城市公园'];
  
  const 场景描写 = `公园里人声鼎沸，相亲角的展示板上挂满了密密麻麻的征婚信息。

${配置.参与者层次 === '高端' ? '这里条件都很好，大多是高学历、高收入、高颜值的\"三高\"人群。' : ''}
${配置.参与者层次 === '中端' ? '人群层次参差不齐，有普通白领，也有一些条件不错的。' : ''}
${配置.参与者层次 === '低端' ? '条件相对一般，但大家都很真诚地想找对象。' : ''}

${相亲者.性别 === '男' ? '你看到几个替子女来相亲的父母正在热烈讨论。' : '你注意到几个男生正在偷偷观察你。'}

${Math.random() > 0.5 ? '有人主动过来和你搭话：\"你是来相亲的吗？\"' : '你看到一张引起你注意的征婚启事...'}
`;

  const 潜在风险 = [
    '条件可能注水',
    '婚介所的人混在其中',
    '照片与本人不符',
    '家底可能被夸大',
  ];

  if (配置.参与者层次 === '低端') {
    潜在风险.push('可能遇到骗子');
    潜在风险.push('信息真实性低');
  }

  return {
    标题: `【相亲角 - ${配置.位置}】`,
    时间地点: `${配置.位置} - 周${['日', '一', '六'][Math.floor(Math.random() * 3)]}`,
    场景描写,
    氛围: '热闹、功利、充满机会与陷阱',
    可能的收获: [
      '认识潜在的相亲对象',
      '了解市场行情',
      '积累相亲经验',
    ],
    潜在风险,
  };
}

