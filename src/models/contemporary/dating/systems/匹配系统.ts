// ==================== 条件评分 ====================

/**
 * 计算男性视角条件评分
 */
export function 计算男性视角评分(对象: 相亲对象核心状态): number {
  const 权重 = 相亲条件权重['男性视角'];
  let score = 0;

  // 外貌评分
  const 外貌评分: Record<string, number> = {
    '帅气/漂亮': 10,
    '普通': 7,
    '一般': 5,
    '难看': 3,
  };
  score += (外貌评分[对象.外貌] || 5) * 权重.外貌;

  // 年龄评分（女性年龄越低分数越高）
  const 年龄评分 = Math.max(0, 10 - Math.max(0, 对象.年龄 - 22) * 0.5);
  score += 年龄评分 * 权重.年龄;

  // 性格评分（简化处理）
  score += 7 * 权重.性格;

  // 学历评分
  const 学历评分: Record<string, number> = {
    '博士': 10, '硕士': 8, '本科': 6, '本科以下': 4,
  };
  score += (学历评分[对象.学历] || 6) * 权重.学历;

  // 收入评分
  const 收入评分 = Math.min(10, 对象.收入 / 5);
  score += 收入评分 * 权重.收入;

  // 家庭背景评分
  const 家庭评分: Record<string, number> = {
    '富裕': 10, '城市中产': 7, '城市底层': 5, '农村': 3,
  };
  score += (家庭评分[对象.家庭背景] || 5) * 权重.家庭背景;

  return Math.min(100, score * 10);
}

/**
 * 计算女性视角条件评分
 */
export function 计算女性视角评分(对象: 相亲对象核心状态): number {
  const 权重 = 相亲条件权重['女性视角'];
  let score = 0;

  // 收入评分
  const 收入评分 = Math.min(10, 对象.收入 / 8);
  score += 收入评分 * 权重.收入;

  // 房产评分
  const 房产评分: Record<string, number> = {
    '有房无贷': 10, '有房贷': 7, '无房': 3,
  };
  score += (房产评分[对象.房产 as keyof typeof 房产评分] || 5) * 权重.房产;

  // 家庭背景评分
  const 家庭评分: Record<string, number> = {
    '富裕': 10, '城市中产': 7, '城市底层': 5, '农村': 4,
  };
  score += (家庭评分[对象.家庭背景] || 5) * 权重.家庭背景;

  // 性格评分
  score += 7 * 权重.性格;

  // 外貌评分
  const 外貌评分: Record<string, number> = {
    '帅气/漂亮': 10, '普通': 7, '一般': 5, '难看': 3,
  };
  score += (外貌评分[对象.外貌] || 5) * 权重.外貌;

  // 学历评分
  const 学历评分: Record<string, number> = {
    '博士': 10, '硕士': 8, '本科': 6, '本科以下': 4,
  };
  score += (学历评分[对象.学历] || 6) * 权重.学历;

  // 年龄评分（男性年龄适中最好）
  let 年龄评分 = 10;
  if (对象.年龄 < 25) 年龄评分 = 5;
  else if (对象.年龄 > 45) 年龄评分 = 3;
  else if (对象.年龄 > 38) 年龄评分 = 6;
  score += 年龄评分 * 权重.年龄;

  return Math.min(100, score * 10);
}

// ==================== 成功率评估 ====================

export interface 匹配结果 {
  成功率: number;
  男方总分: number;
  女方总分: number;
  差距: number;
  问题点: string[];
  建议: string;
}

/**
 * 评估相亲成功率
 */
export function 评估相亲成功率(
  男方: 相亲者核心状态,
  女方: 相亲对象核心状态
): 匹配结果 {
  const 男方总分 = 计算个人综合条件(男方);
  const 女方总分 = 计算女性视角评分(女方);

  const 问题点: string[] = [];
  let 成功率 = 50;

  // 条件差距
  const 差距 = Math.abs(男方总分 - 女方总分);
  if (差距 > 30) {
    成功率 = 20;
    问题点.push('条件差距过大');
  } else if (差距 > 20) {
    成功率 = 35;
    问题点.push('条件有些差距');
  } else if (差距 < 15) {
    成功率 = 60;
  }

  // 年龄匹配
  if (男方.性别 === '男') {
    if (男方.年龄 < 女方.年龄 - 5) {
      成功率 -= 15;
      问题点.push('男性年龄偏小');
    }
    if (男方.年龄 > 女方.年龄 + 10) {
      成功率 -= 10;
      问题点.push('年龄差距较大');
    }
  } else {
    if (女方.年龄 < 男方.年龄 - 5) {
      成功率 -= 10;
      问题点.push('女性年龄偏小');
    }
    if (女方.年龄 > 男方.年龄 + 8) {
      成功率 -= 15;
      问题点.push('姐弟恋年龄差距大');
    }
  }

  // 物质匹配
  const 男方物质 = 计算个人综合条件(男方);
  if (男方物质 > 女方.收入 * 2) {
    成功率 += 10;
  }

  // 动机匹配
  if (男方.相亲动机 === '玩玩而已' && 女方.真实动机 === '真心想结婚') {
    成功率 -= 30;
    问题点.push('动机不合');
  }

  // 骗子警告
  if (女方.是骗子) {
    成功率 = 成功率 * 0.5;
    问题点.push('对象疑似骗子');
  }

  // 矫正成功率
  成功率 = Math.max(5, Math.min(95, 成功率));

  // 生成建议
  let 建议 = '';
  if (成功率 > 70) {
    建议 = '条件较匹配，可以尝试发展';
  } else if (成功率 > 50) {
    建议 = '有一定差距，需要努力经营';
  } else if (成功率 > 30) {
    建议 = '差距较大，建议谨慎';
  } else {
    建议 = '不太匹配，建议止损';
  }

  return {
    成功率,
    男方总分,
    女方总分,
    差距,
    问题点,
    建议,
  };
}

// ==================== 快速筛选 ====================

/**
 * 快速判断是否值得见面
 */
export function 快速筛选(
  男方: 相亲者核心状态,
  女方: 相亲对象核心状态
): {
  通过: boolean;
  原因: string[];
} {
  const 原因: string[] = [];

  // 年龄筛选
  if (男方.性别 === '男' && 女方.年龄 < 22) {
    原因.push('女性年龄太小');
  }
  if (男方.性别 === '女' && 女方.年龄 < 25) {
    原因.push('男性年龄太小');
  }
  if (女方.年龄 > 45) {
    原因.push('年龄偏大');
  }

  // 收入筛选
  if (男方.性别 === '男' && 女方.收入 > 男方.收入 * 3) {
    原因.push('女性收入过高，可能看不上');
  }

  // 动机筛选
  if (女方.是骗子) {
    原因.push('疑似骗子');
  }

  return {
    通过: 原因.length === 0,
    原因,
  };
}

// ==================== 彩礼计算 ====================

/**
 * 计算建议彩礼金额
 */
export function 计算彩礼(
  男方收入: number,
  女方收入: number,
  地区: '农村' | '县城' | '地级市' | '省会' | '一线城市'
): {
  建议彩礼: number;
  范围: { min: number; max: number };
  谈判空间: string;
} {
  const 基准: Record<string, { min: number; max: number }> = {
    '农村': { min: 5, max: 10 },
    '县城': { min: 10, max: 20 },
    '地级市': { min: 15, max: 30 },
    '省会': { min: 20, max: 50 },
    '一线城市': { min: 50, max: 100 },
  };

  const 范围 = 基准[地区];
  const 中位数 = (范围.min + 范围.max) / 2;

  // 根据双方收入调整
  let 建议彩礼 = 中位数;
  if (男方收入 > 50) {
    建议彩礼 = Math.min(范围.max, 建议彩礼 * 1.3);
  } else if (男方收入 < 15) {
    建议彩礼 = Math.max(范围.min, 建议彩礼 * 0.7);
  }

  return {
    建议彩礼: Math.round(建议彩礼),
    范围,
    谈判空间: 建议彩礼 > 中位数 ? '偏难' : '有空间',
  };
}

/**
 * 计算嫁妆
 */
export function 计算嫁妆(
  彩礼: number,
  模式: '传统' | '现代' | '极端' | '谈判'
): {
  嫁妆: number;
  说明: string;
} {
  switch (模式) {
    case '传统':
      return {
        嫁妆: Math.round(彩礼 * (0.5 + Math.random() * 0.5)),
        说明: '传统习俗，嫁妆为彩礼的50%-100%',
      };
    case '现代':
      return {
        嫁妆: Math.round(彩礼 * (1.0 + Math.random() * 0.2)),
        说明: '现代做法，彩礼带回并添置嫁妆',
      };
    case '极端':
      return {
        嫁妆: Math.round(彩礼 * (0 + Math.random() * 0.3)),
        说明: '极端情况，彩礼留下很少返还',
      };
    case '谈判':
      return {
        嫁妆: 彩礼,
        说明: '双方协商，彩礼嫁妆持平',
      };
  }
}
