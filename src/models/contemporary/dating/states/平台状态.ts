// 更新平台状态
export function 更新平台状态(
  state: 婚恋平台状态,
  updates: Partial<婚恋平台状态>
): 婚恋平台状态 {
  return { ...state, ...updates };
}

// 增加曝光
export function 增加平台曝光(
  state: 婚恋平台状态
): 婚恋平台状态 {
  return 更新平台状态(state, {
    曝光次数: state.曝光次数 + 1,
    好评率: Math.max(0, state.好评率 - 5),
    投诉率: Math.min(100, state.投诉率 + 5),
  });
}

// 平台整顿
export function 平台整顿(
  state: 婚恋平台状态
): 婚恋平台状态 {
  return 更新平台状态(state, {
    审核力度: Math.min(100, state.审核力度 + 20),
    举报响应: Math.min(100, state.举报响应 + 20),
    骗子密度: Math.max(0, state.骗子密度 - 15),
  });
}

// 获取平台安全评级
export function 获取平台安全评级(state: 婚恋平台状态): {
  评级: 'A' | 'B' | 'C' | 'D';
  描述: string;
} {
  const 安全分 = (
    (100 - state.骗子密度) * 0.4 +
    state.审核力度 * 0.3 +
    state.举报响应 * 0.2 +
    state.真实用户率 * 0.1
  );

  if (安全分 >= 80) {
    return { 评级: 'A', 描述: '安全可靠' };
  } else if (安全分 >= 60) {
    return { 评级: 'B', 描述: '基本安全' };
  } else if (安全分 >= 40) {
    return { 评级: 'C', 描述: '存在风险' };
  } else {
    return { 评级: 'D', 描述: '高危平台' };
  }
}

// 获取平台摘要
export function 获取平台摘要(state: 婚恋平台状态): string {
  const { 评级, 描述 } = 获取平台安全评级(state);
  return `${state.平台名称}（${state.类型}）`
    + `| 注册${state.注册人数 / 10000}万`
    + `| 活跃${state.活跃人数 / 10000}万`
    + `| 骗子密度:${state.骗子密度}%`
    + `| 安全评级:${评级}`
    + `| 好评率:${state.好评率}%`
    + `| 曝光:${state.曝光次数}次`;
}
