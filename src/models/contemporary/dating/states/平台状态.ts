// 创建默认婚恋平台
export function 创建默认婚恋平台(partial?: Partial<婚恋平台状态>): 婚恋平台状态 {
  return {
    平台ID: partial?.平台ID || `平台_${Date.now()}`,
    平台名称: partial?.平台名称 || '某婚恋平台',
    类型: partial?.类型 || '严肃婚恋',

    // 用户数据
    注册人数: partial?.注册人数 || 1000000,
    活跃人数: partial?.活跃人数 || 500000,
    付费用户: partial?.付费用户 || 100000,
    男女比例: partial?.男女比例 || '1:1',

    // 安全属性
    骗子密度: partial?.骗子密度 || 30,
    审核力度: partial?.审核力度 || 50,
    举报响应: partial?.举报响应 || 40,
    真实用户率: partial?.真实用户率 || 70,

    // 常见骗局
    常见骗局: partial?.常见骗局 || ['红包党', '杀猪盘'],

    // 口碑
    好评率: partial?.好评率 || 60,
    投诉率: partial?.投诉率 || 20,
    曝光次数: partial?.曝光次数 || 10,
  };
}

