// ==================== 核心状态接口 ====================

// 配送员状态
export interface 配送员状态 {
  ID: string;
  化名: string;
  本名?: string;
  性别: '男' | '女';
  年龄: number;

  // 职业信息
  配送类型: 配送类型[];
  配送时段: 配送时段[];
  配送区域: string[];

  // 数据
  完成订单数: number;
  好评率: number;              // 0-100
  月收入: number;

  // 客户
  深夜订单比例: number;         // 0-100
  独居客户接触数: number;       // 接触过的独居客户数
  异性客户数: number;

  // 经历
  骚扰经历次数: number;         // 被骚扰或骚扰别人的次数
  越界行为: number;             // 越界行为次数
  客户投诉: number;             // 被投诉次数

  // 心理
  工作满意度: number;           // 0-100
  疲劳度: number;               // 0-100
  压力指数: number;             // 0-100
  冲动指数: number;             // 0-100，工作中的冲动程度

  // 安全
  安全意识: number;              // 0-100
  法律意识: number;             // 0-100
  自我控制: number;             // 0-100
}

// 客户状态
export interface 配送客户状态 {
  ID: string;
  客户名: string;
  性别: '男' | '女';
  年龄: number;

  // 类型
  客户类型: 配送客户类型;
  居住类型: '独居' | '合租' | '与家人同住';

  // 订单
  下单频率: number;             // 每月下单次数
  深夜下单: boolean;            // 是否经常深夜下单
  给小费习惯: number;           // 0-100

  // 安全
  防范意识: number;             // 0-100
  开门接收: boolean;            // 是否经常开门接收
  被骚扰经历: boolean;          // 是否有过被骚扰经历

  // 配送员评价
  对配送员评价: Record<string, number>;  // 配送员ID -> 评分
}
