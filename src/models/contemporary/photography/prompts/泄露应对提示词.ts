/**
 * 泄露应对提示词
 * 
 * 照片泄露事件发生后模特应对的系统提示词
 */

import type { 
  模特核心状态,
  泄露类型,
  传播范围,
} from './types';

/**
 * 生成泄露发现场景提示词
 */
export function 生成泄露发现提示词(params: {
  模特: 模特核心状态;
  泄露类型: 泄露类型;
  传播范围: 传播范围;
  发现渠道: string;
}): string {
  const { 模特, 泄露类型, 传播范围, 发现渠道 } = params;

  return `【系统角色】
你是模特${模特.姓名}，你发现自己的照片被泄露了。

【泄露信息】
- 泄露照片数：未知
- 内容尺度：未知
- 传播范围：${传播范围}
- 发现渠道：${发现渠道}

【影响评估】
- 心理创伤：${模特.心理阴影程度}/100
- 名誉损失：未知
- 职业影响：未知

【事件经过】
${模特.姓名}收到了朋友的消息，朋友说在网上看到了她的照片...

【心理状态】
- 震惊程度：100/100
- 愤怒程度：80/100
- 羞耻程度：${getShameBySpread(传播范围)}/100
- 想要报复：50/100

【可选应对方式】
1. 联系平台删除（平台投诉）
2. 报警处理（治安/刑事）
3. 委托律师发律师函
4. 找人查泄露源头
5. 发声澄清
6. 暂时冷处理
7. 心理疏导

【回复要求】
- 体现复杂的心理状态
- 不同处理方式有不同后果
- 展现模特的内心挣扎
- 留有后续发展空间`;
}

function getShameBySpread(传播范围: 传播范围): number {
  const 表: Record<传播范围, number> = {
    '未传播': 0,
    '小范围': 30,
    '论坛传播': 50,
    '社交媒体': 70,
    '图片网站': 80,
    '成人网站': 95,
    '全网扩散': 100,
  };
  return 表[传播范围] || 30;
}

/**
 * 生成泄露维权提示词
 */
export function 生成维权提示词(params: {
  模特: 模特核心状态;
  泄露类型: 泄露类型;
  处理方式: '报警' | '平台投诉' | '律师函' | '发声澄清' | '冷处理';
}): string {
  const { 模特, 泄露类型, 处理方式 } = params;

  const 处理方式描述: Record<string, string> = {
    '报警': '向公安机关报案，追究泄露者法律责任',
    '平台投诉': '联系各大平台要求删除照片',
    '律师函': '委托律师发送律师函警告',
    '发声澄清': '在社交媒体公开发声，澄清事实',
    '冷处理': '不理会，等待热度自然下降',
  };

  return `【特殊场景：泄露维权】

模特${模特.姓名}选择${处理方式描述[处理方式]}...

【当前处境】
- 泄露类型：${泄露类型}
- 心理阴影程度：${模特.心理阴影程度}/100

【${处理方式}详情】
${get处理详情(处理方式)}

【${模特.姓名}的选择理由】
${get选择理由(模特, 处理方式)}

【可能的结果】
${get可能结果(处理方式)}

【回复要求】
1. 体现维权的决心
2. 描写内心的纠结与担忧
3. 描写处理过程中的困难
4. 展示最终结果`;
}

function get处理详情(处理方式: string): string {
  const 详情库: Record<string, string> = {
    '报警': '警方受理了案件，开始调查泄露源头。\n需要提供身份证明和证据材料。\n案件可能需要一段时间才能有结果。',
    '平台投诉': '向微博、小红书、贴吧等平台提交申诉。\n需要提供身份证和原图证据。\n部分平台已删除，但其他平台仍在传播。',
    '律师函': '律师已经准备好了律师函。\n发送给了主要的传播者和平台。\n有些人删除了内容，有些人置之不理。',
    '发声澄清': '在微博发布了澄清声明。\n获得了部分网友的支持。\n但也引来了恶意攻击和谣言。',
    '冷处理': '决定不理会这件事。\n一段时间后热度确实在下降。\n但照片依然存在于网络深处...',
  };
  return 详情库[处理方式] || '处理详情未知...';
}

function get选择理由(模特: 模特核心状态, 处理方式: string): string {
  if (处理方式 === '报警') {
    return '这是犯罪，必须让泄露者付出代价！';
  } else if (处理方式 === '平台投诉') {
    return '先尝试最简单的办法，希望能把照片删掉...';
  } else if (处理方式 === '律师函') {
    return '需要有法律威慑力，但又不想把事情闹大...';
  } else if (处理方式 === '发声澄清') {
    return '我不想被动挨打，我要主动出击！';
  } else {
    return '算了，事情已经这样了，回应只会更糟糕...';
  }
}

function get可能结果(处理方式: string): string {
  const 结果库: Record<string, string> = {
    '报警': '成功抓获泄露者，获得赔偿\n但案件细节被曝光，舆论压力增大',
    '平台投诉': '大部分照片被删除\n但已经有人截图备份',
    '律师函': '部分人删除了内容\n但敲诈者反而变本加厉',
    '发声澄清': '得到广泛支持，舆论反转\n但也遭受了网络暴力',
    '冷处理': '热度逐渐下降\n但偶尔还是被人翻出来讨论',
  };
  return 结果库[处理方式] || '结果未知...';
}

/**
 * 生成泄露后心理疏导提示词
 */
export function 生成心理疏导提示词(params: {
  模特: 模特核心状态;
  泄露类型: 泄露类型;
  传播范围: 传播范围;
}): string {
  const { 模特, 泄露类型, 传播范围 } = params;

  return `【特殊场景：心理疏导】

模特${模特.姓名}正在接受心理咨询...

【当前状态】
- 泄露类型：${泄露类型}
- 传播范围：${传播范围}
- 心理阴影程度：${模特.心理阴影程度}/100

【咨询师的话】
"这不是你的错。"
"你有权感到愤怒和受伤，这是正常的情绪反应。"
"重要的是，你还在这里，你没有被这件事击倒。"
"给自己一些时间，慢慢走出来。"

【${模特.姓名}的内心】
${get心理独白(传播范围)}

【回复要求】
1. 体现心理创伤的复杂性
2. 展示逐渐恢复的过程
3. 表达内心的挣扎与希望
4. 保持真实，不刻意煽情`;
}

function get心理独白(传播范围: 传播范围): string {
  if (传播范围 === '全网扩散' || 传播范围 === '成人网站') {
    return '"我以为我再也走不出来了..."\n"但现在我想明白了一件事——"\n"这不是我的错，我不应该为此羞耻。"';
  } else if (传播范围 === '社交媒体' || 传播范围 === '图片网站') {
    return '"刚开始真的很崩溃..."\n"但慢慢地，我发现生活还要继续"\n"我不能被这件事定义我的人生。"';
  } else {
    return '"还好发现得早..."\n"虽然很受伤，但我会好起来的"\n"这次经历让我更加懂得保护自己了。"';
  }
}
