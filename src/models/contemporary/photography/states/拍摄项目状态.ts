// ==================== 拍摄项目工厂函数 ====================

export function 创建拍摄项目(params: {
  项目ID: string;
  模特ID: string;
  摄影师ID: string;
  约定拍摄类型: 写真类型;
  约定场所: 拍摄场所;
  约定尺度: 拍摄尺度;
  约定服装: 服装类型[];
  约定时长: number;
  约定价格: number;
}): 拍摄项目状态 {
  const {
    项目ID,
    模特ID,
    摄影师ID,
    约定拍摄类型,
    约定场所,
    约定尺度,
    约定服装,
    约定时长,
    约定价格,
  } = params;

  return {
    项目ID,
    摄影师ID,
    模特ID,

    // 约定信息
    约定拍摄类型,
    约定场所,
    约定尺度,
    约定服装,
    约定时长,
    约定价格,

    // 实际执行（初始与约定相同）
    实际拍摄类型: 约定拍摄类型,
    实际尺度: 约定尺度,
    实际服装: [...约定服装],
    实际时长: 0,
    实际价格: 约定价格,

    // 进度追踪
    当前回合: 0,
    总回合数: Math.ceil(约定时长 / 2), // 每2小时一个回合
    拍摄阶段: '准备',
    递进次数: 0,
    尺度递进次数: 0,
    递进方向: '尺度维持',
    递进触发: [],

    // 照片信息
    拍摄张数: 0,
    精选张数: 0,
    精修张数: 0,
    交付状态: '未交付',

    // 泄露风险
    原片风险: 0,
    精修风险: 0,

    // 违规记录
    越界行为: [],
    模特投诉: false,

    // 时间戳
    创建时间: Date.now(),
    更新时间: Date.now(),
  };
}

