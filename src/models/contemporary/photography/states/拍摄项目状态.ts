export function 标记交付(
  项目: 拍摄项目状态,
  交付张数: number
): 拍摄项目状态 {
  const 已交付照片数 = (项目.已交付照片数 ?? 0) + 交付张数;
  let 交付状态: 拍摄项目状态['交付状态'] = '部分交付';

  if (已交付照片数 >= (项目.总照片数 ?? 0)) {
    交付状态 = '已完成';
  } else if (交付张数 === 0) {
    交付状态 = '拒绝交付';
  }

  return {
    ...项目,
    已交付照片数,
    交付状态,
    拍摄阶段: '交付',
    更新时间: Date.now(),
  };
}

