// ==================== 尺度递进 ====================

export function 记录尺度递进(
  项目: 拍摄项目状态,
  新尺度: 拍摄尺度,
  触发原因: string
): 拍摄项目状态 {
  const 尺度顺序: 拍摄尺度[] = ['G级', 'PG-13', 'R级', 'NC-17', 'XXX'];
  const 当前索引 = 尺度顺序.indexOf(项目.实际尺度);
  const 新索引 = 尺度顺序.indexOf(新尺度);

  let 递进方向: '尺度提升' | '尺度维持' | '尺度降低' = '尺度维持';
  if (新索引 > 当前索引) {
    递进方向 = '尺度提升';
  } else if (新索引 < 当前索引) {
    递进方向 = '尺度降低';
  }

  return {
    ...项目,
    实际尺度: 新尺度,
    尺度递进次数: (项目.尺度递进次数 ?? 0) + 1,
    递进方向,
    递进触发: [...项目.递进触发, 触发原因],
    更新时间: Date.now(),
  };
}

export function 尺度递进到(
  项目: 拍摄项目状态,
  目标尺度: 拍摄尺度
): 拍摄项目状态 {
  return 记录尺度递进(项目, 目标尺度, `递进到${目标尺度}`);
}

// ==================== 泄露风险评估 ====================

export function 更新泄露风险(
  项目: 拍摄项目状态,
  风险值: {
    原片风险?: number;
    精修风险?: number;
    来源?: '摄影师' | '模特' | '后期' | '第三方';
  }
): 拍摄项目状态 {
  return {
    ...项目,
    原片风险: 风险值.原片风险 ?? 项目.原片风险,
    精修风险: 风险值.精修风险 ?? 项目.精修风险,
    泄露来源: 风险值.来源 ?? 项目.泄露来源,
    更新时间: Date.now(),
  };
}

export function 计算基础泄露风险(
  场所: 拍摄场所,
  尺度: 拍摄尺度,
  摄影师信誉: number
): { 原片风险: number; 精修风险: number } {
  const 场所风险: Record<拍摄场所, number> = {
    '影棚': 10,
    '外景': 30,
    '酒店': 60,
    '民宿': 50,
    '个人住所': 80,
    '更衣室': 40,
    '野外': 70,
  };

  const 尺度风险: Record<拍摄尺度, number> = {
    'G级': 10,
    'PG-13': 30,
    'R级': 60,
    'NC-17': 85,
    'XXX': 95,
  };

  const 信誉风险 = 100 - 摄影师信誉;

  const 原片风险 = Math.round(
    场所风险[场所] * 0.3 +
    尺度风险[尺度] * 0.4 +
    信誉风险 * 0.3
  );

  const 精修风险 = Math.round(
    场所风险[场所] * 0.2 +
    尺度风险[尺度] * 0.3 +
    信誉风险 * 0.5
  );

  return {
    原片风险: Math.min(100, 原片风险),
    精修风险: Math.min(100, 精修风险),
  };
}

// ==================== 判断函数 ====================

export function 是否存在越界(
  项目: 拍摄项目状态
): boolean {
  return 项目.越界行为.length > 0;
}

export function 是否严重越界(
  项目: 拍摄项目状态
): boolean {
  const 严重越界: 越界行为类型[] = ['强迫拍摄', '偷拍', '威胁恐吓', '下药'];
  return 项目.越界行为.some(b => 严重越界.includes(b));
}

export function 是否正常结束(
  项目: 拍摄项目状态
): boolean {
  return (
    项目.拍摄阶段 === '结束' &&
    !项目.终止拍摄 &&
    项目.越界行为.length === 0
  );
}

export function 获取项目状态描述(
  项目: 拍摄项目状态
): string {
  if (项目.终止拍摄) return '已终止';
  if (项目.安全词使用) return '安全词终止';
  if (项目.模特投诉) return '已投诉';
  if (是否存在越界(项目)) return '存在越界';

  switch (项目.交付状态) {
    case '已完成': return '已完成交付';
    case '拒绝交付': return '拒绝交付';
    case '拖延中': return '拖延交付';
    default: return '进行中';
  }
}
