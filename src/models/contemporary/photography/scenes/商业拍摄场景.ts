/**
 * 生成商业拍摄互动要点
 */
export function 生成商业拍摄互动要点(params: {
  模特: 模特核心状态;
  摄影师: 摄影师核心状态;
  拍摄场所: 拍摄场所;
}): string {
  const { 模特, 摄影师, 拍摄场所 } = params;

  const 常见问题: Record<string, string[]> = {
    '影棚': [
      '摄影师技术指导',
      '灯光调整',
      '服装细节',
      '后期风格',
      '交付时间',
    ],
    '外景': [
      '天气影响',
      '路人围观',
      '服装妆容',
      '拍摄时间',
    ],
    '酒店': [
      '私密性',
      '安全措施',
      '服装更换',
      '灯光控制',
    ],
    '民宿': [
      '场景风格',
      '卫生条件',
      '换装便利',
      '拍摄时间',
    ],
    '个人住所': [
      '安全顾虑',
      '设备专业度',
      '出片质量',
      '后期服务',
    ],
    '更衣室': [
      '隐私保护',
      '换装便利',
      '临时设备',
    ],
    '野外': [
      '安全保障',
      '天气变化',
      '服装选择',
      '隐私保护',
    ],
  };

  const 问题列表 = 常见问题[拍摄场所] || 常见问题['影棚'];

  return `【互动要点】
- ${问题列表.join('\n- ')}

【模特状态】
- ${模特.姓名}（${模特.类型}）
- 保护意识：${模特.保护意识}
- 合作摄影师数：${模特.合作摄影师数}

【摄影师状态】
- ${摄影师.姓名}（${摄影师.类型}）
- 信誉：${摄影师.信誉}
- 合作模特数：${摄影师.合作模特数}`;
}

