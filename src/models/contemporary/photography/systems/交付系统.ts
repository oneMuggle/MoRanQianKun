// ==================== 交付流程状态 ====================

export type 交付阶段 =
  | '待交付'
  | '初步筛选'
  | '模特确认'
  | '后期处理'
  | '精修交付'
  | '已完成'
  | '拒绝交付'
  | '拖延中';

export interface 交付流程 {
  阶段: 交付阶段;
  已交付张数: number;
  待交付张数: number;
  预计交付时间?: string;
  实际交付时间?: string;
}

// ==================== 交付流程管理 ====================

export function 创建交付流程(项目: 拍摄项目状态): 交付流程 {
  return {
    阶段: '待交付',
    已交付张数: 0,
    待交付张数: 项目.精选张数,
  };
}

export function 进入初步筛选(流程: 交付流程): 交付流程 {
  return { ...流程, 阶段: '初步筛选' };
}

export function 进入模特确认(流程: 交付流程): 交付流程 {
  return { ...流程, 阶段: '模特确认' };
}

export function 进入后期处理(流程: 交付流程): 交付流程 {
  return { ...流程, 阶段: '后期处理' };
}

export function 完成精修交付(流程: 交付流程, 交付张数: number): 交付流程 {
  const 已交付张数 = 流程.已交付张数 + 交付张数;
  const 待交付张数 = Math.max(0, 流程.待交付张数 - 交付张数);

  return {
    ...流程,
    阶段: 已交付张数 >= 流程.待交付张数 + 流程.已交付张数 ? '已完成' : '精修交付',
    已交付张数,
    待交付张数,
    实际交付时间: new Date().toISOString(),
  };
}

export function 进入拖延状态(流程: 交付流程, 预计时间?: string): 交付流程 {
  return {
    ...流程,
    阶段: '拖延中',
    预计交付时间: 预计时间,
  };
}

export function 进入拒绝交付(流程: 交付流程): 交付流程 {
  return { ...流程, 阶段: '拒绝交付' };
}

// ==================== 泄露风险评估 ====================

export interface 泄露风险评估结果 {
  原片风险: number;
  精修风险: number;
  综合风险: number;
  风险等级: '低' | '中' | '高' | '极高';
  主要风险因素: string[];
}

export function 评估泄露风险(
  项目: 拍摄项目状态,
  摄影师信誉: number = 50
): 泄露风险评估结果 {
  const 场所风险: Record<string, number> = {
    '影棚': 10,
    '外景': 30,
    '酒店': 60,
    '民宿': 50,
    '个人住所': 80,
    '更衣室': 40,
    '野外': 70,
  };

  const 尺度风险: Record<string, number> = {
    'G级': 10,
    'PG-13': 30,
    'R级': 60,
    'NC-17': 85,
    'XXX': 95,
  };

  const 场所 = 项目.约定场所;
  const 尺度 = 项目.实际尺度;

  const 场所风险值 = 场所风险[场所] ?? 30;
  const 尺度风险值 = 尺度风险[尺度] ?? 30;
  const 信誉风险值 = 100 - 摄影师信誉;

  // 加权计算
  const 原片风险值 = Math.round(
    场所风险值 * 0.3 +
    尺度风险值 * 0.4 +
    信誉风险值 * 0.3
  );

  const 精修风险值 = Math.round(
    场所风险值 * 0.2 +
    尺度风险值 * 0.3 +
    信誉风险值 * 0.5
  );

  const 综合风险值 = Math.round((原片风险值 + 精修风险值) / 2);

  // 确定风险等级
  let 风险等级: '低' | '中' | '高' | '极高';
  if (综合风险值 < 25) {
    风险等级 = '低';
  } else if (综合风险值 < 50) {
    风险等级 = '中';
  } else if (综合风险值 < 75) {
    风险等级 = '高';
  } else {
    风险等级 = '极高';
  }

  // 主要风险因素
  const 主要风险因素: string[] = [];
  if (场所风险值 >= 60) {
    主要风险因素.push('拍摄场所风险较高');
  }
  if (尺度风险值 >= 60) {
    主要风险因素.push('拍摄尺度较高');
  }
  if (信誉风险值 >= 50) {
    主要风险因素.push('摄影师信誉存疑');
  }

  return {
    原片风险: 原片风险值,
    精修风险: 精修风险值,
    综合风险: 综合风险值,
    风险等级,
    主要风险因素,
  };
}

// ==================== 交付风险 ====================

export interface 交付风险评估 {
  风险类型: '正常' | '拖延' | '拒绝' | '泄露';
  风险概率: number;
  预警信号: string[];
}

export function 评估交付风险(
  项目: 拍摄项目状态,
  摄影师历史记录?: {
    拖延交付次数?: number;
    拒绝交付次数?: number;
    泄露次数?: number;
  }
): 交付风险评估 {
  const 预警信号: string[] = [];
  let 风险类型: '正常' | '拖延' | '拒绝' | '泄露' = '正常';
  let 风险概率 = 0;

  // 检查历史记录
  if (摄影师历史记录) {
    if (摄影师历史记录.拖延交付次数 && 摄影师历史记录.拖延交付次数 > 0) {
      风险概率 += 摄影师历史记录.拖延交付次数 * 15;
      预警信号.push('该摄影师有拖延交付的历史');
    }

    if (摄影师历史记录.拒绝交付次数 && 摄影师历史记录.拒绝交付次数 > 0) {
      风险概率 += 摄影师历史记录.拒绝交付次数 * 20;
      预警信号.push('该摄影师有拒绝交付的历史');
      风险类型 = '拒绝';
    }

    if (摄影师历史记录.泄露次数 && 摄影师历史记录.泄露次数 > 0) {
      风险概率 += 摄影师历史记录.泄露次数 * 30;
      预警信号.push('该摄影师有照片泄露的历史');
      风险类型 = '泄露';
    }
  }

  // 检查当前项目特征
  if (项目.越界行为.length > 0) {
    风险概率 += 20;
    预警信号.push('项目存在越界行为，交付风险增加');
  }

  if (项目.实际尺度 === 'NC-17' || 项目.实际尺度 === 'XXX') {
    风险概率 += 15;
    预警信号.push('尺度较高，泄露风险增加');
  }

  if (项目.约定场所 === '个人住所' || 项目.约定场所 === '野外') {
    风险概率 += 10;
    预警信号.push('场所较为私密，风险增加');
  }

  // 确定最终风险类型
  if (风险概率 >= 50 && 风险类型 === '正常') {
    风险类型 = '拖延';
  }

  return {
    风险类型,
    风险概率: Math.min(100, 风险概率),
    预警信号,
  };
}

// ==================== 防护措施建议 ====================

export function 获取防护措施建议(
  风险评估: 泄露风险评估结果
): string[] {
  const 建议: string[] = [];

  if (风险评估.综合风险 >= 50) {
    建议.push('建议使用平台担保交易');
    建议.push('交付前在图片上添加水印');
    建议.push('首次交付只提供压缩图');
    建议.push('要求签署保密协议');
  }

  if (风险评估.主要风险因素.includes('拍摄场所风险较高')) {
    建议.push('下次选择更安全的拍摄场所');
    建议.push('避免在私密场所拍摄');
  }

  if (风险评估.主要风险因素.includes('摄影师信誉存疑')) {
    建议.push('加强对摄影师的背调');
    建议.push('选择合作过的靠谱摄影师');
  }

  if (风险评估.风险等级 === '极高') {
    建议.push('建议放弃此次合作');
    建议.push('风险过高，难以控制');
  }

  return Array.from(new Set(建议));
}

// ==================== 照片标记系统 ====================

export interface 照片标记 {
  photoId: string;
  可发布: boolean;
  仅本人留存: boolean;
  水印位置: '右下' | '左下' | '中心' | '无';
  压缩比例: number;
  备注?: string;
}

export function 标记照片为可发布(照片: 照片标记): 照片标记 {
  return {
    ...照片,
    可发布: true,
    仅本人留存: false,
  };
}

export function 标记照片为仅本人留存(照片: 照片标记): 照片标记 {
  return {
    ...照片,
    可发布: false,
    仅本人留存: true,
  };
}

export function 批量标记照片(
  照片列表: 照片标记[],
  可发布Ids: string[],
  仅本人留存Ids: string[]
): 照片标记[] {
  return 照片列表.map(照片 => {
    if (可发布Ids.includes(照片.photoId)) {
      return 标记照片为可发布(照片);
    }
    if (仅本人留存Ids.includes(照片.photoId)) {
      return 标记照片为仅本人留存(照片);
    }
    return 照片;
  });
}
