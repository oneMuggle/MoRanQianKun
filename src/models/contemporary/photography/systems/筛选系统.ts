// ==================== 摄影师红线和评分 ====================

export interface 摄影师红线指标 {
  有偷拍投诉: boolean;
  有越界记录: boolean;
  匿名约拍: boolean;
  场所偏僻: boolean;
}

export interface 摄影师可信度评估 {
  可信度评分: number;                    // 0-100
  建议: '强烈推荐' | '可以接受' | '谨慎选择' | '强烈不推荐';
  风险提示: string[];
  优点列表: string[];
}

// ==================== 评估函数 ====================

export function 评估摄影师可信度(
  摄影师: 摄影师核心状态,
  红线: 摄影师红线指标,
  有朋友推荐: boolean = false
): 摄影师可信度评估 {
  const 风险提示: string[] = [];
  const 优点列表: string[] = [];
  let 可信度评分 = 100;

  // 红线检查（严重扣分）
  if (红线.有偷拍投诉) {
    可信度评分 -= 50;
    风险提示.push('该摄影师有偷拍投诉记录');
  }

  if (红线.有越界记录) {
    可信度评分 -= 30;
    风险提示.push('该摄影师有越界骚扰记录');
  }

  if (红线.匿名约拍) {
    可信度评分 -= 15;
    风险提示.push('该摄影师不愿意透露真实身份');
  }

  if (红线.场所偏僻) {
    可信度评分 -= 15;
    风险提示.push('拍摄场所过于偏僻，存在风险');
  }

  // 信誉等级调整
  switch (摄影师.信誉等级) {
    case '业界大佬':
      可信度评分 += 10;
      优点列表.push('业界大佬，口碑极好');
      break;
    case '资深摄影师':
      可信度评分 += 5;
      优点列表.push('资深摄影师，经验丰富');
      break;
    case '有争议':
      可信度评分 -= 20;
      风险提示.push('该摄影师在圈内存在争议');
      break;
    case '名声较差':
      可信度评分 -= 40;
      风险提示.push('该摄影师圈内口碑较差');
      break;
    case '惯犯':
      可信度评分 -= 60;
      风险提示.push('该摄影师有多次投诉记录');
      break;
  }

  // 投诉记录调整
  if (摄影师.被投诉次数 > 0) {
    可信度评分 -= Math.min(30, 摄影师.被投诉次数 * 5);
    风险提示.push(`该摄影师有${摄影师.被投诉次数}次投诉记录`);
  }

  if (摄影师.越界投诉次数 > 0) {
    可信度评分 -= Math.min(40, 摄影师.越界投诉次数 * 10);
    风险提示.push(`该摄影师有${摄影师.越界投诉次数}次越界投诉`);
  }

  if (摄影师.圈内警告次数 > 0) {
    可信度评分 -= Math.min(30, 摄影师.圈内警告次数 * 10);
    风险提示.push(`该摄影师在圈内被警告过${摄影师.圈内警告次数}次`);
  }

  // 朋友推荐加分
  if (有朋友推荐) {
    可信度评分 += 15;
    优点列表.push('有朋友推荐');
  }

  // 评分范围限制
  可信度评分 = Math.max(0, Math.min(100, 可信度评分));

  // 生成建议
  let 建议: '强烈推荐' | '可以接受' | '谨慎选择' | '强烈不推荐';
  if (可信度评分 >= 80) {
    建议 = '强烈推荐';
  } else if (可信度评分 >= 60) {
    建议 = '可以接受';
  } else if (可信度评分 >= 40) {
    建议 = '谨慎选择';
  } else {
    建议 = '强烈不推荐';
  }

  return { 可信度评分, 建议, 风险提示, 优点列表 };
}

// ==================== 摄影师分类评估 ====================

export interface 摄影师分类 {
  分类: 'A类' | 'B类' | 'C类' | 'D类';
  名称: string;
  特征: string[];
  风险等级: number;
}

export function 分类摄影师(摄影师: 摄影师核心状态): 摄影师分类 {
  const { 信誉等级, 越界投诉次数, 偷拍投诉次数, 圈内警告次数 } =摄影师;

  // A类：业界大佬
  if (信誉等级 === '业界大佬') {
    return {
      分类: 'A类',
      名称: '业界大佬',
      特征: [
        '作品质量极高',
        '合作过的模特评价好',
        '报价合理',
        '没有投诉记录',
        '愿意签合同/付定金',
      ],
      风险等级: 1,
    };
  }

  // B类：普通摄影师
  if (信誉等级 === '资深摄影师' || 信誉等级 === '普通摄影师') {
    return {
      分类: 'B类',
      名称: '普通摄影师',
      特征: [
        '作品质量一般',
        '基本没有投诉',
        '报价正常',
        '合作过但无特别印象',
      ],
      风险等级: 2,
    };
  }

  // C类：有争议摄影师
  if (信誉等级 === '有争议') {
    return {
      分类: 'C类',
      名称: '有争议摄影师',
      特征: [
        '传过投诉/绯闻',
        '约拍条件过于优厚（可能有陷阱）',
        '作品质量一般但报价很高',
        '需要谨慎',
      ],
      风险等级: 3,
    };
  }

  // D类：危险摄影师
  return {
    分类: 'D类',
    名称: '危险摄影师',
    特征: [
      '多次被投诉',
      '有偷拍/越界记录',
      '要求模糊（不签合同、不付定金）',
      '场所选择可疑',
      '圈内警告过',
    ],
    风险等级: 4,
  };
}

// ==================== 匹配度计算 ====================

export function 计算模特摄影师匹配度(
  模特: 模特核心状态,
  摄影师: 摄影师核心状态,
  标准: 模特选摄影师标准 = 默认选摄影师标准
): {
  总分: number;
  各维度得分: Record<keyof 模特选摄影师标准['权重系数'], number>;
  建议: string;
} {
  const 各维度得分: Record<string, number> = {};

  // 作品质量（基于摄影师技术水平）
  各维度得分.作品质量 = 摄影师.技术水平;

  // 价格合理（假设模特报价在合理范围）
  const 模特期望价格 = 模特.拍摄报价;
  const 价格差异 = Math.abs(摄影师.拍摄报价 - 模特期望价格) / 模特期望价格;
  各维度得分.价格合理 = Math.max(0, 100 - 价格差异 * 100);

  // 口碑评价
  各维度得分.口碑评价 = 100 - 摄影师.被投诉次数 * 10;

  // 朋友推荐（简化处理）
  各维度得分.朋友推荐 = 模特.信任摄影师名单.includes(摄影师.摄影师ID) ? 100 : 50;

  // 沟通态度（基于是否有越界投诉）
  各维度得分.沟通态度 = Math.max(0, 100 - 摄影师.越界投诉次数 * 20);

  // 场所安全（基于摄影师类型和设备等级）
  const 安全场所得分: Record<string, number> = {
    '商业摄影师': 90,
    '专业人像摄影师': 80,
    '独立摄影师': 60,
    '网红摄影师': 50,
    '老法师': 20,
    '私房摄影师': 30,
    '偷拍惯犯': 5,
  };
  各维度得分.场所安全 = 安全场所得分[摄影师.摄影师类型] ?? 50;

  // 后期风格（基于后期能力）
  各维度得分.后期风格 = 摄影师.后期能力;

  // 计算加权总分
  const 总分 = Object.keys(各维度得分).reduce((sum, key) => {
    const weight = 标准.权重系数[key as keyof typeof 标准.权重系数];
    return sum + (各维度得分[key] * weight);
  }, 0);

  // 生成建议
  let 建议: string;
  if (总分 >= 80) {
    建议 = '非常推荐，是合适的合作对象';
  } else if (总分 >= 60) {
    建议 = '可以考虑，建议进一步了解';
  } else if (总分 >= 40) {
    建议 = '需要谨慎，建议观察期';
  } else {
    建议 = '不建议合作，存在较高风险';
  }

  return { 总分: Math.round(总分), 各维度得分, 建议 };
}

// ==================== 筛选决策 ====================

export function 是否应该接受约拍(
  模特: 模特核心状态,
  摄影师: 摄影师核心状态,
  有朋友推荐: boolean = false
): {
  接受: boolean;
  理由: string;
  附加建议: string[];
} {
  // 检查是否在黑名单
  if (模特.黑名单摄影师.includes(摄影师.摄影师ID)) {
    return {
      接受: false,
      理由: '该摄影师在您的黑名单中',
      附加建议: ['不要考虑这次合作'],
    };
  }

  // 检查红线
  if (摄影师.偷拍投诉次数 && 摄影师.偷拍投诉次数 > 0) {
    return {
      接受: false,
      理由: '该摄影师有偷拍投诉记录',
      附加建议: ['绝对不要合作', '建议告知其他模特'],
    };
  }

  // 评估可信度
  const 评估 = 评估摄影师可信度(摄影师, {
    有偷拍投诉: (摄影师.偷拍投诉次数 ?? 0) > 0,
    有越界记录: 摄影师.越界投诉次数 > 0,
    匿名约拍: false,
    场所偏僻: false,
  }, 有朋友推荐);

  if (评估.建议 === '强烈不推荐') {
    return {
      接受: false,
      理由: `可信度评分仅${评估.可信度评分}分，风险过高`,
      附加建议: 评估.风险提示,
    };
  }

  if (评估.建议 === '谨慎选择') {
    return {
      接受: true,
      理由: `可信度评分${评估.可信度评分}分，可以尝试但需谨慎`,
      附加建议: ['确保签订合同', '告知朋友拍摄地点', '保持警惕'],
    };
  }

  return {
    接受: true,
    理由: `可信度评分${评估.可信度评分}分，${评估.建议}`,
    附加建议: 评估.优点列表,
  };
}
