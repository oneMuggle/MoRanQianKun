// 旅游行业从业者状态
export interface 旅游从业者状态 {
  ID: string;
  化名: string;
  本名?: string;
  性别: '男' | '女';
  年龄: number;

  // 职业
  旅游身份: 旅游身份类型;
  所属机构?: string;
  从业年限: number;

  // 客户
  服务客人数量: number;
  私密行程次数: number;              // 私人行程次数
  置换合作次数: number;              // 以置换为名的合作
  旅途邂逅次数: number;              // 旅途中的艳遇

  // 收入
  月收入: number;
  灰色收入: number;                  // 灰色收入

  // 工作
  工作压力: number;                   // 0-100
  工作满意度: number;                  // 0-100
  职业倦怠: number;                   // 0-100

  // 心理
  职业认同: number;                   // 0-100
  诱惑承受: number;                   // 0-100，面对诱惑的能力
  收入满意度: number;                 // 0-100

  // 安全
  安全意识: number;                    // 0-100
  法律风险: number;                    // 0-100
  行业名声: number;                    // 0-100
}

