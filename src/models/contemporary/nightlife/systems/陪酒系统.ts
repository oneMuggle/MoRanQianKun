/**
 * 计算服务满意度
 */
export function 计算服务满意度(params: {
  唱功: number;
  酒量: number;
  社交能力: number;
  察言观色: number;
  应对技巧: number;
  醉酒程度: number; // 0-100
}): number {
  const { 唱功, 酒量, 社交能力, 察言观色, 应对技巧, 醉酒程度 } = params;

  // 醉酒影响服务能力
  const 醉酒衰减 = 醉酒程度 > 50 ? (醉酒程度 - 50) * 0.5 : 0;

  const 综合评分 = (
    唱功 * 0.15 +
    酒量 * 0.15 +
    社交能力 * 0.25 +
    察言观色 * 0.25 +
    应对技巧 * 0.2
  ) - 醉酒衰减;

  return Math.max(0, Math.min(100, 综合评分));
}

/**
 * 判断是否可能被挖走（跳槽到其他妈咪）
 */
export function 判断跳槽可能性(params: {
  当前收入: number;
  妈咪抽成: number;
  江湖地位: number;
  在职时长: number; // 月数
}): { 可能性: number; 原因: string } {
  const { 当前收入, 妈咪抽成, 江湖地位, 在职时长 } = params;

  let 可能性 = 20;
  let 原因 = '';

  // 抽成太高
  if (妈咪抽成 > 0.3) {
    可能性 += 20;
    原因 += '妈咪抽成太高 ';
  }

  // 收入太低
  if (当前收入 < 10000) {
    可能性 += 15;
    原因 += '收入太低 ';
  }

  // 有一定江湖地位想单干
  if (江湖地位 > 70 && 在职时长 > 12) {
    可能性 += 25;
    原因 += '想自己当妈咪 ';
  }

  // 在职时间短，可能性更高
  if (在职时长 < 3) {
    可能性 += 10;
  }

  return {
    可能性: Math.min(100, 可能性),
    原因: 原因 || '正常稳定',
  };
}

