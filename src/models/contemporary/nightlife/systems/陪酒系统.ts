/**
 * 出台服务评估
 */
export function 评估出台风险(params: {
  客人魅力: number;
  客人给小费: number;
  场所档次: '经济型' | '中档型' | '高档型' | '豪华型' | '顶级私人';
  公主醉酒值: number;
  自我保护: number;
}): { 风险等级: '低' | '中' | '高'; 风险描述: string[] } {
  const { 客人魅力, 客人给小费, 场所档次, 公主醉酒值, 自我保护 } = params;

  const 风险描述: string[] = [];
  let 风险值 = 0;

  // 客人魅力低但给钱多 - 风险
  if (客人魅力 < 5 && 客人给小费 > 3000) {
    风险值 += 30;
    风险描述.push('钱多但看着不对劲');
  }

  // 场所档次低
  if (场所档次 === '经济型') {
    风险值 += 20;
    风险描述.push('经济场所安保差');
  }

  // 醉酒
  if (公主醉酒值 > 50) {
    风险值 += 30;
    风险描述.push('喝多了判断力下降');
  }

  // 自我保护意识低
  if (自我保护 < 40) {
    风险值 += 20;
    风险描述.push('自我保护意识差');
  }

  let 风险等级: '低' | '中' | '高';
  if (风险值 < 30) {
    风险等级 = '低';
  } else if (风险值 < 60) {
    风险等级 = '中';
  } else {
    风险等级 = '高';
  }

  return { 风险等级, 风险描述 };
}
