/**
 * 创建默认场所状态
 */
export function 创建场所状态(params: {
  场所ID: string;
  场所名称: string;
  类型: 夜场类型;
  档次?: 场所档次;
}): 夜场场所状态 {
  const {
    场所ID,
    场所名称,
    类型,
    档次 = 获取默认档次(类型),
  } = params;

  return {
    场所ID,
    场所名称,
    类型,
    档次,

    营业时间: '20:00 - 02:00',
    当前客流: 10 + Math.floor(Math.random() * 50),
    包厢占用: 30 + Math.floor(Math.random() * 60),

    在职公主: 10 + Math.floor(Math.random() * 30),
    在职少爷: 类型 === '夜总会' ? 5 + Math.floor(Math.random() * 15) : 0,
    模特数量: 类型 === '夜总会' || 类型 === '高端KTV' ? 10 + Math.floor(Math.random() * 20) : 0,

    安保水平: 获取安保水平(档次),
    监控覆盖: 40 + Math.floor(Math.random() * 60),
    报警系统: 档次 !== '经济型',

    口碑评分: 3 + Math.random() * 2,
    宰客程度: 获取宰客程度(档次),
    涉黄程度: 获取涉黄程度(类型, 档次),
    涉毒风险: 类型 === '夜总会' || 类型 === '会员制私人会所' ? 30 + Math.random() * 40 : 10,

    背后势力: 档次 === '顶级私人' || 档次 === '豪华型' ? '某江湖势力' : undefined,
    官方关系: 获取官方关系(档次),
  };
}

/**
 * 获取默认档次
 */
function 获取默认档次(类型: 夜场类型): 场所档次 {
  const 档次表: Record<夜场类型, 场所档次> = {
    '量贩式KTV': '经济型',
    '高端KTV': '高档型',
    '夜总会': '豪华型',
    '会员制私人会所': '顶级私人',
    '蹦迪酒吧': '中档型',
    '静吧': '经济型',
    '商务会所': '顶级私人',
  };
  return 档次表[类型];
}

/**
 * 获取安保水平
 */
function 获取安保水平(档次: 场所档次): 安保水平 {
  if (档次 === '顶级私人' || 档次 === '豪华型') return '强';
  if (档次 === '高档型' || 档次 === '中档型') return '中';
  return '弱';
}

/**
 * 获取宰客程度
 */
function 获取宰客程度(档次: 场所档次): number {
  if (档次 === '顶级私人') return 10;
  if (档次 === '豪华型') return 30;
  if (档次 === '高档型') return 50;
  if (档次 === '中档型') return 60;
  return 70;
}

/**
 * 获取涉黄程度
 */
function 获取涉黄程度(类型: 夜场类型, 档次: 场所档次): number {
  let 基础 = 0;
  
  if (类型 === '夜总会' || 类型 === '会员制私人会所') 基础 = 60;
  else if (类型 === '高端KTV' || 类型 === '商务会所') 基础 = 40;
  else if (类型 === '量贩式KTV') 基础 = 10;
  else 基础 = 20;

  if (档次 === '顶级私人' || 档次 === '豪华型') return 基础 + 20;
  if (档次 === '高档型') return 基础 + 10;
  return 基础;
}

/**
 * 获取官方关系
 */
function 获取官方关系(档次: 场所档次): number {
  if (档次 === '顶级私人') return 80;
  if (档次 === '豪华型') return 60;
  if (档次 === '高档型') return 50;
  if (档次 === '中档型') return 30;
  return 20;
}

