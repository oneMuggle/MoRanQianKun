/**
 * 聚会结果判定
 */
export function 判定聚会结果(params: {
  参与人数: number;
  平均醉酒值: number;
  暧昧事件数量: number;
  是否有冲突: boolean;
}): {
  结果类型: '开心散场' | '暧昧升温' | '酒后乱性' | '不欢而散';
  描述: string;
  影响: string[];
} {
  const { 参与人数, 平均醉酒值, 暧昧事件数量, 是否有冲突 } = params;

  let 结果类型: string;
  let 描述: string;
  const 影响: string[] = [];

  if (是否有冲突) {
    结果类型 = '不欢而散';
    描述 = '聚会因矛盾而散场，有人提前离开';
    影响.push('朋友关系受损');
    影响.push('可能有人醉酒受伤');
  } else if (平均醉酒值 > 70 && 暧昧事件数量 > 3) {
    结果类型 = '酒后乱性';
    描述 = '喝得太多，有人做出了后悔的事';
    影响.push('可能有人后悔');
    影响.push('关系可能变化');
    影响.push('可能有人受伤');
  } else if (暧昧事件数量 > 2) {
    结果类型 = '暧昧升温';
    描述 = '几对人的关系有了微妙的变化';
    影响.push('可能发展成情侣');
    影响.push('有人表白成功');
  } else {
    结果类型 = '开心散场';
    描述 = '大家玩得很开心，平平安安回家';
    影响.push('友谊加深');
    影响.push('下次再约');
  }

  return { 结果类型: 结果类型 as any, 描述, 影响 };
}

