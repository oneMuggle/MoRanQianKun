/**
 * 危机恢复场景
 */
export function 生成危机恢复场景(params: {
  危机类型: 危机事件;
  恢复进度: number; // 0-100
}): {
  场景描述: string;
  心理状态: string;
  恢复建议: string[];
} {
  const { 危机类型, 恢复进度 } = params;

  let 场景描述 = '';
  let 心理状态 = '';

  if (恢复进度 < 30) {
    场景描述 = '你还在危机的阴影中，无法摆脱...';
    心理状态 = '痛苦、恐惧、后悔';
  } else if (恢复进度 < 60) {
    场景描述 = '事情开始慢慢好转，但阴影仍存...';
    心理状态 = '逐渐平静，但仍有顾虑';
  } else {
    场景描述 = '你已经走出了危机的阴影...';
    心理状态 = '释然、重生、警惕';
  }

  return {
    场景描述,
    心理状态,
    恢复建议: 获取应对建议(危机类型),
  };
}

/**
 * 评估夜场安全等级
 */
export function 评估场所安全(params: {
  场所档次: '经济型' | '中档型' | '高档型' | '豪华型' | '顶级私人';
  安保水平: '弱' | '中' | '强';
  监控覆盖: number;
  涉黄程度: number;
}): {
  安全等级: '安全' | '较安全' | '危险' | '极度危险';
  风险因素: string[];
} {
  const { 场所档次, 安保水平, 监控覆盖, 涉黄程度 } = params;

  let 安全等级: '安全' | '较安全' | '危险' | '极度危险';
  const 风险因素: string[] = [];

  // 涉黄程度
  if (涉黄程度 > 70) {
    风险因素.push('涉黄程度高');
  }

  // 安保水平
  if (安保水平 === '弱') {
    风险因素.push('安保薄弱');
  }

  // 场所档次
  if (场所档次 === '经济型') {
    风险因素.push('经济型场所人员复杂');
  }

  // 计算综合安全等级
  let 危险值 = 0;
  if (涉黄程度 > 60) 危险值 += 30;
  if (场所档次 === '经济型') 危险值 += 20;
  if (安保水平 === '弱') 危险值 += 30;
  if (监控覆盖 < 50) 危险值 += 20;

  if (危险值 < 30) {
    安全等级 = '安全';
  } else if (危险值 < 50) {
    安全等级 = '较安全';
  } else if (危险值 < 70) {
    安全等级 = '危险';
  } else {
    安全等级 = '极度危险';
  }

  return { 安全等级, 风险因素 };
}
