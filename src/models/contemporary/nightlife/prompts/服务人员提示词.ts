/**
 * 服务人员心理描写
 */
export function 描述服务人员心理(服务人员: 服务人员核心状态): string {
  const 心理元素: string[] = [];

  // 麻木程度
  if (服务人员.麻木度 > 80) {
    心理元素.push('对这一切已经完全麻木，像机器一样重复着每天的工作');
  } else if (服务人员.麻木度 > 50) {
    心理元素.push('开始对这一切变得麻木');
  } else if (服务人员.麻木度 < 30) {
    心理元素.push('内心仍有羞耻感和挣扎');
  }

  // 赚钱欲望
  if (服务人员.赚钱欲望 > 70) {
    心理元素.push('赚钱是最重要的，其他都可以忍受');
  }

  // 债务压力
  if (服务人员.债务 > 0) {
    心理元素.push(`身上背着${服务人员.债务}元的债务`);
  }

  // 转型意愿
  if (服务人员.计划转型) {
    心理元素.push(`内心渴望转型，目标：${服务人员.转型方向}`);
  }

  // 酒精依赖
  if (服务人员.酒精依赖 > 60) {
    心理元素.push('对酒精产生了依赖');
  }

  // 级别
  if (服务人员.级别 === '头牌') {
    心理元素.push('作为头牌，有一定的地位和骄傲');
  } else if (服务人员.级别 === '高级') {
    心理元素.push('正在向头牌努力');
  }

  return 心理元素.length > 0 
    ? 心理元素.join('；') 
    : '心理状态复杂，说不清自己的想法';
}
