// 职场角色核心状态
export interface 职场角色状态 {
  ID: string;
  化名: string;
  本名?: string;
  性别: 职场性别;
  年龄: number;

  // 职场信息
  当前职位: 职场身份;
  公司名称: string;
  企业类型: 企业类型;
  入职时长: number;        // 月数
  月薪: number;

  // 权力关系
  直接上级ID?: string;
  下属名单: string[];
  同事关系: Record<string, 职场关系>;

  // 职业发展
  晋升进度: number;        // 0-100，晋升进度
  工作能力: number;        // 0-100
  人际关系: number;       // 0-100
  职场名声: number;        // 0-100

  // 骚扰相关
  潜规则试探次数: number;
  骚扰类型经历: 骚扰类型[];
  骚扰应对: 骚扰应对;
  骚扰阴影: number;        // 0-100，心理阴影程度

  // 安全属性
  防范意识: number;        // 0-100
  酒后乱性风险: number;    // 0-100
  职场曝光风险: number;    // 0-100

  // 心理状态
  心理健康: number;        // 0-100
  工作满意度: number;      // 0-100
  焦虑程度: number;        // 0-100
  抑郁程度: number;        // 0-100

  // 历史记录
  职场事件经历: 职场事件[];
  潜规则历史: {
    时间: string;
    对象: string;
    类型: 骚扰类型;
    结局: '拒绝' | '接受' | '半推半就';
  }[];
}

// 职场关系状态
export interface 职场关系状态 {
  关系ID: string;
  双方ID: [string, string];
  
  // 关系类型
  关系类型: 职场关系;
  权力差距: number;        // 0-100，权力差距程度

  // 关系进展
  亲密度: number;          // 0-100
  信任度: number;          // 0-100
  暧昧程度: number;        // 0-100

  // 职场影响
  工作配合度: number;      // 0-100
  晋升影响: number;        // 正负值，影响程度

  // 事件记录
  关键事件: {
    时间: string;
    事件: 职场事件;
    场景: 职场场景;
    细节: string;
  }[];

  // 潜在风险
  曝光风险: number;        // 0-100
  后果风险: 后果类型[];
}

