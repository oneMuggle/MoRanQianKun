/**
 * 触发线下互动事件
 */
export function 触发线下事件(
  当前关系: 粉丝关系,
  粉丝等级: 粉丝等级
): 线下互动事件 {
  // 根据关系和等级筛选可能的事件
  const 可用事件 = 线下互动事件池.filter(事件 => {
    if (事件.事件类型 === '越界行为') {
      // 高等级粉丝越界概率降低
      return ['榜一', '皇帝'].includes(粉丝等级);
    }
    return true;
  });
  
  return 可用事件[Math.floor(Math.random() * 可用事件.length)];
}

