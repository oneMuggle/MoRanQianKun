/**
 * 生成开播场景描述
 */
export function 生成开播场景(
  人设: 主播人设,
  直播内容: 直播内容类型
): { 开场白: string; 欢迎语: string[]; 互动话题: string } {
  const 开场白列表 = 开播开场白[人设];
  const 开场白 = 开场白列表[Math.floor(Math.random() * 开场白列表.length)];
  
  const 欢迎语列表 = [
    ...欢迎语['守护'].map(s => s.replace('{守护名}', '某某')),
    ...欢迎语['铁粉'].map(s => s.replace('{铁粉名}', '某某')),
    ...欢迎语['普通'],
  ];
  const 随机欢迎 = [
    欢迎语列表[Math.floor(Math.random() * 欢迎语列表.length)],
    欢迎语列表[Math.floor(Math.random() * 欢迎语列表.length)],
  ];
  
  const 话题列表 = 互动话题池[直播内容];
  const 话题 = 话题列表[Math.floor(Math.random() * 话题列表.length)];
  
  return {
    开场白,
    欢迎语: 随机欢迎,
    互动话题: 话题,
  };
}

