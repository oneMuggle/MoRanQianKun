/**
 * 深夜暧昧升级触发
 */
export function 检查深夜暧昧触发(
  话题: string,
  主播羞耻度: number,
  粉丝等级: string
): boolean {
  // 深夜情感倾诉 → 性相关话题
  const 敏感话题 = ['秘密', '念念不忘', '告白', '缘分'];
  const 话题敏感 = 敏感话题.some(t => 话题.includes(t));
  
  // 主播羞耻度低（更放开）且粉丝等级高
  const 主播放开 = 主播羞耻度 < 40;
  const 粉丝亲密 = ['守护', '榜一', '皇帝'].includes(粉丝等级);
  
  return 话题敏感 && (主播放开 || 粉丝亲密);
}
