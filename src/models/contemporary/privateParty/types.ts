  推荐人ID?: string;

  // 参与情况
  参加派对次数: number;
  派对类型偏好: 派对类型[];
  平均参与程度: 参与程度;
  当前关系状态: 派对关系;

  // 圈子声誉
  圈子名声: number;        // 0-100，圈内名声
  圈内人脉: number;         // 0-100，认识的圈内人
  吸引力: number;           // 0-100，对异性的吸引力

  // 心理状态
  心理接受度: number;       // 0-100，对圈子活动的接受程度
  羞耻度: number;            // 0-100，做这些事的羞耻感
