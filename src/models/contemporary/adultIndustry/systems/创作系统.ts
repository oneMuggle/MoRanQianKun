/**
 * 计算创作收益
 */
export function 计算创作收益(params: {
  内容类型: 内容类型;
  内容分级: 内容分级;
  粉丝基数: number;
  活跃粉丝比例: number;
}): {
  预估收益: number;
  收益区间: { min: number; max: number };
  主要收入来源: 变现模式[];
} {
  const { 内容类型, 内容分级, 粉丝基数, 活跃粉丝比例 } = params;

  const 基础收益 = 内容分级平台匹配[内容分级].变现能力 === '极高' ? 500 :
                   内容分级平台匹配[内容分级].变现能力 === '高' ? 300 :
                   内容分级平台匹配[内容分级].变现能力 === '中' ? 150 : 50;

  const 类型乘数 = 内容类型创作难度[内容类型].收益潜力 === '极高' ? 3 :
                   内容类型创作难度[内容类型].收益潜力 === '高' ? 2 :
                   内容类型创作难度[内容类型].收益潜力 === '中' ? 1 : 0.5;

  const 粉丝乘数 = Math.min(10, 粉丝基数 / 1000) * (0.5 + 活跃粉丝比例);

  const 预估收益 = Math.round(基础收益 * 类型乘数 * 粉丝乘数);

  return {
    预估收益,
    收益区间: {
      min: Math.round(预估收益 * 0.3),
      max: Math.round(预估收益 * 2),
    },
    主要收入来源: 内容分级 === 'G级' ? ['订阅制', '打赏制'] :
                 内容分级 === 'R级' ? ['订阅制', '按次付费', '打赏制'] :
                 内容分级 === 'NC-17级' ? ['按次付费', '定制内容', '私聊收费'] :
                 ['定制内容', '线下服务', '私聊收费'],
  };
}

type 变现模式 = '订阅制' | '按次付费' | '打赏制' | '定制内容' | '私聊收费' | '线下服务' | '广告分成';
