/**
 * 计算综合隐私保护评分
 */
export function 计算隐私保护评分(params: {
  当前隐私层级: 隐私层级;
  已实施措施: string[];
}): {
  保护评分: number;      // 0-100
  泄露风险评分: number;  // 0-100
  薄弱环节: string[];
  建议: string;
} {
  const { 当前隐私层级, 已实施措施 } = params;

  const 层级配置 = 隐私层级配置[当前隐私层级];

  // 基础保护评分
  let 保护评分 = 50 - (parseInt(层级配置.风险等级 === '最低' ? '0' :
                                 层级配置.风险等级 === '低' ? '20' :
                                 层级配置.风险等级 === '中' ? '50' : '80') || 50);

  // 根据已实施措施增加保护评分
  for (const 措施 of 隐私保护措施) {
    if (已实施措施.includes(措施.措施名称)) {
      保护评分 += 措施.防护效果 * 0.3;
    }
  }

  保护评分 = Math.max(0, Math.min(100, 保护评分));

  // 计算泄露风险
  let 泄露风险评分 = 50 * 层级配置.曝光风险系数;

  // 根据薄弱环节增加风险
  const 薄弱环节: string[] = [];
  for (const 渠道 of 隐私泄露渠道) {
    if (渠道.风险等级 === '高' && !已实施措施.some(m =>
      隐私保护措施.find(p => p.措施名称 === m)?.防范渠道.includes(渠道.渠道名称)
    )) {
      薄弱环节.push(渠道.渠道名称);
      泄露风险评分 += 渠道.泄露可能性 * 0.5;
    } else if (渠道.风险等级 === '中' && !已实施措施.some(m =>
      隐私保护措施.find(p => p.措施名称 === m)?.防范渠道.includes(渠道.渠道名称)
    )) {
      薄弱环节.push(渠道.渠道名称);
      泄露风险评分 += 渠道.泄露可能性 * 0.3;
    }
  }

  泄露风险评分 = Math.max(0, Math.min(100, 泄露风险评分));

  // 生成建议
  let 建议: string;
  if (薄弱环节.length === 0) {
    建议 = '隐私保护措施较为完善，建议保持';
  } else if (薄弱环节.length <= 2) {
    建议 = `建议加强以下方面的保护：${薄弱环节.join('、')}`;
  } else {
    建议 = `隐私保护存在较多薄弱环节，建议全面加强保护措施`;
  }

  return { 保护评分: Math.round(保护评分), 泄露风险评分: Math.round(泄露风险评分), 薄弱环节, 建议 };
}

/**
 * 评估曝光后果严重程度
 */
export function 评估曝光后果(params: {
  曝光渠道: string;
  身份确认度: number; // 0-100，身份被确认的程度
}): {
  严重程度: '轻微' | '中等' | '严重' | '致命';
  影响范围: string[];
  建议行动: string[];
} {
  const { 曝光渠道, 身份确认度 } = params;

  let 严重程度: '轻微' | '中等' | '严重' | '致命' = '轻微';
  const 影响范围: string[] = [];
  const 建议行动: string[] = [];

  // 根据身份确认度判定严重程度
  if (身份确认度 >= 80) {
    严重程度 = '致命';
  } else if (身份确认度 >= 60) {
    严重程度 = '严重';
  } else if (身份确认度 >= 40) {
    严重程度 = '中等';
  }

  // 根据曝光渠道判定影响范围
  if (曝光渠道 === '平台数据泄露' || 曝光渠道 === '黑客攻击') {
    影响范围.push('粉丝圈内部传播');
    影响范围.push('可能被联系勒索');
    建议行动.push('立即联系平台要求删除');
    建议行动.push('收集证据，必要时报警');
  } else if (曝光渠道 === '粉丝截图/录屏') {
    影响范围.push('社交媒体传播');
    影响范围.push('可能被制作成表情包/梗');
    建议行动.push('联系发布平台删除');
    建议行动.push('发表声明澄清');
  } else if (曝光渠道 === '竞争对手抹黑') {
    影响范围.push('行业声誉受损');
    影响范围.push('可能被恶意传播');
    建议行动.push('收集证据，法律维权');
    建议行动.push('发表声明澄清');
  } else if (曝光渠道 === '生活中被认出') {
    影响范围.push('家人朋友可能知道');
    影响范围.push('工作可能受影响');
    建议行动.push('提前告知家人');
    建议行动.push('准备应对方案');
  }

  // 根据严重程度添加建议
  if (严重程度 === '致命' || 严重程度 === '严重') {
    建议行动.push('考虑暂时停更');
    建议行动.push('评估是否需要转型或退出');
  }

  return { 严重程度, 影响范围, 建议行动 };
}
