/**
 * 生成粉丝回复风格
 */
export function 获取粉丝回复风格(状态: 粉丝核心状态): {
  语气: string;
  行为特点: string[];
  越界倾向: '低' | '中' | '高';
} {
  const { 类型, 骚扰倾向, 妄想程度 } = 状态;

  let 语气 = '正常热情';
  let 越界倾向: '低' | '中' | '高' = '低';
  const 行为特点: string[] = [];

  if (类型 === '轻度粉丝') {
    语气 = '随意轻松';
    行为特点.push('免费内容消费');
  } else if (类型 === '高付费粉丝') {
    语气 = '大爷心态';
    行为特点.push('期望特殊对待');
    越界倾向 = '中';
  } else if (类型 === '私粉') {
    语气 = '亲密感';
    行为特点.push('有私聊关系');
    越界倾向 = '中';
  } else if (类型 === '私生饭') {
    语气 = '过度热情';
    行为特点.push('跟踪倾向');
    越界倾向 = '高';
  }

  if (骚扰倾向 > 50) {
    行为特点.push('可能骚扰');
  }

  if (妄想程度 > 60) {
    行为特点.push('妄想与创作者有特殊关系');
  }

  return { 语气, 行为特点, 越界倾向 };
}

/**
 * 生成粉丝互动提示词
 */
export function 生成粉丝互动提示词(params: {
  粉丝状态: 粉丝核心状态;
  创作者化名: string;
  互动场景: '直播间' | '私信' | '评论区' | '线下';
}): string {
  const { 粉丝状态, 创作者化名, 互动场景 } = params;

  let 场景描述: string;
  let 互动要点: string;

  if (互动场景 === '直播间') {
    场景描述 = `粉丝${粉丝状态.化名}进入${创作者化名}的直播间`;
    互动要点 = '打赏互动、弹幕评论、等待主播回应';
  } else if (互动场景 === '私信') {
    场景描述 = `粉丝${粉丝状态.化名}给${创作者化名}发送私信`;
    互动要点 = '私聊请求、表达支持、可能的越界暗示';
  } else if (互动场景 === '评论区') {
    场景描述 = `粉丝${粉丝状态.化名}在${创作者化名}的作品下评论`;
    互动要点 = '留言互动、表达喜爱、可能引战';
  } else {
    场景描述 = `粉丝${粉丝状态.化名}参加${创作者化名}的线下活动`;
    互动要点 = '见面互动、拍照合影、可能越界';
  }

  return `【场景】
${场景描述}

【粉丝信息】
- 化名：${粉丝状态.化名}
- 类型：${粉丝状态.类型}
- 沉迷程度：${粉丝状态.沉迷程度}/100
- 妄想程度：${粉丝状态.妄想程度}/100

【互动要点】
${互动要点}

【注意】
- 体现粉丝心理
- 不同类型粉丝的言行差异
- ${粉丝状态.类型 === '私生饭' ? '高度注意：此粉丝有跟踪倾向，可能越界' : ''}
- ${粉丝状态.妄想程度 > 60 ? '注意：此粉丝可能妄想与创作者有特殊关系' : ''}`;
}

