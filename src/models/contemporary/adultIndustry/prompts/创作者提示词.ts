/**
 * 生成创作者回复风格
 */
export function 获取创作者回复风格(状态: 创作者核心状态): {
  语气: string;
  用词特点: string[];
  暧昧程度: '保守' | '适度' | '大胆';
  常见话题: string[];
} {
  const { 职业阶段, 职业认同度 } = 状态;

  let 语气 = '专业亲切';
  let 暧昧程度: '保守' | '适度' | '大胆' = '适度';
  const 用词特点: string[] = ['专业术语', '粉丝互动'];
  const 常见话题: string[] = ['创作心得', '粉丝互动', '日常生活'];

  if (职业阶段 === '新人') {
    语气 = '略带紧张';
    用词特点.push('小心翼翼');
    常见话题.push('入行经历');
  } else if (职业阶段 === '稳定期') {
    语气 = '自信从容';
    用词特点.push('游刃有余');
    常见话题.push('行业洞察');
  } else if (职业阶段 === '瓶颈期') {
    语气 = '略带疲惫';
    用词特点.push('反思总结');
    常见话题.push('转型想法');
  }

  if (职业认同度 > 70) {
    暧昧程度 = '大胆';
    用词特点.push('职业化');
  } else if (职业认同度 < 30) {
    暧昧程度 = '保守';
    用词特点.push('内心矛盾');
  }

  return { 语气, 用词特点, 暧昧程度, 常见话题 };
}

/**
 * 生成创作者日常对话提示词
 */
export function 生成日常对话提示词(params: {
  创作者状态: 创作者核心状态;
  对话场景: '工作' | '生活' | '粉丝互动';
  粉丝类型?: string;
}): string {
  const { 创作者状态, 对话场景, 粉丝类型 } = params;

  let 场景描述: string;
  let 回复要点: string;

  if (对话场景 === '工作') {
    场景描述 = '创作者在工作室准备拍摄内容';
    回复要点 = '分享创作过程、专业性、职业认同';
  } else if (对话场景 === '生活') {
    场景描述 = '创作者在日常生活中';
    回复要点 = '展现真实一面、隐私保护意识、生活的两面性';
  } else {
    场景描述 = `创作者与${粉丝类型 || '粉丝'}互动`;
    回复要点 = '维护粉丝关系、适度暧昧、边界意识';
  }

  return `【场景】
${场景描述}

【创作者信息】
- 化名：${创作者状态.化名}
- 入行：${创作者状态.入行时长}个月
- 职业阶段：${创作者状态.职业阶段}

【回复要点】
${回复要点}

【注意】
- 体现创作者的职业身份
- 展现行业特殊性
- 暧昧内容适度`;
}
