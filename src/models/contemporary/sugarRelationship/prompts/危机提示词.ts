// ==================== 辅助函数 ====================

export function 获取危机配置(危机类型: 危机场景类型) {
  return 危机配置表[危机类型];
}

export function 判断危机严重程度(
  糖宝: 糖宝核心状态,
  糖爹: 糖爹核心状态,
  关系: 糖关系状态完整
): '低' | '中' | '高' | '极高' {
  let score = 0;

  // 糖宝因素
  score += 糖宝.曝光风险 > 70 ? 30 : 糖宝.曝光风险 > 40 ? 15 : 0;
  score += 糖宝.焦虑度 > 70 ? 15 : 糖宝.焦虑度 > 40 ? 8 : 0;

  // 糖爹因素
  score += 糖爹.配偶知情 ? 20 : 0;
  score += 糖爹.原配攻击性 > 70 ? 25 : 糖爹.原配攻击性 > 40 ? 10 : 0;

  // 关系因素
  score += 关系.状态 === '危机期' ? 20 : 0;
  score += 关系.糖宝依赖度 > 70 ? 10 : 0;

  if (score >= 80) return '极高';
  if (score >= 50) return '高';
  if (score >= 25) return '中';
  return '低';
}
