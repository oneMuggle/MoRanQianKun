// ==================== 风险增加事件 ====================

// 模拟风险增加
export function 模拟风险增加(
  糖宝: 糖宝核心状态,
  糖爹: 糖爹核心状态,
  关系: 糖关系状态完整,
  场景: keyof typeof 曝光场景配置
): {
  新的糖宝曝光风险: number;
  新的糖爹曝光风险: number;
  是否触发危机: boolean;
  触发事件?: string;
} {
  const config = 曝光场景配置[场景];
  const 风险增量 = config.风险增量;

  // 已有安全措施可以降低风险
  const 实际增量 = config.可预防 ? 风险增量 * 0.7 : 风险增量;

  const 新的糖宝曝光风险 = Math.min(100, 糖宝.曝光风险 + 实际增量 * 0.8);
  const 新的糖爹曝光风险 = Math.min(100, 糖爹.曝光风险 + 实际增量 * 0.5);

  // 风险超过阈值可能触发危机
  const 是否触发危机 = 新的糖宝曝光风险 > 70 || Math.random() < 0.1;

  return {
    新的糖宝曝光风险: Math.round(新的糖宝曝光风险),
    新的糖爹曝光风险: Math.round(新的糖爹曝光风险),
    是否触发危机,
    触发事件: 是否触发危机 ? `${场景}导致曝光危机` : undefined,
  };
}

