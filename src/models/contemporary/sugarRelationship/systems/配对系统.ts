// ==================== 匹配计算 ====================

// 计算糖宝对糖爹的吸引力
export function 计算糖宝吸引力(糖宝: 糖宝核心状态, 糖爹: 糖爹核心状态): number {
  let score = 50;

  // 年龄匹配：糖爹喜欢年轻漂亮的
  if (糖宝.年龄 < 25) score += 15;
  else if (糖宝.年龄 < 30) score += 10;
  else if (糖宝.年龄 > 35) score -= 10;

  // 外貌类型匹配
  switch (糖宝.外貌类型) {
    case '清纯学生':
      score += 20; // 大多数糖爹喜欢
      break;
    case '邻家女孩':
      score += 15;
      break;
    case '高冷御姐':
      score += 10;
      break;
    case '火辣性感':
      score += 15;
      break;
    case '文艺气质':
      score += 12;
      break;
    case '网红脸':
      score += 8;
      break;
  }

  // 身份类型加分
  if (糖宝.身份类型 === '在校学生' || 糖宝.身份类型 === '应届毕业生') {
    score += 10;
  }

  // 物质依赖度高表示更容易被满足
  score += (糖宝.物质依赖度 - 50) * 0.3;

  // 羞耻度高表示更谨慎（可能是新手或更清纯）
  score += (糖宝.羞耻度 - 50) * 0.2;

  return Math.max(0, Math.min(100, score));
}

// 计算糖爹对糖宝的吸引力（从糖宝视角）
export function 计算糖爹吸引力(糖宝: 糖宝核心状态, 糖爹: 糖爹核心状态): number {
  let score = 50;

  // 财富等级影响
  switch (糖爹.财富等级) {
    case '普通干爹':
      score += 10;
      break;
    case '金主':
      score += 25;
      break;
    case '超级金主':
      score += 40;
      break;
    case '传奇干爹':
      score += 60;
      break;
  }

  // 年龄影响：太老扣分
  if (糖爹.年龄 > 55) score -= 15;
  else if (糖爹.年龄 > 45) score -= 5;
  else if (糖爹.年龄 < 35) score += 10; // 年轻富二代加分

  // 已婚扣分（风险更高）
  if (糖爹.婚姻状态 === '已婚') score -= 15;
  else if (糖爹.婚姻状态 === '离异') score += 5;

  // 大方程度
  score += (糖爹.慷慨度 - 50) * 0.4;

  // 糖宝的入行次数（经验丰富可能扣分）
  if (糖宝.入行次数 > 3) score -= 10;

  return Math.max(0, Math.min(100, score));
}

// 计算匹配成功率
export function 计算匹配成功率(
  糖宝: 糖宝核心状态,
  糖爹: 糖爹核心状态,
  渠道: keyof 寻找渠道配置 | keyof 糖爹寻找渠道配置
): number {
  const 糖宝吸引力 = 计算糖宝吸引力(糖宝, 糖爹);
  const 糖爹吸引力 = 计算糖爹吸引力(糖宝, 糖爹);

  // 渠道加成
  const 渠道配置 = 糖宝寻找渠道[渠道 as keyof 寻找渠道配置] 
    || 糖爹寻找渠道[渠道 as keyof 糖爹寻找渠道配置];
  const 渠道加成 = 渠道配置 ? 渠道配置.找到优质对象概率 * 30 : 0;

  // 双向吸引力平均
  const 双向吸引力 = (糖宝吸引力 + 糖爹吸引力) / 2;

  // 综合得分
  const 综合 = 双向吸引力 * 0.6 + 渠道加成;

  // 风险因素调整
  const 曝光风险调整 = 100 - (糖宝.曝光风险 + 糖爹.曝光风险) / 2;

  return Math.max(0, Math.min(100, 综合 * 0.7 + 曝光风险调整 * 0.3));
}

// ==================== 筛选函数 ====================

// 筛选合适的糖宝（从糖爹视角）
export function 筛选合适糖宝(
  糖宝列表: 糖宝核心状态[],
  糖爹: 糖爹核心状态,
  渠道?: keyof 糖爹寻找渠道配置
): 糖宝核心状态[] {
  return 糖宝列表
    .filter(糖宝 => !糖宝.黑名单.includes(糖爹.ID)) // 排除黑名单
    .filter(糖宝 => !糖宝.当前糖爹ID || 糖宝.当前糖爹ID === 糖爹.ID) // 排除已有稳定关系的
    .map(糖宝 => ({
      糖宝,
      吸引力: 计算糖爹吸引力(糖宝, 糖爹),
    }))
    .sort((a, b) => b.吸引力 - a.吸引力)
    .slice(0, 10)
    .map(item => item.糖宝);
}

// 筛选合适的糖爹（从糖宝视角）
export function 筛选合适糖爹(
  糖爹列表: 糖爹核心状态[],
  糖宝: 糖宝核心状态,
  渠道?: keyof 寻找渠道配置
): 糖爹核心状态[] {
  return 糖爹列表
    .filter(糖爹 => !糖宝.黑名单.includes(糖爹.ID)) // 排除黑名单
    .filter(糖爹 => !糖爹.当前糖宝ID || 糖爹.当前糖宝ID === 糖宝.ID) // 排除已有稳定关系的
    .map(糖爹 => ({
      糖爹,
      吸引力: 计算糖爹吸引力(糖宝, 糖爹),
    }))
    .sort((a, b) => b.吸引力 - a.吸引力)
    .slice(0, 10)
    .map(item => item.糖爹);
}

// 模拟配对结果
export function 模拟配对(
  糖宝: 糖宝核心状态,
  糖爹: 糖爹核心状态,
  渠道: keyof 寻找渠道配置 | keyof 糖爹寻找渠道配置
): {
  成功: boolean;
  匹配度: number;
  障碍: string[];
  建议: string[];
} {
  const 匹配度 = 计算匹配成功率(糖宝, 糖爹, 渠道);
  const 障碍: string[] = [];
  const 建议: string[] = [];

  // 检查障碍
  if (糖宝.黑名单.includes(糖爹.ID)) {
    障碍.push('该糖爹在黑名单中');
  }
  if (糖爹.花心度 > 80 && 糖爹.同时糖宝数 > 2) {
    障碍.push('该糖爹同时维持过多关系');
  }
  if (糖爹.曝光风险 > 70) {
    障碍.push('该糖爹曝光风险过高');
  }
  if (糖宝.计划转型 && 糖宝.转型方向 !== '继续做') {
    障碍.push('糖宝计划转型，可能不是长期对象');
  }

  // 生成建议
  if (匹配度 > 70) {
    建议.push('匹配度很高，建议深入了解');
  } else if (匹配度 > 50) {
    建议.push('匹配度中等，可以尝试');
  } else {
    建议.push('匹配度较低，建议寻找其他对象');
  }

  if (糖爹.慷慨度 > 80) {
    建议.push('该糖爹大方，可以争取更高零花钱');
  }

  return {
    成功: 障碍.length === 0 && 匹配度 > 40,
    匹配度,
    障碍,
    建议,
  };
}
