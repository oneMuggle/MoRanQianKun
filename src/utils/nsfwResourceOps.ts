/**
 * 执行NSFW互动资源变化
 * 返回新的资源状态和变化详情
 */
export function 执行NSFW互动资源变化(
  当前状态: NSFW资源状态,
  互动类型: NSFW互动类型
): 资源变化结果 {
  const 变化 = 获取NSFW互动资源变化(互动类型);

  let 新状态 = { ...当前状态 };
  let 精力变化 = 0;
  let 代币变化 = 0;
  let 声誉变化 = 0;
  let 风险变化 = 0;
  const 描述列表: string[] = [];

  // 精力处理
  if (变化.精力消耗 > 0) {
    const 精力结果 = 消耗精力(新状态, '社交互动' as any, {
      ...默认精力消耗配置,
      社交互动: 变化.精力消耗,
    });
    if (!精力结果.精力不足) {
      新状态 = 精力结果.新状态;
      精力变化 = -变化.精力消耗;
      描述列表.push(`精力 -${变化.精力消耗}`);
    } else {
      return {
        新资源状态: 当前状态,
        精力变化: 0, 代币变化: 0, 声誉变化: 0, 风险变化: 0,
        描述: `精力不足！需要 ${变化.精力消耗} 精力，当前只有 ${当前状态.精力值}`,
      };
    }
  } else if (变化.精力消耗 < 0) {
    const 恢复结果 = 恢复精力(新状态, Math.abs(变化.精力消耗));
    新状态 = 恢复结果.新状态;
    精力变化 = 恢复结果.实际恢复;
    描述列表.push(`精力 +${恢复结果.实际恢复}`);
  }

  // 代币获取
  if (变化.代币获取 > 0) {
    const 代币结果 = 获得亲密度代币(新状态, 变化.代币获取, 变化.描述);
    新状态 = 代币结果.新状态;
    代币变化 = 变化.代币获取;
    描述列表.push(`代币 +${变化.代币获取}`);
  }

  // 风险预算
  if (变化.风险消耗 > 0) {
    const 新风险 = Math.max(0, 新状态.风险预算 - 变化.风险消耗);
    风险变化 = 新风险 - 新状态.风险预算;
    新状态.风险预算 = 新风险;
    描述列表.push(`风险 -${变化.风险消耗}`);
  } else if (变化.风险消耗 < 0) {
    新状态 = 恢复风险预算(新状态, Math.abs(变化.风险消耗));
    风险变化 = Math.abs(变化.风险消耗);
    描述列表.push(`风险 +${Math.abs(变化.风险消耗)}`);
  }

  return {
    新资源状态: 新状态,
    精力变化,
    代币变化,
    声誉变化,
    风险变化,
    描述: `${变化.描述}：${描述列表.join('，')}`,
  };
}

/**
 * 回合结束时的自动资源处理
 * 自然恢复精力和风险预算
 */
export function 回合结束资源处理(
  当前状态: NSFW资源状态,
  本回合是否休息: boolean
): 资源变化结果 {
  let 新状态 = { ...当前状态 };
  const 描述列表: string[] = [];

  // 精力处理（休息或自然恢复）
  const 精力结果 = 回合结束精力处理(新状态, 本回合是否休息);
  新状态 = 精力结果.新状态;
  描述列表.push(精力结果.描述);

  // 风险预算自然恢复
  const 风险恢复量 = 本回合是否休息 ? 20 : 10;
  const 旧风险 = 新状态.风险预算;
  新状态 = 恢复风险预算(新状态, 风险恢复量);
  const 实际风险恢复 = 新状态.风险预算 - 旧风险;
  if (实际风险恢复 > 0) {
    描述列表.push(`风险预算 +${实际风险恢复}`);
  }

  return {
    新资源状态: 新状态,
    精力变化: 新状态.精力值 - 当前状态.精力值,
    代币变化: 0,
    声誉变化: 0,
    风险变化: 新状态.风险预算 - 当前状态.风险预算,
    描述: `回合结束：${描述列表.join('，')}`,
  };
}

