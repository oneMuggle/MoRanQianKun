/**
 * 生成夜场场景提示词
 */
export function 生成夜场场景提示词(上下文: 夜场场景上下文): string {
  const {
    夜场类型,
    场所档次,
    当前场景,
    消费者状态,
    服务人员状态,
    场所状态,
    内容强度,
  } = 上下文;

  const 场景线索: string[] = [];

  // 基础场景设定
  场景线索.push(`【场景设定】`);
  场景线索.push(`- 夜场类型：${夜场类型}`);
  场景线索.push(`- 场所档次：${场所档次}`);

  if (场所状态) {
    场景线索.push(`- 场所名称：${场所状态.场所名称}`);
    场景线索.push(`- 安保水平：${场所状态.安保水平}`);
    场景线索.push(`- 涉黄程度：${场所状态.涉黄程度}%`);
  }

  // 醉酒状态
  if (消费者状态) {
    场景线索.push(`\n【消费者状态】`);
    场景线索.push(`- 醉酒程度：${消费者状态.醉酒程度}（${消费者状态.醉酒值}%）`);
    场景线索.push(`- 理智程度：${消费者状态.理智程度}/100`);
    场景线索.push(`- 冲动程度：${消费者状态.冲动程度}/100`);
    场景线索.push(`- 社交目的：${消费者状态.社交目的}`);
  }

  // 服务人员状态
  if (服务人员状态) {
    场景线索.push(`\n【服务人员状态】`);
    场景线索.push(`- 化名：${服务人员状态.化名}`);
    场景线索.push(`- 入行时长：${服务人员状态.入行时长}个月`);
    场景线索.push(`- 麻木度：${服务人员状态.麻木度}/100`);
    场景线索.push(`- 羞耻度：${服务人员状态.羞耻度}/100`);
  }

  // 场景类型特定内容
  switch (当前场景) {
    case '商务接待':
      场景线索.push(`\n【场景类型】商务接待`);
      场景线索.push(`- 客户重要性：${消费者状态?.社交目的 === '商务应酬' ? '重要' : '普通'}`);
      场景线索.push(`- 氛围：高档包厢、洋酒、公主服务`);
      break;
    case '朋友聚会':
      场景线索.push(`\n【场景类型】朋友聚会`);
      场景线索.push(`- 活动：唱歌、骰子游戏、真心话大冒险`);
      场景线索.push(`- 氛围：热闹、酒精、暧昧游戏`);
      break;
    case '服务场景':
      场景线索.push(`\n【场景类型】服务场景`);
      场景线索.push(`- 服务内容：陪酒、唱歌、游戏互动`);
      场景线索.push(`- 氛围：职业化服务、暧昧博弈`);
      break;
    case '危机场景':
      场景线索.push(`\n【场景类型】危机场景`);
      场景线索.push(`- 风险：仙人跳、下药、越界行为`);
      场景线索.push(`- 氛围：紧张、危险`);
      break;
  }

  // NSFW 约束
  场景线索.push(`\n${构建夜场NSFW叙事约束(内容强度)}`);

  return 场景线索.join('\n');
}

