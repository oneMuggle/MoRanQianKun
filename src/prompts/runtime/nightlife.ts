/**
 * 整合夜场 NSFW 到运行时提示词
 * 类似于 campusNSFW 的构建方式
 */
export function 整合夜场NSFW提示词(
  customPrompt: string,
  options?: {
    启用NSFW模式?: boolean;
    夜场场景上下文?: 夜场场景上下文;
    夜场NSFW场景类型?: 夜场NSFW场景类型;
    醉酒程度?: 醉酒程度;
    暧昧值?: number;
    内容强度?: 夜场内容强度;
  }
): string {
  const { 启用NSFW模式, 夜场场景上下文, 夜场NSFW场景类型, 醉酒程度, 暧昧值, 内容强度 } = options || {};

  if (!启用NSFW模式) {
    return customPrompt;
  }

  const 组件: string[] = [];

  // 夜场 NSFW 约束
  if (夜场NSFW场景类型 && 内容强度) {
    组件.push(构建夜场NSFW完整叙事约束({
      场景类型: 夜场NSFW场景类型,
      醉酒程度: 醉酒程度 || '微醺',
      暧昧值: 暧昧值 || 0,
      内容强度,
    }));
  }

  // 场景上下文
  if (夜场场景上下文) {
    组件.push(生成夜场场景提示词(夜场场景上下文));
  }

  组件.push(customPrompt);

  return 组件.filter(Boolean).join('\n\n').trim();
}
