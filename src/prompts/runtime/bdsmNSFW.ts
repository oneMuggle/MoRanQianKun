// ==================== SM 叙事约束 ====================

/**
 * 构建 SM/支配服从叙事约束
 */
export const 构建SM叙事约束 = (
  权力倾向: 权力倾向,
  服从度: number,
  已解锁场景: SM场景类型[]
): string => {
  const 倾向描述: Record<权力倾向, string> = {
    '绝对支配': 'NPC是绝对的主导者，喜欢发号施令和控制。享受看到对方服从的快感。',
    '偏支配': 'NPC偶尔主动引导节奏，享受被服从的感觉。在亲密场景中倾向于控制方。',
    '切换者': 'NPC根据情境和心情在支配和服从之间切换。没有固定的权力倾向。',
    '偏服从': 'NPC享受被引导的感觉，服从指令会获得满足感。渴望被认可和肯定。',
    '绝对服从': 'NPC主动寻求指令，服从本身就是最大的满足。极度渴望被控制。',
  };

  const 服从度描述 = 服从度 >= 80 ? 'NPC极度信任和服从，几乎无条件执行指令'
    : 服从度 >= 60 ? 'NPC信任度较高，愿意尝试更深入的互动'
      : 服从度 >= 40 ? 'NPC基本服从，但仍有保留和犹豫'
        : 服从度 >= 20 ? 'NPC仍在试探阶段，服从意愿不强'
          : 'NPC对服从感到抗拒，可能需要更多引导';

  const 场景描述 = 已解锁场景.length > 0
    ? `当前可用的 SM 场景类型：${已解锁场景.join('、')}`
    : '当前无可用 SM 场景';

  return `【SM/支配服从系统】
NPC权力倾向：${权力倾向}
${倾向描述[权力倾向]}

NPC服从度：${服从度}/100
${服从度描述}

${场景描述}

核心原则：
- 所有 SM 场景必须建立在双方同意的基础上
- 权力交换是动态的，NPC始终保留退出的权利
- 描写重点在心理层面：信任、紧张、期待、羞耻感
- 身体描写为辅，避免过度暴力的描写
- 每个 SM 场景前后要有"正常"叙事作为缓冲`;
};

// ==================== 完整 BDSM 叙事约束 ====================

/**
 * 构建 BDSM 完整叙事约束
 * 综合权力倾向、服从度、SM 场景、活跃任务、日常指令、契约状态生成统一 prompt 注入
 */
export const 构建BDSM完整叙事约束 = (参数: {
  权力倾向?: 权力倾向;
  服从度?: number;
  已解锁SM场景?: SM场景类型[];
  活跃任务?: BDSM调教任务[];
  日常指令?: string[];
  契约状态?: { 类型: 契约类型; 状态: 契约状态; 条款: string[] };
  其他Npc欲望摘要?: string;
}): string => {
  const {
    权力倾向,
    服从度,
    已解锁SM场景,
    活跃任务,
    日常指令,
    契约状态,
    其他Npc欲望摘要,
  } = 参数;

  const 组件: string[] = [];

  if (权力倾向 || 服从度 !== undefined || (已解锁SM场景 && 已解锁SM场景.length > 0)) {
    组件.push(构建SM叙事约束(
      权力倾向 ?? '切换者',
      服从度 ?? 50,
      已解锁SM场景 ?? []
    ));
  }

  if (活跃任务 && 活跃任务.length > 0) {
    const 任务约束 = 构建调教任务系统叙事约束({
      活跃任务,
      日常指令,
      契约状态,
    });
    if (任务约束) 组件.push(任务约束);
  } else if (其他Npc欲望摘要?.includes('BDSM 关系阶段')) {
    组件.push(`【BDSM 关系管线】
当前存在 BDSM 关系。叙事时需注意：
- 根据关系阶段（初识→试探→确立→深入→固化）调整 NPC 的服从程度和互动方式
- 服从度影响 NPC 对指令的执行意愿和态度
- 契约条款是双方必须遵守的约定，叙事中不可违背
- 安全词是关系中的底线，一旦触发必须立即停止 BDSM 相关内容`);
  }

  return 组件.join('\n\n');
};

// ==================== 调教任务系统叙事约束 ====================

/**
 * 构建调教任务系统完整叙事约束
 * 注入到主叙事 prompt 中，让 AI 在生成剧情时考虑任务系统
 */
export const 构建调教任务系统叙事约束 = (参数: {
  活跃任务: BDSM调教任务[];
  日常指令?: string[];
  契约状态?: { 类型: 契约类型; 状态: 契约状态; 条款: string[] };
}): string => {
  const { 活跃任务, 日常指令, 契约状态 } = 参数;

  const 组件: string[] = [];

  if (活跃任务.length > 0) {
    const 进行中任务 = 活跃任务.filter(t => t.状态 === '进行中');
    const 待接受任务 = 活跃任务.filter(t => t.状态 === '待接受');

    组件.push(`【调教任务系统】`);

    if (进行中任务.length > 0) {
      组件.push(`当前进行中的任务：`);
      进行中任务.forEach(t => {
        组件.push(`- ${t.标题}（${t.类型}/${t.难度}）${t.截止时间 ? `，截止：${t.截止时间}` : ''}`);
      });
      组件.push(`叙事时应留意这些任务的状态，可能在剧情中体现任务的进展或结果。`);
    }

    if (待接受任务.length > 0) {
      组件.push(`待接受的任务：`);
      待接受任务.forEach(t => {
        组件.push(`- ${t.标题}（${t.类型}/${t.难度}）`);
      });
      组件.push(`这些任务可以在合适的时机向玩家/NPC 提出。`);
    }
  }

  if (日常指令 && 日常指令.length > 0) {
    组件.push(`【日常指令】\n当前生效的日常指令：${日常指令.join('、')}`);
    组件.push(`叙事中可通过细节体现这些指令的执行情况（如称呼变化、行为异常等）。`);
  }

  if (契约状态) {
    组件.push(`【契约状态】`);
    组件.push(`契约类型：${契约状态.类型}`);
    组件.push(`契约状态：${契约状态.状态}`);
    组件.push(`条款：${契约状态.条款.join('；')}`);
    组件.push(`叙事中应注意契约条款的履行情况。`);
  }

  return 组件.join('\n\n');
};

// ==================== 多角色关系网叙事约束 ====================

/**
 * 构建多角色 BDSM 关系叙事约束
 * 当启用 启用BDSM多角色关系 时注入
 */
export const 构建多角色BDSM叙事约束 = (参数: {
  活跃关系数: number;
  有冲突: boolean;
  最长链长度: number;
  网络摘要?: string;
}): string => {
  const { 活跃关系数, 有冲突, 最长链长度, 网络摘要 } = 参数;

  if (活跃关系数 === 0) return '';

  const 组件: string[] = [];

  组件.push(`【BDSM 多角色关系网 — 叙事约束】`);
  组件.push(`当前存在 ${活跃关系数} 条 BDSM 关系边，${有冲突 ? '存在' : '不存在'}关系冲突。`);

  if (最长链长度 >= 3) {
    组件.push(`存在长度为 ${最长链长度} 的关系链。叙事时应体现：`);
    组件.push(`- 链中上游 NPC 对下游 NPC 的间接影响力`);
    组件.push(`- 链中中间 NPC 的双重身份与心理压力`);
    组件.push(`- "连坐"效应：上游任务完成/失败影响下游 NPC 的态度`);
  }

  if (有冲突) {
    组件.push(`\n【关系冲突叙事指导】`);
    组件.push(`- 矛盾链：NPC 在不同关系中角色冲突，叙事中应体现内心矛盾和行为不一致`);
    组件.push(`- 三角张力：三角关系中嫉妒、竞争、占有欲是核心情感驱动`);
    组件.push(`- 强度失衡：NPC 对不同关系对象的投入差异导致情感失衡`);
  }

  组件.push(`\n【信息隔离】`);
  组件.push(`独立关系之间默认不共享信息。NPC 不应知道其他 BDSM 关系的细节，`);
  组件.push(`除非通过剧情事件（发现、泄露、告密）打破信息隔离。`);

  if (网络摘要) {
    组件.push(`\n${网络摘要}`);
  }

  return 组件.join('\n');
};
