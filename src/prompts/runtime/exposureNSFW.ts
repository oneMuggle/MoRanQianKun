// ==================== 完整叙事约束 ====================

/**
 * 构建露出系统完整叙事约束
 * 综合所有露出子系统状态生成统一的 prompt 注入
 */
export const 构建Exposure完整叙事约束 = (参数: {
  露出偏好等级?: 露出偏好等级;
  紧张度?: number;
  有旁观者?: boolean;
  网络流言等级?: number;
  有无证据?: boolean;
  eraId?: string;
  活跃后果?: 露出后果记录[];
}): string => {
  const { 露出偏好等级, 紧张度, 有旁观者, 网络流言等级, 有无证据, eraId, 活跃后果 } = 参数;
  const 组件: string[] = [];

  if (露出偏好等级 !== undefined && 露出偏好等级 > 0) {
    组件.push(构建露出叙事约束(露出偏好等级, eraId));
  }

  if (紧张度 !== undefined && 紧张度 > 0) {
    组件.push(构建紧张度叙事约束(紧张度));
  }

  if (有旁观者) {
    组件.push(构建旁观者反应约束(true, 紧张度 ?? 0));
  }

  if (网络流言等级 !== undefined && 网络流言等级 > 0) {
    组件.push(构建网络传播约束(网络流言等级, 有无证据 ?? false, eraId));
  }

  if (活跃后果 && 活跃后果.length > 0) {
    组件.push(构建后果叙事约束(活跃后果));
  }

  return 组件.join('\n\n');
};
