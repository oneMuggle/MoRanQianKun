/**
 * 从社交列表构建完整的关系网络
 */
export const 构建关系网络 = (
  社交列表: NPC结构[],
  主角姓名: string,
): 关系网络数据 => {
  const 网络 = 创建默认关系网络(主角姓名);
  const 关系边列表: 人物关系边[] = [];

  // 1. 构建主角 → 每个 NPC 的关系边
  for (const npc of 社交列表) {
    const 关系分类 = 推断关系分类(npc.关系状态 || '', npc.好感度);
    const 亲密度 = npc.亲密度等级 ?? 0;
    const 信任度 = Math.min(100, Math.round(npc.好感度 * 0.7 + 亲密度 * 0.3));
    const 感情值 = npc.好感度;

    const 阶段 = 计算关系谱阶段(npc.好感度, 亲密度, 信任度, 感情值);

    const 边: 人物关系边 = {
      id: 生成关系边ID(主角姓名, npc.姓名),
      主体姓名: 主角姓名,
      客体姓名: npc.姓名,
      客体ID: npc.id,
      关系分类,
      关系阶段: 阶段,
      关系描述: npc.关系状态 || '',
      好感度: npc.好感度,
      亲密度: Math.min(100, 亲密度 * 10),
      信任度,
      感情值,
      互动次数: 0,
      事件记录: [],
    };

    关系边列表.push(边);
  }

  // 2. 构建 NPC → NPC 的关系边（从关系网变量）
  for (const npc of 社交列表) {
    if (!npc.关系网变量 || npc.关系网变量.length === 0) continue;

    for (const 关系项 of npc.关系网变量) {
      const 已有边 = 关系边列表.find(
        b =>
          (b.主体姓名 === npc.姓名 && b.客体姓名 === 关系项.对象姓名) ||
          (b.主体姓名 === 关系项.对象姓名 && b.客体姓名 === npc.姓名),
      );

      if (已有边) {
        已有边.双向关系 = true;
        continue;
      }

      const 客体NPC = 社交列表.find(n => n.姓名 === 关系项.对象姓名);
      const 客体好感度 = 客体NPC?.好感度 ?? 0;
      const 客体分类 = 推断关系分类(关系项.关系, 客体好感度);
      const 客体阶段 = 计算关系谱阶段(客体好感度, 0, 0, 客体好感度);

      const 边: 人物关系边 = {
        id: 生成关系边ID(npc.姓名, 关系项.对象姓名),
        主体姓名: npc.姓名,
        客体姓名: 关系项.对象姓名,
        客体ID: 客体NPC?.id,
        关系分类: 客体分类,
        关系阶段: 客体阶段,
        关系描述: 关系项.关系 + (关系项.备注 ? `（${关系项.备注}）` : ''),
        好感度: 客体好感度,
        亲密度: 0,
        信任度: 0,
        感情值: 客体好感度,
        互动次数: 0,
        事件记录: [],
      };

      关系边列表.push(边);
    }
  }

  网络.关系边列表 = 关系边列表;
  网络.最后更新时间 = new Date().toISOString();

  return 网络;
};

/**
 * 从校园 NSFW 关系数据合并到关系网络
 */
export const 从NPC关系数据合并 = (
  网络: 关系网络数据,
  校园NPC关系数据?: Array<{
    npcId: string;
    npc姓名: string;
    好感度: number;
    亲密度: number;
    信任度: number;
    感情值: number;
    关键事件?: Array<{ 时间: string; 标题: string; 描述: string }>;
  }>,
): 关系网络数据 => {
  if (!校园NPC关系数据 || 校园NPC关系数据.length === 0) return 网络;

  const 主角姓名 = 网络.主角姓名;

  for (const 校园关系 of 校园NPC关系数据) {
    let 边 = 网络.关系边列表.find(
      b => b.主体姓名 === 主角姓名 && (b.客体姓名 === 校园关系.npc姓名 || b.客体ID === 校园关系.npcId),
    );

    if (边) {
      边.好感度 = 校园关系.好感度;
      边.亲密度 = 校园关系.亲密度;
      边.信任度 = 校园关系.信任度;
      边.感情值 = 校园关系.感情值;
      边.关系阶段 = 计算关系谱阶段(校园关系.好感度, 校园关系.亲密度, 校园关系.信任度, 校园关系.感情值);
    } else {
      边 = 创建人物关系边(
        主角姓名,
        校园关系.npc姓名,
        校园关系.npcId,
        '友情',
        计算关系谱阶段(校园关系.好感度, 校园关系.亲密度, 校园关系.信任度, 校园关系.感情值),
        '',
      );
      边.好感度 = 校园关系.好感度;
      边.亲密度 = 校园关系.亲密度;
      边.信任度 = 校园关系.信任度;
      边.感情值 = 校园关系.感情值;
      网络.关系边列表.push(边);
    }

    if (校园关系.关键事件 && 校园关系.关键事件.length > 0) {
      for (const 事件 of 校园关系.关键事件) {
        const 已有事件 = 边.事件记录.find(e => e.标题 === 事件.标题 && e.时间 === 事件.时间);
        if (!已有事件) {
          边.事件记录.push({
            id: `${边.id}-evt-${边.事件记录.length}`,
            时间: 事件.时间,
            事件类型: '其他',
            标题: 事件.标题,
            描述: 事件.描述,
          });
        }
      }
    }
  }

  网络.最后更新时间 = new Date().toISOString();
  return 网络;
};

/**
 * 更新指定关系边的属性
 */
export const 更新关系边 = (
  网络: 关系网络数据,
  edgeId: string,
  updates: Partial<Omit<人物关系边, 'id' | '主体姓名' | '客体姓名'>>,
): 关系网络数据 => {
  const 边 = 网络.关系边列表.find(b => b.id === edgeId);
  if (!边) return 网络;

  Object.assign(边, updates);
  边.关系阶段 = 计算关系谱阶段(边.好感度, 边.亲密度, 边.信任度, 边.感情值);
  网络.最后更新时间 = new Date().toISOString();

  return 网络;
};

/**
 * 为指定关系边添加事件记录
 */
export const 添加关系事件 = (
  网络: 关系网络数据,
  edgeId: string,
  事件: Omit<关系事件记录, 'id'>,
): 关系网络数据 => {
  const 边 = 网络.关系边列表.find(b => b.id === edgeId);
  if (!边) return 网络;

  边.事件记录.push({
    ...事件,
    id: `${edgeId}-evt-${边.事件记录.length}`,
  });
  网络.最后更新时间 = new Date().toISOString();

  return 网络;
};

/**
 * 获取主角的关系摘要
 */
export const 获取主角关系摘要 = (网络: 关系网络数据): string => {
  const 主角姓名 = 网络.主角姓名;
  const 主角关系 = 网络.关系边列表.filter(
    b => b.主体姓名 === 主角姓名 || b.客体姓名 === 主角姓名,
  );

  const 分类统计: Record<string, number> = {};
  for (const 边 of 主角关系) {
    分类统计[边.关系分类] = (分类统计[边.关系分类] || 0) + 1;
  }

  const 统计文本 = Object.entries(分类统计)
    .map(([分类, 数量]) => `${分类}${数量}人`)
    .join('，');

  return `与${主角姓名}建立关系的人物共 ${主角关系.length} 人：${统计文本}`;
};

/**
 * 获取指定 NPC 的关系摘要
 */
export const 获取NPC关系摘要 = (网络: 关系网络数据, npc姓名: string): string => {
  const npc关系 = 网络.关系边列表.filter(
    b => b.主体姓名 === npc姓名 || b.客体姓名 === npc姓名,
  );

  if (npc关系.length === 0) return `${npc姓名}暂无关系记录`;

  const 关系列表 = npc关系.map(b => 获取关系摘要(b)).join('；');
  return `${npc姓名}的关系：${关系列表}`;
};

/**
 * 获取指定角色的所有关系边
 */
export const 获取角色关系边 = (网络: 关系网络数据, 角色姓名: string): 人物关系边[] => {
  return 网络.关系边列表.filter(
    b => b.主体姓名 === 角色姓名 || b.客体姓名 === 角色姓名,
  );
};

/**
 * 转换为图谱数据（带性别信息）
 */
export const 转为图谱数据 = (
  网络: 关系网络数据,
  社交列表?: NPC结构[],
): { nodes: 图谱节点[]; edges: 图谱边[] } => {
  const 性别映射 = new Map<string, '男' | '女'>();
  if (社交列表) {
    for (const npc of 社交列表) {
      性别映射.set(npc.姓名, npc.性别);
    }
  }
  return 将关系网络转为图谱数据(网络, 性别映射);
};

/**
 * 懒初始化：如果存档中没有关系谱，从社交数据自动构建
 */
export const 懒初始化关系网络 = (
  社交列表: NPC结构[],
  主角姓名: string,
  现有关系谱?: 关系网络数据,
): 关系网络数据 => {
  if (现有关系谱 && 现有关系谱.关系边列表.length > 0) {
    return 现有关系谱;
  }
  return 构建关系网络(社交列表, 主角姓名 || '少侠');
};
