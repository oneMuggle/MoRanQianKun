// 事件触发权重（相对于基准值的倍数）
export const 事件触发权重: Record<学期事件类型, number> = {
  '开学': 1.0,
  '期中': 1.5,  // 考试期间事件减少
  '期末': 2.0,   // 考试期间事件最少
  '节假日': 0.5, // 假期事件减少
  '社团节': 1.2,
  '运动会': 1.0,
  '校庆': 0.8,
  '毕业': 1.5,
};

/**
 * 创建学期日历
 */
export function 创建学期日历(
  学年: number,
  学期: 1 | 2,
  开始日: string
): 学期日历 {
  const 事件列表 = 生成学期事件(学年, 学期);
  
  return {
    当前学年: 学年,
    当前学期: 学期,
    学年开始日: 开始日,
    学期开始日: 开始日,
    当前进度日: 1,
    学期总天数: 110, // 约18周
    事件列表,
    已发生事件: [],
  };
}

/**
 * 生成学期事件
 */
export function 生成学期事件(学年: number, 学期: 1 | 2): 学期事件[] {
  const 事件: 学期事件[] = [];
  
  // 开学（第1天）
  事件.push(createSemesterEvent('开学', 1));
  
  // 期中考试（第8周左右）
  事件.push(createSemesterEvent('期中', 56));
  
  // 期末考试（第17-18周）
  事件.push(createSemesterEvent('期末', 100));
  
  // 节假日（国庆/五一）
  if (学期 === 1) {
    // 国庆
    事件.push(createSemesterEvent('节假日', 42));
  } else {
    // 五一
    事件.push(createSemesterEvent('节假日', 70));
  }
  
  // 运动会（秋季）
  if (学期 === 1) {
    事件.push(createSemesterEvent('运动会', 35));
  }
  
  // 社团节（春季）
  if (学期 === 2) {
    事件.push(createSemesterEvent('社团节', 50));
  }
  
  // 校庆（随机）
  if (Math.random() > 0.5) {
    事件.push(createSemesterEvent('校庆', 60));
  }
  
  // 毕业（仅最后一个学期）
  if (学年 === 4 && 学期 === 2) {
    事件.push(createSemesterEvent('毕业', 105));
  }
  
  return 事件.sort((a, b) => a.触发日期.localeCompare(b.触发日期));
}

function createSemesterEvent(type: 学期事件类型, day: number): 学期事件 {
  const template = 学期事件模板[type];
  return {
    id: uuidv4(),
    名称: template.名称,
    类型: type,
    标记: template.标记,
    描述: template.描述,
    持续天数: template.持续天数,
    触发日期: `Day${day}`,
    可触发剧情: [],
    已触发剧情: [],
  };
}

/**
 * 推进一天
 */
export function 推进一天(日历: 学期日历): 学期日历 {
  const 新进度日 = 日历.当前进度日 + 1;
  
  // 检查是否触发新事件
  let 已发生事件 = [...日历.已发生事件];
  const 待触发事件 = 日历.事件列表.filter(
    e => !已发生事件.some(已 => 已.id === e.id) && 
         parseInt(e.触发日期.replace('Day', '')) <= 新进度日
  );
  
  已发生事件 = [...已发生事件, ...待触发事件];
  
  // 检查学期是否结束
  if (新进度日 > 日历.学期总天数) {
    // 学期结束，进入新学期
    let 新学年 = 日历.当前学年;
    let 新学期: 1 | 2 = 2;
    if (日历.当前学期 === 2) {
      新学年++;
      新学期 = 1;
    }
    
    return 创建学期日历(新学年, 新学期, 日历.学期开始日);
  }
  
  return {
    ...日历,
    当前进度日: 新进度日,
    已发生事件,
  };
}

/**
 * 获取当前生效的事件
 */
export function 获取当前事件(日历: 学期日历): 学期事件 | null {
  const 当前日 = 日历.当前进度日;
  return 日历.已发生事件.find(
    e => {
      const 触发日 = parseInt(e.触发日期.replace('Day', ''));
      return 当前日 >= 触发日 && 当前日 < 触发日 + e.持续天数;
    }
  ) || null;
}

/**
 * 获取即将到来的事件
 */
export function 获取近期事件(日历: 学期日历, 天数: number = 7): 学期事件[] {
  const 当前日 = 日历.当前进度日;
  return 日历.事件列表
    .filter(e => {
      const 触发日 = parseInt(e.触发日期.replace('Day', ''));
      return 触发日 > 当前日 && 触发日 <= 当前日 + 天数;
    })
    .slice(0, 3);
}

/**
 * 事件对游戏状态的影响
 */
export function 获取事件影响(事件: 学期事件): {
  压力变化: number;
  社交倍率: number;
  学业倍率: number;
  行动限制: string[];
} {
  let 压力变化 = 0;
  let 社交倍率 = 1.0;
  let 学业倍率 = 1.0;
  let 行动限制: string[] = [];
  
  switch (事件.标记) {
    case '考试':
      压力变化 = 30;
      学业倍率 = 1.5;
      社交倍率 = 0.3;
      行动限制 = ['旅行', '社团活动'];
      break;
    case '假期':
      压力变化 = -20;
      学业倍率 = 0.1;
      社交倍率 = 1.5;
      break;
    case '活动':
      压力变化 = 10;
      社交倍率 = 1.3;
      学业倍率 = 0.8;
      break;
    case '仪式':
      压力变化 = 5;
      社交倍率 = 1.2;
      break;
  }
  
  // 特殊事件效果叠加
  if (事件.特殊效果) {
    压力变化 += 事件.特殊效果.压力变化 || 0;
    社交倍率 *= (事件.特殊效果.社交机会 || 1);
    学业倍率 *= (事件.特殊效果.学业效率 || 1);
  }
  
  return { 压力变化, 社交倍率, 学业倍率, 行动限制 };
}

/**
 * 获取学期描述
 */
export function 获取学期描述(日历: 学期日历): string {
  const 剩余天数 = 日历.学期总天数 - 日历.当前进度日;
  const 当前事件 = 获取当前事件(日历);
  const 年级描述 = `大${['一', '二', '三', '四'][日历.当前学年 - 1]}`;
  const 学期描述 = 日历.当前学期 === 1 ? '上学期' : '下学期';
  
  let 进度描述 = '';
  if (剩余天数 > 80) {
    进度描述 = '学期刚开始';
  } else if (剩余天数 > 50) {
    进度描述 = '学期进行中';
  } else if (剩余天数 > 20) {
    进度描述 = '学期过半';
  } else if (剩余天数 > 7) {
    进度描述 = '学期尾声';
  } else {
    进度描述 = '即将放假';
  }
  
  let 事件描述 = '';
  if (当前事件) {
    事件描述 = `，现在是【${当前事件.名称}】`;
  }
  
  return `${年级描述}${学期描述}，第${日历.当前进度日}天/${日历.学期总天数}，${进度描述}${事件描述}。`;
}

/**
 * 检查是否在考试期间
 */
export function 是否在考试期间(日历: 学期日历): boolean {
  const 当前事件 = 获取当前事件(日历);
  return 当前事件?.标记 === '考试';
}

/**
 * 检查是否在假期
 */
export function 是否在假期(日历: 学期日历): boolean {
  const 当前事件 = 获取当前事件(日历);
  return 当前事件?.标记 === '假期';
}

/**
 * 添加自定义事件
 */
export function 添加自定义事件(
  日历: 学期日历,
  事件: 学期事件
): 学期日历 {
  const 新事件列表 = [...日历.事件列表, 事件].sort(
    (a, b) => a.触发日期.localeCompare(b.触发日期)
  );
  
  return {
    ...日历,
    事件列表: 新事件列表,
  };
}
