  '节假日': { 名称: '黄金周假期', 标记: '假期', 持续天数: 7, 描述: '七天长假' },
  '社团节': { 名称: '社团文化节', 标记: '活动', 持续天数: 5, 描述: '一年一度的社团盛会' },
