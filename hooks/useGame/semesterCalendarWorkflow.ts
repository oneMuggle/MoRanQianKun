/**
 * 学期日历事件系统工作流
 * 关键时间节点、事件触发、学年进度
 */

// 使用 crypto.randomUUID 替代 uuid 依赖
const uuidv4 = (): string => crypto.randomUUID();

// 学期事件类型
export type 学期事件类型 = '开学' | '期中' | '期末' | '节假日' | '社团节' | '运动会' | '校庆' | '毕业';
export type 事件标记 = '考试' | '活动' | '假期' | '仪式' | '普通';

// 学期事件
export interface 学期事件 {
  id: string;
  名称: string;
  类型: 学期事件类型;
  标记: 事件标记;
  描述: string;
  持续天数: number;
  触发日期: string;    // 游戏内日期
  可触发剧情: string[];
  已触发剧情: string[];
  特殊效果?: {
    压力变化?: number;
    社交机会?: number;
    学业效率?: number;
  };
}

// 学期日历
export interface 学期日历 {
  当前学年: number;
  当前学期: 1 | 2;
  学年开始日: string;
  学期开始日: string;
  当前进度日: number;   // 学期第几天
  学期总天数: number;   // 通常 90-120 天
  事件列表: 学期事件[];
  已发生事件: 学期事件[];
  下次考试日?: string;
  假期开始日?: string;
}

