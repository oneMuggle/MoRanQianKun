/**
 * 创建默认学业状态
 */
export function 创建默认学业状态(学年: number = 1): 学业状态 {
  return {
    学年,
    学期: 1,
    课程列表: [],
    已获学分: 0,
    总学分: 0,
    GPA: 0,
    累计GPA: 0,
    升学压力: 20,
    奖学金资格: true,
    不及格门数: 0,
    警告状态: '正常',
    最后更新时间: new Date().toISOString(),
  };
}

/**
 * 添加课程
 */
export function 添加课程(
  学业: 学业状态,
  课程名: string,
  授课老师: string,
  课程类型: 课程类型,
  学分: number
): 学业状态 {
  const 新课程: 课程成绩 = {
    课程ID: uuidv4(),
    课程名,
    授课老师,
    课程类型,
    学分,
    出勤率: 100,
    平时表现: 80,
  };
  
  return {
    ...学业,
    课程列表: [...学业.课程列表, 新课程],
    总学分: 学业.总学分 + 学分,
    最后更新时间: new Date().toISOString(),
  };
}

/**
 * 根据学年学期生成课程
 */
export function 生成学期课程(学业: 学业状态): 学业状态 {
  let 新学业 = { ...学业, 课程列表: [] };
  const 大几 = `大${['一', '二', '三', '四'][学业.学年 - 1]}` as '大一' | '大二' | '大三' | '大四';
  const 必修课程名 = 预设必修课程[1]?.[大几] || [];
  
  // 添加必修课
  必修课程名.forEach(课程名 => {
    新学业 = 添加课程(新学业, 课程名, '待定', '必修', 4);
  });
  
  // 添加2-3门选修课
  const 选修数量 = 2 + Math.floor(Math.random() * 2);
  const 可选选修 = 预设选修课程.filter(c => !新学业.课程列表.some(k => k.课程名 === c));
  for (let i = 0; i < Math.min(选修数量, 可选选修.length); i++) {
    const 随机课程 = 可选选修[Math.floor(Math.random() * 可选选修.length)];
    新学业 = 添加课程(新学业, 随机课程, '待定', '选修', 2);
    可选选修.splice(可选选修.indexOf(随机课程), 1);
  }
  
  return 新学业;
}

/**
 * 更新课程成绩
 */
export function 更新课程成绩(
  学业: 学业状态,
  课程ID: string,
  更新字段: Partial<Pick<课程成绩, '出勤率' | '平时表现' | '期中成绩' | '期末成绩'>>
): 学业状态 {
  return {
    ...学业,
    课程列表: 学业.课程列表.map(c =>
      c.课程ID === 课程ID ? { ...c, ...更新字段 } : c
    ),
    最后更新时间: new Date().toISOString(),
  };
}

/**
 * 计算分数对应的等级
 */
export function 分数转等级(分数: number): 成绩等级 {
  if (分数 >= 90) return 'A';
  if (分数 >= 80) return 'B';
  if (分数 >= 70) return 'C';
  if (分数 >= 60) return 'D';
  return 'F';
}

/**
 * 判定最终成绩
 */
export function 判定课程最终成绩(课程: 课程成绩): 课程成绩 {
  // 权重：平时30%，期中20%，期末50%
  const 平时权重 = 0.3;
  const 期中权重 = 0.2;
  const 期末权重 = 0.5;
  
  let 最终分数: number;
  
  if (课程.期末成绩 !== undefined) {
    const 平时分 = 课程.平时表现 * 平时权重 / 100 * 100;
    const 期中分 = (课程.期中成绩 || 0) * 期中权重;
    const 期末分 = 课程.期末成绩 * 期末权重;
    最终分数 = 平时分 + 期中分 + 期末分;
  } else if (课程.期中成绩 !== undefined) {
    最终分数 = 课程.平时表现 * 0.4 + 课程.期中成绩 * 0.6;
  } else {
    最终分数 = 课程.平时表现;
  }
  
  最终分数 = Math.round(最终分数);
  
  return {
    ...课程,
    成绩: 最终分数,
    等级: 分数转等级(最终分数),
    最终成绩: 最终分数,
  };
}

/**
 * 计算学期GPA
 */
export function 计算学期GPA(课程列表: 课程成绩[]): { GPA: number; 已获学分: number; 不及格门数: number } {
  let 总学分绩点 = 0;
  let 已获学分 = 0;
  let 不及格门数 = 0;
  
  课程列表.forEach(课程 => {
    if (课程.等级) {
      const 等级绩点 = 等级GPA映射[课程.等级];
      总学分绩点 += 等级绩点 * 课程.学分;
      已获学分 += 课程.学分;
      
      if (课程.等级 === 'F') {
        不及格门数++;
      }
    }
  });
  
  const GPA = 已获学分 > 0 ? 总学分绩点 / 已获学分 : 0;
  
  return { GPA: Math.round(GPA * 100) / 100, 已获学分, 不及格门数 };
}

/**
 * 判定奖学金资格
 */
export function 判定奖学金资格(学业: 学业状态): { 资格: boolean; 等级?: '国家奖学金' | '一等奖学金' | '二等奖学金' | '三等奖学金' } {
  const GPA = 学业.GPA;
  const 不及格 = 学业.不及格门数 > 0;
  
  if (不及格) {
    return { 资格: false };
  }
  
  if (GPA >= 3.8) {
    return { 资格: true, 等级: '国家奖学金' };
  }
  if (GPA >= 3.5) {
    return { 资格: true, 等级: '一等奖学金' };
  }
  if (GPA >= 3.0) {
    return { 资格: true, 等级: '二等奖学金' };
  }
  if (GPA >= 2.5) {
    return { 资格: true, 等级: '三等奖学金' };
  }
  
  return { 资格: false };
}

/**
 * 更新学业压力
 */
export function 更新学业压力(学业: 学业状态, 事件影响: number = 0): 学业状态 {
  // 基础压力
  let 基础压力 = 20;
  
  // 学期因素：期末压力增加
  if (学业.学期 === 1) {
    基础压力 += 学业.学年 * 5;
  } else {
    基础压力 += 学业.学年 * 5 + 10;
  }
  
  // GPA影响：低GPA增加压力
  if (学业.GPA < 2.5) {
    基础压力 += 20;
  } else if (学业.GPA < 3.0) {
    基础压力 += 10;
  }
  
  // 不及格增加压力
  基础压力 += 学业.不及格门数 * 15;
  
  // 奖学金资格影响
  if (!学业.奖学金资格) {
    基础压力 += 10;
  }
  
  // 计算最终压力
  const 最终压力 = Math.max(0, Math.min(100, 基础压力 + 事件影响));
  
  // 警告状态
  let 警告状态: '正常' | '学业警告' | '严重警告' = '正常';
  if (最终压力 >= 70) {
    警告状态 = '严重警告';
  } else if (最终压力 >= 50) {
    警告状态 = '学业警告';
  }
  
  return {
    ...学业,
    升学压力: 最终压力,
    警告状态,
    最后更新时间: new Date().toISOString(),
  };
}

/**
 * 学期结束，结算成绩
 */
export function 学期结算(学业: 学业状态): 学业状态 {
  // 判定每门课程最终成绩
  const 结算后课程 = 学业.课程列表.map(c => 判定课程最终成绩(c));
  
  // 计算GPA
  const { GPA, 已获学分, 不及格门数 } = 计算学期GPA(结算后课程);
  
  // 更新累计GPA（简化计算）
  const 旧总学分 = 学业.已获学分 + (学业.总学分 - 学业.课程列表.reduce((sum, c) => sum + c.学分, 0));
  const 累计GPA = 旧总学分 > 0 
    ? (学业.累计GPA * 旧总学分 + GPA * 已获学分) / (旧总学分 + 已获学分)
    : GPA;
  
  // 奖学金判定
  const 奖学金 = 判定奖学金资格({ ...学业, GPA, 不及格门数 });
  
  // 创建新学期学业状态
  let 新学年 = 学业.学年;
  let 新学期: 学期 = 2;
  if (学业.学期 === 2) {
    新学年 = 学业.学年 + 1;
    新学期 = 1;
  }
  
  return 更新学业压力({
    ...学业,
    学年: 新学年,
    学期: 新学期,
    课程列表: [],
    已获学分: 学业.已获学分 + 已获学分,
    GPA,
    累计GPA: Math.round(累计GPA * 100) / 100,
    奖学金资格: 奖学金.资格,
    奖学金等级: 奖学金.等级,
    不及格门数,
    最后更新时间: new Date().toISOString(),
  });
}

/**
 * 获取学业状态描述
 */
export function 获取学业描述(学业: 学业状态): string {
  const 年级描述 = `大${['一', '二', '三', '四'][学业.学年 - 1]}`;
  const 学期描述 = 学业.学期 === 1 ? '上学期' : '下学期';
  const GPA描述 = 学业.GPA >= 3.5 ? '学霸级' : 学业.GPA >= 3.0 ? '优秀' : 学业.GPA >= 2.5 ? '良好' : 学业.GPA >= 2.0 ? '一般' : '需要努力';
  const 压力描述 = 学业.升学压力 >= 70 ? '压力巨大' : 学业.升学压力 >= 50 ? '压力较大' : 学业.升学压力 >= 30 ? '稍有压力' : '轻松愉快';
  
  let 奖学金描述 = '';
  if (学业.奖学金资格 && 学业.奖学金等级) {
    奖学金描述 = `，有${学业.奖学金等级}资格`;
  }
  
  return `${年级描述}${学期描述}，GPA ${学业.GPA.toFixed(2)}（${GPA描述}）${奖学金描述}，${压力描述}。`;
}

/**
 * 升学判定（每学年结束时）
 */
export function 判定升学(学业: 学业状态): boolean {
  // 硬性条件：GPA >= 2.0，不及格门数 <= 1
  if (学业.GPA < 2.0 || 学业.不及格门数 > 1) {
    return false;
  }
  
  // 大四不需要升学
  if (学业.学年 >= 4) {
    return true; // 毕业
  }
  
  return true;
}
