/**
 * 校园学业追踪系统工作流
 * 课程管理、成绩判定、GPA计算、升学压力
 */

// 使用 crypto.randomUUID 替代 uuid 依赖
const uuidv4 = (): string => crypto.randomUUID();

// 学年学期
export type 学期 = 1 | 2;
export type 成绩等级 = 'A' | 'B' | 'C' | 'D' | 'F';
export type 课程类型 = '必修' | '选修' | '通识';
export type 考试类型 = '期中' | '期末' | '平时' | '论文';

// 课程成绩
export interface 课程成绩 {
  课程ID: string;
  课程名: string;
  授课老师: string;
  学分: number;
  课程类型: 课程类型;
  成绩?: number;      // 0-100
  等级?: 成绩等级;
  出勤率: number;      // 0-100
  平时表现: number;    // 0-100
  期中成绩?: number;
  期末成绩?: number;
  最终成绩?: number;
}

// 学业状态
export interface 学业状态 {
  学年: number;
  学期: 学期;
  课程列表: 课程成绩[];
  已获学分: number;
  总学分: number;
  GPA: number;              // 0-4.0
  累计GPA: number;          // 截止目前的累计GPA
  升学压力: number;          // 0-100
  奖学金资格: boolean;
  奖学金等级?: '国家奖学金' | '一等奖学金' | '二等奖学金' | '三等奖学金';
  不及格门数: number;
  警告状态: '正常' | '学业警告' | '严重警告';
  最后更新时间: string;
}

