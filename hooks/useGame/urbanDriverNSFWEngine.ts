/**
 * 更新乘客欲望状态
 */
export function 更新乘客欲望状态(
  当前档案: 乘客欲望档案,
  互动类型: string,
  地点: 行程地点,
  设置: 都市网约车NSFW设置
): 乘客欲望档案 {
  const 基础值 = 欲望阶段推进基础值[互动类型] ?? 5;
  const 地点修正 = 地点暴露风险修正[地点] ?? 1.0;
  const 轨道修正 = 关系轨道推进系数[当前档案.关系轨道] ?? 1.0;
  const 醉酒修正 = 当前档案.醉酒状态 ? 醉酒行为加成[当前档案.醉酒状态.等级] : 1.0;

  const 进度增加 = Math.round(基础值 * 地点修正 * 轨道修正 * 醉酒修正 * (0.9 + Math.random() * 0.2));
  const 新阶段进度 = Math.min(100, 当前档案.阶段进度 + 进度增加);

  // 检查阶段升级
  const { 升级, 新阶段 } = 检查欲望阶段升级({ ...当前档案, 阶段进度: 新阶段进度 });
  const 最终阶段 = 升级 ? 新阶段 : 当前档案.当前阶段;
  const 最终进度 = 升级 ? 0 : 新阶段进度;

  // 更新暴露风险
  const 新暴露风险 = 计算暴露风险(当前档案.暴露风险值, 地点, 最终阶段);

  // 更新紧张度
  const 新紧张度 = 计算紧张度(当前档案.紧张度, 地点, 设置.启用行车记录仪系统);

  return {
    ...当前档案,
    当前阶段: 最终阶段,
    阶段进度: 最终进度,
    暴露风险值: 新暴露风险,
    紧张度: 新紧张度,
  };
}

/**
 * 检查欲望阶段升级
 */
export function 检查欲望阶段升级(档案: 乘客欲望档案): { 升级: boolean; 新阶段: 乘客欲望阶段 } {
  if (档案.阶段进度 >= 100) {
    const 当前索引 = 乘客欲望阶段列表.indexOf(档案.当前阶段);
    if (当前索引 < 乘客欲望阶段列表.length - 1) {
      return { 升级: true, 新阶段: 乘客欲望阶段列表[当前索引 + 1] };
    }
  }
  return { 升级: false, 新阶段: 档案.当前阶段 };
}

/**
 * 计算暴露风险
 */
export function 计算暴露风险(_当前风险: number, 地点: 行程地点, 欲望阶段: 乘客欲望阶段): number {
  const 地点基础 = 地点风险[地点] ?? 30;
  const 阶段加成: Record<乘客欲望阶段, number> = { '克制': 0, '试探': 10, '渴望': 25, '沉沦': 40, '支配': 50 };
  const 随机波动 = Math.random() * 10 - 5;

  return Math.min(100, Math.max(0, Math.round(地点基础 * 0.5 + 阶段加成[欲望阶段] + 随机波动)));
}

/**
 * 计算紧张度
 */
export function 计算紧张度(当前紧张度: number, 地点: 行程地点, 启用记录仪: boolean): number {
  const 地点基础 = (地点风险[地点] ?? 30) * 0.3;
  const 记录仪加成 = 启用记录仪 ? 20 : 0;
  const 衰减 = 当前紧张度 * 0.1;

  return Math.min(100, Math.max(0, Math.round(地点基础 + 记录仪加成 + 衰减)));
}

/**
 * 判定行程 NSFW 类型
 */
export function 判定行程NSFW类型(
  地点: 行程地点,
  当前小时: number,
  乘客档案: 乘客欲望档案 | null,
  设置: 都市网约车NSFW设置,
  额外上下文?: {
    乘客数量?: number;
    到达后未离开?: boolean;
    常客搭乘次数?: number;
    行车记录仪开启?: boolean;
  }
): 行程NSFW类型 | null {
  const 深夜 = 是否为深夜(当前小时);
  const 欲望阶段 = 乘客档案?.当前阶段 ?? '克制';
  const 醉酒等级 = 乘客档案?.醉酒状态?.等级 ?? '清醒';
  const 有药物 = !!乘客档案?.药物状态 && 乘客档案.药物状态.生效阶段 !== '未生效';

  // 醉酒搭车
  if (设置.启用醉酒乘客场景 && 醉酒等级 !== '清醒' && 深夜) {
    const 规则 = 行程NSFW判定规则['醉酒搭车'];
    if (规则.地点偏好.includes(地点)) return '醉酒搭车';
  }

  // 饮料下药
  if (设置.启用饮料下药场景 && 有药物) {
    return '饮料下药';
  }

  // 深夜独处
  if (设置.启用深夜独处场景 && 深夜 && (!额外上下文?.乘客数量 || 额外上下文.乘客数量 <= 1)) {
    const 规则 = 行程NSFW判定规则['深夜独处'];
    if (规则.地点偏好.includes(地点)) return '深夜独处';
  }

  // 后座暗示
  if (设置.启用后座暗示场景 && 欲望阶段 !== '克制') {
    const 规则 = 行程NSFW判定规则['后座暗示'];
    if (规则.地点偏好.includes(地点)) return '后座暗示';
  }

  // 停车场秘密
  if (设置.启用停车场秘密场景 && 深夜 && 额外上下文?.到达后未离开) {
    const 规则 = 行程NSFW判定规则['停车场秘密'];
    if (规则.地点偏好.includes(地点)) return '停车场秘密';
  }

  // 拼车暧昧
  if (设置.启用拼车暧昧场景 && (额外上下文?.乘客数量 ?? 1) > 1) {
    const 规则 = 行程NSFW判定规则['拼车暧昧'];
    if (规则.地点偏好.includes(地点)) return '拼车暧昧';
  }

  // 常客关系
  if (设置.启用常客关系系统 && (额外上下文?.常客搭乘次数 ?? 0) >= 3) {
    const 规则 = 行程NSFW判定规则['常客关系'];
    if (规则.地点偏好.includes(地点)) return '常客关系';
  }

  // 行车记录仪
  if (设置.启用行车记录仪系统 && 额外上下文?.行车记录仪开启 && 欲望阶段 !== '克制') {
    const 规则 = 行程NSFW判定规则['行车记录仪'];
    if (规则.地点偏好.includes(地点)) return '行车记录仪';
  }

  // 保底：深夜+单人时至少触发"深夜独处"，确保系统不会停滞
  if (设置.启用深夜独处场景 && 深夜 && (!额外上下文?.乘客数量 || 额外上下文.乘客数量 <= 1)) {
    return '深夜独处';
  }

  return null;
}

/**
 * 生成后果事件
 */
export function 生成后果事件(
  行程类型: 行程NSFW类型,
  暴露风险: number,
  设置: 都市网约车NSFW设置,
  影响NPC: string[] = []
): 后果事件 | null {
  if (!设置.启用后果系统) return null;

  // 根据暴露风险和行程类型判定是否触发后果
  const 触发阈值 = 设置.后果严重程度 === '轻微' ? 80
    : 设置.后果严重程度 === '中等' ? 60
    : 设置.后果严重程度 === '严重' ? 40 : 25;

  if (暴露风险 < 触发阈值) return null;

  // 根据行程类型选择可能的后果
  const 可能后果 = 获取可能后果(行程类型, 设置);
  if (可能后果.length === 0) return null;

  const 随机索引 = Math.floor(Math.random() * 可能后果.length);
  const 后果类型 = 可能后果[随机索引];
  const 严重度 = 计算后果严重度(后果类型, 暴露风险);

  return {
    id: `consequence_${Date.now()}_${Math.random().toString(36).slice(2, 8)}`,
    类型: 后果类型,
    严重程度: 严重度,
    描述: `因「${行程类型}」行程触发了${后果类型}`,
    触发时间: Date.now(),
    影响NPC,
    影响司机: {},
    已处理: false,
  };
}

function 获取可能后果(行程类型: 行程NSFW类型, 设置: 都市网约车NSFW设置): 网约车后果类型[] {
  const 基础后果: Record<行程NSFW类型, 网约车后果类型[]> = {
    '醉酒搭车': ['平台投诉', '乘客依赖', '情感纠葛'],
    '饮料下药': ['药物后遗症', '警察盘查', '网络传播'],
    '深夜独处': ['行车记录仪曝光', '乘客依赖'],
    '后座暗示': ['平台投诉', '行车记录仪曝光'],
    '停车场秘密': ['行车记录仪曝光', '警察盘查', '网络传播'],
    '拼车暧昧': ['网络传播', '情感纠葛', '常客流失'],
    '常客关系': ['乘客依赖', '情感纠葛', '常客流失'],
    '行车记录仪': ['行车记录仪曝光', '网络传播', '勒索威胁'],
  };

  let 后果 = 基础后果[行程类型] ?? [];

  // 根据设置过滤
  if (!设置.启用平台处罚) {
    后果 = 后果.filter(c => c !== '平台投诉' && c !== '差评降权' && c !== '封号处罚');
  }
  if (!设置.启用网络传播) {
    后果 = 后果.filter(c => c !== '网络传播');
  }
  if (!设置.启用警察盘查) {
    后果 = 后果.filter(c => c !== '警察盘查');
  }
  if (!设置.启用勒索威胁) {
    后果 = 后果.filter(c => c !== '勒索威胁');
  }

  return 后果;
}

function 计算后果严重度(后果类型: 网约车后果类型, 暴露风险: number): '轻微' | '中等' | '严重' | '毁灭' {
  const 权重 = 后果严重度权重[后果类型] ?? 50;
  const 综合评分 = (权重 + 暴露风险) / 2;

  if (综合评分 >= 85) return '毁灭';
  if (综合评分 >= 65) return '严重';
  if (综合评分 >= 40) return '中等';
  return '轻微';
}

/**
 * 更新醉酒状态
 */
export function 更新醉酒状态(
  当前状态: 醉酒状态,
  酒精摄入量: '少量' | '中量' | '大量'
): 醉酒状态 {
  const 摄入加成: Record<string, number> = { '少量': 15, '中量': 35, '大量': 60 };
  const 新大胆度 = Math.min(100, 当前状态.行为大胆度 + 摄入加成[酒精摄入量]);
  const 新模糊度 = Math.min(100, 当前状态.记忆模糊度 + Math.round(摄入加成[酒精摄入量] * 0.7));

  const 新等级 = 新大胆度 >= 80 ? '烂醉'
    : 新大胆度 >= 50 ? '醉酒'
    : 新大胆度 >= 20 ? '微醺' : '清醒';

  return {
    等级: 新等级,
    判断力下降: 新大胆度 >= 30,
    行为大胆度: 新大胆度,
    记忆模糊度: 新模糊度,
  };
}

/**
 * 更新药物状态（随时间衰减）
 */
export function 更新药物状态(当前状态: 药物状态, 时间流逝分钟: number): 药物状态 {
  const 衰减速率: Record<NonNullable<药物状态>['生效阶段'], number> = {
    '未生效': 0,
    '初期': 0.5,
    '强烈': 1.5,
    '衰退': 0.3,
  };

  const 衰减 = 衰减速率[当前状态.生效阶段] * 时间流逝分钟;
  const 新清晰度 = Math.min(100, 当前状态.意识清晰度 + 衰减);
  const 新控制度 = Math.min(100, 当前状态.身体控制度 + 衰减 * 0.8);

  const 新阶段 = 新清晰度 >= 80 ? '衰退'
    : 新清晰度 >= 50 ? (当前状态.生效阶段 === '初期' ? '初期' : '强烈')
    : 当前状态.生效阶段 === '未生效' ? '初期' : 当前状态.生效阶段;

  return {
    ...当前状态,
    生效阶段: 新阶段 as 药物状态['生效阶段'],
    意识清晰度: Math.round(新清晰度),
    身体控制度: Math.round(新控制度),
  };
}

