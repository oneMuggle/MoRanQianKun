/**
 * 露出 NSFW 独立引擎 — 后果引擎
 * 暴露成功/失败的真实后续影响：名誉、关系、分离等
 */

import type { 露出后果记录, 后果类型, 后果严重等级, 名誉状态, 旁观者 } from '../../../models/exposureNSFW';

/** 根据露出结果和上下文判定后果 */
export function 判定露出后果(参数: {
  露出结果: '成功' | '失败';
  当前等级: number;
  紧张度: number;
  已察觉旁观者: 旁观者[];
  当前名誉: 名誉状态;
}): 露出后果记录[] {
  const { 露出结果, 当前等级, 紧张度, 已察觉旁观者, 当前名誉 } = 参数;
  const 后果: 露出后果记录[] = [];
  const now = new Date().toISOString();

  if (露出结果 === '失败') {
    const 严重旁观者 = 已察觉旁观者.filter(b =>
      b.反应 === '直接揭穿' || b.反应 === '传播流言'
    );

    if (严重旁观者.length >= 2 && 紧张度 > 80) {
      后果.push(创建后果('社会性死亡', '毁灭', '在多人面前暴露，名誉扫地', 当前名誉, 999, now));
    } else if (严重旁观者.length >= 1 && 紧张度 > 60) {
      后果.push(创建后果('公开曝光', '毁灭', '被当众揭穿，关系面临重构', 当前名誉, 999, now));
    } else if (紧张度 > 50) {
      后果.push(创建后果('名誉受损', '严重', '流言开始扩散，名誉受到影响', 当前名誉, 20, now));
    } else if (已察觉旁观者.length > 0) {
      后果.push(创建后果('流言扩散', '中等', '旁观者将所见告知他人', 当前名誉, 10, now));
    } else {
      后果.push(创建后果('短暂尴尬', '轻微', '险些暴露，心跳加速', 当前名誉, 1, now));
    }
  } else {
    if (紧张度 > 85) {
      后果.push(创建后果('旁人窃语', '轻微', '虽未直接暴露，但异常举动引起窃窃私语', 当前名誉, 5, now));
    }
    if (当前等级 >= 4 && 紧张度 > 60) {
      后果.push(创建后果('流言扩散', '中等', '高调行为引起他人注意', 当前名誉, 10, now));
    }
  }

  return 后果;
}

function 创建后果(
  后果类型: 后果类型,
  严重等级: 后果严重等级,
  描述: string,
  _当前名誉: 名誉状态,
  持续回合: number,
  时间: string
): 露出后果记录 {
  return {
    id: `consequence_${Date.now()}_${Math.random().toString(36).slice(2, 8)}`,
    后果类型,
    严重等级,
    描述,
    触发时间: 时间,
    持续回合,
    剩余回合: 持续回合,
    已解决: false,
  };
}

/** 经过指定回合后衰减后果 */
export function 应用后果衰减(后果: 露出后果记录, 经过回合: number): 露出后果记录 {
  const 新剩余 = 后果.剩余回合 - 经过回合;
  if (新剩余 <= 0) {
    return { ...后果, 剩余回合: 0, 已解决: true };
  }
  return { ...后果, 剩余回合: 新剩余 };
}

/** 批量衰减活跃后果 */
export function 衰减所有活跃后果(后果列表: 露出后果记录[], 经过回合: number = 1): 露出后果记录[] {
  return 后果列表
    .map(c => 应用后果衰减(c, 经过回合))
    .filter(c => !c.已解决);
}

/** 创建默认名誉状态 */
export function 创建默认名誉状态(): 名誉状态 {
  return {
    公开名誉: 50,
    私密名誉: 50,
    风评: '默默无闻',
    标签: [],
    黑历史: [],
  };
}

/** 根据后果更新名誉状态 */
export function 更新名誉状态(名誉: 名誉状态, 后果: 露出后果记录[]): 名誉状态 {
  if (后果.length === 0) return 名誉;

  let { 公开名誉, 私密名誉, 风评, 标签, 黑历史 } = 名誉;

  for (const 该后果 of 后果) {
    switch (该后果.严重等级) {
      case '轻微':
        公开名誉 = Math.max(0, 公开名誉 - 2);
        break;
      case '中等':
        公开名誉 = Math.max(0, 公开名誉 - 8);
        私密名誉 = Math.max(0, 私密名誉 - 3);
        break;
      case '严重':
        公开名誉 = Math.max(0, 公开名誉 - 15);
        私密名誉 = Math.max(0, 私密名誉 - 10);
        break;
      case '毁灭':
        公开名誉 = Math.max(0, 公开名誉 - 30);
        私密名誉 = Math.max(0, 私密名誉 - 20);
        break;
    }

    const 新标签 = 获取后果标签(该后果.后果类型);
    for (const t of 新标签) {
      if (!标签.includes(t)) {
        标签.push(t);
      }
    }

    黑历史.push({
      事件: 该后果.描述,
      时间: 该后果.触发时间,
      影响: 该后果.严重等级,
    });
    if (黑历史.length > 20) {
      黑历史 = 黑历史.slice(-20);
    }
  }

  风评 = 计算风评(公开名誉, 私密名誉, 标签);

  return { 公开名誉, 私密名誉, 风评, 标签, 黑历史 };
}

function 获取后果标签(后果类型: 后果类型): string[] {
  switch (后果类型) {
    case '短暂尴尬': return [];
    case '旁人窃语': return ['引人注意'];
    case '流言扩散': return ['流言缠身'];
    case '信任危机': return ['关系破裂'];
    case '名誉受损': return ['名声不佳'];
    case '强制分离': return ['被迫疏远'];
    case '公开曝光': return ['众人皆知', '丑闻'];
    case '社会性死亡': return ['声名狼藉', '众叛亲离'];
    default: return [];
  }
}

function 计算风评(公开名誉: number, _私密名誉: number, _标签: string[]): string {
  if (公开名誉 >= 80) return '清白之身';
  if (公开名誉 >= 60) return '名声尚可';
  if (公开名誉 >= 40) return '略有风评';
  if (公开名誉 >= 20) return '名声不佳';
  if (公开名誉 >= 10) return '声名狼藉';
  return '社会性死亡';
}

/** 每回合名誉自然恢复 */
export function 名誉自然恢复(名誉: 名誉状态, 回合数: number = 1): 名誉状态 {
  const 恢复量 = 回合数 * 0.5;
  return {
    ...名誉,
    公开名誉: Math.min(100, 名誉.公开名誉 + 恢复量),
    私密名誉: Math.min(100, 名誉.私密名誉 + 恢复量 * 0.3),
  };
}
