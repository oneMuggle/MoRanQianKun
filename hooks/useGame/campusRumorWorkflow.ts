// 不同来源的初始影响力
export const 来源初始影响力: Record<传闻来源, number> = {
  'NPC行为': 30,
  '玩家行为': 50,
  '系统事件': 40,
};

// 不同范围的传播速度（每天）
export const 范围传播速度: Record<传播范围, number> = {
  '小圈子': 1,
  '班级': 3,
  '年级': 8,
  '全校': 15,
};

/**
 * 创建校园传闻
 */
export function 创建传闻(
  内容: string,
  类型: 传闻类型,
  涉及NPC: string[],
  涉及玩家: boolean,
  传播范围: 传播范围,
  真实性: number,
  来源: 传闻来源,
  创建者?: string
): 校园传闻 {
  const 基础影响力 = 来源初始影响力[来源];
  // 真实性影响影响力
  const 影响力 = Math.round(基础影响力 * (真实性 / 100) + (100 - 真实性) * 0.1);
  
  return {
    id: uuidv4(),
    内容,
    类型,
    涉及NPC,
    涉及玩家,
    传播范围,
    真实性,
    影响力,
    持续天数: 类型 === '关系' ? 14 : 类型 === '事件' ? 7 : 10, // 关系类持续较久
    创建时间: new Date().toISOString(),
    创建者,
  };
}

/**
 * 使用模板生成传闻
 */
export function 使用模板生成传闻(
  类型: 传闻类型,
  涉及NPC: string[],
  涉及玩家: boolean,
  传播范围: 传播范围,
  来源: 传闻来源
): 校园传闻 {
  const 模板列表 = 传闻模板[类型];
  const 模板 = 模板列表[Math.floor(Math.random() * 模板列表.length)];
  
  // 填充模板
  let 内容 = 模板;
  const npcNames = 涉及NPC.slice(0, 2); // 最多2个NPC
  npcNames.forEach((npcId, index) => {
    内容 = 内容.replace(`{NPC${index + 1}}`, npcId); // 简化，实际应替换为名字
    内容 = 内容.replace(/\{NPC\}/g, npcId);
  });
  
  // 随机真实性（40-90）
  const 真实性 = Math.floor(Math.random() * 51) + 40;
  
  return 创建传闻(内容, 类型, 涉及NPC, 涉及玩家, 传播范围, 真实性, 来源);
}

/**
 * 传播传闻到更大范围
 */
export function 传播传闻(传闻: 校园传闻, 所有NPC: string[]): 校园传闻 {
  if (传闻.持续天数 <= 0) return 传闻;
  
  // 范围升级
  let 新范围: 传播范围 =传闻.传播范围;
  const 范围升级表: Record<传播范围, 传播范围 | null> = {
    '小圈子': '班级',
    '班级': '年级',
    '年级': '全校',
    '全校': null, // 已到最大
  };
  
  const 升级后 = 范围升级表[传闻.传播范围];
  if (升级后 && Math.random() * 100 < 范围传播速度[传闻.传播范围]) {
    新范围 = 升级后;
  }
  
  // 真实性漂移（传播过程可能失真）
  const 真实性漂移 = Math.floor(Math.random() * 11) - 5; // -5到+5
  const 新真实性 = Math.max(10, Math.min(100, 传闻.真实性 + 真实性漂移));
  
  // 影响力略微下降（传播稀释）
  const 新影响力 = Math.max(10, 传闻.影响力 - 5);
  
  return {
    ...传闻,
    传播范围: 新范围,
    真实性: 新真实性,
    影响力: 新影响力,
    持续天数: 传闻.持续天数 - 1,
    源传闻Id: 传闻.源传闻Id || 传闻.id, // 记录原始传闻
  };
}

/**
 * 衰减传闻（每日处理）
 */
export function 衰减传闻(传闻: 校园传闻): 校园传闻 {
  const 每日衰减 = 5;
  const 真实性衰减 = 2;
  
  return {
    ...传闻,
    影响力: Math.max(0, 传闻.影响力 - 每日衰减),
    真实性: Math.max(0, 传闻.真实性 - 真实性衰减),
    持续天数: 传闻.持续天数 - 1,
  };
}

/**
 * 检查传闻是否过期
 */
export function 传闻是否过期(传闻: 校园传闻): boolean {
  return 传闻.持续天数 <= 0 || 传闻.影响力 <= 0;
}

/**
 * 获取传闻对NPC的影响
 */
export function 获取传闻对NPC的影响(
  传闻: 校园传闻,
  npcId: string
): { 态度变化: number; 信任度变化: number } {
  // NPC是传闻主角
  if (传闻.涉及NPC.includes(npcId)) {
    if (传闻.真实性 >= 70) {
      return { 态度变化: -20, 信任度变化: -15 }; // 高真实性负面传闻影响大
    } else if (传闻.真实性 >= 40) {
      return { 态度变化: -10, 信任度变化: -5 };
    }
    return { 态度变化: -5, 信任度变化: 0 }; // 低真实性传闻影响小
  }
  
  // NPC是旁观者
  return {
    态度变化: Math.floor(传闻.影响力 * 0.1) * -1,
    信任度变化: Math.floor(传闻.真实性 * 0.05) * -1,
  };
}

/**
 * 玩家行为触发传闻生成
 */
export function 玩家行为生成传闻(
  行为类型: '关系确认' | '亲密行为' | '优异表现' | '冲突' | '其他',
  涉及NPC: string[],
  行为描述: string
): 校园传闻 {
  let 类型: 传闻类型 = '其他';
  let 传播范围: 传播范围 = '班级';
  let 真实性 = 60;
  let 来源: 传闻来源 = '玩家行为';
  
  switch (行为类型) {
    case '关系确认':
      类型 = '关系';
      传播范围 = '班级';
      真实性 = 80;
      break;
    case '亲密行为':
      类型 = '关系';
      传播范围 = '小圈子';
      真实性 = 50;
      break;
    case '优异表现':
      类型 = '学术';
      传播范围 = '年级';
      真实性 = 85;
      break;
    case '冲突':
      类型 = '八卦';
      传播范围 = '班级';
      真实性 = 70;
      break;
  }
  
  const 内容 = `【玩家传闻】${行为描述}`;
  
  return 创建传闻(内容, 类型, 涉及NPC, true, 传播范围, 真实性, 来源);
}

/**
 * 获取传闻状态描述
 */
export function 获取传闻描述(传闻: 校园传闻): string {
  const 真实性描述 = 传闻.真实性 >= 80 ? '确凿' : 传闻.真实性 >= 60 ? '可信' : 传闻.真实性 >= 40 ? '存疑' : '离谱';
  const 影响力描述 = 传闻.影响力 >= 80 ? '众人皆知' : 传闻.影响力 >= 60 ? '广为传播' : 传闻.影响力 >= 30 ? '小范围讨论' : '鲜为人知';
  const 范围描述 = 传闻.传播范围;
  
  return `${范围描述}传闻【${真实性描述}】${影响力描述}：${传闻.内容}`;
}

/**
 * 批量处理传闻（每日调用）
 */
export function 批量处理传闻(传闻列表: 校园传闻[], 所有NPC: string[]): 校园传闻[] {
  return 传闻列表
    .map(传闻 => {
      // 先衰减
      let 处理后 = 衰减传闻(传闻);
      // 有概率传播
      if (Math.random() < 0.3 && 处理后.持续天数 > 0) {
        处理后 = 传播传闻(处理后, 所有NPC);
      }
      return 处理后;
    })
    .filter(传闻 => !传闻是否过期(传闻));
}

/**
 * 获取NPC相关的活跃传闻
 */
export function 获取NPC相关传闻(传闻列表: 校园传闻[], npcId: string): 校园传闻[] {
  return 传闻列表.filter(
    传闻 => 传闻.涉及NPC.includes(npcId) && 传闻.影响力 > 20
  );
}

/**
 * 计算传闻网络（谁知道了什么）
 */
export function 计算传闻网络(
  传闻列表: 校园传闻[],
  npcId: string
): { 已知传闻: 校园传闻[]; 未知传闻: 校园传闻[] } {
  // 根据NPC的"社交圈"计算他们可能知道什么
  const 已知传闻: 校园传闻[] = [];
  const 未知传闻: 校园传闻[] = [];
  
  传闻列表.forEach(传闻 => {
    // 简化计算：NPC知道他们涉及的传闻，以及高影响力的全校传闻
    if (传闻.涉及NPC.includes(npcId)) {
      已知传闻.push(传闻);
    } else if (传闻.传播范围 === '全校' && 传闻.影响力 >= 60) {
      已知传闻.push(传闻);
    } else if (传闻.传播范围 === '年级' && 传闻.影响力 >= 70) {
      已知传闻.push(传闻);
    } else if (传闻.传播范围 === '班级' && 传闻.影响力 >= 50) {
      已知传闻.push(传闻);
    } else {
      未知传闻.push(传闻);
    }
  });
  
  return { 已知传闻, 未知传闻 };
}
