/**
 * 计算露出偏好衰减：长期无互动时等级进度回退
 */
export function 计算露出衰减(参数: {
  当前露出状态: 露出状态 | undefined;
  无互动回合: number;
}): 露出状态 | undefined {
  const { 当前露出状态, 无互动回合 } = 参数;
  if (!当前露出状态 || 当前露出状态.当前等级 === 0) return 当前露出状态;

  let 新进度 = 当前露出状态.等级进度;
  let 新等级: number = 当前露出状态.当前等级;

  // 每 3 个无互动回合减少 10 进度
  const 进度回退 = Math.floor(无互动回合 / 3) * 10;
  新进度 = Math.max(0, 新进度 - 进度回退);

  // 如果进度归零且等级 > 0，回退一级
  if (新进度 === 0 && 新等级 > 0 && 无互动回合 >= 10) {
    新等级 = Math.max(0, 新等级 - 1);
    新进度 = 50;
  }

  return {
    ...当前露出状态,
    当前等级: 新等级 as 露出偏好等级,
    等级进度: 新进度,
  };
}

/**
 * 计算权力天平推进
 */
export function 计算权力天平推进(参数: {
  当前值: number;
  波动范围: number;
  已交互次数: number;
  选择倾向: '主导' | '被引导' | '平等';
  欲望阶段: 欲望阶段;
}): { 新值: number; 新波动范围: number; 是否固化: boolean } {
  const { 当前值, 波动范围, 已交互次数, 选择倾向, 欲望阶段 } = 参数;

  let 推进值 = 0;
  if (选择倾向 === '主导') 推进值 = -15;
  else if (选择倾向 === '被引导') 推进值 = 15;
  else 推进值 = Math.random() < 0.5 ? -5 : 5;

  let 收敛系数 = 1.0;
  if (欲望阶段 === '渴望') 收敛系数 = 0.8;
  else if (欲望阶段 === '沉沦') 收敛系数 = 0.5;
  else if (欲望阶段 === '支配') 收敛系数 = 0.2;

  const 随机波动 = (Math.random() - 0.5) * 2 * 波动范围 * 收敛系数;
  const 新值 = Math.max(-100, Math.min(100, Math.round(当前值 + 推进值 + 随机波动)));

  let 新波动范围 = 波动范围;
  if (已交互次数 > 0 && 已交互次数 % 5 === 0) {
    新波动范围 = Math.max(5, Math.round(波动范围 * 0.9));
  }

  const 是否固化 = 欲望阶段 === '支配' || 新波动范围 <= 5;

  return { 新值, 新波动范围, 是否固化 };
}

