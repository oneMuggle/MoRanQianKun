/**
 * 计算欲望阶段推进
 */
export function 计算欲望阶段推进(
  当前档案: NPC欲望档案,
  互动类型: 校园亲密互动类型,
  地点暴露风险: string,
  选择倾向: string
): number {
  const 基础值 = 欲望阶段推进基础值[互动类型] ?? 5;
  const 地点加成 = 地点暴露风险基础值[地点暴露风险] !== undefined
    ? 1.0 + (地点暴露风险基础值[地点暴露风险] / 100 - 0.5) * 0.5
    : 1.0;
  const 系数 = 选择系数[选择倾向] ?? 1.0;
  const 随机波动 = 0.9 + Math.random() * 0.2; // 0.9-1.1

  const 进度增加 = Math.round(基础值 * 地点加成 * 系数 * 随机波动);
  const 新进度 = Math.min(100, 当前档案.阶段进度 + 进度增加);

  return 新进度;
}

