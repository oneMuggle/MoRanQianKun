/**
 * 晋升仪式引擎
 * 处理位分晋升的条件判定、仪式选择、效果计算
 */

import type { NSFW资源状态 } from '../../../models/nsfwCore/resources';
import type { 后宫系统状态, 仪式规模 } from '../../../models/nsfwCore/multiPersonNSFW';
import {
  晋升仪式参数,
  晋升仪式结果,
  多人NSFW场景参数,
  仪式规模配置,
} from '../../../models/nsfwCore/multiPersonNSFW';
import {
  计算晋升仪式效果,
  生成多人NSFW叙事约束,
} from '../../../models/nsfwCore/multiPersonNSFWUtils';

/**
 * 检查成员是否满足晋升条件
 */
export function 检查晋升条件(
  成员: 后宫系统状态['成员列表'][number],
  目标位分等级: number
): { 满足: boolean; 原因: string | null } {
  // 亲密度等级要求: 目标位分等级 >= 4 需要 Lv5, >= 3 需要 Lv4
  const 需求亲密度 = 目标位分等级 >= 4 ? 5 : 目标位分等级 >= 3 ? 4 : 3;
  if (成员.亲密度等级 < 需求亲密度) {
    return { 满足: false, 原因: `亲密度等级不足（需要 Lv${需求亲密度}，当前 Lv${成员.亲密度等级}）` };
  }

  // 信任度要求: >= 75
  if (成员.信任度 < 75) {
    return { 满足: false, 原因: `信任度不足（需要 75，当前 ${成员.信任度}）` };
  }

  return { 满足: true, 原因: null };
}

/**
 * 初始化晋升仪式
 */
export function 初始化晋升仪式(
  成员: 后宫系统状态['成员列表'][number],
  目标位分: string,
  仪式规模: 仪式规模
): 晋升仪式参数 {
  const 配置 = 仪式规模配置[仪式规模];

  return {
    npcId: 成员.npcId,
    姓名: 成员.姓名,
    原排位: 成员.位分,
    新排位: 目标位分,
    仪式规模,
    亲密代币消耗: 配置.亲密代币消耗,
    精力消耗: 配置.精力消耗,
  };
}

/**
 * 处理晋升仪式
 */
export function 处理晋升仪式(
  参数: 晋升仪式参数,
  后宫系统: 后宫系统状态,
  资源状态: NSFW资源状态,
  亲密代币: number,
  时代ID: string,
  基础叙事约束: string
): {
  成功: boolean;
  结果: 晋升仪式结果;
  新资源状态: NSFW资源状态;
  新亲密代币: number;
  错误: string | null;
} {
  // 检查精力
  if (资源状态.精力值 < 参数.精力消耗) {
    return {
      成功: false,
      结果: {} as 晋升仪式结果,
      新资源状态: 资源状态,
      新亲密代币: 亲密代币,
      错误: `精力不足！需要 ${参数.精力消耗} 精力，当前只有 ${资源状态.精力值}`,
    };
  }

  // 检查亲密代币
  if (亲密代币 < 参数.亲密代币消耗) {
    return {
      成功: false,
      结果: {} as 晋升仪式结果,
      新资源状态: 资源状态,
      新亲密代币: 亲密代币,
      错误: `亲密代币不足！需要 ${参数.亲密代币消耗}，当前只有 ${亲密代币}`,
    };
  }

  // 计算效果
  const 结果 = 计算晋升仪式效果(参数);

  // 消耗资源
  const 新资源状态: NSFW资源状态 = {
    ...资源状态,
    精力值: 资源状态.精力值 - 参数.精力消耗,
  };
  const 新亲密代币 = 亲密代币 - 参数.亲密代币消耗 + 结果.获得亲密代币;

  // 生成叙事提示词
  const 配置 = 仪式规模配置[参数.仪式规模];
  const 叙事参数: 多人NSFW场景参数 = {
    场景类型: '晋升仪式',
    参与成员: [{
      姓名: 参数.姓名,
      位分: 参数.新排位,
      亲密度等级: 后宫系统.成员列表.find(m => m.npcId === 参数.npcId)?.亲密度等级 ?? 0,
      嫉妒值: 后宫系统.成员列表.find(m => m.npcId === 参数.npcId)?.嫉妒值 ?? 0,
      和谐度: 后宫系统.和谐度,
      欲望阶段: 配置.NSFW档位 === '完全展开' ? '沉沦' : '克制' as any,
      人格特征: '',
    }],
    当前互动成员: 参数.姓名,
    是否多人场景: false,
    成员间关系摘要: '',
    平均和谐度: 后宫系统.和谐度,
    内容强度: 配置.NSFW档位,
    时代ID,
  };
  结果.叙事提示词 = 生成多人NSFW叙事约束(叙事参数, 基础叙事约束);

  return { 成功: true, 结果, 新资源状态, 新亲密代币, 错误: null };
}

/**
 * 应用晋升仪式结果到游戏状态
 */
export function 应用晋升仪式结果(
  后宫系统: 后宫系统状态,
  结果: 晋升仪式结果,
  目标NpcId: string,
  新位分: string,
  新位分等级: number
): 后宫系统状态 {
  const 新和谐度 = Math.max(0, Math.min(100, 后宫系统.和谐度 + 结果.和谐度变化));

  const 新成员列表 = 后宫系统.成员列表.map(成员 => {
    if (成员.npcId === 目标NpcId) {
      return {
        ...成员,
        位分: 新位分,
        位分等级: 新位分等级,
        信任度: Math.max(0, Math.min(100, 成员.信任度 + 结果.信任变化)),
      };
    }
    // 其他成员嫉妒变化
    if (结果.其他成员嫉妒变化 > 0) {
      return {
        ...成员,
        嫉妒值: Math.max(0, Math.min(100, 成员.嫉妒值 + 结果.其他成员嫉妒变化)),
      };
    }
    return 成员;
  });

  const 新事件历史 = [
    ...后宫系统.事件历史,
    {
      类型: '晋升仪式' as const,
      描述: 结果.描述,
      时间: new Date().toISOString(),
      参与成员: [目标NpcId],
    },
  ];

  return {
    ...后宫系统,
    成员列表: 新成员列表,
    和谐度: 新和谐度,
    事件历史: 新事件历史,
  };
}
