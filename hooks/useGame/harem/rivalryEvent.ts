/**
 * 检查是否触发争宠事件
 */
export function 检查争宠事件触发(
  后宫系统: 后宫系统状态,
  上次触发回合: number,
  当前回合: number
): { 触发: boolean; 邀约成员: 争宠事件成员摘要[] } {
  // 至少间隔 5 回合
  if (当前回合 - 上次触发回合 < 5) {
    return { 触发: false, 邀约成员: [] };
  }

  // 至少 2 名成员嫉妒 >= 50 且亲密度 >= Lv3
  const 候选成员 = 后宫系统.成员列表.filter(
    m => m.嫉妒值 >= 50 && m.亲密度等级 >= 3
  );

  if (候选成员.length < 2) {
    return { 触发: false, 邀约成员: [] };
  }

  // 取嫉妒值最高的 2-3 名
  const 邀约成员 = 候选成员
    .sort((a, b) => b.嫉妒值 - a.嫉妒值)
    .slice(0, 3)
    .map(m => ({
      npcId: m.npcId,
      姓名: m.姓名,
      亲密度等级: m.亲密度等级,
      嫉妒值: m.嫉妒值,
      信任度: m.信任度,
    }));

  return { 触发: true, 邀约成员 };
}

/**
 * 初始化争宠事件
 */
export function 初始化争宠事件(
  邀约成员: 争宠事件成员摘要[],
  可用行动点: number
): 争宠事件参数 {
  return {
    邀约成员,
    可用行动点,
  };
}

/**
 * 生成争宠事件选项
 */
export function 生成争宠选项(
  参数: 争宠事件参数
): { 类型: string; 标签: string; 描述: string; 可用: boolean }[] {
  const 选项: { 类型: string; 标签: string; 描述: string; 可用: boolean }[] = [];

  // 选择成员选项
  for (const 成员 of 参数.邀约成员) {
    选项.push({
      类型: `选择成员:${成员.npcId}`,
      标签: `选${成员.姓名}`,
      描述: `${成员.姓名} 亲密度 +5，其他人嫉妒 +20`,
      可用: true,
    });
  }

  // 全部拒绝
  选项.push({
    类型: '全部拒绝',
    标签: '全部拒绝',
    描述: '全员嫉妒 +10，和谐度 +5',
    可用: true,
  });

  // 说服一起（需要 2 行动点）
  const 说服可用 = 参数.可用行动点 >= 2;
  选项.push({
    类型: '说服一起',
    标签: '说服一起',
    描述: `和谐度 +15，每人亲密度 +2（成功率待定，需 2 行动点）`,
    可用: 说服可用,
  });

  return 选项;
}

/**
 * 处理争宠事件选择
 */
export function 处理争宠事件(
  参数: 争宠事件参数,
  选择: 争宠事件选择,
  后宫系统: 后宫系统状态,
  时代ID: string,
  基础叙事约束: string
): {
  成功: boolean;
  结果: 争宠事件结果;
  新行动点: number;
  错误: string | null;
} {
  // 验证"说服一起"的行动点
  if (选择.类型 === '说服一起' && 参数.可用行动点 < 2) {
    return {
      成功: false,
      结果: {} as 争宠事件结果,
      新行动点: 参数.可用行动点,
      错误: '行动点不足，说服一起需要 2 行动点',
    };
  }

  // 计算结果
  const 结果 = 计算争宠事件结果(参数, 选择, 后宫系统.和谐度);

  // 计算行动点消耗
  const 行动点消耗 = 选择.类型 === '说服一起' ? 2 : 1;
  const 新行动点 = 参数.可用行动点 - 行动点消耗;

  // 生成叙事提示词
  const 叙事参数: 多人NSFW场景参数 = {
    场景类型: '争宠之夜',
    参与成员: 参数.邀约成员.map(m => ({
      姓名: m.姓名,
      位分: 后宫系统.成员列表.find(hm => hm.npcId === m.npcId)?.位分 ?? '',
      亲密度等级: m.亲密度等级,
      嫉妒值: m.嫉妒值,
      和谐度: 后宫系统.和谐度,
      欲望阶段: '克制' as any,
      人格特征: '',
    })),
    当前互动成员: 选择.类型 === '选择成员'
      ? 参数.邀约成员.find(m => m.npcId === 选择.目标NpcId)?.姓名 ?? ''
      : 参数.邀约成员.map(m => m.姓名).join('、'),
    是否多人场景: 选择.类型 !== '选择成员',
    成员间关系摘要: '',
    平均和谐度: 后宫系统.和谐度,
    内容强度: '适度展开',
    时代ID,
  };
  const 叙事提示词 = 生成多人NSFW叙事约束(叙事参数, 基础叙事约束);

  return {
    成功: true,
    结果: { ...结果, 描述: 结果.描述, 叙事提示词: 叙事提示词 } as 争宠事件结果,
    新行动点,
    错误: null,
  };
}

/**
 * 应用争宠事件结果到游戏状态
 */
export function 应用争宠事件结果(
  后宫系统: 后宫系统状态,
  结果: 争宠事件结果
): 后宫系统状态 {
  const 新和谐度 = Math.max(0, Math.min(100, 后宫系统.和谐度 + 结果.和谐度变化));

  const 新成员列表 = 后宫系统.成员列表.map(成员 => {
    const 亲密度变化 = 结果.亲密度变化[成员.npcId] ?? 0;
    const 嫉妒变化 = 结果.嫉妒变化[成员.npcId] ?? 0;
    return {
      ...成员,
      亲密度等级: 成员.亲密度等级 + 亲密度变化,
      嫉妒值: Math.max(0, Math.min(100, 成员.嫉妒值 + 嫉妒变化)),
    };
  });

  const 新事件历史 = [
    ...后宫系统.事件历史,
    {
      类型: '争宠之夜' as const,
      描述: 结果.描述,
      时间: new Date().toISOString(),
      参与成员: Object.keys(结果.亲密度变化).length > 0
        ? Object.keys(结果.亲密度变化)
        : Object.keys(结果.嫉妒变化),
    },
  ];

  return {
    ...后宫系统,
    和谐度: 新和谐度,
    成员列表: 新成员列表,
    事件历史: 新事件历史,
  };
}
