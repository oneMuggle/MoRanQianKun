// ==================== 风险评估 ====================

/**
 * 评估泄露风险，返回 0-100 的加权风险值
 */
export function 评估泄露风险(
  项目: 拍摄项目状态,
  摄影师: 摄影师核心状态
): number {
  const 场所风险 = 场所风险映射[项目.实际场所];
  const 尺度风险 = 尺度风险映射[项目.实际尺度];
  const 信誉风险 = 信誉风险映射[摄影师.信誉];
  const 交付风险 = 项目.交付方式 ? 交付风险映射[项目.交付方式] ?? 30 : 30;

  return Math.min(100, Math.round(
    场所风险 * 0.20 +
    尺度风险 * 0.30 +
    信誉风险 * 0.25 +
    交付风险 * 0.25
  ));
}

// ==================== 尺度递进 ====================

/**
 * 计算尺度递进概率（0-1）
 */
export function 计算尺度递进概率(
  摄影师越界倾向: number,
  模特保护意识: 模特保护意识,
  当前尺度: 拍摄尺度
): number {
  const 基础概率 = 尺度递进基础概率[当前尺度];
  const 越界系数 = 摄影师越界倾向 / 100;
  const 保护系数 = 保护意识风险系数[模特保护意识];

  return Math.min(1, 基础概率 * (1 + 越界系数) * 保护系数);
}

/**
 * 随机判定是否尺度递进
 */
export function 判定是否尺度递进(概率: number): boolean {
  return Math.random() < 概率;
}

/**
 * 获取下一个尺度等级
 */
export function 下一尺度(当前: 拍摄尺度): 拍摄尺度 {
  const 顺序: 拍摄尺度[] = ['G级', 'PG-13', 'R级', 'NC-17', 'XXX'];
  const idx = 顺序.indexOf(当前);
  return idx < 顺序.length - 1 ? 顺序[idx + 1] : 当前;
}

// ==================== 越界行为 ====================

/**
 * 判定是否触发越界行为，返回类型或 null
 */
export function 判定越界行为(
  摄影师: 摄影师核心状态,
  场所: 拍摄场所,
  当前尺度: 拍摄尺度
): 越界行为类型 | null {
  const 场所风险 = 场所风险映射[场所];
  const 尺度风险 = 尺度风险映射[当前尺度];

  const 越界概率 = (
    场所风险 * 0.3 +
    尺度风险 * 0.3 +
    摄影师.越界倾向 * 0.4
  ) / 100;

  if (Math.random() >= 越界概率) return null;

  const 总权重 = Object.values(越界行为权重).reduce((a, b) => a + b, 0);
  let 随机值 = Math.random() * 总权重;

  for (const [类型, 权重] of Object.entries(越界行为权重)) {
    随机值 -= 权重;
    if (随机值 <= 0) return 类型 as 越界行为类型;
  }

  return '言语骚扰';
}

// ==================== 摄影师口碑 ====================

/**
 * 计算摄影师综合口碑评分（0-100）
 */
export function 计算摄影师口碑评分(摄影师: 摄影师核心状态): number {
  const 基础分 = 摄影师.技术水平 * 0.4 + 摄影师.沟通能力 * 0.2;
  const 信誉加成 = ({
    '业界大佬': 30, '资深摄影师': 20, '普通摄影师': 10,
    '有争议': -10, '名声较差': -25, '惯犯': -40,
  } as const)[摄影师.信誉];
  const 投诉惩罚 = 摄影师.投诉累计 * 5;
  const 回头客加成 = Math.min(15, 摄影师.回头客数量 * 2);

  return Math.max(0, Math.min(100, Math.round(
    基础分 + 信誉加成 + 回头客加成 - 投诉惩罚
  )));
}

// ==================== 摄影师筛选 ====================

/**
 * 筛选并排序摄影师列表，按口碑从高到低
 */
export function 筛选摄影师(
  摄影师列表: 摄影师核心状态[],
  模特保护意识: 模特保护意识
): 摄影师核心状态[] {
  return [...摄影师列表]
    .filter(p => {
      if (模特保护意识 === '极度保护' && p.口碑评分 < 50) return false;
      if (模特保护意识 === '适度保护' && p.口碑评分 < 30) return false;
      return true;
    })
    .sort((a, b) => b.口碑评分 - a.口碑评分);
}

// ==================== 泄露事件判定 ====================

/**
 * 判定是否触发泄露事件
 */
export function 判定泄露事件(
  风险值: number,
  频率设置: '低' | '中' | '高'
): { 触发: boolean; 类型: 泄露类型; 传播范围: 传播范围 } {
  const 频率系数 = { '低': 0.3, '中': 0.6, '高': 1.0 }[频率设置];
  const 触发概率 = (风险值 / 100) * 频率系数 * 0.5;

  const 触发 = Math.random() < 触发概率;
  if (!触发) return { 触发: false, 类型: '意外泄露', 传播范围: '未传播' };

  const 泄露类型分布: 泄露类型[] = 风险值 >= 80
    ? ['意外泄露', '盗取传播', '内部人员泄露', '主动泄露']
    : 风险值 >= 50
      ? ['意外泄露', '内部人员泄露', '二手传播']
      : ['意外泄露', '二手传播'];

  const 类型 = 泄露类型分布[Math.floor(Math.random() * 泄露类型分布.length)];

  const 传播分布: 传播范围[] = 风险值 >= 80
    ? ['小范围', '论坛传播', '社交媒体', '成人网站']
    : 风险值 >= 50
      ? ['小范围', '论坛传播', '社交媒体']
      : ['小范围', '未传播'];

  const 传播范围 = 传播分布[Math.floor(Math.random() * 传播分布.length)];

  return { 触发, 类型, 传播范围 };
}

