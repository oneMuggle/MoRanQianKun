/**
 * 追踪传播演化（传播随时间变化）
 */
export const 追踪传播演化 = (
  当前传播: 传播范围,
  速度: '静止' | '缓慢' | '快速' | '爆发'
): 传播范围 => {
  const 传播顺序: 传播范围[] = [
    '未传播', '小范围', '论坛传播', '社交媒体', '图片网站', '成人网站', '全网扩散'
  ];
  
  const 当前索引 = 传播顺序.indexOf(当前传播);
  if (当前索引 < 0 || 当前索引 >= 传播顺序.length - 1) {
    return 当前传播; // 已达上限
  }
  
  const 步长表 = { '静止': 0, '缓慢': 1, '快速': 2, '爆发': 3 };
  const 步长 = 步长表[速度];
  
  const 新索引 = Math.min(当前索引 + 步长, 传播顺序.length - 1);
  return 传播顺序[新索引];
};

