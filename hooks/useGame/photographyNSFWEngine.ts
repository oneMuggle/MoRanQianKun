/**
 * 判定越界行为（基于权重的越界行为判定）
 */
export const 判定越界行为 = (
  摄影师: 摄影师核心状态,
  场所: 拍摄场所,
  当前尺度: 拍摄尺度
): 越界行为类型 | null => {
  // 场所风险决定是否可能越界
  const 场所风险 = 场所风险映射[场所] || 30;
  if (场所风险 < 30) return null; // 低风险场所不触发
  
  // 摄影师越界倾向决定是否越界
  if (摄影师.越界倾向 < 30) return null;
  
  // 随机判定
  if (Math.random() > 摄影师.越界倾向 / 100) return null;
  
  // 根据越界倾向选择越界类型
  const 越界类型池: 越界行为类型[] = [];
  
  if (摄影师.越界倾向 > 80) 越界类型池.push('下药', '强迫拍摄', '威胁恐吓');
  else if (摄影师.越界倾向 > 60) 越界类型池.push('偷拍', '肢体接触', '服装要求');
  else if (摄影师.越界倾向 > 40) 越界类型池.push('要求加拍', '言语骚扰');
  else 越界类型池.push('言语骚扰');
  
  // 偷拍倾向额外加权
  if (摄影师.偷拍倾向 > 50) 越界类型池.push('偷拍', '偷拍');
  
  const 随机索引 = Math.floor(Math.random() * 越界类型池.length);
  return 越界类型池[随机索引];
};

/**
 * 计算摄影师口碑评分（综合评分 0-100）
 */
export const 计算摄影师口碑评分 = (摄影师: 摄影师核心状态): number => {
  // 好评率权重
  let 评分 = 摄影师.好评率 * 0.5;
  
  // 投诉惩罚
  评分 -= Math.min(摄影师.投诉次数 * 5, 25);
  
  // 合作模特数加成（边际递减）
  评分 += Math.min(摄影师.合作模特数 * 2, 20);
  
  // 被拒绝次数惩罚
  评分 -= Math.min(摄影师.被拒绝次数 * 3, 15);
  
  return Math.max(0, Math.min(100, Math.round(评分)));
};

/**
 * 筛选摄影师（按口碑排序，过滤红线指标）
 */
export const 筛选摄影师 = (
  摄影师列表: 摄影师核心状态[],
  模特保护意识: 模特保护意识
): 摄影师核心状态[] => {
  // 过滤红线指标
  const 可接受列表 = 摄影师列表.filter(p => {
    // 惯犯直接排除
    if (p.信誉 === '惯犯') return false;
    // 偷拍惯犯排除
    if (p.偷拍倾向 > 90) return false;
    // 下药行为（通过越界倾向>95判断）
    if (p.越界倾向 > 95) return false;
    return true;
  });
  
  // 保护意识高的模特更挑剔
  if (模特保护意识 === '极度保护') {
    return 可接受列表
      .filter(p => p.信誉 !== '名声较差' && p.信誉 !== '有争议')
      .sort((a, b) => 计算摄影师口碑评分(b) - 计算摄影师口碑评分(a));
  }
  
  return 可接受列表.sort((a, b) => 计算摄影师口碑评分(b) - 计算摄影师口碑评分(a));
};

/**
 * 判定泄露事件（概率泄露触发）
 */
export const 判定泄露事件 = (
  风险值: number,
  频率设置: '低' | '中' | '高'
): { 触发: boolean; 类型: 泄露类型; 传播范围: 传播范围 } => {
  // 频率设置决定基础触发阈值
  const 阈值表 = { '低': 70, '中': 50, '高': 30 };
  const 阈值 = 阈值表[频率设置];
  
  // 风险值低于阈值不触发
  if (风险值 < 阈值) {
    return { 触发: false, 类型: '意外泄露', 传播范围: '未传播' };
  }
  
  // 风险值高于90必然触发
  if (风险值 > 90) {
    const 类型表: 泄露类型[] = ['主动泄露', '盗取传播', '内部人员泄露'];
    const 传播表: 传播范围[] = ['论坛传播', '社交媒体', '图片网站'];
    return {
      触发: true,
      类型: 类型表[Math.floor(Math.random() * 类型表.length)],
      传播范围: 传播表[Math.floor(Math.random() * 传播表.length)],
    };
  }
  
  // 中间区间概率触发
  const 触发概率 = (风险值 - 阈值) / (90 - 阈值);
  if (Math.random() < 触发概率) {
    const 类型表: 泄露类型[] = ['意外泄露', '二手传播', '钓鱼传播'];
    const 传播表: 传播范围[] = ['小范围', '论坛传播', '社交媒体'];
    return {
      触发: true,
      类型: 类型表[Math.floor(Math.random() * 类型表.length)],
      传播范围: 传播表[Math.floor(Math.random() * 传播表.length)],
    };
  }
  
  return { 触发: false, 类型: '意外泄露', 传播范围: '未传播' };
};

