/**
 * narrativeGrammar.ts
 * 叙事语法引擎核心逻辑
 *
 * 提供叙事输出的结构化解析、验证和规范化功能：
 * 1. 解析叙事块（正文、变量规划、剧情规划、短期记忆）
 * 2. 提取各类叙事行（旁白、角色台词、判定）
 * 3. 验证叙事格式合规性
 * 4. 规范化不规范的叙事文本
 *
 * 纯函数，不直接修改状态，返回操作结果供调用方使用。
 */

import type {
    旁白行,
    角色台词行,
    判定行,
    判定类型,
    判定结果,
    叙事块,
    验证结果,
    验证错误,
    解析选项,
} from '../../models/narrativeGrammar';

// ==================== 常量 ====================

/** 旁白行匹配正则 */
const 旁白行正则 = /^【旁白】(.+)$/;

/** 角色台词行匹配正则 */
const 角色台词行正则 = /^【(.+?)】(.+)$/;

/** 判定行匹配正则 */
const 判定行正则 = /^【判定】\[(.+?)\](.+?)｜(.+)$/;

/** 判定类型映射 */
const 判定类型映射: Record<string, 判定类型> = {
    '通用': '通用',
    '对抗': '对抗',
    '洞察': '洞察',
    '先机': '先机',
    '瞄准': '瞄准',
    '接战': '接战',
    '防御': '防御',
    '伤害': '伤害',
    '态势': '态势',
    '反击': '反击',
    '反馈': '反馈',
    '消耗': '消耗',
    '衰退': '衰退',
};

/** 判定结果映射 */
const 判定结果映射: Record<string, 判定结果> = {
    '成功': '成功',
    '失败': '失败',
    '大成功': '大成功',
    '大失败': '大失败',
};

// ==================== 解析函数 ====================

/**
 * 解析旁白行
 *
 * @param 行 - 原始行
 * @returns 旁白行对象，如果解析失败返回 null
 */
export const 解析旁白行 = (行: string): 旁白行 | null => {
    const match = 行.match(旁白行正则);
    if (!match) return null;

    return {
        类型: '旁白',
        内容: match[1].trim(),
        原始行: 行,
    };
};

/**
 * 解析角色台词行
 *
 * @param 行 - 原始行
 * @returns 角色台词行对象，如果解析失败返回 null
 */
export const 解析角色台词行 = (行: string): 角色台词行 | null => {
    // 排除判定行（判定行也匹配【】格式）
    if (行.startsWith('【判定】')) return null;

    const match = 行.match(角色台词行正则);
    if (!match) return null;

    return {
        类型: '角色台词',
        角色名: match[1].trim(),
        内容: match[2].trim(),
        原始行: 行,
    };
};

/**
 * 解析判定行
 *
 * @param 行 - 原始行
 * @returns 判定行对象，如果解析失败返回 null
 */
export const 解析判定行 = (行: string): 判定行 | null => {
    const match = 行.match(/^【判定】\[(.+?)\](.+?)｜(.+)$/);
    if (!match) return null;

    const 判定类型Str = match[1].trim();
    const 判定类型 = 判定类型映射[判定类型Str];

    // 解析触发对象部分
    const 行动名与触发对象 = match[2].trim();
    const 行动名匹配 = 行动名与触发对象.match(/^(.+?)｜触发对象(.+)$/);

    let 行动名 = 行动名与触发对象;
    let 触发对象 = '';

    if (行动名匹配) {
        行动名 = 行动名匹配[1].trim();
        触发对象 = 行动名匹配[2].trim();
    }

    // 解析判定值、难度和结果
    const 判定信息 = match[3];
    const 判定值难度匹配 = 判定信息.match(/判定值(\d+)\/难度(\d+)/);
    const 结果匹配 = 判定信息.match(/结果=(.+)$/);

    if (!判定值难度匹配 || !结果匹配) return null;

    const 判定值 = parseInt(判定值难度匹配[1], 10);
    const 难度 = parseInt(判定值难度匹配[2], 10);
    const 结果 = 判定结果映射[结果匹配[1].trim()];

    if (结果 === undefined) return null;

    // 解析各字段
    const 基础匹配 = 判定信息.match(/基础\s*B\(([+-]?\d+)/);
    const 环境匹配 = 判定信息.match(/环境\s*E\(([+-]?\d+)/);
    const 状态匹配 = 判定信息.match(/状态\s*S\(([+-]?\d+)/);
    const 幸运匹配 = 判定信息.match(/幸运\s*L\(([+-]?\d+)/);
    const 装备匹配 = 判定信息.match(/装备\s*Q\((.+?)[,)]]/);

    // 解析触发对象中的玩家和角色
    let 玩家: string | undefined;
    let 角色名: string | undefined;

    if (触发对象) {
        const 玩家匹配 = 触发对象.match(/玩家:(.+?)$/);
        const 角色匹配 = 触发对象.match(/NPC:(.+?)$/);

        if (玩家匹配) 玩家 = 玩家匹配[1].trim();
        if (角色匹配) 角色名 = 角色匹配[1].trim();
    }

    return {
        类型: '判定',
        判定类型: 判定类型 || '通用',
        行动名,
        触发对象,
        玩家,
        角色名,
        判定值,
        难度,
        基础: 基础匹配 ? parseInt(基础匹配[1], 10) : 0,
        环境: 环境匹配 ? parseInt(环境匹配[1], 10) : 0,
        状态: 状态匹配 ? parseInt(状态匹配[1], 10) : 0,
        幸运: 幸运匹配 ? parseInt(幸运匹配[1], 10) : undefined,
        装备: 装备匹配 ? 装备匹配[1].trim() : undefined,
        结果,
        原始行: 行,
    };
};

/**
 * 解析叙事块
 *
 * @param 文本 - 包含标签的完整叙事文本
 * @param 选项 - 解析选项
 * @returns 解析后的叙事块
 */
export const 解析叙事块 = (
    文本: string,
    选项?: 解析选项
): 叙事块 => {
    const 严格模式 = 选项?.严格模式 ?? false;

    const 结果: 叙事块 = {
        正文: [],
    };

    // 提取正文内容
    const 正文匹配 = 文本.match(/<正文>([\s\S]*?)<\/正文>/);
    if (正文匹配) {
        const 正文内容 = 正文匹配[1];
        const 行列表 = 正文内容.split('\n').filter(行 => 行.trim());

        for (const 行 of 行列表) {
            const  trimmed行 = 行.trim();
            if (!trimmed行) continue;

            // 尝试解析判定行
            const 判定 = 解析判定行(trimmed行);
            if (判定) {
                结果.正文.push(判定);
                continue;
            }

            // 尝试解析角色台词行
            const 角色台词 = 解析角色台词行(trimmed行);
            if (角色台词) {
                结果.正文.push(角色台词);
                continue;
            }

            // 尝试解析旁白行
            const 旁白 = 解析旁白行(trimmed行);
            if (旁白) {
                结果.正文.push(旁白);
                continue;
            }

            // 严格模式下记录无法解析的行
            if (严格模式) {
                // 尝试作为旁白处理
                结果.正文.push({
                    类型: '旁白',
                    内容: trimmed行,
                    原始行: trimmed行,
                });
            }
        }
    }

    // 提取变量规划
    const 变量规划匹配 = 文本.match(/<变量规划>([\s\S]*?)<\/变量规划>/);
    if (变量规划匹配) {
        结果.变量规划 = 变量规划匹配[1].trim();
    }

    // 提取剧情规划
    const 剧情规划匹配 = 文本.match(/<剧情规划>([\s\S]*?)<\/剧情规划>/);
    if (剧情规划匹配) {
        结果.剧情规划 = 剧情规划匹配[1].trim();
    }

    // 提取短期记忆
    const 短期记忆匹配 = 文本.match(/<短期记忆>([\s\S]*?)<\/短期记忆>/);
    if (短期记忆匹配) {
        结果.短期记忆 = 短期记忆匹配[1].trim();
    }

    // 提取 thinking
    const thinking匹配 = 文本.match(/<thinking>([\s\S]*?)<\/thinking>/);
    if (thinking匹配) {
        结果.thinking = thinking匹配[1].trim();
    }

    // 提取行动选项
    const 行动选项匹配 = 文本.match(/<行动选项>([\s\S]*?)<\/行动选项>/);
    if (行动选项匹配) {
        结果.行动选项 = 行动选项匹配[1].trim();
    }

    // 提取 disclaimer
    const disclaimer匹配 = 文本.match(/<disclaimer>([\s\S]*?)<\/disclaimer>/);
    if (disclaimer匹配) {
        结果.disclaimer = disclaimer匹配[1].trim();
    }

    return 结果;
};

// ==================== 提取函数 ====================

/**
 * 提取所有旁白行
 *
 * @param 文本 - 原始文本
 * @returns 旁白行数组
 */
export const 提取旁白行 = (文本: string): 旁白行[] => {
    const 叙事块 = 解析叙事块(文本);
    return 叙事块.正文.filter((行): 行 is 旁白行 => 行.类型 === '旁白');
};

/**
 * 提取所有角色台词行
 *
 * @param 文本 - 原始文本
 * @returns 角色台词行数组
 */
export const 提取角色台词 = (文本: string): 角色台词行[] => {
    const 叙事块 = 解析叙事块(文本);
    return 叙事块.正文.filter((行): 行 is 角色台词行 => 行.类型 === '角色台词');
};

/**
 * 提取所有判定行
 *
 * @param 文本 - 原始文本
 * @returns 判定行数组
 */
export const 提取判定行 = (文本: string): 判定行[] => {
    const 叙事块 = 解析叙事块(文本);
    return 叙事块.正文.filter((行): 行 is 判定行 => 行.类型 === '判定');
};

/**
 * 提取 judge 标签内容
 *
 * @param 文本 - 原始文本
 * @returns judge 内容数组
 */
export const 提取Judge内容 = (文本: string): string[] => {
    const 正则 = /<judge>([\s\S]*?)<\/judge>/g;
    const 结果: string[] = [];
    let match;

    while ((match = 正则.exec(文本)) !== null) {
        结果.push(match[1].trim());
    }

    return 结果;
};

