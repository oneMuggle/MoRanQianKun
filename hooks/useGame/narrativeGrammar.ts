/**
 * 验证叙事块格式
 *
 * @param 叙事块 - 待验证的叙事块
 * @returns 验证结果
 */
export const 验证叙事格式 = (叙事块: 叙事块): 验证结果 => {
    const 错误列表: 验证错误[] = [];

    // 验证正文存在
    if (叙事块.正文.length === 0) {
        错误列表.push({
            行号: 0,
            原始行: '',
            错误类型: '缺少必填字段',
            错误信息: '正文内容为空',
        });
    }

    // 验证正文行格式
    叙事块.正文.forEach((行, 索引) => {
        if (行.类型 === '旁白') {
            if (!行.内容.trim()) {
                错误列表.push({
                    行号: 索引 + 1,
                    原始行: 行.原始行,
                    错误类型: '缺少必填字段',
                    错误信息: '旁白内容为空',
                });
            }
        } else if (行.类型 === '角色台词') {
            if (!行.角色名.trim()) {
                错误列表.push({
                    行号: 索引 + 1,
                    原始行: 行.原始行,
                    错误类型: '缺少必填字段',
                    错误信息: '角色名为空',
                });
            }
            if (!行.内容.trim()) {
                错误列表.push({
                    行号: 索引 + 1,
                    原始行: 行.原始行,
                    错误类型: '缺少必填字段',
                    错误信息: '台词内容为空',
                });
            }
        } else if (行.类型 === '判定') {
            if (!行.判定类型) {
                错误列表.push({
                    行号: 索引 + 1,
                    原始行: 行.原始行,
                    错误类型: '缺少必填字段',
                    错误信息: '判定类型缺失',
                });
            }
            if (isNaN(行.判定值) || isNaN(行.难度)) {
                错误列表.push({
                    行号: 索引 + 1,
                    原始行: 行.原始行,
                    错误类型: '值类型错误',
                    错误信息: '判定值或难度不是有效数字',
                });
            }
            if (!行.结果) {
                错误列表.push({
                    行号: 索引 + 1,
                    原始行: 行.原始行,
                    错误类型: '缺少必填字段',
                    错误信息: '判定结果缺失',
                });
            }
        }
    });

    return {
        有效: 错误列表.length === 0,
        错误列表,
    };
};

/**
 * 验证原始文本中的标签结构
 *
 * @param 文本 - 待验证的原始文本
 * @returns 验证结果
 */
export const 验证标签结构 = (文本: string): 验证结果 => {
    const 错误列表: 验证错误[] = [];

    // 检查必填标签
    const has正文 = /<正文>[\s\S]*<\/正文>/.test(文本);
    const has短期记忆 = /<短期记忆>[\s\S]*<\/短期记忆>/.test(文本);

    if (!has正文) {
        错误列表.push({
            行号: 0,
            原始行: '',
            错误类型: '标签错误',
            错误信息: '缺少必填标签 <正文>',
        });
    }

    if (!has短期记忆) {
        错误列表.push({
            行号: 0,
            原始行: '',
            错误类型: '标签错误',
            错误信息: '缺少必填标签 <短期记忆>',
        });
    }

    // 检查标签是否正确闭合
    const 标签对 = [
        ['<正文>', '</正文>'],
        ['<变量规划>', '</变量规划>'],
        ['<剧情规划>', '</剧情规划>'],
        ['<短期记忆>', '</短期记忆>'],
        ['<thinking>', '</thinking>'],
        ['<judge>', '</judge>'],
        ['<行动选项>', '</行动选项>'],
        ['<disclaimer>', '</disclaimer>'],
    ];

    标签对.forEach(([开始标签, 结束标签]) => {
        const 开始计数 = (文本.match(new RegExp(开始标签, 'g')) || []).length;
        const 结束计数 = (文本.match(new RegExp(结束标签, 'g')) || []).length;

        if (开始计数 !== 结束计数) {
            错误列表.push({
                行号: 0,
                原始行: '',
                错误类型: '标签错误',
                错误信息: `标签 ${开始标签} 和 ${结束标签} 数量不匹配`,
            });
        }
    });

    // 检查顶层标签出现次数
    const 顶层标签 = ['<正文>', '<变量规划>', '<剧情规划>', '<短期记忆>', '<thinking>'];

    顶层标签.forEach(标签 => {
        const 计数 = (文本.match(new RegExp(标签, 'g')) || []).length;
        if (计数 > 1) {
            错误列表.push({
                行号: 0,
                原始行: '',
                错误类型: '标签错误',
                错误信息: `顶层标签 ${标签} 出现多次（最多1次）`,
            });
        }
    });

    return {
        有效: 错误列表.length === 0,
        错误列表,
    };
};

// ==================== 规范化函数 ====================

/**
 * 规范化叙事文本
 *
 * @param 原始文本 - 原始叙事文本
 * @returns 规范化后的文本
 */
export const 规范化叙事文本 = (原始文本: string): string => {
    let 结果 = 原始文本;

    // 规范化换行
    结果 = 结果.replace(/\r\n/g, '\n').replace(/\r/g, '\n');

    // 移除多余的空行
    结果 = 结果.replace(/\n{3,}/g, '\n\n');

    // 规范化标签周围的空白
    结果 = 结果.replace(/\n\s*</g, '<');
    结果 = 结果.replace(/>\s*\n/g, '>\n');

    // 规范化旁白格式
    结果 = 结果.replace(/【旁白】\s*/g, '【旁白】');

    // 规范化角色台词格式
    结果 = 结果.replace(/【(.+?)】\s*/g, '【$1】');

    // 规范化判定格式
    结果 = 结果.replace(/【判定】\s*/g, '【判定】');

    return 结果;
};

/**
 * 提取叙事统计信息
 *
 * @param 叙事块 - 叙事块
 * @returns 统计信息
 */
export const 提取叙事统计 = (叙事块: 叙事块): {
    旁白数: number;
    台词数: number;
    判定数: number;
    总行数: number;
} => {
    const 旁白数 = 叙事块.正文.filter(行 => 行.类型 === '旁白').length;
    const 台词数 = 叙事块.正文.filter(行 => 行.类型 === '角色台词').length;
    const 判定数 = 叙事块.正文.filter(行 => 行.类型 === '判定').length;

    return {
        旁白数,
        台词数,
        判定数,
        总行数: 叙事块.正文.length,
    };
};

