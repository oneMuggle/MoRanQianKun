// ---- 旅行状态 ----

export interface 旅行状态 {
    进行中: boolean;
    目的地: 旅行目的地 | null;
    出发时间: string | null;
    预计到达时间: string | null;
    旅行事件: 旅行事件[];
}

export interface 旅行目的地 {
    地图: 地图结构;
    建筑: 建筑结构 | null;
    距离等级: 距离等级;
}

export type 距离等级 = '同城' | '邻近' | '远方' | '遥远';

export interface 旅行事件 {
    类型: '遭遇' | '发现' | '天气' | '休息' | '抵达';
    描述: string;
    影响: 旅行事件影响 | null;
}

export interface 旅行事件影响 {
    类型: '时间消耗' | '精力消耗' | '物品获取' | '属性变化';
    值: number | string;
    说明: string;
}

// ---- 距离计算 ----

const 距离等级时间消耗: Record<距离等级, number> = {
    '同城': 30,
    '邻近': 60,
    '远方': 180,
    '遥远': 360,
};

export function 计算距离等级(
    当前位置: { 大地点: string; 中地点: string; 小地点: string },
    目标位置: { 大地点: string; 中地点: string; 小地点: string }
): 距离等级 {
    const 大地点相同 = 归一化(当前位置.大地点) === 归一化(目标位置.大地点);
    const 中地点相同 = 归一化(当前位置.中地点) === 归一化(目标位置.中地点);
    const 小地点相同 = 归一化(当前位置.小地点) === 归一化(目标位置.小地点);

    if (小地点相同) return '同城';
    if (大地点相同 && 中地点相同) return '邻近';
    if (大地点相同) return '远方';
    return '遥远';
}

export function 计算旅行时间(距离: 距离等级, 基础分钟: number): number {
    return Math.round(距离等级时间消耗[距离] * (基础分钟 / 60));
}

// ---- 旅行可行性 ----

export interface 旅行可行性 {
    可行: boolean;
    原因?: string;
    预计耗时: number;
    距离等级: 距离等级;
}

export function 评估旅行可行性(
    _角色: 角色数据结构,
    当前位置: { 大地点: string; 中地点: string; 小地点: string },
    目标地图: 地图结构
): 旅行可行性 {
    const 目标位置 = {
        大地点: 目标地图.归属?.大地点 || '',
        中地点: 目标地图.归属?.中地点 || '',
        小地点: 目标地图.归属?.小地点 || '',
    };

    const 距离 = 计算距离等级(当前位置, 目标位置);

    if (归一化(当前位置.大地点) === 归一化(目标位置.大地点) &&
        归一化(当前位置.中地点) === 归一化(目标位置.中地点) &&
        归一化(当前位置.小地点) === 归一化(目标位置.小地点)) {
        return { 可行: false, 原因: '已在此地，无需旅行', 预计耗时: 0, 距离等级: '同城' };
    }

    const 预计耗时 = 距离等级时间消耗[距离];

    return { 可行: true, 预计耗时, 距离等级: 距离 };
}

// ---- 时间推进 ----

export function 推进游戏时间(当前时间: string, 消耗分钟: number): string {
    const 规范化 = normalizeCanonicalGameTime(当前时间);
    if (!规范化) return 当前时间;

    const parts = 规范化.split(':').map(Number);
    let [年, 月, 日, 时, 分] = parts;

    分 += 消耗分钟;

    while (分 >= 60) {
        分 -= 60;
        时 += 1;
    }

    while (时 >= 24) {
        时 -= 24;
        日 += 1;
    }

    const 当月天数 = 30;
    while (日 > 当月天数) {
        日 -= 当月天数;
        月 += 1;
    }

    while (月 > 12) {
        月 -= 12;
        年 += 1;
    }

    return `${年}:${String(月).padStart(2, '0')}:${String(日).padStart(2, '0')}:${String(时).padStart(2, '0')}:${String(分).padStart(2, '0')}`;
}

