/**
 * 检查并判定是否触发泄露事件
 * 应在拍摄项目完成后或定期调用
 */
export function 检查泄露事件(
  项目: 拍摄项目状态,
  摄影师: 摄影师核心状态,
  设置: 写真NSFW设置
): 泄露事件状态 | null {
  if (!设置.启用泄露事件) return null;

  const 风险值 = 评估泄露风险(项目, 摄影师);
  const 判定结果 = 判定泄露事件(风险值, 设置.泄露事件频率);

  if (!判定结果.触发) return null;

  return 创建默认泄露事件(
    `leak_${Date.now()}`,
    项目.id,
    项目.模特Id,
    判定结果.类型,
    判定结果.传播范围
  );
}

/**
 * 推进泄露事件传播
 * 每回合调用一次，传播范围可能扩大
 */
export function 推进泄露传播(
  事件: 泄露事件状态,
  模特: 模特核心状态
): { 更新后事件: 泄露事件状态; 更新后模特: 模特核心状态 } {
  let 更新后事件 = { ...事件 };
  let 更新后模特 = { ...模特 };

  更新后事件.传播回合 += 1;

  // 根据传播范围和类型更新影响
  const 传播影响 = 计算传播影响(更新后事件.传播范围, 更新后事件.泄露类型, 更新后事件.传播回合);

  更新后事件 = {
    ...更新后事件,
    心理影响: Math.min(100, 更新后事件.心理影响 + 传播影响.心理),
    名誉影响: Math.min(100, 更新后事件.名誉影响 + 传播影响.名誉),
    职业影响: Math.min(100, 更新后事件.职业影响 + 传播影响.职业),
    生活影响: Math.min(100, 更新后事件.生活影响 + 传播影响.生活),
    传播范围: 传播影响.新范围 ?? 更新后事件.传播范围,
  };

  // 影响模特心理状态
  更新后模特 = {
    ...更新后模特,
    安全感: Math.max(0, 更新后模特.安全感 - 传播影响.心理 / 2),
    自我认同: Math.max(0, 更新后模特.自我认同 - 传播影响.名誉 / 3),
  };

  // 更新事件状态
  if (更新后事件.心理影响 + 更新后事件.名誉影响 < 20) {
    更新后事件.状态 = '已平息';
  } else if (更新后事件.心理影响 >= 80 || 更新后事件.名誉影响 >= 80) {
    更新后事件.状态 = '已发酵';
  }

  return { 更新后事件, 更新后模特 };
}

/**
 * 处理模特对泄露事件的应对
 */
export function 应对泄露事件(
  事件: 泄露事件状态,
  应对策略: '公开澄清' | '法律维权' | '沉默忍受' | '主动承认'
): 泄露事件状态 {
  let 应对效果: 泄露事件状态['应对效果'] = '部分有效';

  switch (应对策略) {
    case '公开澄清':
      应对效果 = 事件.名誉影响 < 50 ? '有效' : '部分有效';
      break;
    case '法律维权':
      应对效果 = '有效';
      break;
    case '沉默忍受':
      应对效果 = 事件.传播范围 === '未传播' || 事件.传播范围 === '小范围' ? '部分有效' : '无效';
      break;
    case '主动承认':
      应对效果 = 事件.心理影响 < 60 ? '有效' : '部分有效';
      break;
  }

  return {
    ...事件,
    模特应对: 应对策略,
    应对效果,
  };
}

/**
 * 计算传播对各方面影响的增长
 */
function 计算传播影响(
  当前范围: 传播范围,
  类型: 泄露类型,
  传播回合: number
): { 心理: number; 名誉: number; 职业: number; 生活: number; 新范围?: 传播范围 } {
  const 基础增长 = Math.min(5, 传播回合 * 2);

  const 范围系数: Record<传播范围, number> = {
    '未传播': 0, '小范围': 1, '论坛传播': 2, '社交媒体': 3,
    '图片网站': 3, '成人网站': 4, '全网扩散': 5,
  };

  const 系数 = 范围系数[当前范围] ?? 1;
  const 心理 = Math.round(基础增长 * 系数 * 1.5);
  const 名誉 = Math.round(基础增长 * 系数 * 2);
  const 职业 = Math.round(基础增长 * 系数 * 1);
  const 生活 = Math.round(基础增长 * 系数 * 0.8);

  // 可能升级传播范围
  let 新范围: 传播范围 | undefined;
  if (传播回合 > 3 && 当前范围 === '小范围') {
    新范围 = '论坛传播';
  } else if (传播回合 > 5 && 当前范围 === '论坛传播') {
    新范围 = '社交媒体';
  } else if (传播回合 > 7 && 当前范围 === '社交媒体') {
    新范围 = 类型 === '主动泄露' ? '全网扩散' : '成人网站';
  }

  return { 心理, 名誉, 职业, 生活, 新范围 };
}

