/**
 * NSFW 精力管理系统
 * 精力消耗计算、恢复机制、不足惩罚、规划建议
 */

import type { NSFW资源状态, 精力消耗配置 } from '../../../models/nsfwCore/resources';
import {
  消耗精力,
  恢复精力,
  精力不足惩罚,
  默认精力消耗配置,
} from '../../../models/nsfwCore/resources';

// ==================== 类型定义 ====================

export interface 精力状态指示器 {
  等级: '充沛' | '正常' | '一般' | '不足' | '严重不足';
  颜色: string;
  判断力惩罚: number;
  风险增加: number;
  NPC反应惩罚: number;
}

export interface 精力规划建议 {
  可执行行为: string[];
  不可执行行为: string[];
  建议恢复: boolean;
  预计恢复回合: number;
}

// ==================== 核心函数 ====================

/**
 * 精力消耗计算 — 考虑技能加成和惩罚
 */
export function 精力消耗计算(
  当前状态: NSFW资源状态,
  行为类型: keyof 精力消耗配置,
  配置: 精力消耗配置 = 默认精力消耗配置,
  已解锁技能: string[] = []
): {
  新状态: NSFW资源状态;
  实际消耗: number;
  精力不足: boolean;
  惩罚: { 判断力下降: number; 风险增加: number; NPC反应变差: number };
  描述: string;
} {
  let 实际消耗 = 配置[行为类型];
  if (已解锁技能.includes('话术精通') && (行为类型 === '性癖探索' || 行为类型 === '敏感点探索')) {
    实际消耗 = Math.round(实际消耗 * 0.8);
  }
  if (已解锁技能.includes('潜行大师') && 行为类型 === '潜行策略') {
    实际消耗 = Math.round(实际消耗 * 0.85);
  }
  if (已解锁技能.includes('摄影技巧') && (行为类型 === '拍摄推进' || 行为类型 === '后期处理')) {
    实际消耗 = Math.round(实际消耗 * 0.85);
  }

  const 临时配置 = { ...配置, [行为类型]: 实际消耗 };
  const 结果 = 消耗精力(当前状态, 行为类型, 临时配置);

  const 新精力 = 结果.新状态.精力值;
  const 惩罚 = 精力不足惩罚(新精力);

  const 描述 = 结果.精力不足
    ? 结果.描述
    : `消耗了 ${实际消耗} 精力，剩余 ${新精力}${惩罚.判断力下降 > 0 ? `，⚠️ 判断力下降${惩罚.判断力下降}%` : ''}`;

  return {
    新状态: 结果.新状态,
    实际消耗,
    精力不足: 结果.精力不足,
    惩罚,
    描述,
  };
}

/**
 * 精力恢复机制
 */
export function 精力恢复机制(
  当前状态: NSFW资源状态,
  恢复类型: '自然恢复' | '休息' | '营养餐' | '深度休息' | '道具恢复'
): { 新状态: NSFW资源状态; 恢复量: number; 描述: string } {
  switch (恢复类型) {
    case '自然恢复': {
      const r = 恢复精力(当前状态, 10, 100);
      return { ...r, 恢复量: r.实际恢复 };
    }
    case '休息': {
      const r = 恢复精力(当前状态, 25, 100);
      return { ...r, 恢复量: r.实际恢复 };
    }
    case '营养餐': {
      const r = 恢复精力(当前状态, 20, 100);
      return { ...r, 恢复量: r.实际恢复 };
    }
    case '深度休息': {
      const r = 恢复精力(当前状态, 50, 100);
      return { ...r, 恢复量: r.实际恢复 };
    }
    case '道具恢复': {
      const r = 恢复精力(当前状态, 30, 100);
      return { ...r, 恢复量: r.实际恢复 };
    }
    default: {
      const r = 恢复精力(当前状态, 10, 100);
      return { ...r, 恢复量: r.实际恢复 };
    }
  }
}

/**
 * 精力不足惩罚指示器
 */
export function 获取精力惩罚(当前精力: number): 精力状态指示器 {
  const 惩罚 = 精力不足惩罚(当前精力);

  let 等级: 精力状态指示器['等级'];
  let 颜色: string;

  if (当前精力 >= 80) {
    等级 = '充沛';
    颜色 = '#22c55e';
  } else if (当前精力 >= 60) {
    等级 = '正常';
    颜色 = '#84cc16';
  } else if (当前精力 >= 40) {
    等级 = '一般';
    颜色 = '#eab308';
  } else if (当前精力 >= 20) {
    等级 = '不足';
    颜色 = '#f97316';
  } else {
    等级 = '严重不足';
    颜色 = '#ef4444';
  }

  return {
    等级,
    颜色,
    判断力惩罚: 惩罚.判断力下降,
    风险增加: 惩罚.风险增加,
    NPC反应惩罚: 惩罚.NPC反应变差,
  };
}

/**
 * 精力规划建议
 */
export function 精力规划建议(
  当前状态: NSFW资源状态,
  配置: 精力消耗配置 = 默认精力消耗配置
): 精力规划建议 {
  const 可执行: string[] = [];
  const 不可执行: string[] = [];

  for (const [行为, 消耗] of Object.entries(配置)) {
    if (当前状态.精力值 >= 消耗) {
      可执行.push(行为);
    } else {
      不可执行.push(`${行为}(需要${消耗}精力)`);
    }
  }

  const 建议恢复 = 可执行.length < 3;
  const 预计恢复回合 = 当前状态.精力值 < 20
    ? Math.ceil((50 - 当前状态.精力值) / 10)
    : 0;

  return {
    可执行行为: 可执行,
    不可执行行为: 不可执行,
    建议恢复,
    预计恢复回合,
  };
}

/**
 * 每回合结束时的精力处理
 */
export function 回合结束精力处理(
  当前状态: NSFW资源状态,
  本回合是否休息: boolean
): { 新状态: NSFW资源状态; 描述: string } {
  if (本回合是否休息) {
    const 结果 = 精力恢复机制(当前状态, '休息');
    return { 新状态: 结果.新状态, 描述: `休息恢复了 ${结果.恢复量} 精力` };
  }

  const 结果 = 恢复精力(当前状态, 5, 100);
  return { 新状态: 结果.新状态, 描述: 结果.描述 };
}
