// ==================== 事件处理 ====================

/**
 * 处理待处理的跨系统事件
 */
export function 处理跨系统事件(
  总线: 事件总线状态
): { 新总线: 事件总线状态; 产生的后果: 后果条目[] } {
  const 新待处理事件: 跨系统事件[] = [];
  const 已处理事件: 跨系统事件[] = [...总线.已处理事件];
  const 产生的后果: 后果条目[] = [];

  for (const 事件 of 总线.待处理事件) {
    const 匹配规则 = 预定义联动规则.filter(
      r => r.触发系统 === 事件.源系统 && r.触发事件类型 === 事件.事件类型
    );

    let 事件已处理 = false;

    for (const 规则 of 匹配规则) {
      if (事件.已处理系统.includes(规则.触发系统)) continue;

      const 影响结果 = 规则.影响函数(事件);

      if (影响结果.后果) {
        const 后果: 后果条目 = {
          ...影响结果.后果,
          id: `consequence_${Date.now()}_${Math.random().toString(36).slice(2, 8)}`,
          已持续回合: 0,
          创建时间: new Date().toISOString(),
          过期时间: new Date(Date.now() + 影响结果.后果!.持续时间 * 3600000).toISOString(),
          是否激活: true,
        };
        产生的后果.push(后果);
      }

      事件.已处理系统.push(规则.触发系统);
      事件已处理 = true;
    }

    if (事件已处理 && 事件.已处理系统.length >= 匹配规则.length) {
      已处理事件.push(事件);
    } else {
      新待处理事件.push(事件);
    }
  }

  return {
    新总线: {
      ...总线,
      待处理事件: 新待处理事件,
      已处理事件: 已处理事件,
      最后更新时间: new Date().toISOString(),
    },
    产生的后果,
  };
}

/**
 * 根据当前状态生成跨系统事件
 */
export interface 事件生成输入 {
  系统名称: 子系统名称;
  事件类型: string;
  事件描述: string;
  影响数据: Record<string, any>;
}

export function 生成跨系统事件(输入: 事件生成输入): 跨系统事件 {
  const 匹配规则 = 预定义联动规则.filter(
    r => r.触发系统 === 输入.系统名称 && r.触发事件类型 === 输入.事件类型
  );

  const 影响系统 = 匹配规则.flatMap(r => r.影响系统);

  return {
    id: `cross_event_${Date.now()}_${Math.random().toString(36).slice(2, 8)}`,
    源系统: 输入.系统名称,
    事件类型: 输入.事件类型,
    事件描述: 输入.事件描述,
    影响系统: [...new Set(影响系统)],
    影响数据: 输入.影响数据,
    时间: new Date().toISOString(),
    已处理系统: [],
  };
}
