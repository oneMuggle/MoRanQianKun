export function 创建初始事件总线(): 事件总线状态 {
  return {
    待处理事件: [],
    已处理事件: [],
    最后更新时间: new Date().toISOString(),
  };
}

export function 添加跨系统事件(
  总线: 事件总线状态,
  事件: Omit<跨系统事件, 'id' | '时间' | '已处理系统'>
): 事件总线状态 {
  const 新事件: 跨系统事件 = {
    ...事件,
    id: `cross_event_${Date.now()}_${Math.random().toString(36).slice(2, 8)}`,
    时间: new Date().toISOString(),
    已处理系统: [],
  };

  return {
    ...总线,
    待处理事件: [...总线.待处理事件, 新事件],
    最后更新时间: new Date().toISOString(),
  };
}

