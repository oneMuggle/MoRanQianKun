// ---- 执行锻造 ----

export function 执行锻造(
    配方ID: string,
    角色: 角色数据结构
): 锻造结果 {
    const 配方 = 锻造配方库.find(p => p.ID === 配方ID);
    if (!配方) {
        return { 成功: false, 错误: '未找到该锻造配方' };
    }
    
    // 检查材料
    const 材料检查 = 检查锻造材料(配方, 角色.物品列表);
    if (!材料检查.可锻造) {
        return {
            成功: false,
            错误: `材料不足：${材料检查.缺少材料.map(m => `${m.物品标识}需要${m.需要}个，现有${m.已有}个`).join('；')}`,
        };
    }
    
    // 计算成功率
    const 成功率 = 计算锻造成功率(配方, 角色);
    const 随机值 = Math.random();
    
    // 消耗材料
    const 新物品列表 = 消耗锻造材料(角色.物品列表, 材料检查.匹配材料);
    const 消耗记录 = 材料检查.匹配材料.map(m => ({
        名称: m.物品.名称,
        数量: 1,
    }));
    
    if (随机值 > 成功率) {
        // 锻造失败
        return {
            成功: false,
            消耗材料: 消耗记录,
            失败原因: `锻造失败！（成功率${Math.round(成功率 * 100)}%）`,
        };
    }
    
    // 锻造成功
    const 产物 = 生成锻造产物(配方);
    const 经验获得 = Math.round(配方.基础成功率 * 50);
    
    return {
        成功: true,
        产物,
        消耗材料: 消耗记录,
        经验获得,
    };
}

// ---- 获取可锻造配方列表 ----

export function 获取可锻造配方(角色: 角色数据结构): 锻造配方[] {
    return 锻造配方库.filter(配方 => {
        const 检查 = 检查锻造材料(配方, 角色.物品列表);
        return 检查.可锻造;
    });
}

// ---- 配方预览 ----

export function 预览配方需求(配方: 锻造配方): string {
    return 配方.材料需求.map(req => {
        const 匹配文本 = req.匹配方式 === 'ID' ? `ID:${req.物品标识}` :
                         req.匹配方式 === '类型' ? req.物品标识 :
                         `品质:${req.物品标识}`;
        return `${匹配文本} ×${req.数量}`;
    }).join(' + ');
}
