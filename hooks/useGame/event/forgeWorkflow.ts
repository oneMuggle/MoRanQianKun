export function 生成随机词条(品质: 物品品质, 数量: number): 物品词条[] {
    const 词条数量 = Math.min(数量, Math.floor(Math.random() * 数量) + 1);
    const 结果: 物品词条[] = [];
    
    // 词条池根据品质选择
    let 池 = 可用词条池;
    let 可用数量 = 词条数量;
    
    // 高品质更可能有多条词条
    if (品质 === '上品' || 品质 === '极品' || 品质 === '绝世' || 品质 === '传说') {
        池 = [...可用词条池, ...可用词条池]; // 高品质双倍权重
    }
    
    // 随机选择不重复词条
    const 已选名称 = new Set<string>();
    for (let i = 0; i < 词条数量 && 池.length > 0; i++) {
        const 索引 = Math.floor(Math.random() * 池.length);
        const 词条 = 池[索引];
        if (!已选名称.has(词条.名称)) {
            结果.push({ ...词条 });
            已选名称.add(词条.名称);
        }
        池.splice(索引, 1);
    }
    
    // 低概率附加负面词条
    if (品质 === '凡品' || 品质 === '良品') {
        if (Math.random() < 0.2) {
            const 负面索引 = Math.floor(Math.random() * 负面词条池.length);
            结果.push({ ...负面词条池[负面索引] });
        }
    }
    
    return 结果;
}

// ---- 成功率计算 ----

export interface 锻造结果 {
    成功: boolean;
    产物?: 游戏物品;
    消耗材料?: Array<{ 名称: string; 数量: number }>;
    经验获得?: number;
    错误?: string;
    失败原因?: string;
}

export function 计算锻造成功率(
    配方: 锻造配方,
    角色: 角色数据结构,
    当前装备?: 游戏物品
): number {
    let 成功率 = 配方.基础成功率;
    
    // 悟性修正：悟性 > 50 时每点 +0.5%
    if (角色.悟性 > 50) {
        成功率 += (角色.悟性 - 50) * 0.005;
    }
    // 悟性 < 30 时每点 -1%
    if (角色.悟性 < 30) {
        成功率 -= (30 - 角色.悟性) * 0.01;
    }
    
    // 根骨修正：根骨 > 40 时每点 +0.3%
    if (角色.根骨 > 40) {
        成功率 += (角色.根骨 - 40) * 0.003;
    }
    
    // 功法熟练度修正
    if (配方.功法要求) {
        const 功法 = 角色.功法列表.find(g => g.ID === 配方.功法要求!.功法ID);
        if (功法) {
            if (功法.当前熟练度 >= 配方.功法要求.最低熟练度) {
                成功率 += (功法.当前熟练度 - 配方.功法要求.最低熟练度) * 0.001;
            } else {
                成功率 -= (配方.功法要求.最低熟练度 - 功法.当前熟练度) * 0.005;
            }
        } else {
            // 没有对应功法，成功率大减
            成功率 -= 0.2;
        }
    }
    
    // 品质越高越难
    const 品质难度修正: Record<物品品质, number> = {
        '凡品': 0,
        '良品': -0.05,
        '上品': -0.1,
        '极品': -0.15,
        '绝世': -0.2,
        '传说': -0.25,
    };
    成功率 += 品质难度修正[配方.产物.品质] || 0;
    
    // 限制在 5% - 95% 之间
    return Math.max(0.05, Math.min(0.95, 成功率));
}

// ---- 材料检查 ----

export interface 材料检查结果 {
    可锻造: boolean;
    匹配材料: Array<{ 物品: 游戏物品; 配方需求: typeof 锻造配方库[0]['材料需求'][0] }>;
    缺少材料: Array<{ 物品标识: string; 需要: number; 已有: number }>;
}

export function 检查锻造材料(
    配方: 锻造配方,
    物品列表: 游戏物品[]
): 材料检查结果 {
    const 匹配材料: 材料检查结果['匹配材料'] = [];
    const 缺少材料: 材料检查结果['缺少材料'] = [];
    
    // 复制需求数量
    const 需求计数: Record<string, number> = {};
    for (const req of 配方.材料需求) {
        需求计数[req.物品标识] = req.数量;
    }
    
    // 遍历物品列表查找匹配
    const 可用物品 = [...物品列表];
    for (const req of 配方.材料需求) {
        let 已匹配 = 0;
        
        for (let i = 0; i < 可用物品.length && 已匹配 < req.数量; i++) {
            const 物品 = 可用物品[i];
            let 匹配 = false;
            
            if (req.匹配方式 === 'ID' && 物品.ID === req.物品标识) {
                匹配 = true;
            } else if (req.匹配方式 === '类型' && 物品.类型 === req.物品标识) {
                匹配 = true;
            } else if (req.匹配方式 === '品质' && 物品.品质 === req.物品标识) {
                匹配 = true;
            }
            
            if (匹配) {
                匹配材料.push({ 物品, 配方需求: req });
                可用物品.splice(i, 1);
                i--;
                已匹配++;
            }
        }
        
        if (已匹配 < req.数量) {
            缺少材料.push({
                物品标识: req.物品标识,
                需要: req.数量,
                已有: 已匹配,
            });
        }
    }
    
    return {
        可锻造: 缺少材料.length === 0,
        匹配材料,
        缺少材料,
    };
}

// ---- 消耗材料 ----

export function 消耗锻造材料(
    物品列表: 游戏物品[],
    匹配材料: 材料检查结果['匹配材料']
): 游戏物品[] {
    const 新列表 = [...物品列表];
    
    for (const { 物品 } of 匹配材料) {
        const 索引 = 新列表.findIndex(i => i.ID === 物品.ID);
        if (索引 >= 0) {
            if (新列表[索引].堆叠数量 > 1) {
                新列表[索引] = { ...新列表[索引], 堆叠数量: 新列表[索引].堆叠数量 - 1 };
            } else {
                新列表.splice(索引, 1);
            }
        }
    }
    
    return 新列表;
}

