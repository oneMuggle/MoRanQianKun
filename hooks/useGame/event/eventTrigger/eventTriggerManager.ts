/**
 * 创建增强事件（V2）
 */
export const 创建增强事件 = (
    id: string,
    名称: string,
    配置: {
        类型?: '预约' | '条件' | '定时' | '周期' | '链式';
        触发条件?: { kind: '回合偏移'; 偏移量: number } | { kind: '回合绝对'; 目标回合: number };
        事件数据?: Record<string, unknown>;
        描述?: string;
        优先级?: number;
        过期回合?: number;
        标签?: string[];
        周期性配置?: 周期性配置;
        事件链列表?: 事件链[];
        增强条件?: 增强条件;
        事件分组ID?: string;
    }
): 游戏事件 => {
    return {
        id,
        类型: 配置.类型 || '预约',
        名称,
        描述: 配置.描述,
        触发条件: 配置.触发条件 || { kind: '回合偏移', 偏移量: 1 },
        事件数据: 配置.事件数据 || {},
        状态: '待触发',
        创建回合: 0,
        优先级: 配置.优先级,
        过期回合: 配置.过期回合,
        标签: 配置.标签,
        周期性配置: 配置.周期性配置,
        事件链列表: 配置.事件链列表,
        增强条件: 配置.增强条件,
        事件分组ID: 配置.事件分组ID,
    };
};

