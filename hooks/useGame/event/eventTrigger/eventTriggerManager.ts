/**
 * 事件管理器配置
 */
export interface 事件管理器配置 {
    最大保留事件数?: number;
    启用自动清理?: boolean;
    清理间隔回合?: number;
}

/**
 * 调度结果
 */
export interface 调度结果 {
    应触发的事件: 游戏事件[];
    待更新事件: 游戏事件[];
    链式触发事件: 游戏事件[];
}

// ==================== 常量 ====================

const 默认配置: 事件管理器配置 = {
    最大保留事件数: 100,
    启用自动清理: true,
    清理间隔回合: 50,
};

// ==================== 事件管理器 ====================

/**
 * 事件管理器类
 * 提供事件生命周期的完整管理
 */
export class 事件管理器 {
    private 配置: 事件管理器配置;
    private 上次清理回合: number = 0;

    constructor(配置: 事件管理器配置 = {}) {
        this.配置 = { ...默认配置, ...配置 };
    }

    /**
     * 调度事件 - 检查哪些事件应触发
     *
     * @param 事件列表 - 当前事件列表
     * @param 当前回合 - 当前游戏回合
     * @param 游戏状态 - 游戏状态（用于条件求值）
     * @returns 调度结果
     */
    调度事件(
        事件列表: 游戏事件[],
        当前回合: number,
        游戏状态: Record<string, unknown>
    ): 调度结果 {
        let 事件 = [...事件列表];

        // 1. 检查过期事件并自动更新状态
        事件 = this.更新事件过期状态(事件, 当前回合);

        // 2. 执行自动清理
        if (this.配置.启用自动清理 && 当前回合 - this.上次清理回合 >= (this.配置.清理间隔回合 || 50)) {
            事件 = 清理已过期事件(事件, 当前回合);
            this.上次清理回合 = 当前回合;
        }

        // 3. 检查普通到期事件
        const 到期事件 = 检查到期事件(事件, 当前回合);

        // 4. 检查周期性触发
        const 周期事件 = 事件.filter(e =>
            e.状态 === '待触发' && 检查周期性触发(e, 当前回合)
        );

        // 5. 检查增强条件
        const 条件事件 = 事件.filter(e => {
            if (e.状态 !== '待触发' || !e.增强条件) return false;
            return 求值增强条件(e.增强条件, 游戏状态);
        });

        // 6. 合并应触发事件（去重）
        const 应触发IDs = new Set([
            ...到期事件.map(e => e.id),
            ...周期事件.map(e => e.id),
            ...条件事件.map(e => e.id),
        ]);
        const 应触发的事件 = 事件.filter(e => 应触发IDs.has(e.id));

        // 7. 查找链式触发事件
        const 链式触发事件: 游戏事件[] = [];
        for (const 触发了的事件 of 应触发的事件) {
            const 链事件 = 查找链式触发事件(触发了的事件.id, 事件, 当前回合);
            链式触发事件.push(...链事件);
        }

        // 8. 更新事件的已触发次数
        const 待更新事件 = [...应触发的事件, ...链式触发事件].map(e =>
            更新周期触发计数(e)
        );

        return {
            应触发的事件,
            待更新事件,
            链式触发事件,
        };
    }

    /**
     * 更新事件的过期状态
     */
    private 更新事件过期状态(
        事件列表: 游戏事件[],
        当前回合: number
    ): 游戏事件[] {
        return 事件列表.map(事件 => {
            const 过期结果 = 检查事件过期(事件, 当前回合);
            if (过期结果) {
                return 计算事件新状态(事件, 过期结果.状态);
            }
            return 事件;
        });
    }

    /**
     * 处理事件分组互斥
     *
     * @param 事件列表 - 事件列表
     * @param 分组ID - 分组ID
     * @returns 处理后的事件列表
     */
    处理分组互斥(事件列表: 游戏事件[], 分组ID: string): 游戏事件[] {
        return 处理事件组互斥(事件列表, 分组ID);
    }

    /**
     * 获取分组内的待触发事件
     */
    获取分组事件(事件列表: 游戏事件[], 分组ID: string): 游戏事件[] {
        return 获取分组待触发事件(事件列表, 分组ID);
    }
}

