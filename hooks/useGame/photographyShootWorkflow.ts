// 写真拍摄生命周期工作流
// 管理拍摄项目的开始、推进、完成和取消

import type { 拍摄项目状态, 模特核心状态, 摄影师核心状态 } from '../../models/photographyNSFW/states';
import type { 写真NSFW设置 } from '../../models/photographyNSFW';
import type { 拍摄尺度, 拍摄场所, 写真类型, 拍摄风格, 服装类型 } from '../../models/photographyNSFW/types';
import {
  评估泄露风险,
  计算尺度递进概率,
  判定是否尺度递进,
  下一尺度,
  判定越界行为,
  创建默认拍摄项目,
} from './photographyNSFWEngine';

// ==================== 拍摄项目管理 ====================

/**
 * 创建新的拍摄项目
 */
export function 创建拍摄项目(
  模特: 模特核心状态,
  摄影师: 摄影师核心状态,
  配置: {
    写真类型?: 写真类型;
    场所?: 拍摄场所;
    风格?: 拍摄风格;
    尺度?: 拍摄尺度;
    服装?: 服装类型;
    最大回合?: number;
  } = {}
): 拍摄项目状态 {
  return 创建默认拍摄项目(
    `shoot_${Date.now()}`,
    模特.id,
    摄影师.id,
    配置.写真类型,
    配置.场所,
    配置.风格,
    配置.尺度,
    配置.服装
  );
}

/**
 * 推进一个拍摄回合
 * 返回更新后的项目状态和可能的事件
 */
export function 推进拍摄回合(
  项目: 拍摄项目状态,
  模特: 模特核心状态,
  摄影师: 摄影师核心状态,
  设置: 写真NSFW设置
): {
  更新后项目: 拍摄项目状态;
  更新后模特: 模特核心状态;
  事件: 拍摄事件[];
} {
  const 事件: 拍摄事件[] = [];
  let 更新后项目 = { ...项目 };
  let 更新后模特 = { ...模特 };

  // 增加回合计数
  更新后项目 = {
    ...更新后项目,
    当前回合: 更新后项目.当前回合 + 1,
  };

  // 更新阶段
  if (更新后项目.当前回合 === 1) {
    更新后项目.拍摄阶段 = '第一组拍摄';
  } else if (更新后项目.当前回合 === 2) {
    更新后项目.拍摄阶段 = '换装';
  } else if (更新后项目.当前回合 === 3) {
    更新后项目.拍摄阶段 = '第二组拍摄';
  } else if (更新后项目.当前回合 >= 更新后项目.最大回合) {
    更新后项目.拍摄阶段 = '收尾';
  }

  // 检查最大回合
  if (更新后项目.当前回合 >= 更新后项目.最大回合) {
    更新后项目 = {
      ...更新后项目,
      拍摄阶段: '已完成',
      泄露风险评分: 评估泄露风险(更新后项目, 摄影师),
    };
    事件.push({ 类型: '拍摄完成', 项目Id: 更新后项目.id });
    return { 更新后项目, 更新后模特, 事件 };
  }

  // 尺度递进判定
  if (设置.启用尺度递进) {
    const 越界倾向 = 摄影师.越界倾向;
    const 递进概率 = 计算尺度递进概率(越界倾向, 模特.保护意识, 更新后项目.实际尺度);
    if (判定是否尺度递进(递进概率)) {
      const 旧尺度 = 更新后项目.实际尺度;
      const 新尺度 = 下一尺度(旧尺度);
      if (新尺度 !== 旧尺度) {
        // 模特同意判定
        const 模特同意 = 模特.信任度 > 40 && 模特.安全感 > 30;
        if (模特同意) {
          更新后项目 = {
            ...更新后项目,
            实际尺度: 新尺度,
            尺度变更历史: [
              ...更新后项目.尺度变更历史,
              { 旧尺度, 新尺度, 回合: 更新后项目.当前回合, 模特是否同意: true },
            ],
          };
          事件.push({ 类型: '尺度递进', 项目Id: 更新后项目.id, 旧尺度, 新尺度 });
        } else {
          更新后项目 = {
            ...更新后项目,
            尺度变更历史: [
              ...更新后项目.尺度变更历史,
              { 旧尺度, 新尺度: 旧尺度, 回合: 更新后项目.当前回合, 模特是否同意: false },
            ],
          };
          更新后模特 = {
            ...更新后模特,
            信任度: Math.max(0, 更新后模特.信任度 - 5),
          };
          事件.push({ 类型: '尺度递进被拒', 项目Id: 更新后项目.id, 尺度: 旧尺度 });
        }
      }
    }
  }

  // 越界行为判定
  if (设置.启用越界识别) {
    const 越界 = 判定越界行为(摄影师, 更新后项目.实际场所, 更新后项目.实际尺度);
    if (越界) {
      更新后项目 = {
        ...更新后项目,
        越界行为记录: [
          ...更新后项目.越界行为记录,
          { 类型: 越界, 回合: 更新后项目.当前回合, 模特回应: '待定' },
        ],
      };
      // 越界对模特的影响
      const 信任下降 = 越界 === '威胁恐吓' || 越界 === '下药' ? 30
        : 越界 === '肢体接触' || 越界 === '强迫拍摄' ? 20
        : 越界 === '偷拍' ? 25
        : 10;
      const 安全感下降 = 越界 === '偷拍' ? 25
        : 越界 === '威胁恐吓' ? 20
        : 15;
      更新后模特 = {
        ...更新后模特,
        信任度: Math.max(0, 更新后模特.信任度 - 信任下降),
        安全感: Math.max(0, 更新后模特.安全感 - 安全感下降),
      };
      事件.push({ 类型: '越界行为', 项目Id: 更新后项目.id, 越界类型: 越界 });
    }
  }

  // 更新泄露风险评分
  更新后项目.泄露风险评分 = 评估泄露风险(更新后项目, 摄影师);

  return { 更新后项目, 更新后模特, 事件 };
}

/**
 * 取消拍摄项目
 */
export function 取消拍摄项目(项目: 拍摄项目状态): 拍摄项目状态 {
  return {
    ...项目,
    拍摄阶段: '已取消',
  };
}

/**
 * 投诉拍摄项目
 */
export function 投诉拍摄项目(项目: 拍摄项目状态): 拍摄项目状态 {
  return {
    ...项目,
    拍摄阶段: '已投诉',
    违规记录: [...项目.违规记录, `投诉于回合${项目.当前回合}`],
  };
}

// ==================== 事件类型 ====================

export type 拍摄事件 =
  | { 类型: '拍摄完成'; 项目Id: string }
  | { 类型: '尺度递进'; 项目Id: string; 旧尺度: 拍摄尺度; 新尺度: 拍摄尺度 }
  | { 类型: '尺度递进被拒'; 项目Id: string; 尺度: 拍摄尺度 }
  | { 类型: '越界行为'; 项目Id: string; 越界类型: string };
